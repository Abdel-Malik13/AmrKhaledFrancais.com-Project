                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue à tous avec un sourire d'espoir,</p>
<p>Le sourire d'aujourd'hui a un rapport avec le football...</p>
<p>L'idée est simple : une question qui a intrigué les psychologues « Pourquoi les gens aiment le football ? »</p>
<p>Pour quelle raison ? dans le monde entier pourquoi tant d'amour pour le foot ?</p>
<p>Les statistiques disent que le nombre de gens qui se sont passionnés pour le football a doublé et redoublé dernièrement ...</p>
<p>Alors les psychologues se sont mis a étudier le sujet , pourquoi une telle passion, pourquoi un tel amour...</p>
<p>Ils ont découvert que la raison essentielle se résume dans le cadre du but composé de 3 barres...</p>
<p>Donc 3 barres, le ballon les traverse et ca fait but !</p>
<p>1-0 le match est terminé.</p>
<p>Mais alors pourquoi je vous raconte ca ?</p>
<p>Les gens ont besoin de voir une réussite devant leurs yeux, les gens ont besoin de voir un but,</p>
<p>La vie des gens est devenue compliquée...</p>
<p>On ne sait plus qui a réussi , ni qui a échoué, On a envie de voir un projet qui a réussi, On a envie de voir une grande réalisation... Les gens sont fatigués dans tous les pays du monde parce que la vie est devenue compliquée...</p>
<p>Alors le football dans tout ca, parait très simple !</p>
<p>Le ballon a traversé la ligne , ca fait donc but. Et les supporters explosent de joie, tout le monde a vu le ballon rentrer, le match est terminé par un score de 2-1 ... Une équipe a gagné .... Une équipe a soulevé la coupe... La réalisation est claire et nette, facile à comprendre et à repérer !</p>
<p>Tu veux que ta vie soit plus délicieuse ? Peux tu réaliser un petit projet , et dire « j'ai marqué un but ! »</p>
<p>Peux tu marquer un but dans ta vie ? Tu me demanderas « comment ? »</p>
<p>Je te donne un exemple, nous en Egypte on a fait un programme pour luter contre l'analphabétisme, alors disons qu'instruire 120 mille analphabètes serait un but.</p>
<p>Qu'ils apprennent tous à lire et écrire cette année, et qu'on les rassemble au stade du Caire, et nous dirons que ces 120.000 personnes, on passé le test et l'ont réussi... Ca fait but !</p>
<p>Peux tu aussi trouver un but à marquer dans ta vie ?</p>
<p>Tu peux aussi chercher un but à atteindre, et ca peut être un petit but au début, un but facile à marquer ;)</p>
<p>Par exemple : Ma maman sera satisfaite de moi d'ici une semaine... Le plan c'est de rendre heureuse ma maman, alors je vais trouver des choses qu'elle apprécie, et lui en offrir une par jour...</p>
<p>Le jour ou ta maman te dira « je suis contente de toi » ca fera but !</p>
<p>Peux tu chercher un but dans ta vie professionnelle ?</p>
<p>Si tu n'as pas de travail, peux tu trouver un but ? tu me diras « je n'ai déjà pas de travail, alors comment avoir un but ?» dans ce cas je te dirai « apprend de nouvelles connaissances, un métier, des techniques, comment faire des photographies, comment mieux maîtriser l'ordinateur... »</p>
<p>Et le jour ou tu aura acquis une nouvelle connaissance, ca fera but !</p>
<p>Mais si tu restes à la maison en disant « je souffre du chômage » te plaindre de ta situation, tu ne verras jamais de réalisation ni de but...</p>
<p>Comme tu aimes le football car le ballon traverse les poteaux et fait but, commence à chercher un but avec toi-même.... Mais attention à ne pas marquer contre ton camp ;)</p>
<p>Marque un but qui te laissera dire au monde entier, la tête haute « j'ai marqué un but ! »</p>
<p>A ce moment là tu enchaineras les buts les uns après les autres, parce que tu auras changé, parce que tu auras déjà gouté la joie et le plaisir d'avoir marqué un but ;)</p>
<br>
<p>Un sourire d'espoir pour nous tous,</p>
<p>à demain si Dieu le veut.</p>
