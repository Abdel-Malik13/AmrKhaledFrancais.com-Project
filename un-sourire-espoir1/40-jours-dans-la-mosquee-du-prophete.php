                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
<p>J'ai envie de commencer l'épisode d'aujourd'hui avec une parole du Prophète (bénédictions et paix soient sur lui) dans laquelle il dit</p>
<p><strong class="prophete">« Que l'un de vous marche pour rendre service à son frère, est meilleur pour lui que d'observer une retraite spirituelle (I'tikaf) de 40 jours dans cette mosquée »</strong></p>
<p>Passer 40 jours dans la mosquée du Prophète (bpsl) ?</p>
<p>Oui la mosquée du Prophète (bpsl)!</p>
<p>Ca veut dire si je vais voir un pauvre pour l'aider à résoudre son problème, ma récompense sera meilleure que si je passe 40 jours à faire des adorations dans la mosquée du Prophète (bpsl)...</p>
<p>Mais pourquoi exactement 40 jours ?</p>
<p>Parce que dans une autre parole, le Prophète (bpsl) a dit</p>
<p><strong class="prophete">« Celui qui prie pour Allah pendant 40 jours en groupe en faisant le 1er takbir (en commençant chaque prière) avec l'Imam, il sera exempté de 2 choses : le Feu de l'Enfer, et l'hypocrisie. »</strong></p>
<p>C'est comme si le Prophète (bpsl) te dit que te déplacer pour aider quelqu'un dans le besoin te donnera une plus grande récompense que ces 40 jours de prières et d'adorations.</p>
<p>C'est comme s'il te motivait à aider les autres,</p>
<p>Que tu te serves de ta foi en Dieu pour réformer la société.</p>
<p>Je pensais que le Prophète (bpsl) allait nous demander juste de prier et de jeuner...</p>
<p>Non... Non... Non... Il te demande de réformer la société, d'améliorer la vie des autres !</p>
<p>Aide un nécessiteux, ca sera comme si tu avais visité la mosquée du Prophète (bpsl)</p>
<p>Il y'a des gens qui économisent toutes leurs vies pour prendre l'avion et passer 3 jours à la mosquée du Prophète (bpsl)...</p>
<p>Mes chers il y'a plein de personnes dans le besoin autour de nous,</p>
<p>Mais malheureusement on ferme nos yeux...</p>
<p>Savez-vous ce qu'on fait parfois ? (et ne le prenez pas mal)</p>
<p>On fait comme un père qui part à la plage avec sa femme et ses enfants,</p>
<p>Ils sont assis devant la mer, passent du bon temps, sirotent un verre de jus...</p>
<p>Puis soudainement une personne se noie,</p>
<p>Elle crie et appelle au secours...</p>
<p>Le père met ses mains sur les yeux de ses enfants, pour qu'ils ne voient pas cette scène difficile,</p>
<p>Puis il leur dit « Rentrons à la maison »</p>
<p>Et ils partent...</p>
<p>Qu'est ce qu'on dirait sur ce père ?</p>
<p>Un lâche n'est ce pas ?</p>
<p>Il y'a pourtant plein de gens qui font la même chose dans la vraie vie,</p>
<p>Des gens dorment dans la rue, ou dans des bidonvilles,</p>
<p>Tu me diras « Qu'est ce que je peux y faire ? Ce sont des millions, je ne pourrais pas régler ce problème »</p>
<p>Prend la main d'une seule de ces personnes... Aide une seule de ces personnes...</p>
<p>Souviens-toi de la parole du Prophète (bpsl)</p>
<p><strong class="prophete">« Que l'un de vous marche pour rendre service à son frère, est meilleur pour lui qu'observer une retraite spirituelle (I'tikaf) de 40 jours dans cette mosquée »</strong></p>
<p>Tu as dit vrai Ô Messager de Dieu !</p>
<p>Aidez les pauvres mes chers,</p>
<p>Donnez-leur un coup de main,</p>
<p>Et Dieu vous donnera une saveur comme si vous aviez passé 40 jours dans la mosquée du Prophète (bpsl)</p>
<p>Imaginez qu'on commence le mois de Ramadan avec cette mentalité ?</p>
<p>Si chacun de nous aide une seule famille dans le besoin...</p>
<p>Un magnifique sourire d'espoir se dessinera.</p>
<p>Motivons-nous et bougeons au service des autres.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
