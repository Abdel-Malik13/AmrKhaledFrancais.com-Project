                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est une histoire imaginaire,</p>
<p>Mais le sens qui se cache derrière... Je vous le dis maintenant ou à la fin ?</p>
<p>Non laissez moi vous le dire à la fin.</p>
<p>On raconte que 2 amis marchaient en plein désert,</p>
<p>Ils étaient très copains,</p>
<p>Mais pendant qu'ils marchaient ils se sont disputés,</p>
<p>Un peu comme tous les amis se disputent de temps en temps...</p>
<p>Le premier a bousculé le deuxième, et il a réagi en lui donnant une gifle,</p>
<p>Celui qui a été giflé était choqué, il n'y croyait pas...</p>
<p>C'est possible que mon ami fasse ca ?</p>
<p>Il a tracé sur le sable « Aujourd'hui, mon meilleur ami m'a frappé sur le visage »</p>
<p>Puis il a gardé le silence...</p>
<p>Le 2ème l'a vu en train d'écrire... Puis ils ont continué leur chemin,</p>
<p>Au bout d'un moment ils se sont réconciliés, ils ont commencé à se reparler et ils ont tourné la page...</p>
<p>Des jours plus tard ils sont partis ensemble à la plage,</p>
<p>Et pendant qu'ils nageaient, le même qui a été giflé était encore malchanceux...</p>
<p>Il allait se noyer,</p>
<p>Son meilleur ami (qui l'a giflé) est venu le sauver de la mort,</p>
<p>Une fois sorti de l'eau il a pris une pierre aiguisée, puis il est parti graver sur un rocher « Aujourd'hui, mon meilleur ami a sauvé ma vie »</p>
<p>Son ami qui l'a giflé la 1ère fois, et qui l'a sauvé la 2ème lui a demandé :</p>
<p>« Pourquoi la 1ère fois tu as écrit sur du sable, et que cette fois tu as gravé sur une roche ? »</p>
<p>Il lui a répondu « Quand une personne chère nous blesse, on doit noter ce qu'il a fait sur du sable... Pour que les vents du pardon viennent l'effacer,</p>
<p>Mais quand quelqu'un de cher nous rend service, qu'il nous aide... Il faut graver ce qu'il a fait dans la roche, pour que ca ne s'efface jamais »</p>
<p>Peux-tu faire la même chose avec ta femme ou ton mari ?</p>
<p>Peux-tu faire ca avec tes amis ?</p>
<p>Peux-tu faire ca avec tes frères et sœurs ?</p>
<p>Vous me croyez si je vous dis que cette méthode de vivre est très très très belle ?</p>
<p>Quand quelqu'un de proche te fait du mal, écris ce qu'il te fait sur du sable pour que ca se gomme vite...</p>
<p>Ne le garde pas dans ton cœur, mais juste dans ta tête,</p>
<p>Ecris-le légèrement pour que le 1er vent qui passe te le fasse oublier...</p>
<p>Mais quand quelqu'un te sort d'une situation difficile, grave-le dans ton cœur pour que tu ne l'oublies jamais.</p>
<p>Une très belle façon de vivre avec nos proches et nos amis.</p>
<p>Malheureusement des gens font le contraire :</p>
<p>Quand on fait du bien avec lui, il l'écrit sur du sable,</p>
<p>Et quand on lui fait du mal, il le grave dans la roche...</p>
<p>Et il demande le divorce, et la maison tombe en ruines...</p>
<p>Il y'a des gens qui agissent comme ca avec leurs frères ou sœurs,</p>
<p>Et des gens qui agissent comme ca avec les membres de leur famille,</p>
<p>Et des gens qui agissent comme ca avec leurs amis...</p>
<p>Je vous en prie mes chers,</p>
<p>Le meilleur sourire d'espoir se dessine quand tu pardonnes facilement, sur du sable ;)</p>
<p>Et quand quelqu'un est là pour toi, grave-le dans ton cœur pour toujours.</p>
<p>Testez cette idée, et vous comprendrez le sens d'un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
