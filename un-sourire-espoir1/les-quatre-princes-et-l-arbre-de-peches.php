                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>On raconte qu'un roi avait 4 fils,</p>
<p>Il voulait leur donner une leçon qui leur serve pour toujours,</p>
<p>Qu'ils prennent leur temps avant de juger et qu'ils soient comprennent les situations des gens avant de les disputer ou les punir,</p>
<p>Le roi a donc convoqué ses 4 fils, et leur a donné une mission,</p>
<p>Il leur a indiqué l'endroit d'un Pêcher (arbre de pêche) à 100 km du château,</p>
<p>Chacun d'eux ira voir cet arbre, et reviendra pour décrire ce qu'il a vu...</p>
<p>Les 4 devront partir à des dates différentes que le roi va fixer,</p>
<p>Le roi était futé, il a envoyé le 1er en plein printemps,</p>
<p>Le 2ème en été,</p>
<p>Le 3ème en automne,</p>
<p>Et le dernier en hiver...</p>
<p>Chaque prince est parti sans comprendre le but derrière l'idée de son père,</p>
<p>Puis pourquoi partir si loin, il y'a des arbres partout ?</p>
<p>Et pourquoi décrire un arbre ?</p>
<p>N'importe qui peut décrire un arbre, ca ne fera pas de moi quelqu'un de plus intelligent...</p>
<br>
<p>A la fin le roi a réuni ses 4 fils, et leur a demandé la description de l'arbre qu'ils ont vu,</p>
<p>Le 1er qui était parti en plein printemps a dit que l'arbre était d'un vert magnifique, plein de vie...</p>
<p>Le 4ème qui est parti en hiver a dit que l'arbre était triste, sans aucune feuille verte...</p>
<p>Le 1er a dit non c'est un menteur...</p>
<p>Le 4ème a dit non c'est toi le menteur...</p>
<p>Le 2ème qui est parti en été a dit vous vous trompez tous les deux, l'arbre n'est pas du tout comme vous le prétendez, il a une belle odeur, il est entouré de fleurs...</p>
<p>Les deux autres ont dit pas vrai, tu t'es trompé d'endroit !</p>
<p>Le roi a demandé au 3ème de décrire ce qu'il a vu, il a dit avoir vu plein de pêches sur l'arbre, par contre il n'avait pas d'odeur, et aucune fleur autour...</p>
<p>Et les quatre se sont disputés,</p>
<p>Et le roi était amusé de ce qu'il voyait...</p>
<p>Chacun des 4 princes croyait détenir la vérité...</p>
<p>L'idée vous est arrivée ?</p>
<p>Les 4 ont raison,</p>
<p>Les 4 sont sincères,</p>
<p>Les 4 ont vu le même arbre mais dans des saisons et des états différents...</p>
<br>
<p>Ne juge jamais une personne alors que tu ne l'as vu que dans une seule saison de sa vie,</p>
<p>Ou que tu ne l'as vu que dans une seule situation,</p>
<p>:)</p>
<br>
<p>A tous ceux qui disent « untel est une mauvaise personne ! Je l'ai vu l'autre jour faire ceci ou cela »</p>
<p>Alors que cette personne est bien, c'est juste que ce jour là il n'était pas dans son assiette... Ca peut arriver, il était dans son hiver à lui donc de mauvaise humeur :)</p>
<br>
<p>Mais aussi à ceux qui disent « Untel est formidable, je vais le marier à ma fille ! Parce que j'ai vu ce qu'il a fait l'autre jour dans telle situation »</p>
<p>Prends ton temps pour le voir dans d'autres situations et dans d'autres saisons,</p>
<p>Le mariage est une grande responsabilité, c'est la vie de ta fille qui est en jeu...</p>
<br>
<p>Je dis ca aussi et surtout à toutes les filles, qui tombent vite sous le charme d'un homme, et accepte de se marier avec lui avant de bien se renseigner...</p>
<p>Prenez le temps de vérifier,</p>
<p>En se basant sur une seule situation, on risque de perdre le sourire d'espoir pour toute la vie...</p>
<br>
<p>Un arbre change selon les saisons... Que dire des humains...</p>
<p>Si les 4 princes ont été en désaccord sur un arbre... Alors imaginez comment ils seraient en désaccord sur une personne...</p>
<br>
<p>Un sourire d'espoir, pour prendre notre temps avant de juger les autres dans un sens ou dans l'autre.</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
