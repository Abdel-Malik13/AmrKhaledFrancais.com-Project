                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                       
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<p>Disons la vérité...</p>
<p>Souvent les arabes ne savent pas travailler en équipe,</p>
<p>Ils ne savent pas travailler main dans la main en ne formant qu'un seul groupe,</p>
<p>Et des millions d'entre nous rêvent de devenir directeurs ou patrons...</p>
<p>Mais peu pensent à travailler en groupe,</p>
<p>Je vais vous raconter une histoire drôle :</p>
<p>On raconte qu'on a organisé une course de kayak sur le fleuve du Nil,</p>
<p>Entre deux équipes, une composée d'arabes,</p>
<p>Et l'autre composée de japonais...</p>
<p>Et les japonais sont arrivés loin loin devant...</p>
<p>Une défaite cuisante pour nous,</p>
<p>Donc on a commencé à faire des analyses,</p>
<p>Essayer de comprendre en quoi les japonais sont meilleurs que nous...</p>
<p>Les japonais ont nommé un seul chef d'équipe... Et 9 rameurs...</p>
<p>Mais les arabes étaient eux 9 chefs... Et une seule personne ramait vraiment...</p>
<p>:)</p>
<p>Les arabes ont dit, ce n'est pas possible, nous allons faire de grands changements dans notre méthode pour la prochaine course...</p>
<p>On a donc refait la course, et les japonais ont gagné largement encore...</p>
<p>Pareil on a refait une analyse détaillée de la course,</p>
<p>Et on a trouvé que les japonais n'ont rien changé... 1 chef d'équipe, et 9 rameurs.</p>
<p>Mais les arabes après leur réorganisation sont devenus :</p>
<p>1 chef d'équipe... 2 adjoints chefs... 3 responsables... et à la fin 1 seul ramait sérieusement.</p>
<p>Tout le monde voulait commander et donner son avis, et à la fin il n y'avait plus vraiment de chef d'équipe...</p>
<p>Cette histoire fait rire car on s'y reconnait :)</p>
<br>
<p>Le Japon mes chers est un pays sans ressources,</p>
<p>Un pays sans pétrole,</p>
<p>Leur terre n'est pas exploitable pour l'agriculture,</p>
<p>Et c'est un pays souvent frappé par les tremblements de terre...</p>
<p>Ils doivent donc faire face à beaucoup de problèmes...</p>
<br>
<p>Et nos pays arabes sont remplis de ressources naturelles,</p>
<p>A mon humble avis, la seule différence entre nous et eux réside dans leur capacité à travailler ensemble,</p>
<p>Sans vouloir écraser son interlocuteur,</p>
<p>En trouvant toujours un terrain d'entente,</p>
<p>L'important chez eux ce n'est pas « qui est le patron »</p>
<p>L'important c'est de former une équipe soudée...</p>
<br>
<p>Pouvons nous faire la même chose ?</p>
<p>Pouvons nous en tant qu'arabes et musulmans accepter d'avoir un seul chef,</p>
<p>Et travailler main dans la main ?</p>
<p>Se donner à fond, pour améliorer la situation de nos pays,</p>
<p>Je vous prie vous tous les jeunes qui m'écoutez,</p>
<p>Si les anciens n'ont pas réussi à travailler ensemble, faites le en tant que nouvelle génération,</p>
<p>Peux tu toi personnellement t'entrainer,</p>
<p>Et prendre la décision de travailler avec les autres, et non pas commander les autres...</p>
<p>Je te jure qu'à ce moment là un sourire d'espoir se dessinera,</p>
<p>Non seulement sur ton visage et le mien,</p>
<p>Mais un sourire sur tous nos pays,</p>
<br>
<p>Merci,</p>
<br>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
