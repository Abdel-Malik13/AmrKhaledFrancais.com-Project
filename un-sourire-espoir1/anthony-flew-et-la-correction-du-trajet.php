                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un une nouvelle parole, avec un nouveau sourire d'espoir dans une nouvelle journée.</p>
<p>Ce que je veux vous dire aujourd'hui, c'est que l'Egypte a corrigé son trajet depuis la révolution du 25 janvier.</p>
<p>En tant qu'égyptiens on est heureux que ce soit arrivé, et le monde entier en témoigne,</p>
<p>Et quand on parle de « correction de trajet » c'est un mot immense,</p>
<p>C'est-à-dire quelqu'un marchait dans une direction, puis il a découvert qu'il faisait fausse route,</p>
<p>Alors il a décidé de faire demi tour et d'avancer dans le bon sens.</p>
<p>Quelqu'un qui voulait partir en Alexandrie, mais il a découvert qu'il prenait la direction du désert,</p>
<p>Il a découvert ca au bout de 50 km ou 100 km,</p>
<p>Mais il a eu le courage de s'arrêter, de faire demi tour, et changer le sens de sa marche.</p>
<p>Imaginez s'il s'était entêté en disant « j'ai honte de reconnaitre mon erreur, je garde ma fierté, j'ai déjà fais 50 km, que vont dire les gens de moi si je fais demi tour ? »</p>
<p>L'Egypte et les égyptiens, ont trouvé le courage de prendre une décision pour tout le pays.</p>
<p>Ils ont trouvé le courage de faire demi tour, et d'avancer enfin dans le bon sens.</p>
<p>Moi j'ai une question pour toi...</p>
<p>Toi personnellement, es tu satisfait de ton trajet ?</p>
<p>Est-ce que tu avances dans le bon sens ?</p>
<p>Ne t'entête jamais, en avançant dans le mauvais sens, alors qu'au fond de toi tu sais que tu as tord...</p>
<p>Je te parle à toi qui m'écoute, que tu sois un homme ou une femme...</p>
<p>Je ne parle plus de l'Egypte.</p>
<p>C'est possible que des pays entiers corrigent leur trajet, et que toi tout seul tu continues à faire fausse route ?</p>
<p>Le mauvais chemin n'est pas forcément de toucher la corruption ou de tricher, ou de manger de l'argent illicite.</p>
<p>Non, la mauvaise trajectoire peut être par exemple une mauvaise relation,</p>
<p>Que tu te comportes mal avec ton épouse, en sachant qu'au fond de toi tu es injuste envers elle... Et pourtant tu insistes dans cette mauvaise voie...</p>
<p>Que tu sois divorcé, et que tu prives ta femme ou ton mari de voir ses enfants... Alors qu'au fond tu sais qu'ils en ont besoin... Et pourtant tu insistes dans cette mauvaise voie...</p>
<p>C'est mal te comporter avec tes parents, alors qu'au fond de toi tu es déçu de ce que tu leur fais... Et pourtant tu insistes dans cette mauvaise voie...</p>
<p>Peux tu rectifier ton trajet ?</p>
<p>Peux tu trouver le courage de dire Non ! c'est insensé d'avancer encore 100 km dans ce mauvais sens...</p>
<p>Et toi qui a choisi un métier, alors que tu ne l'aimes pas, tu n'y prends aucun plaisir, et pourtant tu insistes dans cette voie...</p>
<p>Peux tu corriger ta direction ?</p>
<p>En vérité il y'à un personnage lorsque j'ai lu son histoire, ca m'a beaucoup surpris.</p>
<p>C'est un philosophe et écrivain anglais qui s'appelle Antony Flew,</p>
<p>C'était un professeur de philosophie dans l'université d'Oxford décédé en 2010.</p>
<p>Cet homme mes chers, a passé sa vie à enseigner l'athéisme,</p>
<p>Proclamer qu'il n y'à pas de Dieu,</p>
<p>Et écrire des analyses et des thèses pendant 60 ans...</p>
<p>Cet homme est connu pour être l'athée le plus influent du 20ème siècle.</p>
<p>Cet homme a écrit des ouvrages que les athéistes prennent comme références.</p>
<p>Et cet homme en 2004 s'est arrêté, et il a eu le courage de dire « tout ce que j'ai dis et écris jusqu'ici est faux »</p>
<p>Et il a pris les mêmes thèses pour désormais prouver que Dieu existe...</p>
<p>Et il a écrit un livre qui s'appel « Le voyage d'un cerveau »</p>
<p>Et ce livre a été traduit en plusieurs langues et est facilement trouvable .</p>
<p>Cet homme est à quel point immense ?</p>
<p>Je ne dis pas qu'il est devenu musulman, ce n'est pas le sujet.</p>
<p>Le sujet c'est le courage de cet homme, quand il a découvert la vérité, il n'a pas hésité à dire que pendant 60 ans il était dans le faux.</p>
<p>Et il a affronté le monde entier, et il a reçu plein de critiques, et les gens n'ont pas compris pourquoi il avait basculé...</p>
<p>Mais cet homme a voulu rendre justice à lui-même , et rester intègre dans l'histoire de l'humanité.</p>
<p>C'est possible que tu sois plus faible qu'Anthony Flew ?</p>
<p>Je t'en supplie, à partir d'aujourd'hui, regarde les mauvaises voies de ta vie,</p>
<p>Et arrête toi !</p>
<p>Et fais demi tour !</p>
<p>Et sois courageux !</p>
<p>Et corrige ton trajet !</p>
<p>Et à ce moment là tu vivras avec un sourire d'espoir.</p>
<br>
<p>Prenez soin de vous, et à bientôt avec un nouveau sourire d'espoir,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
