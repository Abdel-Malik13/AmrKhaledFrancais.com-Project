                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui raconte l'histoire de deux chevaux,</p>
<p>Ecoutez là comme une devinette,</p>
<p>Pour voir qui d'entre vous trouvera la morale de l'histoire avant la fin ;)</p>
<p>On raconte donc qu'il y'avait 2 chevaux,</p>
<p>Leur propriétaire les utilisait pour transporter des bagages etc.</p>
<p>Le premier cheval était devant,</p>
<p>Et le deuxième cheval derrière,</p>
<p>Leur propriétaire gagnait sa vie grâce à ses deux chevaux...</p>
<p>Le cheval de devant était très actif, toujours motivé, plein d'énergie !</p>
<p>Le cheval de derrière était paresseux, il n'aimait ni travailler ni prendre des choses sur son dos...</p>
<p>Quand le propriétaire chargeait les deux chevaux de bagages...</p>
<p>Celui de devant avançait vite, mais celui de derrière était lent, il retardait tout le monde...</p>
<p>Donc au bout d'un moment le propriétaire a du alléger le cheval de derrière,</p>
<p>Il mettait moins de choses sur son dos, et plus de choses sur le dos du cheval de devant...</p>
<p>Le cheval de derrière a beaucoup aimé ce système :)</p>
<p>Il s'est dit « Je vais avancer de moins en moins vite...Comme ca le propriétaire me donnera de moins en moins de bagages... Et il alourdira le cheval de devant »</p>
<p>Plus le temps passait, plus le propriétaire chargeait le cheval de devant et allégeait celui de derrière,</p>
<p>Au bout d'un moment, le cheval de devant transportait tous les bagages, et celui de derrière n'avait plus rien sur son dos...</p>
<p>Il pensait avoir trouvé l'astuce, il se moquait de son camarade « Ta motivation te fait transpirer et souffrir, et moi ma paresse me rend léger et tranquille... à la fin c'est moi le gagnant, tu transportes tout la journée, et le soir on mange pareil et on dort pareil»
<p>Un jour le propriétaire s'est dit, pourquoi donner autant à manger aux deux chevaux ?</p>
<p>Il donnait moins à manger au cheval de derrière, et plus à manger à celui de devant car il dépensait beaucoup plus d'énergie la journée...</p>
<p>Puis un jour il s'est dit que le cheval de derrière ne transportait plus de bagage donc il ne servait plus à rien, pourquoi lui donner à manger tout court ?</p>
<p>Il a décidé de l'emmener chez le boucher pour l'abattre et vendre sa viande...</p>
<p>Le cheval de derrière a mal fini...</p>
<p>Car il croyait être plus malin que les autres,</p>
<p>Il laissait les autres travailler à sa place,</p>
<p>Il pensait que ceux qui travaillent sérieusement sont bêtes,</p>
<p>Qu'en étant paresseux son salaire restera toujours le même,</p>
<p>Que travailler dur ou pas... à la fin on est tous pareils...
<p>Malheureusement beaucoup de gens travaillent avec la mentalité du cheval de derrière,</p>
<p>« Mon grand, fais semblant de travailler, laisse les autres faire les tâches difficiles... Et à la fin on est tous pareils »</p>
<p>Non mes chers,</p>
<p>Dieu a mis des règles dans l'univers,</p>
<p><strong>{ Et dis: «Œuvrez, car Allah va voir votre œuvre, de même que Son messager et les croyants, et vous serez ramenés vers Celui qui connaît bien l'invisible et le visible. Alors Il vous informera de ce que vous faisiez» }</strong></p>
<p>Sourate 9 - At-Tawba : Le Désaveu ou Le Repentir (verset 105)</p>
<br>
<p>Dieu voit tout,</p>
<p>Et il informera chacun de ce qu'il faisait réellement,</p>
<p>Celui qui travaillait sérieusement et honorait son contrat,</p>
<p>Et celui qui faisait semblant de travailler et ne méritait pas son salaire...</p>
<br>
<p>Je t'en prie ne dis jamais « Je me suis donné à fond pour mon patron...Et à la fin mon collègue qui ne fait rien touche un plus grand salaire que le mien... »</p>
<p>Dieu a mis des règles dans cet univers,</p>
<p>Si tu fais bien ton travail, c'est lui qui te récompensera.</p>
<p>Et celui qui fait le malin, se fera démasquer tôt ou tard comme le cheval de derrière, et les gens finiront par dire qu'il ne sert à rien, et il perdra sa place...</p>
<p>Pour avoir un sourire d'espoir, il faut se dépenser, bien s'appliquer dans ce qu'on fait, et ne pas compter sur les chemins raccourcis.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
