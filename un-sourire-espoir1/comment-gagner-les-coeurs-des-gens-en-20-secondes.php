                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>Il existe dans l'être humain quelque chose de spécial...</p>
<p>L'être humain a besoin d'affection,</p>
<p>Je vous jure que l'être humain est faible par nature,</p>
<p>L'être humain a besoin que quelqu'un prenne soin de lui...</p>
<br>
<p>Avant je lisais une histoire du prophète sws, et je n'avais jamais compris cette histoire...</p>
<p>Jusqu'au jour où j'ai compris à quel point l'être humain a besoin d'affection, a besoin qu'on lui pardonne, a besoin qu'on lui donne un sourire, a besoin qu'on lui dise une parole gentille...</p>
<br>
<p>Le jour où les musulmans sont revenus à la Mecque, c'était un jour de grande victoire,</p>
<p>Et la tribu de Qoraych qui a combattu le prophète sws, et lui ont fait beaucoup de mal...</p>
<p>Ce jour là le prophète sws leur a dit, <strong class="prophete">« allez en paix, vous êtes tous libres ! »</strong></p>
<p>C'est-à-dire la guerre est finie, ceux qui lui ont fait du mal sont pardonnés, et peuvent se sentir en sécurité...</p>
<p>Mais parmi eux il restait un homme au cœur encore rempli de haine...</p>
<p>Cet homme s'appelait Foudala ibn 'Oumeyr,</p>
<p>Foudala a caché un couteau dans sa poche,</p>
<p>Et il a décidé d'arriver par derrière et poignarder le prophète sws...</p>
<br>
<p>Après que le prophète sws t'ai pardonné ?</p>
<p>Après qu'il t'ai laissé en liberté ?</p>
<p>Pourquoi autant de haine et de dureté ?</p>
<br>
<p>Et donc pendant que le prophète sws tournait autour de la Ka'aba (la mosquée sacrée à la Mecque)</p>
<p>Foudala approchait doucement...</p>
<p>Jusqu'à ce qu'il soit derrière le dos du prophète sws...</p>
<p>Le prophète sws a su son intention,</p>
<p>Il s'est donc retourné en disant <strong class="prophete">« À quoi pensais-tu Foudala ? »</strong></p>
<p>Foudala a alors paniqué en disant : « A rien ! J'invoquais Dieu ! »</p>
<p>Le prophète sws rit alors et lui dit : <strong class="prophete">« Implore plutôt le Pardon de Dieu »</strong>.</p>
<p>Puis Foudala raconte, « J'ai vu la main du prophète sws se lever alors j'ai pensé qu'il allait me frapper, Mais il fit preuve de douceur à mon égard et posa sa main sur ma poitrine...</p>
<p>Avant qu'il ne retire sa main (de ma poitrine), il est devenu l'être que je chérie le plus. »</p>
<br>
<p>Le prophète sws regardait Foudala dans les yeux, en souriant, et en passant sa main sur sa poitrine...</p>
<p>Un simple geste a tout changé,</p>
<p>Foudala disait qu'avant ce geste , le prophète sws était la personne qu'il détestait le plus au monde...</p>
<p>Et après ce geste, le prophète sws était devenu la personne qu'il aimait le plus au monde.</p>
<br>
<p>Ce geste prend combien de minutes mes chers ?</p>
<p>Même pas une minute,</p>
<p>20 secondes au plus...</p>
<p>C'est possible que 20 secondes changent la plus grande haine en plus grand amour ?</p>
<br>
<p>Soyez doux avec les gens mes chers,</p>
<p>Pardonnez leur,</p>
<p>Donnez leur de l'affection,</p>
<p>Donnez leur un sourire sincère,</p>
<p>Mais pas un sourire hypocrite... Les gens sont intelligents... Ils savent distinguer entre les deux.</p>
<p>Soyez doux avec les pauvres,</p>
<p>Soyez doux avec les faibles,</p>
<p>Soyez doux avec vos enfants,</p>
<p>Soyez doux avec celui qui vous a fait du mal,</p>
<p>Je vous jure que j'ai appris de cette histoire du prophète sws une grande chose,</p>
<p>A chaque fois que je touche l'épaule de quelqu'un ou sa poitrine...Ca a un effet magique,</p>
<p>L'être humain est faible, il a besoin que quelqu'un lui donne de la tendresse.</p>
<br>
<p>Un sourire d'espoir pour qu'on soit doux avec les autres,</p>
<br>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
