                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>La parole d'aujourd'hui a un rapport avec un appel que j'ai reçu hier,</p>
<p>Un jeune m'a appelé hier et il pleurait au téléphone, je lui ai demandé ce qu'il avait, il m'a dit « je suis amoureux d'une fille, et je n'arrive pas à l'épouser, j'ai envie de l'épouser et elle a envie que je l'épouse, mais je manque de moyens, je n'ai pas de travail, je n'ai pas d'appartement, et pas de solution à l'horizon... Je suis fatigué parce que je l'aime... Je veux juste savoir si je suis fautif ou pas ? »</p>
<p>En vérité il m'a fait mal au cœur, et j'ai compris ce qu'il ressentait, et je lui ai dit (et je dis la même chose à beaucoup de jeunes qui ressentent la même chose que lui)</p>
<p>Je veux leur dire, l'amour est un sentiment merveilleux, je ne parle pas de l'amour parental, ni de l'amitié... Non je parle de l'amour que Dieu a créé entre l'homme et la femme...</p>
<p>Oui celui que Dieu a créé !</p>
<p>Parce que c'est le secret de la continuité de la vie, sans ce sentiment là il n y'aurait plus d'humains depuis longtemps...</p>
<p>Ce sentiment que Dieu a créé entre l'homme et la femme est la base de l'humanité.</p>
<p>Alors il ne faut surtout pas la voir comme un tabou, ou quelque chose de mal.</p>
<p>L'amour est quelque chose de formidable, le tout c'est de savoir comment la canaliser dans la bonne direction.</p>
<p>Et la bonne direction, c'est le mariage.</p>
<p>Alors le jeune m'a dit « je sais déjà tout ce que tu viens de me dire, le problème c'est que je n'ai pas les moyens de me marier »</p>
<p>Alors je lui ai dis, j'ai deux conseils à te donner :</p>
<p>Le premier conseil, Accroche toi à l'espoir, Demande à Dieu de t'aider, Fais des prières, met ton espoir en Dieu.</p>
<p>Adresse toi à Dieu en disant «Mon Dieu, ma seule envie c'est que sentiments se transforment en mariage, Aide moi mon Dieu »</p>
<p>Et je lui ai dis, après avoir prié Dieu, active toi et multiplie les efforts dans ce bas monde !</p>
<p>Travaille ! Souffre un peu, mais n'ai jamais honte de faire n'importe quel travail,</p>
<p>Tu vas transpirer et souffrir un peu, mais va voir ton beau père et dis lui « J'ai envie d'épouser votre fille, et je vais souffrir pour l'épouser »</p>
<p>La vie est une bataille, pour obtenir du bonheur dans la vie, il faut faire des efforts, payer le prix...</p>
<p>Beaucoup de jeunes ne sont pas prêts à faire des efforts, ni à souffrir, ils veulent obtenir les joies de la vie sans en payer le prix...</p>
<p>Crois moi , on n'obtient rien dans ce bas monde sans en payer le prix !</p>
<p>Par Dieu dans ce bas monde on n'obtient rien sans en payer le prix !</p>
<p>Si même le paradis à un prix : Adorer Dieu et le craindre, et faire l'effort...</p>
<p>Si toi tu aimes cette fille, et tu désires en faire ta femme, Si Dieu le veut ca se fera, Multiplie les prières et met ton grand espoir en Dieu , c'est le conseil numéro 1</p>
<p>Le conseil numéro 2, travaille, fais des efforts, ne baisse pas les bras devant le chômage,</p>
<p>Ne baisse pas les bras devant les obstacles, creuse dans la roche, et sache que Dieu est avec toi, et qu'il sait ce qu'il y'à dans ton cœur, et c'est lui qui a créé ce sentiment, Il va vous marier par sa volonté, et il va vous rendre heureux par sa volonté,</p>
<p>Mais multiplie les efforts, et accroche toi à l'espoir, et à ce moment là tu auras par la volonté de Dieu un grand sourire d'espoir !</p>
<br>
<p>Merci, et que la paix soit sur vous.</p>
