                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Le sourire d'aujourd'hui nous vient d'une petite fille qui s'appel Hind,</p>
<p>Hind a 7ans,</p>
<p>C'est son papa qui m'a raconté cette anecdote,</p>
<p>Son papa était assis avec elle,</p>
<p>Et Hind lui a dit « Papa la fête est arrivée »</p>
<p>Et son papa a de l'humour,</p>
<p>Il a couru vers la porte de la maison,</p>
<p>Il a regardé à gauche est à droite... Puis il lui a dit « Non la fête n'est pas encore arrivée » :)</p>
<p>Et en effet ce n'était pas encore la date de la fête,</p>
<p>La petite en toute innocence est partie ouvrir la porte,</p>
<p>Elle a regardé à gauche et à droite,</p>
<p>Puis elle est revenue en disant « Si la fête est bien arrivée ! »</p>
<p>Son papa lui a dit « Non ma fille elle n'est pas encore arrivée »</p>
<p>Elle lui a dit Oui elle est arrivée...</p>
<p>Et ils sont restés oui...non...oui...non...</p>
<p>Et quand la petite en a eu marre,</p>
<p>Elle a dit «tu ne peux pas la voir, la fête vient seulement pour les enfants » :)</p>
<p>L'anecdote est terminée,</p>
<p>Mais alors pourquoi je vous l'ai racontée ?</p>
<p>Ce que je veux vous dire c'est quand on grandit, on ne voit plus la beauté des choses qui nous entourent...</p>
<p>Des choses qui nous rendaient heureux quand on était enfants... Ne nous contentent plus une fois qu'on grandit...</p>
<p>Parfois malheureusement le sourire ne vient que chez les enfants,</p>
<p>C'est pour cela qu'on a besoin de l'innocence des enfants autour de nous,</p>
<p>Parfois on a besoin de prendre nos enfants par la main,</p>
<p>Pour retrouver le gout de la vie,</p>
<p>L'enfance est une très belle chose,</p>
<p>Nos enfants sont une très belle chose,</p>
<p>A tous les parents qui s'éloignent de leurs enfants,</p>
<p>Sais tu que tu t'éloignes du bonheur et du sourire ?</p>
<p>Les parents qui travaillent jour et nuit, en disant mon rôle c'est de ramener de l'argent pour vous nourrir...</p>
<p>Et leurs enfants ont besoin de passer du temps avec eux ...</p>
<p>Et ces parents mêmes ignorent qu'ils ont besoin de leurs enfants, plus que leurs enfants ont besoin d'eux...</p>
<p>Ce ne sont pas nos heures de travail qui vont satisfaire nos enfants...</p>
<p>Ils ont besoin de ta présence,</p>
<p>Et toi tu as besoin de leur innocence pour sentir de nouveau le gout de la fête, et le gout de la vie,</p>
<p>Dieu nous a donné par sa miséricorde, des petits enfants,</p>
<p>Qui naissent devant nos yeux,</p>
<p>Et grandissent devant nos yeux,</p>
<p>Pour retrouver nos souvenir d'enfance...</p>
<p>Pour retrouver le sourire et la magie qu'on avait quand on était du même âge qu'eux...</p>
<p>Pour retrouver le sourire d'espoir, et le sentiment de sécurité, et le sentiment de bonheur qu'on a perdu une fois adultes...</p>
<p>Je vous prie à tous les parents, rapprochez vous de vos enfants,</p>
<p>Et ceux qui n'ont pas encore d'enfants, prenez soin d'un orphelin,</p>
<p>Tu l'aideras et il te donnera en retour du bonheur et de l'innocence,</p>
<p>Dieu remplira ton cœur de bonheur !</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
