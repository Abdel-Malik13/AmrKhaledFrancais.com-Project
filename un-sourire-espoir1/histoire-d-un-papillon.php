                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui raconte l'histoire d'un papillon...</p>
<p>Un jour un petit garçon a vu un papillon en train de sortir de son cocon,</p>
<p>Il l'a observé pendant 1 heure...</p>
<p>Le papillon luttait pour sortir du petit trou, mais il n'y arrivait pas,</p>
<p>Malgré ses efforts, il restait prisonnier à l'intérieur,</p>
<p>Le petit garçon a eu pitié du papillon, alors il a décidé de l'aider...</p>
<p>Que va-t-il faire ?</p>
<p>Il a ramené des ciseaux, puis il a découpé le cocon pour laisser le papillon sortir,</p>
<p>Bien sûr le papillon a pu sortir facilement, mais il n'a pas pu s'envoler...</p>
<p>Pourquoi ?</p>
<p>Ses ailes sont restées affaiblies, elles ne se sont pas bien développées, elles ne pouvaient pas soulever son poids...</p>
<p>Le papillon tournait sur lui-même et n'arrivait pas à s'envoler... Et il a fini par mourir.</p>
<p>Le petit garçon était plein de bonnes intentions, il voulait l'aider,</p>
<p>Mais ce qu'il ignorait c'est que pour que le papillon grandisse et s'envole,</p>
<p>Il faut qu'il reste dans son cocon, et qu'il fasse preuve de détermination,</p>
<p>Il faut qu'il lutte, qu'il bouge, qu'il se batte,</p>
<p>Pour faire parvenir un certain liquide jusqu'à ses ailes,</p>
<p>Et c'est grâce à ce liquide qu'elles se renforcent et peuvent le soulever dans l'air,</p>
<p>A la fin le papillon ne sort de son cocon que lorsqu'il est assez fort pour s'envoler.</p>
<p>Cette histoire n'est pas seulement l'histoire d'un papillon,</p>
<p>C'est aussi l'histoire de plein de personnes dans la vie,</p>
<p>Combien de pères et de mères surprotègent leurs enfants,</p>
<p>Et leurs facilitent tout dans la vie...</p>
<p>Jusqu'à ce que ces enfants perdent leur détermination et leur esprit combatif,</p>
<p>L'esprit qui pousse une personne à s'envoler dans ce bas monde et réussir,</p>
<p>Si tu es un jeune homme ou une jeune femme, je vais te donner un conseil :</p>
<p>Ne fais pas comme ce papillon qui n'a pas pu bouger et a fini par mourir su place...</p>
<p>Ne cherche pas la vie facile,</p>
<p>N'attend pas que tout te soit servi sur un plateau en or,</p>
<p>La vie sans lutte,</p>
<p>La vie sans difficultés à surmonter,</p>
<p>Sans épreuves dans la vie, on fait du sur place,</p>
<p>On ne devient pas forts comme ce papillon.</p>
<p>Je m'adresse à tous les jeunes, il existe une culture appelée « La culture de la transpiration »</p>
<p>Il faut que tu transpires, que tu te fatigues, que tu te battes, que tu forges ta détermination... Et c'est là que tu te mettras sur tes 2 pieds, et que tu connaitras la réussite !</p>
<p>Ne pensez jamais que ceux qui réussissent étaient assis tranquillement et du jour au lendemain ils y sont parvenus...</p>
<p>Souffrez un peu, dépensez vos efforts !</p>
<p>J'ai un ami qui possède de grandes fermes où on cultive des patates, il les exporte vers l'étranger,</p>
<p>Ces grandes fermes, il les a installées en plein désert,</p>
<p>Il m'a dit « Malgré le grand chômage en Egypte, beaucoup de jeunes refusent de venir travailler ici...</p>
<p>Ils disent non à rester des heures sous le soleil... Et toute la journée toucher des pommes de terre, me salir et me blesser les mains ? Non ce n'est pas un travail pour moi... »</p>
<p>Elle est où la culture de la transpiration ?</p>
<p>A tous les jeunes, souvenez-vous de ce papillon,</p>
<p>Il est resté faible, il a fini par mourir... Car on lui a facilité la tâche, il n'a pas eu l'occasion de se battre.</p>
<p>Faites des efforts et vous verrez un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
