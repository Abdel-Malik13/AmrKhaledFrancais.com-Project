                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>Beaucoup d'adultes quand ils voient un jeune entrain de commettre une erreur,</p>
<p>Réagissent avec dureté.</p>
<p>Un jeune par exemple qui fait une nouvelle faute, et bien il faut lui hurler dessus...</p>
<p>Et afficher son erreur en grand devant les autres...</p>
<p>Et parfois ce sont les parents qui font ca à leurs enfants...</p>
<p>Quelqu'un qui a menti par exemple... Aie c'est une catastrophe !</p>
<p>On le met dans un coin, en lui disant « tu es un menteur ! »</p>
<p>Et lui va répondre « c'est faux, je n'ai rien fais, je n'ai rien dis à cette personne... »</p>
<p>Et là on l'affiche encore plus, et on le déshabille encore plus...</p>
<p>Et on va chercher la personne dont il a parlé,</p>
<p>Et on la met devant lui, et on demande à la personne « Alors il a menti ou pas ? »</p>
<p>Alors la personne va dire « oui il m'a dis ca l'autre jour »</p>
<p>Alors on lui dit « Et ben tu vois ! C'est la preuve que tu es bel et bien un menteur ! »</p>
<p>Aieeee.... Pourquoi autant de dureté ?</p>
<p>Pourquoi on déshabille les gens comme ca ?</p>
<p>Et pourquoi même avec nos enfants on fait la même chose ?</p>
<p>Je sais que certains parmi ceux qui m'écoutent, vont me dire « Ben que veux tu qu'on fasse ? il a menti, on ne va pas non plus l'encourager ou lui dire merci... »</p>
<p>Non je vais te montrer une façon formidable,</p>
<p>« L'art de se comporter avec les jeunes »</p>
<p>« L'art de réagir face aux fautifs »</p>
<p>Je vais vous raconter une histoire du prophète Mohamed sws (que la paix et le salut d'Allah soient sur lui)</p>
<p>Pendant que le prophète sws, faisait le pèlerinage, des personnes faisaient le pèlerinage en même temps ...</p>
<p>Et parmi ces personnes il y'avait un jeune qui s'appelait Amr ibn chouraid,</p>
<p>Amr ibn chouraid était un jeune adolescent, et donc commettait des erreurs comme tous les ados,</p>
<p>Alors pendant qu'ils étaient dans la ville de Mina (environ 5 km à l'Est de La Mecque)</p>
<p>Amr ibn chouraid en sortant de sa tente a vu un groupe de femmes assises entre elles,</p>
<p>Il s'est assis parmi elles, alors qu'il est en plein pèlerinage.</p>
<p>Et comme il est malchanceux, le prophète sws passait par là :)</p>
<p>Le prophète lui a donc demandé <strong class="prophete">« Pour quelle raison es tu assis parmi les femmes ? »</strong></p>
<p>Amr ibn chouraid a donc paniqué, et dans la précipitation il a menti en disant « Ô ! Prophète d'Allah, j'ai perdu la corde avec laquelle je tenais ma bête, alors je me suis assis pour demander à ces femmes si elles ne l'avaient pas vu »</p>
<p>Un énorme mensonge bien sûr :)</p>
<p>Alors le prophète sws l'a regardé en souriant, puis il est parti...</p>
<p>Alors il va l'abandonner comme ca ?</p>
<p>Amr ibn chouraid raconte, j'ai croisé le prophète sws le lendemain...</p>
<p>Alors il m'a demandé <strong class="prophete">« Amr, as-tu retrouvé ta corde ? »</strong></p>
<p>Je suis resté en silence... Et le prophète sws est parti comme la fois d'avant...</p>
<p>Puis j'ai encore croisé le prophète sws une autre fois,</p>
<p>(Remarquez comment le prophète sws ne voulait pas l'afficher, le mettre à nu devant son mensonge, l'humilier... Il le soigne avec miséricorde et douceur)</p>
<p>Donc pour la troisième fois il lui a demandé <strong class="prophete">« Alors Amr, as-tu retrouvé ta corde ? »</strong></p>
<p>Alors j'ai dis « Non Ô prophète d'Allah »</p>
<p>Il m'a encore souri, puis il est parti...</p>
<p>Puis je l'ai croisé une quatrième fois, et il m'a encore demandé <strong class="prophete">« Alors Amr, as-tu retrouvé ta corde ? »</strong></p>
<p>Alors j'ai répondu « Pardonne moi Ô messager d'Allah »</p>
<p>Il m'a alors regardé en souriant, et m'a dit <strong class="prophete">« Que Dieu te pardonne Amr »</strong></p>
<p>Allah... Avec tant de douceur...</p>
<p>C'est pas la peine de le dénuder, et lui dire en face tu es un menteur, avoue !</p>
<p>Pourquoi on pense que les gens sont idiots et ne comprennent pas ?</p>
<p>Et pourquoi on pense que les gens ne ressentent rien ?</p>
<p>Et pourquoi on pense que nos enfants ne ressentent rien quand ils sont fautifs...</p>
<p>Parfois un seul regard, un seul sourire est plus efficace que mille punitions ou humiliations... plus fort que mille coups de pression...</p>
<p>Et si nous soignons le fautif avec miséricorde ?</p>
<p>Le remettre sur le droit chemin, mais sans le blesser ...</p>
<p>Comment le conseiller, mais sans l'afficher...</p>
<p>Prenons exemple sur le prophète sws, et apprenons à éduquer nos enfants d'une façon positive.</p>
<p>A ce moment nous verrons un sourire d'espoir sur nos visages et ceux de nos enfants.</p>
<p>Je vous remercie, et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
