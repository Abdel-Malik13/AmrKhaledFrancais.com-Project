                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui est consacré à l'histoire d'un ami,</p>
<p>Cette histoire montre la valeur de la femme quand celle-ci est intelligente, patiente, et douce...</p>
<p>Quand elle prend soin de son mari,</p>
<p>Quand elle fait attention aux sentiments de son mari,</p>
<p>Ce genre de femme n'a pas de prix,</p>
<p>Son mari peut faire le tour du monde il reviendra vers elle à la fin...</p>
<p>Il ne pourra jamais se passer d'elle !</p>
<p>On dit qu'un homme ne se contente jamais d'une seule femme dans sa vie...</p>
<p>Sauf ce genre de femme !</p>
<p>C'est impossible qu'il la laisse tomber.</p>
<p>Je vous raconte son histoire :</p>
<p>Mon ami me raconte que son épouse était tombée enceinte,</p>
<p>Ils vivent dans un pays européen,</p>
<p>Après qu'elle ait consulté le médecin, on lui a dit qu'il y'a des chances que ce bébé naisse avec une malformation.</p>
<p>Et comme ce n'est que le 3ème mois elle pouvait faire un avortement...</p>
<p>Cette femme a pris le temps de réfléchir,</p>
<p>Elle est revenue voir le médecin qui lui a confirmé les 20% ou 30% de chances que le bébé naisse avec une malformation.</p>
<p>Elle a dit non je souhaite le garder.</p>
<p>Le médecin lui a demandé si elle voulait qu'on en parle à son mari...</p>
<p>Elle a dit non surtout pas... Mon mari a de grandes responsabilités professionnelles, il voyage beaucoup, il gère plein de projets... Je ne veux pas le perturber avec cette nouvelle, je la garderai pour moi jusqu'à la naissance du bébé.</p>
<p>Puis elle a signé un engagement sur ce qu'elle venait de dire, comme la loi le prévoyait.</p>
<p>Cette femme a soulevé seule tout le poids pendant des mois,</p>
<p>Des jours son mari la trouvait entrain de pleurer,</p>
<p>Quand il demandait ce qu'elle avait, elle disait c'est juste passager...</p>
<p>Et elle priait Dieu,</p>
<p>Elle comptait sur Dieu,</p>
<p>Elle demandait à Dieu qu'il préserve son enfant...</p>
<p>Cette situation a duré 6 mois,</p>
<p>Jusqu'au jour où elle a accouché par césarienne,</p>
<p>En se réveillant de l'anesthésie la première chose qu'elle a demandé c'est de voir le docteur pour s'assurer que son bébé est en bonne santé...</p>
<p>Après l'avoir examiné le docteur lui a dit que son bébé était en pleine santé :)</p>
<p>Elle a dit « Mon Dieu je te remercie »</p>
<p>Elle a appelé son mari pour lui raconter tout depuis le début,</p>
<p>« J'ai gardé ce secret pendant 6mois, c'est pour cette raison que certaines fois je pleurais ou que je n'étais pas à l'aise,</p>
<p>Maintenant je peux te rassurer, le bébé est en pleine santé. »</p>
<p>On dit des femmes qu'elles se plaignent facilement et qu'elles ne savent pas garder un secret...</p>
<p>Mais ce genre d'épouse n'a pas de prix,</p>
<p>Tellement sensible, tellement prévoyante, tellement forte... Qu'elle dessinera un sourire d'espoir sur le visage de son mari toute sa vie.</p>
<p>Un sourire d'espoir sur les visages de ses enfants aussi.</p>
<p>Cette épouse est un exemple à suivre.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
