                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Le sourire d'espoir d'aujourd'hui est consacré à la femme,</p>
<p>Et le respect de la femme,</p>
<p>Et l'amour de la femme,</p>
<p>Et l'estime de la femme,</p>
<br>
<p>Je vais vous raconter des histoires qui débordent d'amour et d'affection :</p>
<p>Des histoires sur le prophète sws, et ses compagnons Omar, Ali, et Abou bakr...</p>
<br>
<p>14 ans après le décès de la femme du prophète Khadija,</p>
<p>A chaque fête , et à chaque grand repas,</p>
<p>Le prophète sws prenait la moitié de la nourriture, et demandait à ce qu'on la distribue aux amies de Khadija...</p>
<p>Quand quelqu'un frappait à la porte et qu'il demandait qui c'était,</p>
<p>« c'est Hala... »</p>
<p>Il arrivait avec un large sourire en disant Hala... Hala... la sœur de Khadija...</p>
<p>Et lui réservait le meilleur des accueils,</p>
<p>Quelle est cette affection pour une épouse décédée depuis 14ans...</p>
<p>Il y a des maris qui après une semaine du décès de leur épouse, retirent ses photos de la maison, et commencent une nouvelle vie :)</p>
<p>Vous imaginez le respect et la fidélité du prophète sws envers sa femme 14 ans après...</p>
<br>
<p>Je veux aussi vous parler d'abou Bakr , qui était marié à Asmae bint Oumaiss,</p>
<p>Dans son testament, Abou bakr a fait une demande rare...</p>
<p>La seule laver son corps serait son épouse Asmae et personne d'autre.</p>
<p>Abou bakr le véridique... Voulait honorer sa femme même après sa mort.</p>
<br>
<p>Je veux aussi vous parler de Omar, qui était marié à Keltoum la fille d'Ali ibn abou taleb,</p>
<p>Et ils s'aimaient profondément...</p>
<p>Omar est décédé un lundi... Et son épouse Keltoum est décédée de tristesse le lundi suivant.</p>
<p>Ils s'aimaient à ce point...</p>
<br>
<p>Je veux aussi vous parler de Ali qui était marié à Fatima la fille du prophète sws</p>
<p>Et il lui écrivait des poèmes d'amour,</p>
<p>« Et la fille de Mohamed est ma maison et mon bonheur...</p>
<p>Mon sang et ma chair sont mélangés avec les siens pour ne former qu'un... »</p>
<p>Il lui disait à quel point il l'aimait,</p>
<p>Mais il plaisantait aussi par moments,</p>
<p>Un jour il est rentré et l'a trouvé entrain de macher un siwak,</p>
<p>(Un bout de bois d'araq qui remplaçait la brosse à dents à l'époque)</p>
<p>Alors Ali s'est adressé au bout de bois en disant</p>
<p>« Ô bois d'araq tu as gagné le droit de toucher sa bouche,</p>
<p>Mais n'as-tu pas peur que je te vois ?</p>
<p>Si tu étais un homme je t'aurai combattu en duel :) »</p>
<br>
<p>Si à ce point le prophète sws et ses compagnons montraient leurs sentiments et leur amour...</p>
<p>Ne te gêne jamais pour faire la même chose avec ton épouse ou ton mari !</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
