<p>Et à la fin vous êtes surpris qu'il y'ait un divorce,</p>
<p>Et qu'il n'y ait plus d'amour à la maison,</p>
<p>Agissez avant que la situation s'aggrave.</p>
<p>Faites justement comme Jana,</p>
<p>Les parents de Jana ne s'entendaient plus,</p>
<p>Ils en sont arrivés au point de divorcer...</p>
<p>Jana est une fillette de 14ans,</p>
<p>Celui qui me raconte cette histoire est le père de Jana,</p>
<p>Il m'a dit « Je ne m'entendais plus avec ma femme, on allait divorcer...</p>
<p>On n'arrivait plus à communiquer,</p>
<p>Et l'ambiance dans la maison était devenue insupportable,</p>
<p>Et les enfants étaient au courant qu'on allait divorcer,</p>
<p>Il n'y avait plus de retour en arrière possible...</p>
<p>Le grand frère de Jana a eu une réaction violente,</p>
<p>Il a déprimé, il a pleuré et il a quitté la maison...</p>
<p>La famille s'éparpillait dans tous les sens,</p>
<p>Mais Jana était une petite fille positive,</p>
<p>Elle voyait que dans ce chaos elle pouvait faire quelque chose,</p>
<p>Elle m'a donné rendez-vous à un endroit et une heure précise,</p>
<p>Elle m'a dit qu'elle voulait passer du temps avec moi...</p>
<p>Puis elle a donné rendez-vous à sa maman à la même heure et au même endroit,</p>
<p>En arrivant on s'est retrouvé à 3 alors que c'était prévu qu'on soit 2 »</p>
<p>Et Jana a géré le rendez-vous comme une adulte,</p>
<p>Elle a dit à ses parents « Asseyez-vous s'il vous plait »</p>
<p>Puis elle a sorti l'album photo de la famille,</p>
<p>Photo par photo...</p>
<p>« Tu te rappelles de cette photo Papa... Tu te rappelles de cette photo maman... C'est le jour de votre mariage, vous étiez les personnes les plus heureuses au monde...</p>
<p>Regarde Papa comment tu lui souriais...</p>
<p>Regarde Maman comment tu étais heureuse à ses cotés...</p>
<p>Regardez comment vos yeux étaient remplis d'étoiles...</p>
<p>Puis une autre photo...</p>
<p>Le jour où je suis né... Regardez comment on était tous heureux...</p>
<p>Jana faisait défiler les photos les une après les autres... Et les souvenirs qui vont avec...</p>
<p>Le père de Jana me raconte qu'il ne pouvait pas retenir ses larmes,</p>
<p>Et sa mère s'est aussi mise à pleurer,</p>
<p>Jana s'est transformée d'une petite enfant,</p>
<p>A une fille responsable qui prend les choses en main et qui réussit là où les adultes ont échoué...</p>
<p>Ses parents se sont réconciliés,</p>
<p>Et leur foyer tient jusqu'au jour d'aujourd'hui...</p>
<p>Et la tempête est passée grâce à Jana,</p>
<p>Même si son grand frère a craqué et a quitté la maison,</p>
<p>Jana est restée positive et a réussi à réunir sa famille,</p>
<br>
<p>Je dis la même chose à tous les enfants dont les parents ne s'entendent plus,</p>
<p>En tant que leur fils ou leur fille tu auras plus d'influence que n'importe quel autre adulte ou ami de la famille...</p>
<p>Tu auras plus d'influence qu'un million de discours de spécialistes qu'ils pourraient écouter,</p>
