<p>J'ai envie de dire à tous les enfants,</p>
<p>Ne pensez jamais que c'est seulement vous qui apprenez des choses de vos parents,</p>
<p>Vos parents ont aussi besoin d'apprendre des choses de vous,</p>
<p>Ils ont aussi besoin de votre amour et de votre tendresse,</p>
