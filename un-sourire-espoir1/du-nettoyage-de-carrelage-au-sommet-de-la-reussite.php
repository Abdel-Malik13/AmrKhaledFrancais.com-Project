                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>Je vais vous raconter l'histoire d'un ami :</p>
<p>Il s'appelle Dr. Mohamed,</p>
<p>Cette histoire montre la grandeur et l'intelligence de son père,</p>
<br>
<p>Je demande toujours aux jeunes de bouger,</p>
<p>De s'activer,</p>
<p>De travailler pendant les vacances scolaires,</p>
<p>De ne jamais rester sans rien faire...</p>
<br>
<p>Je voulais leur donner un exemple concret,</p>
<p>Pendant que le Dr. Mohamed était au lycée,</p>
<p>Son père possédait une maison d'édition,</p>
<p>Il est donc venu voir son fils en disant :</p>
<p>« Écoute, chez moi personne ne prend d'argent de poche pendant l'été, Si tu veux de l'argent travaille pendant tes vacances, et je te donnerai la même somme que ce que tu gagneras par jour... »</p>
<p>Un très bel exemple à suivre pour tous les parents...</p>
<p>Le père du Dr. Mohamed possédait une imprimerie,</p>
<p>Et quel est le bas de l'échelle dans ce domaine ?</p>
<p>C'est de vendre des journaux...</p>
<p>Le fils du patron de la plus grande maison d'édition en Egypte a donc passé ses vacances dans la rue en criant « Journaux... Journaux...Journaux... »</p>
<p>Et il vendait aux passants,</p>
<p>Le soir il allait voir son père en disant aujourd'hui j'ai gagné tant... Et son père lui donnait la même somme en plus.</p>
<p>C'est l'exemple concret d'une personne qui va réussir dans la vie !</p>
<p>A tous les jeunes,</p>
<p>Activez-vous,</p>
<p>Lancez-vous,</p>
<p>Travaillez et transpirez un peu comme le Dr. Mohamed !</p>
<br>
<p>La première année il a donc vendu des journaux dans la rue,</p>
<p>La deuxième année son père lui a dit : « Pendant les vacances, tu travailleras cette fois à l'imprimerie, tu vas passer la serpillère et nettoyer le carrelage... »</p>
<p>Dr Mohamed a donc travaillé au plus bas de l'échelle, en nettoyant le sol...</p>
<p>La troisième année son père lui a dit « Cette fois tu vas aider le responsable technique, en remplissant l'encre, en gérant les cartouches... etc. »</p>
<p>La quatrième année il était à l'université alors son père l'a envoyé passer son stage en Angleterre,</p>
<p>dans une imprimerie ou il apprenait les techniques pour mélanger les encres...</p>
<p>La cinquième année son père voulait qu'il travaille dans la maison d'édition pour connaitre le domaine de l'édition...</p>
<p>Remarquez comment ce père transforme son fils en homme au fur et à mesure...</p>
<p>L'année suivante son père lui a demandé de passer du temps avec les écrivains, comprendre comment ils écrivent, comment ils réfléchissent, rassembler leurs feuilles, les classer, lire leurs livres et les noter...</p>
<p>L'année d'après il a envoyé son fils finir ses études en Angleterre, à l'imperial university de Londres...</p>
<br>
<p>Dr. Mohamed est aujourd'hui une des plus grandes réussites en Egypte,</p>
<p>Il a ouvert sa propre maison d'édition,</p>
<p>Car il a bataillé,</p>
<p>Car il a travaillé dur pour réussir,</p>
<p>Ceux qui restent dans la file d'attente après avoir fini leurs études...</p>
<p>Sans bouger en pensant que le plus dur c'est d'avoir obtenu leur diplôme,</p>
<p>La file d'attente est très longue...</p>
<p>Et celui qui reste dans la file sans bouger se fait écraser par le temps...</p>
<br>
<p>Pour dépasser tout le monde dans la file et arriver en premier, il faut rester actifs, faire des petits boulots, commencer par le bas de l'échelle... Ce sont eux qui arrivent en tête.</p>
<p>Ils sont assurés de réussir et de garder un sourire d'espoir toute leurs vies.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
