                                        <br>
                                        <p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle des couleurs : rouge, bleu, vert, jaune... etc.</p>
<p>Mais quel est le rapport entre notre émission et les couleurs ?</p>
<p>Laissez-moi vous dire une chose mes chers :</p>
<p>Avant la révolution en Egypte, on était sous un seul régime qui nous oppressait... Alors on se taisait et on ne voyait qu'une seule couleur...</p>
<p>Mais après la révolution on est devenus avec beaucoup d'idées et beaucoup de couleurs...</p>
<p>La question est : Est-ce qu'on va s'accepter les uns les autres ?</p>
<p>Est-ce qu'on va s'entendre les uns avec les autres ?</p>
<p>Ou bien chacun dira « Ma couleur uniquement ! Je ne veux pas voir les autres couleurs »</p>
<p>Ou bien les couleurs vont se mélanger?</p>
<p>Ou alors il y'aura une couleur qui voudra dominer les autres ?</p>
<p>Est-ce que les couleurs vont se mixer ?</p>
<p>Avant de parler d'Egypte, laissez-moi vous raconter l'histoire des couleurs...</p>
<p>A la base combien y'a-t-il de couleurs ?</p>
<p>Il n y'en a que 3 !</p>
<p>Les 3 couleurs primaires de l'arc en ciel sont le rouge, le bleu et le jaune...</p>
<p>Et en mélangeant ces 3 couleurs on obtient toutes les couleurs du monde, vrai ou pas ?</p>
<p>A la base elles sont trois, et elles ont donné naissance à des millions d'autres couleurs,</p>
<p>Si tu es dans ta voiture, ou dans ta chambre sur l'ordinateur, regarde autour de toi et commence à compter les couleurs que tu vois...</p>
<p>Qu'est ce qui s'est passé ?</p>
<p>Les 3 couleurs primaires se sont métissées,</p>
<p>Mais à des pourcentages qui ne laissent pas une couleur annuler une autre...</p>
<p>Comprenez bien mes paroles, je parle de notre pays en parlant des couleurs...</p>
<p>Imaginez si ces 3 couleurs refusent de se mélanger :(</p>
<p>La vie deviendrait pauvre...</p>
<p>Et imaginez si une des 3 couleurs domine une autre... Il ne resterait plus que 2 couleurs... Ca serait encore plus triste !</p>
<p>Vous comprenez ce que je dis ?</p>
<p>Dieu (exalté soit-il) a voulu nous montrer un exemple concret grâce à sa création...</p>
<p>Quand les trois couleurs se sont métissées en tolérant l'autre,</p>
<p>Elles se sont mélangées à des doses convenables,</p>
<p>(A la fin chaque couleur reste libre et indépendante)</p>
<p>Résultat : la vie est devenue très richeeeeeeeeeeee en couleurs !</p>
<p>Sinon elle aurait été très triste si les 3 étaient bornées...</p>
<p>Vous avez compris l'idée derrière les couleurs ?</p>
<p>Notre pays est pareil,</p>
<p>Si on sait se mélanger, et s'accepter,</p>
<p>Même si nos couleurs sont différentes,</p>
<p>Même si nos idées sont différentes,</p>
<p>Le pays deviendra très très très riche !</p>
<p>Mais si on refuse de se mixer,</p>
<p>Ou qu'une couleur veut s'imposer et effacer les autres... Le pays deviendra plus pauvre...</p>
<p>Vous aimeriez voir votre pays plus riche ou plus pauvre ?</p>
<p>Acceptez le mélange avec les autres,</p>
<p>Acceptez le fusionnement positif,</p>
<p>Mais en même temps restez fiers de votre méthode et de votre différence,</p>
<p>On verra alors plein de couleurs, et on dessinera un grand sourire d'espoir :)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
