                                        <br>
                                        <p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est une question :</p>
<p>Qu'est ce qui arrive quand l'honnêteté et le perfectionnisme se rassemblent chez quelqu'un ?</p>
<p>Si quelqu'un est minutieux dans son travail, et qu'il est droit... Si ces deux qualités se trouvent chez lui qu'est ce qui se produira au bout d'un moment ?</p>
<p>Je vais vous dire ce qui va se produire : C'est obligé que des occasions en or se présentent, et que cette personne connaisse une grande réussite !
<p>Je vous le jure, c'est une règle de la vie,</p>
<p>Une règle que Dieu a mise dans cet univers,</p>
<p>L'occasion se présentera... Elle se présentera... à toute personne intègre et sérieuse dans son travail... L'occasion se présentera... Elle se présentera... Je le répète et j'insiste en le disant,</p>
<p>Je suis sur de ce que je dis,</p>
<p>Si tu es de bonne foi, et que tu es soigneux dans ton travail,</p>
<p>Je te jure par Dieu !</p>
<p>Tu n'as pas d'argent ?</p>
<p>Tu n'as pas l'occasion d'être promu dans ton travail ?</p>
<p>Tu as envie que quelqu'un découvre ton talent ?</p>
<p>Je te jure par Dieu que l'occasion va se présenter!</p>
<p>Si tu es honnête et appliqué dans ton travail... Sois tranquille, tu grandiras et tu réussiras, c'est une des règles du ciel.</p>
<p>Tu connaîtras inévitablement la réussite !</p>
<p>Je vais vous raconter l'histoire de l'oncle Saber :</p>
<p>Haj Saber était un artisan monteur de lunettes,</p>
<p>Il travaillait dans les années 40 en Egypte,</p>
<p>Il était créatif et excellait dans son travail,</p>
<p>Mais il n'avait pas d'argent...</p>
<p>Il travaillait chez un patron suisse qui possédait le plus grand magasin d'optique en Egypte,</p>
<p>Les célébrités venaient faire leurs lunettes dans cette boutique... etc.</p>
<p>Le patron suisse avait beaucoup d'employés,</p>
<p>La moitié d'entre eux n'était pas minutieux dans leur travail,</p>
<p>Et la moitié d'entre eux travaillaient sans se soucier...</p>
<p>Mais au milieu d'eux se trouvait un travailleur très concentré, très perfectionniste...</p>
<p>La patron suisse l'a testé plusieurs fois...</p>
<p>Saber est très très très intègre,</p>
<p>Et Saber est très très très soigneux dans son travail...</p>
<p>Je te jure que l'occasion en or se présentera à toi Saber !</p>
<p>C'est obligé, c'est une règle divine !</p>
<p>Qu'est ce qui va se passer ?</p>
<p>Le patron suisse qui a passé tant d'années à faire grandir sa boutique a pris de l'âge,</p>
<p>Son seul fils vivait en Suisse.</p>
<p>Le patron suisse a eu un cancer,</p>
<p>Les médecins lui ont dit qu'il ne lui restait plus beaucoup de temps à vivre...</p>
<p>L'homme a commencé à réfléchir « Pourquoi je devrais rester en Egypte, il faut que je rentre en Suisse »</p>
<p>En plus c'était une période difficile de l'histoire, pendant la 2ème guerre mondiale...</p>
<p>Il a décidé de quitter l'Egypte en urgence...</p>
<p>Il ne pourra jamais vendre sa boutique à temps...</p>
<p>Que faire dans ce cas ?</p>
<p>Il va appeler son employé intègre et minutieux...</p>
<p>Il va partir voir le jeune Saber en disant « Saber tu es quelqu'un de très perfectionniste, notre savoir faire ne va pas se perdre... Et tu es quelqu'un de très intègre, tu ne mangeras jamais ma part... J'aimerais te vendre le magasin pour le peu d'argent que tu possèdes</p>
<p>Et le reste de la somme, tu pourras me le donner petit à petit pendant les 4 prochaines années... tu le donneras à mon fils, car je ne pense pas vivre jusque là »</p>
<p>Il a signé, et il a vendu le magasin à Saber,</p>
<p>Saber est devenu sans s'y attendre le patron du plus grand magasin d'optique en Egypte,</p>
<p>Une occasion en or s'est présentée...</p>
<p><strong>{ Ceux qui croient et font de bonnes œuvres... vraiment Nous ne laissons pas perdre la récompense de celui qui fait le bien. }</strong></p>
<p>Sourate 18 - Al-Kahf : La Caverne (verset 30)</p>
<br>
<p>C'est une histoire vraie que je vous raconte mes chers,</p>
<p>Saber a fait grandir la boutique,</p>
<p>C'est devenu une chaine de magasins d'optique,</p>
<p>Avec énormément de branches,</p>
<p>Et par fidélité, les magasins ont gardé le nom de l'ancien patron suisse jusqu'au jour d'aujourd'hui.</p>
<p>Pas au nom de haj Saber,</p>
<p>Et il a dit à ses enfants de toujours garder les magasins avec le nom de cet homme suisse.</p>
<p>Même après son décès, il a continué à envoyer l'argent à son fils en Suisse,</p>
<p>Saber a grandi, il a réussi, il est devenu riche, et Dieu lui a ouvert les portes,</p>
<p>Et il a éduqué ses enfants de la meilleure des façons,</p>
<p>Mais la plus grande leçon qu'il leur a donné, c'est de toujours garder le nom du monsieur suisse dans les boutiques par loyauté envers lui.</p>
<p>C'est magnifique quand 3 choses se réunissent chez une même personne : Intégrité + perfectionnisme + fidélité</p>
<p>Avec ces trois qualités, on réussit dans la vie, Dieu est satisfait de nous, et on réussira dans l'au-delà.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
