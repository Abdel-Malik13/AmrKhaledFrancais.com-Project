                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui raconte une histoire qu'a vécue le Prophète (bénédictions et paix soient sur lui)</p>
<p>Une histoire pleine d'enseignements pour nous !</p>
<p>A tous les jeunes,</p>
<p>A tous les étudiants,</p>
<p>A tous les nouveaux diplômés,</p>
<p>L'idée c'est :</p>
<p>Qui veut réussir dans la vie ?</p>
<p>Qui veut arriver en tête ?</p>
<p>Qui veut dépasser tous les autres et arriver le premier ?</p>
<p>Le premier dans le sport,</p>
<p>Le premier à l'école,</p>
<p>Le premier dans la poésie et l'art,</p>
<p>Le premier dans sa relation avec Dieu...</p>
<p>Qui veut faire partie des précurseurs ?</p>
<p>Au début de l'islam le Prophète (bpsl) a croisé un homme avec un nom difficile à prononcer :</p>
<p>Dou Al Jawchane Adababi,</p>
<p>Le Prophète (bpsl) l'a croisé juste après la bataille de Badr,</p>
<p>Les musulmans avaient vaincu le clan de Qoraych,</p>
<p>Mais ce n'était pas une victoire définitive,</p>
<p>D'autres batailles allaient venir,</p>
<p>Faites bien attention à la manière avec laquelle le Prophète (bpsl) s'est adressé à lui...</p>
<p>Il lui a dit <strong class="prophete">« Ô Dou Al Jawchane, N'aimerais-tu pas être parmi les premiers ? »</strong></p>
<p>Le Prophète (bpsl) lui proposait donc de devenir musulman,</p>
<p>Mais pourquoi ne pas lui avoir dit « N'aimerais-tu pas devenir musulman ? »</p>
<p>Le Prophète (bpsl) lui a proposé d'être parmi les premiers musulmans...</p>
<p>Le Prophète (bpsl) parlait à l'ambition qui était en lui,</p>
<p>Le Prophète (bpsl) faisait bouger en lui l'envie de réussir et d'arriver en tête avant les autres...</p>
<br>
<p>Que dis-tu d'être le premier dans ta famille à faire la prière du matin à l'heure ?</p>
<p>Que dis-tu d'être le premier dans ta rue à faire un projet avec un chrétien et montrer l'exemple à suivre ?</p>
<p>Que dis-tu d'être parmi les premiers, et finir la lecture du Coran avant l'arrivée du Ramadan ?</p>
<p>Que dis-tu d'être celui qui obéit le plus à ses parents ?</p>
<p>Que di-tu d'être le premier dans ton entreprise à proposer une idée innovante ?</p>
<p>N'aimerais-tu pas être parmi les premiers ? Comme l'a dit le Prophète (bpsl) à Dou Al Jawchane...</p>
<p>Mais en verité Dou Al Jawchane n'avait pas d'ambition...</p>
<p>Il a répondu « Non, mais je vais attendre, si vous remportez la victoire je vous rejoindrai... Et si les autres gagnent alors il ne me sera rien arrivé de mal»</p>
<p>Dou Al Jawchane voulait tenir le bâton par le milieu...</p>
<p>Le Prophète (bpsl) a compris, et l'a laissé.</p>
<p>Le temps s'est écoulé,</p>
<p>Des années plus tard les musulmans ont gagné,</p>
<p>Ils sont arrivés à la Mecque,</p>
<p>Et Bilal est monté faire l'appel à la prière...</p>
<p>Et les gens venaient en foule pour devenir musulmans,</p>
<p>Ca y est il n y'avait plus de premiers...</p>
<p>Et parmi ces gens qui venaient il y'avait Dou Al Jawchane,</p>
<p>Le Prophète (bpsl) l'a vu arriver de loin,</p>
<p>Il lui a sourit,</p>
<p>Le sourire de la désolation...</p>
<p>C'est maintenant qu'il arrivait... Au milieu de tous les autres... Pourtant le Prophète (bpsl) était venu le voir en personne pour lui proposer d'être parmi les premiers...</p>
<p>Dou Al Jawchane s'est mis devant le Prophète (bpsl) puis il a prononcé la Chahada « Je témoigne qu'il n'y a pas d'autre dieu qu'Allah et que tu es son Messager. »</p>
<p>L'histoire se termine là...</p>
<p>Mais il reste une dernière ligne,</p>
<p>La personne qui a rapporté l'histoire dit dans son commentaire qu'il a vu Dou Al Jawchane se mordre les doigts de ne pas avoir été parmi les premiers.</p>
<br>
<p>L'idée est arrivée ?</p>
<p>Pour ne pas avoir de regrets,</p>
<p>Sois le premier dans chaque chose que tu fais dans la vie,</p>
<p>Ne reste pas toujours dans la foule...</p>
<p>De temps en temps prend l'initiative,</p>
<p>A l'école ou dans tes études par exemple,</p>
<p>Ou dans ta façon de te comporter avec ta maman,</p>
<p>Ou sois le premier à créer une association ou à réaliser un projet dans ton quartier,</p>
<p>Nourris ton ambition,</p>
<p>Active-toi,</p>
<p>Et arrive en tête au moins dans un domaine ou une chose que tu sais faire :)</p>
<p>Si tu appliques la parole du Prophète (bpsl)</p> <p><strong class="prophete">« N'aimerais-tu pas être parmi les premiers ? »</strong></p>
<p>Tu vivras avec un sourire d'espoir !</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
