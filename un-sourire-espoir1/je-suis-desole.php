                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un une nouvelle parole, avec un nouveau sourire d'espoir.</p>
<p>La parole d'aujourd'hui est une parole magique !</p>
<p>Je vous jure par Dieu que cette parole règle des tonnes de problèmes.</p>
<p>Et cette parole éteint le feu de la colère.</p>
<p>Vous remarquez le nombre de disputes qui se produisent tous les jours entre les gens ?</p>
<p>Dans la rue, dans la maison,</p>
<p>Et des maris avec leurs épouses,</p>
<p>Et des parents avec leurs enfants,</p>
<p>Et des jeunes avec les adultes,</p>
<p>Et entre conducteurs et motards,</p>
<p>Et entre collègues au travail,</p>
<p>Je vais vous donner le mot magique qui va régler tout ca !</p>
<p>« Je suis désolé »</p>
<p>Je vous jure par Dieu, cette parole à quel point elle est belle !</p>
<p>Cette parole éteint le feu de la colère d'une telle efficacité !</p>
<p>Et n'ai jamais honte en la prononçant...</p>
<p>Quand tu commets une erreur, Je te jure par Dieu que ce n'est pas honteux de la prononcer...</p>
<p>Et ne pense jamais que c'est une faiblesse,</p>
<p>Je te jure par Dieu que s'excuser est une force !</p>
<p>Et où est le problème si en conduisant ta voiture tu commets une erreur, et qu'au lieu de sortir ta main et le menacer ou crier, tu dis simplement « je suis désolé » au conducteur d'à coté ?</p>
<p>Vous savez ce que je vous dis, je l'ai beaucoup essayé dans ma vie,</p>
<p>Et le conducteur d'à coté n'est pas content, et il veut se disputer pour ce que tu as fais,</p>
<p>Et peut-être même que tu n'es pas si fautif que ca,</p>
<p>Regarde le simplement en disant « je suis désolé »</p>
<p>Et la personne devant toi va se calmer entièrement, et te dire « c'est moi qui m'excuse, je vous en prie passez devant »</p>
<p>Imagine entre toi et ton épouse, et la dispute est à propos d'un bouton de veste, ou trop de sel dans le repas, et au bout d'un moment tu dis « ce n'est pas grave, je me suis énervé pour rien, je suis désolé »</p>
<p>Imagine avec ton supérieur au travail, tu discutes, tu discutes, et tu discutes pour prouver que tu n'as pas tord... Jusqu'à ce que la personne en face de toi soit fatiguée,</p>
<p>Prononce juste la formule magique « je suis désolé » elle est délicieuse , et très facile, et très belle !</p>
<p>Imagine avec ton père ou avec ta mère ?</p>
<p>Leur dire « je suis désolé » je te jure par Dieu que c'est une force !</p>
<p>Et je te jure par Dieu que ca signifie que tu as confiance en toi-même !</p>
<p>Et que tu es fort, et que tu n'es pas faible, et en la disant la personne devant toi va te respecter,</p>
<p>Pourquoi es tu donc gêné de la prononcer ?</p>
<p>Pourquoi as-tu peur de la prononcer ?</p>
<p>Excuse toi simplement quand tu commets une erreur... Ou même la moitié d'une erreur ;)</p>
<p>Je vais vous raconter une situation qui vas vous faire rire :</p>
<p>Un soir pendant le ramadan, je donnais un discours dans une mosquée, c'était en 2001 ou 2002</p>
<p>Et c'était pendant les 10 derniers jours du ramadan, et on était trop serrés dans la mosquée...</p>
<p>20 ou 30 mille personnes, et pendant la prière les téléphones des gens n'arrêtaient pas de sonner</p>
<p>Je suis calme par nature, mais ce soir là, les téléphones sonnaient tellement et nous gênaient dans la prière que ca m'a mis en colère .</p>
<p>Alors j'ai pris le microphone,</p>
<p>Et j'ai commencé par dire, que ca ne se fait pas, que c'est indigne d'une maison de Dieu, veuillez tous éteindre vos téléphones... etc</p>
<p>Et pendant que je disais ca, Une sonnerie de téléphone s'entendait de plus en plus...</p>
<p>Augmentait... Augmentait... Augmentait... Et plus le volume de la sonnerie augmentait plus je m'énervais...</p>
<p>Alors j'ai mis la main dans ma poche, et finalement ce téléphone qui sonnait c'était le mien :)</p>
<p>Comment fond donc les gens pour sortir d'une situation pareille ?</p>
<p>Des personnes vont éteindre discrètement leur mobile, et faire semblant que ce n'était pas elles...</p>
<p>Et d'autres personnes vont continuer à disputer les autres, et dire ca ne se fait pas, comme si c'était le téléphone de quelqu'un d'autre qui sonnait...</p>
<p>Mais moi mes chers, je suis sorti de cette situation avec un sourire.</p>
<p>Je leur ai dis, c'est vrai que laisser le téléphone allumé dans la mosquée est une très mauvaise chose, mais parfois on n'a pas le choix... et j'ai dis ca en riant</p>
<p>« Je suis désolé, c'est mon téléphone qui sonnait, ca ne se reproduira plus jamais »</p>
<p>Les gens ont sourit, rit, et les jours suivants tout le monde pensait à éteindre leurs téléphones portables.</p>
<p>Je te propose de t'entrainer à prononcer le mot magique... beau... reposant... qui te fait gagner le respect des autres... qui fait voir aux autres à quel point tu as confiance en toi...</p>
<p>« Je suis désolé »</p>
<br>
<p>Prenez soin de vous,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
