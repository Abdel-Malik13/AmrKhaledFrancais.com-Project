<p>Tu n'as pas aidé les gens autour de toi,</p>
<p>Tu n'as pas construit des choses utiles,</p>
<p>Tu as vécu : Tu t'es levé, tu as dormi, tu as mangé, Et les jours se sont écoulés sans que tu n'aies rien fait de bon...</p>
<p>Ne te laisse pas voler ta vie !</p>
<p>Si le mois de Ramadan est <strong>{ un nombre déterminé de jours }</strong></p>
<p>Ta vie aussi est <strong>{ un nombre déterminé de jours }</strong></p>
