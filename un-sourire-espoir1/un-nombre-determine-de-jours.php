<p>Ils se terminent très vite...</p>
<p>Je vous dis qu'hier... hier... Je jouais au foot avec mes copains,</p>
<p>J'avais 17ans, et dans nos têtes il n y'avais rien d'autre que le football...</p>
<p>Les jours sont passés, et j'ai des enfants, et j'ai grandi...</p>
<p>La vie est extrêmement courte,</p>
<p>Je vous en prie, ne vous laissez pas voler votre vie !</p>
<p>Moi personnellement je ne regrette pas ma vie,</p>
