                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>J'ai envie de vous demander,</p>
<p>Comment on se comporte avec nos jeunes ?</p>
<p>Comment on se comporte avec nos enfants ?</p>
<p>Vous me croyez si je vous dis que parfois on se comporte avec nos enfants d'une seule et même façon...</p>
<p>Cette façon c'est la dispute ,</p>
<p>L'affrontement,</p>
<p>La punition,</p>
<p>Les rapports de force,</p>
<p>Je peux te dire quelque chose ?</p>
<p>N'as-tu pas remarqué que cette façon ne donne plus de résultat ?</p>
<p>Qu'avec tes enfants, tu te disputes, et tu hausses le ton... etc etc</p>
<p>Et à la fin rien ne change ?</p>
<p>Peux tu essayer une autre solution que celle là ?</p>
<p>Change de clé !</p>
<p>Ca veut dire quoi change de clé ?</p>
<p>Imagine que tu possédes une maison,</p>
<p>Et à chaque fois que tu veux ouvrir la porte, la clé ne fonctionne pas...</p>
<p>Et toi tu t'entêtes à ouvrir la porte avec cette clé et aucune autre...</p>
<p>Change donc de clé avec tes enfants...</p>
<p>La dispute et l'engueulade ne fonctionne plus désormais,</p>
<p>C'est une nouvelle génération...</p>
<p>Les temps ont changé...</p>
<p>La punition ne donne plus de résultats,</p>
<p>Trouve donc une nouvelle clé,</p>
<p>Tu me demanderas quelle est cette nouvelle clé ?</p>
<p>Réfléchissons ensemble,</p>
<p>Trouvons une nouvelle façon de faire passer le message à nos enfants,</p>
<p>Je vais te raconter une anecdote qui t'aidera à comprendre :</p>
<p>Je connais un couple de parents, qui avaient un fils adolescent,</p>
<p>Et ils essayaient de lui parler, mais à cet âge il ne voulait pas les écouter,</p>
<p>Et ils insistaient, et leur fils ignorait tout ce qu'ils lui disaient...</p>
<p>Sais tu ce qu'ils ont fait ?</p>
<p>Ils ont fait une chose très astucieuse,</p>
<p>Ils ont commencé à lui écrire des lettres...</p>
<p>Des lettres qu'ils posaient sur son bureau ou à coté de son lit,</p>
<p>Et sur l'enveloppe il y'avait cette phrase « Lis la quand tu seras seul »</p>
<p>Et dans la lettre il y'avait des paroles comme « Nous t'aimons, et nous ne voulons pas te déranger, Nous allons t'écrire nos conseils et nos avis sur des lettres... Et tu es libre de les lire ou pas, tu resteras notre fils... »</p>
<p>Puis ils ont commencé à donner leurs conseils... 1 puis 2 puis 3 puis 4</p>
<p>Comme une série,</p>
<p>Et vous me croyez si je vous dis que c'est le fils lui-même qui m'a raconté ca une fois qu'il est devenu adulte...</p>
<p>Il m'a dit « Ce qui m'a protégé pendant mon adolescence, ce sont les lettres que Papa et Maman me laissaient, je faisais semblant de ne pas les lire comme chaque adolescent j'étais fier et têtu... Et je les lisais puis je les remettaient à leur place comme si personne n'y avait touché... Ils me parlaient du danger des drogues... Et me disaient mon fils, mon amour je t'en supplie n'y touche jamais... Et il me parlaient avec amour, et je sentais qu'ils étaient sincères... Je me sentais en confiance... Je savais qu'ils n'allait pas m'écrire une lettre gentille pour me disputer une semaine après... Mes parents sont le secret de ma réussite grâce aux lettres qu'ils me laissaient...»</p>
<p>As-tu déjà pensé à envoyer à ton enfant un email ?</p>
<p>As-tu déjà pensé à laisser une lettre à ton fils ?</p>
<p>As-tu déjà essayé de devenir ami avec ta fille ?</p>
<p>Que penses tu de l'idée du père ami, et de la mère amie...</p>
<p>Moi et mon fils sommes amis...</p>
<p>Moi et ma fille sommes copines...</p>
<p>On sort ensemble, On va au cinéma ensemble, On mange du pop corn ensemble, On fait du sport ensemble, On se ballade ensemble, On joue aux jeux vidéo ensemble...</p>
<p>On est amis !</p>
<p>On est copains !</p>
<p>A tous les parents s'il vous plait changez de clé...</p>
<p>La méthode d'avant ne sert plus, les temps ont changé,</p>
<p>La punition et l'affrontement ne donne plus de résultats...</p>
<p>Change donc de clé, et deviens ami avec ton enfant,</p>
<p>Et innove et crée de nouvelles clés,</p>
<p>Tu gagneras son cœur,</p>
<p>Et tu dessineras un sourire d'espoir sur son visage et sur les visages de toute la famille !</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
