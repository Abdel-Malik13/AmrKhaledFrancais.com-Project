                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>

<p>L'épisode d'aujourd'hui est une très belle parole du Prophète (bénédictions et paix soient sur lui) dans laquelle il dit <strong class="prophete">« Dieu a des serviteurs qu'il a sélectionnés pour rendre service aux gens, les gens se précipitent vers eux. Ce sont eux qui sont en sûreté le jour de la résurrection, ce sont eux qui sont en sûreté le jour de la résurrection, ce sont eux qui sont sûreté le jour de la résurrection.»</strong></p>
<p>Elle est très belle cette parole du Prophète (bpsl)</p>
<p>Il y'a des serviteurs que Dieu a créé et a choisi pour régler les problèmes des autres, pour rendre service, pour aider les pauvres... Les gens affluent vers eux car ils savent qu'ils règlent les problèmes, ce sont eux qui sont en sûreté/sécurité le jour de la résurrection (le Prophète (bpsl) l'a répété 3 fois)</p>
<p>Fais-tu partie de ces gens ?</p>
<p>Dieu t'a-t-il choisi pour en faire partie ?</p>
<p>Mon frère le Ramadan approche,</p>
<p>Pourquoi tu ne te lances pas ?</p>
<p>Propose tes services,</p>
<p>Comme les soldats qui se portent volontaires devant leur commandant,</p>
<p>En disant « Moi... Moi... Choisissez-moi ! »</p>
<p>Pourquoi ne pas faire la même chose avec Dieu le très haut ?</p>
<p>Dis « Je suis disponible et je vais consacrer mon temps libre à aider les autres »</p>
<p>Ou « J'ai commencé à aider les autres, sélectionne moi Ô mon Seigneur »</p>
<p>Par contre est-ce que les gens affluent vers toi ?</p>
<p>Est-ce qu'ils viennent vers toi, ou pensent tout de suite à toi dès qu'ils ont un problème ?</p>
<p>Remarque bien :</p>
<p>C'est parce que les autres se précipitent vers eux (quand ils sont en panique ou soucieux ou inquiets), que ces gens sont en sûreté / tranquillité le jour de la résurrection...</p>
<p>Vous imaginez mes chers ?</p>
<p>As-tu commis des fautes dans ta vie ?</p>
<p>Tu me diras « Bien sûr ! »</p>
<p>Tu as peur que le jour de la résurrection ces fautes se transforment en catastrophes ?</p>
<p>Tu me diras encore « Bien sûr ! »</p>
<p>Tu auras très peur n'est-ce pas ?</p>
<p>Imagine que tu peux être serein ce jour là...</p>
<p>Sur terre les gens venaient vers toi inquiets, et tu les rassurais, puis tu réglais leurs problèmes...</p>
<p>La sûreté le jour de la résurrection est dans le fait que tu rends services maintenant.</p>
<p>Cette parole du Prophète est très belle !</p>
<p>Alors le mois de Ramadan arrive bientôt,</p>
<p>Comment comptes-tu rendre service aux autres ?</p>
<p>Je vais offrir le panier du Ramadan (Pour nourrir une famille)...</p>
<p>Je vais donner à manger à des pauvres...</p>
<p>Je vais réconcilier entre deux personnes qui se sont fâchées...</p>
<p>Que chacun regarde autour de lui et trouve une façon de rendre service.</p>
<p>Imaginez si chaque jour tu appliques cette idée « Je vais rendre service à une personne » pendant tout le mois,</p>
<p>C'est sûr que Dieu t'aura sélectionné, ce sont 30 jours = 30 fois !</p>
<p>Surtout que le mois de Ramadan est le meilleur mois de l'année,</p>
<p>Puis tu as l'air de vouloir continuer même après le Ramadan...</p>
<p>Félicitations à toi !</p>
<p>Tu seras serein le jour de la résurrection.</p>
<p>Un sourire d'espoir pas ici bas, mais un sourire d'espoir le jour de la résurrection,</p>
<p>Félicitations !</p>
<p>Mes chers motivez-vous, et trouvez les personnes que vous allez aider,</p>
<p>Vous aurez un sourire d'espoir et une sécurité le jour de la résurrection.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
