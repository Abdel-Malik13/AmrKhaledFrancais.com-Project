                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'histoire d'aujourd'hui vient d'Egypte,</p>
<p>Les égyptiens mes chers sont des gens authentiques...</p>
<p>J'ai envie de vous raconter cette histoire car elle est très très très belle,</p>
<p>Pourquoi on raconte toujours les mauvaises choses, et qu'on voit juste les défauts ?</p>
<p>L'autre a volé, et l'autre a trahi... etc.</p>
<p>Moi j'ai envie de montrer notre bon côté :</p>
<p>Cette histoire s'est passée il y'a quelques années seulement,</p>
<p>C'est un homme d'affaires,</p>
<p>Il achetait et vendait du maïs, du blé... etc.</p>
<p>Ses usines étaient au delta en Egypte,</p>
<p>Cet homme était musulman et il avait un associé chrétien,</p>
<p>Ils se connaissaient depuis plusieurs années et avaient l'habitude de travailler ensemble,</p>
<p>Une bonne affaire s'est présentée : une grande quantité de marchandises allait être importée, et ce sont eux qui allaient la distribuer dans tout le pays.</p>
<p>Et l'associé chrétien devait partir en voyage, donc il n'a pas eu le temps de signer le contrat,</p>
<p>Par contre il a laissé la moitié de l'argent à son associé musulman.</p>
<p>Celui qui me raconte c'est justement lui,</p>
<p>Il a appelé son associé chrétien pour lui dire « Il faut absolument que tu sois présent pour signer le contrat, pour que ta part soit garantie »</p>
<p>Il lui a dit qu'il ne pourrait pas, car il avait une urgence et qu'il devait voyager.</p>
<p>Il lui a répondu « Personne n'a la garantie d'être en vie demain, donc fais l'effort de venir signer puis pars en voyage juste après »</p>
<p>Son associé chrétien lui a dit « Je signerai à mon retour d'ici 3 jours, signe le contrat seul, et prend tout en charge en attendant mon retour »</p>
<p>Deux jours après cette discussion, l'associé chrétien est décédé...</p>
<p>Il est décédé sans signer, donc officiellement il n'avait aucun droit.</p>
<p>Son associé pouvait tout prendre, personne n'était au courant de rien,</p>
<p>Mais par intégrité il a appelé le fils de l'homme décédé en disant « Je t'en prie, viens avec ta maman, je dois vous voir...»</p>
<p>« Madame, je dois à votre mari tant de millions, c'est sa part dans la grande affaire qu'on a eu, malheureusement il n'a pas eu l'occasion de signer, il n y'a aucune preuve, mais vous recevrez tout l'argent cette semaine »</p>
<p>Et il a tenu sa parole...</p>
<p>Mais pourquoi je vous raconte tout ca ?</p>
<p>Pour vous parler d'intégrité ?</p>
<p>Non juste pour vous parler des égyptiens,</p>
<p>C'est une histoire vraie, mais il y'en a des millions d'autres...</p>
<p>L'homme est décédé et sa femme et son fils ne l'auraient jamais su, il n'y avait aucune preuve matérielle,</p>
<p>Mais Dieu le très haut lui le savait...</p>
<p>Je vous en prie mes chers, racontez à vos enfants ce genre d'histoires,</p>
<p>Que toutes les histoires qu'on raconte ne tournent pas autour d'escroqueries et de vols,</p>
<p>Il y'a bien sûr des arnaques et de la malhonnêteté, je vis parmi les gens... J'entends et je vois...</p>
<p>Mais je vous en prie, mettons en valeur les bonnes choses,</p>
<p>Montrons les exemples à suivre,</p>
<p>Je veux que mon fils sache qu'il existe plein de gens honnêtes et intègres,</p>
<p>Car si tous les exemples qu'il voit sont négatifs de qui il pourra s'inspirer ?</p>
<p>Il se sentira étranger dans ce monde...</p>
<p>Le sourire d'espoir ne viendra que si on raconte aux plus petits des histoires comme celle de cet épisode,</p>
<p>Le pays est rempli de bonnes personnes, il existe plein de gens qui font le bien...</p>
<p>Racontons leurs histoires à nos enfants.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
