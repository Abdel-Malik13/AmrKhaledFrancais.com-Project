                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage...</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Regardez mes chers,</p>
<p>J'ai envie de vous dire une chose :</p>
<p>Quand on vit pour nous même, on nait petits, on vit petits, et on meurt petits...</p>
<p>Mais quand on vit avec et pour les autres, notre vie s'allonge avec la vie de chaque être humain à qui on a donné un sourire...</p>
<p>Dans le sourire d'espoir d'aujourd'hui j'ai envie de te dire :</p>
<p>Peux tu rendre heureux les gens autour de toi ?</p>
<p>Peux tu penser à ceux qui t'entourent ?</p>
<p>Peux tu ne pas vivre pour toi-même ?</p>
<p>Je t'en supplie ne vis pas uniquement pour toi-même !</p>
<p>L'histoire que je vais vous raconter est l'histoire d'un petit garçon de 10 ans...</p>
<p>Ce garçon partait dans un club, et jouait avec ses amis,</p>
<p>Et ses parents lui donnaient de l'argent de poche pour se faire un peu plaisir,</p>
<p>Le garçon avait envie de manger une glace,</p>
<p>Alors il est parti s'asseoir au café du club,</p>
<p>Et il a appelé le serveur pour lui demander « Combien coûte une glace au chocolat ? »</p>
<p>Le serveur lui a répondu « 5 livres égyptiennes » (environ 0.60 €)</p>
<p>Le garçon trouvait que c'était cher, mais pas grave...</p>
<p>Il a sorti les pièces de sa poche et a commencé à les compter,</p>
<p>Et le serveur le regardait pendant ce temps là...</p>
<p>Bien sur celui qui raconte l'histoire c'est le serveur,</p>
<p>Il raconte « J'ai vu qu'il a trouvé dans sa poche 5 livres exactement »</p>
<p>Alors le garçon a pris les 5 livres et a regardé le serveur...</p>
<p>Le serveur commençait à s'impatienter... Tu vas acheter, ou tu vas juste nous faire perdre du temps...</p>
<p>Le garçon a donc regardé le serveur et lui a demandé « Et combien coute une glace sans chocolat ? »</p>
<p>Le serveur lui a répondu sur un ton plus haut « 4 livres monsieur...» (environ 0.50€)</p>
<p>Alors le petit s'est mis à recompter ses pièces,</p>
<p>Et ca énervait le serveur, car il pensait que ce petit était entrain de lui faire perdre son temps... qu'il avait des clients plus importants à servir...</p>
<p>Le petit garçon a donc trouvé qu'il avait toujours 5 livres alors il a demandé à avoir une glace sans chocolat.</p>
<p>Le serveur lui a donc ramené une glace sans chocolat à 4 livres,</p>
<p>Le petit garçon a finit de manger sa glace, puis il est parti...</p>
<p>Quand le serveur est revenu, il a versé des larmes...</p>
<p>Savez vous pourquoi ?</p>
<p>Parce que le jeune garçon a laissé sur la table 4 livres + 1 livre de pourboire...</p>
<p>Le petit a préféré prendre la glace la moins chère pour laisser 1 livre au serveur...</p>
<p>Les parents de ce petit garçon lui ont appris à ressentir les gens,</p>
<p>Dieu va prendre soin de ce petit garçon pendant toute sa vie...</p>
<p>Peux tu s'il te plait éduquer tes enfants de façon à ce qu'ils ressentent les gens ?</p>
<p>Peux tu emmener tes enfants dans un quartier pauvre, et transporter ensemble des sacs de riz, et distribuer des aides...</p>
<p>Peux tu faire gouter à tes enfants la chose la plus délicieuse dans la vie ?</p>
<p>Qu'ils ne vivent pas seulement pour eux-mêmes, mais aussi pour les autres...</p>
<p>Croyez moi mes chers, le plaisir de donner est bien meilleur que le plaisir de recevoir,</p>
<p>Croyez moi, quand on vit pour nous même, on nait petits, on vit petits, et on meurt petits...</p>
<p>Mais quand on vit pour les autres, notre vie s'allonge avec la vie de chaque personne à qui on a donné un sourire...</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
