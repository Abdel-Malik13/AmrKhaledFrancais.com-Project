                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'histoire d'aujourd'hui s'est passée en Angleterre,</p>
<p>En me baladant au hyde park de Londres, j'ai croisé un homme assez âgé,</p>
<p>Il faisait froid ce jour là,</p>
<p>Il essayait de grimper à un arbre :)</p>
<p>J'ai cherché à savoir ce qu'il faisait,</p>
<p>En réalité il y'avait un petit nid que le vent avait déplacé,</p>
<p>Dans le nid se trouvait un oisillon coincé entre les branches qui criait...criait...criait...</p>
<p>Cet homme retraité voulait donc grimper à l'arbre pour remettre le nid à sa place,</p>
<p>Regardez bien ce qui va se passer :</p>
<p>Il n'arrivait pas à grimper car il n'était plus très jeune...</p>
<p>Il essayait, puis réessayait... Et après beaucoup de tentatives il a réussi à grimper,</p>
<p>Au moment où il a mis sa main pour déplacer le nid,</p>
<p>L'oisillon a paniqué, il a commencé à le mordre,</p>
<p>L'homme a crié tellement il s'était fait mal...</p>
<p>Il est redescendu... Il a attendu une minute pour voir ses doigts...</p>
<p>Puis il avait décidé de regrimper,</p>
<p>Il n'a pas retenu la leçon :)</p>
<p>Une fois en haut, il a remis sa main pour déplacer le nid,</p>
<p>L'oisillon l'a encore mordu,</p>
<p>Il est redescendu...</p>
<p>Puis il a regrimpé une troisième fois, il s'est fait mordre encore plus fort...</p>
<p>Puis une quatrième fois, et il se faisait mordre à chaque fois,</p>
<p>Un ami de cet homme lui a dit « C'est de ta faute, tu t'es fait mordre 4fois, et tu continues à grimper ! »</p>
<p>Cet homme lui a répondu par une phrase formidable...</p>
<p>Il lui a dit « La nature de cet oisillon c'est de mordre... Et ma nature à moi c'est d'aider... Sa nature restera de mordre, et ma nature restera de vouloir l'aider »</p>
<p>Il est remonté encore et encore... Et il a fini par remettre le nid à sa place, et l'oisillon a arrêté de crier.</p>
<p>L'homme est descendu avec des blessures, mais sa phrase raisonnait dans ma tête</p>
<p>« Sa nature c'est de mordre... Ma nature c'est d'aider...»</p>
<p>Vous avez compris ce que je veux dire ?</p>
<p>Comporte-toi avec les gens selon ta nature, selon ton éducation,</p>
<p>Et non-pas selon les leurs...</p>
<p>Même si leur comportement est mauvais, et qu'ils te font du mal,</p>
<p>Ne les imite pas, c'est à eux de t'imiter !</p>
<p>Savez-vous que Dieu fait la même chose avec nous ?</p>
<p>Combien de fois on a commis des erreurs et des péchés...</p>
<p>Mais Dieu le très haut nous traite avec miséricorde et douceur,</p>
<p>Regarde cette magnifique invocation que le Prophète (bénédictions et paix soient sur lui) nous a apprise :</p>
<p><strong class="prophete">« Ô Allah, ne nous traite pas avec ce qui est digne de nous, mais traite-nous avec ce qui est digne de toi, et c'est toi qui est Le plus digne d'être craint; et c'est toi qui détient le pardon »</strong></p>
<br>
<p>Celui qui te fait du mal parce que c'est dans ses valeurs, ne lui fait pas de mal car tes valeurs sont différentes...</p>
<p>Même si toutes les personnes autour de toi perdent leurs valeurs, reste droit et garde les tiennes,</p>
<p>Ne te rabaisse jamais,</p>
<p>Garde tes bonnes valeurs,</p>
<p>Garde ton sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
