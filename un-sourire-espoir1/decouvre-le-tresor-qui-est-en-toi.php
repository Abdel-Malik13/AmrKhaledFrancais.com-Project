                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
                                        <br>
<p>Bienvenue avec un une nouvelle parole, avec un nouveau sourire d'espoir dans une nouvelle journée.</p>
<p>La parole d'aujourd'hui je souhaite la titrer « Découvre le trésor qui est en toi »</p>
<p>Je vous jure que Dieu a créé en chacun de nous un trésor !</p>
<p>Ca veut dire quoi au fond de nous il y'à un trésor ?</p>
<p>Ca veut dire chacun de nous possède un talent exceptionnel, ce talent sera la source de notre force, et la source de notre fierté, et la source de notre subsistance, et la source de notre réussite dans la vie...</p>
<p>Dieu est juste mes chers,</p>
<p>Ne pensez jamais que Dieu a donné à quelques uns des talents immenses, et qu'il en a privé des millions d'autres...</p>
<p>Chacun de nous sans exception a en lui un trésor !</p>
<p>C'est juste qu'il y'à des personnes qui découvrent ce trésor, alors elles vivent des vies exceptionnelles, et attirent les compliments de tout le monde, et réussissent, et atteignent les sommets...</p>
<p>Et les autres personnes alors ?</p>
<p>Malheureusement beaucoup de personnes vieillissent et finissent par être enterrées avec leur trésor.</p>
<p>Mais Dieu lui ? Non ! Il a donné à chaque être humain son trésor personnel.</p>
<p>Toi qui m'écoute en ce moment, n'imagine pas un seul instant que tu n'as pas de trésor ou de don... C'est entièrement faux.</p>
<p>Dieu est juste !
<p>Chacun a eu sa part de don, il faut juste être intelligent et savoir mettre la main dessus.</p>
<p>Qui d'entre vous pourrait découvrir son trésor ?</p>
<p>Si tu le découvres tu pourras accéder à de l'or.</p>
<p>Et tu ne va pas seulement en tirer de l'argent, tu vas en tirer aussi de l'estime, du respect,</p>
<p>Car tu auras réussi dans un domaine, médecine, architecture, art...</p>
<p>Découvre juste le trésor que Dieu t'a donné.</p>
<p>Vous savez mes chers, j'ai commencé par faire des études dans une faculté de commerce,</p>
<p>Et je n'aimais pas les calculs, mais bon mes notes m'ont permises d'entrer dans cette filière...</p>
<p>J'ignorais à ce moment là le trésor qui était en moi,</p>
<p>Ensuite j'ai travaillé comme expert comptable,</p>
<p>Et je détestais les calculs,</p>
<p>Puis j'ai été promu vice-directeur dans un grand cabinet de comptabilité en Egypte,</p>
<p>Et je détestais faire ce métier...</p>
<p>Pourtant ensuite j'ai ouvert mon propre cabinet de comptabilité,</p>
<p>Parce que j'ignorais le trésor que je possédais en moi.</p>
<p>A ce point ? je détestais les chiffres et les calculs,</p>
<p>Ce que j'aime faire c'est communiquer avec les personnes, créer des liens humains,</p>
<p>Imaginez à quel point j'étais éloigné de mon trésor ?</p>
<p>Et puis j'ai fini par découvrir mon trésor, « Comment ? » c'est là toute la question.</p>
<p>Je te demande depuis le début de découvrir ton trésor, tu vas me dire « et comment faire ? »</p>
<p>La réponse c'est : bouge dans la vie, multiplie les efforts, fais partie d'une association à but humanitaire, suis des cours différents de ta formation initiale, apprend une nouvelle langue, apprend à maitriser une nouvelle chose sur internet plutôt que passer le temps à tchater sur facebook ou jouer à des jeux sur ton téléphone mobile , apprend une nouvelle technique, teste un nouveau sport, l'essentiel c'est de bouger pour mettre la main sur ton don.</p>
<p>Il y'à en réalité deux façons de découvrir ton trésor :</p>
<p>Un expert qui te montre ton don,</p>
<p>Comme le prophète sws quand il observait les compagnons, et disait à Khalid ibn al-walid n'apprend pas trop de Coran, ton don est dans l'art de guerre, tu es né pour être un chef, un meneur d'hommes.</p>
<p>Mais il disait à Omar, ne pars pas beaucoup en guerre, ton rôle est d'être conseiller ou ministre c'est là ou tu es le plus utile.</p>
<p>Et il disait à Bilal, Dieu t'a donné une belle voix, tu vas entrer dans l'histoire si tu utilises ta voix pour faire l'appel à la prière.</p>
<p>Le prophète sws était donc un expert pour montrer à chacun le trésor qui est en lui.</p>
<p>Mais alors toi si tu ne trouves pas cet expert pour t'aider, que peux tu faire ?</p>
<p>Le prophète sws n'est plus là pour te dire ne gâche pas ton temps, ton trésor est à cet endroit précis.</p>
<p>Alors il te reste la deuxième façon : Active toi !</p>
<p>Teste plusieurs choses dans la vie,</p>
<p>Fais plusieurs métiers,</p>
<p>Côtoie différentes personnes différentes,</p>
<p>Jusqu'à ce que le trésor commence à scintiller et à clignoter,</p>
<p>Et quand il aura apparu, ta vie deviendra meilleure,</p>
<p>Et tu seras riche au sens propre et figuré,</p>
<p>Et tu seras utile à ta société et à toute l'humanité,</p>
<p>Je t'en prie, commence par bouger à partir d'aujourd'hui pour découvrir le trésor au fond de toi.</p>
<p>Merci, un nouveau sourire d'espoir,</p>
<br>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
