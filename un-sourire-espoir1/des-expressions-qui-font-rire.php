                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui raconte comment les gens réfléchissent...</p>
<p>Je vais te raconter des façons de raisonner, et elles te feront rire, car tu vas te reconnaitre dedans :)</p>
<p>Tu vas rire car tout le monde fait la même erreur,</p>
<p>Mais je t'en prie pendant que tu ris, dis-toi que tu ne raisonneras plus jamais de cette façon.</p>
<p>Je vais te citer des expressions et des exemples, le titre de cet épisode est « Moi et les autres »</p>
<p>Ou « Nous et les autres »</p>
<p>Ce qu'on dit sur nous et ce qu'on dit sur les autres...</p>
<p>Regarde mon cher :</p>
<p>Si quelqu'un d'autre échoue, c'est à cause de sa stupidité... Il se lance dans un projet sans avoir d'expérience et sans savoir ce qu'il fait...</p>
<p>Mais si moi j'échoue... Alors l'erreur est humaine...</p>
<p>On ne dit pas ca ?</p>
<p>:)</p>
<p>Si quelqu'un d'autre met trop de temps à accomplir une tâche au travail... C'est parce qu'il est paresseux et qu'il fait exprès de perdre du temps...</p>
<p>Mais si moi je mets trop de temps à accomplir une tâche au travail... C'est parce que je suis perfectionniste, et que je veux rendre une copie propre !</p>
<p>Vrai ou pas ?</p>
<p>:)</p>
<p>Si un collègue au travail ne fait pas ce qu'il devait faire... C'est parce qu'il est négligent et pas sérieux...</p>
<p>Mais si moi je ne fais pas ce que je devais faire... C'est parce qu'ils me donnent trop de travail, et que je suis débordé !</p>
<p>:)</p>
<p>Si quelqu'un d'autre demande un arrêt maladie... C'est parce qu'il fait semblant d'être malade...</p>
<p>Mais si moi je demande un arrêt maladie... C'est parce que je suis vraiment malade... Je n'arrive même pas à bouger...</p>
<p>:)</p>
<p>Si quelqu'un d'autre prend une mauvaise décision... C'est parce qu'il ne comprend rien...qu'il est nonchalant...</p>
<p>Mais si moi je prends la même mauvaise décision... C'est parce que les conditions dans lesquelles je me trouve m'ont obligé à la prendre... Et au contraire c'est une décision pleine de sagesse !</p>
<p>:)</p>
<p>Si un collègue parle au directeur avec gentillesse et sourire... C'est parce qu'il est fayot...</p>
<p>Mais si moi je parle au directeur avec le sourire... C'est parce que je suis poli et très bien éduqué...</p>
<p>:)</p>
<p>Si un collègue parle au même directeur avec un ton dur... Oulalaaa il est malpoli...</p>
<p>Mais si moi je durcis le ton avec mon supérieur... C'est parce que je suis franc, que j'ai du caractère et que j'ai mis les points sur les i...</p>
<p>:)</p>
<p>Vous vous rendez compte mes chers de la différence de notre indulgence avec nous même et avec les autres ?</p>
<p>Si un collègue au travail fait quelque chose qu'on ne lui a pas demandé... C'est parce qu'il veut se faire remarquer...</p>
<p>Mais si moi je fais la même chose... C'est parce que j'ai l'esprit vif et que je prends des initiatives...</p>
<p>:)</p>
<p>Si un collègue s'absente de son bureau... C'est sur qu'il fait ca pour fuir le travail et nous laisser le faire à sa place...</p>
<p>Mais si moi je m'absente... C'est parce que j'ai quelque chose d'important à faire, et que j'ai tout préparé avant de partir...</p>
<p>:)</p>
<p>Beaucoup de gens font la même chose... Beaucoup de gens raisonnent de cette façon...</p>
<p>Le Prophète (bénédictions et paix sur lui) a dit dans une parole :</p>
<p><strong class="prophete">« L'un de vous voit une épine dans l'œil de son frère, et oublie une branche dans son œil »</strong></p>
<br>
<p>Peut-on nous concentrer d'avantage sur nos erreurs à nous...</p>
<p>Au lieu d'accuser les autres,</p>
<p>Lui ne sait rien,</p>
<p>Lui est mauvais,</p>
<p>Lui est ceci ou cela...</p>
<p>Peux-tu réfléchir à comment toi tu vas t'améliorer et réussir ?</p>
<p>Je vous jure mes chers que quand on se concentre sur les défauts des autres, on oublie de réussir...</p>
<p>Et pire on n'empêche pas la réussite des autres... On bloque seulement la notre...</p>
<p>Mais si on se concentre sur nos défauts, sur comment les gommer... à ce moment on réussit et on laisse les autres réussir...</p>
<p>Un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
