                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>L'épisode d'aujourd'hui nous explique comment craindre Dieu...</p>
<p>Beaucoup pensent que craindre Dieu c'est prier, jeuner...</p>
<p>Non mes chers il n y'à pas que ca !</p>
<br>
<p>Il faut craindre Dieu également dans notre vie sociale,</p>
<p>On fait tous très attention à nos rapports avec Dieu...</p>
<p>Mais un peu moins attention à nos rapports avec les gens...</p>
<br>
<p>Sais tu que Dieu pardonne quand tu commets une faute avec lui,</p>
<p>Mais quand tu es fautif avec une autre personne, il ne te pardonne pas avant que cette personne t'ai pardonné...</p>
<br>
<p>Malheureusement on se concentre quand il s'agit de nos rapports avec Dieu, mais on néglige nos rapports avec les créatures de Dieu...</p>
<br>
<p>J'ai une histoire étonnante qui nous vient de Syrie :</p>
<p>C'est un homme riche qui vivait avec son épouse...</p>
<p>Il possédait des commerces, des immeubles, et de grandes affaires...</p>
<p>Avec le temps sa femme a senti le comportement de son mari changer,</p>
<p>Comme s'il cachait un secret...</p>
<p>Cette femme était intelligente, elle a enquêté discrètement, et elle a découvert qu'il avait épousé une deuxième femme sans lui dire...</p>
<p>Cette femme par politesse n'a rien dit... Elle a gardé ca pour elle,</p>
<p>Les jours passaient, et elle se comportait normalement avec son mari,</p>
<p>C'était son choix, bon ou mauvais, mais c'était son choix...</p>
<p>Son mari est décédé au bout d'un moment,</p>
<p>Et on a commencé à partager l'héritage...</p>
<p>Et c'est là la grande surprise,</p>
<p>Cette femme a refusé que l'héritage soit partagé seulement entre elle et ses enfants...</p>
<p>Elle a dit par crainte de Dieu, nous devons donner aux gens leurs droits,</p>
<p>Elle a demandé à ses enfants d'aller voir la 2ème épouse pour lui donner sa part d'héritage...</p>
<p>Cette femme est exemplaire,</p>
<p>Elle a compris le sens de la crainte de Dieu,</p>
<p>Ce n'est pas seulement mettre le voile, prier, jeuner et lire le Coran...</p>
<p>La crainte de Dieu c'est aussi dans notre comportement, dans nos valeurs, dans nos relations avec les autres.</p>
<p>Elle a donc envoyé à la 2ème épouse sa part d'héritage,</p>
<p>Et là nouvelle surprise...</p>
<p>La deuxième épouse a refusé en disant que cet argent ne lui revenait pas de droit, elle avait divorcé une semaine avant le décès de son mari...</p>
<p>Voici l'exemple concret de deux femmes exceptionnelles !</p>
<p>On voit autour de nous des familles se disperser,</p>
<p>Des frères finir au tribunal pour des problèmes d'héritage...</p>
<p>Ces deux femmes ne se connaissaient pas, mais elles connaissaient Dieu,</p>
<p>La 1ère a dit : « Donner lui sa part d'héritage »</p>
<p>Et la 2ème a dit : « j'ai divorcé une semaine avant son décès »</p>
<p>Qui peut en faire de même ?</p>
<p>Dire que je crains Dieu dans l'héritage,</p>
<p>Et je crains Dieu dans mes relations avec les autres,</p>
<p>Et je crains Dieu avec mes sœurs,</p>
<p>Mes chers celui qui vivra avec ces valeurs, goutera au sourire d'espoir !</p>
<p>Donner aux autres leurs droits, et vous vivrez heureux sur terre et dans l'au-delà.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
