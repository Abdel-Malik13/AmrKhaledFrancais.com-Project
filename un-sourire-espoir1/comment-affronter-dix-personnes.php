                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<br>
<p>Aujourd'hui je veux vous raconter une chose qui m'est arrivée pendant que j'étais étudiant à l'université,</p>
<p>Mes parents m'ont appris depuis que je suis petit à agir face aux injustices tout en restant poli...</p>
<p>Ca veut dire, si tu vois une chose déplaisante, dénonce là mais avec politesse,</p>
<p>Face à une injustice ne reste jamais sans rien faire, en tant que spectateur,</p>
<p>Agis mais selon les règles de courtoisie, sans te battre ni élever le ton...</p>
<p>La situation que je vais vous raconter a changé ma vie :</p>
<p>J'étais en 3ème année de commerce,</p>
<p>En pleine période d'examens, je révisais toute la nuit jusqu'à l'heure du fajr (prière du matin)</p>
<p>30min avant l'heure de la prière j'arrêtais les révisions, mais j'avais peur de m'endormir avant la prière,</p>
<p>Alors je prenais ma voiture modeste, et j'allais prendre quelque chose à manger chez un camion à pizzas...</p>
<p>Après avoir mangé, j'allais directement prier à la mosquée,</p>
<p>Puis je rentrais dormir.</p>
<p>Et le lendemain je refaisais la même chose...</p>
<br>
<p>Comme c'était la période des examens, la rue était vide la nuit,</p>
<p>Un jour je me suis arrêté devant le camion à pizzas,</p>
<p>J'ai baissé la vitre en attendant qu'il me serve...</p>
<p>Et soudainement une dizaine de jeunes sont arrivés dans 3 ou 4 voitures,</p>
<p>Chacun d'eux était plus grand et plus costaud que moi :)</p>
<p>Ils sont sortis de leurs voitures,</p>
<p>Ils ont mis la musique à fond,</p>
<p>Puis une fille est sortie d'une des voitures,</p>
<p>Elle s'est mise à danser au milieu,</p>
<p>Et les autres l'applaudissaient...</p>
<p>J'allais mourir de l'intérieur,</p>
<p>Comment c'est possible de voir une chose pareille ?</p>
<p>Une scène qu'on n'a pas l'habitude de voir en Egypte,</p>
<p>J'étais très gêné par ce que je voyais,</p>
<p>Et je voulais réagir, faire quelque chose...</p>
<p>D'un coté je ne peux pas rester immobile, et ne rien faire,</p>
<p>De l'autre côté je suis quelqu'un de poli et respectueux...</p>
<p>Que faire dans ce cas ?</p>
<p>Un coin de ma tête me disait : rentre dormir... ils sont dix costauds et tu es tout seul...</p>
<p>Mais je n'arrivais pas à partir...</p>
<p>Un autre coin de ma tête me disait : Va prier à la mosquée...</p>
<p>Mais je n'arriverai pas à me concentrer pendant la prière, j'aurai l'esprit perturbé...</p>
<p>Un autre coin de ma tête me disait : Laisse tomber et rentre réviser...</p>
<p>Pareil je n'y arriverai pas...</p>
<p>Rentre dormir alors...</p>
<p>Ma conscience ne me laissera pas trouver le sommeil...</p>
<p>Il faut absolument que je fasse quelque chose.</p>
<p>J'étais encore jeune et sans expérience dans la vie :)</p>
<p>Alors j'ai ouvert la porte de la voiture, et je la tenais dans ma main pour vite remonter au cas où...</p>
<p>Et je me suis adressé aux jeunes en disant « Au fait ca ne se fait pas ce que vous êtes entrain de faire »</p>
<p>Un des jeunes m'a répondu « Pourquoi tu restes loin... Approche-toi et viens nous dire ce que tu as à dire »</p>
<p>J'ai pensé ca y est je vais me faire taper dessus :)</p>
<p>Mais j'ai pris mon courage à deux mains et j'ai dit « Bien sûr je vais venir vous parler »</p>
<p>En réalité cette scène a eu un grand impact dans ma vie,</p>
<p>Je me suis rendu compte que celui qui agit face à une injustice trouve une grande force en lui,</p>
<p>Tant qu'il reste poli...</p>
<p>Très fort, même s'il est en petit nombre et moins fort physiquement...</p>
<p>Je me suis mis au milieu des 10</p>
<p>Et le jeune m'a poussé par l'épaule en me disant « Alors tu disais quoi tout à l'heure ? »</p>
<p>Je lui ai répondu « Ca ne se fait pas de faire ce que vous faites »</p>
<p>Il m'a dit « Ah ouais ?! Et pourquoi ? »</p>
<p>J'ai répondu « Vous ne savez pas que Notre Seigneur (qu'Il soit glorifié et exalté) descend chaque nuit au ciel de la terre au dernier tiers de la nuit, et Il dit : <strong>Qui est en train de prier pour que Je lui réponde ?</strong> <strong>Qui demande quelque chose pour que Je lui accorde ?</strong> <strong>Qui demande miséricorde pour que Je lui pardonne ?</strong> »</p>
<p>« Si vous voulez faire ce que vous faites, allez au moins chez vous, fermez la porte et faites ça sans que personne ne le sache... Mais pas en pleine rue »</p>
<p>Dès que j'ai dit ca, la musique s'est arrêtée,</p>
<p>La fille qui dansait est remontée dans une voiture,</p>
<p>Personne ne disait plus rien,</p>
<p>J'étais le seul à parler et ils m'écoutaient attentivement...</p>
<p>A cette époque je n'avais encore jamais parlé devant un microphone, ni donné un discours... Ni même imaginé que je le ferai un jour.</p>
<p>Mais cette situation a bouleversé mon cœur,</p>
<p>Car ils m'ont écouté,</p>
<p>J'ai réussi à les convaincre sans me battre,</p>
<p>Ils m'ont respecté,</p>
<p>Et j'ai fait leur connaissance par la suite...</p>
<p>Et j'ai réussi ma mission,</p>
<p>Et ca m'a servi pour le reste de ma vie,</p>
<p>Agis face aux injustices avec politesse et courtoisie et tu gagneras,</p>
<p>Teste cette idée et tu verras par toi-même.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
