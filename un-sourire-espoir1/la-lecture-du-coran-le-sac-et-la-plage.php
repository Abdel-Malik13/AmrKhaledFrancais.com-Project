                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>L'épisode d'aujourd'hui parle du noble Coran,</p>
<p>Est-ce que tu lis le noble Coran ?</p>
<p>Est-ce que tu sens que le noble Coran t'est très très très utile dans ta vie ?</p>
<p>Est-ce que le noble Coran te rapproche de Dieu ?</p>
<br>
<p>On lit tous le noble Coran pour gagner des bonnes actions, et pour chercher la satisfaction de Dieu.</p>
<p>Mais est-ce qu'on lit le noble Coran pour corriger nos défauts ?</p>
<p>Est-ce qu'on voit le noble Coran comme un soin pour nos cœurs ?</p>
<br>
<p>Dieu le très haut dit</p>
<p><strong>{ Nous faisons descendre du Coran, ce qui est une guérison et une miséricorde pour les croyants.}</strong></p>
<p>Sourate 17 - Al-Isra' : Le Voyage Nocturne (verset 82)</p>
<br>
<p>Le noble Coran est une guérison pour nous !</p>
<br>
<p>Et dans un autre verset Dieu le très haut dit</p>
<p><strong>{ Ô gens! Certes, une preuve évidente vous est venue de la part de votre Seigneur. Et Nous avons fait descendre vers vous une lumière éclatante.}</strong></p>
<p>Sourate 4 - An-Nissa : Les Femmes (verset 174)</p>
<p>Le noble Coran est une lumière qui illumine ton âme et ton cœur,</p>
<p>Le noble Coran est plein de principes et de bons comportements pour nous guider,</p>
<br>
<p>Je sais que certains me diront « Je suis d'accord avec tout ce que tu dis, mais quand je lis le noble Coran mes défauts ne changent pas...</p>
<p>Mon comportement ne change pas même après avoir beaucoup lu le noble Coran... »</p>
<p>Je leur répondrai en disant :</p>
<p>Mon cher tu ne fais pas attention à une chose très importante,</p>
<p>Le noble Coran purifie les âmes de l'intérieur,</p>
<p>Ca veut dire quoi ?</p>
<p>Au fond de nous on est pleins de problèmes, d'inquiétudes...</p>
<p>Pleins de poussières à cause de ce qu'on voit, ce qu'on entend, ou ce qu'on fait la journée...</p>
<br>
<p>Le noble Coran vient justement nettoyer cette poussière de l'intérieur,</p>
<p>Enlever la rouille qui s'est installée sur nos cœurs...</p>
<br>
<p>Je vais vous raconter une histoire pour mieux comprendre :</p>
<p>Un enfant est parti voir son père en disant « Papa, je ne lis pas le noble Coran, car quand je le lis je n'arrive pas à prendre un des principes que j'y trouve, et l'appliquer dans ma vie...</p>
<p>Je lis les versets les uns après les autres... Et à la fin j'oublie tout »</p>
<br>
<p>Son père a eu une idée formidable,</p>
<p>Il lui a donné un sac en paille en disant</p>
<p>« Le fond du sac est sale,</p>
<p>Je veux que tu partes à la plage et que tu me le remplisses d'eau... »</p>
<p>Le garçon a remplis le sac d'eau salée,</p>
<p>Et sur le chemin, il n y'avait déjà plus d'eau... Elle avait fui par les trous...</p>
<p>Le père lui a demandé « Elle est où l'eau que je t'ai demandée ? »</p>
<p>Le fils a dit « elle a coulé sur le chemin »</p>
<p>Le père a pris le sac en paille, et il a dit « Regarde au fond fiston, le sac est devenu propre... Toute la saleté qui s'y trouvait a disparu,</p>
<p>Le sac n'a pas retenu l'eau, mais elle l'a nettoyé...</p>
<p>Quand tu lis le noble Coran il arrive la même chose avec ton cœur »</p>
<br>
<p>Quand tu lis le noble Coran, c'est comme si tu plongeais ton cœur dans un océan plein d'eau pure et propre...</p>
<p>A la sortie tous les problèmes et toutes les inquiétudes ont disparu...</p>
<p>Peut-être que tu n'as pas retenu un principe et une valeur et que tu l'appliques dans ta vie,</p>
<p>Mais crois moi, le niveau de propreté de ton âme a augmenté,</p>
<p>Tu as été purifié de l'intérieur...</p>
<p>Tu comprends maintenant la signification de ce verset</p>
<p><strong>{ Nous faisons descendre du Coran, ce qui est une guérison et une miséricorde pour les croyants.}</strong></p>
<p>Sourate 17 - Al-Isra' : Le Voyage Nocturne (verset 82)</p>
<br>
<p>Le noble Coran est une guérison et une miséricorde pour nous,</p>
<p>Il apaise nos cœurs,</p>
<p>Ce n'est pas forcément parce que tu lis beaucoup,</p>
<p>Lis même 10 versets par jour,</p>
<p>Mais ne rompt jamais le lien avec le livre sacré de Dieu,</p>
<p>Reste toujours en contact avec le message que Dieu t'a envoyé,</p>
<p>C'est sûr et certain que les paroles de Dieu vont t'être utiles,</p>
<p>C'est sûr et certain que les paroles de Dieu vont te faire du bien,</p>
<p>Lisons le noble Coran, et dessinons des sourires d'espoir dans nos cœurs.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
