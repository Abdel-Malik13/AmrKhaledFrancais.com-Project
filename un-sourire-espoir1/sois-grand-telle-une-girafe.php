                                        <br>
                                        <p>Quand la vie semble difficile, parfois nous avons besoin d'entendre une nouvelle idée qui nous aide,
entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne
qui a réussi...</p> <p>Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage
L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue à tous avec un nouveau sourire d'espoir,</p>
<p>En vérité je me suis trouvé à l'aéroport de Londres, car je voyageais d'un pays vers un autre, et l'avion a fait une escale à Londres.</p>
<p>J'ai aperçu une grande affiche publicitaire, j'essayais de lire le nom de la marque, mais je n'en ai trouvé aucune, mais alors quel était le but de cette affiche ?</p>
<p>L'affiche en réalité était une image montée, de trois girafes , et bien sûr la girafe est grande de taille, et son cou est long, elle voulait atteindre un arbre pour manger ses feuilles... Et les 3 girafes essayent d'atteindre cet arbre mais n'y arrivent pas, car l'arbre est plus haut qu'elles...</p>
<p>Et donc après un montage photo, ils ont rajouté une quatrième girafe au dessus des 3 autres , celle-ci a atteint l'arbre et s'est mise à manger ses feuilles en toute facilité.</p>
<p>Et en bas de la photo il y'à un très joli message « Si tu es grand et que tu peux devenir encore plus grand, alors n'hésite jamais ! »</p>
<p>Une façon de dire que les trois girafes sont grandes, mais il y'à une quatrième qui avait un rêve encore plus grand , alors elle est montée encore et encore...</p>
<p>En réalité beaucoup de gens sont doués et possèdent un grand potentiel, dans le sport, dans l'art, dans l'industrie, dans la culture... Mais il n'ont pas l'ambition d'être encore plus grands, ils se contentent du niveau qu'ils ont atteint.</p>
<p>Et je me suis renseigné, pourquoi ont-il mis cette affiche ? Ils m'ont dit ce n'est pas une publicité pour une entreprise, c'est une affiche pour tous les gens pour les encourager, que chacun se sente grand dans un domaine, et qu'il peut devenir encore plus grand... Crois en toi, nourris ton ambition !</p>
<p>Comme si c'est pour nous dire, sois en concurrence avec toi-même , dépasse-toi, ne te satisfait jamais de ce que tu as atteint.</p>
<p>Parfois on se contente d'être bon dans un domaine, mais alors grandis, élève ton niveau. Je suis toujours étonné quand quelqu'un crée une société et qu'il se contente qu'elle soit une réussite dans son quartier ou sa ville... Mais alors vise d'être la meilleure société du pays, vise de devenir une multinationale, vise d'être la meilleure société mondiale, elles est où l'ambition ? pourquoi tu n'entres pas en concurrence avec toi-même ? pourquoi tu ne fais pas grandir ta réussite encore et encore ?</p>
<p>Je vous raconte cette histoire comme si c'est pour vous dire cette phrase encore : que chaque personne douée, que chaque personne qui a réussi, à tous les chefs d'entreprises, à chaque personne qui excelle dans quelque chose, je te dédie cette belle phrase encore « Si tu es grand et que tu peux devenir encore plus grand, alors n'hésite jamais ! »</p>
<p>Je vous remercie, à Demain si Dieu le veut avec un nouveau sourire d'espoir.</p>
