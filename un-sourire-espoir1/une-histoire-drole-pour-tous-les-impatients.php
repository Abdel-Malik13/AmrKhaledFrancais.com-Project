                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui te dit :</p>
<p>Tu veux garder ton sourire d'espoir ?</p>
<p>Ne prends jamais une décision dans la précipitation !</p>
<p>L'histoire d'aujourd'hui aidera toutes les personnes qui jugent trop vite, ou décident trop vite...</p>
<p>Ils changeront d'avis à la fin :)</p>
<p>On raconte que le directeur d'une entreprise était quelqu'un qui décidait trop vite,</p>
<p>Il avait un style autoritaire et pas un style consultatif...</p>
<p>Ses décisions étaient fortes et courageuses, mais malheureusement il manquait de patience,</p>
<p>Un jour il est sorti de son bureau, et il a aperçu un jeune adossé au mur...</p>
<p>Tous les employés étaient en train de travailler sauf lui,</p>
<p>Tous les autres étaient à leurs postes sauf lui,</p>
<p>Le directeur n'a pas du tout apprécié, mais il n'a pas pris le temps de demander,</p>
<p>Il l'a bien observé puis il est parti sans dire un mot...</p>
<p>Le lendemain il a retrouvé le même jeune encore adossé au mur les bras croisés,</p>
<p>Encore une fois il n'a pas pensé à lui demander ce qu'il faisait,</p>
<p>Il est parti directement le voir en disant « Combien vous touchez par mois ? »</p>
<p>Le jeune n'a pas aimé la question...</p>
<p>Sous la pression du directeur il a fini par dire «à peu près 1000 livres égyptiennes par mois »</p>
<p>Le directeur a sorti de sa poche 1000 livres égyptiennes en disant « Voici votre salaire du mois, vous êtes viré ! »</p>
<p>Puis il a élevé la voix pour que tous les autres employés l'entendent</p>
<p>« Cet endroit est fait pour les gens qui veulent travailler,</p>
<p>Mais que je vous trouve hier adossé au mur,</p>
<p>Et aujourd'hui pareil,</p>
<p>Voici votre paie, et je ne veux plus vous voir ici ! »</p>
<p>Le jeune a pris l'argent avec le sourire,</p>
<p>Puis il est parti en courant sans se retourner...</p>
<p>Les employés se sont mis à rire,</p>
<p>Le directeur commençait à sentir quelque chose d'anormal...</p>
<p>Il s'est approché d'un des employés, et lui a demandé « Ce jeune que je viens de virer, il occupait quel poste ? »</p>
<p>Il lui a répondu « Au fait il ne travaillait pas ici, c'est le livreur de pizza, il vient ici tous les jours à l'heure du déjeuner, il attendait juste qu'on rassemble la somme d'argent pour le payer »</p>
<p>:)</p>
<p>L'idée est arrivée ?</p>
<p>Les gens qui se précipitent, qui tranchent sans avoir pris le temps de comprendre...</p>
<p>J'aime beaucoup un verset du noble Coran, qui parle du prophète Salomon :</p>
<p><strong>{ Puis il passa en revue les oiseaux et dit: «Pourquoi ne vois-je pas la huppe? est-elle parmi les absents?» }</strong></p>
<p>Sourate 27 - An-Naml : Les Fourmis (verset 20)</p>
<p>Tous les oiseaux devaient être présents à ce moment là,</p>
<p>Mais la huppe était absente,</p>
<p>Salomon aurait pu dire « La huppe n'est pas venue, sa punition sera ceci et cela »</p>
<p>Mais si on lui répondait « Au fait la huppe est là-bas, c'est vous qui ne l'avez pas vue »</p>
<p>Dans ce cas, de quoi il aurait eu l'air ?</p>
<p>Salomon par sagesse a d'abord commencé par lui-même :</p>
<p>«Pourquoi ne vois-je pas la huppe? »</p>
<p>C'est peut-être moi qui ne la vois pas...</p>
<p>Ensuite il a posé la question :</p>
<p>« Est-elle parmi les absents? »</p>
<p>Ce très beau verset nous montre la bonne manière de gérer,</p>
<p>D'abord se remettre en question, avant de remettre les autres en question.</p>
<p>Si tu es maman, je t'en prie ne tape pas ton enfant avant de comprendre pour quelle raison il a fait cette bêtise...</p>
<p>Si tu es directeur ou employé, ne fais pas de reproches à tes collègues avant de bien comprendre la situation...</p>
<p>Comme Salomon, fais d'abord une pause,</p>
<p>Consulte,</p>
<p>Et ensuite décide.</p>
<p>Ca ne te coutera rien, car tu décideras à la fin... Mais au moins tu auras compris avant de trancher.</p>
<p>Un sourire d'espoir pour tous les gens qui se précipitent, Décidons dans le calme et après réflexion.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
