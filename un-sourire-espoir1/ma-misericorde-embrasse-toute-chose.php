                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
                                        <br>
<p>Bienvenue avec une nouvelle parole et un nouveau sourire d'espoir.</p>
<p>Le titre de l'épisode d'aujourd'hui est très simple et très beau , et va vous rendre heureux.</p>
<p>Ne désespère jamais de la miséricorde de Dieu !</p>
<p>Quelque soient tes actes... Quelque soient tes erreurs... Même si tu penses que les gens te voient comme quelqu'un de mauvais... Ton Dieu est lui miséricordieux !</p>
<p>Ton Dieu est lui généreux !</p>
<p>Je vous le jure mes chers !</p>
<p>N'ai jamais le moindre doute, Ne pense pas à un seul instant que Dieu ne sera pas miséricordieux envers toi !</p>
<p>Mais reviens vers lui...</p>
<p>Il y'à des personnes qui lorsqu'elles font un péché, pensent qu'il n y'à plus d'espoir.</p>
<p>Disent que c'est impossible que Dieu leur pardonne après ce qu'elles ont fait...</p>
<p>Justement moi je veux que tu vives avec cette idée :</p>
<p>Peu importe ce que tu as fais dans le passé... Dieu va te pardonner !</p>
<p>Reviens juste vers lui, fais un premier pas.</p>
<p>Tu me diras « tu n'as aucune idée ce que j'ai pu faire... J'ai fais des catastrophes dans ma vie, depuis 10 ans je commet un péché très grave ...»</p>
<p>Moi je te répond « Je te jure par Dieu, qu'il va te pardonner ton péché qui dure 10ans en une journée ! »</p>
<p>Tu me diras « Et comment ? j'ai honte de moi-même de la multitude des péchés que j'ai commis dans ma vie... Je ne pourrai même pas te les citer, sinon tu risquerais de me détester »</p>
<p>Moi je pourrais peut-être te détester si tu me les dis, mais ton Dieu lui ne pourra jamais te détester crois moi !</p>
<p>Et pourquoi donc ?</p>
<p>Parce que Dieu t'aime,</p>
<p>Parce que tu es une créature chère pour lui,</p>
<p>Parce que tu es un être humain,</p>
<p>Parce que Dieu est miséricordieux,</p>
<p>Parce que la miséricorde de Dieu est étendue, et qu'il pardonne tous les péchés.</p>
<p>Je suis très touché par un compagnon qui s'appelait Abdallah ibn Rawaha, il était venu chercher conseil auprès du prophète sws, et ce dernier lui a dit :</p>
<p><strong class="prophete">« Ne désespère pas, si tu commets neuf mauvaises actions, fais les suivre par une bonne. La bonne action vaut dix fois plus et ainsi tu seras racheté. »</strong></p>
<p>Allah !</p>
<p>Le prophète sws voulait lui dire, si tu fais dans ta journée 9 mauvaises choses, fais au moins une bonne chose dans ta journée, et cette bonne chose effacera les 9 mauvaises...</p>
<p>Avec autant de facilité ?</p>
<p>Oui par Dieu avec autant de facilité !</p>
<p>Dieu est très miséricordieux... Dieu aime pardonner à ses créatures... Dieu n'aime pas compliquer les choses pour nous...</p>
<p>Si tu fais un seul pas envers Dieu, juste un pas ... Ca suffit à ce qu'il te pardonne tout ce que tu as fais par le passé...</p>
<p>Je tenais à vous dire ca, parce que certaines personnes ressentent un poids énorme sur leur dos...</p>
<p>Des personnes qui ont l'impression de trainer avec elles des montagnes de fautes,</p>
<p>Des fautes envers leurs parents,</p>
<p>Des fautes envers leurs maris ou épouses,</p>
<p>Des fautes, et des fautes, et des fautes... au point d'en être gêné et au point de se détester soi même, et au point de ne plus savoir quoi faire...</p>
<p>Donc pour ces personnes, j'ai une solution simple : Fais un pas vers Dieu, demande lui pardon, et repenti toi, dis lui que tu ne commettra plus jamais ce péché, que ca fait partie du passé désormais.</p>
<p>Je vais vous dire quelque chose qui va vous aider encore plus à comprendre :</p>
<p>Ton péché que tu as fais, il est limité ou pas limité ?</p>
<p>C'est-à-dire il est limité dans un temps , tu l'as fais pendant une heure précise, ou il dure pour l'éternité ?</p>
<p>Evidemment il est limité, tu l'as fais dans un temps et dans un endroit précis,</p>
<p>Et maintenant regarde la miséricorde de Dieu, elles est limitée ou pas limitée ?</p>
<p>Lis ce verset du Saint Coran</p>
<p><strong>{ Et Ma miséricorde embrasse toute chose }</strong> Sourate 7 : Al-A'râf -- Verset 156</p>
<p>Donc la miséricorde de Dieu est illimitée, elle englobe toute chose !</p>
<p>Et si tu poses une chose illimitée sur une chose limitée, laquelle des deux cache l'autre ?</p>
<p>Tu comprends le message que je veux te faire passer ?</p>
<p>Si tu doutes un instant que Dieu ne va pas te pardonner et te traiter avec miséricorde, c'est comme si tu disais « non sa miséricorde est limitée, et pire : plus limitée que ton péché »</p>
<p>Non mon cher, c'est toi qui es limité si tu penses comme ca !</p>
<p>Le pardon de Dieu est grand, et sa miséricorde est étendue, elle embrasse toute chose !</p>
<p>Je vous en prie mes chers, faites de cette journée un nouveau départ avec Dieu,</p>
<p>Adressez vous à Dieu en demandant pardon, que vous regrettez votre péché et que ca ne va plus se reproduire...</p>
<p>Si tu fais ca, je te jure par Dieu que sa miséricorde va t'embrasser, et que tout ce que tu as commis sera pardonné, et que tu commenceras une nouvelle vie, et une nouvelle journée avec un nouveau sourire d'espoir !</p>
<p><strong>{ Et Ma miséricorde embrasse toute chose }</strong> Sourate 7 : Al-A'râf -- Verset 156</p>
<br>
<p>Merci, et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
