                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Tu me crois si je te dis que l'une des plus belles choses que tu peux faire dans cette vie, c'est de donner du bonheur à un enfant,</p>
<p>Surtout si c'est une fille,</p>
<p>Quelle chance ont les parents d'une fille !</p>
<p>Maintenant j'ai compris pourquoi le Prophète sws disait</p>
<p><strong class="prophete">« Celui qui a trois filles et qui se montre miséricordieux envers elles tout en leur offrant hébergement, nourriture et vêtement, gagnera sans aucun doute le Paradis. »</strong></p>
<p>Un homme dit : « Ô Messager d'Allah, et celui qui en a deux ? »</p>
<p><strong class="prophete">« Et celui qui en a deux [aura aussi la même récompense] »</strong> répondit le Prophète sws.</p>
<br>
<p>Celui qui donne du bonheur à une fille, Dieu lui donne en retour un grand bonheur, et Dieu le satisfait,</p>
<p>A chaque papa et à chaque maman,</p>
<p>Peux tu faire un câlin à tes enfants,</p>
<p>Surtout à tes filles ?</p>
<p>Je vous jure cette parole est dédicacée à toutes les filles,</p>
<p>Une fille a besoin de tendresse,</p>
<p>Une fille a besoin d'affection,</p>
<p>Protégez vos filles, câlinez les et protégez les,</p>
<p>Si vous ne le faites pas elles iront chercher cette affection en dehors de la maison,</p>
<p>Et elles tomberont peut-être sur quelqu'un qui se jouera d'elles et profitera d'elles...</p>
<p>Je vais vous raconter une petite histoire :</p>
<p>Il y'avait un père ayant une fille de 5 ou 6 ans,</p>
<p>Une petite enfant donc,</p>
<p>Le père n'avait pas le temps,</p>
<p>Tout le temps au travail... Tout le temps occupé...</p>
<p>La mère lui disait « trouve du temps pour tes enfants, Occupe toi de ta fille... »</p>
<p>Et le père ne trouvait jamais le temps...</p>
<p>A la fin le père a cédé sous la pression de son épouse,</p>
<p>Il a donc pris sa fille dans un jour de repos, et l'a emmené dans un café,</p>
<p>Et il continuait à travailler sur son ordinateur portable...</p>
<p>Il a senti que sa fille s'ennuyait, alors il a voulu lui remonter le moral,</p>
<p>Il lui a dit «Tu sais Ghada, je t'aime beaucoup »</p>
<p>Et il a continué à travailler sur son ordinateur...</p>
<p>Ghada l'a pris par la main, et elle lui a dit « Encore Papa... Encore »</p>
<p>Il lui a demandé « Encore quoi ? »</p>
<p>Elle lui a dit « Dis moi encore de belles choses comme tu viens de faire... »</p>
<p>Il lui a dit « Tu es très chère à mes yeux, et tu es la chose la plus importante dans ma vie... »</p>
<p>Ghada a dit « Encore Papa... encore... »</p>
<p>Jusqu'à quel point nos enfants ont besoin de tendresse et d'affection...</p>
<p>N'osez jamais les priver de cet amour,</p>
<p>Une autre maman me racontait que son bébé a été hospitalisé en urgences,</p>
<p>Et les médecins lui ont interdit de toucher son enfant...</p>
<p>Ils se voyaient à travers la vitre,</p>
<p>Elle mourrait d'envie de toucher son bébé...</p>
<p>Vous savez ce qu'elle a fait ?</p>
<p>Elle a demandé au docteur de remettre un fil avec une pile au bout,</p>
<p>Et elle tenait l'autre pile,</p>
<p>Et ils sentaient tous les deux le courant passer,</p>
<p>Les médecins ont vu l'état de l'enfant se stabiliser,</p>
<p>Cette présence le rassurait,</p>
<p>Peux tu donner de l'affection à tes enfants...</p>
<p>Peux tu les embrasser, les câliner, leur dire des mots doux...</p>
<p>Peux tu dessiner un sourire d'espoir sur leurs visages...</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
