                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>La parole d'aujourd'hui concerne les événements qui se passent partout dans le monde...</p>
<p>Je n'arrive pas à comprendre qu'il y'à des gens qui font couler du sang avec une telle facilité...</p>
<p>Avec une telle violence...</p>
<p>Par dizaines et par centaines de morts...</p>
<p>Je tenais à vous raconter une histoire que j'ai vu dans ma vie :</p>
<p>Et je dédicace cette histoire à chacun de ceux qui ont osé faire couler le sang,</p>
<p>J'étais en visite aux états unis il y'à 2 ou 3 ans,</p>
<p>Et on m'avait dit qu'il existait un château transformé en musée,</p>
<p>Ce château appartenait à une dame,</p>
<p>Quel est donc l'histoire de cette dame ?</p>
<p>Quelle est donc l'histoire de ce château ?</p>
<p>Et pourquoi il s'est transformé en musée que les gens viennent visiter ?</p>
<p>Cette dame habitait dans ce château,</p>
<p>Puis après son décès c'est devenu un lieu de visite,</p>
<p>Vous allez donc vous demander quel est le rapport entre cette dame et son château, et ce que j'ai cité au début...</p>
<p>Pour comprendre, le mari de cette dame était un marchand d'armes aux états unis pendant la guerre civile (guerre de sécession entre 1861 et 1865)</p>
<p>Et donc cet homme vendait des armes aux 2 parties en conflit,</p>
<p>Et il leur a tellement vendu d'armes qu'il s'est extrêmement enrichi,</p>
<p>Puis il a acheté un grand terrain pour y construire un château de grande envergure,</p>
<p>Et son épouse était donc très heureuse de cette richesse,</p>
<p>Mais elle a fini par se réveiller et avoir des états d'âmes (bien sûr elle n'est pas croyante comme nous)</p>
<p>Mais elle a commencé à se dire que les personnes mortes à cause des armes de son mari,</p>
<p>Allaient devenir des fantômes et venir les hanter...</p>
<p>Puis elle a vécu dans une grande terreur,</p>
<p>Et elle a commencé à construire de plus en plus de chambres...</p>
<p>Pour pouvoir dormir chaque jour dans une nouvelle chambre en pensant (par erreur) que ces fantômes la menaçaient...</p>
<p>Imaginez à quel point elle souffrait de l'intérieur...</p>
<p>C'est ce qui arrive à chaque personne qui tue quelqu'un...</p>
<p>Et elle est restée tourmentée, en changeant chaque jour de chambre,</p>
<p>Vivant dans une grande peur,</p>
<p>Et j'ai visité personnellement ce château, et c'est un endroit qui met mal à l'aise,</p>
<p>Et cette dame dormait donc chaque nuit dans une chambre différente,</p>
<p>Au total il y'à 500 chambres dans ce château...</p>
<p>Imaginez 500 chambres...</p>
<p>Et à la fin, cette dame est décédée dans son lit,</p>
<p>Elle est décédée terrorisée,</p>
<p>Aucun fantôme n'est jamais apparu, mais la terreur était au fond d'elle-même,</p>
<p>Et sa vie ainsi que celle de son mari furent gâchées, car ils n'ont jamais connu la tranquillité,</p>
<p>A cause de ce qu'ils ont fait aux autres...</p>
<p>Mes chers, n'allumez jamais une mèche dans votre pays,</p>
<p>Ne participez jamais à un conflit ou à une turbulence ,</p>
<p>Sinon le sourire d'espoir disparaitra de vos vies et de la vie de vos enfants...</p>
<p>« Ô mon Dieu ! fais en sorte qu'on ne soit jamais au départ d'un feu ou d'un conflit »</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
