                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>J'ai reçu beaucoup d'appels de parents qui écoutent l'émission en compagnie de leurs enfants,</p>
<p>Et au fait je suis très content d'entendre des enfants me dire qu'ils ne partent pas à l'école sans avoir entendu « le sourire d'espoir » du jour :)</p>
<p>Parfois ce sont des garçons et des filles du primaire qui me le disent, et ca me fait énormément plaisir !</p>
<br>
<p>Les parents m'ont donc appelé pour me dire «tout le temps tu demandes au parents de bien se comporter avec leurs enfants... Y'en a que pour les parents... Il manque quelque chose :)</p>
<p>Il faut dire aux enfants aussi de bien se comporter avec leurs parents ! »</p>
<br>
<p>Je suis très content d'avoir d'abord donné aux enfants leurs droits,</p>
<p>Maintenant je leur dédie un épisode et un très joli verset du Coran, on connait tous ce verset par cœur, mais personne ne pense à sa signification :</p>
<p><strong>{Et par miséricorde, abaisse pour eux l'aile de l'humilité, et dis: «Ô mon Seigneur, fais-leur, à tous deux, miséricorde comme ils m'ont élevé tout petit». }</strong></p>
<p>Sourate 17 - Al-Isra' : Le Voyage Nocturne (verset 24)</p>
<br>
<p>Mais ca veut dire quoi l'aile de l'humilité...</p>
<p>L'humilité a des ailes ?</p>
<p>L'humilité peut voler ?</p>
<p>Puis l'humilité est quelque chose qui peut s'abaisser ?</p>
<p>On ne comprend pas tellement le sens...</p>
<br>
<p>L'histoire de cette aile, le Coran l'a prise de la nature...</p>
<p>C'est quelque chose qui arrive aux oiseaux,</p>
<p>Quand 2 oiseaux se battent,</p>
<p>Et que l'un des deux veut se rendre, savez vous ce qu'il fait ?</p>
<p>Vous l'avez surement vu dans un film ou dans un documentaire,</p>
<p>L'oiseau qui abandonne le combat étend son aile, et la met par terre...</p>
<p>Et quand l'aile s'abaisse, la tête de l'oiseau en fait de même,</p>
<p>C'est une réaction automatique.</p>
<p>Tu peux essayer de faire la même chose en étendant tes bras, et en les abaissant pour toucher le sol...</p>
<p>Ta tête s'abaissera automatiquement !</p>
<br>
<p>Dieu demande donc aux jeunes d'apprendre des oiseaux,</p>
<p>Car ils aiment les animaux,</p>
<p>Et ils aiment regarder les documentaires sur discovery channel... etc</p>
<br>
<p>C'est la beauté du Coran,</p>
<p>Il donne aux plus petits un exemple de la nature,</p>
<p>Pour qu'ils le voient autour d'eux,</p>
<p>Et même s'ils ne le voient pas, ils pourront l'imaginer...</p>
<br>
<p>L'oiseau qui perd donc, abaisse son aile, et son bec et sa tête en font de même,</p>
<p>Peux-tu faire pareil avec tes parents ?</p>
<p>A tous les jeunes qui m'écoutent, garçons ou filles...</p>
<p>Savez-vous pourquoi Dieu a cité « l'aile de l'humilité » et pas l'aile tout court ?</p>
<p>Parce qu'avec nos parents on manque d'humilité... on s'entête...</p>
<p>On sait que nos parents ont raison, et veulent notre bien...</p>
<p>Mais le diable ! je le dis en serrant les dents....</p>
<p>Le diable te fait dire non... Juste pour tenir tête...</p>
<p>Ne conduis pas vite... Si je conduirai vite c'est ma voiture je fais ce que je veux !</p>
<p>Appelle nous dès que tu arrives pour nous rassurer... Non je ne suis plus un gamin !</p>
<p>Parfois c'est vrai que nos parents font des demandes difficiles...</p>
<p>Mais mon cher abaisse pour eux l'aile de l'humilité...</p>
<p>et dis: «Ô mon Seigneur, fais-leur, à tous deux, miséricorde comme ils m'ont élevé tout petit»</p>
<br>
<p>Je vais te dire une chose et ne le prends pas mal,</p>
<p>Ta maman avait plein de rêves dans la vie...</p>
<p>Et quand tu es né elle a abandonné tous ses rêves pour pouvoir t'élever...</p>
<br>
<p>Sois donc miséricordieux avec elle, et garde l'aile de l'humilité abaissée pour elle,</p>
<p>Pour que Dieu soit satisfait de toi,</p>
<p>Et pour gouter le sourire d'espoir,</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
