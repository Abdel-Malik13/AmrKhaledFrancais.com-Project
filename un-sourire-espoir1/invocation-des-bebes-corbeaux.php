                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>Tout le monde en ce moment est inquiet sur ses sources de revenus,</p>
<p>Au milieu de ces crises économiques, chômage, récession... etc</p>
<p>Au milieu de tout ca je reste déterminé à dessiner sur vos visages un sourire d'espoir :)</p>
<p>Tu te demanderas comment je compte faire au milieu de tous ces problèmes ?</p>
<p>C'est simple mon cher,</p>
<p>La subsistance est garantie,</p>
<p>Je te jure que Dieu te donnera toujours de quoi vivre,</p>
<p>Tu dois juste faire un petit effort,</p>
<p>Un petit effort puis c'est tout !</p>
<p>Et après cet épisode ne prends plus jamais d'antidépresseurs,</p>
<p>Et après cet épisode ne sois plus jamais inquiet pour demain,</p>
<p>Tu dois juste bouger un petit peu... La subsistance quant à elle est garantie... Dieu est toujours là !</p>
<p><strong>« Ar-Razzaq »</strong> est un des 99 noms de Dieu, signifie : Celui qui pourvoit et accorde toujours la subsistance.</p>
<br>
<p>Je vais vous raconter une très belle histoire :</p>
<p>Il y'avait un homme syrien plein de bonté,</p>
<p>A chaque fois que je le voyais il disait cette invocation « Ô Seigneur, accorde-nous notre subsistance comme tu le fais avec les bébés corbeaux »</p>
<p>Mais quel est le rapport entre les bébés corbeaux et la subsistance ?</p>
<p>C'est une leçon que nous donne Dieu le très haut...</p>
<p>On sait tous que les corbeaux sont noirs,</p>
<p>Ce qui se passe c'est que quand les bébés corbeaux sortent de leurs œufs,</p>
<p>Ils sont blancs...</p>
<p>Car ils n'ont pas encore de plumes,</p>
<p>Les plumes des corbeaux sont noires, mais leur peau est blanche...</p>
<p>(J'ai découvert ca dans un documentaire animalier)</p>
<p>Quand les parents corbeaux voient sortir des bébés blancs... Ils ne les reconnaissent pas...</p>
<p>Même si c'est elle qui a pondu l'œuf,</p>
<p>La maman ignore son bébé parce qu'il n'est pas de la même couleur qu'elle...</p>
<p>Les parents abandonnent leurs bébés, ils ne leurs donnent pas à manger.</p>
<p>Mais <strong>« Ar-Razzaq »</strong> lui ne les abandonne pas !</p>
<p>Des petits insectes sont attirés par cette couleur blanche,</p>
<p>Ils viennent vers ce bébé que personne ne veut nourrir...</p>
<p>Et le bébé corbeau se nourrit de ces petits insectes que Dieu lui a envoyés,</p>
<p>Il se nourrit exclusivement de ces petits insectes pendant une semaine ou deux,</p>
<p>La subsistance spéciale que Dieu lui a accordée...</p>
<p>Jusqu'à ce que ses plumes poussent enfin,</p>
<p>A ce moment là il devient noir,</p>
<p>La maman corbeau reconnait enfin son bébé,</p>
<p>Elle vient le nourrir et s'occuper de lui...</p>
<p>Mais les 2 premières semaines qui le nourrissait ?</p>
<p><strong>« Ar-Razzaq »</strong> Dieu le très haut, Celui qui pourvoit et accorde toujours la subsistance.</p>
<p>D'où l'invocation de cet homme syrien :</p>
<p>« Ô Seigneur, accorde-nous notre subsistance comme tu le fais avec les bébés corbeaux »</p>
<br>
<p>Il y'a un très joli verset dans le noble Coran qui dit</p>
<p><strong>{ Il n'y a point de bête sur terre dont la subsistance n'incombe à Allah qui connaît son gîte et son dépôt; tout est dans un Livre explicite.}</strong></p>
<p>Sourate 11 -- Hûd (verset 6)</p>
<br>
<p>Et toi aussi ta subsistance incombe à Dieu,</p>
<p>Donc n'ai jamais peur pour tes sources de revenus,</p>
<p>A tous ceux qui ont peur de perdre leur travail,</p>
<p>A toutes les mamans qui ont peur pour l'avenir de leurs enfants,</p>
<p>Soyez rassurés, la subsistance est garantie par Dieu,</p>
<p>Tout ce qui compte c'est se lever le matin et faire les efforts pour l'obtenir.</p>
<p>Celui qui nourrit les bébés corbeaux abandonnés par tout le monde... Et bien capable de te nourrir non ?</p>
<p>Alors c'est un sourire d'espoir ou pas :)</p>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
