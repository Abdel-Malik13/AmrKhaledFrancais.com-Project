                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide,
entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne
qui a réussi...</p> <p>Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage
L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
<p>Il y'à un problème dans notre pays qu'on n'arrive pas à gérer...</p>
<p>Et on se comporte avec une extrême dureté face à ce problème...</p>
<p>Il y'à des personnes qui ont des besoins particuliers,</p>
<p>Ces personnes existent en grand nombre en Egypte,</p>
<p>Mais personne ne s'occupe d'eux,</p>
<p>Et personne ne leur vient en aide...</p>
<p>Le sourire d'espoir d'aujourd'hui leur est dédicacé,</p>
<p>Aux millions de gens dans cette situation...</p>
<p>L'histoire que je vais vous raconter, est l'histoire d'une de ces personnes :</p>
<p>C'est un petit enfant qui s'appelle Adam,</p>
<p>Cet enfant avait un retard de développement mental...</p>
<p>Adam a mis trop de temps pour apprendre à parler,</p>
<p>Et sa maman a commencé à tester un traitement pour soigner ses troubles de la parole,</p>
<p>Mais son père lui était très en colère... très démoralisé par cette situation...</p>
<p>Il se disait : « Pourquoi mon Dieu tu m'a donné un enfant dans cet état... Pourquoi je mérite ca ?? »</p>
<p>Et le père trouvait cette situation tellement infernale qu'il a failli demander le divorce...</p>
<p>L'enfant avait 4ans, et ne montrait pas de signes de progression...</p>
<p>Et le père commençait à fuir son fils...</p>
<p>Pourquoi donc agir ainsi ? Pourquoi ne pas simplement accepter la volonté de Dieu ?</p>
<p>Et un jour pendant une dispute, la mère a beaucoup insisté « Prend ton fils au moins une fois, et sors avec lui »</p>
<p>Le père s'est trouvé forcé à sortir promener son fils,</p>
<p>Et il l'a emmené dans une pâtisserie pour lui acheter du chocolat,</p>
<p>Et dès qu'ils sont entrés dans le magasin, il y'avait un décor...</p>
<p>Et dans ce décor se trouvait un vase rempli de fleurs...</p>
<p>Mais bien sûr c'est une pâtisserie, ils ne vendent pas de fleurs...</p>
<p>L'enfant s'est arrêté devant le vase... Il voulait prendre une fleur...</p>
<p>Le père lui disait « Mon cher, ce ne sont pas des gâteaux, si tu veux je te prend du chocolat, mais ca ce sont des fleurs, ca ne se mange pas »</p>
<p>Et l'enfant ne s'intéressait pas aux gâteaux, il fixait les fleurs... Je veux ca et rien d'autre...</p>
<p>Et l'enfant tenait la fleur dans la main, et ne voulait plus la lâcher...</p>
<p>Et le père continuait de lui dire « C'est une fleur, ca ne se mange pas... »</p>
<p>Et le père était gêné par ce qu'il pensait être une stupidité ou un manque d'intelligence...</p>
<p>Le père se disait, cet enfant ne répond pas au traitement... Il ne comprend rien de rien...</p>
<p>Et à la fin le père a fini par se décourager... en disant « si c'est ca que tu veux, alors prends la , mange la, fais ce que tu veux avec !»</p>
<p>Le père est rentré chez lui le soir en étant complètement démoralisé...</p>
<p>« J'ai fais quoi mon Dieu pour mériter cet enfant difficile ? »</p>
<p>Et pendant qu'il était dans son lit, il a entendu les pas d'Adam arriver dans la chambre...</p>
<p>Et quand Adam est entré dans la chambre de ses parents, il tenait la fleur dans sa main...</p>
<p>Et là un miracle...</p>
<p>Adam n'est pas stupide, Adam venait offrir la fleur à son père...</p>
<p>Adam ne voulait pas prendre la fleur pour la manger...</p>
<p>Adam voulait prendre la fleur pour l'offrir à son père...</p>
<p>Alors que le père pensait son enfant idiot, et que ca lui gâchait la vie...</p>
<p>Le père raconte : « à cet instant, toute ma vie a basculé... Je me suis réveillé... Dieu m'a donné un grand bienfait appelé Adam... Je suis chanceux, je ne suis pas malheureux... J'ai un ange dans ma maison...</p> Pourquoi je le voyais comme un malheur... »
<p>Pourquoi quand des personnes sont différentes de nous, on les voit comme une charge ou une source de difficulté...</p>
<p>Dans notre pays on traite malheureusement des millions de ces personnes, comme si elles étaient un poids pour la société...</p>
<p>Mais je vous jure que Dieu fait d'eux un bien pour leurs familles et pour la société.</p>
<p>Cette histoire d'Adam, m'a beaucoup bouleversé personnellement,</p>
<p>J'ai vu en Adam toutes les personnes qu'on pense inutiles, alors qu'au fond elles sont remplies de sentiments et de tendresse...</p>
<p>Le père raconte : « Depuis ce jour là j'adorais Adam, et le bonheur est revenu à la maison, et le sourire d'espoir est revenu chez nous. »</p>
<br>
<p>Je vous remercie,</p>
<p>et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
