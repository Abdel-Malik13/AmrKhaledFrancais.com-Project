                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :<p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,<p>
<p>Le titre de l'épisode d'aujourd'hui est « Ne sous estime jamais personne »<p>
<p>Ne dis jamais de quelqu'un « Il ne comprend rien de rien... »<p>
<p>Ne dis jamais de quelqu'un « Il ne fait que des erreurs et des péchés, c'est une mauvaise personne... Il est loin de Dieu »<p>
<p>Ne dis jamais de quelqu'un « Il est faible ou débile... »<p>
<p>Sois modeste avec les gens,<p>
<p>Toute personne devant toi qu'elle soit faible, pauvre ou analphabète,<p>
<p>Toute personne a en elle des qualités et des points forts,<p>
<p>Ne sous estime jamais l'intelligence des gens,<p>
<p>Ne pense jamais que tu es le seul à comprendre...<p>
<p>Je vais vous raconter une histoire étonnante :<p>
<p>Elle est arrivée à l'imam Ahmad ibn Hanbal (un très grand théologien, fondateur de l'une des quatre grandes écoles juridiques sunnites, connue sous le nom de "hanbalite")<p>
<p>Vous me croyez si je vous dis qu'un jour celui qui a aidé l'imam Ahmad ibn Hanbal à rester sur le droit chemin est un voleur ?<p>
<p>Le gouverneur voulait que l'imam Ahmad lui donne une fatwa (un avis juridique donné par un spécialiste de loi islamique sur une question particulière)<p>
<p>Et l'imam Ahmad ne voulait pas lui donner son accord car c'est contraire aux lois de Dieu.<p>
<p>Ca a déplu au gouverneur qui a décidé de l'emprisonner,<p>
<p>Puis de lui donner des coups de fouet en prison...<p>
<p>L'imam Ahmad disait qu'il ne craignait pas la prison,<p>
<p>Mais il craignait les coups de fouet, il avait peur de craquer...<p>
<p>Au moment où ils sont venus le chercher pour le fouetter, il commençait à faiblir,<p>
<p>Et qui il croise sur le chemin ?<p>
<p>Un voleur nommé Abou Haytham Al-Tayyar,<p>
<p>Al-Tayyar c'est son surnom qui signifie : le voleur de linge et de portefeuilles.<p>
<p>Chaque fois qu'il se faisait arrêter pour avoir volé du linge, on le mettait en prison...<p>
<p>Le lendemain on le relâchait il volait des portefeuilles...<p>
<p>Et ça ne s'arrêtait jamais.<p>
<p>Pendant qu'on emmenait l'imam Ahmad se faire fouetter, on sortait Abou Haytham qui lui venait juste d'être fouetté...<p>
<p>Abou Haytham était un habitué, il a vu dans le regard de l'imam Ahmed de l'anxiété et de la peur...<p>
<p>Il l'a regardé en disant « Ô imam ! Reste droit... Si tu vis tu auras gardé ton honneur... Et si tu meurs tu seras mort en martyr... J'ai pris au cours de ma vie 10 milles coups de fouets, et je les ai supportés pour le diable... Alors supporte-les pour le tout miséricordieux »<p>
<p>Ces paroles ont fortifié l'imam Ahmad,<p>
<p>Il a été fouetté et il a tenu,<p>
<p>Il a été menacé et il est resté droit,<p>
<p>Grâce aux paroles d'Abou Haytham le voleur...<p>
<p>Ne sous estime jamais personne,<p>
<p>L'imam Ahmed n'a jamais oublié Abou Haytham dans ses prières, il demandait à Dieu de le guider et lui pardonner ses péchés...<p>
<p>Son fils lui demandait qui est cet Abou Haytham à qui tu penses souvent ?<p>
<p>Il lui répondait c'est un homme qui m'a sauvé,<p>
<p>Ne sous estimez jamais un pauvre,<p>
<p>Ne sous estimez jamais une personne qui ne sait ni lire ni écrire,<p>
<p>Dieu a donné à chacun de nous sa part d'intelligence,<p>
<p>Ne sous estimez donc personne,<p>
<p>Et apprenez à vos enfants à faire pareil.<p>
<p>A ce moment un sourire d'espoir se dessinera sur toute la société.<p>
<br>
<p>Je vous remercie, et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.<p>
