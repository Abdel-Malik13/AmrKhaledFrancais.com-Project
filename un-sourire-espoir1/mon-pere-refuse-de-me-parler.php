                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle d'une fille, c'était lors d'un rassemblement pour former les jeunes de l'association « les bâtisseurs de la vie »</p>
<p>Cette fille devait avoir 17 ans ou 18 ans,</p>
<p>Elle est passée à côté de moi en pleurs...</p>
<p>Je lui ai demandé ce qui lui arrivait,</p>
<p>Elle m'a répondu « Mon père et ma mère ont divorcé depuis longtemps...</p>
<p>Depuis leur divorce mon père me fuit,</p>
<p>Tu me crois si je te dis que mon père ne répond pas à mes messages ?</p>
<p>Tu me crois si je te dis que j'ai trouvé par hasard mon père inscrit sur facebook, je lui ai envoyé une invitation 'Papa je suis ta fille'.... Il m'a bloquée pour que je ne puisse plus accéder à sa page...</p>
<p>Le jour où il l'a fait, j'ai pleuré de toutes mes larmes...</p>
<p>Je rêvais d'avoir un père qui me ressente et que je ressente,</p>
<p>Pourquoi mon père me punit alors qu'il s'est disputé avec ma mère ?</p>
<p>Je suis sensée être un bout de lui-même... »</p>
<p>En vous racontant l'histoire de cette fille, mon cœur se déchire... Alors comment doit être le sien...</p>
<p>Elle me disait des choses difficiles à entendre,</p>
<p>Comment elle lui a envoyé un message le jour de son anniversaire « Joyeux anniversaire papa, et bonne santé»</p>
<p>Et il ne lui a pas répondu...</p>
<p>Y'a-t-il des gens au cœur si endurci que ca ?</p>
<p>Cette fille m'a parlé pendant 5 minutes, elle m'a raconté des situations,</p>
<p>Comme le jour où elle lui a dit « J'ai envie de te voir, j'ai des choses à te raconter... Un jeune homme veut demander ma main, et j'ai envie de te consulter, avoir ton avis car tu es mon papa »</p>
<p>Elle était choquée par sa réponse « Je suis en voyage, je n'ai pas le temps, va voir ton oncle il va t'aider »</p>
<p>Aie... Je jure Aie mon cœur !</p>
<p>Pourquoi ?</p>
<p>A tous les pères et mères, vos enfants ont besoin de vous,</p>
<p>Je le jure... Surtout vos filles,</p>
<p>Te rends-tu compte que ta fille a énormément besoin de toi ?</p>
<p>Tu peux travailler toute la journée...</p>
<p>Et tu peux avoir décidé après le divorce de refaire ta vie...</p>
<p>Mais elle reste de ta chair et ton sang.</p>
<p>A tous les parents occupés... Vos filles ont beaucoup besoin de vous...</p>
<p>J'ai dit à cette fille « Acceptes-tu de me considérer comme ton père ? »</p>
<p>Elle a pleuré encore plus... Elle m'a répondu « Ca m'honore, mais j'ai besoin de mon véritable papa »</p>
<p>Je m'adresse à chaque papa et à chaque maman qui m'écoutent :</p>
<p>Aidez vos enfants à communiquer avec vous,</p>
<p>Et même si tu es divorcé(e), ne prive surtout pas tes enfants du droit de parler à leur mère / père...</p>
<p>Ne punissez jamais 2 fois vos enfants :</p>
<p>La 1ère fois au moment du divorce,</p>
<p>Et la 2ème fois en les privant de voir leur père / mère.</p>
<p>Vos enfants ont besoin de leurs 2 parents... Ils en ont extrêmement besoin !</p>
<p>Ne dis pas à tes enfants après ton divorce « Vous allez voir votre papa une fois tous les 15 jours »</p>
<p>Tu n'as pas le droit... Tu n'as pas le droit...</p>
<p>Ne dis pas à tes enfants « Vous allez voir votre maman une fois tous les 15 jours »</p>
<p>Tu n'as pas le droit... Tu n'as pas le droit...</p>
<p>Comment peux-tu faire ca ? Comment ?</p>
<p>Si vous divorcez, faites-le d'une belle façon...</p>
<p>Comme Dieu le dit dans le noble Coran</p>
<p><strong>{ Et quand vous divorcez d'avec vos épouses, et que leur délai expire, alors, reprenez-les conformément à la bienséance ou libérez-les conformément à la bienséance... }</strong></p>
<p>Sourate 02 - Al-Baqara : La Vache (verset 231)</p>
<p>Bienséance = politesse, courtoisie, conduite sociale conforme aux usages...</p>
<p>Même si tu n'arrives pas à le faire pour ton ancien mari/ femme... Fais-le pour les enfants,</p>
<p>Pourquoi nos enfants devraient payer 2 fois pour nos fautes ?</p>
<p>Pourquoi ils devraient assumer les conséquences de nos erreurs...</p>
<p>Je vous en prie soyez miséricordieux envers vos enfants,</p>
<p>Je vous en prie donnez-leur de l'affection et de la tendresse,</p>
<p>Ta fille a besoin d'amour, et si elle ne le trouve pas à l'intérieur de la maison... Elle ira le chercher dehors...</p>
<p>Et ensuite tu diras que cette fille a fait des mauvaises choses...</p>
<p>Câlinez vos enfants, prenez soin d'eux, surtout vos filles.</p>
<p>Préservez le sourire d'espoir dans vos maisons.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
