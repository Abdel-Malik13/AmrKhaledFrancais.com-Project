                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>L'épisode d'aujourd'hui parle du patron d'une entreprise,</p>
<p>Un matin avant que les employés arrivent, il a collé une affiche avec écrit dessus « Celui qui bloquait votre carrière et empêchait votre promotion dans l'entreprise est décédé. Les obsèques auront lieu demain »</p>
<p>Tu as déjà entendu une chose pareille ? :)</p>
<p>Un panneau écrit en grandes lettres à l'entrée de la société,</p>
<p>Le patron n'est pas fou, il sait ce qu'il fait,</p>
<p>Un geste plein de sagesse et d'intelligence...</p>
<p>La personne qui vous empêchait d'obtenir votre promotion, et bloquait votre carrière professionnelle est décédée hier... Les obsèques auront lieu demain...</p>
<br>
<p>Les employés étaient touchés, ils présentaient leurs condoléances,</p>
<p>Mais en même temps ils se demandaient qui c'était ?</p>
<p>Celui qui refusait que tous les employés obtiennent une promotion et une augmentation de salaire...</p>
<p>Il bloquait tout le monde, il travaillait dans le secret... C'est sûrement une mauvaise personne, tout le monde voulait connaitre son identité !</p>
<p>Le lendemain tous les salariés sont arrivés habillés en noir pour présenter leurs condoléances,</p>
<p>Un par un ils entraient voir le directeur dans son bureau,</p>
<p>Et après avoir présenté leurs condoléances, ils demandaient qui était cette personne...</p>
<p>Le directeur leur demandait d'entrer dans une autre pièce où se trouvait le cercueil,</p>
<p>Chaque employé va entrer seul dans cette pièce, ca va le faire méditer et réfléchir...</p>
<p>Les gens ne comprenaient pas l'intérêt de ce que leur demandait le directeur...</p>
<p>Ils ont formé une file d'attente devant la pièce,</p>
<p>Chaque employé entrait, jetait un coup d'œil dans le cercueil...</p>
<p>Puis ressortait le visage marqué sans dire un mot.</p>
<br>
<p>Quelqu'un de vous a deviné ce qui se trouvait dans le cercueil ?</p>
<p>Qu'on lui donne un cadeau de la part de l'émission :)</p>
<br>
<p>Dans le cercueil se trouvait simplement un miroir...</p>
<p>Quand un employé ouvrait le cercueil, il voyait son visage dans le miroir,</p>
<p>Et à côté du miroir le directeur a mis un message « La seule personne au monde qui peut empêcher ta progression, ta promotion et ta réussite c'est toi-même ! »</p>
<p>Le patron les a secoués,</p>
<p>Ce qui empêche ta réussite c'est tes erreurs,</p>
<p>C'est que tu crois que tes capacités sont limitées,</p>
<p>C'est que tu n'as pas d'ambition,</p>
<p>C'est que tu n'as pas confiance en toi-même,</p>
<p>C'est que tu ne veux pas faire d'efforts,</p>
<p>Le miroir... Ce qui t'empêche de réussir c'est toi et personne d'autre...</p>
<br>
<p>A tous les étudiants qui passent des examens,</p>
<p>A tous les jeunes diplômés,</p>
<p>A tous les salariés,</p>
<p>A tous les parents,</p>
<br>
<p>Ta vie change le jour où tu décides de changer,</p>
<p>Vous comprenez le sens de ce verset du noble Coran</p>
<p><strong>{ En vérité, Allah ne modifie point l'état d'un peuple, tant que les [individus qui le composent] ne modifient pas ce qui est en eux-mêmes. }</strong></p>
<p>Sourate 13 - Ar-Ra'd : Le Tonnerre (verset 11)</p>
<br>
<p>Regarde toi dans le miroir, décide de changer, et tu verras le monde qui t'entoure changer positivement :)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
