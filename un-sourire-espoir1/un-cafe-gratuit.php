                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>Aujourd'hui c'est une histoire à la fois vraie et très belle,</p>
<p>Je l'ai vue de mes propres yeux en Angleterre,</p>
<p>Pas à Londres ni à Birmingham ni dans une grande ville,</p>
<p>Mais dans un petit village que j'ai visité,</p>
<p>Dans ce village il y'avait des gens aisés financièrement et d'autres aux revenus très modestes...</p>
<p>Dans ce petit village j'ai vu une expérience que je n'ai vue nulle part ailleurs dans le monde,</p>
<p>Je vais vous la raconter, et si Dieu le veut elle vous plaira :</p>
<p>J'étais assis dans un café quand un homme est entré,</p>
<p>Il a dit au serveur « Je prendrai deux cafés s'il vous plait »</p>
<p>(Il était tout seul donc je ne comprenais pas, et vous non plus vous ne comprendrez pas avant la fin de l'histoire)</p>
<p>« Je prendrai deux cafés, un des deux est cadeau »</p>
<p>Je ne comprenais toujours pas ce que voulait dire un café cadeau...</p>
<p>Le serveur lui a ramené une tasse de café, le client a payé le prix de deux tasses de café,</p>
<p>Puis le serveur a noté sur un tableau « 1 café cadeau »</p>
<p>Quelqu'un a compris pourquoi ?</p>
<p>Je mourrais d'envie de comprendre...</p>
<p>Un peu plus tard, deux personnes sont entrées, elles ont commandé 3 cafés dont un est cadeau...</p>
<p>Donc c'est une chose connue dans le village, tout le monde comprenait sauf moi.</p>
<p>Les deux personnes ont payé le prix de trois cafés,</p>
<p>Et le serveur a rajouté sur le tableau « 1 café cadeau »</p>
<p>Un autre homme est entré, il a commandé 3 cafés donc 2 cadeaux...</p>
<p>Comme il était tout seul, il a bu une seule tasse de café, et le serveur a rajouté sur le tableau 2 autres cafés cadeaux,</p>
<p>Tous les clients comprenaient, le serveur comprenait, mais moi je ne comprenais toujours pas...</p>
<p>Au bout d'un moment, un homme qui avait l'air pauvre est entré, un ouvrier modeste,</p>
<p>Il a regardé le tableau, puis il a commandé au serveur un café,</p>
<p>Le serveur lui a ramené une tasse de café, cet homme l'a bue puis il est parti sans payer...</p>
<p>Vous avez sûrement compris l'idée,</p>
<p>Les habitants de ce village se sont mis d'accord pour aider les pauvres mais sans les gêner,</p>
<p>Sans leur donner de l'argent dans la main,</p>
<p>Sans les mettre mal à l'aise...</p>
<p>Dans ce village les gens modestes entrent, lisent le tableau et voient directement combien de cafés gratuits sont disponibles,</p>
<p>Elle est belle cette initiative n'est ce pas ?</p>
<p>Je suis sur qu'elle vous a donné un sourire d'espoir :)</p>
<p>Mes chers apprenons à aider les nécessiteux sans les mettre mal à l'aise,</p>
<p>Cette histoire est très humaine,</p>
<p>Dans ce village on aide les pauvres sans qu'ils viennent nous le demander,</p>
<p>Peux-tu aider discrètement une personne sans qu'elle sache qui l'a fait ?</p>
<p>Peux-tu aller dans un hôpital et régler les frais d'une personne qui n'a pas beaucoup de moyens ?</p>
<p>Peux-tu faire un don du sang pour les gens qui en ont besoin ?</p>
<p>Peux-tu sans que personne ne te le demande, aider les gens dans des situations difficiles ?</p>
<p>Peux-tu réfléchir à des méthodes intelligentes pour aider, sans sortir l'argent de la poche et le remettre directement à la personne ?</p>
<p>A ce moment un sourire d'espoir se dessinera dans la société.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
