                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue à tous avec un nouveau sourire d'espoir.</p>
<p>Aujourd'hui J'ai une histoire à vous raconter,</p>
<p>Une histoire que j'ai lu et quand je l'ai lu, j'ai été surpris de voir que des gens proches de moi font la même chose...</p>
<p>L'histoire est la suivante :</p>
<p>On raconte qu'un un père de famille a été hospitalisé pour une crise cardiaque...Il était presque dans le coma,</p>
<p>C'est en marchant dans la rue qu'il a eu cette crise, et donc une fois qu'on l'a ramené à l'hôpital, les responsables ont cherché dans ses affaires et ont trouvé un numéro de téléphone,</p>
<p>Quand ils ont appelé, un jeune homme a déroché et donc ils lui ont dit « ton père est à l'hôpital, il faut venir vite sur place »</p>
<p>L e jeune homme est bien sur venu en courant à l'hôpital,</p>
<p>Il est arrivé dans la chambre, il a regardé le père de famille allongé, et en l'observant bien il était presque dans le coma,</p>
<p>le père a pourtant tendu sa main et l'a mise dans la main du jeune homme.</p>
<p>Alors le jeune homme est resté à coté de son père, main dans la main durant 2 heures.... Puis 3 heures... Puis 4 heures... Puis toute la nuit... Assis à coté de lui avec beaucoup de tendresse... Tenant le père par la main, et s'assurant qu'il va bien toute la nuit... Et l'embrassant, et faisant des prières pour lui ...</p>
<p>Un fils plein de tendresse et d'affection!</p>
<p>Jusqu'au moment ou le père s'est endormi, et son état commençait à se stabiliser.</p>
<p>Et les médecins sont venus remercier le jeune homme, car le fait de le tenir par la main a eu un grand impact sur l'état du père, ca le calmait psychologiquement et ca le rassurait.</p>
<p>Et la nuit est passée, et le jeune homme était fatigué car il n'avait pas dormi, mais Dieu merci, les médecins l'ont rassuré sur l'état de santé de son père.</p>
<br>
<p>Et là la surprise, le jeune homme est parti voir les responsables, et leur a dit « s'il vous plait vérifier encore le numéro que vous vouliez appeler , parce que cet homme n'est pas mon père»</p>
<p>Ils lui ont demandé étonnés « dans ce cas pourquoi es tu resté toute la nuit ? »</p>
<p>Le jeune homme leur a répondu « vous vous êtes trompés de numéro en m'appelant, je suis venu car je pensais qu'il s'agissait de mon père, mais quand je suis arrivé et que cet homme a attrapé ma main, j'ai pensé que par devoir.. par humanité... par miséricorde...</p>
<p>Je devais rester et lui tenir compagnie, s'il était dans le coma, et qu'il pensait que j'étais son fils, alors pour rien au monde je ne devais le priver de ce sentiment de parenté. »</p>
<p>Allah ! une très belle histoire...</p>
<p>Dedans on trouve deux jolis sens :</p>
<p>Le premier c'est la miséricorde, soyez miséricordieux envers les gens ,même si vous ne les connaissez pas...</p>
<p>Vous imaginez il est resté une nuit entière à l'hôpital... Imaginez comment Dieu va se comporter en retour avec ce jeune homme ?</p>
<p>Dieu va le récompenser à quel point ?</p>
<p>Dieu va être miséricordieux envers lui jusqu'à quel point quand il sera dans le besoin ?</p>
<p>Par Dieu, Dieu va le remercier pour ce qu'il a fait,</p>
<p>Par Dieu c'est impossible qu'il en soit autrement !</p>
<p>Mais il y'à aussi un deuxième sens dans cette histoire :</p>
<p>A tous les enfants, soyez miséricordieux avec vos parents...</p>
<p>Imagine ton père de qui tu t'éloignes, par moments il a juste envie de tenir par la main...</p>
<p>Je vous en prie, à tous les jeunes hommes, et à toutes les jeunes femmes, ce soir embrasse la main de ton père.</p>
<p>Ne pensez jamais que parce que nos parents nous disputent, et parfois on est en désaccord avec eux... Que les sentiments sont partis...</p>
<p>A toutes les filles, sais tu que quand ta maman était jeune elle avait 1000 rêves, et quand tu es venue au monde, elle a abandonné tous ces rêves, pour un seul rêve, toi !</p>
<p>S'il vous plait soyez miséricordieux avec vos parents, parfois ils ont besoin d'un câlin, d'un mot gentil...</p>
<p>Parfois ils ont juste besoin d'un sourire... Un sourire d'espoir.</p>
<br>
<p>Prenez soin de vous, et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
