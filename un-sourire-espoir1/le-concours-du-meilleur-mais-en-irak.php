                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouvel épisode,</p>
<p>Le sourire d'espoir d'aujourd'hui nous vient d'un agriculteur,</p>
<p>Il cultivait du maïs,</p>
<p>(C'est une histoire vraie qui nous vient d'Irak)</p>
<p>Chaque année dans sa région on organisait un concours pour élire le meilleur maïs.</p>
<p>Et le gouvernement remettait des prix et des attestations aux vainqueurs.</p>
<p>La surprise c'est que chaque année ce modeste agriculteur remportait le premier prix...</p>
<p>Ca a attisé la curiosité d'un journaliste, il s'est dit c'est sur cet agriculteur a un secret, il faut que je le découvre...</p>
<p>Il a donc fait une interview de l'agriculteur, pour connaitre les raisons de sa réussite.</p>
<p>Quand il lui a demandé « Comment faites-vous pour produire le meilleur maïs de la région d'année en année ? »</p>
<p>L'agriculteur lui a répondu en toute simplicité « Je distribue mes meilleurs graines à mes voisins qui produisent du maïs »</p>
<p>Le journaliste n'a pas compris il a demandé des explications,</p>
<p>L'agriculteur lui a répondu « Je sélectionne mes meilleures graines, puis je vais les offrir à mes voisins agriculteurs, pour qu'ils les plantent chez eux... C'est un cadeau gratuit que je leur offre»</p>
<p>Le journaliste ne comprenait toujours pas « Si vous donnez vos meilleures graines, vos voisins auront un meilleur maïs et vont remporter le concours à votre place »</p>
<p>Le modeste agriculteur lui a donné une leçon, il lui a dit « Ne savez-vous pas que quand le vent vient, il transporte les graines d'un champ à l'autre... Si mes voisins avaient des mauvaises graines, mon maïs serait aussi touché... Mais en leur donnant les meilleures graines j'ai la garantie d'avoir le meilleur maïs vu que mon champ a un voisinage d'excellente qualité »</p>
<br>
<p>L'idée vous est parvenue ?</p>
<p>Ta réussite passe aussi par la réussite des autres,</p>
<p>Changeons de mentalité mes chers,</p>
<p>Arrêtons de penser que pour réussir il faut que les autres échouent</p>
<p>Dans la vie il existe un modèle gagnant-gagnant :</p>
<p>Ca veut dire gagne et laisse les autres gagner = tous les deux vous allez gagner encore plus que si tu étais tout seul.</p>
<p>Mais penser à gagner seul et faire perdre les autres, c'est un mauvais modèle,</p>
<p>Je vous jure que ca ne ramène ni réussite, ni sourire d'espoir !</p>
<p>Pas la peine d'éteindre les bougies des autres pour que ta bougie ai l'air plus lumineuse...</p>
<p>Au contraire ta lumière + la lumière de l'autre + la lumière de l'autre = La vie devient plus lumineuse !</p>
<p>C'est vrai ou pas ?</p>
<p>C'est la méthode de cet agriculteur avec ses graines et ses voisins concurrents.</p>
<p>As-tu déjà vu quelqu'un marcher heureux dan la rue alors que tout le monde est triste ?</p>
<p>Il ne pourra pas, pour qu'il se sente heureux il faut que tout le monde le soit aussi.</p>
<p>Une partie du bonheur de l'individu dépend du bonheur du groupe.</p>
<p>Appliquons la méthode de cet agriculteur dans nos vies, et dessinons un sourire d'espoir :)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
