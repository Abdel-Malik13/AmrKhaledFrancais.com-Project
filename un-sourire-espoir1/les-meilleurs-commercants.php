                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>Beaucoup de gens m'ont demandé quelle est la chose qui dessine le plus un sourire d'espoir ?</p>
<p>C'est bien le nom de l'émission... Alors quelle est la meilleure chose qui donne de l'espoir ?</p>
<p>En vérité mes chers, j'ai trouvé que ce qui donne le plus un sourire d'espoir, c'est le bon comportement !</p>
<p>Le bon comportement met de l'espoir dans ton pays, dans ta maison, dans toi-même...</p>
<p>Je vais vous raconter une histoire :</p>
<p>Dans l'épisode précédent je parlais de Mohamed el-Fatih "le Conquérant",</p>
<p>Qui a fait la conquête de Constantinople (Ancienne appellation d'Istanbul en Turquie)</p>
<p>Constantinople était la capitale de l'empire byzantin donc ce n'était pas rien...</p>
<p>Pendant des siècles et des siècles personne n'y parvenait... Elle était extrêmement protégée...</p>
<p>Et ce jeune de 23ans est celui qui a réussi à le faire.</p>
<p>Mais quel est le rapport entre tout ca et le bon comportement ?</p>
<p>Le jour qui précédait l'assaut, Mohamed el-Fatih (Mehmed II) a fait quelque chose de très spécial.</p>
<p>Il s'est déguisé, puis il est parti au marché de Constantinople,</p>
<p>Il voulait vérifier une chose avant la bataille...</p>
<p>Il est parti tôt le matin au marché,</p>
<p>Il est entré chez un commerçant qui vendait de la nourriture, du blé, des fruits secs... etc.</p>
<p>Il a commencé à lui dire « Je prendrai ca, puis ca... Et il achetait en grande quantité... »</p>
<p>Quand il a fini, il a demandé au commerçant de lui calculer le total,</p>
<p>Le commerçant a commencé à calculer...</p>
<p>Et Mohamed el-Fatih lui a dit «Attendez, rajoutez-moi encore ca et ca ... »</p>
<p>Le commerçant (qui ne savait pas que c'était Mohamed el-Fatih) lui a répondu « Vous savez quoi ? Vous m'avez acheté plein de choses... Mais mon voisin commerçant n'a encore rien vendu... Vous devriez aller acheter des choses chez lui aussi, pour qu'il passe une bonne journée... Moi j'aurai le sourire car j'aurai bien vendu aujourd'hui, je veux qu'il ait le même sourire que le mien »</p>
<p>Vous voyez comment le sourire d'espoir vient :)</p>
<p>Mohamed el-Fatih a beaucoup apprécié la proposition,</p>
<p>Il est parti voir le commerçant d'à coté,</p>
<p>Il a acheté... Acheté... Acheté... Et au moment de payer il a refait la même expérience, il a dit au commerçant « Attendez, rajoutez-moi ca et ca »</p>
<p>Le commerçant lui a répondu « Vous savez quoi ? J'aimerai que vous alliez acheter ce qui vous manque chez mon voisin, je ne veux pas qu'il soit de mauvaise humeur, alors que moi j'aurai le sourire toute la journée»
<p>Si moi je me sens bien, et que celui qui est à côté de moi se sent mal... Je ne pourrais pas me sentir bien...</p>
<p>Imagine si tu vas te balader dans une rue, ou que tu visites un pays... Si tu as le sourire et que tous les gens sont de mauvaise humeur, tu vas aussi perdre ton sourire.</p>
<p>Croyez-moi mes chers, il n y'a pas de sourire d'espoir sans bon comportement.</p>
<p>Ces commerçants ont de grandes valeurs, ils ne veulent pas être les seuls à gagner,</p>
<p>Ils veulent partager avec ceux qui les entourent,</p>
<p>C'est un des plus beaux comportements, vouloir rendre heureux les autres,</p>
<p>Le sourire d'espoir ne se dessine jamais avec les mauvais comportements, la voracité, l'égoïsme, l'opportunisme sans principes...</p>
<p>C'est impossible de sourire seul, puisque tu vis entouré de gens,</p>
<p>Quand Mohamed el-Fatih a vu ces commerçants il a dit « Soyez optimistes, la victoire est arrivée ! »</p>
<p>Et il a remporté la victoire, et l'islam est arrivé à Constantinople,</p>
<p>Car quand les gens ont de bonnes valeurs, C'est obligé que Dieu les soutienne et qu'il amplifie leurs biens...</p>
<p>Quel est votre avis sur ce que je vous dis ?</p>
<p>Quel est votre avis sur les bons comportements et leurs rapports avec le sourire d'espoir ?</p>
<p>Donne du bonheur autour de toi,</p>
<p>Aide ceux qui t'entourent,</p>
<p>Teste aujourd'hui et tu verras à quel point tu auras le sourire,</p>
<p>Ai un bon comportement, partage avec les autres, et vous vivrez tous heureux.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
