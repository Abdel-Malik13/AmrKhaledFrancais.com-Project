                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui te dit : Sois audacieux !</p>
<p>Ca veut dire quoi être audacieux ?</p>
<p>Souvent les gens ont des capacités et des dons pour réussir dans la vie,</p>
<p>Ce qui leur manque c'est du courage... Faire le premier pas... Oser !</p>
<p>Beaucoup de personnes ont un grand potentiel,</p>
<p>Ces paroles sont adressées à tous les jeunes,</p>
<p>Vous pouvez accomplir de grandes réussites si vous prenez l'initiative.</p>
<p>Je vais vous raconter une histoire sympathique :</p>
<p>Celui qui me l'a racontée est un jeune marocain,</p>
<p>Il est doué pour écrire des livres, et il a le potentiel de devenir un grand écrivain...</p>
<p>Malgré ca, le premier livre qu'il a écrit, aucune maison d'édition n'a accepté de le publier,</p>
<p>Il a insisté... Il a fait le tour des autres maisons d'éditions... Mais sans résultat.</p>
<p>Après énormément de tentatives, une maison d'édition a enfin accepté de publier son livre,</p>
<p>Son livre est sorti pendant le salon du livre marocain,</p>
<p>Comme vous savez lors des salons, on met une table pour les plus grands écrivains pour signer les dédicaces et faire des conférences...</p>
<p>Et notre jeune écrivain faisait partie du public,</p>
<p>Il a remarqué qu'une des chaises était vide, un grand écrivain s'était absenté...</p>
<p>Il s'est forcément dit que la personne n'allait pas tarder à arriver,</p>
<p>10min... 15min... 20min... La chaise restait vide,</p>
<p>Les organisateurs n'avaient pas mis le nom de l'écrivain à qui elle appartenait,</p>
<p>Puis ca y est les gens ont fini par oublier qu'une des chaises était vide... Mais lui ne l'a pas oublié...</p>
<p>Savez-vous ce qu'il a fait ?</p>
<p>Il est parti s'asseoir sur la chaise, et il a présenté son livre aux visiteurs,</p>
<p>Sans que personne ne l'ai invité...</p>
<p>Bien sur ca aurait été malpoli qu'il aille s'asseoir si la personne allait arriver... Mais il a attendu longtemps... Et comme personne ne venait, il s'est dit pourquoi pas moi ?</p>
<p>C'est donc ca prendre des initiatives !</p>
<p>Que fait-on dans son cas ?</p>
<p>On dit « Je suis gêné »</p>
<p>« De quoi j'aurai l'air si un des organisateurs me remarque ? »</p>
<p>Et alors, s'ils te remarquent, qu'auras tu perdu ?</p>
<p>C'est comme ca qu'on apprend dans la vie, et c'est comme ca qu'on grandit...</p>
<p>Notre jeune écrivain a donc pris une dizaine de ses livres, il a rassemblé son courage et il est parti s'asseoir parmi les grands écrivains,</p>
<p>Une des personnes responsables de l'événement a remarqué qu'il s'était assis... Elle a voulu s'assurer que c'est la bonne personne... Vérifier son identité...</p>
<p>En la voyant arriver, il a encore fait preuve d'audace,</p>
<p>Il l'a regardé avec un sourire et en toute confiance en disant le premier « Je m'excuse, je suis arrivé en retard »</p>
<p>Après ca, la personne responsable était gênée de lui poser des questions, elle lui a juste répondu</p>
<p>« Pas grave, voulez-vous boire quelque chose ? »</p>
<p>Encore une fois il a été audacieux il lui a répondu « Un verre de thé s'il vous plait»</p>
<p>Et son tour est venu, il a parlé au micro,</p>
<p>Il a présenté son livre,</p>
<p>Et les gens ont été séduits,</p>
<p>Les photographes le prenaient en photo,</p>
<p>La presse lui posait des questions,</p>
<p>Et il signait des dédicaces...</p>
<p>Notre jeune écrivain m'a raconté « Le salon de l'année suivante, j'étais officiellement invité, et je faisais même partie des premiers,</p>
<p>Mais si la première fois je n'avais pas osé, si je n'avais pas présenté mon livre avec confiance, jamais je n'aurai été invité »</p>
<p>Comme lui sois courageux,</p>
<p>Ai confiance en toi,</p>
<p>C'est vrai que tu ne dois pas prendre ce qui appartient aux autres,</p>
<p>Mais si une chance se présente saisis-là,</p>
<p>Prends l'initiative et dessine un sourire d'espoir dans ta vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
