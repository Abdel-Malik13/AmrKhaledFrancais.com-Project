                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'histoire d'aujourd'hui nous vient des iles Comores...</p>
<p>C'est un pays en Afrique, la plupart de ses habitants sont musulmans,</p>
<p>Et beaucoup de comoriens viennent étudier à l'université d'Al-Azhar en Egypte,</p>
<p>Un de ces étudiants est un ami, C'est lui qui m'a raconté cette histoire vraie,</p>
<p>Elle m'a tellement touchée que j'ai versé des larmes en l'écoutant...</p>
<p>C'est arrivé en 1989,</p>
<p>Il y'a eu un grave tremblement de terre,</p>
<p>En quelques minutes les montagnes ont tremblé,</p>
<p>Et beaucoup de personnes sont décédées...</p>
<p>Il y'a eu beaucoup de dégâts matériels,</p>
<p>Dans un endroit isolé vivait un agriculteur, mon ami le connaissait...</p>
<p>La maison de cet agriculteur a été touchée par les secousses, mais elle n'a pas été démolie,</p>
<p>Il s'est rassuré que sa femme allait bien,</p>
<p>Puis il est parti en courant à l'école primaire où se trouvait son fils...</p>
<p>Malheureusement l'école se trouvait au cœur du choc,</p>
<p>Quand cet homme est arrivé devant l'école, il n'a trouvé que des ruines...</p>
<p>Le bâtiment a été détruit en entier,</p>
<p>Et tous les parents pleuraient en disant que les enfants sont morts...</p>
<p>Cet agriculteur a été choqué,</p>
<p>Il s'est rappelé que depuis la naissance de son fils Ibrahim,</p>
<p>Il le rassurait toujours en disant « Je serai toujours à tes côtés ! »</p>
<p>Cette phrase raisonnait dans la tête du père « Je serai toujours à tes côtés ! »... « Je serai toujours à tes côtés ! »... « Je serai toujours à tes côtés ! »...</p>
<p>Il voyait que son fils était sous les décombres, et qu'il n'était pas à côté de lui...</p>
<p>Les larmes coulaient de ses yeux... Son fils était mort...</p>
<p>Puis au bout d'un moment, il a rassemblé ses forces, avec une grande détermination il a essuyé ses larmes,</p>
<p>Il s'est concentré en pensant que la phrase « Je serai toujours à tes côtés ! » n'est pas seulement des paroles... Il faut passer à l'action...</p>
<p>Même si mon fils est mort, je le sortirai des décombres !</p>
<p>Il a commencé à chercher l'endroit exact où se trouvait la classe de son fils,</p>
<p>Son fils était du côté arrière droit du bâtiment...</p>
<p>Il est parti en courant,</p>
<p>S'est mis sur ses genoux,</p>
<p>Et creusait,</p>
<p>Les gens le regardaient...</p>
<p>Certains parents essayaient de le raisonner, il leur répondait « Soit vous m'aidez, soit vous me laissez ! »</p>
<p>Ils le laissaient et partaient...</p>
<p>Il continuait à retirer les pierres,</p>
<p>Il se blessait et du sang coulait de ses mains...</p>
<p>Un pompier venait lui dire « Monsieur vous faites quoi ? »</p>
<p>Il lui répondait « Soit vous m'aidez, soit vous me laissez ! »</p>
<p>Le pompier le laissait et partait...</p>
<p>Pareil un policier venait le voir en pensant qu'il avait perdu la raison,</p>
<p>Il lui disait la même chose « Soit vous m'aidez, soit vous me laissez ! »</p>
<p>Il a creusé pendant 37 heures...</p>
<p>C'est une histoire vraie mes chers!</p>
<p>Il a fini par bouger une grosse pierre,</p>
<p>Puis il a vu un espace vide,</p>
<p>Il a crié « Ibrahim ! Ibrahim ! Ibrahim ! »</p>
<p>Il a entendu une voix lui répondre « Je suis ici papa ! J'ai dit à mes camarades de classe que tu viendrais parce que tu m'as promis que tu seras toujours à mes côtés ! »</p>
<p>Le tremblement de terre a formé une sorte de triangle, et les enfants étaient dedans...</p>
<p>14 élèves sont décédés,</p>
<p>Mais 22 élèves ont été sauvés et parmi eux Ibrahim.</p>
<p>S'il était arrivé avec des heures de retard, ils n'auraient pas survécu,</p>
<p>On a emmené le père à l'hôpital pour soigner ses blessures,</p>
<p>Et l'enfant a grandi en disant « Papa est toujours à mes côtés ! »</p>
<p>Je dédicace cette histoire à tous les parents,</p>
<p>Donne toujours de la confiance à tes enfants,</p>
<p>Même s'ils sont loin de toi, ou que leur comportement ne te plait pas...</p>
<p>Rassure les toujours et montre leur que tu es à leur côté.</p>
<p>Quelques soient les problèmes qu'ils affronteront dans leurs vies, ils pourront les surmonter car ils ont confiance en vous.</p>
<br>
<p>Que Dieu t'aide, et te garde toujours auprès d'eux.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
