                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est une chose qui fait rire,</p>
<p>Mais qui m'est arrivée malheureusement...</p>
<p>Le titre de cet épisode est « Comment on m'a volé »</p>
<p>C'est vraiment arrivé,</p>
<p>Je me trouvais dans un endroit avec plein de monde,</p>
<p>Je portais un jean, et mon téléphone portable était dans ma poche gauche...</p>
<p>Je vais vous raconter ce qui m'est arrivé sur un ton léger, mais à la fin il y'a une morale,</p>
<p>Que ce qui m'est arrivé vous soit utile.</p>
<p>Je trouve que le voleur a utilisé une technique brillante...</p>
<p>J'avais donc le téléphone dans ma poche gauche, il y'avait une grande foule,</p>
<p>Je saluais plein de personnes, et plein de personnes me saluaient,</p>
<p>Mais je faisais attention à mon téléphone,</p>
<p>Et comme je portais un jean, la poche était serrée,</p>
<p>Alors tant que le téléphone était dedans, je le sentais...</p>
<p>Je saluais les gens,</p>
<p>Puis il est arrivé (celui qui va me voler)</p>
<p>Il est arrivé en souriant « Bienvenue... Bienvenue Dr. Amr !»</p>
<p>Puis il m'a pris dans ses bras,</p>
<p>Très bien, mais je faisais toujours attention à mon téléphone...</p>
<p>Sauf que je n'avais pas vu que cet homme avait dans sa main une éponge mouillée,</p>
<p>Il a posé cette éponge sur ma poche droite,</p>
<p>Soudainement j'ai senti de l'eau sur ma poche droite,</p>
<p>Alors toute ma concentration et mes deux mains sont parties vers cette poche mouillée,</p>
<p>:)</p>
<p>Et pendant que j'étais occupé à comprendre ce qui se passait avec ma poche droite,</p>
<p>Il a mis sa main dans ma poche gauche et il a pris mon téléphone portable...</p>
<p>Je n'ai rien senti...</p>
<p>C'est quand cet homme est parti que je me suis rendu compte que mon téléphone n'était plus là.</p>
<p>Mais il ne s'est pas arrêté ici...</p>
<p>Il est parti voir un des jeunes de l'association qui m'accompagnait,</p>
<p>Il lui a dit «J'aimerai prendre ton numéro de téléphone et ton prénom, car j'ai demandé des choses au Dr. Amr »</p>
<p>Il lui a bien sûr communiqué ses coordonnées...</p>
<p>Cet homme l'a appelé une heure après en disant « J'ai en ma possession le téléphone portable du Dr., est ce que je peux lui parler ? »</p>
<p>Quand je lui ai parlé il m'a dit « Dans votre répertoire il y'a 7000 contacts, c'est sûr qu'ils sont précieux, 7000 numéros doivent sûrement valoir 10.000 livres égyptiennes (1100€)</p>
<p>Je suis prêt à vous rendre votre téléphone si vous me payez cette somme,</p>
<p>Parce que j'ai des enfants à nourrir,</p>
<p>Et que j'ai besoin que vous m'aidiez.»</p>
<p>Au final je n'ai pas payé les 10.000 livres égyptiennes, je lui ai dit que j'avais une copie des 7000 contacts... Mais de l'intérieur j'étais vexé et énervé :)</p>
<p>Mais si je vous raconte cette histoire c'est pour dire : Cet homme a une grande intelligence,</p>
<p>Mais pourquoi cette intelligence n'est pas employée dans le bien ?</p>
<p>Pourquoi il exprime son intelligence dans le mal ?</p>
<p>Imaginez si cet homme avait utilisé ce talent qu'il a, et son ingéniosité dans le bien ce que ca aurait donné ?</p>
<p>Il aurait gagné de l'or !</p>
<p>Il aurait vécu comme un roi,</p>
<p>Mais le diable s'est joué de lui,</p>
<p>Mes chers, ne laissez jamais le diable se moquer de vous, et transformer vos talents en argent illicite, en vol, en arnaques etc.</p>
<p>Alors que tes capacités sont immenses, il suffit juste que tu les mettes dans la bonne direction...</p>
<p>Moi j'ai été volé,</p>
<p>Et lui a pris le téléphone portable,</p>
<p>Mais c'est impossible qu'un sourire d'espoir se dessine sur son visage !</p>
<p>Car ce n'est pas la bonne façon de vivre dans ce bas monde.</p>
<p>Mes chers, si vous voulez un sourire d'espoir dans vos vies, employez votre intelligence et vos capacités dans le bien et non pas dans le mal.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
