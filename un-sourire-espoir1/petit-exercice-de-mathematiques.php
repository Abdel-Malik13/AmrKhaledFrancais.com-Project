                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est un petit exercice de mathématique...</p>
<p>Préparez une feuille et un stylo pour calculer avec moi :)</p>
<p>A la fin de ce calcul vous trouverez un sens formidable...</p>
<p>L'histoire raconte qu'un père avait 3 fils,</p>
<p>Le père est décédé en laissant à ses trois enfants 17 chameaux...</p>
<p>Note donc 3 enfants et 17 chameaux :)</p>
<p>Dans son testament, le père a légué au plus grand la moitié des chameaux,</p>
<p>Le fils du milieu devait hériter du tiers des chameaux,</p>
<p>Et le petit devait hériter de 9/10 des chameaux.</p>
<p>Donc le grand devait prendre la moitié, le moyen devait prendre le tiers, et le petit devait prendre le neuvième...</p>
<p>Le problème c'est que le père a imposé une condition difficile,</p>
<p>Il leur a dit « N'abattez aucun des chameaux... Surtout pas ! »</p>
<p>Que chacun prenne sa part de l'héritage mais sans abattre un seul chameau...</p>
<p>Ce père était exceptionnel, il voulait donner une leçon à ses enfants même après sa mort.</p>
<p>Les 3 fils ont bloqué au moment de diviser...</p>
<p>Le premier a calculé la moitié de 17 ca donne 8,5 ... Donc il faut obligatoirement abattre un chameau, sinon c'est impossible.</p>
<p>Mais leur père était clair là-dessus.</p>
<p>Le deuxième a calculé le tiers de 17 ca donne 6,5 ...</p>
<p>Et le troisième 17/9 = 1,88 ...</p>
<p>Les 3 enfants ne savaient pas quoi faire, ils tournaient en rond...</p>
<p>Un bédouin passait par là sur son chameau,</p>
<p>Il leur a demandé ce qui se passait,</p>
<p>Ils lui ont expliqué l'histoire « Notre père nous a laissé 17 chameaux, et on n'arrive pas à les partager »</p>
<p>Le bédouin a rit en disant « Votre père est un génie, il vous donne une grande leçon... Prenez mon chameau, et ajoutez-le à votre héritage...</p>
<p>Leur nombre deviendra 17 + 1 (le mien) = 18</p>
<p>Et maintenant divisez :</p>
<p>Pour le premier, la moitié de 18 = 9 chameaux c'est ta part</p>
<p>Pour le deuxième, le tiers de 18 = 6 chameaux pour toi</p>
<p>Pour le troisième, 18 divisés par 9 = Ca donne 2 chameaux</p>
<p>Calculez le total : 9 + 6 + 2 = 17 chameaux</p>
<p>Il va rester un chameau de trop c'est le mien ;)</p>
<p>Je vous ai aidé à résoudre votre problème donc je le reprends.</p>
<br>
<p>Refaites le calcul chez vous c'est très facile !</p>
<p>Mais alors qu'a voulu enseigner ce père à ses enfants ?</p>
<p>La morale est immense :</p>
<p>Parfois les gens ont des problèmes qu'ils n'arrivent pas à résoudre...</p>
<p>Quand toi tu arrives avec ta bonne volonté et tu leur complète ce qui manque,</p>
<p>Le problème se règle, et Dieu le très haut te rembourse ce que tu as dépensé !</p>
<p>Par exemple en Egypte on a lancé le projet de lutte contre l'analphabétisme,</p>
<p>Dans 15 jours, nous voulons inscrire 100mille analphabètes pour qu'ils suivent des cours...</p>
<p>Le souci c'est que les membres de notre association « Les bâtisseurs de la vie » ne sont pas assez nombreux pour aller chercher cent mille personnes,</p>
<p>On a donc besoin que tu viennes nous aider avec ton chameau ;)</p>
<p>Nous avons besoin que tout ceux qui ont dans leur entourage une personne qui ne sait ni lire ni écrire, l'inscrive sur la liste que nous avons mis en place sur facebook.</p>
<p>Si tu rajoutes ton chameau à nos 17 chameaux, le problème sera résolu, nous rassemblerons 100.000 personnes dans les classes,</p>
<p>Et Dieu le très haut te remboursera le temps et les efforts que tu as dépensé pour nous aider.</p>
<p>Apprenons à nos enfants la même chose que ce père avec ses trois fils,</p>
<p>Mon cher si tu vois une personne perdue face à un problème, ne reste pas les bras croisés...</p>
<p>Passe à l'action et aide-la avec de l'argent ou du soutien moral,</p>
<p>Le problème sera réglé, et Dieu te récompensera pour ton effort.</p>
<p>Notre pays a besoin qu'on s'entraide en ces temps difficiles.</p>
<p>Mettons en pratique ce petit exercice de mathématique et dessinons un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
