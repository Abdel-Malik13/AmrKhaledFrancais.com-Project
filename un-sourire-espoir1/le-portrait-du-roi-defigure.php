                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'idée d'aujourd'hui c'est :</p>
<p>Arrives-tu à dessiner les gens sans leurs défauts ?</p>
<p>Peux-tu voir les gens mais sans leurs défauts ?</p>
<p>Tu me diras comment, puisque tous les gens ont des défauts...</p>
<p>Le problème c'est qu'on ne voit que les défauts des gens,</p>
<p>A chaque fois qu'un mari voit son épouse, il se rappelle de tous ses défauts... Alors il n'arrive plus à lui sourire...</p>
<p>Et pareil entre un enfant et ses parents, il oublie les bonnes choses qu'ils ont faites pour lui... Et ne voit que les critiques ou les punitions qu'ils lui donnent...</p>
<p>C'est un vrai problème au quotidien.</p>
<p>Je vais vous raconter une histoire :</p>
<p>C'est l'histoire d'un roi qui ne voyait que d'un seul œil,</p>
<p>Quand il était plus jeune, il a eu un accident qui a défiguré son 2ème œil...</p>
<p>Il avait une cicatrice sur le visage qui le complexait,</p>
<p>Même si c'était un roi juste avec son peuple,</p>
<p>Le problème c'est que quand les gens pensaient à lui, ils pensaient d'abord à lui comme quelqu'un de défiguré...</p>
<p>Le roi aimait chasser,</p>
<p>Et peut-être que c'est lors d'une partie de chasse qu'il a perdu son œil...</p>
<p>L'essentiel c'est que le roi voulait changer l'image que les gens avaient de lui,</p>
<p>Alors il a fait venir plein d'artistes peintres pour lui dessiner un joli portrait ...</p>
<p>Il leur a dit « Je veux que vous fassiez mon portrait, mais sans laisser apparaitre ma cicatrice... »</p>
<p>Ils lui ont répondu «C'est impossible, si on vous dessine avec un œil normal, ca sera le portrait d'une autre personne... Pour que ca soit votre portrait, il faut qu'il contienne vos défauts... »</p>
<p>Le roi n'a pas aimé leur réponse,</p>
<p>Il a continué à chercher,</p>
<p>Puis il a fini par trouver un artiste peintre qui lui a dit « Malgré vos défauts, je peux vous faire un joli portrait sans problème »</p>
<p>Savez-vous comment il a procédé ?</p>
<p>Il a fait un portrait du roi, en train de chasser,</p>
<p>En pleine concentration, avec le viseur du fusil sur son œil défiguré,</p>
<p>Et son autre œil grand ouvert...</p>
<p>Vous avez compris l'idée ?</p>
<p>Du coup le portrait du roi était beau, sans montrer son œil défiguré,</p>
<p>Et donnait de lui l'image d'un roi courageux et excellent chasseur...</p>
<p>Le roi a aimé son portrait,</p>
<p>Il a commencé à le coller dans les rues,</p>
<p>Et les gens pensaient beaucoup moins à ses défauts physiques, et plus à sa justice...</p>
<br>
<p>Je vous raconte cette histoire parce qu'on fait souvent la même chose,</p>
<p>On dessine dans nos têtes le portrait des gens en mettant en avant leurs défauts...</p>
<p>Et en oubliant leurs qualités, ou les bonnes choses qu'ils ont faites...</p>
<p>Pouvez-vous masquer les défauts des gens, pas seulement devant eux,</p>
<p>Mais aussi dans vos têtes ?</p>
<p>Peux-tu dessiner une nouvelle image positive de ton épouse ou ton mari ?</p>
<p>Peux-tu voir (désolé de le dire) ton papa et ta maman d'une façon positive, et mettre en avant les bonnes choses qu'ils ont faites pour toi ?</p>
<p>Savez-vous que le Prophète (paix et bénédictions soient sur lui) nous l'a recommandé dans une de ses paroles :</p>
<p><strong class="prophete">« Le croyant ne doit pas détester sa femme, car si un comportement en elle lui déplaît, un autre lui plaira. »</strong></p>
<br>
<p>Même si ta femme a des défauts, pèse là avec ses qualités en même temps,</p>
<p>Dessine une image positive d'elle,</p>
<p>D'ailleurs fais la même chose pour l'image que tu as de toi même,</p>
<p>Met en avant tes qualités, pas tes défauts...</p>
<p>Pour vivre heureux, et dessiner un sourire d'espoir chez toi et chez les autres.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
