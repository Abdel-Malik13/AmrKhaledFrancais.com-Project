                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>L'épisode d'aujourd'hui est très romantique et très sentimental,</p>
<br>
<p>Personnellement je crois en l'amour,</p>
<p>Pour moi c'est un très joli sentiment,</p>
<p>Un sentiment que Dieu nous a créé pour nous aider à supporter les difficultés de cette vie,</p>
<p>Tous les genres d'amour, entre parents et enfants...</p>
<p>Mais surtout entre l'homme et la femme,</p>
<p>Et encore plus quand cet amour se transforme en mariage,</p>
<p>Et en une vie de couple heureux,</p>
<br>
<p>Dieu a fait de l'amour un sentiment très noble,</p>
<p>Dedans réside le secret de la continuité de l'humanité,</p>
<br>
<p>S'il n'existait pas l'amour entre l'homme et la femme,</p>
<p>Il n y'aurait pas eu de mariages,</p>
<p>Donc pas d'enfants,</p>
<p>Donc l'humanité n'aurait pas continué d'exister.</p>
<br>
<p>Dieu a donc fait de l'amour la clé de notre continuité,</p>
<p>A ce point c'est un sentiment précieux....</p>
<p>Je vous raconte tout ca, car on nous parle beaucoup d'histoires d'amour qui finissent mal... Mais dernièrement j'ai vu une histoire d'amour qui a réussi,</p>
<p>Lui était en deuxième année à l'université,</p>
<p>Elle était dans la même classe que lui,</p>
<p>Il l'a aimé d'un grand amour, et s'est énormément attaché à elle...</p>
<p>C'était un amour pur et sincère,</p>
<p>Il est parti demander sa main à ses parents,</p>
<p>Le père de la fille a refusé en disant qu'il n'avait pas encore fini ses études...</p>
<p>Quand tu seras diplômé reviens demander sa main...</p>
<p>Aie! il faut attendre 3 ans minimum ?</p>
<p>Il a gardé son amour... Il s'est accroché...</p>
<p>Et il a préservé la fille qu'il aimait,</p>
<p>Il n'est pas sorti avec elle, ni manqué de respect...</p>
<p>S'il l'avait fait je ne pense pas que cet amour aurait survécu,</p>
<p>Cet amour a survécu car tous les deux avaient une bonne intention,</p>
<p>Ils ont décidé de patienter le temps nécessaire,</p>
<p>Des années s'il le fallait,</p>
<p>Leur patience a fini par payer,</p>
<p>Il a fini ses études,</p>
<p>Il est revenu voir le père de la fille,</p>
<p>Et cette fois il a accepté,</p>
<p>Ils se sont mariés,</p>
<p>Et aujourd'hui ils ont 4 enfants,</p>
<br>
<p>C'est une histoire que j'ai vu de mes propres yeux,</p>
<p>Et je voulais la partager avec vous,</p>
<p>Surtout les jeunes,</p>
<p>Sache que tu as le droit de défendre ton amour,</p>
<p>Mais met le sur le bon chemin...</p>
<p>Un chemin avec un mariage à l'arrivée,</p>
<p>Et patiente pour ce très beau sentiment...</p>
<p>Comme dans l'histoire que j'ai racontée,</p>
<p>Mohamed et Dalia sont leurs prénoms,</p>
<p>Ils vivent heureux aujourd'hui avec deux filles et deux garçons,</p>
<p>Après avoir patienté 5 ans l'un pour l'autre,</p>
<p>Une histoire vraie, pleine de noblesse et d'humanité,</p>
<p>Grace à ces 5 années, ils garderont un sourire d'espoir toutes leurs vies.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
