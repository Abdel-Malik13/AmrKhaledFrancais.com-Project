                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>Le sourire d'espoir d'aujourd'hui parle de l'art de conduire une voiture :)</p>
<p>Tu sais conduire ?</p>
<p>Tu maitrises bien la conduite ?</p>
<p>Quand tu conduis, tu regardes dans les rétroviseurs combien de fois ?</p>
<p>Quand tu conduis tu regardes devant toi, ou tu regardes dans les rétroviseurs ?</p>
<p>Tu me diras c'est quoi cette question basique ?</p>
<p>Bien sûr je regarde devant !</p>
<p>Donc à quel moment tu regardes dans les rétroviseurs ?</p>
<p>Juste de temps en temps pour vérifier la distance entre moi et la voiture derrière...</p>
<p>Donc tu sais bien conduire :)</p>
<p>Tu me diras tout le monde aurait donné la même réponse...</p>
<p>Tu me crois si je te dis que nous ne savons pas faire la même chose dans nos vies...</p>
<p>On est toujours entrain de regarder derrière,</p>
<p>On passe notre temps à regarder dans les rétroviseurs, et personne ne regarde devant...</p>
<p>Mes chers, Ce n'est vraiment pas raisonnable...</p>
<p>On est tout le temps entrain de parler du passé, et dire que c'était mieux avant...</p>
<p>Imaginez un conducteur qui passe son temps à regarder les rétroviseurs... Combien d'accidents va-t-il provoquer ?</p>
<p>A quel point va-t-il dévier de la route ?</p>
<p>Tu vas rire de lui ou pas ?</p>
<p>Et bien malheureusement nous faisons la même chose...</p>
<p>Nous faisons la même chose dans les médias,</p>
<p>Et nous faisons ca dans nos vies personnelles,</p>
<p>Parfois nous cherchons des faits divers anciens car on les trouve excitants...</p>
<p>J'ai envie de regarder devant mes chers , J'ai envie qu'on conduise tout droit...</p>
<p>J'ai envie qu'on conduise tous en regardant devant,</p>
<p>Juste par moments regarder dans les rétroviseurs pour voir ce qui s'est passé derrière nous...</p>
<p>Et apprendre des erreurs du passé, et ne pas reproduire les même erreurs...</p>
<p>Mais la base c'est de regarder droit devant !</p>
<p>Cette image de voiture est très très très semblable à notre situation actuelle,</p>
<p>Tu m'écoutes peut-être en conduisant ta voiture, et comme je te dis de ne pas regarder dans les rétroviseurs, je vais provoquer un accident :)</p>
<p>Non sérieusement, comme tu regardes devant en conduisant,</p>
<p>Dans ta vie je t'en prie fais la même chose,</p>
<p>Parlons de demain,</p>
<p>Parlons d'espoir,</p>
<p>Parlons d'avenir,</p>
<p>Cessons de retourner le couteau dans la plaie,</p>
<p>Oublions les blessures du passé,</p>
<p>Et concentrons nous sur demain,</p>
<p>Et à ce moment on dessinera un sourire d'espoir tous ensemble !</p>
<p>Parce que celui qui regarde droit devant, voit de l'espoir,</p>
<p>Et parce que celui qui regarde derrière provoque des accidents.</p>
<br>
<p>Un sourire d'espoir, et un regard vers l'avenir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
