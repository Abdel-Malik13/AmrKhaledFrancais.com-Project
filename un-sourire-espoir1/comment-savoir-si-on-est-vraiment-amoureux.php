                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle d'amour,</p>
<p>Pas l'amour envers les parents ;)</p>
<p>Mais l'amour entre l'homme et la femme.</p>
<p>Je vais vous raconter une histoire d'amour que je trouve rare...</p>
<p>C'est une histoire d'amour arabe,</p>
<p>Souvent on pense que les histoires d'amour viennent de l'occident,</p>
<p>Non c'est une histoire d'amour de chez nous...</p>
<p>Lui est arabe et elle aussi,</p>
<p>Mais ils sont de nationalités différentes,</p>
<p>Ils se sont aimés d'un amour exceptionnel,</p>
<p>Un amour très très très fort...</p>
<p>Mais lui m'a raconté quelque chose d'inhabituel,</p>
<p>Et c'est pour cette chose que j'ai décidé de faire cet épisode...</p>
<p>Il avait envie de l'épouser,</p>
<p>Il vivait dans l'espoir de demander sa main,</p>
<p>Et son cœur était rempli d'un amour peu commun envers elle...</p>
<p>Il m'a raconté quelque chose d'étonnant,</p>
<p>Il m'a dit « Soudainement, j'ai senti que j'aimais faire le bien,</p>
<p>J'aimais beaucoup les gens,</p>
<p>Je voulais rendre les gens heureux,</p>
<p>C'est parce que j'étais amoureux d'elle que je voulais donner du bonheur aux autres,</p>
<p>Je suis devenu généreux grâce à cet amour,</p>
<p>Je suis devenu doux avec les autres,</p>
<p>J'étais miséricordieux avant, mais pas à ce point,</p>
<p>Je suis devenu très tendre avec les gens, mon comportement a changé depuis que je suis amoureux d'elle »</p>
<p>En écoutant les paroles de cet homme,</p>
<p>J'ai eu envie de partager ce sens avec vous,</p>
<p>Avec tous les jeunes, qui veulent savoir si leur amour est sincère ou pas...</p>
<p>Il existe tant de personnes qui disent aimer quelqu'un,</p>
<p>Pour savoir si c'est sincère, il faut voir si dans cet amour il y'a un désir de construire,</p>
<p>L'amour sincère est celui qui te pousse à donner aux autres,</p>
<p>Ton amour est sincère quand il te remplit de douceur et de miséricorde,</p>
<p>L'amour sincère augmente en toi l'envie de faire le bien.</p>
<p>L'amour artificiel quant à lui n'augmente pas en toi l'envie de faire le bien et la miséricorde envers les autres...</p>
<p>L'homme de l'histoire n'a pas épousé celle qu'il aimait à la fin,</p>
<p>Elle s'est mariée avec quelqu'un d'autre...</p>
<p>Mais il me disait « La miséricorde n'est pas morte dans mon cœur,</p>
<p>Le bien a continué de grandir en moi, et je suis devenu très proche de Dieu.</p>
<p>Parce que je m'adressais à Dieu en lui demandant de me donner sa miséricorde, et de soulager mon cœur »</p>
<p>Vous vous rendez compte à quel point l'amour est une très belle chose ?</p>
<p>L'essentiel est qu'il aille dans le bon chemin,</p>
<p>Le grand amour de cet homme envers cette femme, s'est transformé en miséricorde envers les gens,</p>
<p>Croyez-moi cet amour était sincère,</p>
<p>Vous aussi désormais vous pouvez juger votre amour,</p>
<p>Vous pouvez savoir s'il est sincère ou artificiel...</p>
<p>S'il vous donne envie de construire et faire le bien alors vous aurez un sourire d'espoir grâce à votre amour.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
