                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle d'une histoire qu'on dit vraie (mais je n'en suis pas très sur)</p>
<p>J'avais envie de vous la raconter car elle traite un problème qu'on a chez nous...</p>
<p>Laissez-moi d'abord vous la raconter, ensuite je vous dirai sa morale :</p>
<p>On dit qu'une société vendait des produits alimentaires, des fruits... Etc.</p>
<p>Donc elle devait avoir une grande chambre froide, comme celles qu'on voit dans les films,</p>
<p>On y stockait les produits en attendant de les vendre, alors vous imaginez comment il faisait froid à l'intérieur.</p>
<p>C'était un Jeudi (avant les vacances), un employé est entré vérifier que tout était en place, compter les cartons... Etc.</p>
<p>Pendant qu'il travaillait à l'intérieur, la porte de la chambre froide s'est fermée par accident,</p>
<p>Quelle catastrophe !</p>
<p>Alors qu'il était à l'intérieur, il a entendu « Tac » la grande porte blindée s'est fermée...</p>
<p>Il est parti en courant comme un fou,</p>
<p>Il frappait... frappait... Mais personne n'ouvrait,</p>
<p>Puis il s'est rappelé que c'était le jour d'avant les vacances, tout le monde est déjà parti... La porte ne va pas s'ouvrir avant 2 ou 3 jours...</p>
<p>Cet homme a compris qu'il allait mourir... Personne n'allait l'entendre, ni lui ouvrir...</p>
<p>Il est parti s'asseoir dans un coin,</p>
<p>Il a commencé à trembler,</p>
<p>Il sentait qu'il avait de plus en plus froid,</p>
<p>Il a pris une feuille et un stylo pour raconter les dernières heures de sa vie,</p>
<p>Il a écrit « Je suis coincé dans la chambre froide... Je sens que les parties de mon corps se gèlent... J'ai des fourmis dans mes doigts, j'ai du mal à bouger mes mains et mes pieds... »</p>
<p>Et son écriture devenait de plus en plus faible...</p>
<p>L'homme a fini par décéder.</p>
<p>Quand ils ont ouvert la porte des jours plus tard, ils ont trouvé son corps sans vie.</p>
<p>Mais il y'avait une surprise... Laquelle ?</p>
<p>Le câble électrique du frigo était débranché,</p>
<p>Avant les vacances on coupe toujours le courant...</p>
<p>Mais alors qu'est ce qui a tué cet homme ?</p>
<p>L'illusion...</p>
<p>Cet homme a cru dans sa tête que la température était en dessous de zéro, et qu'il allait mourir,</p>
<p>Et il a faibli petit à petit... Jusqu'à craquer...</p>
<p>Mais pourquoi je vous raconte tout ca ?</p>
<p>Je vous en prie mes chers, ne baissez jamais les bras face à une idée erronée... Ne vivez pas dedans jusqu'à ce qu'elle détruise votre vie.</p>
<p>Ne perd jamais confiance en toi, ne pense jamais que tu es faible, ne te décourage pas, ne dis jamais « Je n'y arrive pas, c'est impossible à réaliser »</p>
<p>N'entre jamais dans le frigo des illusions, ne pense jamais qu'il va te tuer alors qu'il est éteint...</p>
<p>Ne fais jamais ca !</p>
<p>Ne laisse pas une petite chose te dominer, te tyranniser, contrôler ta vie... Tu vois cette chose comme immense, mais en réalité elle est inexistante.</p>
<p>Parfois on entre exprès dans le frigo des illusions et on pense qu'on est finis...</p>
<p>Je m'adresse aussi aux filles : Ne vis jamais dans un amour artificiel, tu pleures et tu es écœurée par la vie alors que cet homme ne te mérite pas...</p>
<p>Si tu perds ton travail, et que tu te retrouves au chômage... Ne dis pas j'ai tout perdu, il n y'a plus d'espoir... Ne fais pas de dépression, surtout pas !</p>
<p>C'est juste une illusion, lève toi le matin, bouge et fais des efforts et tu finiras par réussir !</p>
<p>Ne dites jamais que vous êtes finis car vous avez perdu quelque chose ou quelqu'un...</p>
<p>Ne sors jamais d'une relation amoureuse en disant « Je n'arriverai jamais à me marier, ni à trouver le bonheur » Surtout pas!</p>
<p>Mes chers, ne laissez pas les illusions vous faire perdre votre sourire d'espoir...</p>
<p>N'entrez jamais dans le frigo des illusions.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
