                                        <br>
                                        <p>In the name of Allah (God) The Compassionate, The Merciful.</p>

<p>Welcome to a new episode of Basmit Amal (A Smile of Hope).</p>

<p>A Smile of Hope today, is a story that people say is a true one. I am not sure if it's true or not, but I want to tell it because it helps solve a big problem often faced.</p><p>Let me tell you the story, and then tell you the moral of it.</p>
<p>Once upon a time there was a company, that used to sell food, fruits, etc and they needed a really big fridge.</p><p> They used a big fridge, just like those in the moies.</p> <p>It was a fridge that's really huge and has lots of space to move around in.</p> <p>This fridge protecs the food from going bad, such as the bananas, so the fruits don't go bad until they sell them.</p>
<p>One day, the employee who used to enter the supplies in and out of the fridge entered the fridge (and I'am sure you can imagine the temperature inside the fridge). It was a Thursday (the day before the weekend), and he was in the fridge so he can take inventory.</p>
<p>All of a sudden, someone closed the door of the fridge in error while the employee was inside.</p>
<p>He heard the click of the door as it closed.</p>
<p>The employee ran like crazy (can you imagine someone stuck in a fridge), and he kept on knocking and slamming on the door, and no one opened the door.</p>
<p>He continued knocking and slamming on the door, and he remembered that it's Thursday.</p>
<p>He realized it's the end of the work week, and that the fridge won't be opened again until two days later. The man realized he will die, and that no one heard him or opened the door.</p>
<p>The man sat down, and began shivering.</p>
<p>He felt like he was shivering so much because of the cold. He began writing  paper, as if he knew he was going to die. He wrote, "I am now stuck     in the fridge. I feel like my limbs are freezing. I can't feel my arms, legs, and fingers anymore."</p>
<p>The handwriting kept on fading away and getting weaker. The man died.</p>
<p>The next day they opened the fridge, and they found that the man died. However, there was a surprise. The fridge was off. The fridge gets disconnected on thursday, at the time he had entered the fridge. The fridge wasn't on, and wasn't connected to the electricity.</p>
<p>So what killed him? The illusion killed him.</p>
<p>The man believed that the fridge is really cold, and that it was below freezing and that he was going to die. He continued to get weaker piece by piece until he couldn't take it anymore.</p>
<p>What am I trying to tell you? I am telling you not to surrender to a false idea, and allow it to ruin your life. Don't loose your self-confidence and feel like you're weak and that you can't take it anymore because something is really hard.</p>
<p>Don't take the choice to enter the fridge while it's off, and imagine that it will kill you. Don't let something small control your life and see it as if it's huge, while it's something that doesn't even exist. The fridge of illusion- Sometimes we enter the fridge of illusion, and we imagine that we lost it all. Young ladies, don't live in false love, and keep crying and loosing your life, when he doesn't even deserve you.</p>
<p>Don't let the loss of your job in a company, make you say I lost it all and get depressed. Never, because it's an illusion. Work, move forward, make a defference, and you will become successful. Don't lose a place, and say that you lost it all. Don't let a love story that failed make you imagine that you won't get married, and tht you lost it all.</p>
<p>Never, people, allow that an illusion control you, or take away your smile fillew with hope. A smile of hope is that you never live in an illusion ever.</p>
<p>Thank you and Salamu Aliekum wa Rahamtullah.</p>
