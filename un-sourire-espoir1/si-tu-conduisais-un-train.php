                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                       <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle des bonnes décisions,</p>
<p>Comment faire le bon choix devant 2 options difficiles ?</p>
<p>2 options amères comme on dit...</p>
<p>Et sur quels critères il faut choisir.</p>
<p>Laissez-moi vous raconter une histoire pour mieux comprendre :</p>
<p>On dit qu'un groupe d'enfants jouaient sur les rails d'un train,</p>
<p>Il y'avait plusieurs rails,</p>
<p>Des rails fonctionnels, sur lesquels les trains passaient,</p>
<p>Et des rails non-fonctionnels, sur lesquels aucun train ne passait...</p>
<p>Sur les 10 ou 15 enfants, un seul s'est mis à l'écart et a dit « Moi je vais jouer tout seul sur les rails qui ne fonctionnent pas »</p>
<p>Et les autres enfants ont dit « Le train ne passera pas tout de suite, alors allons jouer sur les rails fonctionnels»</p>
<p>Puis ils ont mis des buts, les rails délimitaient le terrain, et ils jouaient au ballon...</p>
<p>L'enfant qui s'est mis à l'écart avait envie de jouer avec eux... Mais il était prudent, il ne voulait pas jouer sur les rails dangereux...</p>
<p>Maintenant je veux que tu imagines que le train arrive,</p>
<p>Et que tu te trouves à l'entre-deux des rails,</p>
<p>Là où tu peux changer la direction du train...</p>
<p>Et que tu dois prendre une décision,</p>
<p>Le train arrive, et tu dois décider,</p>
<p>Tu vois d'un côté 20 garçons qui jouent sur des rails fonctionnels,</p>
<p>Et de l'autre tu vois un seul garçon jouer sur des rails non-fonctionnels, mais que tu peux mettre en fonction...</p>
<p>Comment tu vas réagir ?</p>
<p>Tu laisseras le train continuer dans la même direction et tuer un grand nombre d'enfants ?</p>
<p>Ou tu vas changer la direction du train et sacrifier la vie d'un seul enfant qui joue sur les rails sensés être non-fonctionnels ?</p>
<p>Peux-tu réfléchir un petit moment ?</p>
<p>Que vas-tu décider ?</p>
<p>Il me semble que la plupart des gens diront « 20 enfants d'un côté et un enfant de l'autre... Changeons la direction du train... On aura sacrifié une seule vie pour en sauver une dizaine »</p>
<p>C'est une décision sage, d'un point de vue logique et même sentimental.</p>
<p>Mais cet enfant s'est bien comporté...</p>
<p>Il n'a pas voulu jouer sur les rails fonctionnels...</p>
<p>Sur les 20 enfants, il est le seul à avoir fait le bon choix...</p>
<p>Donc tu vas sacrifier celui qui a bien agi pour sauver les autres qui ont mal agi ?</p>
<p>Vous comprenez où je veux en venir ?</p>
<p>A la fin je veux dire ceci :</p>
<p>En prenant une décision difficile, entre 2 options amères comme on dit...</p>
<p>Choisis ce qui est juste, ne choisis pas ce qui est plus grand en nombre ou en quantité,</p>
<p>Peux-tu s'il te plait en prenant tes décisions, ne pas trancher en fonction du nombre ?</p>
<p>Mais trancher en fonction de ce qui est juste,</p>
<p>Quand une personne prend une bonne décision, et que tous les autres vont dans la direction opposée,</p>
<p>Tu ne dois pas suivre les autres car ils sont en plus grand nombre,</p>
<p>Tu dois suivre celui qui a raison.</p>
<p>En faisant des choix, demande toi « Qu'est ce qui est juste ? »</p>
<p>« De quel côté se trouve la vérité ? »</p>
<p>Et décide en fonction de la vérité, et non pas en fonction du nombre élevé,</p>
<p>C'est une façon de raisonner qui va te donner une grande paix,</p>
<p>Car sans elle, tu resteras désorienté,</p>
<p>Pour retrouver ton orientation demande toi toujours « Qu'est ce qui est juste ? »</p>
<p>Ne juge pas en fonction de la quantité, mais en fonction de la vérité.</p>
<p>A ce moment tu trouveras ton bonheur.</p>

<p>Merci, c'était un nouveau sourire d'espoir.</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
