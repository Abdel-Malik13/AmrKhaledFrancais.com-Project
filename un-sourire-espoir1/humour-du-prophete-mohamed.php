                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle des blagues,</p>
<p>Les égyptiens sont connus pour beaucoup en faire,</p>
<p>Et s'ils ne trouvent pas de quoi rire, ils rient d'eux-mêmes.</p>
<p>Pareil pour les habitants de Homs en Syrie, ils aiment inventer des blagues.</p>
<p>Et comme l'émission s'appelle « un sourire d'espoir » il fallait que je vous parle des bienfaits de l'humour.</p>
<p>Grâce à l'humour tu peux débloquer des situations compliquées, ou résoudre des problèmes difficiles...</p>
<p>Mais aujourd'hui je ne vais pas vous raconter les blagues des égyptiens ni celles des syriens,</p>
<p>Je vais plutôt vous raconter comment le Prophète (bénédictions et paix soient sur lui) utilisait l'humour et la bonne plaisanterie pour détendre les gens autour de lui,</p>
<p>Par exemple le jour du Khandak (le fossé)</p>
<p>Quand la tribu de Qoraych et leurs alliés au nombre de 10.000 ont décidé d'attaquer les musulmans au nombre de 3.000,</p>
<p>Les musulmans se sont trouvés dans une situation difficile,</p>
<p>Ils ont du creuser un fossé pour les séparer de leurs ennemis,</p>
<p>Sous une grande chaleur,</p>
<p>Et souffrant tellement de faim que le Prophète (bpsl) serrait son ventre avec une pierre pour calmer la douleur...</p>
<p>Médine est une région rocailleuse, très pénible à creuser,</p>
<p>Un des compagnons qui s'appelait Soheib, creusait dans un rocher,</p>
<p>Il était fatigué, assoiffé et de la poussière était entrée dans son œil droit...</p>
<p>Le Prophète (bpsl) a remarqué que Soheib n'était pas bien, il fallait donc qu'il le détende dans ces conditions difficiles,</p>
<p>Soheib a pris une datte pour la manger,</p>
<p>Et le Prophète (bpsl) lui a dit <strong class="prophete">« Comment ça Soheib ? Tu manges une datte alors que tu as de la poussière dans l'œil ? »</strong></p>
<p>(Bien sûr il n y'a aucun rapport entre les deux, c'est juste une plaisanterie)</p>
<p>Soheib l'a compris et a répondu par une autre plaisanterie en disant « Non ô Messager de Dieu, je ne mâche que du côté gauche »</p>
<p>:)</p>
<p>Et les deux ont rit.</p>
<br>
<p>Toujours le même jour du khandak (le fossé)</p>
<p>Le Prophète (bpsl) a croisé un compagnon qui s'appelait Abou Oumama,</p>
<p>Son visage était devenu rose car il a passé beaucoup de temps à creuser sous le soleil...</p>
<p>Le prophète (bpsl) connaissait bien son prénom, mais pour le taquiner il lui a dit</p>
<p><strong class="prophete">« Bienvenue Abou al Ward »</strong> (en arabe ca signifie rose)</p>
<p>Abou Oumama lui a répondu « As-tu oublié mon Nom Ô Messager de Dieu ? Je suis Abou Oumama »</p>
<p>Le Prophète (bpsl) lui a répondu <strong class="prophete">« Non tu es Abou al Ward »</strong></p>
<p>Abou Oumama a encore dit « Je suis Abou Oumama Ô Messager de Dieu »</p>
<p>Le Prophète (bpsl) lui a dit <strong class="prophete">« Si tu voyais ton visage et ce que le soleil lui avait fait, tu saurais que tu es Abou Al Ward »</strong></p>
<p>:)</p>
<p>Et Abou Oumama a rigolé, ce soleil qui était la source de sa souffrance était devenu une chose qui le fait rire.</p>
<p>Et encore le même jour du khandak (le fossé)</p>
<p>Pourquoi ce jour spécialement ?</p>
<p>Parce que c'était un jour très pénible, donc le Prophète (bpsl) a multiplié les plaisanteries pour détendre les musulmans.</p>
<p>Donc le même jour le Prophète (bpsl) était avec Ali Ibn Abou Taleb,</p>
<p>Ils mangeaient des dattes,</p>
<p>Et à chaque fois que le Prophète (bpsl) mangeait une datte, il mettait le noyau devant Ali Ibn Abou Taleb...</p>
<p>Au bout d'un moment, Il y'avait plein de noyaux devant Ali ibn Abou Taleb,</p>
<p>Alors le Prophète (bpsl) l'a regardé en disant <strong class="prophete">« Ali As-tu mangé toutes ces dattes tout seul ? »</strong></p>
<p>(C'est possible que tu les ai toutes mangées sans m'en donner un peu...)</p>
<p>Ali Ibn Abou Taleb a répondu « Et toi Ô Messager de Dieu as-tu mangé les dattes avec leurs noyaux ? »</p>
<p>:)</p>
<br>
<p>Je tenais à vous raconter ces trois situations du Prophète (bpsl) pour qu'on fasse la même chose,</p>
<p>Dans les situations difficiles, cherche une blague ou une plaisanterie pour détendre l'atmosphère...</p>
<p>Quand les temps sont durs,</p>
<p>En pleine crise économique,</p>
<p>Cherche la plaisanterie qui fera rire les gens, qui leur fera du bien...</p>
<p>Si tout le monde est triste et déprimé, ne le soit pas aussi !</p>
<p>Si dans la maison tout le monde est de mauvaise humeur, sois celui qui les détend avec ton humour...</p>
<p>Pareil dans la rue, ou dans ton travail, ou avec tes voisins,</p>
<p>Sois positif et dessine un sourire d'espoir chez eux :)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
