                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui raconte l'histoire d'un jeune appelé Abdallah,</p>
<p>Abdallah devait avoir 16ans,</p>
<p>Il était avec sa famille en Alexandrie,</p>
<p>Ils passaient la journée à la plage,</p>
<p>Abdallah est parti se baigner,</p>
<p>Ce jour là un tourbillon s'est formé dans l'eau,</p>
<p>Et il n'a pas su réagir, ou il ne savait pas bien nager...</p>
<p>Il a commencé à se faire aspirer,</p>
<p>Il criait,</p>
<p>Il voyait la mort devant ses yeux...</p>
<p>Un homme (pêcheur) l'a entendu, il a sauté dans l'eau,</p>
<p>Il a nagé... nagé... nagé...</p>
<p>Il l'a sauvé,</p>
<p>Il l'a sorti de l'eau,</p>
<p>Il l'a allongé sur le sable,</p>
<p>Abdallah était entre la vie et la mort,</p>
<p>Mais Dieu merci, il a été sauvé.</p>
<p>L'homme qui l'a sorti était essoufflé, il était à bout de force,</p>
<p>La famille d'Abdallah le remerciait,</p>
<p>Mais ce pêcheur n'était pas un homme normal, il était plein de sagesse,</p>
<p>Il leur a dit une phrase spéciale...</p>
<p>Il a regardé Abdallah en disant « J'espère que ta vie méritait d'être sauvée »</p>
<p>Ce n'est pas une phrase normale n'est-ce pas ?</p>
<p>Ecoute là une deuxième fois « J'espère que ta vie méritait d'être sauvée »</p>
<p>Ce n'est pas une insulte,</p>
<p>Cet homme lui posait la question,</p>
<p>Et laissez-moi vous poser la même question,</p>
<p>Si tu étais à la place d'Abdallah, est-ce que tu vois que ta vie mérite d'être sauvée ?</p>
<p>Tu fais quoi d'utile dans la vie ?</p>
<p>Je vais te poser quelques questions pour te faire réfléchir...</p>
<p>Est-ce que ta vie a une grande valeur ?</p>
<p>Est-ce que les gens autour de toi ont peur que tu partes ?</p>
<p>Est-ce que tu as un but dans la vie ?</p>
<p>Et est-ce que tu as peur de partir avant d'avoir atteint ce but ?</p>
<p>Moi personnellement j'ai un but, et je demande à Dieu d'étendre ma vie au moins jusqu'à ce que mon but soit atteint...</p>
<p>J'aimerai participer à la renaissance de notre pays, et à la réussite des musulmans.</p>
<p>Est-ce que toi aussi tu as un but que tu aimerais atteindre avant de mourir ?</p>
<p>Est-ce que ta vie est importante ?</p>
<p>Est-ce que ta vie mérite qu'on abandonne tout, et qu'on vienne en courant pour la sauver ?</p>
<p>As-tu compris la phrase de ce pêcheur ?</p>
<p>Et imagine si tu te noyais comme Abdallah « Non...Non... je ne veux pas encore partir... J'ai encore de grandes choses à faire...Les autres ont besoin de moi... »</p>
<p>Ou alors « Les jours pour moi se suivent et se ressemblent... Ma vie est banale... »</p>
<p>Il y'a une très belle phrase qui dit « Si tu n'ajoutes rien à la vie, tu deviens un poids pour elle »</p>
<p>Une phrase de Moustapha Sadek El Rafei, un grand intellectuel égyptien,</p>
<p>Ecoute-là encore une fois « Si tu n'ajoutes rien à la vie, tu deviens un poids pour elle »</p>
<p>Est-ce que tu es un poids et un fardeau pour la vie ?</p>
<p>Ou est-ce que tu es vraiment utile à ton pays et à la société,</p>
<p>Tu fais le bien,</p>
<p>Tu aides les autres,</p>
<p>Tu partages tes connaissances,</p>
<p>Tu inventes des choses,</p>
<p>Tu donnes du bonheur autour de toi...</p>
<p>Je t'en prie donne à la vie, n'attend pas que ca soit elle qui te donne...</p>
<p>Et dessine un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
