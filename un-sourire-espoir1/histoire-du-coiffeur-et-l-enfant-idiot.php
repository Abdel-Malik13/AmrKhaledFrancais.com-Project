                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle des gens qui se croient malins... Mais qui se font avoir à la fin.</p>
<p>Ou des gens qui sous-estiment l'intelligence des autres.</p>
<p>Vous savez que c'est un problème chez beaucoup de personnes ? Une sorte d'arrogance...</p>
<p>Il voit toujours l'autre comme moins intelligent que lui, alors que c'est le contraire à la fin.</p>
<p>Je vais vous raconter une histoire drôle :</p>
<p>(C'est une histoire vraie d'ailleurs)</p>
<p>Elle s'est passée dans un village,</p>
<p>Dans ce village vivait un petit garçon que les gens sous-estimaient,</p>
<p>Il y'avait aussi un coiffeur qui tenait un salon, il était du genre prétentieux...</p>
<p>Imaginez cette scène : Le coiffeur coupait les cheveux d'un client,</p>
<p>Le coiffeur voulait se faire remarquer...</p>
<p>Le petit garçon passait devant le salon de coiffure,</p>
<p>Alors le coiffeur a dit à son client « Je vais te montrer l'enfant le plus stupide au monde, et je vais te le prouver ! » puis il a éclaté de rire...</p>
<p>Il a appelé le petit garçon « Viens voir par ici »</p>
<p>L'enfant est entré...</p>
<p>Le coiffeur a mis dans une main 25 centimes, et dans l'autre main 5 euros...</p>
<p>Puis lui a demandé « Tu vas prendre ce qu'il y'a dans ma main droite (25 centimes) ou ce qu'il y'a dans ma main gauche (5 euros) ? Qu'est ce que tu choisis ?»</p>
<p>L'enfant a regardé les deux mains... Il a pris les 25 centimes, puis il est parti...</p>
<p>Le coiffeur était mort de rire, il a dit à son client « Je t'avais bien dit n'est ce pas ? Cet enfant n'apprend jamais... Chaque fois il fait le même mauvais choix... Quel idiot ! »</p>
<p>Le client est sorti du salon en pensant que cet enfant est tout simplement stupide...</p>
<p>Sur le chemin il l'a croisé en train de manger une glace,</p>
<p>Cet homme par curiosité est parti lui demander « Pourquoi tu prends les 25 centimes mon chéri... Pourquoi tu ne prends pas les 5 euros ? »</p>
<p>Le petit garçon lui a répondu « Car le jour où je prendrai les 5 euros, le jeu sera terminé »</p>
<p>Quelqu'un a compris ?</p>
<p>Cet enfant a compris que le coiffeur le prenait pour un imbécile,</p>
<p>Et cet enfant a compris que le coiffeur aimait se faire remarquer par ses clients,</p>
<p>Donc s'il prenait les 5 euros, le coiffeur n'allait plus le rappeler...</p>
<p>Le coiffeur se croyait très malin, mais en réalité le petit garçon était 100 fois plus malin que lui.</p>
<p>Chaque fois il prenait les 25 centimes, car une petite somme régulière vaut mieux qu'une grosse somme occasionnelle.</p>
<p>Il laissait le coiffeur jouer au malin et allait manger une glace pour se rafraîchir :)</p>
<p>Parfois on pense qu'on est les seuls intelligents, et que les autres ne comprennent pas grand-chose...</p>
<p>Je t'en prie ne sous-estime jamais quelqu'un de plus petit que toi,</p>
<p>Ni tes employés,</p>
<p>Ni même tes enfants,</p>
<p>Parfois on commet des fautes, et nos enfants les comprennent,</p>
<p>On croit se moquer du petit de 5 ans, alors qu'il maîtrise le téléphone portable et internet... Il a déjà de l'avance sur nous ;)</p>
<p>A tous les parents : Pour garder un sourire d'espoir, ne sous-estimez pas vos enfants, car peut-être qu'ils font durer le jeu exprès sans que vous vous en rendiez compte ;)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
