                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>J'ai une chose importante à vous dire aujourd'hui,</p>
<p>Les amis sont ce qui influence le plus une personne,</p>
<p>Dis moi qui sont tes amis, je te dirai si tu vas réussir dans la vie ou pas...</p>
<p>Dis moi qui sont tes amis, je te dirai si tu vas te rapprocher de Dieu ou pas...</p>
<p>Dis moi qui sont tes amis, je te dirai si tu seras un bon fils ou une bonne fille pour tes parents ou pas...</p>
<p>3 choses que les mauvaises fréquentations influencent,</p>
<p>Ce n'est pas pour te faire la morale,</p>
<p>C'est après l'expérience de toute une vie que je te le dis,</p>
<p>Je te jure par Dieu c'est la vérité</p>
<p>Les amis ont une énorme influence dans 3 choses :</p>
<p>1- Ta relation avec tes parents</p>
<p>2- Ta relation avec Dieu</p>
<p>3- Ta relation avec toi-même, et donc ta réussite dans la vie</p>
<p>Et la vie se résume dans ces trois choses, du coup tes amis ont une influence sur ta réussite tout court...</p>
<p>Si tu veux connaitre une personne, regarde qui sont ses amis,</p>
<p>Si tu veux savoir si une personne ira loin ou pas, regarde qui sont ses amis,</p>
<p>Regarde les comportements et les centres d'intérêts de ses amis... Et tu comprendras vite.</p>
<br>
<p>Les mauvaises fréquentations sont ce qu'il y'a de plus dangereux dans la vie,</p>
<p>Il m'est arrivé une histoire avec un jeune homme il n'y a pas longtemps...</p>
<p>C'est un jeune homme plein de bonté,</p>
<p>Mais sa vie tombait en ruines...</p>
<p>A cause de ses amis,</p>
<p>Il vivait en Suisse,</p>
<p>Il est d'origine arabe,</p>
<p>Mais il était très égaré,</p>
<p>Pas un seul péché qu'il n'a pas fait...</p>
<p>Sa vie était devenue sombre... Des fautes suivies de fautes... Suivies d'autres fautes...</p>
<p>Des péchés, et des échecs, et des problèmes avec ses parents...</p>
<p>Très éloigné de Dieu...</p>
<p>Sa vie tombait en ruines,</p>
<p>Puis d'un coup il a décidé de changer son entourage,</p>
<p>(Je l'ai connu personnellement il y'a un mois)</p>
<p>Il a décidé de changer d'amis,</p>
<p>Mais comment faire alors qu'il est très proche d'eux et qu'il vit parmi eux ?</p>
<p>Il a décidé de quitter la Suisse,</p>
<p>Il s'est dit « Il faut que je change d'endroit, pour connaitre de nouvelles personnes qui m'aideront à me réconcilier avec mes parents, et me rapprocher de Dieu »</p>
<p>Il m'a appelé,</p>
<p>Je le connaissais personnellement depuis longtemps, mais il vivait dans un monde complètement différent du mien...</p>
<p>Il m'a dit « Est-ce que je peux venir en Egypte, pour passer un mois ou deux ou trois...Ma famille est aisée financièrement, je peux louer une maison, mais je veux que tu me mettes en contact avec des jeunes de l'association (les bâtisseurs de la vie) je veux passer du temps avec eux, et peut-être qu'après un mois ou deux ma vie changera... »</p>
<p>Entre nous, j'ai eu peur au début,</p>
<p>Il va venir dans un univers qu'il ne connait pas,</p>
<p>Comment il va faire pour s'acclimater dans une autre société, une autre mentalité... etc</p>
<p>Mais il semblait qu'il avait une énorme détermination « Je veux changer... je veux changer ! »</p>
<p>Mohamed Walid est son prénom,</p>
<p>Je lui ai dit « D'accord Mohamed tu peux venir »</p>
<p>Le jeune homme est venu, et aujourd'hui pendant que j'enregistre l'émission ça fait un mois qu'il est ici.</p>
<p>Et il est devenu une personne complètement différente,</p>
<p>Il pense positif,</p>
<p>Il est devenu très proche de sa maman,</p>
<p>Il fait ses prières à l'heure,</p>
<p>Il a arrêté de fumer et de boire,</p>
<p>Mohamed a l'air plus intelligent,</p>
<p>Mohamed a l'air plein de talents,</p>
<p>Mohamed a un grand potentiel en lui,</p>
<p>Je pensais qu'il allait venir pour que je l'aide, et au final c'est lui qui m'aide...</p>
<p>Mohamed a entièrement changé,</p>
<p>Par une simple décision, celle de changer ses mauvaises fréquentations,</p>
<p>Mohamed a été fort et courageux le jour où il a dit « Stop ! Je veux changer et réussir dans la vie »</p>
<br>
<p>Si tu es dans sa même situation, tu peux faire la même chose que Mohamed,</p>
<p>Si tu ne peux pas changer d'endroit, va au moins voir des personnes qui peuvent t'aider,</p>
<p>Des gens qui peuvent te conseiller et te booster dans ta vie professionnelle,</p>
<p>Tes amis sont la clé pour que tu dessines un sourire d'espoir dans ta vie,</p>
<p>Que ta relation avec tes parents s'améliore,</p>
<p>Et que tu te rapproches de Dieu.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
