                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est une devinette...</p>
<p>Qui gagne le plus dans ce bas monde, le gentil ou le malin ?</p>
<br>
<p>Certains diront le gentil... D'autres le malin...</p>
<p>Pour vous aider je vais d'abord expliquer qui est le gentil et qui est le malin :</p>
<br>
<p>Le gentil est celui qui marche tout droit,</p>
<p>Il fait bien son travail puis c'est tout... Il ne zigzague pas...</p>
<br>
<p>Et quand je dis le malin, ca ne signifie pas l'arnaqueur,</p>
<p>Mais jute celui qui connait les astuces et prend les raccourcis...</p>
<br>
<p>A votre avis qui des deux gagne le plus dans ce bas monde ?</p>
<br>
<p>Beaucoup diront le malin,</p>
<p>Moi je dis non, le gentil gagne beaucoup plus à la fin.</p>
<p>Je vais vous raconter une histoire :</p>
<p>Quand je vivais en Angleterre, j'ai connu deux étudiants qui préparaient leur doctorat...</p>
<p>C'est donc très important pour leur avenir,</p>
<p>Mais ca demande beaucoup de dépenses...</p>
<p>Tous les deux avaient besoin d'une bourse d'études,</p>
<p>Le premier a choisi de se concentrer sur ses études, obtenir de bonnes notes pour décrocher la bourse...</p>
<p>Le gentil s'est donc dit qu'il allait réviser, travailler dur... Puis c'est tout.</p>
<p>Le deuxième lui a cherché un chemin plus court, il a cherché une fille égyptienne de nationalité britannique...</p>
<p>Le malin se disait qu'il allait d'abord épouser cette fille pour obtenir la nationalité britannique... Puis l'amour viendra avec le temps...</p>
<br>
<p>Les jours ont passé...</p>
<p>Et en apparence le malin a gagné,</p>
<p>Car le gentil n'a pas réussi à avoir sa bourse et il est rentré en Egypte...</p>
<p>Et le malin a lui réussi à obtenir sa bourse et a poursuivi ses études...</p>
<p>Les deux étaient amis, je les voyais toujours ensemble,</p>
<p>Et le malin se moquait de son copain en lui disant « Tu as révisé, tu t'es cassé la tête... Puis à la fin tu es rentré en Egypte... »</p>
<br>
<p>Mais non !</p>
<p>Savez-vous pourquoi le gentil gagne à la fin ?</p>
<p>Parce que Dieu est toujours du coté des gens droits !</p>
<p>Dieu les récompense et leur rembourse ce qu'ils ont perdu...</p>
<p>C'est une histoire vraie que j'ai vu de mes propres yeux.</p>
<br>
<p>Le temps s'est écoulé,</p>
<p>Et le plan de notre ami le malin... a été découvert par sa fiancée... qu'il tenait plus à sa nationalité qu'à elle...</p>
<p>Alors elle l'a laissé tomber, et sa bourse aussi... Il a du rentrer en Egypte.</p>
<br>
<p>Mais le gentil qui a choisi de tout donner pour obtenir ses notes... L'université a fini par le rappeler car une place s'est libérée.</p>
<p>A la fin ses révisions et ses notes ont payé...</p>
<br>
<p>C'est toute l'histoire de la vie...</p>
<p>Au début le malin semble toujours gagner,</p>
<p>Mais à long terme je vous jure que le gentil est celui qui gagne.</p>
<p>Parce que Dieu est de son coté,</p>
<p>Parce que son intention est bonne,</p>
<p>Parce qu'il a dépensé son énergie dans le bon sens,</p>
<p>Parce qu'il n'a pas perdu son temps en allant à gauche et à droite...</p>
<br>
<p>Je vous le jure,</p>
<p>Pour avoir un vrai sourire d'espoir,</p>
<p>Ai une bonne intention,</p>
<p>Travaille dur,</p>
<p>Et Dieu sera avec toi.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
