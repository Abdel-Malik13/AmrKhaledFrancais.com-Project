                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui te donne une idée,</p>
<p>Si tu appliques cette idée dans ta vie, tu avanceras toujours en souriant :)</p>
<p>Réfléchis avant de juger !</p>
<p>Ca veut dire quoi ?</p>
<p>Ca veut dire : quand tu vois une chose devant toi, ne te précipite pas,</p>
<p>N'attaque pas d'entrée en disant « L'autre est dans l'erreur, ca ne se fait pas »</p>
<p>Prends le temps de réfléchir mon cher...</p>
<p>Je vais vous raconter une histoire drôle,</p>
<p>C'est une histoire vraie d'ailleurs :</p>
<p>Une dame attendait son train à la gare,</p>
<p>Ca s'est passé en Angleterre,</p>
<p>La dame avait faim, elle est partie acheter des biscuits et un sachet de pop-corn...</p>
<p>Elle s'est assise sur le banc et elle a commencé à manger,</p>
<p>Une jeune fille est venue s'asseoir sur le même banc,</p>
<p>Et là surprise !</p>
<p>La fille mettait sa main dans le sachet de pop-corn, et mangeait sans avoir demandé l'autorisation...</p>
<p>La dame allait devenir folle ;)</p>
<p>Elle se disait au fond d'elle « C'est quoi ce culot ? »</p>
<p>La fille continuait à manger du sachet de pop-corn...</p>
<p>La dame avait du mal à y croire, son visage devenait rouge de colère,</p>
<p>Elle avait envie de taper la jeune fille ou lui crier dessus « C'est quoi cette mauvaise éducation ? »</p>
<p>La fille la regardait avec le sourire, et continuait à mettre sa main dans le sachet de pop-corn...</p>
<p>La dame éloignait le sachet de la fille,</p>
<p>La fille s'approchait et mettait encore sa main dedans...</p>
<p>La dame pensait « C'est quoi ca ? Je n'ai jamais vu une chose pareille, la nouvelle génération est si mal élevée que ca ? »</p>
<p>Il restait des miettes au fond du sachet, la fille les a mangées avec le sourire... Puis elle est partie :)</p>
<p>La dame est montée dans son train en étant furieuse « Je n'aurai pas du me taire, j'aurai du l'insulter ! »</p>
<p>Elle s'est assise sur son siège dans le train,</p>
<p>Et en ouvrant son sac à main elle a trouvé le sachet de pop-corn en entier...</p>
<p>L'autre sachet de pop-corn appartenait en réalité à la fille :)</p>
<p>Elle ne faisait que manger ce qui lui appartenait.</p>
<p>Mais la colère a empêché la dame de réfléchir et de vérifier son sac à main.</p>
<p>La fille était innocente depuis le début :)</p>
<p>Mon cher réfléchis avant de juger,</p>
<p>Garde ton calme,</p>
<p>Avant de t'énerver et t'emporter,</p>
<p>Avant de te disputer...</p>
<p>Qu'il est beau le verset du noble Coran, qui parle du Prophète Salomon</p>
<p><strong>{ Puis il passa en revue les oiseaux et dit: «Pourquoi ne vois-je pas la huppe? est-elle parmi les absents? }</strong></p>
<p>Sourate 27 - An-Naml : Les Fourmis (verset 20)</p>
<br>
<p>Salomon s'est d'abord demandé :</p>
<p>« Pourquoi ne vois-je pas la huppe? »</p>
<p>C'est peut-être moi qui ne la vois pas...</p>
<p>Et c'est ensuite il a posé la question :</p>
<p>« Est-elle parmi les absents? »</p>
<br>
<p>Comme Salomon avant de dire « Je vais engueuler et punir »</p>
<p>D'abord réfléchis et demande,</p>
<p>Ne fais pas comme la dame de l'histoire :)</p>
<p>Tu vivras avec un sourire, et tu vivras avec de l'espoir tant que tu garderas ton calme et que tu prendras le temps de réfléchir avant d'agresser les gens et les critiquer.</p>
<p>A la fin tu trouveras le sachet de pop-corn en entier dans ton sac :)</p>
<br>
<p>Que Dieu vous rende heureux,</p>
<p>Et qu'il dessine un sourire d'espoir dans nos vies.</p>
<br>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
