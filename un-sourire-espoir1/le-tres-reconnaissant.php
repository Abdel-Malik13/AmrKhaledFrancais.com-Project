                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle d'un des 99 noms de Dieu :</p>
<p><strong>Ash-shakour { Le Très-Reconnaissant }</strong></p>
<p>Que veut dire <strong>{ Le Très-Reconnaissant }</strong>?</p>
<p>Si tu fais une bonne chose, c'est obligé qu'il te remercie pour !</p>
<p>Aussi simple que ca...</p>
<p>Je jure que si tu nettoie de la poussière sur une route, il te remerciera pour ton geste !</p>
<p>Si tu allais écraser un insecte puis tu t'es retenu en disant «Je le laisse en vie, il ne m'a rien fait » Dieu te remerciera pour !</p>
<p>Si tu embrasses la main de ta maman il te remerciera pour !</p>
<p>Si tu donnes une datte à un pauvre, ou même 5 centimes... Il te remerciera pour !</p>
<p>Si tu souris au visage d'un être humain... Il te remerciera pour !</p>
<p><strong>{ Le Très-Reconnaissant }</strong> !</p>
<p>Il est impossible que tu fasses une bonne chose sans qu'il te remercie pour !</p>
<p>Vous imaginez ce que ce nom de Dieu produira dans vos vies ?</p>
<p>Une ambition sans limite...</p>
<p>Pourquoi une ambition ?</p>
<p>Car il te récompensera pour chaque bonne action que tu feras,</p>
<p>Imagine le nombre de bonnes actions que tu peux faire en 24h ?</p>
<p>Par exemple en ce moment j'enregistre cette émission dans le studio, avec l'intention d'aider les gens,</p>
<p>Je jure qu'il me remerciera pour cette émission, tant que mon intention est pour lui !</p>
<p>Puis en quittant le studio je téléphonerai à ma maman pour prendre de ses nouvelles... Il me remerciera pour !</p>
<p>Ensuite à chaque fois que je ferai une bonne action, il me remerciera,</p>
<p>Et si j'en fais encore plus ?</p>
<p>Il me remerciera encore plus !</p>
<p>Fais bien attention, <strong>{ Le Très-Reconnaissant }</strong> ne te remerciera jamais à la hauteur de ton acte,</p>
<p>Son nom n'est pas Ash-shakir (Le reconnaissant)...</p>
<p>Son nom est Ash-shakour <strong>{ Le Très-Reconnaissant }</strong> !</p>
<p>Il multiplie sans limite ta récompense !</p>
<p>Les arabes utilisent l'expression une bête shakour,</p>
<p>Pour une vache par exemple, qui mange peu mais donne beaucoup de lait,</p>
<p>Tu lui donnes un peu d'herbes, elle te rend beaucoup de lait...</p>
<p>Les arabes utilisent aussi l'expression une terre shakour,</p>
<p>Si tu l'arroses avec un peu d'eau, elle te donne beaucoup de plantes ou légumes...</p>
<p>Ce nom d'Allah Ash-shakour, si tu fais un effort, il multiplie et re-multiplie ta récompense,</p>
<p>Imagine l'ambition que ce nom te donne ?</p>
<p>Si tu dis : Après avoir écouté cet épisode je ferai encore plus de bien !</p>
<p><strong>{ Le Très-Reconnaissant }</strong> multipliera ta récompense...</p>
<p>Alors j'en ferai encore et encore !</p>
<p>Il te multipliera ta récompense encore et encore !</p>
<p>Mais jusqu'à quand ?</p>
<p>Jusqu'à l'infini !</p>
<p>Alors que dis-tu de faire plus de bien dans ta vie ?</p>
<p>Les 99 noms de Dieu sont magnifiques !</p>
<p>Ils te motivent à réussir dans la vie,</p>
<p>Ils te poussent à faire le bien et lancer de grands projets...</p>
<p>Le technicien qui enregistre le son de cette émission,</p>
<p>Si son intention est d'être utile aux gens, faire en sorte que ma voix soit enregistrée avec la meilleure qualité... C'est obligé que Dieu le récompense pour son travail !</p>
<p>Quelqu'un qui m'écoute en ce moment, et qui décide de prendre son téléphone et d'appeler son papa ou sa maman... Je jure que <strong>{ Le Très-Reconnaissant }</strong> le remerciera pour !</p>
<p>Le Prophète (bénédictions et paix soient sur lui) a dit dans une parole :</p>
<p><strong class="prophete">« Allah a pardonné à une prostituée qui, en passant près d'un puits, vit un chien haletant tourner autour, peu s'en fallait qu'il ne meure de soif. Elle ôta alors sa chaussure, la fit attacher à son foulard et la remplit d'eau afin de l'abreuver. Par ce geste, Allah lui pardonna »</strong></p>
<p>Remarquez c'est une femme qui exerce le pire métier du monde...</p>
<p>Si elle se présente devant mille savants, aucun d'eux ne lui pardonnera !</p>
<p>Mais Dieu est très reconnaissant,</p>
<p>Il a vu dans le cœur de cette femme de l'humanité, alors il lui a pardonné.</p>
<p>Il a remercié le geste d'une prostituée...</p>
<p>Alors que dire de moi ou toi ?</p>
<p>Son nom est <strong>{ Le Très-Reconnaissant }</strong> mes chers !</p>
<p>Alors elle est où la motivation ?</p>
<p>Elle est où l'ambition ?</p>
<p>Faisons encore plus de bons gestes,</p>
<p>Multiplions le bien,</p>
<p>Et nous verrons comment <strong>{ Le Très-Reconnaissant }</strong> nous remerciera !</p>
<p>Un très beau sourire d'espoir nous attend.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
