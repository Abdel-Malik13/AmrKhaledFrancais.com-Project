                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui est un conseil pour les filles,</p>
<p>Mais je vais le commencer par une histoire très lointaine que j'ai vue dans un documentaire animalier il y'a six mois...</p>
<p>Ils avaient filmé des hérissons,</p>
<p>Ces petits animaux avec plein d'épines sur leur corps,</p>
<p>Ils étaient en grand nombre dans un endroit avec de la neige...</p>
<p>Ils avaient forcément très froid,</p>
<p>Pour se réchauffer ils n'avaient pas d'autre choix que de se grouper...</p>
<p>Les hérissons ont commencé à se rapprocher les uns des autres,</p>
<p>Mais à cause de leurs épines ils se blessaient les uns les autres,</p>
<p>Aie...Aie...Aie...</p>
<p>Ils avaient mal donc ils s'éloignaient...</p>
<p>Puis ils avaient encore froid,</p>
<p>Donc ils se rapprochaient encore les uns des autres...</p>
<p>Et même problème ils se faisaient mal,</p>
<p>A ce moment ils ont changé de stratégie,</p>
<p>La caméra les a filmés au ralenti,</p>
<p>Ils approchaient les uns des autres, mais d'une façon calculée...</p>
<p>Ca veut dire petit à petit,</p>
<p>Jusqu'à atteindre une distance intermédiaire, où ils ont un peu de chaleur,</p>
<p>Mais sans se toucher les uns les autres, et ainsi éviter de se blesser...</p>
<p>Les hérissons ont réussi à trouver la bonne distance, et grâce à leur nouvelle technique ils avaient la chaleur sans les blessures :)</p>
<br>
<p>En voyant cette stratégie des hérissons, je tenais à donner un conseil aux filles :</p>
<p>Soyons honnêtes,</p>
<p>Beaucoup de filles ont envie de se marier,</p>
<p>Et le rêve et l'espoir de se marier préoccupe toutes les filles, et c'est tout à fait normal.</p>
<p>C'est le droit de chaque fille, et avec l'âge elle pense que les occasions se font de plus en plus rares...</p>
<p>Mais la précipitation, ou l'impatience donne parfois lieu à un rapprochement non calculé... Qui peut faire mal... Comme des épines dans le cœur...</p>
<p>Je vous jure que je vous raconte tout ça par amour.</p>
<p>Je te parle comme ma sœur, et j'ai peur pour toi,</p>
<p>Tu veux te marier, et c'est ton droit de vouloir te marier,</p>
<p>Mais j'ai peur que ce désir de te marier te pousse à trop te rapprocher de quelqu'un, sans qu'il soit bien calculé...</p>
<p>Que tu te rapproches d'un seul coup d'un collègue au travail,</p>
<p>Ou d'un camarade de classe,</p>
<p>Et que tu te retrouves soudainement avec des épines et des blessures dans ton cœur...</p>
<p>Mais alors quelle est la solution puisque tu as besoin de te marier ?</p>
<p>La solution c'est un rapprochement calculé comme les hérissons,</p>
<p>C'est fixer la limite,</p>
<p>Le voir jusqu'à quand ?</p>
<p>L'écouter jusqu'à quand ?</p>
<p>A quel moment en parler à ma famille ?</p>
<p>Quel est le rôle de mes amis dans cette histoire ?</p>
<p>Avoir hâte de te marier, ne doit pas pousser à un rapprochement rapide qui blesse, et qui ruine ton avenir...</p>
<p>Un conseil précieux à toutes les filles,</p>
<p>Inspirez vous de la technique des hérissons,</p>
<p>Le rapprochement calculé est la meilleure façon de trouver le mari idéal,</p>
<p>Celui qui te donnera un sourire d'espoir mais sans blessures et sans souffrances.</p>
<p>Que Dieu te protège et te rende heureuse.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
