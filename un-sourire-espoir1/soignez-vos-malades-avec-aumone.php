                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>Il y'à une parole du Prophète (paix et bénédictions soient sur lui) dans lequel il dit :</p>
<p><strong class="prophete">« Soignez vos malades avec l'aumône »</strong></p>
<br>
<p>L'aumône peut aider à soigner les gens,</p>
<p>Tu me diras qu'il n'y a pas de preuve à ca,</p>
<p>Qu'il n'y a pas de rapport logique entre l'aumône et la guérison...</p>
<p>Mais le Prophète (pbsl) l'a dit.</p>
<br>
<p>Je vais vous raconter une histoire vraie mes chers,</p>
<p>C'est la personne à qui c'est arrivé qui me racontait</p>
<p>Que son fils a commencé par montrer des signes de fatigue,</p>
<p>Son fils était encore jeune, mais il ne pouvait plus courir avec ses copains...</p>
<p>Il l'a donc emmené consulter des médecins,</p>
<p>Et très mauvaise surprise,</p>
<p>Son fils avait un cancer,</p>
<p>Vous imaginez si votre enfant de 7 ou 8ans est atteint d'un cancer...</p>
<p>Le père me racontait :</p>
<p>« Au milieu de cette tourmente, il m'est arrivé une situation très surprenante...</p>
<p>En marchant un soir dans la rue, j'ai aperçu une vieille femme fouiller dans les poubelles,</p>
<p>C'est très malheureux comme scène à voir,</p>
<p>Elle cherchait quelque chose à manger pour ses enfants...</p>
<p>Ca m'a énormément touché, alors je lui ai demandé pourquoi elle faisait ca,</p>
<p>Elle m'a expliqué qu'elle était veuve, et qu'elle élevait seule 4 orphelins...</p>
<p>J'ai demandé à cette femme de me suivre. »</p>
<p>Ce qu'on fait dans ce genre de situations, c'est qu'on sort de l'argent de notre poche et qu'on le donne à la personne...</p>
<p>Cet homme n'a pas fait ca,</p>
<p>Cet homme a fait quelque chose de formidable,</p>
<p>Il l'a emmenée chez son boucher,</p>
<p>Puis il lui a dit, chaque semaine cette femme viendra et vous lui donnerez des viandes et ceci et cela... et ne prenez jamais d'argent chez elle, je vous paye un mois à l'avance.</p>
<p>Puis il l'a emmenée chez son épicier, et pareil il lui a dit, chaque semaine cette femme viendra et vous lui donnerez de l'huile, du sucre, de la farine... etc et c'est moi qui vous réglera.</p>
<p>Puis il l'a emmenée chez son tailleur, et encore une fois il lui a expliqué que tous les 6 mois cette femme viendra prendre des vêtements pour elle et ses 4 enfants, et que c'était lui qui réglerait la facture.</p>
<p>Cet homme a donc organisé la vie de cette femme,</p>
<p>Puis lui a demandé « Avez-vous besoin d'autre chose madame ? »</p>
<p>Elle s'est mise à prier pour lui, et lui était en pleur pour son fils...</p>
<p>Il me racontait :</p>
<p>« 2 mois après ca, j'ai emmené mon fils faire d'autres analyses, le médecin m'a dit non je veux son frère jumeau...</p>
<p>Cet enfant est en pleine santé, donc c'est sûr que c'est son frère jumeau qui est venu la 1ère fois,</p>
<p>J'ai dit au médecin que je n'avais qu'un seul fils...</p>
<p>Ils ont refait les analyses,</p>
<p>Puis dans un autre hôpital...</p>
<p>L'enfant n'a rien, il est en pleine santé.</p>
<p>Il y'avait une erreur dans les premières analyses...</p>
<p>J'ai serré mon fils dans mes bras,</p>
<p>Et en rentrant j'ai pleuré,</p>
<p>J'ai remercié Dieu,</p>
<p>J'ai fais des prosternations,</p>
<p>Mais je me demandais ce qui était arrivé...</p>
<p>Jusqu'au jour où j'ai ouvert un livre et que je suis tombé sur cette parole du Prophète (pbsl)</p>
<p><strong class="prophete">« Soignez vos malades avec l'aumône »</strong></p>
<br>
<p>Comme si Dieu voulait m'expliquer ce qui s'était passé...</p>
<br>
<p>Ce sourire d'espoir est dédicacé à tous les gens qui sont malades,</p>
<p>Ou qui ont des malades dans leur entourage,</p>
<p>Aux parents qui ont peur pour leurs enfants,</p>
<p>Il y'à plein de nécessiteux et d'orphelins autour de vous,</p>
<p>Aidez-les, et Dieu vous soignera.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
