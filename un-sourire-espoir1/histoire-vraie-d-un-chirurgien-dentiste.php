                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>Aujourd'hui je veux vous raconter l'histoire d'un chirurgien dentiste,</p>
<p>Il est iraquien, mais il vit au Emirats,</p>
<p>Après avoir eu son diplôme il a donc immigré aux Emirats, puis il y a ouvert un cabinet dentaire,</p>
<p>Il s'appel Docteur Majd,</p>
<p>Quand je l'ai rencontré, il m'a dit « je veux te raconter mon histoire dans la vie, pour que tu la racontes à ton tour aux autres »</p>
<p>J'ai bien sur demandé quelle était son histoire,</p>
<p>Il m'a dit « Sois généreux envers ta famille, Dieu sera généreux envers toi... Si tu es généreux envers les gens, c'et obligé que Dieu soit généreux envers toi... C'est une règle que j'ai apprise dans la vie... Et c'est toute l'histoire de ma vie.</p>
<p>Dès que je me suis installé aux Emirats, j'ai ouvert un cabinet,</p>
<p>Le loyer était trop cher, et je n'avais pas beaucoup d'argent,</p>
<p>Mais je mettais tout ce que j'avais dans le loyer dans l'espoir que des clients allaient venir...</p>
<p>Mais personne ne venait,</p>
<p>Un mois...puis deux mois... puis 3 mois... Et le cabinet restait vide...</p>
<p>Et l'argent que j'avais s'était épuisé dans le loyer, et il ne me restait plus rien...</p>
<p>J'ai donc commencé à emprunter pour payer le loyer...</p>
<p>Puis personne ne voulait plus m'emprunter...</p>
<p>Et le loyer s'accumulait,</p>
<p>Et je demandais au propriétaire de me laisser un peu plus de temps...</p>
<p>Jusqu'au jour où il m'a appelé pour me dire « attention, vous avez accumulé 18mille dirhams émirati de retard (environ 3800 €) , je vais transmettre votre dossier au tribunal, donc vous payez directement le tribunal, soit ils vont fermer votre cabinet et vous expulser du pays»</p>
<p>Le docteur Majd me racontait « le monde est devenu sombre à mes yeux, j'ai senti que tout était fini, et que ma vie l'était aussi...</p>
<p>Et à cet instant précis, mon téléphone sonnait, et c'était mon père qui m'appelait,</p>
<p>Ce jour là, il ne me restait plus que 2000 dirhams émirati pour vivre (environ 417 €)</p>
<p>Mon père m'a dit « rassure moi que tu vas bien mon fils»</p>
<p>J'ai dis à mon père « c'est surtout toi qui dois me rassurer que tu vas bien, comment va votre situation en Iraq ? »</p>
<p>Mon père m'a répondu « Notre situation est assez difficile, et en ce moment c'est très délicat pour nous... »</p>
<p>J'ai dis à mon père « Ne t'inquiètes pas papa, demain je vous transfert 2000 dirhams pour vous aider »</p>
<p>Il m'a dit « c'est vrai mon fils ? »</p>
<p>J'ai dis « Oui papa, je vous envoie l'argent demain »</p>
<p>J'ai donc raccroché, et je me suis dis 2000 dirhams ne servent à rien de toute façon dans ma situation, ils ne pourront pas effacer ma dette de 18 mille dirhams... L'important c'est que mon papa puisse régler ses problèmes...</p>
<p>J'ai donc transféré les 2000 dirhams en Iraq, en sachant au fond que c'était terminé pour moi...</p>
<p>Et le jour où je devais aller au tribunal, je suis d'abord parti voir le propriétaire, si jamais il pouvait être miséricordieux envers moi, et retarder la date...</p>
<p>J'ai donc quitté le cabinet, et sur le chemin, j'ai reçu un appel de la secrétaire,</p>
<p>Elle me disait « Dr. Majd, il faut revenir vite, le cabinet est plein à craquer »</p>
<p>Je lui ai dis « Ce n'est pas le moment de faire une blague... »</p>
<p>Elle a dit « Non je vous assure, revenez vous verrez par vous-même »</p>
<p>Je suis donc revenu au cabinet en courant, et j'ai trouvé plein d'enfants, plein de femmes, et un homme âgé parmi eux...</p>
<p>Cet homme était un émirati qui a ramené toute sa famille, se soigner le même jour,</p>
<p>Je les ai donc soigné un par un , j'ai travaillé toute la journée,</p>
<p>Et à la fin , il m'a demandé quel était le montant de tous les soins,</p>
<p>On a donc compté, et à notre grande surprise le montant était 22 mille dirhams,</p>
<p>Soit les 18 mille pour payer le loyer + les 2 mille que j'ai transféré à mon père + 2 mille comme si Dieu m'avait remboursé et donné la même somme en plus par générosité... »</p>
<p>Et depuis ce jour là, le cabinet du docteur Majd n'a pas cessé de grandir,</p>
<p>Vous pouvez voir cet homme pour avoir la preuve de ce que je vous raconte,</p>
<p>(cherchez Dr. Majd Naji sur google)</p>
<p>Il passe sur les grandes chaines du moyen orient,</p>
<p>Il donne des émissions, et est connu pour être le meilleur chirurgien dentiste au moyen orient,</p>
<p>A ce point ?</p>
<p>Oui mes chers,</p>
<p>Soyez généreux envers vos familles, et envers les pauvres... Dieu sera généreux envers vous !</p>
<p>A ce moment vous verrez un grand sourire d'espoir.</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
