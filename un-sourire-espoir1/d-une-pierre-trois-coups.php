                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>Le sourire d'espoir d'aujourd'hui est avec le Prophète (bénédictions et paix sur lui)</p>
<p>Une histoire de loyauté,</p>
<p>Qu'elle est belle la loyauté du Prophète Mohamed (bpsl) !</p>
<p>Je vous jure mes chers que vous allez aimer encore plus le Messager de Dieu après cette histoire :</p>
<p>Quelles sont les 3 personnes qui ont le plus aidé le Prophète (bpsl) dans sa vie ?</p>
<p>Les 3 qui ont été les plus présents à ses côtés ?</p>
<p>Je vais te les citer,</p>
<p>Son épouse Khadija (la mère des croyants),</p>
<p>Son oncle Abou Taleb (Qui n'a pas voulu devenir musulman)</p>
<p>Et les Ansâr (les gens de Médine, qui l'ont accueilli quand il a été contraint de quitter La Mecque)</p>
<p>Ce sont donc les personnes qui ont le plus soutenu le Prophète Mohamed (bpsl)</p>
<p>Sa femme Khadija est décédée lorsqu'il était encore à la Mecque,</p>
<p>Son oncle Abou Taleb est aussi décédé lorsqu'il était encore à la Mecque,</p>
<p>Et les Ansâr sont restés à ses côtés jusqu'à la victoire finale...</p>
<p>Le Prophète (bpsl) voulait rester loyal envers les 3,</p>
<p>10 ans après le décès de son épouse Khadija et son oncle Abou Taleb , il va les honorer...</p>
<p>Il y'a des hommes qui 2 mois après le décès de leurs épouses, cachent les photos pour les oublier et chercher une nouvelle femme :)</p>
<p>Le Prophète (bpsl) n'a jamais oublié les 3,</p>
<p>Il va le montrer le jour de la victoire... Le jour où les musulmans vont retourner à la Mecque...</p>
<p>Vous connaissez l'expression d'une pierre trois coups ?</p>
<p>Le Messager de Dieu va honorer les 3 dans une seule situation...</p>
<p>Les musulmans sont donc retournés à la Mecque, ils ont remporté la victoire,</p>
<p>Oussama Ibn Zayd un des compagnons est venu lui dire « Où vas-tu loger Ô Messager de Dieu ? »</p>
<p>Le Prophète (bpsl) avait toujours sa maison et celle de son épouse Khadija,</p>
<p>Le problème c'est que quand le Prophète (bpsl) a été contraint de quitter Médine, le fils d'Abou Taleb (son oncle) a confisqué la maison,</p>
<p>Il s'appelait Akil Ibn Abou Taleb (Le frère d'Ali Ibn Abou Taleb)</p>
<p>Akil n'était pas musulman,</p>
<p>Donc quand Oussama Ibn Zayd a demandé au Prophète « Où vas-tu loger Ô Messager de Dieu ? »</p>
<p>Il a répondu <strong class="prophete">« Et est-ce que Akil nous a laissé une maison... »</strong></p>
<p>(Il a confisqué ma maison et la maison de ma femme chérie Khadija...)</p>
<p>Oussama Ibn Zayd lui a répondu « Alors tu vas la lui reprendre Ô Messager de Dieu ! »</p>
<p>(Tu es en position de force, tu dois reprendre ce qui t'appartient)</p>
<p>Le Prophète (bpsl) lui a répondu <strong class="prophete">« Non, par loyauté envers son père Abou Taleb »</strong></p>
<p>(Le jour de ma victoire je ne vais pas déshonorer Abou Taleb après tout ce qu'il a fait pour moi... Même si son fils n'est pas musulman... et qu'il a pris ma maison de force...)</p>
<p>« Mais alors où vas-tu loger Ô Messager de Dieu ? »</p>
<p>(Sûrement à côté de la Ka'aba, la mosquée sacrée de la Mecque pour que les gens viennent te voir...)</p>
<p>Le Prophète (bpsl) a répondu <strong class="prophete">« Non, Mettez moi une tente pas loin de la tombe de Khadija »</strong></p>
<p>(Tu te souviens d'elle après tant d'années Ô Messager de Dieu... Mais sa tombe est loin de la ka'aba, c'est dans un endroit appelé Al-Awali... Mais pourquoi cette décision ?</p>
<p>Pour qu'elle partage le jour de victoire comme elle a partagé les jours difficiles... Même si elle n'est plus là)</p>
<p>Vous imaginez l'humanité et la beauté du Prophète Mohamed (bpsl)...</p>
<p>Son amour éternel, et son immense respect pour son épouse...</p>
<p>Mais alors pourquoi une tente ?</p>
<p>Il pouvait construire une maison à la Mecque et s'y installer...</p>
<p>Une tente car il comptait retourner à Médine,</p>
<p>Pourtant La Mecque était sa ville natale,</p>
<p>Il est retourné à Médine par loyauté envers les Ansâr, qui l'ont soutenu et aidé...</p>
<p>C'est pour cette raison que le Prophète (bpsl) a été enterré à Médine,</p>
<p>Chacun de nous aime être enterré dans sa ville,</p>
<p>Mais le Prophète (bpsl) par loyauté envers les habitants de Médine a choisi d'y retourner...</p>
<p>Quand on part visiter la tombe du Prophète (bpsl) à Médine c'est un signe éternel de sa loyauté,</p>
<br>
<p>Dans une seule situation, il a fait preuve de loyauté envers les 3 personnes qui l'ont le plus aidé dans sa vie...</p>
<p>1- Le Prophète (bpsl) a laissé sa maison à Akil par loyauté envers son oncle Abou Taleb.</p>
<p>2- Il s'est logé pas loin de la tombe de sa femme Khadija.</p>
<p>3- Il a choisi une tente plutôt que construire une maison même s'il est le chef victorieux, pour rassurer les Ansâr, et leur dire qu'il reviendra à Médine.</p>
<br>
<p>Un sourire d'espoir avec la loyauté du Prophète (bpsl)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
