                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est sur la façon de notre Prophète Mohamed (bénédictions et paix soient sur lui) de se comporter avec les gens,</p>
<p>Une façon pure, élégante, douce... Surtout face au gens qui lui font du mal.</p>
<p>Je vais vous raconter plusieurs histoires rapides :</p>
<p>Le Prophète (bpsl) était à la Mecque, et la tribu de qoraich étaient extrêmement gênés par son message, ne supportaient pas les musulmans... etc.</p>
<p>Ils ont décidé de changer le nom du Prophète (bpsl)</p>
<p>Son nom est Mohamed qui signifie « Loué » ou « Digne de louanges »</p>
<p>Ils ont décidé de ne plus l'appeler par son nom, mais par un autre nom qui signifie le contraire de Mohamed...</p>
<p>Comment le Prophète (bpsl) va réagir face à cette provocation ?</p>
<p>Et comment les compagnons vont réagir ?</p>
<p>Qoraich ont commencé à appliquer leur idée...</p>
<p>(...) est parti...</p>
<p>(...) est revenu ...</p>
<p>(...) a fait ...</p>
<p>Un nom très dur à entendre...</p>
<p>Les compagnons sont partis voir le Prophète (bpsl) en étant très atteints... Ils lui ont dit « Ô Messager de Dieu as-tu vu ce qu'ils ont fait ? »</p>
<p>Ils t'appellent par cet autre nom...</p>
<p>Le Prophète (bpsl) a dit en souriant <strong class="prophete">« Laissez-les, ils insultent une autre personne... Moi je m'appelle Mohamed »</strong></p>
<p>Ils parlent d'une autre personne.... Laissez-les dire ce qu'ils veulent, ils ne parlent pas de moi.</p>
<p>Et il a continué à le répéter aux compagnons jusqu'à ce qu'ils se soient calmés,</p>
<p>« C'est vrai qu'ils ne parlent pas de toi, ton nom est Mohamed »</p>
<p>Ce nom a disparu de l'histoire, et si je ne vous ai pas raconté cette histoire, elle serait morte et enterrée.</p>
<p>Et Mohamed est resté Mohamed.</p>
<p>Vous avez compris ce que je veux dire ?</p>
<p>Je veux dire que la renommée du Prophète (bpsl) est très élevée comme Dieu l'a dit dans le noble Coran :</p>
<p><strong>{ 1. N'avons-Nous pas ouvert pour toi ta poitrine?</strong></p>
<p><strong>2. Et ne t'avons-Nous pas déchargé du fardeau</strong></p>
<p><strong>3. qui accablait ton dos?</strong></p>
<p><strong>4. Et exalté pour toi ta renommée? }</strong></p>
<p>Sourate 94 - Ach-Charh : L'Ouverture</p>
<p>Ta renommée est exaltée dans toute l'humanité, et jusqu'au jour de la résurrection.</p>
<p>Chaque jour à l'heure de l'Adhan (l'appel à la prière), et sur la terre entière, on dit :</p>
<p>« J'atteste qu'il n'y a de vraie divinité hormis Allah »</p>
<p>Suivi de :</p>
<p>« J'atteste que Muhammed est le Messager de Allah »</p>
<p>Tu es cher Ô Messager de Dieu, tu es grand Ô Messager de Dieu !</p>
<p>Souvenez-vous toujours, c'est lui-même qui nous a montré comment répondre aux insultes quand il a dit <strong class="prophete">« En réalité ils insultent une autre personne... Moi je suis Mohamed »</strong></p>
<p>Mes chers, concentrez-vous surtout sur le fait d'être des musulmans dont le Prophète (bpsl) sera satisfait,</p>
<p>Mettez plutôt vos efforts dans le bon sens, pour montrer à quel point l'islam est une belle religion, et à quel point le Prophète (bpsl) est grand.</p>
<p>C'est la bonne façon de défendre le Messager de Dieu (bpsl) :</p>
<p>1- Montrer les valeurs et le bon comportement du Prophète (bpsl)</p>
<p>2- Réussir dans la vie en tant que musulmans</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
