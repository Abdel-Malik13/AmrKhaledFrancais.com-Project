                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle des grandes capacités, et de l'immense énergie qu'on a au fond de nous, mais que nous ignorons...</p>
<p>Savez-vous mes chers qu'au fond de nous on a un énorme potentiel, une force inimaginable, mais malheureusement nous ne les découvrons pas...</p>
<p>Et celui qui les découvre, réussit de plus en plus, atteint les sommets, gagne beaucoup d'argent, accomplit de grandes choses dans sa vie, et il obtient le respect et l'estime des autres...</p>
<p>Mes chers, on avance dans la vie avec seulement 5% de nos vraies capacités,</p>
<p>Que tu sois un homme ou une femme, imagine que tu as 100 unités d'énergie en toi, tu n'en utilises que 5 durant toute ta vie,</p>
<p>Mais alors où vont les 95% ?</p>
<p>Ils restent au fond de toi, mais tu ignores leur existence.</p>
<p>Je vais vous raconter une histoire:</p>
<p>On raconte que pendant La guerre de Bosnie-Herzégovine, les réfugiés fuyaient le chaos et l'horreur,</p>
<p>Ils devaient traverser des montagnes enneigées dans un froid extrême pour atteindre une zone sure,</p>
<p>Une famille composée d'un homme très âgé, de sa femme, et de leur petit fils, ont décidé de traverser...</p>
<p>Mais ils étaient affaiblis par l'âge, et emmener un enfant rend la traversée encore plus difficile,</p>
<p>Ils ont croisé un groupe de soldats qui avançaient dans le même sens,</p>
<p>Il leurs ont dit « S'il vous plait, emmenez-nous avec vous »</p>
<p>Un des chefs soldats leur a dit « Nous ne pouvons pas vous prendre sous notre responsabilité, par contre on peut porter l'enfant, et vous deux vous nous suivrez à notre rythme, mais si un de vous se fatigue on ne pourra pas s'arrêter pour l'attendre, nous sommes en mission et nous devons avancer vite »</p>
<p>Ils ont commencé à marcher, les soldats étaient bien sûr jeunes et forts physiquement...</p>
<p>Le vieil homme sentait la fatigue,</p>
<p>Son épouse résistait beaucoup mieux, mais son mari était à bout de force,</p>
<p>Il s'est arrêté au milieu de la route,</p>
<p>Il est tombé de fatigue,</p>
<p>Le chef soldat lui a dit « Nous sommes désolés, nous ne pouvons pas faire une pause pour vous attendre, nous allons continuer la route avec votre enfant et votre femme, mais nous ne pouvons rien faire pour vous »</p>
<p>Sa femme était intelligente, elle est partie chercher le petit enfant,</p>
<p>Elle l'a déposé devant lui en disant « Moi je continue la route avec les soldats, l'enfant reste avec toi, soit tu le portes, soit il meurt avec toi »</p>
<p>Imaginez la force de cette femme ?</p>
<p>Puis elle est partie avec les soldats sans se retourner,</p>
<p>2minutes et demi après, le vieil homme les a rejoint en portant l'enfant...</p>
<p>Il a sorti la force qui était en lui.</p>
<p>Et je vous jure que chacun de nous possède la même force,</p>
<p>Tout le monde a fini par traverser,</p>
<p>Et le vieil homme n'était pas seulement le premier arrivé, mais en plus il a porté l'enfant tout le reste du trajet.</p>
<p>On possède tous la même capacité au fond de nous, mais la paresse ne la laisse pas s'exprimer.</p>
<p>Je connais un coach sportif qui a demandé à un jeune de faire 20 pompes,</p>
<p>Le jeune a fait 20 pompes...</p>
<p>Le coach lui a demandé d'en rajouter encore 20,</p>
<p>Le jeune a répondu qu'il ne pouvait pas, il était épuisé...</p>
<p>Le coach l'a forcé à les faire...</p>
<p>Puis lui a demandé encore 20 autres,</p>
<p>Le jeune a répondu que c'était impossible, il en avait déjà fait 40...</p>
<p>Le coach l'a encore forcé,</p>
<p>Puis lui a demandé encore 20 autres,</p>
<p>Le jeune a encore répondu qu'il n'en pouvait plus, ses bras n'avaient plus d'énergie,</p>
<p>Le coach l'a encore forcé,</p>
<p>Il en a fait 80,</p>
<p>Puis 100,</p>
<p>Arrivé à 100 pompes le jeune est tombé, il a dit que ce n'était plus possible.</p>
<p>La différence entre 20pompes et 100pompes, est la même différence entre la force que tu crois posséder et la force cachée en toi.</p>
<p>On a un grand potentiel, il suffit juste de se motiver, et d'appuyer sur l'accélérateur encore et encore...</p>
<p>Tu finiras par réussir.</p>
<p>Et tu vivras heureux.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
