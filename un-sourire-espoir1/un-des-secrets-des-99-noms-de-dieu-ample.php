                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>Aujourd'hui je veux vous parler d'un nom des 99 noms de Dieu :</p>
<p>C'est un nom que j'aime beaucoup,</p>
<p>Ce nom est : <strong>« Al-Wasi' »</strong></p>
<p>(Traduction : L'Ample, le Vaste, l'Immense)</p>
<p>Peut-être que vous n'y avez jamais pensé... Ca signifie quoi L'Ample?</p>
<p>Ca signifie celui qui amplifie les choses pour ses serviteurs...</p>
<p>Celui qui amplifie leur subsistance...</p>
<p>Celui qui fait accroitre ce qui leur donne...</p>
<p>Il amplifie... Il amplifie... Son nom est l'Ample...</p>
<p>Et je vais vous donner un secret mes chers,</p>
<p>Veux tu que Dieu amplifie tes biens ?</p>
<p>Je te donnerai un secret parmi les secrets de ce nom sacré,</p>
<p>Ai une bonne intention !</p>
<p>Ca veut dire quoi ?</p>
<p>Ca veut dire si tu as une bonne intention, vis avec cette bonne intention et Dieu va accroitre tes biens...</p>
<p>Tu me diras que tu ne comprends toujours pas,</p>
<p>Je te donnerai un exemple concret qui t'aidera à comprendre :</p>
<p>C'est l'histoire d'un très grand homme d'affaires en Arabie Saoudite,</p>
<p>Son nom est Shaikh Abdul Rahman Fakieh,</p>
<p>C'est une des plus grandes... grandes... grandes fortunes en Arabie Saoudite,</p>
<p>Cet homme possède la grande chaine de restaurants « Al Tazaj »</p>
<p>Cet homme possède l'hôtel Hilton Makkah, et d'autres grands hotels, ainsi que plein de grandes sociétés...</p>
<p>Dieu a amplifié ses biens d'une façon incroyable !</p>
<p>Et je faisais mon pèlerinage avec cet homme...</p>
<p>Alors je lui ai demandé « Shaikh Abdul Rahman Fakieh, comment avez-vous fait pour devenir aussi riche ? »</p>
<p>Il m'a répondu «mon fils, l'amplification est une bonne intention »</p>
<p>Et je n'ai pas compris comme vous au début...</p>
<p>J'ai demandé ce que ca voulait signifier,</p>
<p>Il m'a dit « Je vais te raconter mon histoire : J'ai commencé avec une petite boutique d'un mètre carré... Une boutique toute minuscule... Et dedans je vendais du poulet élevé dans une ferme minuscule... Que mon père m'a laissé...</p>
<p>Puis soudainement un virus est apparu, il a tué les poulets des autres fermes à la Mecque, et il ne restait plus que notre petite ferme à nous...</p>
<p>Je me suis donc trouvé tout seul à nourrir les gens de la Mecque...</p>
<p>Et je pouvais monopoliser tout le marché,</p>
<p>Un poulet coutait 5 rials (environ 1,04€)</p>
<p>Et je pouvais doubler le prix à 10 rials (environ 2,07€)</p>
<p>J'étais en position de force... Mais je n'ai rien fais de tout ca...</p>
<p>Je vendais le poulet à seulement 6 rials (environ 1,24€ ) »</p>
<p>Et je lui ai demandé pourquoi donc ?</p>
<p>Il m'a répondu « Parce que j'ai pris une bonne intention avec Dieu, je me suis adressé à Dieu en lui disant que je voulais nourrir ses serviteurs»</p>
<p>Je dédicace ces paroles à tous les hommes d'affaires ,</p>
<p>Il a dit à Dieu qu'il voulait nourrir ses serviteurs...</p>
<p>« Je pouvais les vendre à 10 rials, et ils vont me les acheter car ils n'ont pas le choix...</p>
<p>Mais je vais les vendre uniquement à 6 rials »</p>
<p>Vous imaginez 4 rials de différence (0,83€)</p>
<p>Si vous multipliez par mille poulets ca donne 4000 rials (829,56€)</p>
<p>Et par un million de poulets ca donne 4 millions de rials (829.560€)</p>
<p>Shaikh Abdul Rahman Fakieh me disait « Je suis resté à vendre le poulet à 6 rials pendant deux ans...</p>
<p>Comme si Dieu me testait... Tu vas garder ta bonne intention ou pas ?</p>
<p>Et mon intention est restée la même, tout ce que je voulais c'était nourrir les gens...</p>
<p>Alors Dieu m'a ouvert les portes, je te jure que je ne savais même pas d'où venait l'argent...</p>
<p>Il y'avait tellement d'argent que j'avais envie de dire stop ca suffit...</p>
<p>Et l'argent continuait à s'amplifier...</p>
<p>C'est pour ca que je t'ai dis l'amplification est une bonne intention »</p>
<p>Le nom de Dieu <strong>« L'ample »</strong> a été cité 13 fois dans le saint Coran...</p>
<p>La surprise c'est que quand le nom <strong>« Al-Wasi' »</strong> est cité, juste après est cité un autre nom de Dieu : <strong>« Al-'Alim »</strong> (traduction : Le Très-Savant, l'Omniscient )</p>
<p><strong>{ Car Allah multiplie la récompense à qui Il veut et la grâce d'Allah est immense, et Il est Omniscient. }</strong> Sourate 02 - Al-Baqara : La Vache (verset 261)</p>
<p>L' Omniscient parce qu'il a su leurs bonnes intentions , alors il a multiplié leurs biens...</p>
<p>Dieu a su leurs bonnes intentions, alors il leur a élargi leur subsistance...</p>
<p>Mon cher désires tu que Dieu fasse accroitre tes bien ?</p>
<p>Essaye cette histoire,</p>
<p>Prend une bonne intention cette semaine, dans une affaire dans laquelle tu as envie que Dieu l'élargisse...</p>
<p>Puis adresse toi à Dieu en disant « Mon Dieu j'ai envie que cette affaire s'agrandisse, et mon intention est ainsi et ainsi... »</p>
<p>Et applique ton intention !</p>
<p>Et active toi et multiplie les efforts !</p>
<p>Et tu verras ce que Dieu l'Ample fera dans ta vie !</p>
<p>Testez... Pensez à moi... et ne m'oubliez pas dans vos prières :)</p>
<p>Et un sourire d'espoir se dessinera sur vos visages,</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
