                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>Beaucoup de gens me disent, chaque jour tu nous parle d'un sourire d'espoir... D'un sourire d'espoir... Tu ne vois pas qu'autour de nous il n'y a ni sourire ni espoir ?</p>
<p>Et certains me demandent, où il est cet espoir dont tu nous parles ?</p>
<p>Je leur répond en disant « Je vous jure l'espoir est notre seule solution»</p>
<p>Nous avons besoin d'ingurgiter de grandes doses d'espoir, pour sortir de ce climat de dépression qui nous entoure,</p>
<p>Croyez moi mes chers, j'ai un espoir car il n'y a pas d'autre choix,</p>
<p>J'ai un espoir pour que je continue à travailler,</p>
<p>J'ai aussi un espoir car je vois devant moi de belles choses qui donnent espoir :</p>
<p>Je vois devant moi des jeunes radieux,</p>
<p>Je vois devant moi des jeunes filles et garçons motivés qui veulent créer et produire ,</p>
<p>Je vois des jeunes formidables dans des écoles et dans des universités,</p>
<p>Je vous jure que j'ai un espoir,</p>
<p>Et je vous invite tous à avoir le même espoir que le mien :)</p>
<p>Rassurez vous, demain sera meilleur,</p>
<p>Et le monde sera meilleur, et n'ayez pas peur malgré tout ce qui se passe !</p>
<p>Je vais vous dire une chose concrète pour vous aider à me croire :</p>
<p>Il y'à un pays qui s'appelle la Turquie,</p>
<p>Bien sûr tout le monde connait la Turquie,</p>
<p>J'ai visité ce pays en 2001,</p>
<p>A l'époque la devise turque était effondrée,</p>
<p>Un seul dollars américain valait 5 millions de livres turques...</p>
<p>En 2010, le dollars valait 5 livres turques,</p>
<p>En 2001, il était impossible d'aller au détroit du Bosphore tellement c'était pollué... Aujourd'hui c'est devenu un endroit touristique magnifique !</p>
<p>En 2001 l'économie turque était au plus bas, et les banques effondrées, et ils avaient une énorme dette, et ils ont couru supplier le fond monétaire international pour emprunter, et ils se sont battus en plein parlement...</p>
<p>Tu te rends compte à quel point ils étaient au plus bas ?</p>
<p>Aujourd'hui la Turquie fait partie des 15 puissances économiques mondiales...</p>
<p>Dans chaque maison en Europe, en Italie, en Suisse, en Angleterre.... Le quart des produits qu'on y trouve sont faits en Turquie...</p>
<p>Les turques on réussi à faire tout ca ?</p>
<p>La Turquie qui suppliait les banques internationales pour emprunter, aujourd'hui exporte ses produits vers la majorité des pays du monde.</p>
<p>Et le taux de chômage continue de baisser...</p>
<p>Ils ont fait une révolution économique,</p>
<p>J'ai donc espoir car j'ai visité la Turquie en 2001, puis je l'ai visité en 2010,</p>
<p>J'ai espoir car les jeunes sont les mêmes que ca soit en Turquie ou dans un autre pays,</p>
<p>J'ai espoir car notre pays regorge de ressources et de jeunesse,</p>
<p>Souris donc,</p>
<p>Ai un espoir,</p>
<p>Mais travaille !</p>
<p>Compte sur Dieu, et active toi,</p>
<p>Je vous jure que Dieu va bénir nos efforts,</p>
<p>Et qu'on dessinera un sourire d'espoir tous ensemble !</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
