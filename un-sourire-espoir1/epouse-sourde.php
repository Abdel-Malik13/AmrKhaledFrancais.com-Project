                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui est une histoire très drôle :)</p>
<p>L'idée derrière cette histoire est que parfois on pense que le problème vient des autres... Alors que le problème vient de nous,</p>
<p>Parfois on pense que c'est nos enfants qui ne comprennent pas, alors qu'en réalité c'est nous...</p>
<p>Parfois on voit un défaut chez nos épouses, alors qu'en réalité il est chez nous...</p>
<p>Et on se plaint des autres, et ça nous énerve... etc</p>
<br>
<p>On raconte qu'un homme trouvait que son épouse avait des troubles d'audition,</p>
<p>Que plus le temps passait, plus elle avait du mal à entendre...</p>
<p>Et qu'elle arrivait à un stade ou si on ne faisait rien, elle allait devenir complètement sourde,</p>
<p>Elle ne l'écoutait plus quand il lui parlait, il n'arrivait plus communiquer avec elle...</p>
<p>Il a donc décidé de l'emmener consulter un médecin spécialiste,</p>
<p>Avant de prendre rendez-vous chez le médecin, il s'est rappelé qu'un de ses proches s'y connaissait aussi,</p>
<p>Son proche lui a dit « Avant d'aller chez le médecin, je vais te donner une méthode gratuite pour tester ses capacités auditives, tu te mets à une distance de 40 pas de ton épouse, puis tu lui parles normalement comment vas-tu, qu'as-tu cuisiné ce soir... etc</p>
<p>Si elle t'entend alors c'est parfait, sinon avance à 30 pas, et refais la même chose,</p>
<p>Puis si elle ne te répond pas, avance à 20 pas,</p>
<p>Puis à 10 pas... »</p>
<p>Le soir le mari est rentré chez lui,</p>
<p>Il a trouvé sa femme dans la cuisine,</p>
<p>Il s'est dit c'est l'occasion ou jamais...</p>
<p>Il s'est donc éloigné de 40 pas, puis il a commencé à lui parler...</p>
<p>Mais elle ne répondait pas,</p>
<p>Il a avancé à 30 pas, toujours pas de réponse...</p>
<p>20 pas, toujours rien...</p>
<p>10 pas, pareil...</p>
<p>Il est entré dans la cuisine, il s'est mis devant elle,</p>
<p>Il a répété « Chérie qu'as-tu cuisiné ce soir ? »</p>
<p>Elle a répondu « Chéri, pour la 5ème fois c'est du poulet rôti ! »</p>
<p>En réalité c'est lui qui n'entendait rien :)</p>
<p>Le problème venait de lui et non pas d'elle,</p>
<p>C'était à lui de consulter le médecin.</p>
<br>
<p>Parfois on pense que les gens sont fautifs alors que c'est nous...</p>
<p>Parfois on pense que le défaut est ailleurs alors qu'il est chez nous...</p>
<p>Parfois on se dispute avec les autres en disant « J'ai été assez patient jusqu'ici, mais là je n'en peux plus... » Alors qu'en réalité ce sont eux qui nous ont supporté tout ce temps...</p>
<p>Parfois les enfants se disputent avec leurs parents en pensant qu'ils ne comprennent rien, alors qu'ils ont beaucoup plus d'expérience qu'eux...</p>
<p>Parfois les époux font la même chose entre eux...</p>
<br>
<p>Pour dessiner un sourire d'espoir chez ta famille, tes amis et tes collègues au travail, pense d'abord à tes fautes avant de penser aux leurs.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
