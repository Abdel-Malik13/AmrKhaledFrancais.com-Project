                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'histoire d'aujourd'hui parle d'un père et ses 3 fils,</p>
<p>(C'est une histoire symbolique mais avec une très belle idée derrière...)</p>
<p>Ce père était riche, il a rempli un coffre de bijoux, d'or, de perles...</p>
<p>Le coffre en lui-même était cher et bien décoré,</p>
<p>Il a donc pris ce coffre qui pesait lourd et l'a déposé devant ses 3 fils qu'il aimait,</p>
<p>Il leur a dit « Vous êtes tous les 3 mes fils, et j'ai décidé de vous offrir ce qu'il y'a dans ce coffre »</p>
<p>Il a ouvert le coffre, et leur a demandé de prendre ce qu'ils veulent mais à une condition :</p>
<p>Faire une seule tentative, et remplir leurs deux mains...</p>
<p>Le grand fils a forcément de grandes mains :)</p>
<p>Il a mis ses mais dans le coffre, et les a remplies au maximum de bijoux...</p>
<p>Le fils moyen, avait aussi de grandes mains,</p>
<p>Il les a glissées dans le coffre, puis a pris le maximum de ce qu'il pouvait prendre...</p>
<p>Le tour du 3ème fils est venu... C'était le plus petit des trois,</p>
<p>Il a regardé ses deux frères avec leurs grandes mains,</p>
<p>Puis il a regardé ses mains à lui qui étaient toutes petites...</p>
<p>Savez-vous ce qu'il a fait ?</p>
<p>Il n'a pas mis ses mains dans le coffre,</p>
<p>Il est parti faire un câlin à son père,</p>
<p>Il lui a dit « Papa, est-ce que tu m'aimes ? »</p>
<p>Le père lui a répondu « Je t'aime beaucoup mon fils »</p>
<p>Il lui a alors dit « Je veux que ce soit toi papa qui me donne ma part »</p>
<p>Le père a regardé ses deux autres enfants en disant « Vous avez pris tout ce que vous pouviez prendre »</p>
<p>Il a fermé le coffre, et l'a donné en entier au plus petit de ses 3 enfants...</p>
<p>Pourquoi je t'ai raconté cette histoire ?</p>
<p>Parce que moi et toi, tous les jours, on a envie de plein de choses,</p>
<p>Mais notre petite force nous empêche de prendre tout ce qu'on a envie de prendre dans la vie...</p>
<p>Va donc voir Le Fort (un des 99noms de Dieu)</p>
<p>Pourquoi on fait toujours le mauvais choix, et qu'on ne compte que sur nos petites capacités, alors qu'on sait qu'on est faibles ?</p>
<p>Je t'ai raconté l'histoire de ce père avec ses enfants (Dieu est incomparable)</p>
<p>Mais adresse-toi à ton seigneur,</p>
<p>Dis-lui mon Dieu, tous les humains sont tes créatures... Mais moi j'ai choisi de venir vers toi pour m'aider.</p>
<p>Bien sûr après ca il faudra que tu travailles,</p>
<p>Et tu feras des efforts,</p>
<p>Mais tu demanderas à Dieu de choisir à ta place, et te donner tout ce qu'il veut...</p>
<p>Il y'a une très belle phrase qui dit :</p>
<p>« D'habitude on ne s'inquiète pas quand on a un père... Alors comment s'inquiéter quand on a Dieu ? »</p>
<p>Celui qui a un père fort se sent rassuré,</p>
<p>Alors que nous avons Dieu L'Immense, L'Infiniment Grand...</p>
<p>Mon cher, quand la vie te fatigue, et que tu ne trouves pas de solutions,</p>
<p>Dirige-toi vers ton créateur,</p>
<p>Dis-lui que tes mains sont petites, et que tes capacités sont limitées... Et que tu n'as que lui...</p>
<p>Rend-moi fort... Facilite-moi...Choisis à ma place...Donne moi ma subsistance...Règle moi ce problème...Je n'ai personne à part toi... Mon papa me protège, alors que dire de mon Seigneur...Mon Protecteur...</p>
<p>Dis« Mon Dieu » mon cher, et je te jure que tu auras un sourire d'espoir, car <strong>Le Fort</strong>, <strong>Le Tout Puissant</strong>, <strong>L'Immense</strong>... Sera avec toi !</p>
<p><strong>{ Gloire à lui, il s'est élevé (au-dessus de tout) }</strong></p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
