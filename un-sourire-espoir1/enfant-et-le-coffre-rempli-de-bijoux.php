                                        <p>Bismillah El Rahman El Raheem (In the name of God the Merciful).</p>
<br>
<p>Welcome to today's episode of A Smile of Hope (Basmit Amal).</p>
<br>
<p>Today's pisode is about a father of three children. This father was a very rich man.</p>
<p>This is an imaginary story which has a very nice meaning.</p>
<p>One day he filled a big box with pearls, gold and rubies. This box was very expensive and very luxurious. It was very heavy... but he carried it and put it in front of his three children that he truly loves and told them: "You are all my children... and I have decided to give this box to you all" and he opened the box infront of them and asked each one of them to reach out with their hands and take only a hand-full of jewels. Only as much as his palms can carry, but only one time...</p>
<p>The eldest brother, ofcourse, has much larger hands and so he digged his hands deep into the pile of jewels and took out lots of pearls, but only one time.</p>
<p>The middle brother, who also has large hands, he put his big hands into the box and took out so many jewels.</p>
<p>Now was theturn of the youngest brother, whose hands were small compared with his brothers...</p>
<p>He looked at his brothers... Each one having so many pearls and jewels... and he looks to his hans who were small and weak...</p>
<p>So what did he do? Nothing... he did not reach out with his hands... but rather... he ran into his father's arms and asked him : "Do you love me?"</p>
<p>so the father replied "I love you so much, my son"... So he told his father "I don't want to take my share on my own...</p>
<p>Can you give me my share, with your own hands?"...</p>
<p>So the father looked at his other two children and asked them: "Didn't you both get your share?"...</p>
<p>He closed the box and gave the entire box to his youngest son...</p>
<p>Why am I telling you this story? You and I, every day, our weak strengths stop us from taking everything we desire in life...</p>
<p>Then why don't you seek the Mighty One?</p>
<p>Why don't you go to Allah Subhanaho wa Taala?</p>
<p>Why do we always make the wrong choices and only depend on our strengths which are weak, and we know that we are weak.</p>
<p>I am telling you this story and God is my raw model.</p>
<p>Why don't you go to God and tell God "All human beings and your creators, just like this man, all those children are his, but I have come to You, please help me"...</p>
<p>Of course you will have to work and exert effort but you will go to Him to choose for you and to give you.</p>
<p>There is a beautiful statement that says "In normal cases, a person who has a father does not get worried, then how would someone get worried if they have a God?"</p>
<p>Wow... usually people with a father, especially a strong father, does not need to worry... then how can people et worried if they have Allah?</p>
<p>Please, when you are in trouble, and life is giving you a hard time, then go to God and tell him "my hands are small and my capabilities are limited and weak... make me strong and You give me what you wish.</p>
<p>Please make things easier for me... Please fix my problems... They are much stronger than me and I am so weak... I have no one but You.</p>
<p>If my father protects me... then imagne how much protection God will give me...</p>
<p>Always ask Him... and then it will be a smile of hope.</p>
<p>Thank you,</p>
<p>and Wasalamu Aliekum Rahamtullah.</p>
