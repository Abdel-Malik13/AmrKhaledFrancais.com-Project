                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<br>
<p>L'épisode d'aujourd'hui parle de loyauté,</p>
<p>Es-tu loyal ou pas ?</p>
<p>Es-tu loyal envers les gens qui t'ont aidé dans ta vie ?</p>
<p>Es-tu loyal envers ceux qui ont été là pour toi dans les moments difficiles ?</p>
<p>Te souviens-tu de tes professeurs à l'école qui t'ont tant appris ?</p>
<p>Te souviens-tu de cette personne qui t'a sorti d'une mauvaise situation,</p>
<p>Ou l'as-tu oublié parce que le temps vous a éloigné l'un de l'autre ?</p>
<p>Et tu ne lui dis plus merci,</p>
<p>Et tu ne l'appelles plus pour prendre de ses nouvelles...</p>
<p>Elle est où la fidélité...</p>
<p>Penses-tu à ta maman ?</p>
<p>Cette femme a sacrifié sa vie pour toi...</p>
<p>Tu prends souvent de ses nouvelles ?</p>
<p>Tu te donnerais combien sur 10 en fidélité ?</p>
<p>Après cet épisode vas-tu chercher quelqu'un qui t'a aidé par le passé,</p>
<p>Et vas-tu l'appeler pour lui dire que tu as pensé à lui, que tu n'as jamais oublié le service qu'il t'a rendu... Et que tu tenais à le remercier encore une fois...</p>
<br>
<p>Je vous raconte tout ceci car j'ai une très belle histoire du Prophète (paix et bénédictions soient sur lui)</p>
<p>Un homme qui s'appelait Abu Al-bukhturi Ibn Hicham, a aidé le Prophète (pbsl) pendant que la tribu de qoraych encerclait le Prophète (pbsl) et les musulmans...</p>
<p>Et les empêchaient de recevoir de la nourriture, de l'eau, des vêtements...</p>
<p>Et les empêchaient même de se marier... Une période très difficile pour les musulmans qui étaient en petit nombre...</p>
<p>Abu Al-bukhturi Ibn Hicham (qui n'était pas musulman) a déchiré l'accord (qu'avaient signé les chefs de la tribu de qoraych)</p>
<p>Il s'est levé en disant Non ! Ils vont manger et ils vont boire...</p>
<p>Abu Al-bukhturi Ibn Hicham ne s'est pas converti à l'islam... Mais le Prophète (pbsl) n'a jamais oublié ce qu'il a fait,</p>
<p>Il n'était pas musulman... Mais le Prophète (pbsl) a gardé ce qu'il a fait en mémoire...</p>
<p>Parce que le Prophète (pbsl) était loyal envers tous ceux qui l'ont aidé dans sa vie.</p>
<br>
<p>Les choses ont évolué,</p>
<p>Et pendant la bataille de Badr,</p>
<p>Abu Al-bukhturi Ibn Hicham était du côté de qoraych qui combattait les musulmans...</p>
<p>Il n'était donc pas du côté des musulmans...</p>
<p>Oui mais il a une bonne action, il nous a aidé dans un moment difficile... Alors il faut le lui rendre,</p>
<p>C'est possible d'être loyal en pleine guerre ?</p>
<p>Oui c'est possible !</p>
<p>Les musulmans étaient dans cette bataille 300</p>
<p>Et qoraych étaient 1000</p>
<p>Avant l'affrontement le Prophète (pbsl) a dit <strong class="prophete">« Ô gens, que celui qui croise Abu Al-bukhturi Ibn Hicham ne le tue pas... »</strong></p>
<p>Les gens ont demandé « Pourquoi Ô messager de Dieu ? »</p>
<p>Le Prophète (pbsl) leur a répondu <strong class="prophete">« Pour ce qu'il a fait pour nous le jour de l'accord... Par loyauté envers lui »</strong></p>
<br>
<p>Imaginez un peu, y'à t'il pire que quelqu'un qui prend les armes et vient vous combattre ?</p>
<p>Imaginez quelqu'un qui vous a rendu service un jour, puis est devenu ennemi un autre jour ?</p>
<p>Le Prophète (pbsl) est resté loyal même en pleine guerre.</p>
<br>
<p>Pendant la bataille un des musulmans a croisé Abu Al-bukhturi Ibn Hicham et un homme à côté de lui,</p>
<p>Alors il a ignoré Abu Al-bukhturi Ibn Hicham, et il est parti combattre l'autre homme...</p>
<p>Abu Al-bukhturi Ibn Hicham ne comprenait pas,</p>
<p>Le musulman lui a dit « le Prophète (pbsl) nous a ordonné de ne pas te tuer »</p>
<p>Abu Al-bukhturi Ibn Hicham a demandé « Et pourquoi? »</p>
<p>Le musulman lui a dit « Par loyauté envers toi, pour le jour où tu as déchiré l'accord »</p>
<p>Abu Al-bukhturi Ibn Hicham a fini par oublier ce jour là...</p>
<p>Mais le Prophète (pbsl) lui ne l'a pas oublié.</p>
<p>Abu Al-bukhturi Ibn Hicham a dit au musulman « Et si moi je venais te tuer ? »</p>
<p>Le musulman lui a répondu « Alors je fuirai »</p>
<p>Le musulman l'évitait, et Abu Al-bukhturi Ibn Hicham le poursuivait,</p>
<p>Jusqu'à ce qu'il soit coincé...</p>
<p>Le musulman n'avait pas d'autre choix que se défendre... Et il a tué Abu Al-bukhturi Ibn Hicham...</p>
<p>Il est revenu plein de honte voir le Prophète (pbsl) en disant « Je jure que ce n'était pas mon intention... Je jure que ce n'était pas mon intention... Mais... Il allait me tuer... Alors je n'avais pas d'autre choix... »</p>
<p>Le Prophète (pbsl) lui a répondu <strong class="prophete">« Ne t'en fais pas, on voulait juste être loyaux... »</strong></p>
<br>
<p>Es tu loyal dans ta vie à ce point ?</p>
<p>Peux tu prendre une seconde, et te souvenir des gens qui t'ont aidé dans ta vie ?</p>
<p>Les rappeler,</p>
<p>Les remercier,</p>
<p>Prendre de leurs nouvelles,</p>
<p>Même si la vie vous a éloigné,</p>
<p>Et même s'ils sont devenus tes ennemis...</p>
<p>Que la paix et la bénédiction soient sur le Prophète,</p>
<p>Inspirons nous de sa loyauté et de sa fidélité,</p>
<p>Et dessinons un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
