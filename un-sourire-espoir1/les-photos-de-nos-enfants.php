                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est sur la justice entre les enfants,</p>
<p>Nos petits enfants...</p>
<p>Tous les parents qui se posent souvent la question « Est-ce que je suis équitable envers tous mes enfants ? »</p>
<p>Les petits ne parlent pas, ils n'expriment pas leurs sentiments,</p>
<p>Car ils sont encore trop jeunes, ils n'ont pas le langage des adultes,</p>
<p>Ils ne peuvent pas par exemple dire « Papa tu n'es pas juste »</p>
<p>Non ils ne savent pas prononcer ce genre de phrases...</p>
<p>Ou « Papa je ressens de l'injustice, tu préfères mon frère à moi »</p>
<p>Ou une fille qui dit « Maman pourquoi mon frère passe toujours avant moi ? »</p>
<p>Aucun de nos enfants ne dit ca,</p>
<p>Mais peut-être qu'ils le ressentent de l'intérieur,</p>
<p>Et ils peuvent le garder au fond d'eux pendant des années,</p>
<p>Si on favorise un de nos enfants sur l'autre...</p>
<p>Ils peuvent aussi ne jamais le dire toute leurs vies, mais ca laissera des traces dans leurs cœurs.</p>
<p>Pendant ce temps là nous parents, on ne comprend pas pourquoi cet enfant s'est éloigné de nous, pourquoi il s'est renfermé sur lui-même...</p>
<p>Alors que la raison de tout ca c'est un sentiment d'injustice.</p>
<p>Je vais vous raconter ce qui m'est arrivé avec mon fils Omar :</p>
<p>On était en train de jouer ensemble à la maison,</p>
<p>Omar c'est le plus petit de mes deux fils,</p>
<p>Il a regardé les murs où sont accrochées des photos de famille,</p>
<p>Moi et mes parents,</p>
<p>Moi et mes enfants... etc.</p>
<p>Omar m'a surpris en disant « Papa, pourquoi les photos d'Ali (son grand frère) sont plus nombreuses que les miennes ? »</p>
<p>Aie.... :)</p>
<p>J'ai pris cette phrase comme une gifle !</p>
<p>Omar a 5ans, il m'a dit ca en toute innocence...</p>
<p>« Papa pourquoi il y'a plus de photos d'Ali que de moi dans la maison ? »</p>
<p>Je n'ai pourtant jamais fait attention à ca,</p>
<p>Je jure que je ne l'ai pas fait par préférence à un de mes enfants...</p>
<p>Mais lui il l'a senti,</p>
<p>Et il l'a exprimé,</p>
<p>Il l'a exprimé en toute innocence,</p>
<p>Je me suis tu... Puis j'ai cherché une réponse rapide pour m'en sortir...</p>
<p>J'ai fini par lui répondre « Mon chéri, c'est parce qu'il est né avant toi, il est né 5 ans avant toi, alors on a eu l'occasion de prendre beaucoup plus de photos de lui »</p>
<p>Il m'a répondu « Oui mais moi je suis présent, pourquoi vous ne me prenez pas en photo ? »</p>
<p>En réalité j'ai séché, j'ai commencé à transpirer, j'ai senti que j'étais dans une situation délicate...</p>
<p>La question de mon fils de 5ans m'a mis mal à l'aise, mais je jure que je ne l'ai pas fait exprès,</p>
<p>Il l'a observé, et s'en est rendu compte...</p>
<p>Alors j'ai pris ca en considération, et depuis ce jour je fais bien attention aux nombres de photos accrochées dans la maison.</p>
<p>J'ai appris une chose très importante,</p>
<p>J'ai compris pourquoi un jour un homme est venu voir le Prophète (pbsl) en lui disant « Ô Messager de Dieu, je veux que tu sois témoin que j'ai donné à mon fils ce cadeau»</p>
<p>Le Prophète (pbsl) lui a demandé <strong class="prophete">« As-tu donné à tes autres enfants la même chose que ce que tu lui a donné ? »</strong></p>
<p>Cet homme a dit « Non Ô messager de Dieu »</p>
<p>Le Prophète (pbsl) lui a répondu <strong class="prophete">« Trouve un autre témoin, car moi je ne témoignerai pas sur une truquerie »</strong></p>
<p>Que c'est dur à entendre... C'est une falsification... Une truquerie... Une tromperie... une sortie de route...</p>
<p>En faisant ca tu vas dérouter les personnalités de tes autres enfants...</p>
<p>Mes chers soyez justes entre vos enfants,</p>
<p>Soyez justes même dans le nombre de bisous,</p>
<p>Soyez justes dans les câlins,</p>
<p>Soyez justes dans les sourires,</p>
<p>Soyez justes dans le nombre de photos :)</p>
<p>Soyez juste dans l'argent...</p>
<p>Soyez justes mes chers entre vos enfants, pour que toute la famille ait un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
