                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.<p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'histoire d'aujourd'hui nous vient du Mexique :)</p>
<p>Un jour ça nous vient d'Irak, le lendemain du Mexique... Il y'a de l'espoir partout dans le monde !</p>
<p>C'est l'histoire d'un enfant assis sur la plage,</p>
<p>L'océan ramenait sur le sable des étoiles de mer...</p>
<p>C'est une créature qui vit au fond de l'eau, si les vagues la sortent de l'eau elle meurt très vite...</p>
<p>Cet enfant le savait,</p>
<p>Alors il a eu pitié des étoiles de mer...</p>
<p>Dès qu'une vague ramenait une étoile hors de l'eau, il courrait vite la remettre dedans,</p>
<p>Ca faisait rire le père de l'enfant,</p>
<p>Il lui a dit « Sais-tu mon fils combien d'étoiles de mer viennent sur la plage chaque jour ? »</p>
<p>Il a dit « Non papa, combien ? »</p>
<p>Son père lui a répondu « Plus de 1000 étoiles par jour, donc en quelques jours ca devient des dizaines de milliers... à quoi ca sert d'en remettre une ou deux dans l'eau ? »</p>
<p>Cet enfant lui a répondu « Au moins papa je sauverai une ou deux, si je ne peux pas sauver les dizaines de milliers d'autres »</p>
<p>Le père a arrêté de rire, il a compris que son fils était plein de sagesse et qu'il savait ce qu'il faisait.</p>
<p>J'ai raconté cette histoire pour dire :</p>
<p>SI tu ne peux pas faire de grandes choses, fais une petite chose mais d'une grande manière...</p>
<p>Parfois nous avons besoin d'apprendre de nos enfants et de leur innocence,</p>
<p>Ce petit enfant a eu pitié de l'étoile de mer, alors il a simplement décidé de la sauver... Sans se trouver des excuses ou des choses qui démotivent comme se dire « Ca ne sert à rien, il restera des dizaines de milliers d'autres... »</p>
<p>Il s'agit d'étoiles de mer... Mais que dire des humains qui meurent et se noient...</p>
<p>Il y'a de plus en plus de pauvres,</p>
<p>Il y'a beaucoup de gens dans des situations délicates,</p>
<p>N'écoute jamais le diable quand il vient te chuchoter de ne pas les aider car c'est le travail du gouvernement... ou que même si tu leur règle un problème il leur restera plein d'autres problèmes...</p>
<p>Sauve au moins une seule personne !</p>
<p>Aide à régler au moins un seul de leurs problèmes !</p>
<p>Si tu ne peux pas créer une grande association, trouve autour de toi quelqu'un qui a besoin d'aide,</p>
<p>Aide-le à se relever,</p>
<p>Pas forcément en lui donnant de l'argent, parfois un soutien moral ou de l'encouragement suffisent.</p>
<p>On peut aussi apprendre un métier à une personne,</p>
<p>Ou apprendre à lire et à écrire à un analphabète...</p>
<p>Dessinons un sourire d'espoir sur le visage des gens, et Dieu dessinera un sourire d'espoir sur nos visages.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
