                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle d'une chose très très très familiale !</p>
<p>Il n'existe pas un papa et une maman qui n'aiment pas leurs enfants vrai ?</p>
<p>On aime nos enfants beaucoup...beaucoup...beaucoup....</p>
<p>Et s'il leur arrive un malheur ?</p>
<p>Oualalaaaa Nos cœurs se déchirent beaucoup...beaucoup...beaucoup...</p>
<p>Au moment ou le cœur d'un père ou d'une mère se déchire... à ce moment Dieu réalise facilement son souhait...</p>
<p>A ce point les cœurs des papas et des mamans sont chers pour Dieu ?</p>
<p>Le jour ou ton cœur bouge pour ton enfant... Adresse toi à Dieu et demande son aide.</p>
<br>
<p>Je vais vous raconter l'histoire de Moustapha :</p>
<p>Moustapha est un jeune homme qui fait partie de notre fondation « Les bâtisseurs de la vie »</p>
<p>Il est syrien mais il vit à Médine (en Arabie Saoudite)</p>
<p>Je l'ai rencontré il y'à deux ans avec son petit fils,</p>
<p>Son petit fils est extrêmement mignon, il avait 3ans ou 4ans...</p>
<p>Moustapha m'a raconté qu'il y'à un mois il avait du faire face à une situation très difficile...</p>
<p>Sans faire exprès, il avait fermé une porte en métal sur la main de son fils...</p>
<p>Il me raconte « Mon fils à ce moment a crié de toutes ses forces,</p>
<p>Et quand j'ai regardé sa main, ses doigts étaient bleus, bleus,bleus... Il n y'avait plus de sang...</p>
<p>J'ai couru à l'hôpital,</p>
<p>Et le docteur en regardant la main de mon fils, m'a dit qu'il ne pensait pas que mon fils pourrait bouger ses doigts encore...</p>
<p>Il y'à très peu de chances que du sang circule dans ses doigts,</p>
<p>Et ca peut même demander une amputation...</p>
<p>Quand Moustapha me racontait ca, j'avais mal au cœur... Je lui ai demandé ce qu'il avait fait,</p>
<p>Il m'a répondu : J'ai pris mon fils, et j'ai essayé tous les traitements possibles... Mais sans résultat...</p>
<p>Il ne me restait que Dieu...</p>
<p>Alors j'ai passé toute une nuit en racontant ma peine à Dieu...</p>
<p>En lui disant à quel point mon cœur souffrait...</p>
<p>Mon Dieu... Mon Dieu... ce fils est la chose la plus chère au monde pour moi... Je te supplie de m'aider...</p>
<p>Puis j'ai ouvert le Coran , pour chercher les versets qui parlent de résurrection...</p>
<p>Alors j'ai trouvé que dans la sourate 02 Al-Baqara (La vache)</p>
<p>Il y'à beaucoup de versets qui parlent de vie après la mort.</p>
<p><strong>{255. Allah! Point de divinité à part Lui, le Vivant, Celui qui subsiste par lui-même «Al-Qayyûm». Ni somnolence ni sommeil ne Le saisissent.}</strong></p>
<p><strong>{258. N'as-tu pas su (l'histoire de) celui qui, parce qu'Allah l'avait fait roi, argumenta contre Abraham au sujet de son Seigneur? Abraham ayant dit: «J'ai pour Seigneur Celui qui donne la vie et la mort»}</strong></p>
<p>J'ai donc trouvé que cette sourate parlait beaucoup du fait que Dieu donne la vie...</p>
<p>Je la lisais donc, en tenant la main de mon fils...</p>
<p>Et en m'adressant à Dieu: Mon Dieu ressuscite là... Mon Dieu ressuscite là... Mon Dieu ressuscite là...</p>
<p>J'ai passé toute la nuit en faisant ca...</p>
<p>Le lendemain la main de mon fils était toujours dans son état...</p>
<p>J'ai donc passé la deuxième nuit entièrement en faisant la même chose...</p>
<p>Et en étant sur qu'en voyant ma miséricorde envers mon fils, Dieu sera clément avec moi...</p>
<p>Et le troisième jour, l'état de la main de mon fils commençait à s'améliorer...</p>
<p>Le quatrième jour, il s'améliorait encore plus...</p>
<p>Et au bout d'une semaine, la main de mon fils est redevenue comme avant...</p>
<p>Le docteur n'y croyait pas...</p>
<p>Moustapha m'a montré les deux mains de son fils... Et m'a demandé sur laquelle des deux la porte s'était fermée...</p>
<p>Je n'ai pas su laquelle... C'était exactement les mêmes 2 mois après !</p>
<p>Il n'y'à de Dieu que Dieu !</p>
<p>J'ai dis à Moustapha que Dieu était miséricordieux avec chaque papa et maman qui souffrent pour leurs enfants.</p>
<p>A toutes les mamans qui souffrent que leur enfant se drogue...</p>
<p>A tous les pères qui souffrent que leurs enfant a eu un accident ou une maladie...</p>
<p>A tous les parents qui souffrent que leur fille se fait maltraiter par son mari ou son fiancé...</p>
<p>Adressez vous à Dieu...</p>
<p>Adressez vous à Dieu, et donnez à vos enfants de l'amour et de l'affection...</p>
<p>Dieu est généreux !</p>
<p>Ne paniquez jamais... Restez calmes... Et tournez vous vers celui qui donne la vie à toute chose...</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
