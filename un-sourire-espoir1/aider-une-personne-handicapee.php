                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui raconte une situation que j'ai vécue quand j'étais au collège,</p>
<p>Je ne priais pas encore et je ne m'intéressais pas beaucoup à la religion...</p>
<p>Tout ce qui m'intéressait c'était le football et passer du temps avec mes amis,</p>
<p>Ma vie se résumait à ca...</p>
<p>Mais une chose est arrivée,</p>
<p>Une situation qui a du prendre 10 minutes maximum,</p>
<p>Peut-être que c'est grâce à cette situation que Dieu a changé ma vie...</p>
<p>Je me promenais avec ma famille dans les rues de Port-Saïd (Une ville portuaire du nord-est de l'Égypte)</p>
<p>Je me rappelle de ca comme si c'était hier :</p>
<p>Pendant que je marchais, j'ai aperçu un homme handicapé sur une chaise roulante...</p>
<p>Pendant qu'il essayait de descendre du trottoir son fauteuil roulant l'a trahi,</p>
<p>Il a perdu son équilibre,</p>
<p>L'homme est tombé par terre, et il n'arrivait pas à remonter,</p>
<p>Il a commencé à pleurer...</p>
<p>Personne dans la rue n'est venu l'aider,</p>
<p>Comme je vous l'ai dit j'étais à l'époque au collège et dans ma tête il n'y avait que le football et rien d'autre...</p>
<p>J'ai couru vers cet homme,</p>
<p>Mais mon corps n'avait pas beaucoup de force,</p>
<p>Je le prenais dans mes bras,</p>
<p>Je le tenais par les jambes,</p>
<p>Je transpirais,</p>
<p>J'essayais par tous les moyens,</p>
<p>Mon pantalon s'est Sali par la boue,</p>
<p>Et enfin quelqu'un est venu m'aider, et on a fini par remettre l'homme sur son fauteuil roulant...</p>
<p>Il m'a pris dans ses bras et il m'a fait des prières « Que Dieu soit satisfait de toi »</p>
<p>Je me souviens encore de sa prière,</p>
<p>Et j'ignore ce qui s'est passé par la suite,</p>
<p>Tout ce que je sais c'est que quelque mois après j'ai commencé à prier, et je me rapprochais de Dieu.</p>
<p>Je n'oublierai jamais ce jour là,</p>
<p>J'y ai appris une chose très importante : Le bien ne se perd jamais.</p>
<p>Mes chers essayez dans vos vies,</p>
<p>Quand vous bougez pour aider les autres, même si vous n'avez pas beaucoup de moyens, et même si vous êtes faibles...</p>
<p>Juste le fait de bouger finit toujours par payer.</p>
<p>A la fin ce n'est pas moi qui ai remis l'homme sur sa chaise roulante, c'est quelqu'un d'autre qui l'a fait... Mais j'étais le premier à aller le voir, et j'ai bougé avec mon cœur,</p>
<p>Alors Dieu m'a récompensé et m'a guidé dans ma vie.</p>
<p>Je vous raconte cette histoire pour que chacun qui m'écoute n'hésite jamais à faire le bien,</p>
<p>Quand Dieu met devant toi une occasion ou une situation,</p>
<p>Ou quand tu croises une personne en difficulté cours l'aider !</p>
<p>Ca sera ta porte vers la guidée.</p>
<p>Vous avez aussi l'occasion de nous aider dans notre projet de lutte contre l'analphabétisme,</p>
<p>Le 7 Juillet, nous consacrons un seul jour à l'inscription des personnes dans les classes,</p>
<p>Tu peux venir nous rejoindre sur facebook,</p>
<p>Et tu verras comment Dieu te récompensera et te remerciera pour ton effort.</p>
<br>
<p>Merci</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
