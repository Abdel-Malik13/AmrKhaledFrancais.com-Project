                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>L'épisode d'aujourd'hui a un rapport avec l'éducation de nos enfants,</p>
<p>Beaucoup de fois on voit chez nos enfants des choses qui ne nous plaisent pas,</p>
<p>Des habitudes qu'on ne supporte pas,</p>
<p>Toute la journée entrain de jouer aux jeux vidéo...</p>
<p>Toute la journée sur l'ordinateur et facebook...</p>
<p>Toute la journée scotchés devant la télé...</p>
<p>On a l'impression qu'ils perdent leur temps,</p>
<p>Alors on leur crie dessus,</p>
<p>Et on se dispute avec eux,</p>
<p>Et à la fin rien ne change...</p>
<p>Les pères lèvent le ton, et les mères lèvent le ton, sans résultat...</p>
<br>
<p>Mais moi j'ai une nouvelle façon,</p>
<p>Très agréable et très efficace :)</p>
<p>Tu veux que tes enfants changent ?</p>
<p>D'abord participe à leur activité... Et ensuite conseille-les !</p>
<br>
<p>Pour l'instant tu ne fais que les conseiller puis c'est tout,</p>
<p>Ce n'est pas la bonne façon de faire,</p>
<p>Tu ne fais que perdre ton temps et tes efforts pour rien...</p>
<p>Et à la fin les enfants ne t'écoutent pas,</p>
<p>La nouvelle façon que je te propose c'est de participer !</p>
<p>Participer comment ?</p>
<p>Tu me diras, je vais jouer avec eux à la playstation ?</p>
<p>Oui bien sûr :)</p>
<p>Fais-moi confiance et teste cette méthode,</p>
<p>Je vais vous raconter une histoire étonnante :</p>
<p>Une histoire du Prophète (bénédictions et paix soient sur lui)</p>
<p>Une fois une mère est venue se plaindre que son fils passe ses journées à écouter et à réciter des poèmes...</p>
<p>(à cette époque tous les poèmes n'étaient pas jolis... Il y'en avait des très vulgaires... C'était le début de l'islam, donc les nouvelles valeurs et les bonnes mœurs n'étaient pas encore bien installées)</p>
<p>Cet enfant était donc passionné de poésie,</p>
<p>Et toute la journée il était entrain d'en réciter,</p>
<p>Son nom était Amr ibn Oubay,</p>
<p>Sa mère ne savait plus quoi faire alors elle est venue chercher une solution chez le Prophète (bpsl)</p>
<p>On ferait quoi dans ce cas ?</p>
<p>On le punit, on le prive de sortie ?</p>
<p>Non... non... non... Ce n'est pas la méthode du Prophète (bpsl)</p>
<p>Sa méthode c'est de participer,</p>
<p>Que vas-tu faire Ô messager de Dieu ?</p>
<p>En marchant il a croisé Amr ibn Oubay, puis lui a demandé de monter derrière lui sur sa mule,</p>
<p>(C'est l'équivalent d'une voiture aujourd'hui, c'est un honneur de monter avec le Prophète (bpsl) sur sa mule)</p>
<p>Puis le Prophète (bpsl) lui a dit <strong class="prophete">« Alors Amr, que connais tu comme poésie ? »</strong></p>
<p>Amr s'est senti valorisé, il a répondu « Je connais beaucoup beaucoup ! »</p>
<p>Le Prophète (bpsl) lui a dit <strong class="prophete">« Récite moi un vers de poésie »</strong></p>
<p>C'est Amr lui-même qui raconte comment le Prophète (bpsl) l'a influencé,</p>
<p>Il dit « J'ai donc récité un vers »</p>
<p>Le Prophète (bpsl) m'a dit <strong class="prophete">« Excellent, rajoute un autre »</strong></p>
<p>J'ai donc récité un 2ème vers,</p>
<p>Le Prophète (bpsl) m'a dit <strong class="prophete">« Excellent, rajoute un autre »</strong></p>
<p>J'ai donc récité un 3ème vers,</p>
<p>Et il a continué à me demander de rajouter des vers, jusqu'à ce que j'en récite 100...</p>
<p>(100 vers prennent 20min ou 25min pour les réciter</p>
<p>Le Prophète (bpsl) a participé à l'activité favorite d'Amr ibn Oubay pendant 25 minutes)</p>
<p>Puis à la fin il m'a demandé <strong class="prophete">« Ô Amr, sais-tu quel est le meilleur vers de poésie ? »</strong></p>
<p>J'ai demandé lequel...</p>
<p>Le Prophète (bpsl) m'a répondu <strong class="prophete">« Chaque chose à part Dieu est nulle,</strong></p>
<p><strong class="prophete">Chaque grâce est inévitablement éphémère,</strong></p>
<p><strong class="prophete">Chaque bienfait disparaîtra sauf le Paradis »</strong></p>
<p>(A la fin il ne reste rien à Part Dieu... Et à la fin le seul bienfait qui reste est le Paradis...)</p>
<p>Le Prophète (bpsl) lui a donc appris à distinguer la beauté de la médiocrité en poésie...</p>
<p>Puis il lui a dit <strong class="prophete">« Ô Amr, écoute et sélectionne »</strong></p>
<br>
<p>Qu'elle est belle la méthode du Prophète (bpsl)</p>
<p>Il l'a conseillé après avoir participé,</p>
<p>Et après avoir pris le temps de l'écouter,</p>
<p>Et après de lui avoir montré qu'il s'intéressait à lui...</p>
<br>
<p>C'est une méthode en or à tous les parents !</p>
<p>Jusqu'à quand on va continuer la manière forte avec nos enfants « Tu vas m'écouter, tu vas faire ce que je te dis, et tu vas l'appliquer point final ! »</p>
<p>Vous voyez la différence entre l'efficacité de cette méthode et la méthode Prophétique ?</p>
<p>La méthode du Prophète est très très très belle,</p>
<p>Car elle est bâtie sur le respect, l'écoute, le partage,</p>
<p>Puis à la fin, conseille et dirige en douceur...</p>
<p>Si on fait la même chose avec nos enfants, je vous jure qu'un sourire d'espoir se dessinera sur nos visages et sur les leurs aussi :)</p>
<p>Essayez dès maintenant cette nouvelle méthode en participant aux activités de vos enfants,</p>
<p>Et je vous félicite par avance pour le sourire d'espoir que vous dessinerez chez vous :)</p>
<br>
<p>Merci beaucoup,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
