                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est consacré aux mamans,</p>
<p>Aimes-tu ta maman ?</p>
<p>Cherches-tu sa satisfaction ?</p>
<p>Es-tu tendre avec ta maman ?</p>
<p>Tu as fait quoi dernièrement pour la rendre heureuse ?</p>
<p>A Quand remonte le dernier câlin,</p>
<p>La dernière fois que tu as embrassé sa main,</p>
<p>La dernière parole gentille...</p>
<p>Ta maman est très très très chère... Profite de sa présence pendant qu'elle est encore là.</p>
<p>Et surtout fais extrêmement attention à ne jamais la faire pleurer.</p>
<p>Je vous jure mes chers, il n y'a rien qui satisfait Dieu comme le bon comportement envers nos mères.</p>
<p>Tu veux la garantie de vivre heureux ici bas ?</p>
<p>Gagne la satisfaction de ta maman.</p>
<p>J'ai vu plein d'hommes d'affaires, quand je cherche la raison de leur succès, je me rends compte qu'ils n'ont pas une intelligence supérieure à la normale, ni des capacités supérieures aux autres... Mais alors quel est le secret de leur réussite ?</p>
<p>Je trouve toujours que c'est parce que leurs mamans sont satisfaites d'eux.</p>
<p>Rends ta maman heureuse, et tu réussiras dans ta vie personnelle et professionnelle !</p>
<p>Un poète palestinien appelé Mahmoud Darwich a été emprisonné dans une période de sa vie,</p>
<p>Sa maman est venue lui rendre visite avec un peu de café et du pain,</p>
<p>Elle voulait voir son fils, le toucher, se rassurer...</p>
<p>Elle est tombée sur un gardien de prison très dur,</p>
<p>Il a refusé qu'elle remette à son fils le café et le pain,</p>
<p>Il a même refusé qu'elle le touche...</p>
<p>Il l'a renvoyée en pleurs...</p>
<p>Mahmoud Darwich a alors écrit ces vers très émouvants :</p>
<p>« J'ai la nostalgie du café de ma mère,</p>
<p>Du pain de ma mère,</p>
<p>(Que le gardien a refusé de lui remettre)</p>
<p>Des caresses de ma mère...</p>
<p>Et l'enfance grandit en moi,</p>
<p>Jour après jour,</p>
<p>(Plus on grandit, plus on a envie de poser notre tête sur la poitrine de notre maman, et revenir en enfance)</p>
<p>Et je chéris ma vie, car</p>
<p>Si je mourais,</p>
<p>J'aurais honte des larmes de ma mère !</p>
<p>(Je m'accroche à la vie, pour ne pas chagriner ma maman si je décède avant elle) »</p>
<p>De très belles paroles que tu peux écrire sur une carte à offrir, ou envoyer par message à ta mère :</p>
<p>« J'ai la nostalgie du café de ma mère,</p>
<p>Du pain de ma mère,</p>
<p>Des caresses de ma mère...</p>
<p>Et l'enfance grandit en moi,</p>
<p>Jour après jour,</p>
<p>Et je chéris ma vie, car</p>
<p>Si je mourais,</p>
<p>J'aurais honte des larmes de ma mère ! »</p>
<br>
<p>Mon cher prend soin de ta maman, car tu ignores comment ta vie changera le jour où tu vas la perdre.</p>
<p>Personnellement je prie Dieu « Ô mon Dieu, allonge la vie de ma mère »</p>
<p>Car tant qu'elle est en vie et qu'elle pense à moi dans ses prières, je continuerai à réussir.</p>
<p>Quand j'ai des soucis et des inquiétudes,</p>
<p>Quand je dois faire face à de grandes responsabilités,</p>
<p>Je téléphone à ma maman pour lui demander si elle prie pour moi,</p>
<p>Elle me dit oui qu'elle ne m'oublie pas dans ses prières,</p>
<p>Je lui demande si elle est satisfaite de moi,</p>
<p>Elle me répond « Oui je suis satisfaite de toi »</p>
<p>Je vous jure que dès que je raccroche, je suis solide comme le fer, plein de sérénité et d'énergie !</p>
<p>Dès la fin de cet épisode téléphone à ta maman, demande lui si elle est satisfaite de toi, demande lui de te faire tes prières,</p>
<p>Si elle est à côté de toi, embrasse sa main, cherche à égayer sa journée,</p>
<p>Et méfie-toi énormément de sa colère,</p>
<p>Si elle est fâchée contre toi, Dieu le sera aussi.</p>
<p>Et tant que ta maman est satisfaite de toi, tu auras un sourire d'espoir dans ta vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
