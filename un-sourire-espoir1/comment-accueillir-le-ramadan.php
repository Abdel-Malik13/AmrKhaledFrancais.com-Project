                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>

<p>L'épisode d'aujourd'hui dit que le mois de Ramadan est à l'approche...</p>
<p>Laisse-moi donc te poser une question :</p>
<p>Es-tu prêt pour le Ramadan ?</p>
<p>Que comptes-tu faire ?</p>
<p>Quel est ton plan ?</p>
<p>As-tu commencé à te préparer ?</p>
<p>Je sais qu'il y'en a qui me diront « Il reste encore beaucoup de jours avant le Ramadan...</p>
<p>Et tu commences déjà à nous demander si on est prêts ?</p>
<p>Attendons d'abord qu'il arrive... »</p>
<p>Non mon cher, ce n'est pas la bonne façon de procéder,</p>
<p>Sais-tu ce que la plupart de nous font ?</p>
<p>Le premier jour du Ramadan, on est très motivés... On part tous prier à la mosquée... On jeune.... On lit le noble Coran... On multiplie les adorations...</p>
<p>Puis au bout d'une semaine, tout le monde reste à la maison ;)</p>
<p>Souviens-toi comment la mosquée se vide la 2ème semaine de chaque Ramadan...</p>
<p>La 1ère semaine il y'a foule, tellement peu de places que certains sont obligés de prier dehors,</p>
<p>Puis petit à petit la mosquée se vide... se vide... se vide...</p>
<p>Entre le 10 et le 20ème jour du Ramadan, il y'a très peu de monde à la mosquée...</p>
<p>Puis quand les 10 derniers jours arrivent, la mosquée est remplie de monde,</p>
<p>Dans les nuits impaires car il y'a de fortes chances que la nuit du destin tombe dans ces nuits là...</p>
<p>Et malheureusement moins de monde dans les nuits paires...</p>
<p>Mon cher il faut prendre le Ramadan en entier, ce n'est pas une sélection qu'on fait...</p>
<p>Pour comprendre le problème je vais te donner un exemple du monde du football,</p>
<p>Pour les amoureux du ballon rond ;)</p>
<p>Quel est ton avis sur une équipe qui se qualifie pour la coupe du monde, ou pour la coupe d'Europe,</p>
<p>Puis les joueurs commencent la compétition sans s'être entrainés avant ?</p>
<p>Ils arrivent pleins de motivation, avec l'envie de gagner,</p>
<p>Mais ils ne se sont pas entrainés...</p>
<p>Le résultat c'est que sur le terrain ils vont vite s'essouffler, il y'aura des blessures et des crampes...</p>
<p>Dès le 2ème match ils commenceront à perdre et se décourager...</p>
<p>C'est la même idée avec le mois de Ramadan,</p>
<p>La semaine qui précède ce mois devrait être une semaine de préparation,</p>
<p>Comment se préparer alors ?</p>
<p>Mon cher, fais deux ou trois adorations avec Dieu...</p>
<p>Comme quoi ?</p>
<p>Comme donner des aumônes avec l'intention de commencer le Ramadan en étant généreux,</p>
<p>Ou si tu as un grand pêché, repentis-toi et adresse toi à Dieu en disant que tu as décidé de l'arrêter,</p>
<p>Ou si tu n'as pas l'habitude de lire le noble Coran, commence à lire un peu de Coran cette semaine,</p>
<p>Histoire qu'au début du Ramadan, tu ne partes pas de zéro...</p>
<p>Prie 2 unités de prières la nuit,</p>
<p>Lis un peu de Coran avec ton mari,</p>
<p>Fais des doua'a et des invocations...</p>
<p>Ou sinon j'ai une autre idée,</p>
<p>Ramène une feuille et un stylo,</p>
<p>Et note ce que tu comptes accomplir avec Dieu pendant ce Ramadan,</p>
<p>Pour que Dieu te facilite et qu'il t'aide à atteindre la nuit du destin, qu'il te pardonne et réponde à tes doua'a...</p>
<p>Note donc sur une feuille « Je vais faire 1, 2, 3, 4,5 et 6 »</p>
<p>Je vais téléphoner à mes proches,</p>
<p>Ou je vais organiser un diner et rassembler les membres de ma famille pour retisser les liens,</p>
<p>Et je vais satisfaire mon papa et ma maman,</p>
<p>Et j'ai des souhaits que j'aimerai que Dieu me réalise,</p>
<p>Et je vais noter ces souhaits et insister dans mes doua'a durant tout le mois...</p>
<p>Et je lirai le Coran en entier une fois ou deux pendant ce mois,</p>
<p>Et je donnerai cette somme d'argent à des pauvres...</p>
<p>Ainsi tu auras établi un plan de travail, que tu vas accrocher dans ta chambre,</p>
<p>Tu seras bien préparé quand le Ramadan sera arrivé,</p>
<p>Tu seras prêt mentalement,</p>
<p>Mes chers préparez-vous...</p>
<p>Préparez-vous car c'est le mois du pardon et de la miséricorde,</p>
<p>Préparez-vous pour que Dieu nous accepte et soit satisfait de nous, qu'il réalise nos doua'a, qu'il nous aide à atteindre la nuit du destin,</p>
<p>L'affaire a besoin de préparation,</p>
<p>C'est pour cette raison que dans cet épisode je tenais à vous dire « Soyons prêts tous ensemble ».</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
