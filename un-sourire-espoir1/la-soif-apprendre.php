                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un sourire d'espoir,</p>
<p>Le sourire d'aujourd'hui a un rapport avec l'apprentissage...</p>
<p>Des personnes étudient, des personnes révisent, et des étudiants, et des écoles, et des facultés, et des diplômes... etc etc</p>
<p>Tout un grand système d'éducation.</p>
<p>Mais j'ai envie de te poser une question, mais répond honnêtement... Aimes tu réellement apprendre, ou c'est juste le diplôme au bout qui te motive ?</p>
<p>Aimes tu la science, ou aimes tu les mentions ?</p>
<p>Tes enfants sinon, tu leurs dis quoi ? Ramenez de nouvelles connaissances, ou bien ramenez de bonnes notes ?</p>
<p>Et vous les jeunes, vous aimez apprendre, ou vous aimez les bonnes notes ?</p>
<p>Faisons attention, tout notre souci, c'est comment rassembler les bonne notes, pour sauter par-dessus le mur du bac...</p>
<p>Mais je vais vous dire une chose, une nation qui n'aime pas la science, ne pourra jamais aller loin.</p>
<p>Je vais vous raconter une histoire vraie,</p>
<p>On avait demandé à l'imam chafi'i , quel effet ca te fait quand tu apprends de nouvelles connaissances que tu ne connaissais pas avant ?</p>
<p>Il leur a répondu « quand j'entend pour la première fois de nouvelles connaissances que j'ignorais avant, je désire que tous les membres de mon corps se transforment en oreilles, pour savourer ce que j'entends autant que mes 2 oreilles savourent ces nouvelles informations »</p>
<p>C'est ce qu'on appel une personne qui aime apprendre !</p>
<p>On raconte aussi qu'un de nos ancêtres, pendant qu'il mangeait, une goute d'huile est tombée sur ses vêtements, alors il a eu honte de sortir avec cette tâche devant les gens... Et vu qu'il n'avait pas le temps de la laver, il a ramené de l'encre et l'a fait couler dessus, en disant « une tache d'encre, est plus honorable qu'une tache d'huile »</p>
<p>Parce que dans la mentalité de son époque, la science est quelque chose de grand...</p>
<p>Et nous mes chers, on préfère laquelle ? une tache d'huile ou une tache d'encre ?</p>
<p>Nous sommes entrain de manger à longueur de journée ;) et de l'huile coule tout le temps ;)</p>
<p>Mes chers, une nation qui n'aime pas apprendre, ne pourra jamais aller loin.</p>
<p>On dit aussi qu'un jour un jeune très motivé est parti voir Aristote le célèbre philosophe,</p>
<p>En lui disant , j'aimerai apprendre, j'aimerai apprendre... En insistant</p>
<p>Alors Aristote lui a dit, mon fils tu n'as pas l'air de vouloir apprendre, c'est juste de l'excitation...</p>
<p>Le jeune lui a dit, non je suis sérieux, j'aimerai apprendre pour de vrai...</p>
<p>Alors Aristote lui a demandé de le suivre vers une rivière.</p>
<p>Une fois arrivés, Aristote a pris la tête du jeune et l'a mise sous l'eau, et le jeune paniquait, son souffle allait se couper... Alors Aristote l'a sorti, le jeune lui a dit « tu es fou, tu allais me tuer ! »</p>
<p>Aristote lui a alors posé la question « Quand tu te noyais, quelle était la chose que tu désirais le plus ? »</p>
<p>Le jeune lui a dit « le souffle, l'air, pouvoir respirer »</p>
<p>Aristote lui a alors dit « si ton désir d'apprendre, est au même niveau que ton désir de respirer, alors tu seras mon apprenti»</p>
<p>L'idée est arrivée ?</p>
<p>Comprenez vous ce qui fait la fierté d'une nation et la grandeur d'une civilisation ?</p>
<p>A tous les jeunes, à tous les parents, nous voulons changer cette mentalité, aimons apprendre , plus que nous aimons obtenir des diplômes.</p>
<br>
<p>Prenez soin de vous, et à bientôt.</p>
