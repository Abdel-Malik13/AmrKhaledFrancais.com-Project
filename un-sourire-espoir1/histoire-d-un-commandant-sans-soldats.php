                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Le sourire d'aujourd'hui nous vient d'Al charkia (une région en Egypte)</p>
<p>C'est une petite anecdote mais vous comprendrez vite ou je veux en venir,</p>
<p>Hussein est un jeune homme qui m'a raconté qu'il avait une nièce de 5 ans,</p>
<p>Cette petite fille s'appelle Dalia, et lui est donc son oncle,</p>
<p>Une fois Hussein est parti voir Dalia, puis lui a pris son jouet...</p>
<p>Il l'a caché dans son dos... Pour la taquiner un peu...</p>
<p>Dalia voulait donc récupérer son jouet,</p>
<p>Alors son oncle a mis sa main dans sa bouche,</p>
<p>Et lui a dit je viens de le manger :)</p>
<p>Dalia : tu as mangé mon jouet ?? Je vais te ramener mon autre oncle !</p>
<p>Hussein : J'ai mangé tout à l'heure ton autre oncle...</p>
<p>Dalia : Je vais te ramener ma maman !</p>
<p>Son oncle lui a répondu : Ta maman aussi je l'ai dévorée tout à l'heure...</p>
<p>Dalia : Dans ce cas je te ramène ma tante....</p>
<p>Hussein : Si tu la ramènes je la mangerai aussi !</p>
<p>Dalia : Je vais prendre un bâton pour te taper !</p>
<p>Hussein : Je vais aussi manger ton bâton...</p>
<p>Dalia : Je vais te ramener mon Papa !</p>
<p>Hussein : Je vais aussi le manger...</p>
<p>Alors en toute innocence Dalia lui a dit : Dans ce cas tu vas rester seul... Tu n'auras plus personne autour de toi...</p>
<br>
<p>Ce que je veux vous dire, c'est que celui qui veut être seul sur la photo...</p>
<p>Celui qui veut dominer et prendre un maximum de place...</p>
<p>A la fin il se retrouve seul...</p>
<p>Il ne reste plus personne autour de lui...</p>
<br>
<p>Il existe une culture qui s'appel « la culture du vivre seul »</p>
<p>Celui qui veut éteindre les lumières des gens autour de lui... Pour que sa lampe reste la seule allumée...</p>
<p>Mon cher, pour que ta lumière soit visible, ce n'est pas une condition que les autres lumières soient ternes ou éteintes...</p>
<p>Ta lampe peut briller en même temps que les lampes des autres :)</p>
<p>Pourquoi par moments, nous voulons effacer les gens autour de nous, pour rester les seuls sur la photo...</p>
<p>J'ai beaucoup été touché par l'innocence de la petite Dalia,</p>
<p>Quand elle a dit : Quand tu auras mangé tout le monde, tu resteras seul...</p>
<p>Tu serais heureux s'il ne restait plus que toi ?</p>
<p>Quand j'étais petit, j'ai le souvenir d'avoir lu un livre qui s'intitulait « Un commandant sans soldats »</p>
<p>C'est l'histoire d'un commandant qui a éliminé ses soldats un par un...</p>
<p>Et à la fin il ne restait plus que lui...</p>
<p>Quel est l'intérêt d'être commandant quand il n y'à plus de soldats ?</p>
<p>Le terme « commandant » n'a plus de valeur :)</p>
<p>N'éteignez pas les lampes des gens autour de vous mes chers,</p>
<p>Laissez leurs lampes allumées,</p>
<p>Et ne soyez jamais jaloux,</p>
<p>Ne pense jamais : Il faut que j'éteigne sa lampe, comme ca la mienne sera plus lumineuse...</p>
<p>Si ca arrive, je te jure que tu n'en seras pas heureux...</p>
<p>Le bonheur vient quand les lampes de tout le monde sont allumées,</p>
<p>Ce n'est pas celui qui mange les autres qui reste à la fin...</p>
<p>A la fin il n y'à que tes bonnes œuvres qui restent,</p>
<br>
<p>Allumons donc les lampes des personnes autour de nous,</p>
<p>Et dessinons ensemble un large sourire d'espoir :)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
