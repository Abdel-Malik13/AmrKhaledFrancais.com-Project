                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>L'anecdote d'aujourd'hui est très personnelle,</p>
<p>C'est quelque chose que j'ai vécu quand j'avais 20ans,</p>
<p>J'étais tout jeune, et aucun moment je n'imaginais que je serai là entrain de vous parler devant un microphone...</p>
<p>Mais un jour, à un instant précis j'avais fais une demande à Dieu...</p>
<p>A L'époque même moi je ne comprenais pas le sens de cette demande,</p>
<p>Je ne maitrisais pas encore l'art de parler,</p>
<p>Mais je me suis surpris à dire « Ô Allah, fais revivre avec moi la tradition du prophète sws... Et étend ma voix à tous les horizons »</p>
<p>Après avoir dis cette phrase, j'étais le 1er étonné,</p>
<p>Ce sont de grandes paroles, comment j'ai pu les prononcer ?</p>
<p>Mon Dieu étend ma voix à tous les horizons...</p>
<p>J'ai tellement aimé cette phrase que je l'ai beaucoup répétée,</p>
<p>Et durant tout mon séjour à Médine, j'ai insisté en la répétant...</p>
<p>Et Dieu m'a accordé ce que je désirais !</p>
<br>
<p>Et il y a une semaine, je me trouvais encore en Arabie Saoudite,</p>
<p>Je suis entré dans une librairie pour acheter des livres,</p>
<p>Et je suis tombé sur un ouvrage intitulé « la chimie de la prière »</p>
<p>Le titre a attisé ma curiosité , je l'ai donc acheté, mais c'était la 1ère fois que je voyais cet écrivain...</p>
<p>Ahmed-Al-Khairi-Al-Omari</p>
<br>
<p>J'ai lu ce livre, et j'y ai trouvé des choses intéressantes,</p>
<p>Je m'en suis servi pour préparer le discours d'une conférence,</p>
<p>Et ca a été filmé, puis posté sur mon site amrkhaled.net
<p>La surprise c'est que l'écrivain de l'ouvrage est tombé sur cette vidéo...</p>
<p>Il m'a appelé en disant « Je suis Ahmed-Al-Khairi-Al-Omari »</p>
<p>J'étais le premier étonné :)</p>
<p>Il a poursuivi « J'ai vu une de tes conférences où tu parles de mon livre »</p>
<p>J'ai dis « vous êtes le bienvenu »</p>
<p>Il m'a répondu « J'ai fais une demande à Dieu il y a 10 ans »</p>
<p>Et quelle était cette demande ?</p>
<p>Il m'a répondu « J'ai demandé à Dieu de transmettre l'encre de mon stylo jusqu'à la gorge de cet homme, qui a demandé à ce que sa voix soit étendue à tous les horizons... C'était il y a 10 ans, j'ai fini par oublier cette demande... Mais Dieu lui ne l'a pas oubliée...</p>
<p>Avec ma femme donc on regardait ta conférence sur internet, quand tu as cité mon nom et le titre de mon livre...</p>
<p>Et c'est à ce moment que je me suis rappelé de la demande que j'avais faite à Dieu,</p>
<p>« Transmettre l'encre de mon stylo jusqu'à la gorge de cet homme... »</p>
<p>Comment Dieu réalise nos demandes sans qu'on y fasse attention...</p>
<p>Dieu ne laisse échapper aucune demande...</p>
<p>Peu importe ta religion,</p>
<p>Si tu as besoin de quelque chose, Demande à Dieu et il te le donnera...</p>
<p>Toi qui m'écoute en conduisant ta voiture, ou dans ta chambre,</p>
<p>Juste entre toi et Dieu,</p>
<p>Demande lui au fond de toi de régler tel problème, ou t'accorder telle chose...</p>
<p>Et ne pense jamais que ce que tu demandes est difficile...</p>
<p>Et ne pense jamais que les demandes doivent être dans un endroit sacré...</p>
<p>Pas du tout !</p>
<p>Dans ta chambre ou dans ta voiture... L'essentiel c'est la sincérité dans ta demande, et que ca vienne du cœur...</p>
<p>Je te jure que Dieu t'accordera ce que tu lui demandes !</p>
<p>Regarde avec quelle force je te le dis...</p>
<p>Je te jure que Dieu te donnera tout ce que tu lui demandes !</p>
<p>Mon anecdote avec cet écrivain a soulevé mon cœur...</p>
<p>Il avait oublié sa demande... Mais Dieu ne l'a pas oubliée,</p>
<p>Et moi aussi j'ai fais une demande immense quand j'avais 20ans ,et aujourd'hui elle est réalisée devant vos yeux...</p>
<p>Dieu ne laisse passer aucune demande !</p>
<p>Donc garde un sourire d'espoir, et multiplie tes demandes, et sois serein... Dieu te donnera tout ce que tu souhaites.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
