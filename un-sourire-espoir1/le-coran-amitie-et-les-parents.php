                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                        
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>Cet épisode est dédicacé à tous les jeunes,</p>
<p>Filles et garçons,</p>
<p>Je parle souvent du bon comportement envers les parents,</p>
<p>Aujourd'hui je vais parler de quelque chose de nouveau :</p>
<p>Comment devenir ami avec ton papa et ta maman...</p>
<p>Mais de véritables amis !</p>
<p>J'adresse ces paroles autant aux enfants qu'aux parents,</p>
<p>La vie est devenue difficile... Nous avons tous besoin d'avoir un ami sur qui compter,</p>
<p>Et qui est mieux placé que notre papa et notre maman ?</p>
<p>Ca va vous étonner, mais à chaque fois que le saint Coran parle des relations avec les parents,</p>
<p>Il en parle avec le mot « copain » ou « ami »</p>
<p>On pense tous que c'est un nouveau concept, ou que ca vient de l'occident,</p>
<p>Mais non !</p>
<p>C'est un concept qui est dans le saint Coran,</p>
<p><strong>{...Wa Sahibhuma fi a'dounya maaroufa...}</strong></p>
<p>Sourate 31 -- Luqmân (verset 15)</p>
<p>Qui signifie accompagne les de façon convenable...</p>
<p>Sahibhuma vient de Sahib</p>
<p>En arabe ca signifie copain ou ami,</p>
<p>D'où le choix l'expression « accompagne-les » plutôt que « vis avec eux », ou « comporte-toi avec eux »... d'une façon convenable.</p>
<p>Sois copain ou copine avec tes parents,</p>
<p>Et sois copain ou copine avec tes enfants,</p>
<p>Ce n'est pas vis avec eux, en t'enfermant dans ta chambre 24/24,</p>
<p>En leur parlant à peine chaque jour...</p>
<p>Et parfois ils doivent batailler pour obtenir une parole ou un sourire venant de toi...</p>
<p>Non, accompagne les, sois leur copain...</p>
<br>
<p>Elle est étonnante cette histoire du Prophète (paix et bénédictions soient sur lui)</p>
<p>Un jour un homme a demandé au Messager de Dieu (pbsl) : « Ô Messager de Dieu, Qui mérite le plus ma compagnie ? »</p>
<p>Le Messager de Dieu (pbsl) a répondu : <strong class="prophete">« Ta mère »</strong></p>
<p>L'homme a demandé « Puis qui d'autre ? »</p>
<p>(l'air de dire qu'il parlait de copains... d'amis... )</p>
<p>Le Messager de Dieu (pbsl) a répondu : <strong class="prophete">« Puis ta mère »</strong></p>
<p>L'homme a demandé une troisième fois « Puis qui d'autre ? »</p>
<p>(L'air de dire j'ai compris qu'il fallait bien me comporter avec ma maman... Mais pour les amis je choisis qui ? )</p>
<p>Le Messager de Dieu (pbsl) a répondu pour la troisième fois : <strong class="prophete">« Puis Ta mère »</strong></p>
<p>L'homme a demandé une quatrième fois « Puis qui d'autre ? »</p>
<p>Le Messager de Dieu (pbsl) a répondu: <strong class="prophete">« Puis Ton père »</strong></p>
<p>L'homme s'est alors tu... Il a compris que ses deux meilleurs amis dans la vie devaient être sa mère et son père...</p>
<br>
<p>Et toi qui m'écoute, es tu ami avec tes parents ?</p>
<p>Crois moi il n y'à pas une chose plus plaisante que cette amitié.</p>
<p>Tu me diras « Ce sont mes parents qui ne m'en donnent pas l'occasion, tout de suite ils me disputent... »</p>
<p>Je te dirai fais le premier pas comme Itar...</p>
<p>Irar est une jeune femme que je connais,</p>
<p>Son père était toujours occupé par son travail,</p>
<p>Un jour elle est allée le voir directement dans son bureau,</p>
<p>Il lui a demandé « Que se passe-t-il ? Pourquoi tu n'es pas en classe ? »</p>
<p>Elle a dit « Je voulais juste te parler et passer du temps avec toi »</p>
<br>
<p>Comme elle, va vers tes parents et deviens leur copain,</p>
<p>Tu verras que vos relations ne seront plus jamais pareilles,</p>
<p>Et que le foyer deviendra plus chaleureux.</p>
<br>
<p>Et toi le papa ou la maman qui m'écoute,</p>
<p>Je t'en prie, je veux que tu saches que tes enfants ont beaucoup besoin de toi,</p>
<p>Si tu as des amis partout, mais que tu n'es pas ami avec ton enfant... Tu ressentiras un vide dans ta vie.</p>
<br>
<p>Je connais un célèbre acteur qui disait « Où est l'intérêt que je sois célèbre en Chine et au Japon... Que tout le monde viennent me parler... Si quand je rentre chez moi je ne trouve pas mon fils et que j'ai du mal à communiquer avec lui... »</p>
<p>A tous les parents, ne laissez pas la vie vous éloigner de vos enfants,</p>
<p>Ou les problèmes du quotidien vous empêcher de devenir copains avec vos enfants...</p>
<br>
<p>A toutes les mamans, ce n'est pas des disputes et des ordres toute la journée...</p>
<p>Prend le temps de devenir copine avec ton fils ou ta fille,</p>
<p>Sortez ensemble,</p>
<p>Rigolez ensemble,</p>
<p>Dinez dehors ensemble...</p>
<br>
<p>N'attendez pas que l'autre fasse le premier pas,</p>
<p>Ose l'inviter à jouer ou à sortir,</p>
<p>Achète des billets pour l'inviter au cinéma,</p>
<br>
<p>Essayez et vous verrez le sourire d'espoir qui se dessinera dans toute la maison :)</p>
<br>
<p>Appliquons les versets du saint Coran et les paroles du Prophète (pbsl),</p>
<p>Et devenons copains entre parents et enfants.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
