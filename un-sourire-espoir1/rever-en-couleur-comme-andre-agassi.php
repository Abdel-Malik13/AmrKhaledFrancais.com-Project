                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui est une question...</p>
<p>C'est toujours moi qui raconte, alors laisse-moi cette fois te poser une question :)</p>
<p>As-tu défini ton but dans la vie ?</p>
<p>L'as-tu noté sur une feuille ?</p>
<p>Que tu sois un homme ou une femme peux-tu me dire quel est ton but dans la vie ?</p>
<p>Il faut absolument que tu me donnes une réponse...</p>
<p>Que tu sois chez toi, devant la radio ou sur internet,</p>
<p>Si tu n'as pas fixé un objectif dans la vie alors c'est une catastrophe !</p>
<p>Que pensez-vous de quelques personnes qui ont pris l'avion,</p>
<p>Ils partaient en voyage,</p>
<p>Quand l'avion a décollé, une de ces personnes est allée voir le commandant de bord en lui disant « Excusez-moi, où est-ce qu'on va au juste ? »</p>
<p>Que penserez-vous de cette personne :)</p>
<p>Et pire imaginez que le commandant de bord lui répond « Je ne sais pas exactement, on verra bien où on va atterrir en fonction du vent, et en fonction du carburant qu'il va nous rester »</p>
<p>Ca t'a fait rire n'est pas ?</p>
<p>:)</p>
<p>Est-ce qu'on est comme ces personnes dans l'avion ?</p>
<p>Aux états unis, quand les enfants sont encore dans le primaire on leur demande de définir un but...</p>
<p>C'est une séance toutes les semaines qui s'appelle « le but »</p>
<p>L'enfant est encore trop jeune pour comprendre, alors il écrit n'importe quoi...</p>
<p>« Je veux devenir le meilleur ingénieur informatique, je veux devenir comme Bill Gates »</p>
<p>L'enseignant va voir les parents de l'enfant,</p>
<p>Et leur dit « Votre enfant s'intéresse à l'informatique, il faut lui donner un ordinateur, et suivre sa progression pendant les vacances d'été »</p>
<p>A la rentrée de l'année suivante, l'enseignant demande où il en est...</p>
<p>S'il n'a rien fait pendant les vacances on lui dit « Tu as choisi un but qui ne te passionne pas vraiment... Choisis un autre but »</p>
<p>Imagine une séance chaque semaine... Chaque semaine... Chaque semaine... Ce qui va se passer dans la tête de cet enfant une fois arrivé au lycée ?</p>
<p>Vous comprenez pourquoi ils font des multinationales,</p>
<p>Vous comprenez pourquoi un homme du Kentucky qui vendait du poulet dans la rue, avait un but</p>
<p>« Pourquoi ne pas vendre mon poulet et exporter ma recette partout dans le monde, et mettre ma photo dans tous les pays »</p>
<p>C'était son but et il l'a atteint, aujourd'hui il existe des restaurants KFC partout dans le monde...</p>
<p>C'est possible que toi tu n'aies pas de but dans la vie ?</p>
<p>Peux-tu non seulement le noter sur une feuille, mais aussi le voir en couleur ?</p>
<p>Ca veut dire quoi le voir en couleur ?</p>
<p>Le voir en 3D...</p>
<p>Voir et vivre ton rêve dans toutes ses dimensions,</p>
<p>L'imaginer avec tous ses détails,</p>
<p>Comme un joueur de tennis très connu qui s'appelle Andre Agassi,</p>
<p>Il a remporté des dizaines de titres dans sa carrière...</p>
<p>Mais la première fois qu'il a gagné le tournoi de Wimbledon il avait 22ans,</p>
<p>Pendant l'interview qui suit la victoire, le journaliste lui a demandé « Quel est votre sentiment, après avoir remporté le tournoi de Wimbledon pour la première fois de votre vie ? »</p>
<p>Andre Agassi lui a répondu « En réalité ce n'est pas la première fois, c'est la 10ème fois »</p>
<p>Tout le monde a rit, en pensant que c'était une blague,</p>
<p>Il leur a dit « Je ne plaisante pas, ca fait réellement 10ans que je rêve de ce jour, je connaissais la couleur du t-shirt que j'allais porter, j'entendais les applaudissements du public, je me voyais tenir la coupe... Je vivais ce rêve pendant 10ans »</p>
<p>Toi aussi rêve, fixe un but, note-le et vis pour l'atteindre,</p>
<p>Tu savoureras la vie quand tu auras un but.</p>
<p>Et tu comprendras vraiment le sens d'un «sourire d'espoir ».</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
