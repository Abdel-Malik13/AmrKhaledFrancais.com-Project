                                        <br>
                                        <p>Quand la vie semble difficile, parfois nous avons besoin d'entendre une nouvelle idée qui nous aide,
entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne
qui a réussi...</p> <p>Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage
L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue à tous avec un sourire d'espoir,</p>
<p>Le titre du sourire d'aujourd'hui est « la sagesse d'un cheval »</p>
<p>C'est une délicieuse histoire.</p>
<p>On raconte qu'il y'avait un cheval utilisé par son propriétaire pour se déplacer , mais un jour le cheval est tombé dans un grand trou, le propriétaire a essayé de le sortir de là par tous les moyens mais en vain...</p>
<p>Le cheval a commencé à paniquer et à sentir que sa fin est proche, alors il s'est mis à crier et à se débattre dans tous les sens...</p>
<p>Le propriétaire s'est mis à réfléchir pour trouver une solution, il en est venu à la conclusion que le prix que ca va couter de sortir le cheval du trou, est plus cher que le prix du cheval lui-même...</p>
<p>Le cheval est vieux, le sortir de là est compliqué...</p>
<p>Alors il a décidé de l'enterrer vivant, le propriétaire ramenait donc de la terre pour refermer le trou...</p>
<p>Le cheval a vu la terre descendre d'en haut, il a commencé à comprendre qu'il allait mourir.</p>
<p>Le cheval s'est mis à crier encore plus fort, et se débattre encore plus fort... Et au bout d'un moment le cheval s'est calmé entièrement...</p>
<p>Le propriétaire continuait de jeter de la terre dans le trou, et le cheval n'avait plus aucune réaction.</p>
<p>Le propriétaire commençait à se poser des questions « le cheval est déjà mort ? que lui arrive t'il ?»</p>
<p>Il a regardé dans le trou, il a vu que le cheval était encore vivant, mais il l'a trouvé entrain de faire quelque chose d'étonnant...</p>
<p>A chaque fois que de la terre tombait sur le cheval, au lieu de crier et paniquer, il bougeait son dos pour que la terre qui était tombée dessus descende au niveau de ses sabots, puis grimpait dessus...</p>
<p>Et en répétant ca, le niveau de la terre montait, et le cheval aussi...</p>
<p>Et ce qui devait être la cause de sa mort, devenait la cause de son sauvetage... Et le cheval a fini par arriver au sommet et à sortir du trou !</p>
<p>C'est bien sur une histoire symbolique, pour nous dire « ne baisse jamais les bras devant les obstacles ! »</p>
<p>Peu importe comment la vie de ce bas monde est dure, ce que tu vois entrain de te faire du mal, peut être la source de ta réussite ou l'élément déclencheur pour te motiver à partir de l'avant...</p>
<p>Apprenons de la sagesse du cheval, la terre qui tombe sur ton dos, tu peux la mettre sous tes pieds pour grimper plus haut !</p>
<p>La sagesse de ce cheval, beaucoup de gens ont besoin de l'appliquer dans leurs vies.</p>
<p>Beaucoup de personnes paniquent, se plaignent, pleurent et dépensent toute leur énergie en faisant ca... Tout comme le cheval au début de l'histoire, puis une fois toute l'énergie dépensée, à la fin ils se retrouvent sans résultat.</p>
<p>Au lieu de dépenser notre énergie dans des pleurs, de l'inquiétude, la peur de l'avenir... dépensons notre énergie dans la construction d'un projet, dans quelque chose de positif qui améliorera notre situation...</p>
<p>Mes chers, je vous raconte cette histoire aujourd'hui parce que je suis sur que des millions de gens m'écoutent, s'ils prennent un pas positif dans le but d'améliorer leurs vies, même des petits pas, l'un après l'autre, même si ca prend du temps, à la fin on finira par s'en sortir comme le cheval...</p>
<p>Méditez sur cette histoire, prenez soin de vous, et à bientôt avec un nouveau sourire d'espoir.</p>
