                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle des relations familiales,</p>
<p>Des liens entre frères et sœurs,</p>
<p>Des relations entre parents et enfants,</p>
<p>Et des liens de parenté...</p>
<p>Croyez-moi si je vous dis que le fait de prendre soin des membres de votre famille, et une des plus grandes raisons pour lesquelles Dieu élargit ta subsistance, et te donne de plus en plus d'argent...</p>
<p>Je vous jure que c'est la vérité,</p>
<p>Tu veux que tes sources de revenus se multiplient et que ta situation financière s'améliore ?</p>
<p>Prends soin de ta famille, et essaye par tous les moyens de les rendre heureux,</p>
<p>Je vais vous raconter l'histoire de Haj Abdessalam,</p>
<p>Aujourd'hui c'est un homme très aisé financièrement avec plein de fermes et plein de projets...</p>
<p>Il me disait qu'avant il était quelqu'un aux revenus très modestes,</p>
<p>Il m'a juré que ce qu'il gagnait par mois lui et sa femme, ne suffisait même pas à payer le lait des enfants...</p>
<p>Une situation financière extrêmement difficile,</p>
<p>Puis petit à petit il a acheté un bout de terre à cultiver, histoire de trouver de quoi manger...</p>
<p>A ce moment sa sœur est arrivée en lui disant « Mon frère Abdessalam, je veux m'associer avec toi dans cette petite ferme, je te donne un peu d'argent et on devient associés... »</p>
<p>Il lui a répondu « C'est une minuscule ferme, il n y'a pas vraiment de place pour un associé »</p>
<p>Sa sœur est partie en parler à leur maman car leur père était décédé...</p>
<p>La maman est venue convaincre Abdessalam « C'est ta sœur, tu dois l'accepter, prend l'argent... »</p>
<p>Il a pris le peu d'argent en disant « C'est un tout petit bout de terre, c'est juste pour trouver de quoi manger... »</p>
<p>Il a accepté pour sa maman, mais au fond de lui il n'était pas du tout convaincu...</p>
<p>Il me raconte qu'un jour il est parti regarder dans des documents que son père avait laissés,</p>
<p>« Dans un dossier je suis tombé sur un papier, comme si Dieu me l'avait envoyé »</p>
<p>Le père y avait écrit que si un jour Dieu lui donnait de l'argent, il allait en donner spécialement à cette fille... Car elle en a besoin plus que les autres... etc</p>
<p>Le père avait noté un souhait, celui de laisser 1/7 de ce qu'il possède à cette fille,</p>
<p>Il était décédé, et cette feuille a été oubliée pendant des années...</p>
<p>Quand Haj Abdessalam a lu cette feuille, elle a touché son cœur, son père lui a beaucoup manqué,</p>
<p>Il a alors juré que si son projet s'agrandit, non seulement il sera associé avec sa sœur,</p>
<p>Mais il va lui donner 1/7 de ce qu'il possède.</p>
<p>Comme si Dieu a vu l'intention d'un homme qui a perdu son père, mais qui l'aime profondément,</p>
<p>Et cet amour pour son père a fait bouger le sentiment de fraternité envers sa sœur,</p>
<p>Il a eu un déclic, il s'est rendu compte que ce n'est pas une simple personne qui veut s'associer avec lui...</p>
<p>Non c'est sa sœur, c'est son droit, le même sang coule dans leurs veines...</p>
<p>Haj Abdessalam me disait « Ce jour là j'ai pris la décision de donner 1/ 7 de ce que je possède à ma sœur »</p>
<p>Quand Dieu a vu sa belle intention, les portes du ciel se sont ouvertes,</p>
<p>Il est devenu très très très riche... Et il a tenu sa parole, il a donné 1/7 ce qu'il possède à sa sœur,</p>
<p>Et le souhait d'un père ... Qui l'a écrit sur une simple feuille qui n'a rien d'officiel... s'est réalisé après son décès...</p>
<p>Mon cher veux-tu comme Haj Abdessalam, devenir aisé financièrement ?</p>
<p>Prends-soin de tes proches,</p>
<p>Veux-tu que Dieu multiplie ton argent ?</p>
<p>Ai de bonnes intentions pour ta famille,</p>
<p>Fais tout pour les rendre heureux,</p>
<p>Et Dieu dessinera un sourire d'espoir sur ton visage ainsi que sur les leurs.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
