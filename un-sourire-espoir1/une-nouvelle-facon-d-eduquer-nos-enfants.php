                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<p>Et une nouvelle façon d'éduquer nos enfants,</p>
<p>Surtout les adolescents de 14ans,15ans ou 16ans...</p>
<p>Quelle est donc cette façon ?</p>
<p>C'est que quand il commet une erreur, tu l'aides à corriger son erreur,</p>
<p>Au lieu de lui donner des ordres, va faire ca, tu as fais n'importe quoi...</p>
<p>Dis lui viens on va la corriger ensemble, à deux c'est plus facile,</p>
<p>Je vais te raconter une anecdote qui t'aidera à comprendre,</p>
<p>Je connais un père qui se considère ami avec son fils,</p>
<p>Il n'utilise donc pas le système de la punition, mais le système de l'amitié,</p>
<p>Il m'a raconté « Un jour je ramenais mon fils et son copain du club,</p>
<p>J'ai remarqué qu'ils n'étaient pas à l'aise,</p>
<p>Et mon fils a chuchoté quelque chose à l'oreille de son copain,</p>
<p>Donc je lui ai demandé ce qui se passait,</p>
<p>Tu as l'air d'avoir fait une bêtise,</p>
<p>Quand j'ai dis ca il a paniqué,</p>
<p>Donc j'ai eu la confirmation,</p>
<p>Bon tu me racontes ce que tu as fais ?</p>
<p>Il m'a demandé d'abord de lui promettre de ne pas me fâcher,</p>
<p>Je lui ai dit : promis je ne me fâcherai pas, nous sommes amis...</p>
<p>Mon fils m'a expliqué que le soir quand il était au club avec son copain, il ont vu la porte de la piscine ouverte, ils sont entrés, puis ils ont pris toutes les chaises et toutes les serviettes et les ont jetées dans l'eau...</p>
<p>Alors j'ai pensé quelle catastrophe, et j'allais m'énerver contre lui,</p>
<p>Mais mon fils m'a dit , souviens toi tu m'as promis avant que je te raconte...</p>
<p>Je me suis donc calmé et j'ai réfléchi à une solution,</p>
<p>Dieu m'a inspiré une très belle idée,</p>
<p>On va faire demi tour tous les trois, et on va retourner à la piscine pour tout remettre en place...</p>
<p>Et on est retournés en silence, personne ne disait plus un mot dans la voiture... »</p>
<p>Une fois arrivés, le père a retroussé ses manches,</p>
<p>Et tous les trois ils sortaient les chaises et les serviettes de l'eau,</p>
<p>Le père était trempé d'eau,</p>
<p>Le fils lui était aussi trempé mais de honte...</p>
<p>Son père n'a sorti aucun mot,</p>
<p>Aucune insulte,</p>
<p>Aucune critique,</p>
<p>Puis ils sont remontés dans la voiture avec de l'eau partout,</p>
<p>Et toujours aucun mot,</p>
<p>Avant d'arriver à la maison le fils commençait à pleurer...</p>
<p>Il pleurait de honte...</p>
<p>Une nouvelle façon d'éduquer nos enfants, plus puissante que mille enguelades,</p>
<p>Une fois arrivés devant la maison,</p>
<p>Le père a dit à son fils, descend et rentre à la maison sans moi,</p>
<p>Je ne me sens pas bien,</p>
<p>Je n'ai pas su bien t'éduquer,</p>
<p>Je suis le fautif,</p>
<p>(Il ne lui a pas dit tu es un mauvais garçon... Il a dit que c'était de sa faute à lui)</p>
<p>Le fils est resté triste pendant 2 jours, sans savoir comment s'excuser auprès de son père,</p>
<p>Le fils a fini par venir s'excuser,</p>
<p>Mais il en a tiré une grande leçon,</p>
<br>
<p>Une grande différence mes chers,</p>
<p>Entre crier et c'est tout... Sans savoir si la leçon est passée ou pas...</p>
<p>Et entre faire quelque chose de concret... Corriger la bêtise ensemble, et participer avec nos enfants...</p>
<p>Peux tu essayer au moins une fois ?</p>
<p>Sais tu ce qui va se passer ?</p>
<p>Tes enfants vont beaucoup t'aimer,</p>
<p>Ca créera de nouveaux liens entre vous,</p>
<p>Car tu leur auras à la fois appris, respecté, et montré ta grandeur,</p>
<br>
<p>Teste cette nouvelle façon, pense à moi, et ne m'oublie pas dans tes prières.</p>
<br>
<p>Un sourire d'espoir,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
