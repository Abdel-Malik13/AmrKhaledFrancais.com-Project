                                        <br>
                                        <p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>

<p>L'épisode d'aujourd'hui est très sentimental,</p>
<p>Si je te demande : Est-ce que tu aimes beaucoup ta famille ?</p>
<p>Tu me diras oui bien sûr, qui n'aime pas sa famille...</p>
<p>Et si je te demande : Est-ce que tu aimes beaucoup tes enfants ?</p>
<p>Tu me diras oui beaucoup beaucoup beaucoup...</p>
<p>Et ton épouse ou ton mari ?</p>
<p>Et tes parents ?</p>
<p>La réponse est connue d'avance.</p>
<p>Mais si je te demande : Est-ce que tu leur exprimes ton amour ?</p>
<p>Beaucoup de gens n'y arrivent pas,</p>
<p>On aime tous nos proches,</p>
<p>Mais on peut aimer autant qu'on veut, si on n'exprime pas cet amour, les gens ne s'en rendront pas compte...</p>
<p>Il existe des personnes qui ont l'air dures, mais de l'intérieur elles sont très sensibles, c'est juste qu'elles n'arrivent pas à exprimer ce qu'elles ressentent.</p>
<p>Je vais vous raconter l'histoire d'un homme qui était très émotionnel de l'intérieur, mais de l'extérieur il était dur comme une pierre,</p>
<p>Il n'arrivait pas à embrasser ses enfants,</p>
<p>Ni à dire des paroles gentilles,</p>
<p>Ni à sourire aux autres,</p>
<p>Il était sec.</p>
<p>Sa personnalité était comme ca, et sa dureté a causé plein de problèmes avec ses enfants qui se disaient « Il ne nous aime pas, il est sévère »</p>
<p>Du coup eux aussi se sont endurcis,</p>
<p>Et lui de son côté se demandait pourquoi ses enfants étaient si durs avec lui, alors qu'au départ c'est lui qui en est la cause.</p>
<p>Cette dureté grandissait avec le temps, jusqu'à ce qu'elle forme un énorme fossé dans la maison,</p>
<p>Ils vivaient comme des étrangers sous le même toit...</p>
<p>Cet homme avait un ami psychologue, il est parti tout lui raconter,</p>
<p>Le psychologue n'a pas utilisé avec lui une méthode compliquée,</p>
<p>Il lui a juste demandé de s'allonger,</p>
<p>Puis il lui a dit « Imagine que tu vas mourir à cet instant, c'est bête mais imagine juste... Si un membre de ta famille est ici devant toi, qu'aimerais-tu lui dire avant de quitter ce monde? »</p>
<p>L'homme a fermé ses yeux, il s'est mis à parler à son fils « Je t'aime fiston, tu es la chose la plus chère au monde, j'ai travaillé toute ma vie pour toi, je veux te transmettre toute mon expérience dans la vie...»</p>
<p>Puis il disait à sa fille « Tu es la fleur de ma vie, je t'aime de tout mon cœur... »</p>
<p>Il imaginait vraiment être sur le lit de sa mort,</p>
<p>Il exprimait tout ce qu'il avait à l'intérieur,</p>
<p>Et il semble que ce qu'il avait à l'intérieur était d'une grande beauté.</p>
<p>Plus il s'exprimait, plus il pleurait, et ce cœur de pierre s'attendrissait,</p>
<p>Il a pleuré... pleuré... pleuré...</p>
<p>A la fin le psychologue lui a dit une très belle phrase que je tenais à vous offrir,</p>
<p>« Que tu donnes une fleur à quelqu'un de vivant, vaut mieux que tu entoures sa tombe de fleurs après son décès... Va voir les gens que tu aimes pendants qu'ils sont encore là, et offre leur des fleurs de leur vivant»</p>
<br>
<p>Mes chers, exprimer vos sentiments à ceux que vous aimez,</p>
<p>Vous savez comment y arriver ?</p>
<p>Tout simplement en vous entrainant,</p>
<p>Comme le psychologue a entrainé cet homme,</p>
<p>Mais on ne va pas demander à chacun d'imaginer qu'il est sur le lit de sa mort pour s'exprimer,</p>
<p>Même si c'est dur au début, va voir ton fils, prend le dans tes bras et dis lui que tu l'aimes beaucoup...</p>
<p>Que dis-tu d'envoyer tout de suite un sms ou un email à ton papa pour lui dire qu'il est très important dans ta vie.</p>
<p>Pourquoi on a du mal à exprimer notre amour ?</p>
<p>Pourquoi cette difficulté ?</p>
<p>Pourquoi on n'imite pas le Prophète (bénédictions et paix soient sur lui) quand il prenait la main de Mou'ad Ibn Jabal (un compagnon) et lui disait d'abord <strong class="prophete">« Ô Mou'ad je t'aime »</strong> puis il le conseillait juste après.</p>
<p>Pourquoi ne pas être tendres avec les autres ?</p>
<p>Si tu es maman, est-ce raisonnable de ne pas avoir dit à tes enfants que tu les aimais depuis si longtemps ?</p>
<p>Le dernier câlin remonte à quand ?</p>
<p>L'être humain a besoin d'affection,</p>
<p>Les jeunes ont besoin d'affection,</p>
<p>Et savez-vous qui a encore plus besoin d'affection ?</p>
<p>Ce sont nos filles,</p>
<p>Si vous ne donnez pas de l'affection à vos filles dans la maison, elles iront la chercher dehors...</p>
<p>Comblez vos enfants de tendresse et d'affection.</p>
<p>Pareil avec ta femme ou ton mari,</p>
<p>Ton mari a besoin que tu lui donnes de l'amour, avant qu'il s'enferme sur lui-même, et que tu dises « Il a changé, ce n'est plus le même homme que j'ai épousé »</p>
<p>Donne de l'affection à ton papa et à ta maman, les pauvres ils ont sacrifié leurs vies pour toi...</p>
<p>Ne sois pas gêné d'exprimer tes sentiments et dessine un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
