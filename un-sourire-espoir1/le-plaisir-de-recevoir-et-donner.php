                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec une nouvelle parole, un nouveau sourire d'espoir dans une nouvelle journée.</p>
<p>La parole d'aujourd'hui, je veux partager avec vous un sens que la vie m'a appris, un sens très important...</p>
<p>Et apprenez le par cœur s'il vous plait, prenez le comme un conseil précieux, et vivez avec.</p>
<p>Le sens que je veux vous dire, c'est que chaque relation dans ce bas monde, chaque relation entre deux parties est faite de réception et de don.</p>
<p>Si une des deux parties ne fait que prendre sans donner, la relation va échouer, c'est inévitable !</p>
<p>Si une des deux parties, prend du plaisir à donner, mais ne reçois rien en retour, la relation va échouer, c'est aussi inévitable !</p>
<p>Chaque relation dans ce bas monde est bâtie sur cette idée : Donner et recevoir.</p>
<p>Tu me diras, c'est quoi donner et recevoir ? il faut que je donne de l'argent par exemple ?</p>
<p>Non donner et recevoir ce n'est pas une obligation que ca soit des choses matérielles.</p>
<p>Ca peut être moral, tu peux donner des connaissances, et celui qui apprend de toi, te donne de l'attention et de l'intérêt dans ce que tu lui apprends...Le retour peut aussi être des remerciements...</p>
<p>L'essentiel c'est qu'une relation entre deux personnes ne pourra jamais tenir sans cette règle là.</p>
<p>Même entre un mari et sa femme,</p>
<p>Même entre les parents et leurs enfants,</p>
<p>Même entre un élève et son professeur ,</p>
<p>Même entre le patron et ses employés,</p>
<p>Chaque relation dans ce bas monde, sans exception, croyez moi et expérimentez cette idée.</p>
<p>Si tu sens que tu prends de celui qui est en face de toi, et que tu ne lui donnes rien en retour...</p>
<p>Sache que bientôt cette relation va échouer, c'est un signal d'alarme...</p>
<p>Ce que je suis entrain de faire avec cette parole, c'est de déclencher le signal d'alarme dans ton cerveau...</p>
<p>Hein... Révise tes relations, quelles sont les nouvelles ?</p>
<p>Don et réception ?</p>
<p>Moi et mon fils ? si des mamans m'écoutent, je donne à mon enfant, je donne, je donne... mais mon enfant c'est comme s'il n'étais pas là... la relation va bientôt échouer... Un grand problème va se produire...</p>
<p>Moi et mon mari ? si tu donnes à ton mari, tu donnes, tu donnes... en disant « j'aime donner, j'aime mon mari » ... Au bout d'un moment tu ne vas plus l'aimer.</p>
<p>Et si toi son mari, tu ne te réveilles pas, au bout d'un moment la relation va échouer.</p>
<p>Toutes les relations du monde sont basées sur ca !</p>
<p>Je sais que beaucoup de jeunes m'écoutent, et qu'ils sont motivés et prêts à m'aider et travailler avec moi jour et nuit... Bien sur ils ne veulent pas de l'argent en retour, ils s'engagent dans le travail associatif uniquement pour l'amour de Dieu.</p>
<p>Mais alors ils peuvent avoir quoi en retour ?</p>
<p>Ils peuvent avoir de l'expérience, donc moi si je ne leur donne rien en retour...</p>
<p>Ils veulent de l'amour, ils veulent de la considération,</p>
<p>Ils veulent un simple « Merci »</p>
<p>Chaque relation de ce bas monde, désolé je le répète et j'insiste, est basée sur du don et de la réception.</p>
<p>Si tu reçois et ne donne rien en retour, la relation ira mal tôt ou tard... Sois en sur et certain !</p>
<p>Et si tu donnes, même si tu aimes donner et que tu es généreux par nature, et que tu ne reçois rien en retour... Pareil la relation se terminera tôt ou tard.</p>
<p>Tu me diras, mais alors tu attends quoi de moi ? pourquoi tu me racontes tout ca ?</p>
<p>Tout ce que je veux c'est que tu fasses une pause, et que tu révises chacune de tes relations.</p>
<p>Peux tu dès que cet épisode soit terminé, éteins la radio ou l'ordinateur, et reste avec toi-même pendant une minute.</p>
<p>Et demande toi, quelles sont les relations les plus importantes pour moi ? Celles à qui je tiens le plus ?</p>
<p>Est-ce que dans ces relations je donne et je reçois ?</p>
<p>Peux tu acheter un cadeau à ta femme ?</p>
<p>Peux tu appeler ta maman aujourd'hui et lui dire « je t'aime » ?</p>
<p>Peux tu t'asseoir avec ton papa, et lui dire « Je veux apprendre de ton expérience » ? Ca c'est donner à ton père... Quand tu lui demande de partager son expérience dans la vie, tu lui donnes beaucoup, parce que tu le satisfais qu'il ai transmis ses connaissances à son enfant.</p>
<p>Peux tu utiliser ton imagination, et trouver des façons de redonner à ton tour ?</p>
<p>Encore une fois, prendre sans rien redonner, ca mène vers l'échec.</p>
<p>Je vous en prie, révisez vos rapports avec les autres,</p>
<br>
<p>Merci, et que la paix soit sur vous.</p>
