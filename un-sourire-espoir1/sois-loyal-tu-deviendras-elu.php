                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>En vérité je fais partie des amoureux de l'arabe littéraire,</p>
<p>Et je fais partie de ceux qui passent du temps à observer et comparer entre les mots,</p>
<p>Et essayer de comprendre la différence entre deux mots dans lesquels une seule lettre diffère...</p>
<p>Je vous dis ca car en lisant le saint Coran, j'ai remarqué qu'un mot était parfois écrit avec un « i » et parfois avec un « a »</p>
<p>Ce mot est <strong>« Moukhliss »</strong> (traduction : Loyal)</p>
<p>Moukhliss a donc été cité 2 fois avec un « i »</p>
<p>Et 1 fois avec un « a » donc prononcé <strong>« Moukhlass »</strong></p>
<p>Et donc je n'ai pas compris la différence, ce sont quasiment les mêmes mots...</p>
<p>Puis j'ai fini par trouver la différence :</p>
<p>Le premier Moukhliss, c'est une loyauté qui vient de toi même,</p>
<p>Tandis que Moukhlass, c'est une loyauté qui vient de Dieu vers toi,</p>
<p>Ca veut dire quoi donc ?</p>
<p>Ca veut dire si tu es loyal dans ton travail, et que dans ton intention tout ce que tu fais c'est pour satisfaire Dieu, dans ce cas tu es <strong>« Moukhliss »</strong></p>
<p>Puis tu continues à agrandir ta loyauté, et t'élever... Jusqu'à ce que Dieu te choisisse, et fasse toi toi <strong>« Moukhlass »</strong> avec un « a »</p>
<p>Et là le sens du mot devient « pur » ou « élu »</p>
<p>le prophète Yûsuf (Joseph) quand il a été fidèle et très loyal envers Dieu,</p>
<p>Dieu a dit en parlant de lui dans le saint Coran :</p>
<p><strong>{ Ainsi [Nous avons agi] pour écarter de lui le mal et la turpitude. Il était certes un de Nos serviteurs élus. }</strong> Sourate 12 - Yûsuf : Joseph - Verset 24</p>
<p>le mot « élu » a été écrit dans ce verset au pluriel et avec un a <strong>« Moukhlassine »</strong></p>
<p>Ca veut dire Yûsuf a tellement été pur dans ses intentions avec Dieu, que Dieu l'a rendu pur parmi ses créatures.</p>
<p>Posons nous donc la question...</p>
<p>Sommes nous purs dans nos intentions,</p>
<p>Qu'on soit musulmans ou chrétiens,</p>
<p>Dans chaque chose qu'on fait, pensons nous d'abord à Dieu et à sa satisfaction, ou à notre intérêt personnel ?</p>
<p>Ou pensons nous à nos plaisirs avant tout ?</p>
<p>Sommes nous loyaux envers Dieu ?</p>
<p>Y'à t'il parmi nous quelqu'un qui a été tellement pur dans ses intentions, dans chaque chose qu'il a entreprise, que Dieu l'a élevé et rendu pur parmi l'humanité ?</p>
<p>Qui de nous est loyal quand il écrit des lettres, quand il écrit des emails, quand il va sur internet,</p>
<p>Quand il commente un événement, quand il travaille, sommes nous loyaux dans tout ce qu'on fait ?</p>
<p>Pour que Dieu nous choisisse, fasse de nous des élus parmi ses créatures, et fasse de nous la pureté de l'humanité.</p>
<p>Je rêve de ca, que Dieu multiplie les gens loyaux et les gens purs pour dessiner un grand sourire d'espoir !</p>
<br>
<p>Je vous remercie,
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
