                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle de la gratitude...</p>
<p>Il existe un très beau sentiment appelé la gratitude,</p>
<p>La gratitude envers ceux qui ont été à tes côtés,</p>
<p>La gratitude envers les gens qui t'ont aidé dans ta vie,</p>
<p>Es-tu reconnaissant ?</p>
<p>Peux-tu réviser en même temps que moi,</p>
<p>Rembobiner la cassette...</p>
<p>Te souviens-tu de tes enseignants au primaire ?</p>
<p>Te souviens-tu de celui qui t'a sauvé quand tu as commis une faute dans ton travail et que tu allais être limogé ?</p>
<p>Te rappelles-tu du directeur qui t'a appris plein de choses ?</p>
<p>Te souviens-tu de cet ami qui t'a prêté de l'argent quand tu étais dans une situation financière délicate ?</p>
<p>Te rappelles-tu quand tu risquais le divorce, et que cette personne est venue intercéder entre toi et ton mari, et qui a sauvé ton mariage ?</p>
<p>Te souviens-tu de celui qui t'a donné un coup de main jusqu'à ce que tu te maries ?</p>
<p>Es-tu reconnaissant envers ces gens là ?</p>
<p>Ou plutôt te souviens-tu d'eux tout court ?</p>
<p>:)</p>
<p>Désolé de vous le dire comme ca...</p>
<p>Je vais vous raconter une histoire que j'ai vécue personnellement :</p>
<p>Il y'a un homme qui m'a énormément aidé dans ma vie...</p>
<p>Il était là pour moi pendant de nombreuses années,</p>
<p>Son nom est Cheikh Saleh Kamel,</p>
<p>Cet homme m'a aidé dans plein de situations.</p>
<p>Et les jours sont passés,</p>
<p>Puis à un moment, il a du subir une opération du cœur en Allemagne,</p>
<p>Il y'a 2 ans ou 3 ans...</p>
<p>Je vivais encore en Angleterre avant la révolution en Egypte,</p>
<p>Il était donc à l'hôpital en Allemagne, et les visites étaient interdites ou ne devaient pas dépasser 3 min maximum...</p>
<p>Au fond de moi je voulais me rassurer sur son état de santé,</p>
<p>Mais bon les médecins lui ont interdit de parler au téléphone...</p>
<p>Que faire alors ?</p>
<p>Appeler un de ses enfants, ou un de ses proches et leur demander de transmettre mon message à Cheikh Saleh... Puis merci...</p>
<p>Que faire sinon puisque je suis en Angleterre, et lui en Allemagne...</p>
<p>J'en ai parlé à un ami à moi qui m'a dit « Cet homme a été souvent là pour toi ? »</p>
<p>J'ai dit « Oui »</p>
<p>Il m'a dit « Alors tu dois aller lui rendre visite »</p>
<p>Je me suis tu un moment... C'est comme si cette phrase a fait revivre en moi le sentiment de gratitude...</p>
<p>J'ai fait ma valise, j'ai acheté un billet, puis j'ai pris l'avion vers l'Allemagne,</p>
<p>Je me suis dit que ca en valait vraiment la peine.</p>
<p>Je suis arrivé à Munich, je suis venu comme ca sans réserver un hôtel ni rien du tout,</p>
<p>Je suis parti de l'aéroport directement vers l'hôpital,</p>
<p>J'ai vu ses enfants qui m'ont dit que les visites ne devaient pas dépasser 2 minutes,</p>
<p>Je suis entré voir Cheikh Saleh, j'ai embrassé sa tête, je lui ai dit « Bon rétablissement »</p>
<p>Puis je suis ressorti....</p>
<p>Ca veut dire j'ai fait tout ce trajet pour 2 minutes,</p>
<p>J'ai repris le chemin vers l'aéroport pour repartir,</p>
<p>Vous savez quoi mes chers ?</p>
<p>Ce sont les meilleurs moments spirituels dans ma vie !</p>
<p>Une foi d'une saveur unique dans mon cœur,</p>
<p>Tellement savoureuse que je me suis dit c'est surement un moment où mes doua'a (prières) seront exhaussés.</p>
<p>Je m'adressais à Dieu comme si j'avais prié mille unités de prières...</p>
<p>Comme si je revenais d'un pèlerinage...</p>
<p>Un état spirituel magique !</p>
<p>Je me suis rendu compte que la gratitude fait bondir la foi dans ton cœur,</p>
<p>Elle rend Dieu satisfait de toi, et tu le ressens...</p>
<p>Peux-tu essayer ?</p>
<p>Dès que cet épisode sera fini,</p>
<p>Prend ton téléphone, et cherche celui qui t'a le plus aidé dans ta vie,</p>
<p>Appelle-le et dis lui « Je te téléphone pour te remercier et t'exprimer ma gratitude, pour tout ce que tu as fait pour moi »</p>
<p>Puis raccroche et tu goûteras à une foi unique dans ton cœur.</p>
<p>Teste et tu verras un sourire d'espoir se dessiner au fond de toi.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
