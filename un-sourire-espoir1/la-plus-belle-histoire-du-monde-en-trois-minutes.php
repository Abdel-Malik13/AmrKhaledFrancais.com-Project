                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'histoire vraie d'aujourd'hui c'est un ami qui me l'a racontée,</p>
<p>Hussein mon ami me disait :</p>
<p>« J'ai vécu une situation très bizarre,</p>
<p>J'ai pris le train d'Alexandrie pour venir au Caire,</p>
<p>Un homme assez âgé s'est assis à côté de moi, et il était accompagné de son fils...</p>
<p>Son fils doit avoir 25 ans, donc c'est un jeune homme ce n'est plus un enfant,</p>
<p>Pourtant il nous regardait d'une façon anormale...</p>
<p>Il était heureux, il ne tenait pas en place,</p>
<p>Il disait à son père : Moi... Moi je veux m'asseoir du côté de la fenêtre !</p>
<p>Je pensais au fond de moi-même c'est quoi ces gamineries à 25 ans ??!</p>
<p>Puis il disait encore : Papa... Papa regarde les arbres ils nous suivent par derrière, pourtant le train avance très vite !</p>
<p>Ca ressemblait à des paroles d'un enfant immature...</p>
<p>Et à chaque fois son père souriait, il le prenait dans ses bras et lui donnait un câlin,</p>
<p>Puis au bout d'un moment il disait : Papa... Papa regarde cette couleur, c'est du vert n'est-ce pas ? »</p>
<p>Hussein était confus, pourquoi ce jeune homme a le comportement d'un gamin ?</p>
<p>Il disait « Papa... Regarde les animaux là-bas, regarde les oiseaux... Les nuages avancent en même temps que le train... »</p>
<p>Mon ami mourrait d'envie de comprendre,</p>
<p>C'est quoi le problème de ce jeune homme ?</p>
<p>Son comportement commençait sérieusement à le déranger,</p>
<p>Et comme son père lui donnait à chaque fois un câlin, Hussein s'est dit que le jeune homme était sûrement malade...</p>
<p>Il a attendu qu'il s'endorme puis il a dit discrètement à l'homme âgé : « Je connais un très bon ami psychologue... Ca serait bien d'emmener votre fils le voir »</p>
<p>Le vieil homme a souri en disant « En effet mon fils sort de l'hôpital, mais ce n'était pas un hôpital psychiatrique... Mon fils était aveugle pendant plusieurs années, et il a enfin retrouvé la vue grâce à une opération chirurgicale... C'est la 1ère fois depuis des années qu'il peut voir le monde qui l'entoure »</p>
<p>Hussein me raconte « Dès que j'ai entendu ca... J'ai eu un déclic... Je me suis dit pourquoi souvent on juge vite avant d'entendre toute la vérité ?</p>
<p>Pourquoi des couples divorcent et des mariages se terminent avant d'entendre toute la vérité ?</p>
<p>Pourquoi on prend des positions politiques avant d'entendre toute la vérité ?</p>
<p>Pourquoi on devient agressifs, pourquoi on juge les gens avant d'entendre toute leur histoire ? »</p>
<p>Je t'en prie ne tire jamais de conclusions dans la vie avant d'avoir tous les éléments...</p>
<p>L'idée de l'épisode d'aujourd'hui est :</p>
<p>Ne te précipite jamais avant de comprendre, sinon tu risques de faire un pas dans la mauvaise direction...</p>
<p>Le sourire d'espoir ne se dessine que si tu prends ton temps, que tu cherches à obtenir toutes les informations avant de prendre une décision.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
