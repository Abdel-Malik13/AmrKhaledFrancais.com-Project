                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Le titre de cet épisode est très simple</p>
<p>« Non à la généralisation »</p>
<p>Ca signifie quoi non à la généralisation ?</p>
<p>Ne généralise pas dans tout,</p>
<p>Si tu vois une mauvaise chose se produire devant toi... Ben de toute façon tout est mauvais...</p>
<p>Dans telle région il s'est passé un mauvais événement... Ben tous les gens de cette région sont mauvais...</p>
<p>Tu t'es disputé avec telle personne... Ben cette personne est mauvaise de haut en bas...</p>
<br>
<p>Ne raisonne pas ce cette façon...</p>
<p>La généralisation est un aveuglement...</p>
<p>La généralisation est un aveuglement !</p>
<p>Quand une personne commet une faute... Dire que cette personne est mauvaise parce que tu ne vois que son défaut et que tu oublies ses qualités...</p>
<p>Ou généraliser sur tout un pays... Si une personne de se pays se comporte mal... Dire que tous les gens de ce pays sont mauvais...</p>
<p>Ce n'est pas comme ca que ca fonctionne,</p>
<p>La généralisation rend aveugle...</p>
<p>Sois juste mon cher...</p>
<p>Ca veut dire quoi ?</p>
<p>Ca veut dire pèse les gens avec leurs qualités et leurs défauts à la fois,</p>
<p>Vous me croyez si je vous dis que parfois on fait la même chose avec nos enfants ?</p>
<p>Mon fils a commis une erreur... Ben de toute façon il ne sait rien faire d'autre...</p>
<p>Parfois on fait la même chose avec nos épouses et nos maris...</p>
<p>Ta femme commet une faute... tu oublies tout ce qu'elle a fait de bien auparavant... Et tu ne vois plus que sa faute...</p>
<br>
<p>J'aime beaucoup une histoire du prophète sws :</p>
<p>Le mari de sa fille Zainab s'appelait Abou al Asim ibn A'rabii</p>
<p>Et comme mari, Abou al asim remplissait son rôle à la perfection...</p>
<p>Mais Abou al asim refusait de devenir musulman...</p>
<p>Après la révélation, Zainab la fille du prophète sws est devenue musulmane, mais son mari lui refusait...</p>
<p>Abou al Asim ne croyait donc pas son beau père... Il ne voulait pas le reconnaitre comme prophète...</p>
<p>Il y a donc un problème,</p>
<p>Les compagnons sont allés voir le prophète sws pour lui dire, Abou al Asim ne veut pas vous croire, comment pouvez vous le laisser avec votre fille ?</p>
<p>Le prophète sws leur a donné une très très très belle réponse :</p>
<p><strong class="prophete">« S'il me refuse... Alors je ne lui trouve aucun défaut comme beau fils »</strong></p>
<p>Ca veut dire qu'en tant que mari et beau fils il est parfait,</p>
<p>Il a d'autres défauts,</p>
<p>Il refuse de me croire,</p>
<p>Mais il a une grande qualité, il se comporte bien avec ma fille...</p>
<p>Qui d'entre nous traite les gens avec autant de justice ?</p>
<p>Peux tu peser les gens avec leurs qualités et leurs défauts à la fois ?</p>
<p>Peux tu s'il te plait ne pas généraliser, et rester juste ?</p>
<br>
<p>Il y a un joueur de football très connu qui s'appelle Zlatan ibrahimovic ,</p>
<p>J'ai vu une interview de lui il n'y a pas longtemps,</p>
<p>Il jouait dans un grand club, le Fc Barcelone,</p>
<p>Et son entraineur qui s'appelait Pep Guardiola, a dit que ce joueur n'était pas adapté à l'équipe, donc ils l'a forcé à partir...</p>
<p>Je crois que si tu étais à la place d'Ibrahimovic, et que ton patron te licenciait, tu aurais une grande haine envers lui...</p>
<p>Pendant l'interview, le journaliste lui a demandé quel était le meilleur entraineur du monde ?</p>
<p>Ibrahimovic a répondu c'est Guardiola.</p>
<p>Le journaliste l'a donc titillé en disant c'est lui qui vous a poussé vers la sortie...</p>
<p>Et Ibrahimovic a répondu que ca n'empêchait pas qu'il soit le meilleur entraineur au monde.</p>
<p>J'ai eu beaucoup de respect pour lui,</p>
<p>Malgré qu'il l'ai poussé vers la sortie, il continue de dire qu'il est le meilleur entraineur au monde.</p>
<p>Peux tu rester juste à la façon d'Ibrahimovic ?</p>
<p>Peux tu ne pas généraliser,</p>
<p>La généralisation est un aveuglement !</p>
<p>Veux tu vivre avec un sourire d'espoir ?</p>
<p>Sois juste avec les gens ,</p>
<p>En commençant par ta femme et tes enfants,</p>
<p>Jusqu'aux personnes qui travaillent avec toi,</p>
<p>Jusqu'à toute autre personne différente de toi,</p>
<p>Sois juste avec tout le monde.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
