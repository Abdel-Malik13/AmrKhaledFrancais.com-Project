                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui raconte l'histoire de Haj Ismaïl,</p>
<p>C'est lui-même qui me l'a racontée il y'a 7 ou 8ans...</p>
<p>Et jusqu'à maintenant je n'arrive pas à l'oublier.</p>
<p>L'histoire concerne les rapports entre frères et sœurs,</p>
<p>L'importance des liens de parenté,</p>
<p>Et surtout à l'approche du mois sacré de Ramadan...</p>
<p>As-tu de bons rapports avec les membres de ta famille ?</p>
<p>Y'a-t-il des personnes de ta famille à qui tu ne parles plus ?</p>
<p>Je te demande sérieusement :</p>
<p>A quoi ressemble ta relation avec tes frères et sœurs ?</p>
<p>Des rapports normaux, ou vous êtes éloignés, ou vous êtes en dispute à cause d'argent ?</p>
<p>Haj Ismaïl m'a raconté ceci :</p>
<p>J'ai un frère qui m'a volé de l'argent... Je jure par Dieu le très haut j'ai la preuve écrite que mon frère m'a volé ma part d'argent,</p>
<p>J'ai essayé de lui parler une fois, puis deux, puis trois...</p>
<p>Et j'ai demandé à notre grand frère de lui parler pour le raisonner, mais ca n'a servi à rien...</p>
<p>Il ne voulait écouter personne, alors j'étais obligé d'aller au tribunal,</p>
<p>Notre père est décédé, et je sais que ca lui fait du mal dans sa tombe... Mais c'est mon droit, je veux juste reprendre ce qui m'appartient.</p>
<p>J'ai attaqué mon propre frère en justice...</p>
<p>Et on ne se parlait plus,</p>
<p>Et j'ai demandé à mes enfants de couper les liens avec ses enfants,</p>
<p>« N'allez jamais les voir, ne leur téléphonez jamais... Cet homme a arnaqué votre papa ! »</p>
<p>En disant ces paroles, j'avais mal au cœur...</p>
<p>Notre mère est aussi décédée, et dans sa tombe cette situation doit la faire souffrir... Que ses enfants, qui sont sortis du même ventre se fassent la guerre pour une histoire d'argent.</p>
<p>Parfois quand je priais, je ressentais une brulure au fond de moi...</p>
<p>Est-ce que Dieu est satisfait de moi ?</p>
<p>Mais Dieu nous a dit de reprendre ce qui nous appartient...</p>
<p>Vrai.</p>
<p>Mais n y'a-t-il pas un autre moyen que passer par le tribunal ?</p>
<p>(Haj Ismaïl me racontait comme je suis en train de vous le raconter)</p>
<p>Le Ramadan est arrivé,</p>
<p>Et j'ai entendu une parole du Prophète (bénédictions et paix soient sur lui) dans laquelle il dit</p>
<p><strong class="prophete">« La miséricorde ne descend pas sur les gens au milieu desquels se trouve quelqu'un qui a rompu le lien de parenté. »</strong></p>
<p>Du coup même si cet argent m'appartient,</p>
<p>Et même si Dieu nous demande de reprendre ce qui nous revient de droit,</p>
<p>Je me suis rendu compte qu'il existe d'autres valeurs appelées : la grâce, la famille...</p>
<p>J'ai décidé de téléphoner à mon frère, je lui ai dit «Hussein, je sais que tu m'as volé de l'argent... Je suis sur de ce que je dis, mais on est en plein mois de Ramadan, et je veux que la miséricorde de Dieu descende sur moi... Hussein je te pardonne, je te souhaite une bonne année.</p>
<p>Je t'invite à venir avec tes enfants, pour que les petits se retrouvent, je n'aime pas que les frères et les cousins rompent leurs liens de parenté. »</p>
<p>Nos enfants ont repris contact,</p>
<p>Et quand je me suis retrouvé seul dans ma chambre, j'ai pleuré en demandant à Dieu de me rembourser cet argent que j'ai perdu...</p>
<p>(Haj Ismaïl m'a juré)</p>
<p>Par Dieu je jure que cette nuit j'ai vu le Prophète (bpsl) dans mon rêve... Il m'a dit <strong class="prophete">« Ô Ismaïl viens faire un pèlerinage avec moi, je veux qu'on tourne ensemble autour de la Ka'aba »</strong> (La Mosquée sacrée à La Mecque)</p>
<p>Je me suis retrouvé en train de faire un pèlerinage avec le Prophète (bpsl), et au réveil je pleurais et riais en même temps.</p>
<p>Je disais « L'argent n'a plus d'importance ! Dieu m'a donné beaucoup mieux en échange, et c'est sur et certain qu'il me récompensera pour ce que j'ai fait ici bas et dans l'au-delà ! »</p>
<p>Mes chers, pardonnez aux gens pour que Dieu vous pardonne,</p>
<p>Pardonnez surtout à vos proches et aux membres de votre famille,</p>
<p>Et vous verrez la miséricorde de Dieu et sa bénédiction dans vos vies !</p>
<p>Un sourire d'espoir pour vous et vos enfants.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
