                                        <br>
                                        <p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle d'une fille que j'ai croisée un des dix derniers jours du Ramadan,</p>
<p>Après la prière, on restait discuter avec les gens et faire des doua'a... etc.</p>
<p>Une fille est venue me voir pour me raconter un problème entre elle et son fiancé,</p>
<p>Elle voulait que je luis donne mon avis là-dessus,</p>
<p>Elle me racontait qu'elle aime énormément son fiancé,</p>
<p>Et que lui aussi il l'aime énormément,</p>
<p>L'entente entre eux est parfaite,</p>
<p>Son fiancé réussit dans tout,</p>
<p>Et elle est heureuse avec lui...</p>
<p>Il est très bien éduqué et respectueux,</p>
<p>En résumé 10/10</p>
<p>Les deux familles s'entendaient bien,</p>
<p>Les démarches pour le mariage suivaient normalement leur cours...</p>
<p>Et c'est là qu'un problème est apparu :</p>
<p>Ce jeune homme n'aime pas visiter sa famille...</p>
<p>Ça veut dire il ne veut pas rendre visite à sa belle famille ni aller les voir,</p>
<p>Mais pourquoi donc ?</p>
<p>La jeune fille me racontait que son fiancé dit (faites bien attention à ses paroles)</p>
<p>« Est-ce que je vais t'épouser, ou est-ce que je vais épouser ta famille ?</p>
<p>C'est toi que je vais épouser, alors pourquoi je serai obligé de visiter tes parents ?</p>
<p>Je n'irai les voir ni tous les jours, ni même de temps en temps,</p>
<p>Je leur parlerai juste au téléphone pendant les grandes occasions, mais c'est toi que je vais épouser...</p>
<p>Je n'aime pas ces traditions qui obligent à recevoir ou rendre visite aux beaux parents,</p>
<p>Je suis un homme moderne et libre,</p>
<p>Je ne vais pas faire semblant d'aller m'asseoir dans leur salon, et dire bonjour tante ou bonjour oncle... Ces choses là ne sont pas faites pour moi.</p>
<p>Tout ce qui compte pour moi c'est toi et rien d'autre ! »</p>
<p>Cette fille me demandait ce qu'elle devait faire...</p>
<p>Je lui ai répondu « Ecoute ma fille, ce n'est pas un homme qui épouse une femme... Non ce n'est pas vrai !</p>
<p>C'est une famille qui épouse une autre famille »</p>
<p>C'est l'essence même du mariage mes chers,</p>
<p>Le mariage ce n'est pas moi + elle/lui et peu importe le reste...</p>
<p>Le but même du mariage est le rapprochement des parties de la société entre eux,</p>
<p>Quand Dieu le très haut a parlé du mariage dans le noble Coran il a dit</p>
<p><strong>{ Et c'est Lui qui de l'eau a créé une espèce humaine qu'Il unit par les liens de la parenté et de l'alliance... }</strong></p>
<p>Sourate 25 - Al Furqâne: Le Discernement (verset 54)</p>
<p>Il a mis entre vous des liens de parenté, une alliance, de la communication...</p>
<p>Qui a dit que ce sont uniquement deux personnes qui se marient ?</p>
<p>Ca prouve que ce jeune homme n'a pas compris ce qu'est un mariage...</p>
<p>Mes chers ce sont deux familles qui s'unissent,</p>
<p>Il faut que les deux familles soient heureuses et satisfaites,</p>
<p>C'est une alliance à long terme pas entre deux personnes,</p>
<p>Mais entre deux grandes familles qui vont encore plus s'agrandir.</p>
<p>Ce que je veux dire à tous les jeunes qui sont sur le point de se marier :</p>
<p>Quand tu souhaites t'engager avec une fille,</p>
<p>Sache que la chose qui l'honorera le plus c'est que tu honores ta famille,</p>
<p>Et la chose qui la blessera le plus c'est que tu blesses sa famille...</p>
<p>La chose qui fait le plus mal à une personne c'est qu'on se moque de son père ou de sa mère...</p>
<p>Et c'est valable dans les deux sens...</p>
<p>Le sentiment le plus désagréable dans un couple c'est de sentir que l'autre ne respecte pas ta famille, voir juste qu'il ne leur donne pas d'importance...</p>
<p>Le Prophète (bénédictions et paix soient sur lui) a dit dans une parole</p>
<p><strong class="prophete">« Le meilleur d'entre vous, est celui qui est le meilleur avec sa famille »</strong></p>
<br>
<p>Sais-tu quelle est la chose qu'appréciera le plus ton épouse ou ton mari ?</p>
<p>C'est que tu rendes visite à ses parents,</p>
<p>Que tu prennes de leurs nouvelles,</p>
<p>C'est de chercher à les satisfaire,</p>
<p>C'est de sourire quand tu les vois.</p>
<p>Tu me diras parfois c'est lourd je n'y arrive pas... Et alors ?</p>
<p>C'est possible que parfois tu manques de motivation, c'est plutôt normal vu qu'ils sont différents de toi.</p>
<p>Mais sois courageux et responsable !</p>
<p>A votre avis le fiancé de cette fille sera responsable après le mariage ?</p>
<p>Personnellement je crains que non,</p>
<p>J'ai dit à cette fille « Non ! Soit il respecte ta famille, et il se rend compte de l'importance de ce point... Soit tu ne devrais pas faire ta vie avec lui... Si dès maintenant il traite ta famille de cette manière... Demain il te traitera pareil »</p>
<p>Croyez-moi mes chers, le sourire d'espoir ne se dessine que lorsqu'on assume entièrement nos responsabilités et qu'on respecte la famille de l'autre.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
