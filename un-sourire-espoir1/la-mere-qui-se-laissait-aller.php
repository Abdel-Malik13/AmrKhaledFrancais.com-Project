                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                        
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>L'épisode d'aujourd'hui raconte une histoire vraie,</p>
<p>L'histoire d'un jeune homme qui m'a beaucoup beaucoup beaucoup touchée...</p>
<p>Il s'agit de son histoire avec sa maman,</p>
<p>Et comme c'est bientôt la fête des mères, j'avais envie de vous la raconter.</p>
<br>
<p>Ce jeune homme m'a dit :</p>
<p>Notre père est décédé quand on était petits,</p>
<p>Ma mère a du jouer le rôle du père et de la mère à la fois,</p>
<p>Elle faisait tout pour nous,</p>
<p>On l'appelait Mapa :)</p>
<br>
<p>Elle travaillait énormément pour subvenir à nos besoins,</p>
<p>Mais je ne l'avais jamais remarqué...</p>
<br>
<p>Quand je la voyais à la maison, elle ne soignait pas son apparence,</p>
<p>Parfois elle était très nerveuse et nous criait dessus,</p>
<p>Je n'aimais pas sa façon de faire...</p>
<p>En réalité je ne voyais rien de positif chez ma mère...</p>
<p>(En entendant cette phrase ca m'a fait très mal au cœur)</p>
<p>Ce jeune homme m'a dit, ca fait mal à entendre mais c'est la vérité,</p>
<p>Je ne voyais rien de beau chez ma mère,</p>
<p>Elle se laissait aller,</p>
<p>Elle ne se coiffait jamais,</p>
<p>Je n'aimais pas sa corpulence,</p>
<p>Je n'aimais pas son comportement...</p>
<br>
<p>Jusqu'à ce que ma mère décède,</p>
<p>Et ses collègues du travail sont venus nous présenter leurs condoléances,</p>
<p>Et ils ont commencé à me parler de cette mère que je ne connaissais pas...</p>
<p>Son supérieur m'a dit qu'elle travaillait 2 ou 3 fois plus que les autres pour nous acheter de nouveaux vêtements...</p>
<p>Et un de ses collègues m'a dit que parfois elle avait un terrible mal de tête, mais elle voulait faire des heures supplémentaires pour gagner plus pour ses enfants...</p>
<p>Et un troisième me disait que c'était une femme formidable, qu'elle nous aimait énormément...</p>
<p>Plus ils me racontaient... plus je pleurais,</p>
<p>Quel dommage je ne t'ai jamais connue...</p>
<p>J'aurai aimé te connaitre d'avantage...</p>
<p>Pourquoi tous les gens à l'extérieur te voyaient belle sauf moi...</p>
<p>Je ne voyais que ton aspect physique, alors que de l'intérieur tu souffrais pour nous faire grandir...</p>
<p>Tu avais plein de rêves que tu as abandonné pour un seul rêve... Moi et mes frères et sœurs...</p>
<br>
<p>Ce jeune homme me disait :</p>
<p>J'ai serré sa photo, et je l'ai beaucoup aimée après son décès,</p>
<p>Et j'ai essayé de lui offrir le maximum de bonnes actions,</p>
<p>Des prières,</p>
<p>Des invocations,</p>
<p>Lire le Saint Coran,</p>
<p>Donner des aumônes,</p>
<p>Tout ca pour lui dire que je l'aimais...</p>
<p>Malheureusement j'aurai souhaité le lui dire pendant qu'elle était en vie...</p>
<br>
<p>Je sais que cette histoire t'a fait mal au cœur,</p>
<p>Mais si je te la raconte c'est pour te pousser à te rapprocher de ta mère et de ton père pendant qu'ils sont encore là...</p>
<p>Tu ne te rendras compte de leur valeur que le jour où ils ne seront plus là...</p>
<p>Que Dieu les protège,</p>
<p>Remercie Dieu qu'ils soient encore en vie,</p>
<p>Regarde la beauté qu'il y'à en eux,</p>
<p>Regarde l'amour qu'ils ont pour toi...</p>
<p>Je te jure que tu ne trouveras personne qui t'aimera comme ils t'aiment !</p>
<br>
<p>A tous les jeunes, la vie est remplie de traitres et de gens qui sont prêts à te vendre...</p>
<p>Ton père et ta mère ne te vendront jamais... C'est impossible !</p>
<p>Le seul qui a envie de te transmettre gratuitement son expérience, le seul qui va te conseiller sans chercher à t'arnaquer c'est ton père !</p>
<p>Et ta mère est prête à sacrifier sa vie pour toi !</p>
<br>
<p>A tous les jeunes, offrez un sourire d'espoir aujourd'hui à vos mamans et à vos papas.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
