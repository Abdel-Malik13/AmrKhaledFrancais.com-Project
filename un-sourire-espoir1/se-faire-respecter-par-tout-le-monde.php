                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>Que c'est beau quand on voit des arabes ou des musulmans qui réussissent,</p>
<p>Ca nous fait tellement plaisir,</p>
<p>Surtout quand ils sont à l'étranger,</p>
<p>Rendez-vous compte, la réussite est la plus belle chose dans la vie,</p>
<p>Les gens écoutent celui qui réussit, ils n'écoutent pas celui qui échoue,</p>
<p>Les gens aiment celui qui gagne, ils n'aiment pas celui qui perd,</p>
<p>Si tu ne réussis pas dans la vie, et que tu passes 1000 ans à parler des valeurs de l'Islam et des musulmans, des arabes et leur générosité... Personne ne va t'écouter.</p>
<p>Mais si tu réussis... Les gens aiment celui qui a du succès,</p>
<p>On aimait tous les premiers de la classe,</p>
<p>On n'écoutait jamais les derniers de la classe,</p>
<p>On regardait tous le 1er de la classe avec respect,</p>
<p>Même quand on était petits à l'école...</p>
<p>Même les professeurs, même le directeur de l'école...</p>
<p>Celui qui réussit est toujours apprécié,</p>
<p>Celui qui réussit est toujours respecté,</p>
<p>On écoute toujours les paroles de celui qui réussit, même s'il a tord... Alors que dire quand il a raison.</p>
<p>Je vous raconte tout ca car je suis très touché par l'histoire du Dr. Omar Echeikh,</p>
<p>C'est un égyptien qui est parti très jeune en Angleterre,</p>
<p>Il était médecin,</p>
<p>Il a travaillé dans plein d'hôpitaux... Travaillé... Travaillé...</p>
<p>Comme beaucoup d'égyptiens travaillent à l'étranger,</p>
<p>Mais Omar Echeikh avait un grand rêve,</p>
<p>Son rêve était de faire fortune ?</p>
<p>Non, bien sûr il voulait gagner de l'argent, mais ce n'était pas son grand rêve...</p>
<p>Il rêvait de réussir pour que les arabes et les musulmans soient respectés,</p>
<p>Pour donne une bonne image des égyptiens qui vivent à l'étranger,</p>
<p>C'était son intention.</p>
<p>Alors il a commencé à chercher la réussite,</p>
<p>Dr. Omar Echeikh a commencé à tracer le chemin avec son épouse Oumaima,</p>
<p>Petit à petit ils ont ouvert un cabinet à Birmingham,</p>
<p>Puis ils ont commencé à agrandir le cabinet, il grandissait... grandissait...</p>
<p>Ils étaient minutieux dans leur travail, cherchaient toujours à s'améliorer, et la qualité augmentait... Augmentait...</p>
<p>Jusqu'au jour où le ministère de la santé britannique a décidé de faire un classement des meilleurs cabinets,</p>
<p>Et la surprise : Dr. Omar Echeikh épaulé par son épouse ont été classés meilleur cabinet dans la région de West Midlands.</p>
<p>L'année suivante, leur cabinet a été classé 1er dans tout le royaume uni.</p>
<p>Et il est resté 1er pendant 3ans de suite.</p>
<p>Vous imaginez mes chers ?</p>
<p>Dans toute l'Angleterre, parmi tous les médecins anglais et étrangers qui exercent, le meilleur cabinet est celui d'un égyptien,</p>
<p>Le fils d'un agriculteur qui a travaillé dur pour arriver premier,</p>
<p>Je vous raconte l'histoire du Dr. Omar Echeikh pour vous dire à quel point le succès a bon goût,</p>
<p>Tu peux être architecte ou médecin, ou encore un étudiant parmi 10.000 dans ta faculté,</p>
<p>Mais ceux qui réussissent ont une saveur très spéciale,</p>
<p>Tu peux être un employé parmi tant d'autres, mais réussir et briller c'est unique.</p>
<p>Que dis-tu d'exceller dans ce que tu fais ?</p>
<p>Que dis-tu d'arriver premier ?</p>
<p>Pas seulement à l'étranger pour donner une bonne image des musulmans,</p>
<p>Mais aussi dans nos pays, entre nous,</p>
<p>Celui qui réussit est heureux, respecté et apprécié,</p>
<p>C'est une invitation à la réussite !</p>
<p>A ce moment là tu verras un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
