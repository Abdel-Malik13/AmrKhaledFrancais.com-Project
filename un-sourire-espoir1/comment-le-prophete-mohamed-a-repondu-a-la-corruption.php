                                        <br>
                                        <p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est encore avec le Messager de Dieu (bénédictions et paix soient sur lui)</p>
<p>Je parle toujours de la façon qu'a utilisée le Prophète (bpsl) pour répondre à ceux qui ont voulu lui faire du mal...</p>
<p>Chaque personne dans la vie a une manière de répondre quand on lui fait du mal :</p>
<p>Certains réagissent en insultant,</p>
<p>D'autres en se battant,</p>
<p>Et d'autres en ignorant complètement celui qui leur fait du mal...</p>
<p>Et enfin il y'en a qui soignent le mal en ouvrant le dialogue, en cherchant à convaincre, avec sagesse.</p>
<p>Dans l'histoire de l'humanité, celui qui a le mieux appliqué cette dernière méthode est le Prophète (bpsl).</p>
<p>Je vais vous raconter une histoire...</p>
<p>Pendant que la tribu de Qoraich combattait le Messager de Dieu (bpsl), ils se sont réunis et ont dit « Que quelqu'un arrête Mohamed, que quelqu'un aille lui dire de stopper son message, et qu'on est prêts à lui donner tout ce qu'il désire »</p>
<p>En gros ils parlaient de corruption...</p>
<p>Corrompre le Prophète (bpsl) ??</p>
<p>Un homme appelé Outba Ibn Rabiaa s'est levé pour aller parler au Message de Dieu (bpsl)</p>
<p>Il est arrivé en disant « Ô Mohamed, je vais te proposer des choses, ta tribu m'envoient te les suggérer »</p>
<p>Rien qu'en voyant la façon de parler d'Outba, le Prophète (bpsl) a compris tout de suite qu'il allait lui faire des propositions indécentes...</p>
<p>Il ne lui a encore rien proposé, mais d'entrée il a compris la raison de sa venue.</p>
<p>Il est venu faire une proposition grossière, quelque chose d'impolie...</p>
<p>Outba a dit « Je suis venu te proposer des choses, tu choisiras celles qui te plaisent dedans... »</p>
<p>Le Prophète (bpsl) le regarde avec un sourire et lui répond <strong class="prophete">« Dis-moi Ô Abou Al Walid ? »</strong></p>
<p>Le nom de famille d'Outba Ibn Rabiaa était Abou Al Walid,</p>
<p>(Chez les arabes appeler quelqu'un par son nom de sa famille est le sommet du respect, quelque chose qui honore)</p>
<p>Le Prophète (bpsl) ne lui a donc pas répondu « Dis-moi Ô Outba » non... Non... Non...</p>
<p><strong class="prophete">« Dis-moi Ô Abou Al Walid, je suis à ton écoute. »</strong></p>
<p>Qu'elle est belle la politesse du Messager de Dieu (bpsl) ,</p>
<p>Comme il l'a dit dans une parole <strong class="prophete">"Mon Seigneur m'a éduqué et il a parfait mon éducation."</strong></p>
<p>Et comme Dieu lui-même l'a dit dans le noble Coran</p>
<p><strong>{ Et tu es certes, d'une moralité éminente. }</strong></p>
<p>Sourate 68 - Al-Qalam : La Plume (verset 4)</p>
<br>
<p><strong class="prophete">« Dis-moi Ô Abou Al Walid, je suis à ton écoute. »</strong></p>
<p>Je suis toute ouïe, dis moi tout...</p>
<p>Outba a poursuivi « Ô Mohamed, si tu dis ce que tu dis pour gagner de l'argent... On cotisera pour que tu deviennes le plus riche d'entre nous... »</p>
<p>Le Prophète (bpsl) le regardait en silence et en souriant,</p>
<p><strong class="prophete">« Poursuis Ô Abou Al Walid »</strong></p>
<p>Outba poursuivait «Si tu fais ca pour chercher l'honneur, on te nommera chef parmi nous, on ne fera jamais rien sans te consulter »</p>
<p>(Si tu cherches un grand poste, on te l'accordera)</p>
<p>Le Prophète (bpsl) a sourit en disant <strong class="prophete">« Continue Ô Abou Al Walid »</strong></p>
<p>« Et si tu cherches à devenir propriétaire, on t'accordera des propriétés »</p>
<p>« Et si tu veux te marier, on te fera marier avec les plus belles filles arabes »</p>
<p>« Et si ce qui t'arrive est une maladie, on te ramènera le meilleur médecin arabe pour te soigner »</p>
<p>Aie.... C'est quoi ces propositions... C'est quoi cette stupidité ?</p>
<p>Il y'a des gens qui s'énervent et veulent se battre...</p>
<p>Mais regarde la méthode du Prophète (bpsl),</p>
<p>Il lui a dit <strong class="prophete">« As-tu terminé Ô Abou Al Walid ? »</strong></p>
<p>Outba a dit « Oui »</p>
<p>Le Prophète (bpsl) lui a répondu <strong class="prophete">« Peux-tu m'écouter si tu le permets ? »</strong></p>
<p>Outba a dit « Oui »</p>
<p>Le Prophète (bpsl) a commencé à réciter des versets de la Sourate 41 - Fussilat : Les Versets Détaillés,</p>
<p>Qui disent que je suis là uniquement pour transmettre le message et rien d'autre...</p>
<p>Outba a mis sa main sur la bouche du Prophète (bpsl) en disant « Ca suffit Ô Mohamed »</p>
<p>(C'est bon j'ai compris)</p>
<p>Le Prophète (bps) a dit <strong class="prophete">« D'accord Ô Abou Al Walid »</strong></p>
<p>Il a arrêté de parler... Quel calme, quelle élégance, quelle patience, quelle politesse...</p>
<p>Cet homme a tellement été touché, que la tribu de Qoraich ont dit « Abou Al Walid n'est pas revenu avec le même visage qu'en partant »</p>
<p>Même s'il a prononcé des paroles dures, le Prophète (bpsl) lui a donné de la douceur.</p>
<p>Apprenons de la méthode du Messager de Dieu (bpsl) pour dessiner des sourires d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
