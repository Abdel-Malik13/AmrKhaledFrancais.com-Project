                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide,
entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne
qui a réussi...</p> <p>Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage
L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue à tous avec un nouveau sourire d'espoir, dans une nouvelle journée.</p>
<p>La parole d'aujourd'hui parle de nos relations familiales : les pères et les fils, les jeunes et leurs parents. Une parole adressée aux deux à travers une histoire.</p>
<p>Est-ce que vous cous connaissez bien ? Etes-vous amis ? Etes vous complices ? toi et ton fils, est ce que l'amitié est ce qui lie entre nous pères et fils ?</p>
<p>Croyez-moi en ces temps difficiles, c'est de cette relation que nous avons besoin. Au milieu de la difficulté de la vie, en ces temps ou les vrais amis se font rares, un père a besoin que son ami le plus proche soit son fils, sinon pourquoi l'a t'il fait venir au monde... Et un fils a besoin que son ami le plus proche soit son père, c'est qui la personne qui m'aidera le plus au monde grâce à son expérience, sera le plus proche de moi pour que je lui raconte mes soucis...</p>
<p>L'histoire que je vous raconte est l'histoire d'un jeune homme, ce jeune homme je le connaissais personnellement, il était lycéen et jouait au foot en tant que gardien de but, et il était très bon dans ce poste là... Son père n'avait pas le temps du tout, le genre de pères toujours occupé par son travail... Il me racontait, que son équipe était arrivée en finale du tournoi du lycée, et qu'il a demandé à son père de venir assister au match, son père disait ne pas avoir le temps, et le fils a continué d'insister... Le père a fini par venir, et pendant le match il parlait au téléphone, le fils était fâché de l'intérieur du coup il faisait des gestes acrobatiques au milieu de terrain dans l'espoir d'attirer l'attention non pas des spectateurs, mais l'attention de son père.</p>
<p>Au milieu du match son père a reçu un appel, il s'est levé puis est parti... Le jeune homme me racontait « j'ai terminé le match, mais je l'ai terminé avec des larmes en pleurant.. deux années après mon père est décédé... Sur la tombe de mon père, je me suis retrouvé entrain de lui parler, et lui dire je ne t'ai jamais vraiment connu, j'aurai aimé te connaitre, il y'avait quoi de plus important que moi dans cette vie pour que tu le fasses et que tu n'ai pas le temps de connaitre ton fils »</p>
<p>Le jeune homme disait ca d'une voie audible comme s'il s'adressait à son père « j'aurai aimé te connaitre, pourquoi tu ne m'as pas donné l'occasion de te connaitre »</p>
<p>Tout d'un coup, son oncle lui a tapoté l'épaule et lui a dit « tu aurais aussi du aider ton père à te connaitre »</p>
<p>Je raconte cette histoire à chaque père et fils, à toi jeune homme si ton père n'a pas de temps pour toi, va dans son bureau, dis lui je viens ici pour te parler, j'ai envie de discuter avec toi, tu es la personne la plus proche pour moi.</p>
<p>Et vous les pères, consacrez du temps à vos enfants, emmenez les diner ensemble, marchez ensemble, il n y'à rien de plus cher dans se bas monde que l'amitié entre un père et un fils, J'ai envie que l'amitié revienne entre vous, entre mères, pères et enfants,</p>
<p>Croyez moi c'est la meilleure chose qui nous protègera en ces temps difficiles, pouvez-vous essayer aujourd'hui ? Pouvez vous faire le premier pas et devenir amis, essayez et vous dessinerez un sourire d'espoir dans vos maisons.</p>
<p>Je vous remercie, et que la paix soit sur vous.</p>
