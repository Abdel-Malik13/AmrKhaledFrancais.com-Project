                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle du Prophète (bénédictions et paix soient sur lui)...</p>
<p>Sur le Messager de Dieu...</p>
<p>Tout le monde en ce moment parle du film qui le caricature etc. etc.</p>
<p>Mais moi en réalité je veux vous raconter le comportement du Prophète (bpsl) avec ses ennemis...</p>
<p>Comment il s'est comporté avec ceux qui ont lancé des fausses rumeurs à son sujet,</p>
<p>Comme en ce moment face à ce mauvais film on réagit d'une certaine façon...</p>
<p>La logique c'est de chercher à savoir comment s'est comporté celui qui est concerné non ?</p>
<p>Quand le Prophète (bpsl) était encore à la Mecque, la tribu de qoraich sortait des fausses rumeurs à son sujet : Un sorcier, un menteur, un fou etc. etc.</p>
<p>Puis ils ont voulu sortir une rumeur plus forte, qui pousse les arabes à avoir peur du Messager de Dieu...</p>
<p>Ils ont alors lancé une rumeur comme quoi il était possédé par des démons... et que cette possession le poussait à dire des choses qui ensorcèlent les gens...</p>
<p>Et ils ont fait grandir la rumeur,</p>
<p>La saison du pèlerinage a fini par arriver,</p>
<p>Du coup toutes les tribus arabes venaient à la Kaa'ba (Mosquée sacrée) alors qu'elle était remplie de statues...</p>
<p>Qoraich ont voulu faire peur aux gens de peur qu'ils n'écoutent le Prophète (bpsl)</p>
<p>Ils ont voulu agrandir encore plus la rumeur...</p>
<p>(Tu vois leur façon de faire ?)</p>
<p>Ils faisaient circuler l'idée qu'il était possédé par des démons et qu'il ne fallait pas s'en approcher,</p>
<p>Comment va réagir le Prophète (bpsl) face à ca ?</p>
<p>Avec la méthode prophétique :</p>
<p>Un des arabes venu à la Mecque a entendu cette rumeur,</p>
<p>Cet homme soignait dans son village les gens possédés,</p>
<p>Il a donc cru à la rumeur, et il est parti voir le Messager de Dieu,</p>
<p>Il lui a dit « Ô frère des arabes, j'ai entendu dire que tu es possédé par des démons... Et moi je sais soigner ca... Dis-moi de quoi tu souffres pour que je te guérisse »</p>
<p>A votre avis que va faire le Prophète (bpsl) ?</p>
<p>Il va se disputer avec lui, ou le taper, ou lui crier dessus ?</p>
<p>Non... Non... Non... Le Prophète (bpsl) a répondu <strong class="prophete">« Alors écoute-moi pour comprendre »</strong></p>
<p>(Je vais tout te raconter)</p>
<p>Le Prophète (bpsl) a poursuivi : <strong class="prophete">« Toutes les louanges sont à Allah, nous le remercions, nous cherchons son soutien, et nous nous repentons à lui. Et nous cherchons refuge auprès d'Allah contre le mal des gens et contre nos mauvaises actions. Celui qui Allah guide ne sera jamais égaré, et celui qui est égaré ne trouvera pas un compagnon pour le conduire sur le droit chemin (à part Allah) »</strong></p>
<p>L'homme arabe lui a répondu « C'est ce que tu dis aux gens ? »</p>
<p>Le Prophète lui a dit <strong class="prophete">« Oui »</strong> avec le sourire.</p>
<p>L'homme lui a répondu « Je jure que tu n'es pas possédé par des démons... Ces paroles ne viennent pas de la terre, ces paroles viennent du ciel! Je témoigne qu'il n'y a pas d'autre dieu qu'Allah et que tu es son messager.»</p>
<p>Il était venu pour le soigner... Et il a embrassé l'islam.</p>
<p>Vous voyez comment le Prophète (bpsl) réagit face aux rumeurs ?</p>
<p>Vous voyez comment le Prophète (bpsl) se comporte avec ses ennemis et ceux qui veulent lui nuire en les raisonnant ?</p>
<p>Avec sagesse, avec justice...</p>
<p>La vérité est forte mes chers,</p>
<p>Elle n'a pas besoin que tu casses ou que tu brules...</p>
<p>La vérité est forte parce que c'est la vérité !</p>
<p>Paix et salut sur notre Prophète (bpsl)</p>
<p>Il nous a appris la bonne méthode de répondre en dessinant un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
