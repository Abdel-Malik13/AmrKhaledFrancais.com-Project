                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui raconte un événement historique,</p>
<p>Il parle de Mohamed el-Fatih "le Conquérant",</p>
<p>C'est lui qui a fait la conquête de Constantinople (Ancienne appellation d'Istanbul en Turquie)</p>
<p>L'empire ottoman et les musulmans n'arrivaient pas à le faire pendant longtemps,</p>
<p>Autour de Constantinople, il y'avait de l'eau, un fossé, des murailles... Et une grande protection,</p>
<p>Historiquement personne n'était parvenu à conquérir Constantinople,</p>
<p>Et c'est Mohamed el-Fatih qui va y parvenir alors qu'il n'avait que 23ans...</p>
<p>Mais je vais vous raconter le début de l'histoire,</p>
<p>Je ne vais pas vous raconter la conquête en elle-même...</p>
<p>Mohamed el-Fatih a du succéder à son père comme sultan,</p>
<p>Il s'est retrouvé avec de grandes responsabilités à seulement 23ans,</p>
<p>Et comme il était jeune, les problèmes se sont multipliés,</p>
<p>Les gens lui faisaient moins confiance qu'à son père,</p>
<p>A l'intérieur, les responsables de chaque région créaient des problèmes pour obtenir leur indépendance,</p>
<p>A l'extérieur, les pays voisins, et les empires ennemis étaient aussi à l'affut... Ils se disaient un jeune de 23ans, c'est le moment de le conquérir...</p>
<p>Donc des ennuis à l'intérieur et à l'extérieur,</p>
<p>Puis autour de lui, ceux qui l'entouraient essayaient d'obtenir les meilleurs postes, et profiter de son argent...</p>
<p>Des problèmes de partout...</p>
<p>Son ministre est venu lui dire que ca n'allait plus du tout, et qu'il fallait organiser une grande réunion,</p>
<p>« Je vais vous classer les problèmes un par un, selon leur ordre de priorité, pour trouver une solution au premier problème... Puis passer au suivant... Puis au suivant... »</p>
<p>Mohamed el-Fatih lui a répondu :</p>
<p>(Tout l'épisode tourne autour de sa réponse)</p>
<p>Il a donc répondu à son ministre</p>
<p>« Et est-ce que je passerai toute ma vie à régler les problèmes ?</p>
<p>Donne-moi les plans de Constantinople, pour lancer un grand projet,</p>
<p>Nous allons tous travailler sur ce projet,</p>
<p>Et les gens vont bouger avec nous, et leurs problèmes se résoudront tous seuls »</p>
<p>Quel est ce génie ?</p>
<p>Mohamed el-Fatih pensait de cette manière à 23ans...</p>
<p>Mes chers je vous raconte son histoire car on est souvent dans la même situation que lui,</p>
<p>On est entourés de problèmes,</p>
<p>Et si on passait notre temps à les résoudre un par un,</p>
<p>La vie deviendrait dure et sans goût...</p>
<p>Rassemble donc tous tes soucis, dans un seul grand projet à accomplir,</p>
<p>Ou un but à atteindre,</p>
<p>Et à ce moment les problèmes se résoudront tous seuls...</p>
<p>Je vais te donner des exemples concrets :</p>
<p>Moi et mon épouse... On a plein de problèmes à la maison... On n'a pas assez d'argent... on se dispute souvent... Et les enfants... Et l'appartement est trop petit... énormément de soucis... On ne saura jamais tous les résoudre, que faire alors ?</p>
<p>Ensemble trouvons une belle idée,</p>
<p>Et activons-nous pour la concrétiser !</p>
<p>Comme la phrase de Mohamed el-Fatih « Et est-ce que je passerai toute ma vie à régler les problèmes ? »</p>
<p>Il a sorti la carte de Constantinople, et s'est activé avec son peuple pour la conquérir,</p>
<p>Et les gens étaient séduits par cette idée,</p>
<p>Les problèmes ont fondu tous seuls...</p>
<p>Comme lui lançons un grand projet, et rassemblons nous derrière un grand rêve,</p>
<p>Ne passons pas nos vies à sauter d'un problème à un autre...</p>
<p>Démarre un grand rêve seul, ou avec ton épouse, ou avec ton mari, ou avec ta famille, ou dans ton pays...</p>
<p>Je te jure que la vie aura meilleur goût, et que tu dessineras un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
