                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>Je peux te poser une question concernant tes enfants ?</p>
<p>Es tu juste avec tes enfants ?</p>
<p>Tu me diras oui bien sûr !</p>
<br>
<p>Ne le prenez pas mal, mais par moments on est injustes même sans le vouloir...</p>
<p>Personne n'a l'intention d'être injuste avec ses enfants,</p>
<p>C'est juste une question de concentration,</p>
<p>Faire attention aux petits détails...</p>
<br>
<p>La justice n'est pas seulement dans les grandes choses :</p>
<p>Tu as acheté un appartement à un de tes enfants, mais pas à l'autre...</p>
<br>
<p>Non je parle des petites choses,</p>
<p>Dans le nombre de photos dans la maison...</p>
<p>Généralement il y'à plus de photos du grand comme il est là depuis plus longtemps que les autres...</p>
<p>On trouve 4 photos du grand, et une seule photo du petit,</p>
<br>
<p>Ne pensez jamais que nos enfants ne comprennent pas,</p>
<p>Ils font très attention à ce genre de détails...</p>
<p>Mais ils ne le disent pas... Ils gardent ca au fond d'eux...</p>
<p>Nos enfants font plus attention que nous aux détails,</p>
<p>Et comme leur mémoire est encore vide, ils retiennent ces choses plus que nous...</p>
<br>
<p>Parfois le nombre de photos...</p>
<p>Parfois le nombre de bisous et de câlins...</p>
<br>
<p>Le Prophète (paix et bénédiction soient sur lui) se trouvait un jour chez sa fille Fatima,</p>
<p>Ses petits fils Al-Hassan et Al-Hussein allaient dormir...</p>
<p>Et le plus petit Al-Hussein avait soif donc il a demandé à son grand père de l'eau...</p>
<p>Le Prophète (pbsl) est parti lui chercher de l'eau,</p>
<p>Et en revenant le plus grand Al-Hassan lui a dit « Grand père moi d'abord »</p>
<p>Le Prophète (pbsl) lui a répondu <strong class="prophete">« Non, il a demandé en premier, il boira en premier »</strong></p>
<br>
<p>Un simple détail,</p>
<p>Mais dedans nos enfants voient une justice...</p>
<p>Ne brisez jamais le cœur de vos enfants sans le vouloir,</p>
<p>Jusqu'ici je parlais des petites choses : Le nombre de photos, de câlins, de bisous...</p>
<p>Alors que penser quand il s'agit d'argent et d'héritage ?</p>
<br>
<p>Un jour un homme est venu voir le Prophète (pbsl) en lui disant « Ô messager de Dieu, je veux que tu sois témoin que j'ai donné à mon fils ce morceau de terre »</p>
<p>Le Prophète (pbsl) lui a demandé <strong class="prophete">« As-tu donné à tes autres enfants la même chose que ce que tu lui a donné ? »</strong></p>
<p>Cet homme a dit « Non Ô messager de Dieu »</p>
<p>Le Prophète (pbsl) lui a répondu <strong class="prophete">« Trouve un autre témoin, car moi je ne témoignerai pas sur une truquerie »</strong></p>
<br>
<p>Que c'est dur à entendre... C'est une falsification... Une truquerie... Une tromperie... Je ne serai pas témoin de ca...</p>
<br>
<p>Combien de filles se font retirer leur part d'héritage par leurs frères...</p>
<p>Ils leurs disent on la garde pour vous,</p>
<p>Si on vous la laisse, le jour où vous allez vous marier, votre mari mangera votre part...</p>
<p>Alors elle est plus en sécurité avec nous...</p>
<p>Et si jamais vous avez besoin de quelque chose venez nous voir...</p>
<br>
<p>Mon cher c'est son droit... Donne-lui sa part... Sois juste avec tes sœurs...</p>
<p>Sois juste dans l'héritage...</p>
<p>Sois juste avec tes enfants...</p>
<p>Sois juste dans le nombre de photos...</p>
<p>Sois juste dans le nombre de câlins...</p>
<p>Sois juste dans le nombre de bisous...</p>
<p>Sois juste dans le nombre de mots doux...</p>
<p>Sinon ca devient une truquerie,</p>
<p>Et celui qui n'est pas juste dans les petites choses, ne pourra pas l'être quand les grandes choses vont arriver.</p>
<br>
<p>Je t'en prie sois juste avec tes enfants pour qu'ils te voient avec un sourire d'espoir toutes leurs vies.</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
