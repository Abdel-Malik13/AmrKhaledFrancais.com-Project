                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                        
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Je veux vous montrer dans l'épisode d'aujourd'hui à quel point le Prophète (que la paix et les bénédictions de Dieu soient sur lui) donnait de l'importance à l'amour.</p>
<br>
<p>Et à quel point l'amour était cher aux yeux du Prophète (pbsl)</p>
<p>Et à quel point il œuvrait pour que l'amour survive....</p>
<br>
<p>C'est une histoire très connue,</p>
<p>D'un homme qui s'appelait Moughith, il était marié à Barira, et tous les deux étaient esclaves....</p>
<p>Barira travaillait chez Aïcha (l'épouse du Prophète pbsl)</p>
<p>Aïcha a affranchit Barira,</p>
<p>Barira était donc libre, elle a demandé le divorce de Moughit qui lui était encore esclave...</p>
<p>Barira faisait le tour de la Ka'aba (la mosquée sacrée à la Mecque)</p>
<p>Elle était doublement contente...</p>
<p>Contente de s'être libérée de l'esclavage...</p>
<p>Et contente de s'être libérée de Moughith...</p>
<p>Mais Moughit lui l'aimait profondément...</p>
<p>Pendant que barira tournait autour de la Ka'aba, Mourith la suivait en pleurant...</p>
<p>Il la suppliait pour qu'elle lui revienne...</p>
<p>Parfois il y a vraiment des femmes difficiles :)</p>
<p>Son homme était en pleurs, et elle ne le calculait même pas...</p>
<p>Devant la Ka'aba se trouvait le Prophète (pbsl) avec son oncle Al'Abbas,</p>
<p>Al'Abbas a dit au Prophète (pbsl) « As-tu vu ? »</p>
<br>
<p>Le Prophète (pbsl) a répondu <strong class="prophete">« Oui Al'Abbas, je n'ai jamais vu quelqu'un autant aimer comme Moughith aime Barira... Et je n'ai jamais vu quelqu'un autant détester comme Barira déteste Moughith... »</strong></p>
<p>Al'Abbas a dit « Ne vas-tu pas leur parler Ô messager d'Allah...»</p>
<p>Le Prophète (pbsl) a dit <strong class="prophete">« Oui je vais le faire »</strong></p>
<br>
<p>Le Prophète (bpsl) va donc intercéder, pour que Barira revienne à Moughith,</p>
<p>A ce point l'amour était précieux aux yeux du Prophète (bpsl)...</p>
<p>Il est parti voir Barira en lui disant <strong class="prophete">« Ô Barira, ne vois tu pas dans quel état est Moughith ? »</strong></p>
<p>Elle a répondu « Je vois, Ô messager de Dieu »</p>
<p>Le Prophète (pbsl) lui a alors demandé <strong class="prophete">« Ne vas-tu pas revenir à lui... »</strong></p>
<p>Barira a demandé « Est-ce un ordre Ô messager de Dieu ? »</p>
<p>Le Prophète (pbsl) a dit <strong class="prophete">« Non, je ne fais qu'intercéder... »</strong></p>
<p>Barira a alors répondu « Je n'ai plus besoin de lui Ô messager de Dieu »</p>
<p>Le Prophète (pbsl) lui a dit <strong class="prophete">« Tu es libre de choisir... »</strong></p>
<br>
<p>Elle n'est donc pas revenue, et Moughith a souffert un peu...</p>
<p>Mais je vous raconte cette histoire d'un autre angle,</p>
<p>Le Prophète (pbsl) s'est levé... Il a intercédé... Il a fait l'effort... à la fin elle n'a pas accepté...</p>
<p>Mais il a essayé tout ce qu'il a pu pour sauver cet amour...</p>
<p>Et il n'a obligé personne...</p>
<p>Il a laissé Barira décider seule...</p>
<br>
<p>Un très bel exemple du Prophète (pbsl) , et un grand respect pour les sentiments des autres...</p>
<p>Le Prophète (pbsl) a senti la souffrance de Moughith... Il a tenté de l'aider... Il a essayé de défendre son amour.</p>
<p>L'amour a un grand sens dans l'islam, un sentiment très précieux,</p>
<p>Un sentiment qui dessine un sourire d'espoir sur nos visages.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
