                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui te dit en toute clarté :</p>
<p>Enlève le masque que tu portes !</p>
<p>Tu me diras quel masque... Tu veux me faire peur ?</p>
<p>;)</p>
<p>Enlève le masque que tu portes...</p>
<p>Il y'a des gens mes chers qui avancent dans la vie avec des masques,</p>
<p>Une même personne qui a plusieurs visages,</p>
<p>Il apparait ici avec un visage... Là-bas avec un 2ème visage... Puis un 3ème visage ailleurs...</p>
<p>Je vous jure que ca les fatigue,</p>
<p>Car le fait de mettre et enlever à chaque fois de visage, est très pénible,</p>
<p>Ici il joue le rôle du gentil... Là-bas le rôle du méchant...</p>
<p>Ici il rigole avec quelqu'un... Là-bas il devient hypocrite avec quelqu'un d'autre...</p>
<p>Puis à une troisième personne il montre un visage énervé...</p>
<p>Et en réalité aucun de ses visages n'est son vrai...</p>
<p>Ce ne sont que des masques qu'il porte,</p>
<p>Ici il porte le masque de l'intégrité et de la droiture,</p>
<p>Là-bas il porte le masque du pauvre nécessiteux...</p>
<p>Pourquoi ne pas être juste comme il est,</p>
<p>Rester naturel,</p>
<p>Marcher avec beaucoup de visages est très fatiguant,</p>
<p>Et si un jour il se fait démasquer il devient très gêné...</p>
<p>Vous savez à quoi ressemble le fait de mettre des masques ?</p>
<p>Ca ressemble au fait d'escalader une montagne chaque jour... C'est très épuisant !</p>
<p>Alors que marcher avec un seul visage, le vrai visage est une chose très aisée.</p>
<p>Je vais vous raconter une situation drôle :</p>
<p>On raconte que quelqu'un a été nommé directeur dans une entreprise,</p>
<p>Quand il s'est assis dans son bureau, il avait encore du mal à le croire,</p>
<p>Il avait un complexe,</p>
<p>Il sentait que le bureau et la fonction étaient plus grands que lui,</p>
<p>Et il était assis dans son impressionnant fauteuil de direction, il s'y sentait petit...</p>
<p>Avec un téléphone à gauche, et un téléphone à droite,</p>
<p>Il voulait donc montrer aux autres qu'il était quelqu'un d'important...</p>
<p>Vous connaissez les gens qui portent le masque « Je suis quelqu'un d'important »,</p>
<p>La secrétaire est venue lui dire qu'il avait un visiteur,</p>
<p>Il était tout seul dans son bureau, il pouvait lui dire simplement d'entrer...</p>
<p>Mais non, il a dit à la secrétaire de faire patienter la personne dans la salle d'attente,</p>
<p>Puis au bout d'un moment il lui a dit c'est bon tu peux le faire entrer,</p>
<p>Tout ca pour montrer qu'il est occupé,</p>
<p>Il était tellement concentré sur son masque qu'il a oublié de demander à la secrétaire qui était la personne qui venait le voir...</p>
<p>C'est une histoire qui fait rire, mais elle s'est réellement produite :)</p>
<p>La personne qui voulait le voir était un jeune homme qui a fini par entrer, et le directeur tenait le téléphone dans la main</p>
<p>« Allo... Oui... Oui... L'affaire est difficile, mais je m'en occuperai... »</p>
<p>Le jeune homme ne disait pas un mot, il attendait sagement...</p>
<p>Et le directeur l'ignorait... Vous avez surement déjà vu cette scène...</p>
<p>Des masques... Que des masques...</p>
<p>« C'est vrai que ca va être difficile, mais je m'en occuperai personnellement, laissez-moi juste un peu de temps car là je suis débordé par le travail... »</p>
<p>Le directeur mettait trop de temps au téléphone... Et le jeune homme était debout devant lui,</p>
<p>Le directeur a fini par raccrocher, puis il a à moitié regardé le jeune homme en disant « Oui...Oui... Vous êtes là pour quelle raison ? »</p>
<p>Le jeune homme lui a répondu « Je suis le technicien qui vient réparer les téléphones, ils sont en panne »</p>
<p>:)</p>
<p>A la fin le directeur s'est fait démasquer... Il ne parlait à personne au bout du fil.</p>
<p>Comme parfois ca arrive à des jeunes qui veulent impressionner les filles,</p>
<p>Qui font semblant de parler sérieusement au téléphone... Puis il finit par sonner :)</p>
<p>Mon cher abandonne les masques,</p>
<p>Reste naturel,</p>
<p>Reste modeste,</p>
<p>Et crois moi ta vie sera meilleure,</p>
<p>Je te jure que les masques sont lourds à porter,</p>
<p>Ne joue pas le rôle d'un autre,</p>
<p>A ce moment tu dessineras un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
