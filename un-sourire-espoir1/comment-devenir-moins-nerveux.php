                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>En ce moment les gens sont fatigués,</p>
<p>A cause de ce qui se passe dans le monde, de la chaleur ou du froid, des crises économiques,</p>
<p>Les gens sont nerveux, tendus, stressés... etc.</p>
<p>L'épisode d'aujourd'hui leur est dédicacé :)</p>
<p>Comment devenir moins nerveux,</p>
<p>Comment retrouver son calme,</p>
<p>Car quand on s'énerve on se fâche avec les autres,</p>
<p>Parfois on se fâche avec nos enfants ou notre compagnon...</p>
<p>Parfois avec le voisin,</p>
<p>Parfois avec des inconnus dans la rue...</p>
<p>Le problème c'est que tu es conscient que tu fais mal aux gens, mais tu dis « Je n'y peux rien, je suis tendu en ce moment »</p>
<p>Je vais vous raconter l'histoire d'un père,</p>
<p>Son fils était du genre très nerveux,</p>
<p>La moindre chose l'irritait,</p>
<p>Il criait, et se battait pour un oui ou un non...</p>
<p>Son père a utilisé une très belle méthode avec lui :</p>
<p>Il lui a donné une planche de bois, un marteau et des clous,</p>
<p>Puis lui a dit « Chaque fois que tu t'énerves contre quelqu'un, enfonce un clou dans la planche, c'est tout, je ne te demande rien d'autre. »</p>
<p>Il parait que le fils cherchait vraiment à se débarrasser de sa nervosité, il a mis la planche de bois dans sa chambre.</p>
<p>Chaque fois qu'il se disputait avec une personne, il prenait le marteau et enfonçait un clou...</p>
<p>Le premier jour il a planté 30 clous dans la planche...</p>
<p>Il s'est rendu compte que c'était beaucoup trop.</p>
<p>Le lendemain il en a planté 20...</p>
<p>Le troisième jour il est descendu à 10 clous...</p>
<p>Puis le quatrième jour 5 clous...</p>
<p>(C'est une solution gratuite et concrète que je tenais à partager avec vous)</p>
<p>Au bout d'une semaine, il ne plantait plus aucun clou, car il a appris à se maitriser,</p>
<p>Le fait d'enfoncer des clous tac... tac... tac... tac... a joué psychologiquement,</p>
<p>Son papa est venu lui dire « Je suis fier de toi ! Mais j'ai encore une dernière demande »</p>
<p>Il a bien sûr demandé ce que c'était ?</p>
<p>Son papa lui a répondu « Je veux que tu arraches tous les clous que tu as planté dans la planche de bois »</p>
<p>Il a dit « Ca va être difficile, il y'en a beaucoup trop ! »</p>
<p>Le père a insisté,</p>
<p>Le fils a fini par arracher tous les clous,</p>
<p>Mais la planche de bois était pleine de trous...</p>
<p>Le père lui a dit « Il s'est passé la même chose avec chaque personne que tu as blessée à cause de ta nervosité,</p>
<p>Ca a laissé des traces dans leurs cœurs et dans leurs mémoires. »</p>
<p>Et je m'adresse à toi pour te demander combien de clous as-tu enfoncé dans le cœur de ta femme ou ton mari ?</p>
<p>:)</p>
<p>Pareil avec tes parents, est-ce possible que tu aies planté plein de clous dans le cœur de ta maman ?</p>
<p>Imagine les cicatrices qui restent à cause d'un mot dur, ou d'une réaction excessive...</p>
<p>Peux-tu te rappeler de cette planche de bois,</p>
<p>Et à partir d'aujourd'hui retiens toi,</p>
<p>Et si tu as du mal, met une vraie planche dans ta chambre et applique la méthode des clous.</p>
<p>Que Dieu nous guide,</p>
<p>Qu'il protège nos cœurs et nos maisons,</p>
<p>Et qu'il nous aide à garder notre calme en ces temps difficiles.</p>
<p>On ne dessinera un sourire d'espoir que si on arrive à maitriser notre colère.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
