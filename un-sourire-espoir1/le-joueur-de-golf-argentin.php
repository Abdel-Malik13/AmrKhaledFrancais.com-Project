                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est une histoire qui nous vient d'Argentine,</p>
<p>C'est un champion de golf argentin appelé Roberto De Vicenzo</p>
<p>Désolé pour la prononciation, j'ai fait comme j'ai pu ;)</p>
<p>Il venait de remporter un grand tournoi en Amérique latine,</p>
<p>Il a reçu le chèque du vainqueur,</p>
<p>Il l'a montré devant les photographes et les caméras... Le protocole classique après une victoire.</p>
<p>Après avoir fini les interviews et les photos,</p>
<p>Il est monté dans sa voiture pour partir...</p>
<p>Et c'est là qu'une femme est venue le voir,</p>
<p>Elle portait un enfant...</p>
<p>Elle lui a dit « Je t'en prie, cet enfant souffre d'une maladie grave... Il risque de mourir »</p>
<p>Elle a dit qu'elle ne savait pas comment faire pour payer les soins et les frais d'hôpital ...
<p>Roberto De Vicenzo a regardé l'enfant,</p>
<p>Il a été touché par son état et sa faiblesse...</p>
<p>Il a sorti son stylo puis il a fait quelque chose d'exceptionnel :</p>
<p>Il a pris le chèque, et il l'a modifié pour que cette femme puisse l'encaisser.</p>
<p>Et en lui remettant le chèque il lui a dit « Fais en sorte que les jours de ton fils soient remplis de bonheur »</p>
<p>Allah !</p>
<p>Le montant du chèque ne suffit pas seulement pour l'opération... Le montant du chèque pourrait suffire pour toute la vie de cet enfant.</p>
<p>« Fais en sorte que les jours de ton fils soient remplis de bonheur »</p>
<p>Elle est très belle cette phrase !</p>
<p>Il lui a remis le chèque puis il est parti.</p>
<p>La semaine suivante, Roberto déjeunait dans son club de golf,</p>
<p>Un des responsables du club est venu le voir avec un sourire jaune...</p>
<p>Il lui a demandé pourquoi il le regardait de cette façon ?</p>
<p>Le responsable lui a dit « Un des jeunes portiers du club m'a dit que la semaine dernière tu as rencontré une femme, et que tu lui as remis le chèque de ta victoire ? »</p>
<p>Roberto lui a dit « Oui »</p>
<p>Le responsable s'est mis à rire, il lui a dit « Cette femme est une arnaqueuse, elle n'a pas de fils malade, elle n'est pas encore mariée, c'était juste une escroquerie pour te prendre ton argent »</p>
<p>Quel est votre avis ? Il s'est fait avoir, il a perdu son chèque...</p>
<p>En réalité je vous raconte cette histoire rien que pour la réponse de Roberto :</p>
<p>Il a demandé au responsable « Tu parles sérieusement ? »</p>
<p>Il lui a dit « Oui ! »</p>
<p>Il lui a demandé « Alors il n y'a pas d'enfant en danger de mort ? »</p>
<p>Il lui a dit « Oui ! »</p>
<p>Il lui a répondu « C'est la plus belle nouvelle que j'ai entendu toute ma vie ! »</p>
<p>Le responsable lui a demandé pourquoi,</p>
<p>Il a dit « Si l'argent est parti, et qu'il n'y a pas d'enfant malade alors Dieu merci ! »</p>
<p>Quelqu'un raisonne de cette façon ?</p>
<p>Il lui a dit qu'il y'avait des pertes qu'on peut rattraper, et des pertes qu'on ne peut pas rattraper...</p>
<p>Si on perd de l'argent on peut le regagner ailleurs... Mais si un enfant est malade j'aurai continué à avoir mal au cœur, même après avoir donné l'argent.</p>
<p>Puisqu'il n'y a pas d'enfant malade, c'est une excellente nouvelle !</p>
<p>Cet homme est très beau de l'intérieur n'est ce pas ?</p>
<p>Il s'est fait arnaquer, et c'est sûr que la prochaine fois il fera très attention,</p>
<p>Mais il est heureux du fait qu'il n'y ait pas d'enfant malade...</p>
<p>Il est très humain !</p>
<p>Quel est ton avis sur cette histoire ?</p>
<p>Tu me diras que c'est un homme exemplaire,</p>
<p>C'est possible...</p>
<p>Cet homme aime l'humanité, il aime le bien pour tout le monde...</p>
<p>Si tu as en toi cette qualité, tu vivras heureux même si on t'arnaque une fois...</p>
<p>Tu vivras heureux car tu as envie que tous les gens soient en bonne santé.</p>
<p>Es-tu humain comme cet homme argentin ?</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
