                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>J'ai envie de te poser une question :</p>
<p>Comment réagis tu face aux changements de la vie ?</p>
<p>Les acceptes tu avec un cœur grand ouvert ?</p>
<p>Ou bien as-tu du mal à les accepter, ils te mettent mal à l'aise,</p>
<p>Et tu vis dans des soucis et de l'inquiétude, et de la dépression... ?</p>
<p>Notre pays l'Egypte par exemple a changé de mille degrés après la révolte...</p>
<p>Et toi qui m'écoutes, ta vie en 5 ans a changé de mille degrés...</p>
<p>Ta vie personnelle quand tu étais à l'université,</p>
<p>Puis quand tu as été diplômé,</p>
<p>Puis quand tu as changé de ville...</p>
<p>Les changements mes chers...</p>
<p>Ca perturbe certaines personnes...</p>
<p>Puis l'autre s'est marié...</p>
<p>Et l'autre a eu des enfants...</p>
<p>Et l'autre a voyagé...</p>
<p>1000 changements et changements...</p>
<p>Le bas monde dans lequel nous vivons est sans arrêt en mouvement !</p>
<p>Puis tes avis changent,</p>
<p>Et tes idées changent,</p>
<p>Et tes sentiments changent,</p>
<p>Donc tout change ? Oui tout change !</p>
<p>Alors... Moi ca me gêne beaucoup... Et ca me déprime... Et je ne supporte pas ces changements...</p>
<p>Non mon cher, il faut que tu les acceptes avec flexibilité !</p>
<p>Je vais te dire 2 choses qui vont te faire du bien :</p>
<p>Premièrement, tu vois l'appareil qui dessine les battements du cœur ?</p>
<p>Le moniteur qu'on voit dans les hôpitaux, et qui dessine le rythme cardiaque de la personne...</p>
<p>Pour voir si le cœur bat toujours ou pas,</p>
<p>Les traits qu'il dessine montent et descendent... Montent et descendent... sans arrêt !</p>
<p>Et tant que ca monte et ca descend ca signifie que tu es en vie !</p>
<p>Sais tu quand tu meurs ?</p>
<p>Quand les courbes se transforment en un trait horizontal...</p>
<p>Peux tu voir ta vie comme dans ce moniteur ?</p>
<p>Si ta vie se transforme en trait, que se passe t'il ?</p>
<p>L'être humain meurt...</p>
<p>La preuve que ton cœur est vivant, c'est qu'il est en mouvement... En changement continu...</p>
<p>La vie est pareille !</p>
<p>Peux tu accepter les changement de la vie comme une règle universelle...</p>
<p>Comme la preuve que tu es en vie !</p>
<p>Je vais vous dire une chose mes chers :</p>
<p>As tu un album photo ?</p>
<p>Jette un coup d'œil sur une photo de toi quand tu avais 12 ans...</p>
<p>C'est impressionnant comment tu as changé depuis :)</p>
<p>Regarde la photo de ta maman et de ton papa...</p>
<p>comment ils ont changé depuis... Mon Dieu Ils ont entièrement changé !</p>
<p>Regarde les rides sur les visages...</p>
<p>Regarde les cheveux blancs...</p>
<p>Quelqu'un me parlait l'autre jour en me disant « Je vous suis depuis 10ans, Je vous jure par Dieu que je vous écoute depuis 10 ans... Depuis que votre voix était plus fine... »</p>
<p>Ca m'a beaucoup déplu d'entendre ca :)</p>
<p>J'ai dis « Ben elle est comment ma voix maintenant ? »</p>
<p>Il m'a répondu « Vous avez pris de l'âge, votre voix est plus grave, elle a changé avec les années »</p>
<p>Ca ne m'a pas plu du tout !</p>
<p>Je me suis dis même notre voix change ?</p>
<p>La vie est ainsi mes chers, comme le rythme cardiaque ca monte et ca descend sans arrêt,</p>
<p>Et nos albums photo en sont la meilleure preuve,</p>
<p>Tu ne peux pas lutter contre les changements ou les empêcher...</p>
<p>C'est une des règles de l'univers,</p>
<p>Notre pays a beaucoup changé et va encore changer,</p>
<p>Et ta vie personnelle a changé et va encore changer,</p>
<p>Un conseil précieux :</p>
<p>Veux tu qu'un sourire d'espoir se dessine sur ton visage ?</p>
<p>Accepte les changements avec souplesse,</p>
<p>Accepte les parce qu'ils font partie des règles de la vie,</p>
<p>Ne les refuse pas en étant raide et têtu... Jusqu'à ce que tu te fasses briser...</p>
<p>Non ! Sois souple !</p>
<p>Sois comme le silicone et dis toi que c'est une des bases de la vie,</p>
<p>Qu'on vive donc chaque étape de nos vies avec sagesse,</p>
<p>Et intelligence, et acceptation,</p>
<p>Et avec le sourire,</p>
<p>Et avec une grande flexibilité,</p>
<p>Et à ce moment là, je vous le jure mes chers, nous vivrons une meilleure vie,</p>
<p>Et une vie plus calme,</p>
<p>Parce que nous aurons accepté la nature de la vie !</p>
<p>Je vous remercie, et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
