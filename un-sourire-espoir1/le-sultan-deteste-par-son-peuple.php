                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'idée de l'épisode d'aujourd'hui est :</p>
<p>Si tu décides de prendre un chemin pour faire le bien... Ne t'arrête jamais au milieu...</p>
<p>Ca veut dire quoi ?</p>
<p>Par exemple si tu te lances dans un projet pour réformer ton entreprise, ou régler un problème dans ton quartier, ou dans ton pays...</p>
<p>Si au milieu de la réforme tu décides de faire demi-tour,</p>
<p>Le problème deviendra plus grave qu'avant...</p>
<p>Je vais vous raconter une histoire pour mieux comprendre :</p>
<p>On raconte qu'il y'avait un sultan tyran,</p>
<p>Au très mauvais comportement,</p>
<p>Très injuste envers son peuple,</p>
<p>Il faisait peur à tout le monde...</p>
<p>Un jour il a décidé de se déguiser et descendre dans la rue pour voir ce que les peuple pense vraiment de lui...</p>
<p>Il a été surpris que tous les gens le détestent,</p>
<p>Et l'insultent,</p>
<p>Et critiquent ses actes,</p>
<p>Et se moquent de lui,</p>
<p>Et disent vivement qu'on se débarrasse de lui...</p>
<p>Le sultan est revenu dans son palais en étant très inquiet,</p>
<p>Il a pensé à une solution :</p>
<p>Il va trouver une chose que le peuple détestera plus que lui... Donc les gens vont l'oublier et se concentrer sur cette chose...</p>
<p>Il a fait venir un immense éléphant,</p>
<p>Et l'a laissé en liberté dans le pays,</p>
<p>Et bien sur comme c'est l'éléphant du sultan, personne ne pouvait le critiquer ou s'opposer à sa liberté...</p>
<p>L'éléphant faisait trop de dégâts dans le pays,</p>
<p>Il détruisait les récoltes,</p>
<p>Il cassait les ponts,</p>
<p>Il terrorisait les femmes et les enfants,</p>
<p>Les gens avaient peur de sortir...</p>
<p>Du coup plutôt que de parler du sultan et ses problèmes,</p>
<p>Le peuple parlait de l'éléphant et ses dégâts...</p>
<p>Les gens ne savaient plus quoi faire, alors ils sont partis voir le sage du village pour trouver une solution,</p>
<p>(Donc ils vont entamer une réforme)</p>
<p>Mais personne ne peut se tenir devant le sultan et le critiquer ou critiquer son éléphant...</p>
<p>Le vieux sage a trouvé une idée intelligente :</p>
<p>« On partira tous ensemble, Je serai devant le sultan et vous serez derrière moi,</p>
<p>Je dirai « Mon sultan, l'éléphant»</p>
<p>Et vous compléterez ma phrase « A ruiné les récoltes »</p>
<p>Je dirai encore « Mon sultan, l'éléphant »</p>
<p>Et vous direz « A cassé les ponts »</p>
<p>Puis je dirai « Mon sultan, l'éléphant »</p>
<p>Et vous direz « Fait peur aux femmes et aux enfants »</p>
<p>Comme ca on sera tous sauvés,</p>
<p>Moi j'aurai juste cité l'éléphant sans l'accuser,</p>
<p>Et vous, vous n'aurez pas cité l'éléphant dans vos phrases... »</p>
<p>Le lendemain ils se sont tous donné rendez-vous devant le sultan,</p>
<p>Le sage du village s'est mis devant, et les gens derrière lui...</p>
<p>Le vieux sage a dit « Mon sultan, l'éléphant... »</p>
<p>Puis silence...</p>
<p>Le vieux s'est retourné pour voir les gens,</p>
<p>Il a redit « Mon sultan, l'éléphant... »</p>
<p>Personne ne complétait la phrase...</p>
<p>Apparemment ils n'ont pas respecté leur parole,</p>
<p>Le sage du village se trouvait dans une situation délicate, il courrait à sa perte...</p>
<p>Le sultan lui a dit « l'éléphant, l'éléphant, l'éléphant... Y a t-il un problème avec l'éléphant ?? »</p>
<p>Les gens avaient abandonné le vieux sage, alors pour se sauver il a répondu</p>
<p>« Mon sultan, l'éléphant est formidable... Il se sent juste un peu seul, je propose de lui ramener une femelle et d'autres éléphants pour lui tenir compagnie »</p>
<p>Le sultan lui a dit « C'est une excellente idée ! Je vais faire venir 5 ou 6 éléphants supplémentaires »</p>
<p>En repartant, le sage du village s'est adressé aux gens en disant « C'est ce qui arrive à ceux qui se lancent dans une réforme et font demi-tour au milieu ! »</p>
<p>L'idée vous est arrivée ?</p>
<p>Un sourire d'espoir à tous ceux qui veulent œuvrer dans le bien.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
