                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui est très humain...</p>
<p>Comment te comporter avec les gens quand leurs vies deviennent difficiles,</p>
<p>Comment rester doux avec les autres pendant leurs épreuves...</p>
<p>Surtout les personnes proches de toi,</p>
<p>Ton épouse par exemple,</p>
<p>Je vais vous raconter une histoire qui s'est passée dans une autre époque :</p>
<p>On dit qu'un homme s'est marié à une très belle femme,</p>
<p>Et il a vécu de longues années à ses côtés,</p>
<p>Elle était douce avec lui,</p>
<p>Elle a toujours pris soin de lui,</p>
<p>Il trouvait chez son épouse de la beauté et de la tendresse...</p>
<p>Malheureusement un jour, cette femme a été atteinte d'une maladie,</p>
<p>Des boutons son apparus sur son visage,</p>
<p>Ce même visage qui était très beau, a été transformé...</p>
<p>Ca la faisait énormément souffrir,</p>
<p>Elle pensait à son mari qui avant cette maladie admirait son visage pendant des heures, il ne pourra plus le faire,</p>
<p>Son comportement a commencé à changer,</p>
<p>Elle devenait agressive et nerveuse,</p>
<p>Comme si par amour, elle voulait éloigner son mari pour qu'il ne souffre pas avec elle ...</p>
<p>Et là surprise,</p>
<p>Son mari a aussi été atteint d'une maladie qui l'a rendu aveugle...</p>
<p>(Tu me diras c'est quoi cette histoire triste... Pas grave va jusqu'au bout)</p>
<p>Vous me croyez si je vous dis que quand le mari est devenu aveugle,</p>
<p>Cette femme a commencé à se calmer,</p>
<p>C'est psychologique, elle se disait qu'il avait gardé une belle image d'elle dans sa tête,</p>
<p>Les deux ont continué leurs vies ensemble,</p>
<p>Ils ont vécu 10 ou 15ans avec amour...</p>
<p>Et l'épouse est décédée,</p>
<p>Au moment de l'enterrement, les gens ont été surpris de voir que c'est le mari lui-même qui s'en occupait...</p>
<p>Tu n'es pas aveugle ?</p>
<p>Tu n'as pas perdu la vue ?</p>
<p>Non il voyait toujours, mais faisait semblant devant son épouse d'être aveugle...</p>
<p>Pour ne pas la perdre,</p>
<p>Pour qu'elle se sente à l'aise malgré la maladie qui avait changé son visage...</p>
<p>Et il a tenu pendant plusieurs années pour l'amour qu'il avait pour elle.</p>
<p>Tu me diras que c'est une histoire utopique...</p>
<br>
<p>Mais elle peut nous servir de motivation,</p>
<p>Pour soutenir les gens qui nous ont aimés et aidés dans notre vie,</p>
<p>Combien de personnes nous ont rendus des services... Et on a fini par les oublier...</p>
<p>Combien de personnes nous ont aidés... Et lorsque leurs vies ont changé on s'est éloignés d'eux...</p>
<p>Combien de personnes quand leurs situations sont devenues compliquées... On a arrêté de pendre de leurs nouvelles...</p>
<br>
<p>En réalité on ignore si c'est une histoire vraie ou pas,</p>
<p>Elle y'a même des chances qu'elle se soit vraiment produite,</p>
<p>Mais cet homme est formidable,</p>
<p>Es-tu à ce point humain avec ton épouse ou ton mari ?</p>
<p>Es-tu à ce point humain avec tes parents ?</p>
<p>Ton papa a pris de l'âge... Son physique commence à changer... Et ses mouvements aussi...</p>
<p>Est-ce que ton comportement avec lui commence aussi à changer ?</p>
<p>Avec l'âge il ne comprend pas aussi vite qu'avant... Il met plus de temps à réagir... Est-ce que ca t'énerve ?</p>
<p>Est-ce que tu perds ta patience ?</p>
<p>Es-tu humain comme l'homme de l'histoire ?</p>
<p>Je vous jure mes chers que le sourire d'espoir c'est de rester fidèle et loyal envers les gens qui t'ont honoré, qui t'ont aidé, même si la vie les a changés...</p>
<p>Surtout les membres de ta famille, ton épouse, ton papa et ta maman...</p>
<br>
<p>Un sourire d'espoir,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
