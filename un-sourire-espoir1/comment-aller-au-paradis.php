                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>Le sourire d'espoir d'aujourd'hui parle d'une très très très belle chose...</p>
<p>Malheureusement beaucoup de personnes ne la ressentent pas, ne vivent pas avec et n'en rêvent pas...</p>
<br>
<p>Le Paradis !</p>
<p>As-tu envie d'entrer au Paradis ?</p>
<p>Ou tu n'y penses jamais... On vit puis on verra bien...</p>
<p>Personne ne sait si on entrera au Paradis ou pas...</p>
<p>Non mes chers !</p>
<p>Avoir hâte d'être au Paradis est une faveur que Dieu nous a accordée,</p>
<p>C'est un rêve qui te rendra fort dans ta vie,</p>
<p>Je veux entrer au Paradis !</p>
<p>J'ai envie de profiter du Paradis,</p>
<p>Et être entouré de mes proches et de mes amis,</p>
<p>Et profiter des palais du Paradis et des rivières du Paradis,</p>
<p>Et les ballades, et les rigolades,</p>
<p>Et Que Dieu soit satisfait de moi,</p>
<p>Et rencontrer le Prophète (paix et bénédictions soient sur lui) au Paradis,</p>
<p>Aimer et être aimé au Paradis...</p>
<p>Le Paradis mes chers !</p>
<p>Cette vie n'est qu'un test, nous voulons le réussir et aller au Paradis,</p>
<br>
<p>Je vais vous raconter une histoire sympathique :</p>
<p>Si un enfant vient te voir en te disant « J'ai quelque chose à te donner... »</p>
<p>Tu vas t'attendre à recevoir une chose de son niveau... 100 € au maximum...</p>
<p>Mais si le directeur d'une banque te dit « J'ai quelque chose à te donner... »</p>
<p>Tu vas t'attendre à quelque chose de plus grand... 200.000 €</p>
<p>Et si c'est le président d'un pays ou un roi qui te dit la même chose...</p>
<p>Tu te diras là c'est au moins un million d'euros...</p>
<p>Et si c'est le Seigneur de l'univers, le créateur de toute chose qui te dit</p>
<p><strong>« il y a au paradis ce que nul oeil n'a vu, nulle oreille n'a entendu et nul coeur d'humain n'a imaginé »</strong></p>
<p>Parole du Prophète (pbsl)</p>
<br>
<p>Dieu nous dit qu'il nous a préparé au Paradis Ce qu'aucun œil n'a jamais vu...</p>
<p>Ce qu'aucune oreille n'a jamais entendu...</p>
<p>Et ce que personne n'a jamais imaginé...</p>
<br>
<p>Alors tu peux t'attendre à quoi ?</p>
<p>L'enfant tu t'attends à une petite chose...</p>
<p>Le directeur tu agrandis tes attentes...</p>
<p>Le roi tu agrandis encore plus...</p>
<p>Et quand c'est Dieu le très haut ?</p>
<br>
<p>Le rêve du Paradis est très beau,</p>
<p>Mais attention ce n'est pas une anesthésie, surtout pas !</p>
<p>Jamais le Prophète (pbsl) n'a cité le Paradis sans le lier à la réussite dans ce bas monde,</p>
<p>Il te dit dans une parole :</p>
<p><strong class="prophete">« Le Paradis est sous les pieds des mères »</strong></p>
<p>Satisfais ta maman, fais tout pour la rendre heureuse et tu entreras au Paradis...</p>
<p>Ce n'est pas un anesthésiant mais une invitation à faire des efforts dans ta vie,</p>
<p>Ce n'est pas vivons n'importe comment, puis à la fin on entrera au Paradis... Non réussis dans ta vie d'abord, tu entreras au Paradis ensuite !</p>
<p>Et dans une autre parole le Prophète (pbsl) te dit :</p>
<p><strong class="prophete">« Quiconque emprunte un chemin dans le but de rechercher une science, Allah lui facilitera un chemin menant au Paradis »</strong></p>
<p>Instruis-toi, réussis dans tes études... Et Dieu te facilitera le Paradis...</p>
<br>
<p>Rêve du Paradis mon cher,</p>
<p>Le soir en dormant demande à Dieu de te faire entrer au Paradis,</p>
<p>Et le lendemain au réveil active toi, et cherche la réussite dans ta vie,</p>
<p>Et travaille,</p>
<p>Et construis des projets,</p>
<p>Et rend les gens heureux autour de toi,</p>
<p>Que Dieu nous réunisse au Paradis, et dessine sur nos visages un sourire éternel.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
