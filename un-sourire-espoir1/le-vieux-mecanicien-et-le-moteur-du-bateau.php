                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>On raconte qu'un grand navire était tombé en panne,</p>
<p>(C'est une histoire vraie d'ailleurs)</p>
<p>Un des moteurs de ce navire était tombé en panne,</p>
<p>La société à qui appartenait le bateau a appelé tous les experts pour le réparer...</p>
<p>Mais sans résultat,</p>
<p>Tant que le navire restait sans bouger, la société perdait énormément d'argent...</p>
<p>Jusqu'à ce qu'on leur conseille un monsieur très âgé spécialisé dans ce genre de moteurs,</p>
<p>Ils ont donc couru le chercher,</p>
<p>Le vieux mécanicien est arrivé avec sa petite boite à outils...</p>
<p>Avec un très grand calme, il a inspecté le moteur de haut en bas...</p>
<p>Et l'équipage du bateau était autour de lui entrain de regarder...</p>
<p>Ils mourraient d'envie de savoir ce qu'il allait faire,</p>
<p>Le vieux mécanicien a fini par ouvrir sa boite à outils...</p>
<p>Il en a sorti un petit marteau et un clou,</p>
<p>Il a planté le clou dans un coin du moteur,</p>
<p>Et en 5min le moteur a tourné,</p>
<p>Le monsieur a donc remis le marteau dans sa boite à outils, puis il est parti...</p>
<p>Donc 20/20 , mission terminée.</p>
<p>Une semaine après il a envoyé la facture à la société à qui appartenait le navire,</p>
<p>La facture est de combien ?</p>
<p>Le montant est de 10.000 dollars.</p>
<p>Le coup de marteau qu'il a fait valait dix mille dollars ?</p>
<p>Le moteur du bateau coutait des millions de dollars, mais les patrons de la société n'ont pas voulu payer...</p>
<p>Ils ont envoyé une lettre au vieux mécanicien en lui disant « Nous voulons les détails de la facture... »</p>
<p>En gros il fallait qu'il justifie les 10 mille dollars...</p>
<p>Il leur a donc renvoyé une facture comme ceci :</p>
<p>- Enfoncer un clou = 2 dollars</p>
<p>- Savoir où il fallait le planter = 9998 dollars</p>
<p>:)</p>
<br>
<p>Ca veut dire taper sur un clou coutait 2 dollars, Mais l'expertise c'est de savoir à quel endroit il faut le faire...</p>
<br>
<p>Toi aussi qui m'écoute,</p>
<p>Trouve la porte ou il faut frapper pour que le moteur de ta vie tourne...</p>
<p>Les gens frappent à gauche à droite, et sont perdus...</p>
<p>Et certains frappent à des mauvaises portes... pendant toute leurs vies... Sans jamais trouver la bonne porte...</p>
<p>Peux tu profiter de l'expérience d'une personne plus âgée comme le vieux mécanicien ?</p>
<p>Et lui demander : Je dois frapper à quel endroit ?</p>
<p>Je dois aller dans quelle direction ?</p>
<p>Je continue dans quel sens ?</p>
<p>Je vous jure mes chers, et surtout aux jeunes qui m'écoutent,</p>
<p>J'ai peur que ta vie s'écoule sans que tu ai trouvé la bonne porte...</p>
<p>Ce n'est pas le fait de frapper qui compte, c'est savoir à quel endroit... C'est une information qui n'a pas de prix.</p>
<br>
<p>Place donc tes efforts au bon endroit,</p>
<p>Et dépense ton énergie dans la bonne activité,</p>
<p>C'est ce qui fera la différence dans ta vie,</p>
<br>
<p>Je tenais à donner un conseil en or à tous les jeunes, parce qu'il est basé sur la vie de plein de personnes...</p>
<p>Soit tu demandes à une personne plus âgée,</p>
<p>Soit tu demandes à ton Papa...</p>
<p>Ton papa mon cher a plein d'expérience,</p>
<p>Et tu n'oses pas lui demander,</p>
<p>Tu ne veux pas l'écouter,</p>
<p>Tu te disputes avec lui...</p>
<br>
<p>Ton père meurt d'envie de te transmettre ses connaissances gratuitement...</p>
<p>Demande conseil à ton papa...</p>
<p>Le seul qui ne va pas te tromper c'est lui !</p>
<p>Ne sois donc pas têtu,</p>
<p>Ne sois jamais gêné si tu ne sais pas,</p>
<p>Demande !</p>
<p>Mais surtout ne perds pas ton temps en frappant aux mauvaises portes...</p>
<p>En dépensant ton énergie aux mauvais endroits...</p>
<p>Alors que ton papa peut te dire gratuitement à quel endroit précis il faut frapper...</p>
<p>Je vous le répète donc :</p>
<p>- Enfoncer un clou = 2 dollars</p>
<p>- Savoir à quel endroit = ca n'a pas de prix !</p>
<p>:)</p>
<br>
<p>Que Dieu dessine un sourire d'espoir sur vos visages, et améliore vos vies.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
