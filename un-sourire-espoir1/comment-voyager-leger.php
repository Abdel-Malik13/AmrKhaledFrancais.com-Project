                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                        
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Le sourire d'espoir d'aujourd'hui est quelque chose que j'applique dans ma vie,</p>
<p>Je fais partie des gens qui voyagent beaucoup,</p>
<p>D'un aéroport à l'autre... d'un pays à l'autre...</p>
<p>Le voyage et sa difficulté...</p>
<br>
<p>Le Prophète (paix et bénédictions soient sur lui) disait</p> <p><strong class="prophete">« Le voyage est un morceau de châtiment... »</strong></p>
<p>C'est donc une épreuve, une privation de notre confort...</p>
<br>
<p>Voyager est encore plus difficile lorsqu'on doit emmener plusieurs valises,</p>
<p>Les déposer et à l'arrivée attendre pour les récupérer...</p>
<p>Puis tu ne trouves pas te taxi, alors tu marches en étant alourdi, et tu transpires...</p>
<br>
<p>Avec le temps j'ai appris une chose très importante :</p>
<p>Ne pas prendre trop de bagages...</p>
<p>Rester léger,</p>
<p>Voyager léger !</p>
<br>
<p>Je prends avec moi une seule valise à roulettes,</p>
<p>Et après avoir préparé ma valise, je la trouve lourde...alors je la vide,</p>
<p>Je retire un livre,</p>
<p>Puis une deuxième chemise...</p>
<p>Jusqu'à ce que la valise devienne légère,</p>
<p>Et je voyage l'esprit tranquille sans être encombré :)</p>
<p>Mais alors pourquoi je vous raconte tout ca ?</p>
<p>Je veux vous dire si ca est le voyage dans le bas monde, alors comment est le voyage vers Dieu ?</p>
<p>Comment est le voyage du jour du jugement ?</p>
<p>Comment est le voyage à la rencontre de Dieu ?</p>
<br>
<p>Les voyages qu'on fait dans ce bas monde, ne sont rien comparés au grand voyage vers l'au-delà...</p>
<p>Un très long voyage...</p>
<br>
<p>Ce que je veux te dire c'est :</p>
<p>Voyage vers Dieu en étant léger...</p>
<p>Allège tes fautes autant que tu peux,</p>
<p>C'est comme si tu transportais une valise remplie de cailloux et d'épines...</p>
<p>Ouvre ta valise et jette tout ce qui ne sert à rien...</p>
<p>Ce défaut : Entraine-toi pour t'en débarrasser...</p>
<p>Ce péché que tu fais depuis un certain temps : Arrête le et repents toi...</p>
<p>Et ces disputes avec ta femme ou ton mari : Contrôle toi et ne te laisse plus emporter facilement...</p>
<br>
<p>Voyage léger !</p>
<br>
<p>Un jour Le Prophète (bpsl) a dit à Abou Dhar (un compagnon) : <strong class="prophete">« Si tu désirais voyager, tu préparerais le nécessaire du voyage n'est ce pas ? »</strong></p> <p>il a répondu : « Oui, Ô messager d'Allah »</p> alors le Prophète (bpsl) lui a répondu : <strong class="prophete">« Et que fais-tu du voyage du jour du jugement dernier ? Ô Abou Dhar, assure-toi de ton embarcation, car la mer est profonde et prends suffisamment de provisions car le voyage est long »</strong> <p>il lui a alors demandé : « Et que faire donc pour cela, Ô messager d'Allah ? »</p> <p>et le Prophète (bpsl) a répondu : <strong class="prophete">« Jeûne un jour très chaud pour le jour de la dispersion (le jour où les hommes sortiront de leurs tombes), et reste debout à prier dans l'obscurité de la nuit pour l'obscurité des tombes, et fais un pèlerinage pour les affaires plus importantes »</strong>.</p>
<br>
<p>Il faut alléger son embarcation,</p>
<p>Arrêter de refaire ce péché,</p>
<p>Et demander pardon pour cette erreur,</p>
<p>Et se repentir à cet instant même en écoutant ces paroles,</p>
<p>Et s'adresser à Dieu en disant « Mon Dieu, je me suis débarrassé de ces péchés qui allaient m'alourdir le jour du jugement... »</p>
<br>
<p>Il y'à un verset du saint Coran qui dit :</p>
<p><strong>{...Et ils porteront leurs fardeaux sur leurs dos, et quels mauvais fardeaux! }</strong></p>
<p>Sourate 6 - Al-An'am : Les Bestiaux (verset 31)</p>
<br>
<p>Ils porteront leurs péchés sur leurs dos...</p>
<p>Quel mauvais fardeau, comme le décrit Dieu le très haut...</p>
<br>
<p>Allège donc ta valise... Et demande pardon à Dieu,</p>
<p>Et voyage léger vers la rencontre de Dieu,</p>
<p>Pour être heureux, et garder un sourire d'espoir dans ta vie ici bas et dans l'au-delà.</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
