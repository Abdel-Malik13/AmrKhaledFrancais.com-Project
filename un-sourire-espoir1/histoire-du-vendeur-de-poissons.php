                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un sourire d'espoir,</p>
<p>Le sourire d'aujourd'hui parle du bon comportement...</p>
<p>Mes chers j'ai envie de vous dire quelque chose,</p>
<p>On entend beaucoup de versets du coran et beaucoup de paroles du prophète sws ...</p>
<p>Et les musulmans et les chrétiens , et dans l'Evangile et dans la Toras et dans le Coran...</p>
<p>Beaucoup de paroles sur le bon comportement.</p>
<p>On regarde souvent le bon comportement comme une question de religion uniquement.</p>
<p>Les paroles du prophète sws sont très belles, il nous dit <strong class="prophete">«Le Jour de la Résurrection, rien ne pèse plus lourd (sur la Balance) que les bonnes œuvres et le bon comportement.»</strong></p>
<p>Et dans une autre parole <strong class="prophete">«Celui d'entre vous qui sera le plus cher et le plus proche de moi au Jour du Jugement sera celui qui possède la plus haute moralité»</strong></p>
<p>Nous voyons le sujet comme une question de religion et c'est tout...</p>
<p>Savez-vous que la réforme de la vie ne se fait qu'avec les bons comportements ?</p>
<p>Si nous voulons que le monde change, la chose la plus importante dont nous avons besoin est d'avoir un bon comportement.</p>
<p>Si tu veux savoir comment va un pays, demande comment va la moralité avant de demander quand seront les prochaines élections.</p>
<p>Mes chers, chaque projet a besoin d'une base solide, c'est quoi la base solide ? c'est des routes, des moyens de communication, des infrastructures...</p>
<p>Non par Dieu ce n'est pas vrai, la vraie base solide est les bons comportements.</p>
<p>Avant je vivais en Egypte dans un quartier qui s'appelait le quartier des ingénieurs... C'était un très beau quartier, mais malheureusement ce n'est plus le cas... Ce qui est arrivé c'est que des immeubles ont été construits illégalement, à cause de corruption, le résultat c'est que le quartier a été défiguré, et nos souvenirs avec, ensuite on a déménagé de ce quartier.</p>
<p>Je voulais vous donner un exemple concret, comment un mauvais comportement peut gâcher la vie.</p>
<p>J'ai un ami qui vit en Angleterre, cet ami a acheté une maison là-bas, une petite maison avec un jardin, et dans le jardin se trouvait un bassin ...</p>
<p>Quand il a acheté la maison, il y'avait des petits poissons dans le bassin, mais le bassin était couvert par un filet, alors il s'est demandé que fait ce filet là ?</p>
<p>Donc à l'égyptienne ;) il a pris ce filet et l'a jeté...</p>
<p>Le lendemain il n'a trouvé aucun poisson dans le bassin, parce que les oiseaux sont venus les manger.</p>
<p>Le filet servait donc à protéger les poissons des oiseaux.</p>
<p>Alors mon ami a décidé d'aller acheter d'autres poissons pour se rattraper...</p>
<p>Il est donc parti dans une boutique qui vendait des poissons, le vendeur lui a demandé s'il voulait des poissons d'intérieur à mettre dans un bocal, ou bien des poissons à mettre à l'extérieur dans un jardin par exemple.</p>
<p>Mon ami a bien sûr dit c'est pour le bassin, alors le vendeur anglais lui a dit « je suis désolé, mais je ne pourrai pas t'en vendre »</p>
<p>Mon ami lui a alors demandé la raison de ce refus, le vendeur lui a expliqué que ces poissons allaient mourir au bout d'un mois... Parce que l'hiver va arriver, et les poissons n'auront pas le temps de s'acclimater au froid en si peu de temps.</p>
<p>Par contre si tu veux en acheter, reviens moi l'année prochaine pendant l'été, pour que quand tu mettes les poissons dans le bassin, ils aient le temps de s'acclimater petit à petit, et quand l'hiver arrivera ils seront déjà prêts.</p>
<p>Alors mon ami n'a rien acheté ce jour là, mais en partant il s'est demandé, si j'étais en Egypte, le vendeur m'aurait vendu ou pas ?</p>
<p>Vous avez deviné la réponse ;)</p>
<p>S'il avait acheté en Egypte et que les poissons étaient morts au bout d'un mois, il serait revenu à la boutique et le vendeur lui aurait dit c'est parce que tu n'as pas pris ce médicament ou ce remède.</p>
<p>Mes chers, faites moi confiance, si on veut que notre pays réussisse économiquement, demandez comment va l'intégrité, comment va la sincérité, comment se comportent les gens entre eux..</p>
<p>Le bon comportement, le bon comportement, le bon comportement... C'est le sourire d'espoir pour l'économie, Si on satisfait Dieu, il améliorera la situation de notre pays.</p>
<br>
<p>Prenez soin de vous, et à bientôt.</p>
