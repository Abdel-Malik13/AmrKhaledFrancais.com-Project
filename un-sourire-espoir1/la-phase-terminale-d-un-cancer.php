                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>L'épisode d'aujourd'hui parle d'un ami personnel,</p>
<p>Un ami très très très cher...</p>
<p>Il s'appelait Ahmed Andil,</p>
<p>Ahmed Andil est une des personnes les plus gentilles que j'ai connues dans ma vie,</p>
<p>Gentil... Gentil... Gentil à un point inhabituel,</p>
<p>Tout le temps entrain de faire le bien,</p>
<p>Toujours droit...</p>
<p>Une très belle personne de l'intérieur,</p>
<p>Il parait que ce genre de personnes gentilles, quand leur test est fini dans ce bas monde, Dieu ne les laisse pas trop souffrir...</p>
<p>Ahmed a eu un cancer,</p>
<p>Ca l'a beaucoup amoindri physiquement...</p>
<p>4 mois avant qu'il décède (paix à son âme) Ahmed m'a appelé pour me dire qu'il voulait faire un pèlerinage à la Mecque...</p>
<p>Et entre vous et mois, je me suis demandé comment il pourrait faire...</p>
<p>Un pèlerinage demande beaucoup de dépenses d'énergie,</p>
<p>Même en étant en pleine santé, le pèlerinage reste une épreuve physique...</p>
<p>Et Ahmed avait du mal à bouger même chez lui...</p>
<p>Je ne savais pas quoi lui dire alors je lui ai dit « Si Dieu le veut... Si Dieu le veut... »</p>
<p>J'avais envie de lui dire que ce n'était pas une très bonne idée, mais je ne voulais pas le vexer...</p>
<p>Ahmed avait une grande détermination,</p>
<p>Il m'a appelé une semaine avant la date du pèlerinage en me disant « J'ai obtenu le visa, je pars seul faire le pèlerinage »</p>
<p>Quand j'ai vu sa volonté, je lui ai dit dans ce cas je t'accompagne...</p>
<p>Je vous jure mes chers le pèlerinage le plus facile de ma vie est celui que j'ai fais avec Ahmed Andil,</p>
<p>Tout était aisé du début à la fin...</p>
<p>Au moment où on devait tourner autour de la ka'aba (mosquée sacrée)</p>
<p>Il y'avait beaucoup de monde, des millions de personnes... Alors j'ai dis à Ahmed il vaut mieux attendre un ou deux jours lorsqu'il y'aura moins de monde...</p>
<p>On est rentrés dormir,</p>
<p>Et quand je me suis réveillé j'ai vu Ahmed rentrer...</p>
<p>Je lui ai demandé d'où il revenait,</p>
<p>Il m'a dit qu'il avait fini ses tours de la ka'aba,</p>
<p>J'ai demandé comment ? tu arrives à peine à bouger...</p>
<p>Il m'a dit je ne pouvais pas attendre... Je devais finir ce que j'avais à faire.</p>
<br>
<p>On est rentrés en Egypte,</p>
<p>Une semaine après Ahmed est tombé dans un coma profond durant 3 mois...</p>
<p>Ahmed est décédé, et la dernière chose qu'il a faite dans sa vie était son pèlerinage.</p>
<br>
<p>En revenant de la Mecque il n'a pas eu le temps de commettre une seule faute...</p>
<p>Ni le temps de dire un seul mauvais mot à qui que ce soit...</p>
<p>Après le pèlerinage nos péchés sont effacés comme une nouvelle naissance pour nous...</p>
<p>Paix à l'âme d'Ahmed Andil</p>
<br>
<p>Je vous ai raconté son histoire pour vous dire à quel point les personnes gentilles sont chères pour Dieu...</p>
<p>On avait un ami en commun moi et Ahmed,</p>
<p>Qui me disait qu'Ahmed s'accrochait de toutes ses forces à la vie,</p>
<p>Il partait voir tous les médecins, et il essayait tous les traitements...</p>
<p>Cet ami savait qu'Ahmed était en phase terminale alors il lui a dit ceci : « Tu as le droit de t'accrocher à la vie, mais sache que Dieu t'a donné une faveur qu'il ne nous a pas donné à nous autres...</p>
<p>Nous ignorons à quel moment nous allons mourir...</p>
<p>Tu es le seul à savoir à peu près...</p>
<p>Ahmed, Dieu t'a donné cette occasion et t'a donné une chance exceptionnelle »</p>
<p>Apparemment c'est après qu'Ahmed ai entendu ces paroles, qu'il a décidé d'aller faire le pèlerinage,</p>
<p>Pour saisir cette chance unique que Dieu lui a accordé...</p>
<br>
<p>Paix à son âme, il était une personne extrêmement gentille...</p>
<p>Et il a terminé sa vie par un pèlerinage,</p>
<p>Il est décédé comme il est né... Avec ses péchés effacés.</p>
<br>
<p>Je dédie ce sourire d'espoir à sa femme et ses enfants,</p>
<p>Et à lui dans sa tombe,</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
