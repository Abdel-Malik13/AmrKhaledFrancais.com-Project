                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui raconte une histoire vraie,</p>
<p>Un petit garçon pauvre n'avait rien à manger chez lui,</p>
<p>Il a décidé de se rendre dans un village voisin connu pour son hospitalité,</p>
<p>Il allait s'arrêter à la première maison et demander un peu de nourriture pour calmer sa faim...</p>
<p>Quand il a frappé à la porte, une petite fille lui a ouvert,</p>
<p>Il a eu honte de demander à manger,</p>
<p>Il a juste demandé un verre d'eau...</p>
<p>Mais cette petite fillette a senti qu'il n'avait pas seulement soif, mais qu'il avait surtout très faim,</p>
<p>Elle lui a ramené un grand bol rempli de lait...</p>
<p>Ce n'était pas un verre mais vraiment un grand bol,</p>
<p>Le garçon a bu le bol de lait,</p>
<p>Il regardait la petite fillette et n'arrivait pas à croire à quel point elle a été généreuse envers lui...</p>
<p>Quand il a fini de boire, il lui a dit « Je n'ai que 10 centimes dans ma poche, combien je dois donner ? »</p>
<p>La petite fille lui a répondu « Rien du tout ! Ma maman m'a appris à faire le bien sans rien demander en retour, tu as bu le bol de lait tu ne me dois rien en contrepartie. »</p>
<p>Le garçon lui a dit qu'il était reconnaissant et qu'il ne savait pas comment la remercier, puis il est parti...</p>
<p>L'histoire s'est terminée.</p>
<p>Mais le bien ne se perd jamais,</p>
<p>Il y'à Dieu <strong>« Gloire à lui, il s'est élevé (au-dessus de tout) »</strong> !</p>
<p>Des années se sont écoulées depuis ce jour là,</p>
<p>Et ce petit garçon pauvre, qui n'avait rien dans sa poche a fini par entrer à la faculté de médecine,</p>
<p>Il était brillant dans ses études, il a décroché son diplôme,</p>
<p>Puis il est devenu un médecin très reconnu.</p>
<p>La vie a suivi son cours,</p>
<p>Jusqu'au jour où une jeune femme atteinte d'une maladie grave a été admise à l'hôpital,</p>
<p>Elle n'avait pas l'argent pour se faire opérer,</p>
<p>Mais comme sa maladie était rare, on a appelé le médecin reconnu,</p>
<p>Une fois arrivé,</p>
<p>Il lui a demandé d'où elle venait,</p>
<p>Elle lui a dit le nom du village,</p>
<p>Puis il a demandé les détails de l'endroit,</p>
<p>Comme si Dieu le très haut voulait qu'il retrouve la mémoire,</p>
<p>Jusqu'à ce qu'il soit sur qu'il s'agit de la même personne qui lui a servi le bol de lait un jour où il n'avait pas de quoi manger...</p>
<p>Il a supervisé lui-même l'opération,</p>
<p>Et après une longue lutte, et plusieurs jours d'hospitalisation, la jeune femme a retrouvé sa santé...</p>
<p>La facture de l'hôpital se comptait évidemment en milliers de livres égyptiennes,</p>
<p>Le médecin l'a signée en personne en marquant « Facture payée entièrement avec un bol de lait »</p>
<p>Il l'a payée lui-même.</p>
<p>Le bien ne se perd jamais,</p>
<p>Mes chers faites le bien et apprenez à vos enfants que quand on fait le bien on finit toujours par être récompensés.</p>
<p>Jamais les bons actes ne se perdent, aussi petits soient ils,</p>
<p>Je jure par Dieu que si tu es jeune et que tu laisses ta place dans le bus à une personne âgée,</p>
<p>Je jure par Dieu, que la roue va tourner, que le temps va s'écouler et que quand tu deviendras âgé, Dieu mettra sur ton chemin un jeune qui t'aidera.</p>
<p>Dieu a mis cette règle dans l'univers.</p>
<p>Faisons le bien gratuitement pour que Dieu dessine sur nos visages un sourire d'espoir.</p>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
