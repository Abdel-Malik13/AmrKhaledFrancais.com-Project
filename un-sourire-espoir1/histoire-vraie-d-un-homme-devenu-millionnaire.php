                                        <br>
                                        <p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui parle d'une chose que j'ai vue à Djeddah en Arabie Saoudite,</p>
<p>Je visitais plusieurs amis là-bas après avoir fait une Omra (petit pèlerinage),</p>
<p>Un ami (homme d'affaires) m'a demandé d'aller donner un discours chez lui,</p>
<p>Dans une grande maison qui donne sur la mer à Djeddah...</p>
<p>J'ai accepté, et je suis allé prononcer mon discours,</p>
<p>J'ai d'abord été surpris par le grand nombre de personnes présentes mais aussi par la luxueuse demeure,</p>
<p>Puis j'ai remarqué quelque chose d'inhabituel...</p>
<p>Dans le premier rang étaient assises des personnes assez âgées,</p>
<p>Et ces personnes étaient toutes égyptiennes...</p>
<p>L'homme qui m'a invité est saoudien, et tous les autres invités sont saoudiens,</p>
<p>Et tous les autres invités avaient le même âge que le propriétaire...</p>
<p>Quelque chose que je n'arrivais pas à comprendre,</p>
<p>Qui sont les personnes âgées assises au 1er rang ?</p>
<p>L'homme qui m'a invité a la quarantaine, et tous les autres invités ont la quarantaine, ils sont tous saoudiens... Sauf le 1er rang,</p>
<p>Le 1er rang était composé d'égyptiens âgés,</p>
<p>J'ai prononcé mon discours,</p>
<p>Puis j'ai commencé à demander « Qui sont ces gens ? »</p>
<p>En réalité cet homme m'a invité spécialement pour faire plaisir aux personnes du 1er rang...</p>
<p>Mais qui sont-ils ?</p>
<p>Dans cette maison cet homme d'affaire a rassemblé tous ses camarades de classe du primaire et du secondaire,</p>
<p>Et dans le 1er rang étaient assis tous ses professeurs égyptiens...</p>
<p>(Les égyptiens ont laissé une grande trace dans le monde arabe... Que Dieu nous redonne le même poids qu'avant)</p>
<p>Certains de ses professeurs vivaient en Arabie Saoudite, d'autres en Egypte,</p>
<p>Et chaque année il les invite à faire une Omra (petit pèlerinage) suivie de ce rassemblement,</p>
<p>Pour que l'école se retrouve encore une fois...</p>
<p>Cet homme est formidable,</p>
<p>L'argent n'a pas une mauvaise influence sur tous les gens devenus riches...</p>
<p>Il y'a des gens qui une fois devenus riches, deviennent plus croyants et plus généreux...</p>
<p>Je lui ai demandé pourquoi il faisait tout ca,</p>
<p>Il m'a répondu par loyauté, ces professeurs nous ont beaucoup appris, c'est notre devoir de les honorer une fois qu'ils prennent de l'âge,</p>
<p>Et on a partagé plein de moments avec nos camarades de classe, on a mangé ensemble, on a joué ensemble à la récréation,</p>
<p>Pourquoi ne pas se rassembler au moins une fois par an, prendre de nos nouvelles, et écouter des paroles qui augmentent la foi ?</p>
<p>Se rapprocher de Dieu, et en même temps faire quelque chose dans laquelle il y'a de la tendresse et de la chaleur...</p>
<p>J'ai dit « Allah ! »</p>
<p>Vous voyez mes chers comment le bien existe encore ?</p>
<p>Le monde n'est pas comme certains veulent nous le faire croire, que tous les gens sont mauvais... Etc.</p>
<p>Pas du tout je vous le jure !</p>
<p>Nos pays sont pleins de bonnes choses,</p>
<p>Le Prophète (bénédictions et paix soient sur lui) a dit vrai en disant :</p>
<p><strong class="prophete">« Le bien se trouve en moi et en ma communauté jusqu'au Jour de la Résurrection »</strong></p>
<p>Qui a dit que le bien allait finir par disparaitre ?</p>
<p>Hatem cet homme d'affaires m'a fait ressentir une grande loyauté,</p>
<p>Je suis parti voir les professeurs et les saluer,</p>
<p>Ils m'ont dit qu'ils viennent au même endroit chaque année,</p>
<p>Qu'elle est belle la loyauté !</p>
<p>Qu'elle est belle la considération envers les professeurs !</p>
<p>Que c'est beau quand on prend soin de nos instituteurs, qu'on les célébre et qu'on leur fait sentir qu'ils sont chers à nos yeux !</p>
<p>Ce jour là j'ai senti que la foi dans mon cœur a augmenté,</p>
<p>Non pas grâce au discours que j'ai donné,</p>
<p>Mais grâce à ce que j'ai vu avec mes yeux...</p>
<p>Je l'ai beaucoup remercié et je lui ai dit qu'il m'a fait penser à la parole du Prophète (bpsl) dans laquelle il dit <strong class="prophete">« La loyauté dans la promesse est un signe de la foi »</strong></p>
<p>Vous savez ce que ca signifie ?</p>
<p>Les gens entre qui il y'a eu des promesses, qui ont été voisins, qui ont été camarades de classe, qui ont vécu des choses ensemble...</p>
<p>Quand ils préservent ce lien, c'est un signe de la foi.</p>
<p>C'est possible Ô Messager de Dieu ?</p>
<p>Je pensais que les signes de la foi sont la prière et le jeûne...</p>
<p>Non pas seulement !</p>
<p>Celui qui prend soin de ses amis du passé,</p>
<p>Celui qui n'oublie pas ses anciens professeurs,</p>
<p>Celui qui reste loyal,</p>
<p>Celui qui tient ses promesses,</p>
<p>Sont aussi des signes de la foi.</p>
<p>Le sourire d'espoir se dessine véritablement chez ceux qui restent loyaux et fidèles.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
