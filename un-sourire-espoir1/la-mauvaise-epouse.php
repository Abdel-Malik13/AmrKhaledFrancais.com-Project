                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui plaira beaucoup beaucoup beaucoup aux femmes...</p>
<p>Je serai du côté des femmes aujourd'hui :)</p>
<p>C'est une histoire imaginaire ou une histoire vraie je ne sais pas exactement,</p>
<p>L'essentiel est qu'un homme est rentré chez lui après sa journée de travail,</p>
<p>Il a trouvé ses 3 enfants devant la maison en train de jouer dans la boue,</p>
<p>Ils étaient dans leurs pyjamas, ils ne s'étaient pas changés depuis leur réveil,</p>
<p>Mal coiffés, et leurs vêtements étaient salis par la boue...</p>
<p>Cet homme n'avait jamais vu une chose pareille il devenait rouge de colère,</p>
<p>En entrant Il a trouvé des sacs d'ordures et des emballages vides par terre devant l'entrée...</p>
<p>Toute la maison était en désordre,</p>
<p>Le ménage n'a pas été fait,</p>
<p>Le son de la télévision était à fond,</p>
<p>Des jouets et des vêtements par terre,</p>
<p>Dans la cuisine des restes de nourriture et de la vaisselle sale,</p>
<p>La porte du réfrigérateur grande ouverte...</p>
<p>Il était choqué par ce qu'il voyait,</p>
<p>Il est monté à l'étage,</p>
<p>Il a trouvé le même désordre qu'en bas...</p>
<p>Du savon par terre, de l'eau dans la salle de bain,</p>
<p>« C'est pas possible, je me suis trompé de maison... Mais ce sont bien mes enfants qui sont en bas... »</p>
<p>Il a couru dans la chambre à coucher,</p>
<p>Il a trouvé sa femme bien habillée, elle feuilletait un magazine sur le lit,</p>
<p>Il s'est mis en colère...</p>
<p>Il lui a demandé ce qui se passait ?</p>
<p>Elle l'a regardé avec le même sourire qu'elle avait tous les soirs,</p>
<p>Et en tout calme elle lui a dit « Chaque soir, tu rentres du travail et tu me demandes étonné ce que je faisais de si important la journée ?</p>
<p>Justement aujourd'hui je n'ai pas fait ce que j'ai l'habitude de faire,</p>
<p>Et chaque jour tu n'es pas content, et tu sens que je n'ai rien fait de ma journée,</p>
<p>Je n'ai donc rien fait depuis le réveil pour te montrer l'importance de ce que je fais en ton absence...»</p>
<p>L'idée vous est arrivée ?</p>
<p>Soyez miséricordieux avec vos femmes,</p>
<p>Rien de ce que ton épouse fait ne te plait,</p>
<p>Quand tu rentres chez toi tu vois que chaque chose est à sa place,</p>
<p>Tout est propre,</p>
<p>Et tu ne te rends pas compte qu'une femme s'est fatiguée pour que la maison soit dans cet état,</p>
<p>Ensuite tu lui reproches « Pourquoi n'as-tu pas fait ceci, pourquoi n'as-tu pas fait attention à cela... »</p>
<p>Et tu oublies combien d'énergie elle a dépensé pour prendre soin des enfants et de la maison...</p>
<p>Sois indulgent avec ton épouse,</p>
<p>Pareil avec ta sœur ou ta maman,</p>
<p>Il y'a toujours une personne qui travaille dans l'ombre pour que la maison soit en bon état,</p>
<p>Pour que tu trouves l'apaisement quand tu rentres le soir,</p>
<p>Ce que tu ne sais pas c'est combien de temps et combien d'efforts ont été nécessaires pour y parvenir...</p>
<p>J'ai juste peur que les femmes qui écoutent cet épisode ne fassent rien demain,</p>
<p>Qu'elles laissent la maison sans ménage et que leurs maris demandent le divorce :)</p>
<p>Et qu'elles viennent me reprocher « Ce n'est pas un sourire d'espoir, mais une source de problèmes »</p>
<p>N'imitez surtout pas la femme de cette histoire ;)</p>
<p>Je vous ai juste défendu,</p>
<p>J'ai parlé aux hommes pour qu'ils soient doux avec vous et qu'ils se rendent compte de l'importance de ce que vous faites quand ils ne sont pas là.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
