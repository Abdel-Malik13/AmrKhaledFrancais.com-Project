                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                        
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle d'une chose qui m'est arrivée il y'à une semaine en Alexandrie,</p>
<p>Je m'étais arrêté devant un feu rouge,</p>
<p>Et là un groupe d'enfants sont venus nettoyer les pare-brise des voitures...</p>
<p>Dès que je les ai vu arriver, j'ai fait signe de la main pour dire non je ne veux pas...</p>
<p>Et j'ai fermé la vitre,</p>
<p>Quand j'ai regardé de l'autre coté,</p>
<p>Un enfant a attiré mon attention,</p>
<p>Il avait un très beau visage,</p>
<p>Un visage plein d'innocence,</p>
<p>Je lui ai demandé quel était son prénom,</p>
<p>Il m'a répondu qu'il s'appelait Rabii,</p>
<p>J'ai dit « Quel âge as-tu Rabii ? »</p>
<p>Il m'a répondu « 10ans »</p>
<p>Ca m'a fait mal au cœur...</p>
<p>J'ai demandé « Qui t'oblige à faire ca ? »</p>
<p>Il m'a dit « Ma mère m'envoie pour ramener de l'argent »</p>
<p>J'ai demandé « Où vivent tes parents ? »</p>
<p>Il m'a donné le nom du quartier,</p>
<p>Je lui ai dit « As-tu des frères et sœurs ? »</p>
<p>Il m'a répondu « J'en ai 10 et nous vivons dans 2 chambres »</p>
<p>J'ai demandé « Comment vous faites pour dormir ? »</p>
<p>Il m'a répondu « On dort comme on peut les uns sur les autres »</p>
<p>J'ai dit « Rabii as-tu déjà été arrêté par la police ? »</p>
<p>Il m'a répondu « Oui 2 fois, et je suis resté 6 heures dans le poste de police avant qu'ils me relâchent »</p>
<p>J'ai demandé « C'est quoi cette cicatrice que tu as sur le visage ? »</p>
<p>Il m'a répondu « Une fois un conducteur m'a donné beaucoup d'argent... Il m'a donné 10 livres (environ 1,29 €) quelqu'un a voulu me les prendre, j'ai résisté et il m'a donné un coup de couteau sur le visage »</p>
<p>J'ai demandé « Vas-tu à l'école ? »</p>
<p>Il m'a dit « Ma mère m'a sorti de l'école, pour que j'aille chercher de l'argent »</p>
<p>Aie mes chers... Ca m'a fait mal...</p>
<p>J'ai demandé « Quel est ton rêve dans la vie ? »</p>
<p>Il m'a répondu « J'ai envie d'apprendre à lire et à écrire »</p>
<p>J'ai demandé « Pourquoi veux-tu apprendre à lire et à écrire ?»</p>
<p>Il m'a dit « Pour que je ne reste pas comme je suis aujourd'hui »</p>
<p>J'ai dit « Imagine si je te donne une grosse somme d'argent... Qu'est ce qui me garantit que tu vas retourner à l'école... Que tu ne vas pas dépenser cet argent et rester dans la même situation ? »</p>
<p>Il m'a répondu « Ce qui vous le garantit est une chose très simple... Si vous repassez par ici vous ne me verrez plus entrain de nettoyer les pare-brise des voitures»</p>
<p>J'ai versé des larmes quand il m'a dit ca...</p>
<p>Mes chers, j'ai donné à Rabii l'argent, et je lui ai dit « Va voir ta maman, et donne lui cet argent à condition qu'elle te remette à l'école »</p>
<p>J'ignore si Rabii fera ce que je lui ai dit ou pas,</p>
<p>Mais je suis sur et certain qu'une personne qui n'aide jamais un pauvre,</p>
<p>Qui ne défend jamais un faible,</p>
<p>C'est impossible que Dieu l'aide dan sa vie !</p>
<p>Mes chers aidez les pauvres, et Dieu vous en remerciera, vous bénira et sera satisfait de vous.</p>
<p>En réalité ce n'est pas moi qui ai donné l'argent à Rabii,</p>
<p>Mais un jeune homme qui était assis dans la voiture,</p>
<p>Quand je parlais à Rabii, il m'a devancé, il a vite sorti de l'argent et le lui a donné...</p>
<p>Vous me croyez si je vous dis qu'une semaine après,</p>
<p>Ce jeune homme a gagné un voyage gratuit pour la Mecque ?</p>
<p>Comme si Dieu l'a récompensé pour son geste...</p>
<p>Aidez les pauvres mes chers,</p>
<p>Aidez les enfants en situation difficile,</p>
<p>Allez voir une famille pauvre en leur disant, on vous donnera de l'argent tous les mois, on fera un projet pour que vous ayez une source de revenu... à condition que vos enfants aillent à l'école.</p>
<p>Prenez en charge une famille nécessiteuse,</p>
<p>Prenez en charge un orphelin,</p>
<p>Mais faites quelque chose,</p>
<p>Ne vivons par pour nous, en laissant les pauvres sans aide...</p>
<p>Goutons et faisons gouter un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
