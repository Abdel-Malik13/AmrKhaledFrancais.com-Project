                                        <br>
                                        <p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec une nouvelle parole, un nouveau sourire d'espoir dans une nouvelle journée.</p>
<p>La parole d'aujourd'hui, je veux vous y expliquer une chose très importante dans la vie,</p>
<p>Avez-vous remarqué que la vie est très courte ?</p>
<p>Vous allez me dire, dès le début on commence avec du pessimisme ;)</p>
<p>Non sérieusement, la vie est vraiment courte, ce n'est pas pour vous briser le moral ;)</p>
<p>Je veux juste te dire, ne la gâche pas !</p>
<p>Bon développons un peu :</p>
<p>As-tu déjà remarqué que le tiers de ta vie tu le passes à dormir ?</p>
<p>Tu me diras « je dors le tiers de ma vie ? »</p>
<p>Je te répondrai bien sûr, dans la journée il y'à 24 heures, et tu dors en moyenne 8 heures ,</p>
<p>Il y'à aussi des gens qui dorment 10h ;) Mais pas grave, disons 8 heures de sommeil .</p>
<p>8 c'est bien le tiers de 24.</p>
<p>Ca veut dire le tiers de chaque jour tu dors, donc le tiers de ta vie tu dors...</p>
<p>Et donc passons au deuxième tiers :</p>
<p>Tu le consacres à tes besoins biologiques, Aller dans la salle de bain, te laver le visage, manger, boire, prendre ta douche, te raser... Et te mettre devant le miroir, et t'habiller, et saluer les gens....</p>
<p>Tu seras étonné, puis tu me diras alors il me reste combien ?</p>
<p>Il ne te reste que le tiers, ca veut dire quoi ? si tu vas vivre 70 ans, ce n'est pas exact .</p>
<p>Ta vie net sera 25 ans seulement.</p>
<p>Ah bon à ce point la vie est courte ? Oui à ce point !</p>
<p>Donc pourquoi je te raconte tout ca ? C'est pour te casser le moral ?</p>
<p>Non crois moi ;) c'est juste pour te dire ne la gâche pas !</p>
<p>Fais le bien, Par Dieu c'est seulement tes bonnes œuvres qui te restent à la fin...</p>
<p>La nourriture finit par se périmer, les vêtements finissent par s'user, et les maisons finissent par s'écrouler, et l'argent finit par être dépensé...</p>
<p>Alors qu'est ce qui te reste ?</p>
<p>Il te reste les <strong>« les bonnes œuvres qui persistent »</strong> comme le saint Coran les a appelées,</p>
<p>Le bien ! faites le bien mes chers,</p>
<p>J'aime beaucoup ce verset :</p>
<p><strong>{ Les biens et les enfants sont l'ornement de la vie de ce monde. Cependant, les bonnes œuvres qui persistent ont auprès de ton Seigneur une meilleure récompense et [suscitent] une belle espérance. }</strong></p>
<p>Sourate 18 La caverne -- Verset 46</p>
<p>C'est la première fois que le mot <strong>« espérance »</strong> est cité dans le coran !</p>
<p>Oui, <strong>« l'espérance »</strong> est venue avec les <strong>« bonnes œuvres qui persistent »</strong>...</p>
<p>Ca veut dire quoi donc ?</p>
<p>En principe les bonnes œuvres, font parties du passé, donc sont derrière...</p>
<p>Mais Dieu te dit justement ce sont elles qui t'attendent à l'avenir donc devant...</p>
<p>Les bonnes œuvres que tu as faites par le passé, Dieu ne les a pas appelées «les passées » , il les a appelées <strong>« les restantes »</strong>. Ce sont elles qui te restent pour ton avenir, et c'est pour cela donc qu'elles sont sources d'espérance !</p>
<p>Quelle espérance? L'espérance que Dieu te rende heureux grâce au bien que tu as fais avant.</p>
<p>L'espérance d'entrer au Paradis !</p>
<p>L'espérance dans le bonheur que te donnera Dieu ! comme tu as donné du bonheur aux autres...</p>
<p>Par Dieu mes chers, tentez l'expérience, elles ont un gout formidable <strong>« les bonnes œuvres qui persistent »</strong></p>
<p>Allah les a appelées <strong>« les restantes »</strong> et les a appelées <strong>« les bonnes œuvres»</strong></p>
<p>Ca ne veut pas seulement dire la prière et le jeune, <strong>« les bonnes œuvres »</strong> car tu as œuvré pour la réforme de la vie des autres... Allah ! comment il est beau ce sens coranique !</p>
<p>Celui qui te dit ton espérance est dans la réforme de la vie des autres, c'est ce qui fera ton bonheur dans l'avenir et c'est ce qui va te rester.</p>
<p>Faites le bien mes chers... Faites le bien parce que le meilleur plaisir dans ce bas monde c'est le plaisir de donner... Par Dieu c'est bien meilleur que le plaisir de prendre !</p>
<p>Quand on vit pour soi même, on nait petit, on vit petit et on meurs petit !</p>
<p>Mais quand on vit pour les autres, notre vie s'agrandit, avec la vie de chaque personne à qui on a donné le sourire !</p>
<p>Mes chers, ce bas monde est une période courte, et ton temps à toi, est un temps court dans une période courte...</p>
<p>Et le tiers qui te reste, est un tiers court dans un temps court, dans une période courte...</p>
<p>Fais le bien mon cher !</p>
<p>Fais le bien car c'est seulement ca qui va te rester !</p>
<p>Fais le bien pour tes enfants !</p>
<p>Fais le bien pour être heureux !</p>
<p>Fais le bien pour que Dieu soit miséricordieux envers toi !</p>
<p>Fais le bien pour que les gens t'aiment !</p>
<p>Fais du bien aujourd'hui pour retrouver du bien dans le futur !</p>
<p>Fais le bien pour que ta vie s'agrandit !</p>
<p>Rend les gens heureux, et Dieu te rendra heureux,</p>
<p>Tente l'expérience, et tu verras le sourire qui se dessinera sur ton visage si tu donnes du bonheur aux autres, et que tu vis pour les autres...</p>
<br>
<p>Un sourire d'espoir, et que la paix soit sur vous.</p>
