                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>J'ai reçu un Email d'une fille qui écoute cette émission,</p>
<p>En le lisant ca m'a beaucoup touché,</p>
<p>Cette fille aime un garçon,</p>
<p>Et ce garçon a fini ses études, il travaille donc il a les moyens de se marier...</p>
<p>Et cette fille est amoureuse de ce garçon,</p>
<p>Mais elle est pudique et polie,</p>
<p>Elle a envie qu'il la remarque et qu'il vienne demander sa main...</p>
<p>Et comme vous savez le sentiment d'amour ne nous appartient pas,</p>
<p>C'est Dieu qui l'a créé au fond de nous pour qu'il soit le secret de la continuité de l'humanité...</p>
<p>Pour que les gens se reproduisent, et fassent des enfants... Ca commence par le mot « amour »</p>
<p>Puis un mariage... Puis... Puis...</p>
<p>L'essentiel c'est que tout part de ce sentiment.</p>
<br>
<p>Et cette fille voulait placer son amour dans le bon chemin,</p>
<p>Elle disait «Je l'aime et je veux me marier avec lui, je sais qu'il en a les moyens... Mais je ne sais pas comment le lui dire »</p>
<p>En réalité j'étais très heureux de lire son message, j'ai senti que j'étais proche des jeunes,</p>
<p>Qu'ils avaient confiance en moi, et qu'ils pouvaient tout me demander.</p>
<p>Donc au lieu de lui répondre par Email,</p>
<p>J'ai préféré lui répondre dans cet épisode, pour que ma réponse soit entendue par toutes les filles qui se trouvent dans la même situation.</p>
<p>Que peut faire une fille dans ce cas ?</p>
<p>Elle ne peut pas aller lui parler en disant « Je t'aime et je rêve de me marier avec toi »</p>
<p>Car s'il refuse, ca sera un coup très dur,</p>
<p>Et la fierté d'une fille est une chose très très très importante !</p>
<p>Savez-vous pourquoi un homme doit apporter une dot lorsqu'il demande la main d'une femme ?</p>
<p>Ce n'est pas pour l'argent en soi,</p>
<p>C'est pour que l'homme se rende compte que cette femme est précieuse,</p>
<p>Cette femme est très chère...</p>
<p>Tu as fourni des efforts pour l'avoir,</p>
<p>Tu es parti parler à ses parents,</p>
<p>Tu as travaillé dur pour économiser...</p>
<br>
<p>Donc si cette fille part demander à ce garçon de l'épouser... Elle perdra sa valeur.</p>
<p>Malheureusement c'est notre mentalité, quand on nous offre une chose sans qu'on ai lutté pour l'obtenir, on ne se rend pas compte de sa valeur...</p>
<br>
<p>Donc la solution ce n'est pas qu'elle aille lui parler...</p>
<p>Elle peut faire quoi dans ce cas ?</p>
<p>Elle peut rester sans rien lui dire... Mais elle est amoureuse, et elle a le droit d'être heureuse, si elle se tait ca n'avancera pas les choses non plus...</p>
<p>Quelle est donc la solution ?</p>
<p>J'ai trouvé la solution chez notre mère Khadija quand elle voulait se marier avec le Prophète (paix et bénédictions soient sur lui)</p>
<p>Elle avait trouvé un intermédiaire,</p>
<p>Elle a demandé à une amie à elle d'aller parler au Prophète (pbsl) à sa place...</p>
<p>Quand cette amie a vu le Prophète (pbsl) elle lui a demandé : Le moment n'est il pas venu pour que vous vous mariez...</p>
<p>Le Prophète (pbsl) a dit : <strong class="prophete">Oui mais je ne sais pas qui épouser...</strong></p>
<p>Cette amie a dit : Pourquoi pas Khadija...</p>
<p>Le Prophète (pbsl) lui a répondu : <strong class="prophete">Elle accepterait ?</strong></p>
<p>Cette amie a dit : J'irai lui en parler... (comme si elle n'était au courant de rien)</p>
<p>L'intermédiaire doit posséder une sagesse comme cette femme, pour qu'il puisse bien s'en sortir, sans nuire à l'image de personne...</p>
<p>Et le Prophète (bpsl) s'est marié avec Khadija, le mariage le plus important de l'histoire...</p>
<br>
<p>Donc ce que je conseille à cette fille c'est de trouver un intermédiaire sage et intelligent,</p>
<p>Ainsi elle aura gardé son image intacte,</p>
<p>Et en même temps elle sera restée pudique,</p>
<p>Et en même temps elle n'aura pas caché son amour.</p>
<br>
<p>Le tout c'est de trouver la bonne personne pour faire l'intermédiaire avec les qualités nécessaires.</p>
<br>
<p>Que Dieu te rende heureuse, et dessine un sourire d'espoir sur ton visage.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
