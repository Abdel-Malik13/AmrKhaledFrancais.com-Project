                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir, qui éclairera notre vie si Dieu le veut.</p>

<p>Aujourd'hui je vais vous raconter une histoire,</p>
<p>Une histoire qui m'a beaucoup touché personnellement,</p>
<p>Ce que je vais vous raconter m'est arrivé il y'à à peine 3 ou 4 jours.</p>
<p>Cette histoire a elle seule pourrait définir les mots humanité , vivre ensemble, accepter l'autre, la cohabitation, la miséricorde sociétale...</p>
<p>C'est une histoire vraie qui s'est produite dans une de nos maisons,</p>
<p>Je donnais un conférence devant des jeunes hommes et jeunes femmes,</p>
<p>Et dans cette conférence je parlais d'humanité,</p>
<p>Et quand la conférence s'est terminée, une jeune fille de 25ans est venue me voir</p>
<p>Elle m'a dit « Je m'appel Leila, et je veux vous raconter mon histoire, le sommet de l'humanité dans ma maison : mon père est musulman, et ma mère est chrétienne, et j'ai été élevée par mes grands parents qui eux sont chrétiens croyants et pratiquants, mon père musulman a divorcé avec ma mère quand j'étais petite, il nous a abandonné et s'est éloigné, du coup j'ai grandi dans la maison de mes grands parents chrétiens...</p>
<p>Et quand j'ai eu 7ans et que je commençais à comprendre les choses, mes grands parents m'ont dit :</p>
<p>Leila ton père est musulman, et nous sommes chrétiens et fiers de notre religion, mais nous n'allons pas t'imposer notre religion ... (cette histoire s'est passée en Egypte en Alexandrie)</p>
<p>Leila, on va t'apprendre le christianisme, et tu suivras des cours à l'école pour apprendre l'islam. »</p>
<p>Leila apprenait donc les deux religions en même temps,</p>
<p>Leila allait à la fois à l'église et à la mosquée,</p>
<p>Ses grands parents lui ont dit « Quand tu seras grande, tu pourras choisir seule et libre »</p>
<p>Leila me racontait « Quand j'ai eu 18ans, j'ai décidé de choisir l'islam, et je suis allée voir mes grands parents pour le leur annoncer... Et j'ai été surprise de les entendre me dire : Leila nous allons te soutenir quelque soit ton choix... Et jusqu'aujourd'hui mon grand père chrétien me réveille pour faire la prière du matin (Fajr)</p>
<p>Et pendant le ramadan, mes grands parents attendent le coucher du soleil avant de manger, pour qu'on sente qu'on forme une seule famille, et un seul foyer... »</p>
<p>Quand Leila m'a raconté tout ca, ca m'a beaucoup ému, je lui ai dit que tout le discours que j'ai prononcé sur l'humanité, et la paix entre musulmans et chrétiens ne vaut rien à coté de ton histoire...</p>
<p>Et dans le même jour j'avais une autre conférence en Alexandrie, j'ai demandé à Leila si elle m'autorisait à raconter son histoire ?</p>
<p>Elle a bien sur accepté, puis m'a demandé si elle pouvait inviter sa grand-mère, « parce qu'elle vous admire, et que si vous parlez d'elle ca la rendra heureuse».</p>
<p>J'ai dis qu'elle serait la bienvenue.</p>
<p>Et donc j'ai prononcé mon discours, et au milieu j'ai raconté l'histoire de Leila, mais sans citer son prénom pour ne pas la gêner,</p>
<p>J'ai juste dis, cette jeune femme est présente ici dans la salle avec sa grand-mère, j'allais conclure mon discours.</p>
<p>Puis j'ai été surpris par de grands applaudissements au milieu de la salle,</p>
<p>Leila est voilée, elle est montée sur scène avec sa mère et sa grand-mère, et toute la salle était émue.</p>
<p>La salle a été émue par l'humanité, et moi j'ai été touché au plus profond de moi-même, j'ai salué chaleureusement sa grand-mère,</p>
<p>Mais le plus beau encore, c'est que j'ai vu un homme portant la barbe arriver du fond de la salle,</p>
<p>Je me demandais pour quelle raison il s'approchait, il est venu embrasser la main de la grand-mère de Leila.</p>
<p>Que c'est beau l'humanité !</p>
<p>Que c'est beau le vivre ensemble !</p>
<p>L'histoire de Leila est très belle, et je suis heureux et je suis fier de la raconter !</p>
<p>Un sourire d'espoir pour nous tous.</p>
<br>
<p>et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
