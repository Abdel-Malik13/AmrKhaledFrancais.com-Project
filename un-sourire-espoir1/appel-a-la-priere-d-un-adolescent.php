                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle de quelque chose que j'ai vécu quand j'étais adolescent,</p>
<p>Je tenais à partager avec vous cette idée :</p>
<p>Le bien ne se perd jamais...</p>
<p>Les efforts finissent toujours par payer...</p>
<p>Celui qui fait une belle chose avec une intention sincère, tôt ou tard il sera récompensé pour... Même si au début il pense que son projet a échoué.</p>
<p>Quand j'avais 16 ou 17ans, je commençais à prier, j'étais motivé pour aller à la mosquée, j'avais plein d'énergie à dépenser... Donc j'ai voulu faire quelque chose de grand pour l'islam,</p>
<p>Que faire ? Je ne savais pas, mais j'avais une envie forte en moi,</p>
<p>J'étais encore jeune, et pas assez mûr, j'ai décidé de faire l'appel à la prière, pour que les gens entendent ma voix dans le microphone en train de dire « Allah Akbar » (Dieu est [le] plus grand).</p>
<p>Mais les grandes mosquées ne me laisseront jamais faire l'appel à la prière,</p>
<p>J'ai fini par trouver une petite mosquée dans une petite rue, en bas d'un immeuble...</p>
<p>Cette mosquée était souvent fermée car personne ne venait y prier,</p>
<p>J'ai décidé d'ouvrir la mosquée chaque jour et de faire les appels à la prière...</p>
<p>Ma voix n'était pas belle :)</p>
<p>Mais à cet âge j'étais habité par l'idée de faire l'appel à la prière.</p>
<p>Je l'ai fait un jour, puis deux... Puis un mois, puis deux...</p>
<p>Je faisais l'appel pour les 5 prières,</p>
<p>Je partais, je revenais,</p>
<p>Je ne ratais aucune prière,</p>
<p>Et pendant le ramadan Je ne mangeais qu'une datte et un peu d'eau pour faire l'appel à l'heure de rompre le jeune...</p>
<p>Mais personne ne venait à la mosquée...</p>
<p>Vraiment personne :)</p>
<p>J'étais complexé par cette situation,</p>
<p>Je suis resté 6 mois à insister,</p>
<p>Et à la fin j'ai fini par comprendre pourquoi personne ne venait à la mosquée...</p>
<p>Ma voix dérangeait le propriétaire de l'immeuble alors il a débranché le micro, et personne ne m'entendait dans la rue.</p>
<p>Pendant 6 mois j'ai fait l'appel à la prière sans que personne ne l'entende...</p>
<p>Vous imaginez la déception ?</p>
<p>6 mois, 5 appels par jour, des allers et des retours... Et le propriétaire de l'immeuble a coupé le son du micro sans me prévenir...</p>
<p>Le jour où je l'ai su j'ai pleuré comme jamais !</p>
<p>J'avais l'impression d'avoir perdu six mois de ma vie,</p>
<p>Mais aujourd'hui je sais que je ne les ai pas perdus...</p>
<p>Peut-être que c'est grâce à ces 6 mois que je suis en train de vous parler en ce moment,</p>
<p>Je suis en train d'enregistrer dans un studio, et vous êtes des milliers ou plus qui m'écoutent chaque jour...</p>
<p>Il y'a 20ans je pleurais parce qu'on a empêché ma voix d'arriver aux gens du quartier...</p>
<p>La même voix arrive aujourd'hui à un nombre incalculable de personnes.</p>
<br>
<p>Vous avez compris l'idée ?</p>
<p>Les bonnes œuvres ne se perdent jamais,</p>
<p>Celui qui fait une chose avec une bonne intention, même s'il échoue au départ,</p>
<p>Même s'il pense qu'elle n'a servi à rien...</p>
<p>Non je jure par Dieu que tu en récolteras les fruits,</p>
<p>Et que cette chose dessinera sur ton visage un sourire d'espoir même au bout d'un certain temps.</p>
<p>Si vous dépensez de l'énergie et que vous ne réussissez pas sur le coup, ne soyez pas tristes, Dieu vous récompensera pour votre effort.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
