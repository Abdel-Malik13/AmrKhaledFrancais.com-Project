                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle d'oncle Mutwali :)</p>
<p>Qui est donc cet oncle Mutwali ?</p>
<p>C'est le concierge qui s'occupe de tout dans l'immeuble dans lequel j'habite.</p>
<p>Il doit avoir 55ans...</p>
<p>Oncle Mutwali n'a jamais appris à lire ni à écrire,</p>
<p>Il venait souvent me voir en disant « Dr. Amr quelqu'un t'a laissé ce numéro... »</p>
<p>Et quand je regardais le papier je ne voyais pas des chiffres mais des lettres :)</p>
<p>Je lui disais oncle Mutwali ce n'est pas un numéro mais une adresse ;</p>
<p>Il me répondait « ils sont tous pareils pour moi, je ne sais distinguer ni les chiffres ni les lettres... »</p>
<p>Hier en rentrant chez moi, j'ai trouvé oncle Mutwali devant la porte,</p>
<p>Je l'ai salué en demandant comment il allait, il m'a rendu la politesse... Puis je suis parti.</p>
<p>En mettant la clé dans la porte, j'ai fait une pause... Il se passait quelque chose d'anormal,</p>
<p>Oncle Mutwali tenait dans sa main un Coran et était en train de le lire,</p>
<p>Je me suis dit Ah ouais ?</p>
<p>J'ai retiré la clé et je suis revenu le voir,</p>
<p>« Oncle Mutwali qu'es tu en train de faire ? »</p>
<p>Il m'a répondu « Je suis en train de lire le noble Coran »</p>
<p>J'ai demandé « Tu sais lire et écrire ? »</p>
<p>Il m'a répondu « J'ai vu la campagne que vous avez lancé (Le savoir est une force) pour lutter contre l'analphabétisme »</p>
<p>(D'ailleurs je n'en avais jamais parlé à oncle Mutwali)</p>
<p>Il a poursuivi « Quand j'ai vu la campagne, J'ai juré qu'avant le prochain ramadan j'aurai appris à lire le noble Coran... Alors je suis parti les voir à l'association et je leur ai demandé de m'apprendre... Et depuis 3 mois je suis les cours sans le dire à personne, et maintenant j'arrive à détecter les lettres, et je lis le Coran petit à petit... »</p>
<p>J'ai demandé à oncle Mutwali s'il pouvait lire un peu devant moi,</p>
<p>Je n'arrivais pas à le croire, Il a lu du Coran devant moi !</p>
<p>Je lui ai dit « Tu as appris par cœur ce passage et tu es en train de le réciter... :) »</p>
<p>Il m'a répondu « Je jure que non, je lis directement du livre»</p>
<p>Notre projet a réussi, les gens ont appris à lire et à écrire,</p>
<p>C'est possible et ça se concrétise !</p>
<p>Hier un des plus grands patrons en Egypte m'a appelé,</p>
<p>Il était en train de pleurer...</p>
<p>J'ai demandé ce qui lui arrivait ?</p>
<p>Je me suis dit les jeunes de l'association ont fait une chose qu'il ne fallait pas... Ils se sont disputés avec ses employés pendant qu'ils leur apprenaient à lire et à écrire...</p>
<p>Il m'a dit « Je tenais à te féliciter, c'est quoi ces jeunes formidables que tu nous as envoyé,</p>
<p>Les gens ici ont appris à lire et à écrire,</p>
<p>J'ai vu de mes propres yeux des gens passer des examens,</p>
<p>Des gens lire,</p>
<p>Les employés ont appris à écrire leurs prénoms,</p>
<p>Je pleure de joie ! »</p>
<p>Je vous raconte tout ca pour que vous ne pensiez jamais qu'une chose est impossible à réaliser,</p>
<p>Avec des efforts on finit toujours pas y arriver,</p>
<p>Quelque soient les problèmes dans nos pays, on trouvera toujours des solutions !</p>
<p>Faisons juste l'effort et Dieu nous facilitera.</p>
<p>Et on dessinera un grand sourire d'espoir pour fêter notre réussite :)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
