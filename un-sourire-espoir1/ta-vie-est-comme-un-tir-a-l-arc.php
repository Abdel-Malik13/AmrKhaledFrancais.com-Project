                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est quelque chose que j'ai remarquée avec mon fils Ali,</p>
<p>Et que j'ai envie de vous raconter :</p>
<p>Parmi les loisirs de mon fils Ali, il y'a le tir à l'arc,</p>
<p>Il s'entraine, et commence à s'améliorer au point de participer à des championnats,</p>
<p>Et je pars le voir quand il participe à une compétition,</p>
<p>J'y suis donc allé pour encourager mon fils, mais rien de plus.</p>
<p>Il lançait une flèche, puis une autre, puis une autre...</p>
<p>Puis ils comptaient les points pour faire le classement,</p>
<p>Et quand il lui restait une dernière flèche, son entraineur lui a dit « Concentre-toi Ali... Concentre-toi... C'est ta dernière flèche Ali... C'est la seule qui te reste donc concentre-toi bien »</p>
<p>Ali a pris la dernière flèche, il a bien regardé la cible, sa concentration était très supérieure aux précédentes tentatives...</p>
<p>C'est là que j'ai eu une idée :</p>
<p>Je me suis dit que ma vie et la votre sont comme cette dernière flèche...</p>
<p>Mes chers, notre vie est une seule flèche,</p>
<p>Imagine... On va la lancer une seule fois...</p>
<p>Fais attention... Fais bien attention à ce que ta vie ne parte pas dans une mauvaise direction...</p>
<p>Tu ne vas pas vivre deux fois, pour prendre un risque avec la première et te rattraper avec la 2ème,</p>
<p>Tu ne vas vivre qu'une seule fois,</p>
<p>Alors ne risque jamais ta seule chance, ne fais pas un mauvais pari avec,</p>
<p>Pendant que mon fils Ali tenait la flèche dans sa main,</p>
<p>Qu'il était extrêmement concentré,</p>
<p>Je me suis dit « Moi aussi je suis dans la même situation que lui, ma vie est une seule flèche,</p>
<p>Je vais désormais bien me concentrer pour savoir dans quelle direction j'avancerai,</p>
<p>Je ne pourrais pas revenir en arrière,</p>
<p>Une fois que la flèche est lancée elle ne revient plus... »</p>
<p>Vous comprenez mes chers l'idée que je veux partager avec vous ?</p>
<p>Que tu sois garçon ou fille, homme ou femme,</p>
<p>Es-tu satisfait du tir que tu as fait?</p>
<p>Es-tu satisfait de la direction que prend ta vie ?</p>
<p>Imagine tenir un arc dans la main, concentre-toi et lance la flèche,</p>
<p>Où se dirige cette flèche ?</p>
<p>Dans la bonne ou dans la mauvaise direction ?</p>
<p>En vérité même quand mon fils a fini la compétition, cette idée ne quittait pas ma tête,</p>
<p>J'ai demandé à Ali « Que ressentais-tu quand tu lançais ta dernière flèche ? »</p>
<p>Il m'a répondu « Papa, j'avais très très très peur de rater la cible, j'ai mis toute mon énergie, tous mes sentiments, toutes mes capacités et toute ma concentration dans ce dernier tir »</p>
<p>Je lui ai dit « Moi aussi Ali, je tiens beaucoup à ce que la flèche parte dans la bonne direction »</p>
<p>Ali m'a demandé « Quelle flèche papa? »</p>
<p>Je lui ai dit « Non, je parlais à moi-même ;) »</p>
<p>Je vous raconte cette histoire pour que chacun d'entre vous,</p>
<p>Que tu sois au volant de ta voiture, ou sur internet,</p>
<p>Pose-toi la question :</p>
<p>Est-ce que la flèche de ma vie est lancée dans la bonne direction ?</p>
<p>Ta seule chance... Ne prend jamais de risque avec ta seule chance !</p>
<p>A ce moment tu dessineras un sourire d'espoir dans ta vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
