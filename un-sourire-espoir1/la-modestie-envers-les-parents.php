                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui raconte une histoire que j'ai vue de mes propres yeux en Angleterre,</p>
<p>Mais tout d'abord j'ai envie de te demander :</p>
<p>A quoi ressemble ton comportement envers tes parents ?</p>
<p>L'histoire a justement un rapport avec ca,</p>
<p>Est-ce que tu les satisfais ?</p>
<p>Je sais que tu vas me dire « Je les respecte beaucoup »</p>
<p>Mais je ne parle pas de ca...</p>
<p>Les satisfaire, les rendre heureux,</p>
<p>Leur donnes-tu de la tendresse ?</p>
<p>Il existe des personnes qui disent « Je ne manque pas de respect à mes parents, je ne les dévisage pas du regard, je ne claque jamais la porte...»</p>
<p>Oui mais ce n'est pas assez... Ce n'est pas assez...</p>
<p>Est-ce que tu leur montres ton amour ?</p>
<p>Malheureusement il y'a des gens qui sont têtus,</p>
<p>Et il y'a d'autres gens qui sont orgueilleux...</p>
<p>Orgueilleux car par exemple il trouve dur de se baisser, d'embrasser la tête de sa mère, ou lui donner un câlin...</p>
<p>Orgueilleux car quand sa mère lui donne un conseil, il s'entête et fait exprès le contraire en serrant les dents...</p>
<p>L'entêtement vient du diable d'ailleurs,</p>
<p>« Je t'en supplie mon fils, sois prudent sur la route et ne conduis pas vite... »</p>
<p>Cette phrase l'énerve et il conduit vite juste pour tenir tête...</p>
<p>« J'aimerai qu'on se parle au téléphone pour me rassurer, je m'inquiète quand tu rentres tard... »</p>
<p>Alors il refuse de décrocher, ou il éteint son téléphone exprès quand il sort la nuit...</p>
<p>Peux-tu réfléchir avec moi en toute simplicité pourquoi on fait cela ?</p>
<p>Tu pourrais me dire « C'est une longue histoire, ca fait des années qu'on ne s'entend plus... »</p>
<p>Mon cher elle reste ta maman... Satisfais-la pour que Dieu soit satisfait de toi...</p>
<p>L'histoire que je tenais à vous raconter s'est passée devant mes yeux en Angleterre :</p>
<p>Il s'appelle Dr. Aziz, c'est un professeur égyptien qui enseigne dans les universités les plus prestigieuses au Royaume-Uni,</p>
<p>Je marchais avec lui dans la rue d'Oxford à Londres,</p>
<p>Cette rue est très fréquentée par les arabes,</p>
<p>Et vous savez quand on n'est pas dans notre pays, on se sent assez libres de faire ce qu'on veut,</p>
<p>Par contre quand on passe devant des gens de notre communauté, on fait très attention à nos mouvements...</p>
<p>Ce jour là je marchais avec Dr. Aziz et son papa,</p>
<p>Dr. Aziz doit avoir la quarantaine et comme j'ai dit c'est quelqu'un de très respecté,</p>
<p>Pendant qu'on marchait Dr. Aziz s'est arrêté en disant « Une seconde papa »</p>
<p>Je ne comprenais pas pourquoi il s'était arrêté...</p>
<p>Je vous jure mes chers, au milieu de la rue,</p>
<p>Il avait vu que les lacets des chaussures de son père s'étaient défaits,</p>
<p>Il s'est baissé, puis il les a refaits en toute humilité...</p>
<p>Allah !</p>
<p>C'est très beau,</p>
<p>Et encore plus beau quand ca vient de quelqu'un qui a réussi dans la vie,</p>
<p>Et encore plus beau quand ca se produit à Londres, une ville où on pense que la vie est très matérialiste...</p>
<p>Dr. Aziz a refait les lacets de son père, qui l'a caressé en disant « Que Dieu soit satisfait de toi mon fils »</p>
<p>Félicitations Dr. Aziz, rien que cette phrase te suffit !</p>
<p>Grâce à ce simple geste Dieu sera satisfait de toi.</p>
<p><strong>{...«Sois reconnaissant envers Moi ainsi qu'envers tes parents...}</strong></p>
<p>Sourate 31 - Luqmân (verset 14)</p>
<br>
<p>Tant que ton papa et ta maman sont satisfaits de toi, alors Dieu l'est aussi.</p>
<p>C'est sur et certain que Dieu prendra soin du Dr. Aziz toute sa vie.</p>
<p>Une scène que je n'oublierai jamais.</p>
<p>A tous les fils et à toutes les filles, pouvez-vous avoir la même modestie envers vos parents ?</p>
<p>Je vous jure que j'ai revu la même scène venant d'une fille cette fois,</p>
<p>Pareil une fille qui a beaucoup réussi dans sa vie professionnelle,</p>
<p>(On tournait une émission à Prague en République Tchèque)</p>
<p>On est partis manger dans un restaurant très chic,</p>
<p>Elle a dit au milieu des gens « Une seconde papa »</p>
<p>Elle s'est baissée pour refaire ses lacets...</p>
<p>Allah !</p>
<p>Mon cher sois modeste, et baisse toi pour tes parents,</p>
<p>Ne sois pas têtu ni orgueilleux,</p>
<p>Et dessine un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
