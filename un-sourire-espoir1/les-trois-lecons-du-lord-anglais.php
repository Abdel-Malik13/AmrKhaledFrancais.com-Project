                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>Je vous vais vous raconter dans l'épisode d'aujourd'hui une expérience personnelle :</p>
<p>Quand j'étais en Angleterre, j'ai reçu une invitation de l'union des Lords (aristocrates),</p>
<p>Elle réunit les plus grands penseurs et les personnalités anglaises les plus importantes...</p>
<p>Le lord a entendu parler de moi, donc il m'a invité à assister à une des réunions,</p>
<p>Je suis arrivé devant un grand bâtiment luxueux...</p>
<p>A l'intérieur, j'ai commencé à retirer mon manteau...</p>
<p>Et en m'observant le Lord a senti que je n'étais pas à l'aise,</p>
<p>Ou que j'étais impressionné par l'endroit,</p>
<p>Ou que je calculais chacun de mes gestes pour ne pas faire d'erreurs...</p>
<br>
<p>Alors cet homme m'a donné 3 leçons que je n'oublierai pas toute ma vie :</p>
<p>Je vais vous raconter chacune des 3 situations car elles sont gravées dans ma mémoire...</p>
<br>
<p>La première scène c'est quand il a vu que j'étais stressé,</p>
<p>On est arrivés devant des escaliers avec un tapis rouge luxueux...</p>
<p>C'était un homme assez âgé,</p>
<p>Et d'un coup il est monté en courant... Tac...Tac...Tac...</p>
<p>En me disant « On fait la course pour voir qui arrivera le premier en haut »</p>
<p>J'étais étonné par ce qu'il faisait... Il voulait jouer à son âge :)</p>
<p>Alors je courais à moitié pour ne pas le dépasser...</p>
<p>Une fois arrivés en haut il reprenait son souffle en me disant « J'ai vu que vous n'étiez pas à l'aise dans cet endroit... Alors j'ai voulu casser la barrière... Faites comme chez vous et ne faites pas attention à l'endroit ! »</p>
<p>C'était la première leçon... J'ai reçu comme une claque :)</p>
<p>A partir de là je me sentais à l'aise, je ne faisais plus attention à l'endroit.</p>
<br>
<p>La 2ème scène pendant qu'on prenait le thé,</p>
<p>Il y'avait des biscuits emballés posés sur la table,</p>
<p>Le Lord m'a demandé « Vous allez manger ce biscuit ? »</p>
<p>J'ai dit « non »</p>
<p>Il l'a pris, puis il l'a glissé dans sa poche...</p>
<p>Je me suis demandé dans ma tête c'est quoi ce geste... Comment un Lord peut faire ca...</p>
<p>Il a demandé à un autre Lord s'il allait manger son biscuit puis il a fait la même chose, il l'a mis dans sa poche,</p>
<p>Je ne comprenais toujours pas ce qu'il faisait...</p>
<p>En sortant, on est passés à coté de la femme qui nettoyait les vitres, le Lord lui a dit « Good morning » puis il lui a donné un paquet de biscuits,</p>
<p>Puis on est passés à côté de la personne responsable de la sécurité, il lui a dit « Good morning »</p>
<p>Et lui a donné un paquet de biscuits,</p>
<p>Puis pareil avec une personne qui travaillait dans le jardin...</p>
<p>Et là il m'a regardé en disant « Tous les Lords sont amis entre eux... Mais celui qui devient ami avec ces gens modestes, gagne leurs cœurs, et devient le roi parmi tous les autres Lords »</p>
<p>C'était la 2ème claque :)</p>
<p>La troisième en se quittant on s'est échangé nos cartes de visite,</p>
<p>Quand il a pris ma carte, il a passé sa main dessus et elle est devenue blanche...</p>
<p>Un tour de magie,</p>
<p>J'ai demandé « C'est quoi ca elle est passée où l'écriture ? »</p>
<p>Il a repassé sa main sur la carte, et l'écriture est réapparue.</p>
<p>Il m'a dit « Faites bien attention, quand vous ne faites qu'une seule chose les gens pensent que vous ne savez rien faire d'autre... Mais si vous leur montrez que vous savez faire une deuxième chose, là ils pensent que vous savez tout faire... Alors qu'en réalité vous ne savez faire que ces 2 choses et rien d'autre... C'est leur imagination qui fait le reste »</p>
<p>Là je lui ai demandé de m'apprendre son tour de magie,</p>
<p>Il m'a répondu « Non ! C'est à vous de trouver une deuxième chose et que appreniez à la maitriser »</p>
<p>J'ai dit « Je sais jouer au football »</p>
<p>Il m'a répondu « Si les gens vous voient bien jouer au football, en plus de vos émissions, ils vont penser que vous êtes doué dans tous les domaines »</p>
<p>C'était la 3ème leçon que m'a donné le Lord.</p>
<br>
<p>Ayons d'autres activités en plus de notre métier,</p>
<p>Occupons nous des pauvres et des gens modestes,</p>
<p>Et cassons les barrières avec les autres avec un peu d'humour,</p>
<p>3 leçons en or !</p>
<br>
<p>Et un sourire d'espoir à chaque fois qu'on apprend une nouvelle chose.</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
