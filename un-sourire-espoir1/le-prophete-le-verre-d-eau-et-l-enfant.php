                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>Dans cet épisode on parle encore des relations entre parents et enfants,</p>
<p>Mes chers il y'a un très beau principe appelé « le respect et l'estime »</p>
<p>Je sais que certaine me diront « tu parles d'enfants... C'est aux plus jeunes de respecter les adultes »</p>
<p>Dans cet épisode Je parle justement du respect des enfants de 5ans, 6 ans, 7ans et 10ans...</p>
<p>Car c'est ce respect et cette estime qui construit leur personnalité,</p>
<p>Il y'a un très beau proverbe syrien qui dit « Agrandis le il grandira... Diminue le il diminuera... »</p>
<p>Chaque fois que tu traiteras ton enfant comme une grande personne il avancera,</p>
<p>Et chaque fois que tu traiteras ton enfant comme un gamin il reculera,</p>
<br>
<p>Ce principe de respect et d'estime des enfants est très important,</p>
<p>Il crée des hommes solides,</p>
<p>Il crée des femmes respectables,</p>
<p>Il crée des humains généreux et fiers, car ils ont été respectés quand ils étaient plus jeunes,</p>
<br>
<p>Ne frappe jamais ton enfant devant ses copains...</p>
<p>Ne te moque jamais de ton enfant devant ses frères et sœurs...</p>
<br>
<p>Et s'il y'a un professeur qui m'écoute, n'humilie jamais un élève devant ses camarades de classe...</p>
<p>Ces choses là cassent un être humain,</p>
<p>Le respect mes chers... l'estime...</p>
<p>Certains me diront que je suis influencé par les valeurs de l'occident,</p>
<p>Ce sont des choses qu'on lit dans les livres de psychologues occidentaux...</p>
<p>Non mon cher, c'est un principe qui vient de l'islam,</p>
<p>Qui vient de notre Prophète (bénédictions et paix soient sur lui)</p>
<p>Je vais vous raconter une histoire pour appuyer mes propos :</p>
<p>Un jour le Prophète (bpslà) était assis parmi de grandes personnes...</p>
<p>Il y'avait les compagnons les plus connus et les plus fidèles,</p>
<p>Abou bakr le véridique, et Omar ibn Al-Khattab, et Saad ibn Abi-Wakkas...</p>
<p>Des personnes qui avaient dépassé la trentaine,</p>
<p>Et des personnes avec des statuts très importants...</p>
<p>Et à coté du Prophète (bpsl) étais assis un petit garçon de 10ans,</p>
<p>Il faisait très chaud et tout le monde avait soif,</p>
<p>Puis de l'eau est arrivée,</p>
<p>En islam quand on sert de l'eau, on commence par la personne à droite, qui la passe à son tour à la personne à droite...puis ca tourne dans ce sens...</p>
<p>Et à droite du Prophète (bpsl) se trouvait justement ce petit garçon...</p>
<p>Pose-toi la question,</p>
<p>Si ton fils est assis à coté de toi, qu'il y'a des personnes âgées dans ton salon, et que tu dois servir de l'eau,</p>
<p>Tu ne vas même pas remarquer ton fils, c'est comme s'il n'était pas présent,</p>
<p>Tu serviras tout de suite les adultes...</p>
<p>Et si ton fils te dit « Papa... Papa, j'ai soif »</p>
<p>Tu lui répondrais « Tais-toi, sois poli, les grands d'abord ! »</p>
<br>
<p>Mais le Prophète (bpsl) n'a rien fait de tout ca,</p>
<p>Regarde le respect et l'estime qu'il avait même pour un enfant de 10ans :</p>
<p>Le Prophète (bpsl) tenait l'eau, il a regardé l'enfant en disant <strong class="prophete">« Me permets-tu que je commence par les adultes ? »</strong></p>
<p>(Tu es la personne à droite de moi, c'est ton droit de boire en premier, donc j'ai besoin de ta permission pour commencer par les plus grands...</p>
<p>Imaginez le Prophète (bpsl) qui demande la permission d'un enfant...)</p>
<p>L'enfant a regardé le Prophète (bpsl) en disant « Non, ce que tu me donnes je ne le céderai à personne d'autre »</p>
<p>(C'est un honneur que le Prophète (bpsl) en personne me sert de l'eau, c'est une occasion en or alors je ne la laisserai pas filer)</p>
<p>Le Prophète (bpsl) a regardé le petit garçon, puis a regardé les adultes en disant <strong class="prophete">« C'est son droit, je commencerai donc par lui... bois mon fils »</strong></p>
<p>Vous imaginez le degré de respect et d'estime pour un enfant de 10ans ?</p>
<p>On pourrait écrire des livres entiers à partir de cette scène, et apprendre comment nous comporter avec nos enfants,</p>
<p>N'humilie jamais tes enfants,</p>
<p>Ne te moque jamais d'eux,</p>
<p>Ne les punis jamais devant leurs frères et sœurs,</p>
<p>Tu veux le punir ?</p>
<p>Emmène le dans une chambre à part, explique lui qu'il a commis telle bêtise, et que sa punition est ceci et cela...</p>
<p>Mais jamais devant les autres... jamais !</p>
<p>Le respect et l'estime est un principe qui remplit les maisons de sourires d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
