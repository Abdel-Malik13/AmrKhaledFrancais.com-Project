                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                        
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Le sourire d'espoir d'aujourd'hui est spécial pour la fête des mères...</p>
<p>Nos mamans chéries !</p>
<p>Que Dieu les préserve chaque année,</p>
<p>Que Dieu les bénisse et les rende heureuses,</p>
<p>Et que Dieu les protège et nous les garde !</p>
<br>
<p>En réalité on ne se rend pas compte de la valeur de nos mamans dans ce bas monde...</p>
<p>On ne se rend pas compte de ce que représentent nos mamans pour Dieu...</p>
<br>
<p>Il y'à beaucoup de titres dans ce bas monde : Sergent, Directeur général, Docteur...</p>
<p>A votre avis pour Dieu quel est le titre le plus précieux après le titre de « Prophète » ?</p>
<p>Si vous me posez la question je vous dirai le titre de « Maman »</p>
<p>Nos mamans sont très précieuses pour Dieu,</p>
<p>Quand elles prient pour nous, les portes du ciel s'ouvrent...</p>
<br>
<p>Personnellement tant que ma maman fait des prières pour moi, j'avance dans cette vie en étant serein,</p>
<p>Et parfois je l'appelle pour lui demander si elle pense à moi dans ses prières...</p>
<p>Quand elle dit oui je suis complètement rassuré.</p>
<p>Et quand j'ai peur ou que je fais face à un problème je lui demande de multiplier les prières pour moi.</p>
<br>
<p>Nos mamans sont tellement précieuses, qu'on avance dans cette vie grâce à leurs bénédictions...</p>
<br>
<p>J'ai un ami qui s'appelle Khaled,</p>
<p>Khaled est professeur de tennis,</p>
<p>Khaled m'a appelé alors que j'étais à l'aéroport de Genève, j'attendais mon avion pour rentrer au Caire...</p>
<p>Quand le nom de Khaled s'est affiché sur mon portable, je me suis dit pas grave je le rappellerai une fois arrivé en Egypte...</p>
<p>Mais il m'a rappelé 5 fois... Le téléphone sonnait sans arrêt,</p>
<p>Et quand j'ai décroché Khaled était en pleur...</p>
<p>Il me disait « Celle pour qui on t'honorait est décédée... »</p>
<p>La mère de Khaled était décédée...</p>
<p>Il pleurait car ce jour il a ressenti la valeur de sa maman...</p>
<p>Le Prophète (paix et bénédictions soient sur lui) nous a dit dans une parole que quand une personne perd sa maman,</p>
<p>Les anges l'appellent en disant « Celle pour qui on t'honorait est décédée... Désormais multiplie les bonnes œuvres pour qu'on t'honore...»</p>
<p>Les anges te venaient en aide non pas pour toi, mais pour ta maman...</p>
<p>Pour les prières de ta maman...</p>
<p>Désormais elle n'est plus là...</p>
<p>Tu te retrouves seul...</p>
<p>Tu n'as plus que toi-même et tes bonnes œuvres...</p>
<br>
<p>Khaled pleurait en répétant cette phrase « Celle pour qui on t'honorait est décédée... »</p>
<p>J'essayais de le calmer... Il disait la même phrase encore et encore...</p>
<br>
<p>Dès que Khaled a raccroché, j'ai tout de suite appelé ma maman pour lui demander si elle était satisfaite de moi,</p>
<p>« Je t'en supplie sois satisfaite de moi ! »</p>
<p>Elle m'a demandé ce qui se passait ?</p>
<p>Je me suis rendu compte de sa valeur... J'ai eu peur de la perdre...</p>
<p>Parce que les anges m'honorent et me viennent en aide pour elle...</p>
<br>
<p>A tous ceux qui m'écoutent,</p>
<p>Que Dieu préserve vos mamans en cette belle occasion,</p>
<p>Cours la voir,</p>
<p>Ou téléphone lui,</p>
<p>Demande lui si elle est satisfaite de toi,</p>
<p>Fais tout ce que tu peux pour la rendre heureuse,</p>
<br>
<p>Tant que ta maman est satisfaite de toi, les portes du ciel te sont ouvertes,</p>
<p>Et tu réussiras dans la vie avec la volonté de Dieu,</p>
<br>
<p>Mais si ta maman est fâchée contre toi, cours te réconcilier avec elle et la satisfaire... Sinon tu ne verras jamais de réussite dans ta vie !</p>
<br>
<p>Que Dieu protège nos mamans,</p>
<p>Et les rende satisfaites de nous.</p>
<br>
<p>Un sourire d'espoir pour la fête des mères,</p>
<br>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
