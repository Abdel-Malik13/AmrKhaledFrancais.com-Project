                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle du créateur de la grande société FedEx,</p>
<p>Connue dans le monde entier,</p>
<p>Celle qui distribue les colis d'un pays à l'autre, d'un continent à l'autre...</p>
<p>Si tu veux envoyer une lettre, un courrier, des vêtements ou peu importe à un autre pays, FedEx possèdent des avions et des wagons, ils s'occupent de tout.</p>
<p>Celui qui a démarré tout ça s'appelle Frederick W. Smith,</p>
<p>Un jeune américain qui étudiait dans une université appelée « yale university »</p>
<p>Je tenais à te raconter son histoire pour que tu ne penses jamais que tu vas échouer,</p>
<p>Et si tu as une bonne idée, accroche-toi et travaille dur pour la concrétiser...</p>
<p>Quand Frederick W. Smith était étudiant, transporter un coli d'un pays à un autre était quelque chose d'inimaginable,</p>
<p>Pendant sa dernière année, il devait présenter un projet professionnel,</p>
<p>Il est donc arrivé devant ses professeurs,</p>
<p>Et il a exposé son idée de transporter du courrier et des colis partout dans le monde en 48h !</p>
<p>En 48h, je vous livre ce que vous voulez, où vous voulez dans le monde...</p>
<p>Ses professeurs lui ont donné Zéro !</p>
<p>Pourquoi ?</p>
<p>Ils lui ont dit : on vous a demandé un projet concret pas des rêves roses...</p>
<p>Aujourd'hui ca nous semble normal de tout transporter, mais c'était en 1971.</p>
<p>A l'époque il n y'avait pas autant de moyens de transport et de communication,</p>
<p>Mais Frederick W. Smith, lui l'a vu avant tout le monde...</p>
<p>Un des professeurs a eu pitié de lui, il est venu le voir en disant :</p>
<p>Je te laisse un peu de temps pour changer de projet, et au lieu de zéro je te donnerai quelques points...</p>
<p>Frederick W. Smith lui a donné une réponse qui changera tout : "Gardez vos points, je garderai mon rêve ».</p>
<p>Ce n'est pas une histoire de notes à l'école,</p>
<p>Attention, je ne dis pas ca pour que demain tu ailles voir tes professeurs en disant je n'ai pas besoin de vos notes ;)</p>
<p>C'est l'histoire de croire en son rêve.</p>
<p>Après avoir eu son diplôme,</p>
<p>Il s'est lancé avec 5 ou 6 amis,</p>
<p>Ils envoyaient 8 colis par mois... Vous imaginez aujourd'hui c'est des millions de colis qui passent par FedEx, mais au début c'était 8 pas plus...</p>
<p>Et les gens se moquaient d'eux,</p>
<p>Et leurs parents leurs disaient d'arrêter le projet,</p>
<p>Et ils perdaient de l'argent,</p>
<p>Mais ils ont tenu bon...</p>
<p>Et aujourd'hui vous voyez le résultat,</p>
<p>Ils ont des camions partout,</p>
<p>Des bureaux,</p>
<p>Des avions,</p>
<p>Une réussite mondiale !</p>
<p>Frederick W. Smith est connu aujourd'hui,</p>
<p>Mais ses professeurs qui lui ont donné Zéro personne ne les connait...</p>
<p>Donc comme Frederick W. Smith accroche toi bien, travaille dur, et n'écoutent pas ceux qui te rabaissent... Jusqu'au jour où tu réussiras et que tu dessineras un sourire d'espoir :)</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
