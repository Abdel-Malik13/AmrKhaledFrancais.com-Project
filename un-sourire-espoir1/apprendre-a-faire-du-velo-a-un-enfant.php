                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<br>
<p>L'épisode d'aujourd'hui parle de moi, mon fils Ali et comment apprendre à faire du vélo...</p>
<p>Tu me diras en quoi ca te regarde ?</p>
<p>A la fin de l'histoire tu comprendras :)</p>
<br>
<p>Quand je vivais en Angleterre, j'ai décidé d'apprendre à mon fils Ali de faire du vélo,</p>
<p>Pendant mon enfance, mon oncle m'avait appris à faire du vélo, et c'est resté gravé dans ma mémoire...</p>
<p>J'aimais cet oncle et je pensais tout le temps à lui rien que pour ca.</p>
<br>
<p>Donc j'ai voulu refaire la même chose avec mon fils Ali,</p>
<p>Pour qu'il garde cette image de moi d'un père qui lui apprend, et qui l'aide à franchir un cap dans sa vie...</p>
<br>
<p>On est partis dans un parc,</p>
<p>Et j'étais tellement sur de moi que je pensais que j'allais tout lui apprendre en 5 minutes,</p>
<p>Sauf qu'à chaque fois que je le poussais il tombait... Puis retombait...</p>
<p>Puis il a commencé à pleuvoir,</p>
<p>Et Ali commençait à sentir l'échec (Il devait avoir 5 ou 6 ans)</p>
<p>Il sentait qu'il n'avançait pas...</p>
<p>Et j'essayais de l'encourager par tous les moyens... Mais il continuait à tomber...</p>
<p>Jusqu'au moment où j'ai eu une idée,</p>
<p>Je lui ai dit « Regarde Ali, je veux que tu tiennes sans tomber pendant 6 secondes seulement,</p>
<p>Si tu tiens pendant que je compte 1...2...3...4...5...6... on aura réussi notre objectif d'aujourd'hui ! »</p>
<p>Mon objectif de départ c'était de lui apprendre à faire du vélo en une journée,</p>
<p>Mais je l'ai baissé à 6 secondes, pour que quand il rentre à la maison il sente qu'il a progressé...</p>
<p>Vous me croyez si je vous dis que ca a fonctionné ?</p>
<p>Il s'efforçait à tenir pendant les 6 secondes, puis c'est devenu facile pour lui...</p>
<p>Alors je lui ai dit « Maintenant il faut qu'on tienne 10 secondes »</p>
<p>Garde ton équilibre pendant 10 secondes... Et il l'a réussi !</p>
<p>Puis 15 secondes... Puis 20 secondes...</p>
<p>Et ca y est il avait appris à faire du vélo !</p>
<br>
<p>J'ai appris ce jour là une chose très très importante dans la vie,</p>
<p>Et j'ai envie de vous l'offrir dans cet épisode :</p>
<br>
<p>Quand tu veux accomplir un grand projet,</p>
<p>Ne l'attaque pas en entier,</p>
<p>Divise-le en plusieurs petits projets...</p>
<p>6 secondes... Puis 10 secondes...</p>
<p>Car si tu l'attaques en entier, tu ne sentiras pas une grande progression... Et ca finira peut-être par te décourager.</p>
<p>Et une fois découragé tu pourras abandonner...</p>
<p>J'ai appris ca avec mon fils Ali et son vélo.</p>
<br>
<p>Mais tu peux l'appliquer aussi dans tes études,</p>
<p>Dans tes révisions,</p>
<p>Ou dans le grand rêve de ta vie,</p>
<p>Divise-le en plusieurs petits rêves...</p>
<p>En simplifiant les choses on a l'impression d'avancer plus vite !</p>
<br>
<p>Mais mon histoire avec Ali et son vélo n'est pas terminée :)</p>
<p>Car j'ai appris ce jour là une 2ème grande leçon :</p>
<p>Au début Ali me demandait de tenir sa main car il avait peur de tomber du vélo...</p>
<p>Et moi j'étais heureux de le tenir par la main,</p>
<p>C'est un très beau sentiment quand on tient nos enfants par la main...</p>
<p>Puis quand il a réussi à faire les 15 secondes,</p>
<p>Je le tenais toujours par la main...</p>
<p>Et il me disait « Lâche ma main papa... lâche ma main... »</p>
<p>Ca m'a appris qu'il fallait en tant que parents savoir à quel moment tenir nos enfants par la main... Et à quel moment leur donner la liberté...</p>
<p>A quel moment dire à notre enfant « N'ai pas peur, je suis à tes cotés tu ne risques rien ! »</p>
<p>Et à quel moment lui donner la liberté, et ne pas s'entêter à le tenir par la main... Alors qu'il veut se lancer dans la vie.</p>
<br>
<p>Je n'avais pas très envie de lâcher la main d'Ali, mais il a trop insisté donc je l'ai laissé partir...</p>
<p>Et c'était la bonne décision.</p>
<br>
<p>En tant que parents il faut être intelligents et ne pas attendre que nos enfants nous disent de les laisser avant de le faire...</p>
<br>
<p>Je voulais partager ces deux leçons avec vous mes chers :</p>
<p>1- Divise ton projet en plusieurs petits projets.</p>
<p>2- Sois flexible avec tes enfants, sache à quel moment tu es à leur côté, et à quel moment tu leur donnes de l'espace pour qu'ils s'expriment.</p>
<br>
<p>Mon 2ème fils Omar c'est son ami qui lui a appris à faire du vélo, et je le regrette...</p>
<p>Mais pas grave :)</p>
<p>J'ai appris de mes 2 fils pleins de leçons dans ma vie,</p>
<p>Un sourire d'espoir sur le visage de tous nos enfants quand on leur donne leur liberté,</p>
<p>Et un sourire d'espoir lorsqu'on divise nos projets en plusieurs étapes.</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
