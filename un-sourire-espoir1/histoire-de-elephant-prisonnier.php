                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide,
entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne
qui a réussi...</p> <p>Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage
L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées,des bonnes manières,de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Une nouvelle parole, et un nouveau sourire d'espoir, la parole d'aujourd'hui est une histoire,</p>
<p>une histoire qui parle d'un petit éléphant blanc, cet éléphant a été capturé par des braconniers</p>
<p>puis vendu à un grand zoo, dans un autre pays. Le responsable du zoo a pris cet éléphant blanc</p>
<p>et a décidé de le roder, afin qu'il soit soumis et qu'il ne crée pas de problèmes.</p>
<p>Alors le responsable a pris une grande chaine en fer et l'a mise autour d'une des pattes</p>
<p>de l'éléphant.</p>
<p>Et le premier jour, l'éléphant s'est débattu pour enlever la chaine et s'enfuir, mais il n'a pas pu</p>
<p>le deuxième jour, il a encore essayé avec toutes ses forces, mais sa patte lui a fait extrêmement mal</p>
<p>le troisième jour, il a réessayé, mais le mal ne faisait qu'augmenter et augmenter...</p>
<p>et le quatrième jour, l'éléphant face à la douleur a fini par désespérer.</p>
<p>le responsable du zoo était un homme sage et intelligent, il a donc voulu faire une expérience</p>
<p>il a enlevé la chaine en fer pendant que l'éléphant dormait, et l'a remplacé par chaine en bois.</p>
<p>Bien-sur avec une chaine en bois, l'éléphant peut la casser facilement et s'enfuir...</p>
<p>Cependant, le lendemain, l'éléphant est resté à sa place sans bouger, soumis à sa chaine</p>
<p>et les jours se sont suivis, sans que l'éléphant ne fasse la moindre tentative.</p>
<p>Le responsable du zoo a alors décidé de sortir l'éléphant de sa cage, et de le mettre devant les visiteurs</p>
<p>Et l'éléphant continuait à penser qu'il était enchainé.</p>
<p>Un jour un enfant intelligent venu visiter le zoo avec sa maman a posé une question au responsable du zoo :</p>
<p>"L'éléphant n'est enchainé que par une chaine en bois, pourquoi ne s'enfuit-il pas ? Il est assez fort</p>
<p>pour le faire"</p>
<p>Le responsable du zoo lui a répondu "toi tu sais que la chaine est en bois, et moi aussi je sais</p>
<p>que la puissance de l'éléphant est supérieure à la puissance du bois... Mais l'éléphant lui l'ignore,</p>
<p>il n'est pas conscient de ses capacités"</p>
<p>je vous raconte cette histoire, parce-que par moments nous nous mettons des chaines, et nous</p>
<p>pensons que nos capacités sont limitées, et que des obstacles sont partout autour de nous...</p>
<p>Alors que ces obstacles sont en bois et non en fer... C'est juste dans notre esprit que ces</p>
<p>obstacles sont en fer... Et nous ne voulons pas nous lancer, nous activer, et réussir.</p>
<p>Et la différence entre celui qui réussit et celui qui échoue, est que celui qui échoue pense</p>
<p>que les obstacles sont beaucoup plus forts que lui, alors que celui qui réussit est conscient</p>
<p>que ces obstacles ne sont qu'en bois, et qu'il est beaucoup plus fort que ces obstacles.</p>
<p>Chacun de nous doit se poser la question, suis-je pareil que cet éléphant blanc ?</p>
<p>Chacun de nous doit observer les obstacles autour de lui, et se poser la question sont elles en bois ou en fer?</p>
<p>je suis sur que 90% des obstacles qui sont autour de nous ne sont qu'en bois, et que tes capacités</p>
<p>sont beaucoup plus grandes que ces obstacles, mais tu l'ignores...</p>
<p>Et peut-être que les gens autour de toi sont conscients de tes grandes capacités, et que tu es le seul à penser que tes capacités sont faibles...</p>
<p>Je peux te faire une demande ? Explose tes capacités! Active-toi! Lance toi! et tu vas réussir</p>
<p>avec la volonté de Dieu, fais confiance à Dieu,</p>
<p><strong>{ [...] En vérité, Allah ne change pas l'état d'un peuple tant que celui-ci ne change pas ce qui est en lui-même. }</strong> [ Sourate 13 - Verset 11 du Coran ]</p>
<p>le jour où tu te diras à toi même "Je peux réussir!" et que tu t'activeras dans ce sens, Dieu t'aidera, et tu réussiras Inshallah</p>
<p>Je vous remercie beaucoup, et que la paix soit sur vous.</p>
