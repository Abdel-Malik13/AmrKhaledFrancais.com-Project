                                        <br>
                                        <p>Quand la vie semble difficile, parfois nous avons besoin d'entendre une nouvelle idée qui nous aide,
entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne
qui a réussi...</p> <p>Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage
L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue à tous avec un sourire d'espoir,</p>
<p>Beaucoup de personnes ont des soucis dans leurs vies, et des obstacles devant elles, beaucoup de personnes quand il leur arrive un malheur disent « qu'est ce que j'ai fais pour mériter ca ? » et « pourquoi il m'arrive ca à moi ? »</p>
<p>Je vous dédicace une histoire sympathique, c'est une histoire imaginaire,</p>
<p>On dit qu'il existait un roi, et ce roi avait un conseiller, et ce conseiller était positif, à chaque fois qu'il arrivait un malheur dans le royaume, il avait une expression qu'il répétait à chaque fois « c'est peut-être un bien ! »</p>
<p>à chaque fois qu'il se passait quelque chose de mal il disait « c'est peut-être un bien ! »</p>
<p>Ca a commencé à embêter le roi, il n'aimait pas cette philosophie.</p>
<p>Et un jour, le roi sortait avec ses conseillers dans une partie de chasse... Et en tirant une flèche, le fil de l'arc lui a coupé le doigt.</p>
<p>Le roi a commencé à souffrir avec son doigt coupé, alors le conseiller l'a regardé avec un sourire en disant « c'est peut-être un bien ! »</p>
<p>Ca a mis le roi en colère, tout le temps « c'est peut-être un bien ? , c'est peut-être un bien ??»</p>
<p>Le roi a ordonné qu'on le jette en prison, et le conseiller a encore sourit en disant « c'est peut-être un bien ! »</p>
<p>Les jours ont passé, et le roi est encore parti chasser, et là une horde de cannibales sort de nul part et les attaque... C'est bien sur une histoire imaginaire.</p>
<p>Donc les cannibales ligotent le roi et ceux qui l'accompagnent, pour les tuer et en faire un sacrifice...</p>
<p>Et là en regardant le roi, ils voient un doigt coupé, alors ils disent il a un défaut donc il ne servira pas comme sacrifice, ils l'ont donc relâché et laissé partir...</p>
<p>Et là le roi se souvient de l'expression de son ancien conseiller « c'est peut-être un bien ! »</p>
<p>Alors il demande à ce qu'on le sorte de prison, une fois le conseiller devant lui, le roi lui dit j'ai enfin compris le sens de ton expression « c'est peut-être un bien ! »</p>
<p>Si mon doigt ne s'était pas coupé, je serai mort à cet instant !</p>
<p>Mais alors toi, pourquoi tu as dis « c'est peut-être un bien ! » quand j'ai ordonné qu'on te jette en prison ?</p>
<p>Le conseiller lui répond, justement mon doigt à moi est intact, si je vous avais accompagné ils m'auraient tué comme les autres, alors le fait d'être en prison est le meilleur bien qui pouvais m'arriver !</p>
<p>L'idée de cette histoire et sa morale c'est qu'il faut regarder chaque chose qui t'arrive dans ce bas monde par la volonté de Dieu, tu n'y es pour rien, ca t'es arrivé sans que tu le demandes... Mais si Dieu le veut derrière ce mal se cache un bien pour toi...</p>
<p>As-tu compris le sens du verset qui dit <strong>{ Vous pouvez ne pas aimer ce qui vous est bon et aimer ce qui vous est mauvais. Dieu sait et vous ne savez pas }</strong> (Coran 2.216)</p>
<p>Ce verset te permet de voir chaque mal, chaque catastrophe qui t'arrive dans la vie en disant « qui sait... peut-être que derrière se cache un bien pour moi »</p>
<p>Alors tu restes tout le temps positif, et ton cerveau devient comme un jardin, soit tu plantes dedans des roses, soit tu le remplis de pesticides.... Imagine ton cerveau est un jardin, tu aimerais le remplir de quoi ? remplis le de roses d'espoir, de fleurs de positivité.... Ce verset est magnifique</p>
<p><strong>{ Vous pouvez ne pas aimer ce qui vous est bon et aimer ce qui vous est mauvais. Dieu sait et vous ne savez pas }</strong> (Coran 2.216)</p>
<p>A chaque fois que tu feras face à un problème dans ta vie, c'est pas toi qui l'a provoqué, c'est Dieu qui te l'a destiné, regarde le comme si c'était un bien !</p>
<p>On a fait une enquête sur 20 personnages occidentaux qui ont réussi dans plusieurs domaines, on leur a demandé vos grandes réussites elles sont arrivées comment ? On a découvert que 70% des grands projets qui ont réussi avaient derrière eux des problèmes et des obstacles... Mais ces grands obstacles leur ont servi comme motivation pour se dépasser et réussir.</p>
<p>Mes chers, traitez que ce que Dieu vous destine dans ce bas monde avec un peu de positivité, l'avenir qui va en découler est meilleur...</p>
<p><strong>{ Vous pouvez ne pas aimer ce qui vous est bon et aimer ce qui vous est mauvais. Dieu sait et vous ne savez pas }</strong> (Coran 2.216)</p>
<br>
<p>En effet, c'est peut-être un mal pour un bien !</p>
<p>Je vous remercie, à Demain si Dieu le veut avec un nouveau sourire d'espoir.</p>
