                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>

<p>Je veux vous raconter ce qui m'est arrivé la semaine dernière,</p>
<p>La semaine dernière j'ai été invité à participer à une conférence en Tunisie,</p>
<p>Avant j'étais interdit d'y aller,</p>
<p>Donc c'était ma toute première fois en Tunisie.</p>
<p>Et 3 jours avant la date de cette conférence, on m'a donné un autre rendez-vous extrêmement important au Caire.</p>
<p>Ce rendez-vous était le lendemain de la conférence en Tunisie.</p>
<p>Donc si je pars aujourd'hui en Tunisie, il faut que je rentre en Egypte le soir même pour être à l'heure à mon 2ème rendez-vous...</p>
<p>J'ai pensé à annuler la conférence en Tunisie, mais je me suis dit que ca ne se faisait pas...</p>
<p>La 1ère fois que les tunisiens m'invitent, et depuis des années je rêve d'y aller... Il faut que je fasse l'effort !</p>
<p>J'ai alors pris la décision d'aller en Tunisie le matin, reprendre l'avion le soir, et arriver à l'heure pour mon rendez-vous en Egypte.</p>
<p>Je vais devoir courir dans tous les sens mais pas grave, j'arriverai à faire les deux.</p>
<br>
<p>Si vous savez ce qui m'est arrivé :)</p>
<p>Comment j'ai souffert !</p>
<p>Je suis parti à l'aéroport du Caire à 7h du matin, pour prendre l'avion vers la Tunisie...</p>
<p>L'avion devait décoller à 9h du matin (je ne citerai pas le nom de la compagnie)</p>
<p>Mais l'avion n'a pas décollé avant 14h</p>
<p>Je suis resté bloqué à l'aéroport du Caire de 9h à 14h</p>
<p>Et la conférence avait lieu à 18h</p>
<p>L'avion a fini par décoller, et à peine arrivé en Tunisie j'ai couru donner mon discours,</p>
<p>Et Mashallah c'était un stade complet avec 10.000 personnes,</p>
<p>A peine j'ai fini la conférence, j'ai recouru vers l'aéroport,</p>
<p>(Je suis resté en Tunisie 1h30 la durée de la conférence)</p>
<p>Je n'ai même pas eu le temps de boire de l'eau...</p>
<p>Une fois à l'aéroport, l'avion qui devait me ramener en Egypte a eu un retard de 2 heures,</p>
<p>J'ai donc du prendre un autre avion qui faisait une escale en Turquie,</p>
<p>Une fois en Turquie, j'ai raté l'avion qui devait me ramener en Egypte...</p>
<p>Alors j'ai du passer la nuit en Turquie,</p>
<p>Et le lendemain j'ai pris le 1er avion pour arriver à temps pour mon rendez-vous de 9h.</p>
<p>La vérité c'est que j'ai beaucoup souffert :)</p>
<p>Mais pourquoi je vous raconte tout ca ?</p>
<p>Pour vous dire qu'il n y'à rien de mieux que le plaisir de donner,</p>
<p>Je n'ai touché aucun centime de la conférence,</p>
<p>Mais j'ai goûté quelque chose de plus savoureux que l'argent,</p>
<p>J'ai goûté le plaisir de donner qui est meilleur que le plaisir de recevoir...</p>
<p>J'ai vu dans ma vie des millionnaires vivant dans des demeures luxueuses,</p>
<p>Mais ils sont malheureux dans un coin de leur maison...</p>
<p>La tristesse se lit sur leurs visages...</p>
<p>Ceux qui travaillent chez eux rient beaucoup plus qu'eux,</p>
<p>Le bonheur est dans les cœurs, non pas dans les comptes bancaires...</p>
<br>
<p>Le plaisir de donner, quand tu aide une personne,</p>
<p>Quand tu dessine un sourire sur son visage,</p>
<p>Le Prophète (paix et bénédictions soient sur lui) a dit dans une parole :</p>
<p><strong class="prophete">"Celui qui fait pénétrer du bonheur dans une maison musulmane ne méritera rien de moins que le Paradis."</strong></p>
<br>
<p>Rend les gens heureux mon cher,</p>
<p>Rend les gens heureux et Dieu sera satisfait de toi,</p>
<p>Et Dieu te rendra heureux,</p>
<p>Et Dieu te fera entrer au Paradis...</p>
<br>
<p>Je n'ai pas regretté mon voyage en Tunisie,</p>
<p>Au contraire j'en suis très heureux,</p>
<p>Et j'ai aimé le peuple tunisien,</p>
<p>Et la conférence était très belle,</p>
<p>Et les tunisiens étaient formidables,</p>
<p>Accueillants et chaleureux...</p>
<br>
<p>Ils m'ont donné beaucoup mieux que l'argent,</p>
<p>Ils m'ont donné des bons sentiments, de l'amour, de la chaleur...</p>
<br>
<p>Essayez mes chers le plaisir de donner, vous verrez que vous serez beaucoup plus heureux dans vos vies,</p>
<p>Quand on vit pour nous même, on nait petits, on vit petits, et on meurt petits...</p>
<p>Mais quand on vit pour les autres, notre vie s'agrandit avec la vie de chaque personne à qui on a donné du bonheur.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
