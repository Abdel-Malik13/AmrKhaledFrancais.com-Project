                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide,
entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne
qui a réussi...</p> <p>Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage
L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages
des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>Notre sourire d'espoir d'aujourd'hui concerne les relations entre parents en enfants,</p>
<p>Un message à tous les enfants : Soyez miséricordieux avec vos parents...</p>
<p>J'ai reçu un courrier d'un père et d'une mère,</p>
<p>Ils m'y racontent :</p>
<p>« Notre fils était très difficile,</p>
<p>il avait un comportement dur avec nous,</p>
<p>Puis un jour on s'est disputé avec lui, et le ton est monté...</p>
<p>Alors notre fils a eu une réaction très violente,</p>
<p>Il a quitté la maison, il a disparu...</p>
<p>Il a quitté la maison pour trouver un travail ailleurs...»</p>
<p>C'est possible qu'un enfant fasse ca à ses parents ?</p>
<p>Les abandonner et disparaitre dans la nature sans prévenir ...</p>
<p>Ses parents me disaient :</p>
<p>« On a beaucoup pleuré, On s'est beaucoup fatigués,</p>
<p>On a vécu dans l'inquiétude et la peur,</p>
<p>Et on a imprimé des affiches avec sa photo et cette phrase :</p>
<p>'Nous t'en supplions reviens à la maison, ton papa et ta maman ont besoin de toi'</p>
<p>Et on a commencé à les distribuer et les coller partout,</p>
<p>Et on a appelé ses copains dans l'espoir que quelqu'un sache l'endroit où il se trouve... »</p>
<p>C'est possible qu'un fils soit dur à ce point ?</p>
<p>A tous les jeunes, ne faites jamais la même chose à vos parents...</p>
<p>Ses parents poursuivaient :</p>
<p>« Les copains de notre fils nous ont aidé, ils ont collé les affiches partout »</p>
<p>Et une affiche a fini dans un endroit proche de là ou se trouvait le fils...</p>
<p>Le fils était dans un café ce jour là, puis il a aperçu sa photo avec le mot de ses parents...</p>
<p>'Nous t'en supplions reviens à la maison, ton papa et ta maman ont besoin de toi'</p>
<p>Et apparemment à cet instant quelque chose a vibré dans son cœur,</p>
<p>Il s'est réveillé... C'est possible que j'ai fais ca à mes propres parents ?</p>
<p>Il a donc décidé de revenir chez lui,</p>
<p>Et sa maison se trouvait dans un quartier populaire...</p>
<p>Les parents me disaient :</p>
<p>« Nous avions peur que si un jour il revient, il trouve la porte fermée... Alors depuis le jour ou il a fugué, nous n'avons jamais fermé la porte... »</p>
<p>Vous imaginez, tout ce temps la porte de la maison est restée ouverte...</p>
<p>De peur que si jamais il arrive tard le soir, il trouve la porte fermée, alors ca le décourage il fait demi-tour...</p>
<p>Nos parents sont si miséricordieux que ca...</p>
<p>A tous les jeunes, je vous en supplie soyez miséricordieux envers vos parents,</p>
<p>Peu importe comment ils vous semblent sévères, au fond d'eux ils vous aiment...</p>
<p>Sans vous, leurs cœurs se déchirent... Et ils pleurent du sang...</p>
<p>Le fils est donc revenu au milieu de la nuit,</p>
<p>Et quand il a voulu sonner, il a vu la porte ouverte,</p>
<p>Ce n'est pas une chose habituelle alors il a paniqué,</p>
<p>Il y'à eu peut-être un cambriolage ou un malheur,</p>
<p>Il est entré, il est parti réveiller ses parents pour voir si tout allait bien...</p>
<p>Ses parents l'ont serré dans leurs bras,</p>
<p>Ils l'ont embrassé,</p>
<p>Ils ont pleuré...</p>
<p>Mais il leur a posé la question « Comment ca se fait que la porte soit ouverte à cette heure ? »</p>
<p>Ils lui ont dit « On n'a jamais fermé la porte depuis le jour où tu es parti... Depuis que tu es parti nous n'avons pas trouvé le courage de fermer la porte...»</p>
<p>L'histoire de cette famille est terminée...</p>
<p>Mais en lisant leur lettre, il s'est passé quelque chose en moi...</p>
<p>Je me suis demandé si les parents de ce jeune homme ont laissé la porte ouverte...</p>
<p>Alors que dire de la porte de Dieu...</p>
<p>Combien de temps il nous a laissé la porte ouverte...</p>
<p>La miséricorde que Dieu a créé dans les cœurs des parents, nous apprend énormément de choses...</p>
<p>Ca nous apprend que si ton papa et ta maman laissent la porte ouverte quoi que tu fasses...</p>
<p>Alors c'est certain, certain , certain... que notre Seigneur laisse sa porte grande ouverte quoi qu'on fasse...</p>
<p>L'épisode d'aujourd'hui dessinera un sourire d'espoir sur le visage de chaque papa et maman...</p>
<p>Et l'épisode d'aujourd'hui dessinera un sourire d'espoir sur le visage de chaque personne éloignée de Dieu... La porte de ton Seigneur est grande ouverte pour toi...</p>
<br>
<p>Je vous remercie, et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
