                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>

<p>Le sourire d'espoir d'aujourd'hui est une histoire imaginaire,</p>
<p>On raconte que la beauté et la laideur sont parties à la plage,</p>
<p>On a bien dit imaginaire... Donc pas grave, suis moi à mon rythme :)</p>
<p>Donc la beauté et la laideur sont parties à la plage,</p>
<p>Et chacune d'elles portait ses habits,</p>
<p>La beauté portait ses beaux habits,</p>
<p>Et la laideur portait ses habits moches...</p>
<p>Les deux ont donc décidé d'aller se baigner,</p>
<p>Alors elles ont retiré leurs vêtements...</p>
<p>Et pendant qu'elles nageaient, la laideur a aperçu les beaux habits sur le sable,</p>
<p>Alors la laideur est sortie en courant de l'eau,</p>
<p>Et elle a enfilé les habits de la beauté...</p>
<p>La laideur s'est déguisée en beauté,</p>
<p>Et elle est retournée en ville marcher parmi les gens avec ses faux habits...</p>
<p>La beauté quant à elle est sortie de l'eau sans trouver ses beaux habits...</p>
<p>Elle s'est attristée, mais elle n'avait pas d'autre choix que d'enfiler les habits de la laideur...</p>
<p>La beauté ainsi que la laideur sont retournées en ville, et beaucoup de personnes se sont fait avoir depuis...</p>
<p>Des gens ont vu le vol déguisé dans de beaux habits... Alors ils ont volé...</p>
<p>Et une jeunesse a vu des cigarettes et des drogues déguisées dans de belles tenues... Alors elle s'est laissée attirer, et y a touché...</p>
<p>Et des gens... Et des gens... Et des gens...</p>
<p>Et des hommes n'ont pas aperçu la beauté de la femme respectable, et se sont laissé attirer par la nudité...</p>
<p>C'est bien malheureux quand la laideur se déguise en beauté...</p>
<p>Et que la vraie beauté ne sait plus se faire une place...</p>
<p>Mais... Mais... Parce que c'est un sourire d'espoir,</p>
<p>Il existe des gens intelligents,</p>
<p>Il existe des gens qui font bien attention,</p>
<p>Il existe des gens qui ne se laissent pas avoir...</p>
<p>Il existe des gens qui savent que la corruption dissimulée, et les substance illicites, et l'argent démérité... Tout ceci n'est que la laideur servie dans de fausses belles excuses ...</p>
<p>Ne te laisse donc pas arnaquer par la laideur,</p>
<p>Concentre toi un peu devant ce que tu vois,</p>
<p>Ou demande conseil,</p>
<p>Et tu pourras facilement distinguer entre la vraie beauté et la vraie laideur,</p>
<p>Plutôt que de tomber dans des pièges,</p>
<p>Vous avez compris le sens de cette histoire symbolique...</p>
<p>Surtout les jeunes,</p>
<p>Les jeunes ont besoin d'expérience,</p>
<p>Peux tu demander à tes parents ou à des personnes plus âgées si ce que tu vois est une vraie ou une fausse beauté ?</p>
<p>Il n y'à rien de mal à demander mon cher,</p>
<p>Et même si tu n'oses pas demander,</p>
<p>Peux tu prendre au moins le temps de réfléchir avant de décider,</p>
<p>Avec un peu de réflexion tu peux facilement différencier entre la vraie beauté et la fausse,</p>
<p>Tu verras alors beaucoup de vraies belles filles... Alors que tu disais que tu ne trouvais pas la bonne personne pour te marier...</p>
<p>Et parfois tu verras qu'il faut éviter des personnes parmi tes connaissances... Parce qu'elles ont une mauvaise influence sur toi...</p>
<p>Je vous prie tous les jeunes, mais aussi tous les adultes,</p>
<p>Ne vous laissez pas avoir par le tour de la laideur,</p>
<p>Peu importe comment la laideur maitrise son tour, et porte des habits attrayants... Au fond ca reste la laideur et rien d'autre...</p>
<p>Concentrons nous donc,</p>
<p>Prenez soin de vous,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
