                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<p>Soyez les bienvenus avec un nouveau sourire d'espoir.</p>
<br>
<p>Beaucoup de personnes ont des problèmes,</p>
<p>Et beaucoup de mamans souffrent d'avoir perdu leur enfant,</p>
<p>J'ai voulu leur adresser ce sourire d'espoir,</p>
<p>Leur offrir un cadeau,</p>
<p>Leur dire un mot qui les aidera,</p>
<p>J'ai voulu leur parler de la patience,</p>
<p>Je dédicace ce sourire d'espoir à chaque personne qui souffre d'un malheur dans sa vie,</p>
<p>Qu'est ce que je peux donc dire en 3 minutes ?</p>
<p>Je n'ai rien trouvé de mieux qu'un message,</p>
<p>Pas le mien,</p>
<p>Mais un message de Dieu le très haut :</p>
<p><strong>{ Et les endurants auront leur pleine récompense sans compter }</strong></p>
<p>Saint Coran - Sourate 39 - Az-Zumar : Les Groupes (verset 10)</p>
<br>
<p>Souligne bien l'expression <strong>« sans compter »</strong></p>
<p>Et sans compter selon les normes de Dieu, pas selon nos normes à nous...</p>
<p>Ca veut dire quoi donc ?</p>
<p>Imagine tous les malheurs, toutes les maladies, toutes les catastrophes...</p>
<p>Je vous jure qu'ils ne pèsent pas un atome.. ni une goutte dans la mer de l'expression <strong>« sans compter »</strong></p>
<p>Mieux que ca, sais tu qu'en arabe <strong>« sans compter »</strong> a deux sens à la fois :</p>
<p>Ca veut dire qu'on ne leur demandera pas de rendre des comptes,</p>
<p>Donc ils passeront directement de la tombe au Paradis, sans passer par le jugement, et sans rendre des comptes sur ce qu'ils ont fait durant leurs vies...</p>
<p>Tu n'as pas eu cette catastrophe dans ta vie ?</p>
<p>Tu n'as pas souffert de telle maladie ?</p>
<p>Ton enfant est décédé ?</p>
<p>Entre donc toi et ton enfant au Paradis, sans compter (sans rendre des comptes)</p>
<p>C'est donc le premier sens,</p>
<p>Mais il y'à un deuxième sens :</p>
<p><strong>{ Et les endurants auront leur pleine récompense sans compter }</strong></p>
<p>Ca veut dire, qu'ils prennent tout ce qu'ils veulent au Paradis autant qu'ils veulent,</p>
<p>En illimité !</p>
<p>Tu désires quoi donc ?</p>
<p>Je désire un autre château, et plein d'arbres manguiers, et avec mon fils nous voulons deux yachts pour nous amuser... Et je désire... Et je désire... Et je désire... tout ce qu'on peut imaginer...</p>
<p>Sans compter !</p>
<p>Donc soit sans être jugés, soit obtenir tout ce qu'ils veulent au Paradis en illimité,</p>
<p>Ou peut-être même les deux !</p>
<p>Soyez donc patients mes chers,</p>
<p>Prenez sur vous-même,</p>
<p>Patiente, Patiente... Parce que l' expression <strong>« sans compter »</strong> est très belle...</p>
<p>Le prophète sws dit dans une parole :</p>
<p><strong class="prophete">"Aucun homme n'a reçu de meilleur et de plus large don que la patience"</strong></p>
<p>Le prophète a donc cité la patience comme un don,</p>
<p>Donc patiente, car la vie de ce bas monde passera vite,</p>
<p>Et quelque soit ton problème,</p>
<p>Dieu te rassemblera toi et tes proches au Paradis,</p>
<p>Patiente peu importe à quel point ta souffrance est grande, parce que ton Dieu est miséricordieux ,</p>
<p>Et chaque difficulté est suivie par une facilité,</p>
<p>Je te jure par Dieu, qu'une solution va arriver,</p>
<p>Alors souris,</p>
<p>Et patiente,</p>
<p>Et compte sur Dieu,</p>
<p>Et un sourire d'espoir pour chaque maman qui a perdu son enfant,</p>
<p>Que Dieu apaise leurs cœurs, et les réunisse avec leurs enfants au Paradis,</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
