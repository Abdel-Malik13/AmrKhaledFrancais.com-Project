                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui raconte une histoire imaginaire,</p>
<p>Une histoire symbolique mais avec un très grand sens à la fin...</p>
<p>On raconte qu'un village vivait en paix et bonheur,</p>
<p>Un des jeunes de ce village était parti en voyage,</p>
<p>En son absence il s'est passé un grand problème...</p>
<p>Un virus est apparu, il a effacé la mémoire des gens,</p>
<p>Personne au village ne se rappelait plus de rien,</p>
<p>Comme une maladie de l'oubli,</p>
<p>Le lendemain ils vont se réveiller pour cultiver leurs terres, Personne ne se souvient comment faire...</p>
<p>Les parents ne se souvenaient plus de leurs enfants...</p>
<p>Imaginez si virus pareil existait ?</p>
<p>Que les gens oublient même de nourrir leurs enfants...</p>
<p>C'est de l'imagination Dieu merci,</p>
<p>Ca se rapproche de la maladie d'Alzheimer qui entraine la perte progressive de la mémoire,</p>
<p>Certaines personnes atteintes oublient qui sont leurs enfants, qui sont leurs parents, à quoi sert un mouchoir... à ce point...</p>
<p>Les gens du village ont donc tout oublié,</p>
<p>Quand le jeune est rentré il était le seul épargné par la maladie,</p>
<p>Il tentait de leur rappeler des choses, ca leur revenait, mais une minute après ils oubliaient tout...</p>
<p>Le jeune homme a commencé à se poser la question « Quelle est la chose la plus importante à leur rappeler ? »</p>
<p>« Quelle est la chose qu'ils doivent garder à l'esprit même s'ils oublient tout le reste ? »</p>
<p>Et je vous pose la même question :</p>
<p>Si on oublie tout, quelle est la chose qu'il est impossible qu'on oublie ?</p>
<p>Si ce jeune homme devait mettre des panneaux et des annonces dans tout le village,</p>
<p>Quelle est la chose qu'il devrait mettre sur ces affiches pour que les gens s'en souviennent ?</p>
<p>Qui a trouvé ?</p>
<p>Qui a une proposition ?</p>
<p>L'histoire raconte que le jeune homme a vu que la chose la plus importante,</p>
<p>La chose la plus précieuse,</p>
<p>La chose à laquelle ils doivent penser pour oublier les problèmes, ou carrément régler les problèmes...</p>
<p>C'est qu'ils se rappellent de Dieu le très haut.</p>
<p>Qu'ils se rappellent qu'il n y'a de Dieu que Dieu...</p>
<p>Qu'ils se rappellent que notre vie et notre mort sont entre les mains de Dieu le tout Miséricordieux.</p>
<p>Si on oublie tout... N'oublions jamais notre Créateur.</p>
<p>Si on oublie notre passé...</p>
<p>Si on oublie nos accomplissements...</p>
<p>Si on oublie tout...</p>
<p>N'oublions pas notre Dieu.</p>
<p>Met une affiche ou un sticker à l'intérieur de ton cerveau,</p>
<p>« Je me souviens de toi mon Dieu... Tes bienfaits sur moi sont innombrables... Jamais je ne t'oublierai... »</p>
<p>Crois-moi tant que tu te souviendras de Dieu, tu te souviendras de tout le reste.</p>
<p>Lisez ce joli verset du Noble Coran</p>
<p><strong>{ Et ne soyez pas comme ceux qui ont oublié Allah; [Allah] leur a fait alors oublier leurs propres personnes... }</strong></p>
<p>Sourate 59 - Al-Hachr : L'Exode (verset 19)</p>
<p>Rappelle-toi de Dieu, tu te rappelleras de tout le reste, et un sourire d'espoir se dessinera sur ton visage.</p>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
