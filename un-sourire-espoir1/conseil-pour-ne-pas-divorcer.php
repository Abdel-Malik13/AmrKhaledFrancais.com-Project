                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>                                        
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>Un jeune homme et sa fiancée sont venus me voir,</p>
<p>Ils m'ont raconté leur histoire pendant 2 heures :</p>
<p>Ils se sont fiancés,</p>
<p>Ensuite ils ont désengagés,</p>
<p>Puis ils pensent à se réengager... Une longue histoire.</p>
<p>J'ai commencé à leur poser la question « Pourquoi vous vous êtes désengagés à la base ? »</p>
<p>Ils m'ont raconté des choses comme « Il ne prenait plus de mes nouvelles »</p>
<p>« Elle ne me disait plus de paroles gentilles »</p>
<p>« Il n'aime pas venir passer du temps avec moi et ma famille »</p>
<p>Il fait ca... Elle fait ca... Il fait ca... Elle fait ca...</p>
<p>Puis j'ai demandé à chacun d'eux « Quelle est ta plus grande qualité, et quelles qualités cherches-tu chez ton partenaire dans la vie ? »</p>
<p>J'ai trouvé qu'ils convenaient extrêmement bien l'un à l'autre,</p>
<p>Ils sont faits l'un pour l'autre,</p>
<p>Leurs personnalités se complètent,</p>
<p>Et leurs familles sont faites pour s'entendre...</p>
<p>Même niveau social, même niveau de pratique de la religion, même niveau d'études, mêmes ambitions... Tout collait.</p>
<p>Mais d'où vient le problème ?</p>
<p>J'ai découvert que le souci est que les deux sont fainéants...</p>
<p>Aucun des deux ne veut dépenser son énergie,</p>
<p>Faire un pas en direction de l'autre...</p>
<p>Après s'être fiancés, ils sont restés flemmards,</p>
<p>Ils ne sont pas prêts à faire des efforts.</p>
<p>Comme plein de gens mariés ne veulent pas fournir des efforts...</p>
<p>Ca veut dire quoi concrètement ?</p>
<p>Je vais vous le dire sans détour : « L'amour dure seulement si on l'arrose avec des efforts »</p>
<p>Dépense ton énergie, occupe-toi de ton épouse / mari,</p>
<p>Prend de ses nouvelles,</p>
<p>Sortez ensemble,</p>
<p>Fais lui des surprises...</p>
<p>Ca s'appelle faire des efforts.</p>
<p>Les gens sont paresseux en réalité,</p>
<p>Arrose ta relation avec des sentiments,</p>
<p>Arrose ta relation avec des efforts physiques (en sortant ensemble... etc.),</p>
<p>Tu verras que l'amour restera en pleine forme entre vous deux.</p>
<p>Vous remarquez que les couples divorcent pour des choses insignifiantes ?
<p>Pour le bouton d'une chemise...</p>
<p>Pour une dispute sur du sel de trop dans le repas...</p>
<p>Pardonnez-moi le mot, mais c'est quoi ces puérilités ?</p>
<p>Pourquoi les gens divorcent pour des choses pareilles ?</p>
<p>Car ils n'ont pas fait d'efforts,</p>
<p>Quand on dépense beaucoup d'énergie pour obtenir une chose, on se rend compte de sa valeur, alors on veut la préserver pour toujours...</p>
<p>Dépense beaucoup d'énergie pour ton amour, il deviendra précieux à tes yeux, et tu feras toujours les efforts nécessaires pour le préserver.</p>
<p>Parce que tu as donné, parce que tu t'es sacrifié, parce que tu as dépensé ton temps et ton énergie.</p>
<p>Le temps que tu auras dépensé, sera cher pour toi, tes souvenirs seront importants, et la valeur de ton amour augmentera.</p>
<p>Tu me crois si je te dis que c'est la meilleure façon de préserver ton foyer ?</p>
<p>Fais beaucoup d'efforts, alors ta relation deviendra précieuse,</p>
<p>Et quand ta relation deviendra précieuse, tu feras toujours ce qu'il faut pour la préserver...</p>
<p>Quand vous allez vous disputer, et que tu seras sur le point de crier, tu te diras « Pourquoi je vais crier, pourquoi je vais me disputer... Et gâcher tous les efforts et le temps que j'ai dépensé pour construire mon amour ? »</p>
<p>Les 2 jeunes fiancés qui sont venus me voir, se disputaient pour des choses minuscules, le 1er élevait le ton... Le 2ème élevait encore plus le ton...</p>
<p>En réalité, s'ils avaient fait les efforts, au moment de la dispute ils se seraient dit « On a des souvenirs ensemble, on a passé beaucoup de temps pour construire notre relation... On ne va quand même pas abîmer notre effort. »</p>
<p>Un conseil à tous les fiancés, et tous les gens mariés :)</p>
<p>L'amour s'arrose en dépensant notre énergie,</p>
<p>Ne soyez pas paresseux,</p>
<p>Faites des efforts pour voir vos sentiments grandir, votre relation gagner en valeur, et le bonheur durer dans la maison pour toujours.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
