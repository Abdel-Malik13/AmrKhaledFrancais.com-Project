                                        <br>
                                        <p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<p>L'épisode d'aujourd'hui est sur l'art de se comporter avec les gens,</p>
<p>Comment peut-il y avoir un sourire d'espoir si on jette des pierres sur les gens ?</p>
<p>Tu me diras que tu ne jettes des pierres sur personne...</p>
<p>Parfois les paroles sont des pierres qui heurtent, qui blessent,</p>
<p>Elles ne blessent pas le corps et font saigner de l'extérieur,</p>
<p>Elles blessent le cœur et le font saigner de l'intérieur...</p>
<p>L'art de t'exprimer quand tu veux dire quelque chose aux autres,</p>
<p>D'une façon respectueuse,</p>
<p>D'une façon élégante,</p>
<p>D'une façon qui n'écorche pas les cœurs ni qui fait mal à entendre...</p>
<p>Quand notre Prophète Mohamed (bénédictions et paix soient sur lui) voulait transmettre un message il disait</p> <p><strong class="prophete">« Pourquoi certains font ceci et cela »</strong></p>
<p>Pourquoi y'a-t-il des gens qui font cette erreur ?</p>
<p>Jamais il ne dit « Toi, (en l'appelant par son prénom) pourquoi tu fais cette erreur ? »</p>
<p>Je vous raconte tout ca car j'ai vécu personnellement une expérience :</p>
<p>Je suis parti donner un discours devant un groupe de salariés d'une entreprise,</p>
<p>Des employés modestes, très gentils...</p>
<p>En arrivant sur place, la personne qui m'a invité m'a dit le sujet de ce discours doit être sur « l'intégrité »,</p>
<p>J'ai demandé « Y'a-t-il une raison particulière ? »</p>
<p>Il m'a répondu «Oui, car nos employés manquent d'intégrité, alors je veux leur rentrer dedans, pour qu'ils apprennent à devenir honnêtes »</p>
<p>Je lui ai dit « Mais tu as rassemblé les salariés, et tu veux que je monte leur parler d'honnêteté ? »</p>
<p>Il m'a répondu « L'honnêteté ne fait-elle pas partie de l'islam et le Prophète (bpsl) nous a incité à être intègres ? »</p>
<p>J'ai dit « Oui bien sûr »</p>
<p>Il m'a répondu « Alors c'est réglé, parle leur d'intégrité »</p>
<p>Je lui ai dit « Si je le fais de cette façon c'est comme si je leur disais directement : Vous n'êtes pas intègres, on devrait le faire d'une façon correcte en respectant leurs sentiments »</p>
<p>Il m'a répondu « Je ne sais rien, tout ce qui compte pour moi c'est que tu leurs parles d'intégrité »</p>
<p>En vérité le plus simple aurait été de leur dire : Dieu a dit, et le Prophète (bpsl) a dit... Les paroles parlant d'intégrité sont claires.</p>
<p>Si je leur disais : L'intégrité... L'intégrité... L'intégrité... Leurs visages seraient devenus rouges, ils auraient compris que je suis venu leur dire qu'ils sont malhonnêtes...</p>
<p>J'ai commencé à réfléchir et innover,</p>
<p>Comment faire... Comment faire... Comment faire...</p>
<p>Dieu m'a inspiré une idée,</p>
<p>Je vous dédicace cette idée pour qu'ensemble on l'applique avec tout le monde,</p>
<p>Je leur ai demandé si c'était possible d'avoir un tableau et un feutre...</p>
<p>Les employés sont venus s'asseoir et ils étaient nombreux,</p>
<p>Je leur ai dit « Que dites-vous si on marquait sur ce tableau tous les bons comportements dont on a besoin dans notre travail, dans nos usines et dans nos entreprises ? »</p>
<p>Ils ont cité à peu près 30 bons comportements : L'honnêteté, l'intégrité, le perfectionnisme, la miséricorde.... Etc.</p>
<p>Ils ont noté, noté, noté... sur le tableau,</p>
<p>Puis je leur ai dit « Et si on faisait une vente aux enchères ? »</p>
<p>(Remarquez cette belle méthode)</p>
<p>« Si dans notre poche il n y'avait que 100€, et que ca ne suffisait à acheter qu'un seul bon comportement, quel bon comportement on devrait acheter ? Celui dont on a le plus besoin dans notre travail...»</p>
<p>Les employés ont commencé à éliminer les comportements,</p>
<p>Pour en laisser 5,</p>
<p>Puis sur les 5 il fallait en sélectionner 3...</p>
<p>Et à la fin on s'est tous mis d'accord que le comportement dont on a le plus besoin au travail est l'intégrité...</p>
<p>Et j'ai commencé à parler d'intégrité en disant « En fonction de votre choix... »</p>
<p>:)</p>
<p>« En fonction du comportement que vous avez sélectionné,</p>
<p>Je vous parlerai aujourd'hui d'intégrité »</p>
<p>Et j'ai commencé à citer les versets du noble Coran, et les paroles du Prophète (bpsl)...</p>
<p>Et les employés étaient réactifs, certains pleuraient d'émotion,</p>
<p>Car je ne leur ai pas donné le sentiment de les bloquer dans un coin de la pièce et de leur jeter des pierres ou les punir...</p>
<p>Mais quand ils ont eux même choisi le sujet du discours,</p>
<p>Les versets du noble Coran les ont touchés,</p>
<p>Et les paroles du Prophète (bpsl) qui parlent d'intégrité les ont affectés...</p>
<p>Je vous raconte cette expérience pour dire : Si on apprend à toucher les cœurs de nos jeunes,</p>
<p>de nos salariés, de nos enfants d'une façon participative, sans blessures...</p>
<p>Si en même temps qu'on leur apprend des choses on dessine sur leurs visages un sourire d'espoir, le résultat sera très efficace et très émouvant.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
