                                        <br>
                                        <p>In The Name of Allah The Most Gracious The Most Merciful.</p>
<br>
<p>Welcome to a new smile of hope.</p>
<br>
<p>Today a smile of hope is about "bad habits". If you practise a bad habit and you can't give it up.</p>

<p>Do I have a method of "a smile of hope to know how to give it up ? Yes, I have a good innovation to help everyone to give up bad habits.</p>

<p>Bad habits such as smoking, drugs, being irritable, driving carelessly, being huffish, being frown and any bad habits like bad morals, bad behaviors. Any bad behavior you accustom to.</p>

<p>I will give you a model to know how to deal with these bad habits to give them up. I swear by Allah, fathers, mothers, young boys and girls, this idea is wonderful.</p>

<p>It is not my own but I knew it from a friend who lives in "shobrazangi" - a small village in Monefeya Governorate in Egypt. He told me that his son was seventeen - year old, he was very naughty.</p>

<p>He had done a lot of mistakes and bad manners like smoking, stopping practice sports, he couldn't run with his friends, he became meager and weak, he waked for along time till daylight and gets up in he afternoon, he became rude with his mother and irritable. He had got too many bad habits.</p>

<p>His father perplexed and didn't know how to solve this problem. Thereon he remembered an old and wise man (one of his relatives) who lived in the village. Although my friend lives in cairo with his son, he went to this man in the village and told him that his son is ill-mannered. Thereon the old man asked him to bring this son. So he told his son to go with him to the village but the boy refused. "Obey me" his fatther said. Then they went to the old man.</p>

<p>"We will go for a walk n the fields." the old man said to the boy. The boy agreed and they went. While they were walking, the old man pointed to some grass and told him that they were harmful and asked him if he could pick them up. The boy picked them easily.</p>

<p>Afterthat they saw a bush and he asked the boy to dismount it because it annoyed them and the boy dismounted it easily too. Then they passed a large tree. "can you dismount this large tree ? The old man asked. The boy dismounted it and sweltered. Afterthat they saw a larger tree. The boy dismounted it hardly. Finally they saw a palm tree, thereon the boy said, "No I can't, it is impossible, noway and nohow.".</p>

<p>"My dear son, our habits are like seeds, you can control them easily when they are seeds, you can dismount a bush easily but if you delay they will turn into palm trees and you will be powerless and unable to change them." the old man said.</p>

<p>My dear community, you can't accustom bad habit trough another bad habit but this happens when you have an idea or an image such ideas like "let's smoke, let's try drugs."</p>

<p>If this happens, you should challenge it. If you challenge it radically, you will give it up easily but if you challenge it eventually, it will be very difficult. When you have a bad idea, challenge it because if you didn't it will become a mettle, then a will, then an action and finally it will became a habit you can't change.</p>

<p>If you challenged it at first as it was a bush, you could easily dismount it but now after it had grown up you can't. Dear young boys ad girls, we can change our habits while they are small like bushes. Please when you have a bad idea, challenge it. Control it otherwhise it will control you. If it control you, you won't be able to smile "a smile of hope".</p>

<p>Thank you, peace and blessings be upon you.</p>
