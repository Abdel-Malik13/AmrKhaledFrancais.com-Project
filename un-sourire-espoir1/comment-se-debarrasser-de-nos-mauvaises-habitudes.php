                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>
<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout miséricordieux, le très miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir,</p>
<p>L'épisode d'aujourd'hui parle des mauvaises habitudes,</p>
<p>Si tu n'arrives pas à t'en débarrasser, dans le sourire d'espoir d'aujourd'hui tu trouveras la solution :)</p>
<p>Des mauvaises habitudes comme :</p>
<p>Fumer,</p>
<p>Les drogues,</p>
<p>La colère,</p>
<p>Conduire vite,</p>
<p>Etre dur aves les autres,</p>
<p>Avoir du mal à sourire,</p>
<p>Des mauvais comportements...</p>
<p>Je vais vous donner une méthode pour vous débarrasser de tout ca :</p>
<p>Je dédicace cette idée à tous les parents et à tous les jeunes,</p>
<p>En vérité ce n'est pas mon idée, mais celle d'un ami qui m'a raconté ceci</p>
<p>« J'ai un enfant de 17ans, qui commençait à avoir de mauvaises habitudes... Il s'est mis à fumer, il a abandonné le sport, sa santé allait de plus en plus mal...</p>
<p>Quand il jouait au foot avec ses amis, il s'essoufflait trop vite,</p>
<p>Il veillait toute la nuit jusqu'à 5h ou 6h du matin,</p>
<p>Il dormait la journée et se levait à 16h,</p>
<p>Il parlait à sa maman d'une façon pas très convenable...</p>
<p>Il s'énervait pour un rien,</p>
<p>Plein de mauvaises choses... »</p>
<p>Mon ami ne savait plus quoi faire, puis il s'est rappelé que dans le village de ses parents il y'avait un de ses proche,</p>
<p>Un homme assez âgé et plein de sagesse,</p>
<p>(Mon ami vivait avec son fils et sa famille en ville)</p>
<p>Il est parti voir le chef du village pour lui dire que son fils le fatigue beaucoup, il lui a raconté toutes ses mauvaises habitudes...</p>
<p>L'homme âgé lui a dit « Ramène le moi, je saurai le remettre sur le droit chemin »</p>
<p>Le père est donc reparti en ville dire à son fils « Tu pars passer du temps au village »</p>
<p>Le fils n'a pas apprécié, mais le père l'a forcé à partir...</p>
<p>Le chef de village à emmené le jeune marcher avec lui, pour regarder les récoltes...</p>
<p>Ils sont arrivés devant des herbes sauvages, Il lui a dit « Vois-tu ces herbes ? Elles nous créent trop de problèmes, elles nuisent aux récoltes, et ramènent des insectes parasites... Peux --tu les arracher ? »</p>
<p>Le jeune a facilement arraché ces herbes...</p>
<p>Ils ont continué à marcher, puis ils sont arrivés devant un petit arbre,</p>
<p>Le chef du village lui a dit « Peux-tu arracher ce petit arbre ? Il nous dérange dans cet endroit »</p>
<p>Le jeune l'a arraché assez facilement...</p>
<p>Ils ont remarché, puis sont arrivés devant un arbre plus grand, pareil le chef du village lui a demandé de l'arracher,</p>
<p>Le jeune a eu du mal, il a transpiré... Mais à la fin il a pu l'arracher...</p>
<p>Puis ils sont repassés devant un arbre plus grand... Et même histoire...</p>
<p>Le jeune commençait à en avoir marre...</p>
<p>Il a dépensé beaucoup d'énergie pour arracher l'arbre...</p>
<p>Puis ils sont arrivés devant un palmier,</p>
<p>Le chef du village lui a dit « Peux-tu arracher ce palmier ? »</p>
<p>Le jeune lui a répondu « Bien sur que non, ni avec mes mains, ni même avec du matériel, jamais je ne saurais l'arracher »</p>
<p>Le vieux sage l'a regardé en disant « Mon fils, nos mauvaises habitudes sont comme des graines, il est facile de les manier... Puis elles deviennent des herbes, tu peux facilement les arracher... Puis elles deviennent des arbres que tu peux arracher mais avec difficulté... Et plus tu mets du temps à les arracher plus elles grandissent... Le jour où elles deviennent comme des palmiers, tu seras impuissant devant elles... »</p>
<br>
<p>Mes chers, personne ne commence une mauvaise habitude par une mauvaise habitude...</p>
<p>Ca commence toujours par une pensée,</p>
<p>Une idée,</p>
<p>« Tu viens fumer ? »</p>
<p>« Ca te dit de tester ? »</p>
<p>Combat cette idée... Car si tu la combats à son début tu pourras facilement gagner...</p>
<p>Mais si tu la laisse se concrétiser... Ca devient plus compliqué...</p>
<br>
<p>Dis-toi que toutes les mauvaises habitudes commencent par une pensée,</p>
<p>La pensée se transforme en volonté,</p>
<p>« Ca te dit de fumer » devient « Je veux fumer »</p>
<p>Combats cette volonté sinon elle se transforme en acte...</p>
<p>« J'ai fumé »</p>
<p>Combats cet acte sinon il va se transformer en habitude...</p>
<p>« Je fume »</p>
<p>Et cette habitude devient difficile à abandonner...</p>
<p>Mais si tu avais combattu l'idée au départ tu aurais pu facilement la vaincre,</p>
<p>N'attendons pas de finir impuissants face à la gravité de nos mauvaises habitudes...</p>
<p>N'attendons pas de perdre notre sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et sa bénédiction.</p>
                                </article>
