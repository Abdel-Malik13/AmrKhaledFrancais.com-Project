                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est à l'approche de la fête des mères,</p>
<p>Nous avons organisé un concours sur notre page Facebook,</p>
<p>Pour choisir la maman exemplaire,</p>
<p>Il existe beaucoup de concours de mamans exemplaires,</p>
<p>Mais jamais on n'a vu un concours pareil sur internet,</p>
<p>Alors nous avons décidé d'organiser ce concours appelé « Notre maman ».</p>
<p>Et on va offrir des voyages d'Omra (petit pèlerinage) aux 20 mamans gagnantes.</p>
<p>Le principe du jeu c'est : Envoie l'histoire de ta maman, ou un message où tu décris à quel point ta maman est grande.</p>
<p>Nous avons été surpris de recevoir des milliers d'histoires...</p>
<p>Mes chers, nous avons reçu plus de 1200 messages en moins de 3 semaines.</p>
<p>Ça y est nous allons commencer à faire le tri, et choisir les gagnants,</p>
<p>Mais j'en parle dans cet épisode d'un « Sourire d'espoir » pour une autre raison...</p>
<p>J'ai demandé à ce qu'on me prépare un dossier avec les meilleures histoires,</p>
<p>Et quand j'ai commencé à les lire,</p>
<p>Mes larmes ont commencé à couler...</p>
<p>La plupart des histoires sont envoyées par des jeunes garçons et filles, pour parler de leurs mamans.</p>
<p>Ils disent des paroles touchantes,</p>
<p>Et je ne peux pas citer toutes les histoires dans cet épisode,</p>
<p>Mais chacun de ces jeunes raconte à quel point sa maman est grande,</p>
<p>Et ce qui rassemble toutes les histoires est 1 seul mot...</p>
<p>« Sacrifice ».</p>
<p>Le jure que je me suis mis à réfléchir au point en commun entre toutes ces histoires,</p>
<p>Lui, raconte comment sa maman a élevé 5 enfants après le décès de leur papa...</p>
<p>Et lui, raconte l'histoire de sa maman malade, et qui a supporté sa maladie pour élever ses enfants...</p>
<p>Et elle, raconte comment sa maman l'a soutenue quand elle avait des problèmes, et comment elle a tout sacrifié pour que sa fille réussisse dans sa vie.</p>
<p>Et lui... Et lui... Et lui...</p>
<p>Mes larmes coulaient,</p>
<p>Et j'avais envie de courir embrasser la main de ma maman,</p>
<p>Embrasser sa tête,</p>
<p>Et lui dire : Je t'aime.</p>
<p>J'ai été très touché en lisant ces histoires,</p>
<p>Et je me suis demandé : Quel est le meilleur titre dans l'histoire de l'humanité ?</p>
<p>Est-ce que c'est Général ?</p>
<p>Est-ce que c'est Professeur ?</p>
<p>Est-ce que c'est Docteur ?</p>
<p>Est-ce que c'est Ingénieur ?</p>
<p>Quel est le titre le plus prestigieux ?</p>
<p>J'ai trouvé que le plus grand titre dans l'histoire de l'humanité est : Maman.</p>
<p>Je t'aime maman.</p>
<p>S'il te plait, n'attends pas le jour de la fête des mères pour fêter ta maman,</p>
<p>Mais fête-la dès aujourd'hui,</p>
<p>Cherche à la satisfaire,</p>
<p>Embrasse sa main,</p>
<p>Embrasse sa tête,</p>
<p>Et sache que ta maman est la personnification d'un seul mot : « Sacrifice ».</p>
<p>Elle a tout sacrifié pour toi,</p>
<p>Ta maman avait beaucoup de rêves dans la vie, quand elle était jeune,</p>
<p>Mais elle a abandonné tous ses rêves quand tu es né,</p>
<p>Pour se consacrer à un seul projet : Toi.</p>
<p>Satisfais ta maman,</p>
<p>Et comme elle s'est sacrifiée,</p>
<p>Donne-lui de l'affection,</p>
<p>Donne-lui de la tendresse,</p>
<p>Et demande-lui de te faire des Prières,</p>
<p>Car les Prières de nos mamans sont le secret de notre bonheur dans ce bas-monde et dans l'au-delà.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
