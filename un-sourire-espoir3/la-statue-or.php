                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui vient de la Chine,</p>
<p>L'histoire s'est passée il y a plus de 2000 ans...</p>
<p>Quelle est l'histoire ?</p>
<p>Il y avait une région en Chine avec un grand nombre d'habitants,</p>
<p>Ils ont voulu faire quelque chose d'exceptionnel,</p>
<p>Pour laisser une trace qui durera des milliers d'années...</p>
<p>Qu'ont-ils fait ?</p>
<p>Ils ont construit une grande statue en or,</p>
<p>Les femmes ont donné une partie de leurs bijoux,</p>
<p>Tous les gens ont travaillé,</p>
<p>Et ils ont construit une grande statue en or sous la forme d'un être humain.</p>
<p>Mais à cette époque il y avait une armée appelée les Bermudes,</p>
<p>Les Bermudes attaquaient la Chine et sa région,</p>
<p>Les gens du village ont su que les Bermudes étaient à l'approche,</p>
<p>Comment vont-ils réagir ?</p>
<p>Ils ont décidé que la chose la plus chère et la plus précieuse chez eux, c'est cette statue en or,</p>
<p>Si les Bermudes la prennent,</p>
<p>On aura perdu notre argent,</p>
<p>Et on n'aura pas laissé de trace dans l'histoire...</p>
<p>Alors ils ont décidé de couvrir la statue avec de la boue,</p>
<p>Ils ont couvert la statue de haut en bas,</p>
<p>Comme ça elle ressemblera à une statue en terre.</p>
<p>Et en effet, ils ont recouvert la statue par de la boue solide,</p>
<p>Ça a séché.</p>
<p>Et quand les Bermudes sont arrivés, ils ont attaqué, ils ont détruit, ils ont brûlé, ils ont tué...</p>
<p>Ils n'ont rien laissé,</p>
<p>Ils ont tué tous les habitants du village,</p>
<p>Et ils ont cherché des choses à prendre ou à voler,</p>
<p>Alors ils n'ont trouvé dans ce village que cette grande statue en terre,</p>
<p>Ça n'avait aucune valeur pour eux,</p>
<p>Ils l'ont laissée, et ils sont partis.</p>
<p>Des centaines d'années vont passer,</p>
<p>Et 2000 ans plus tard, le gouvernement chinois décide de chercher et prendre soin des monuments du pays,</p>
<p>Puis pendant les recherches, ils ont découvert une grande statue en terre dans un ancien village,</p>
<p>Comme cette statue avait plus de 2000 ans,</p>
<p>Ils ont décidé de l'emmener à Pékin, la capitale...</p>
<p>Les ouvriers sont partis sur place,</p>
<p>Et en voulant déplacer la statue, quelque chose avait l'air anormale...</p>
<p>Un des ingénieurs a pris une pioche, et il a commencé à creuser dans la statue,</p>
<p>Une couleur dorée apparaissait petit à petit,</p>
<p>Ils ne comprenaient pas...</p>
<p>Ils ont continué à gratter,</p>
<p>Et ils ont découvert que la statue était entièrement en or.</p>
<p>Le gouvernement chinois a été mis au courant,</p>
<p>Et la statue a été transportée à la capitale,</p>
<p>Et elle a été posée dans une des grandes places à Pékin.</p>
<p>Et sous la statue, ils ont marqué un mot très important.</p>
<p>Je raconte cette histoire, uniquement pour cette phrase...</p>
<p>Ils ont marqué : « À l'intérieur de la boue, il y a un trésor »</p>
<p>Que veulent-ils dire ?</p>
<p>Et pourquoi je raconte cette histoire ?</p>
<p>Ça veut dire : Dieu a créé chaque être humain avec de la boue,</p>
<p>Mais Il a mis à l'intérieur de chacun un trésor en or,</p>
<p>Des capacités que Dieu a mises au fond de toi.</p>
<p>Dieu (Exalté Soit-Il) dit :</p>
<p><strong>{ Ainsi qu'en vous-mêmes. N'observez-vous donc pas? }</strong></p>
<p>Sourate 51 - Ad-Dâriyât : Qui Éparpillent (verset 21)</p>
<p>Mes chers, à l'intérieur de la boue il y a un trésor,</p>
<p>Au fond de toi, il y a un trésor, mais tu n'y fais pas attention...</p>
<p>Dieu t'a donné des capacités immenses,</p>
<p>Tu as une grande capacité de réussir,</p>
<p>Tu peux réaliser de grandes choses dans ta vie, la vie de ta famille, et la vie de ceux qui t'entourent,</p>
<p>Tu peux avoir un grand succès,</p>
<p>Et une grande influence,</p>
<p>Car à l'intérieur de toi, tu possèdes un trésor, mais tu ne t'en rends pas compte.</p>
<p>Tu ne vois que la boue.</p>
<p>Si tu comprends ça, tu verras un sourire d'espoir dans ta vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
