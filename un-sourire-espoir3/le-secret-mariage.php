                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'un seul mot...</p>
<p>Un mot appelé « Le respect »,</p>
<p>Laissez-moi vous raconter l'histoire :</p>
<p>En 1964 la construction du haut barrage d'Assouan (en Égypte) a commencé,</p>
<p>Et à cette époque, un ingénieur appelé Hamdy Afifi venait d'obtenir son diplôme,</p>
<p>Puis il est parti finir ses études en Allemagne,</p>
<p>Il a passé 4 ans là-bas,</p>
<p>Ensuite il est revenu travailler dans la construction du barrage...</p>
<p>Celui qui me raconte l'histoire c'est sa petite fille, qui s'appelle Nour,</p>
<p>Elle raconte :</p>
<p>Quand mon grand-père est parti faire ses études en Allemagne,</p>
<p>Il a fait connaissance avec une fille allemande,</p>
<p>Il l'a beaucoup beaucoup beaucoup aimée...</p>
<p>Il a voulu l'épouser et faire sa vie avec elle,</p>
<p>Et quand il a fini ses 4 années d'études, et qu'il devait revenir en Égypte,</p>
<p>Cette fille allemande devait faire un choix,</p>
<p>Venir s'installer avec lui en Égypte,</p>
<p>Ou bien rester en Allemagne...</p>
<p>Elle a décidé « Je vais venir vivre en Égypte, et je vais faire ma vie avec lui »</p>
<p>Une relation très très très solide est née,</p>
<p>Et cette relation durera 55 ans.</p>
<p>Nour me raconte « J'ai demandé à ma grand-mère, quel est le secret de la longévité de votre mariage ?</p>
<p>Pourquoi votre mariage est une grande réussite ? »</p>
<p>Sa grand-mère lui a répondu par un seul mot...</p>
<p>Et je dédicace ce mot à tous les époux et à toutes les épouses...</p>
<p>« Le respect ».</p>
<p>« On se respectait beaucoup »,</p>
<p>Puis je ne le dédicace pas uniquement aux couples,</p>
<p>Je le dédicace à tous nos pays...</p>
<p>« Le respect », c'est la base des foyers,</p>
<p>« Le respect », c'est la base de la vie en général.</p>
<p>Nour a répondu à sa grand-mère « Oui, mais explique-moi plus en détail... Tu veux dire quoi par 'respect' ? »</p>
<p>Sa grand-mère lui a dit « Je vais te raconter une histoire :</p>
<p>Au début de notre mariage,</p>
<p>Une fois ton grand-père m'a crié dessus...</p>
<p>Il m'a grondée sévèrement,</p>
<p>Il m'a engueulée,</p>
<p>Et en réalité j'étais fautive,</p>
<p>Je me suis excusée,</p>
<p>Mais il n'a pas accepté mes excuses,</p>
<p>Il a continué à me crier dessus...</p>
<p>Alors je suis partie le voir, et je lui ai dit :</p>
<p>Écoute, notre maison a été fondée sur le respect, de ton côté et du mien,</p>
<p>Entre nous il n'y a ni voix élevée, ni insultes,</p>
<p>Entre nous il y a un grand respect,</p>
<p>Puis je lui ai raconté une histoire. »</p>
<p>Nour a demandé à sa grand-mère « Quelle histoire ? »</p>
<p>Elle lui a répondu « Je lui ai raconté qu'une fois un papa avait emmené son fils dans une région montagneuse,</p>
<p>Il y avait une grande vallée,</p>
<p>Alors un phénomène d'écho s'y produisait...</p>
<p>Le petit ignorait ce qu'était un écho,</p>
<p>Il escaladait la montagne avec son papa,</p>
<p>Il s'est fait mal, alors il a dit « Aieeee ! »</p>
<p>Il a entendu l'écho répéter « Aieeee ! »</p>
<p>Le petit a pensé qu'il y avait quelqu'un d'autre dans la montagne,</p>
<p>Il a demandé « Qui es-tu ? »</p>
<p>(Le papa riait, car le petit ne savait pas que c'était juste un écho)</p>
<p>L'écho lui a répondu « Qui es-tu ? »</p>
<p>Le petit s'est énervé, il a dit « Je te demande qui es-tu ??? »</p>
<p>L'écho lui a répondu « Je te demande qui es-tu ??? »</p>
<p>Le petit s'est emporté en disant « Tu n'es qu'un lâche !!! »</p>
<p>L'écho lui a répondu « Tu n'es qu'un lâche !!! »</p>
<p>Le petit allait répondre,</p>
<p>Mais son papa l'a arrêté,</p>
<p>Il lui a dit « Au lieu de lui dire ca... Dis-lui que tu le respectes »</p>
<p>Le petit a dit « Je te respecte »</p>
<p>L'écho lui a alors répondu « Je te respecte »</p>
<p>Son papa lui a dit « La vie fonctionne de cette manière...</p>
<p>La vie te redonne autant que tu lui donnes,</p>
<p>En fonction de ce que tu dis, l'écho te le rendra...</p>
<p>La vie te respectera autant que tu te respectes toi-même »</p>
<p>Parfois mes chers, l'écho revient après plusieurs années,</p>
<p>Tu aides une personne âgée,</p>
<p>Et le jour où tu vieillis quelqu'un t'aidera...</p>
<p>Tu prends soin de tes parents,</p>
<p>Et plus tard, tes enfants prendront soin de toi...</p>
<p>L'écho concerne tous les domaines de notre vie,</p>
<p>L'écho a besoin que tu lui donnes du respect...</p>
<p>Le respect mes chers,</p>
<p>Le respect entre les maris et les épouses,</p>
<p>Le respect dans nos maisons,</p>
<p>Le respect dans nos pays...</p>
<p>À ce moment, nous obtiendrons un sourire d'espoir.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
