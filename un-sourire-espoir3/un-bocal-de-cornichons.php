                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'une très belle valeur, avec laquelle on doit vivre :</p>
<p>Ne mange jamais d'argent illicite (haram),</p>
<p>Fais grandir tes enfants avec de l'argent halal,</p>
<p>Chaque poil de tes enfants doit pousser avec de l'argent licite.</p>
<p>L'épisode d'aujourd'hui montre à quel point tu dois faire attention à ca,</p>
<p>Et montre que tu peux à la fois offrir la meilleure scolarisation, et donner à tes enfants de beaux principes.</p>
<p>Vous savez qu'on a un problème chez nous ?</p>
<p>On se concentre sur la scolarisation de nos enfants : Physique, Chimie, Géographie...</p>
<p>Révise bien,</p>
<p>Mange bien,</p>
<p>Dors à l'heure,</p>
<p>Et apprends bien à l'école...</p>
<p>Mais qui apprend à ses enfants les principes, comme il leur apprend les mathématiques et la géographie ?</p>
<p>Des bons comportements,</p>
<p>Et qui les éduque avec ces bons comportements,</p>
<p>Et donne une grande importance à ca ?</p>
<p>L'histoire d'aujourd'hui est celle d'un salarié modeste qui s'appelle Oncle Ibrahim :</p>
<p>Oncle Ibrahim est analphabète, il ne sait ni lire ni écrire...</p>
<p>C'est un homme très modeste,</p>
<p>Et avait un fils, et le rêve de sa vie c'est de scolariser son enfant, l'envoyer à l'université, et en faire quelqu'un qui réussit dans sa vie,</p>
<p>Mais avec la réussite dans la vie et le savoir... Il tenait à lui transmettre une valeur très pure : Ne mange jamais d'argent illicite.</p>
<p>Ne mange surtout pas de haram.</p>
<p>L'enfant grandissait, et son papa qui voulait l'envoyer à l'université, et dépenser beaucoup d'argent pour ses études... A fait une chose étonnante :</p>
<p>Il a fait venir son fils,</p>
<p>Puis il a pris un bocal de cornichons et l'a vidé...</p>
<p>Puis il a lavé le bocal,</p>
<p>Et dedans il a mis la 1ère pièce d'argent...</p>
<p>Et il lui a dit « Ce bocal de cornichons, est ce qui paiera tes études.</p>
<p>On va se priver,</p>
<p>On va manger des haricots toute la semaine,</p>
<p>Et on mangera de la viande une fois par semaine...</p>
<p>Mais il faut que tu suives des études,</p>
<p>Et je ne veux pas que tu suives uniquement des études, je veux aussi que tu apprennes à ne manger que du halal,</p>
<p>Regarde ce bocal,</p>
<p>Chaque jour on y mettra le peu d'argent qu'on a économisé... »</p>
<p>Puis oncle Ibrahim, a montré sa main à son fils et lui a dit</p>
<p>« Cette main n'a jamais touché d'argent illicite,</p>
<p>Je préfère qu'elle soit coupée au lieu qu'elle touche de l'argent haram...</p>
<p>Tu vas suivre des études, et tu vas manger de l'argent de ce bocal,</p>
<p>On va se serrer la ceinture,</p>
<p>Et on va supporter la faim... Mais tu feras des études supérieures. »</p>
<p>Le fils raconte :</p>
<p>« J'ai été scolarisé, puis je suis entré à l'université,</p>
<p>Et je voyais mes parents se priver,</p>
<p>Pour économiser quelques sous, et les mettre dans le bocal à cornichons.</p>
<p>Et jamais je n'ai oublié l'image du bocal, et jamais je n'ai oublié l'image de la main de mon papa »</p>
<p>Il a grandi,</p>
<p>Il a réussi,</p>
<p>Il a quitté la maison des ses parents,</p>
<p>Il s'est marié,</p>
<p>Il a eu des enfants,</p>
<p>Puis ses enfants ont grandi, ils avaient 4 ou 5 ans,</p>
<p>Un jour il les a emmenés voir leur grands parents dans leur maison modeste,</p>
<p>Et il a vu que le bocal de cornichons était toujours à sa place...</p>
<p>Mais le bocal était vide.</p>
<p>Il est parti voir son papa et lui a dit « Je ne saurais pas éduquer mes enfants comme tu as su m'éduquer,</p>
<p>Ne laisse pas le bocal vide s'il te plait,</p>
<p>Remplis-le...</p>
<p>Car tu ne le remplissais pas d'argent, mais tu le remplissais de valeurs...</p>
<p>Remplis-le pour que je ramène mes enfants chaque weekend,</p>
<p>Et qu'ils voient le bocal,</p>
<p>Et dis-leur comme tu m'as dit 'Je préfère qu'elle soit coupée au lieu qu'elle touche de l'argent haram...'</p>
<p>Papa, ce que tu m'as appris est plus fort que 1000 cours de religion,</p>
<p>Et plus fort que 1000 conférences sur les valeurs et l'intégrité »</p>
<p>Le bocal de cornichons, et la main qui n'a jamais touché d'argent illicite.</p>
<p>Mes chers, ce n'est pas suffisant d'envoyer vos enfants à l'école,</p>
<p>Ce n'est pas suffisant de surveiller leurs notes, pour savoir combien ils ont eu en mathématiques ou en langues...</p>
<p>C'est beaucoup plus important de leur transmettre une valeur,</p>
<p>Comme l'intégrité.</p>
<p>Comme l'a fait oncle Ibrahim, le modeste analphabète,</p>
<p>Avec son bocal de cornichons, il a dessiné un sourire d'espoir chez les générations à venir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
