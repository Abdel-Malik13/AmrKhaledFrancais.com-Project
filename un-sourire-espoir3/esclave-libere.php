                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Le sourire d’espoir d’aujourd’hui vient de la Mecque,</p>
<p>C’est une histoire qui s’est passée à la Mecque il y a plus de 100 ans,</p>
<p>En vérité je ne connaissais pas cette histoire,</p>
<p>Je l’ai découverte Pendant que je faisais une Omra (petit pèlerinage).</p>
<p>En feuilletant un journal, j’ai lu « Pourquoi tu as placé ta lumière derrière toi ? ».</p>
<p>C’était le titre de l’article, et c’est ce qui m’a poussé à lire l’histoire.</p>
<p>Pourquoi tu as placé ta lumière derrière toi…</p>
<p>Ça signifie quoi ?</p>
<p>Je ne comprenais pas ce que ça voulait dire,</p>
<p>Alors j’ai lu l’histoire,</p>
<p>C’est une histoire vraie :</p>
<p>Un homme était riche, il avait beaucoup d’argent,</p>
<p>Et à cette époque, il y a plus de 100 ans, il y avait encore des esclaves,</p>
<p>Quelqu’un qui travaille pour son maître, et qui lui appartient…</p>
<p>Cet homme était riche,</p>
<p>Et il avait un serviteur qui travaillait pour lui,</p>
<p>Il était à son service du matin au soir.</p>
<p>Cet homme était religieux…</p>
<p>Et quand l’heure de la Prière du Fajr (avant le lever du jour) arrivait,</p>
<p>Son serviteur le réveillait,</p>
<p>Il lui ramenait un récipient rempli d’eau, pour qu’il fasse ses ablutions,</p>
<p>Puis il allumait une lampe,</p>
<p>Et il marchait dans la rue devant son maître, pour lui éclairer la route jusqu’à la mosquée…</p>
<p>Car à cette époque il n’y avait pas d’électricité dans les rues,</p>
<p>Il faisait noir, puis le sol était rempli de cailloux etc.</p>
<p>Alors il tenait la lampe, et il avançait devant son maître, jusqu’à ce qu’ils arrivent à la mosquée.</p>
<p>Cet esclave a servi son maître pendant 15 ou 20 ans…</p>
<p>Un jour pendant qu’ils marchaient vers la mosquée,</p>
<p>Cet homme riche a gardé le silence,</p>
<p>Puis il lui a dit « Écoute Saïd (le prénom de l’esclave),</p>
<p>Dans le testament que j’ai laissé à mes enfants,</p>
<p>J’ai écrit que tu devras être libéré après mon décès.</p>
<p>Pour te récompenser de m’avoir servi pendant ces 20 ans. »</p>
<p>Mais la chose étonnante, c’est que le jeune esclave n’a pas sauté de joie…</p>
<p>Il ne l’a pas remercié,</p>
<p>Il n’a pas embrassé sa main,</p>
<p>Il n’a pas dit 1 seul mot,</p>
<p>Il n’a fait aucun commentaire…</p>
<p>Le lendemain, l’homme riche s’est levé,</p>
<p>L’esclave a allumé la lampe,</p>
<p>Ils sont sortis,</p>
<p>Et là surprise…</p>
<p>Le jeune esclave marchait derrière son maître, et pas devant,</p>
<p>L’homme riche, ne voyait rien devant lui,</p>
<p>Et il ne comprenait pas pourquoi son serviteur était derrière…</p>
<p>Il était très étonné,</p>
<p>Il lui a dit « Qu’est ce qui t’arrive Saïd ?</p>
<p>Pourquoi tu n’éclaires pas devant moi ? »</p>
<p>Il lui a répondu « C’est toi mon maître, qui a placé ta lumière derrière toi ».</p>
<p>Tu m’as promis de me libérer après ta mort,</p>
<p>Tu ne m’as pas donné ma liberté pendant que tu es vivant,</p>
<p>Alors tu m’as poussé à souhaiter ta mort, pour que j’obtienne ma liberté…</p>
<p>Au lieu que je souhaite que tu restes vivant en bonne santé, et que je fasse des Prières pour te remercier.</p>
<p>J’allais de toute façon continuer à te servir,</p>
<p>Car je fais ça depuis plus de 20 ans,</p>
<p>Et jamais tu ne m’as parlé de ce sujet…</p>
<p>Mais quand tu m’en as parlé, tu m’as poussé à souhaiter ta mort,</p>
<p>Pourquoi tu as placé ta lumière derrière toi, et pas devant toi ?</p>
<p>L’homme riche a été touché,</p>
<p>Et il lui a dit « Saïd, tu es libre dès cet instant »,</p>
<p>Et Saïd lui a répondu « Et moi je suis ton domestique loyal dès cet instant ».</p>
<p>Qu’est ce que je veux dire par cette histoire ?</p>
<p>Beaucoup de gens placent leurs lumières derrière eux…</p>
<p>Ils disent « Quand je vais grandir, je ferai ça… »</p>
<p>« Quand je mourrai, ils pourront distribuer mon argent aux pauvres et faire des dons associations caritatives… »</p>
<p>Mais pourquoi ne pas les aider avec ton argent pendant que tu es en vie ?</p>
<p>Place ta lumière devant toi,</p>
<p>Pourquoi tu mets ta lumière derrière toi ?</p>
<p>Et beaucoup de personnes reportent au lendemain…</p>
<p>Elles disent « Quand je grandirai, je ferai des belles choses »</p>
<p>« Dans 10 ans, je ferai du bénévolat… »</p>
<p>« Quand je serai à la retraite, je donnerai de mon temps… »</p>
<p>Pourquoi placer ta lumière derrière toi ?</p>
<p>Place ta lumière devant toi, pour qu’elle t’éclaire ta route…</p>
<p>Pourquoi on ne pense pas à faire le Bien maintenant ?</p>
<p>Pourquoi on repousse le Bien à des années lointaines ?</p>
<p>Fais le Bien maintenant,</p>
<p>Pour que la lumière soit devant toi,</p>
<p>Et pour que tu puisses voir un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
