                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de la sagesse…</p>
<p>Autrefois les gens méditaient sur la vie,</p>
<p>Et trouvaient des sagesses qui éveillent les cerveaux,</p>
<p>Des sagesses qui vivent longtemps,</p>
<p>Car les paroles sages restent vivantes,</p>
<p>Contrairement aux paroles normales qui s’oublient très vite…</p>
<p>La personne sage, et qui dit une parole sage, reste dans la mémoire des gens pendant des milliers d’années.</p>
<p>Ali Ibn Abou Taleb (Un des compagnons du Prophète (bénédictions et paix soient sur lui)) fait partie de ces personnes.</p>
<p>Un jour, quelqu’un lui a demandé « Qui est la plus forte créature de Dieu ? »</p>
<p>Ali Ibn Abou Taleb a réfléchi pendant très longtemps…</p>
<p>Afin de trouver qui est la créature la plus forte de Dieu.</p>
<p>Il s’est mis à faire ce qu’on appelle aujourd’hui du Brainstorming (Remue-méninges en français),</p>
<p>Une technique de résolution créative de problème, de collecte d’idées en groupe.</p>
<p>C’est vrai, il a commencé à réfléchir à haute voix,</p>
<p>Il a dit :</p>
<p>« La créature la plus forte de Dieu est le fer »</p>
<p>Puis Ali a gardé le silence…</p>
<p>Et il a dit</p>
<p>« Mais j’ai regardé le feu, et j’ai vu que le feu faisait fondre le fer…</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est le feu. »</p>

<p>« Puis je me suis rendu compte que l’eau éteignait le feu…</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est l’eau. »</p>

<p>« Mais j’ai vu le ciel, et je me suis rendu compte que les nuages transportaient l’eau…</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est les nuages. »</p>

<p>« Puis j’ai vu le vent, et je me suis rendu compte que le vent déplaçait les nuages…</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est le vent. »</p>

<p>« Mais j’ai vu les montagnes, et je me suis rendu compte que les montagnes empêchaient le vent d’avancer…</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est les montagnes. »</p>

<p>« Mais j’ai vu les humains, et je me suis rendu compte que les humains creusent les montagnes… Et peut-être même les déplacent (comme s’il parlait de ce qu’on voit aujourd’hui)</p>
<p>Et qu’ils transportent l’eau, le feu et le fer…</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est l’être humain. »</p>

<p>« Puis j’ai remarqué que l’être humain, souffre des soucis et des angoisses jour après jour…</p>
<p>Alors je me suis demandé : Est-ce possible que les soucis et les angoisses soient la créature la plus forte de Dieu ? »</p>

<p>« Mais j’ai trouvé que les soucis et les inquiétudes, la joie et la tristesse, la force et la vitalité… Viennent du cœur.</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est le cœur. »</p>

<p>« Puis j’ai trouvé que le cœur ne s’apaise que par l’invocation de Dieu.</p>
<p>Alors je me suis dit que la créature la plus forte de Dieu est l’invocation. »</p>

<p>« Puis j’ai lu ce verset du Coran :</p>
<p><strong>{ Ceux qui ont cru et dont les cœurs s'apaisent à l'évocation d'Allah ;</strong></p>
<p><strong>N’est-ce point par l'évocation d'Allah que les cœurs s'apaisent ? }</strong></p>
<p>(Sourate 13 - Ar-Ra'd : Le Tonnerre - Verset 28) »</p>

<p>« Mon cœur s’est alors rassuré,</p>
<p>Car l’être humain, son cœur, et l’invocation sont les plus fortes créatures de Dieu. »</p>

<p>Qu’elle est belle la sagesse d’Ali Ibn Abou Taleb…</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
