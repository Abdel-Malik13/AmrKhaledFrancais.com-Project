                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>En hiver,</p>
<p>Quand il fait froid,</p>
<p>Tous les gens cherchent à se réchauffer...</p>
<p>Quand il fait très froid, les besoins en couvertures augmentent,</p>
<p>Les couvertures d'hiver.</p>
<p>L'épisode d'aujourd'hui parle des couvertures d'hiver,</p>
<p>Suite à la vague de froid, nous avons lancé une campagne,</p>
<p>Et beaucoup de personnes ont participé,</p>
<p>Beaucoup de gens ont envoyé des couvertures aux pauvres et aux nécessiteux.</p>
<p>Et quelqu'un appelé Ahmed Abd Al Fattah m'a envoyé son histoire par email,</p>
<p>Son histoire est très touchante et très belle :</p>
<p>Il dit que sa maman est décédée (paix à son âme),</p>
<p>Mais une semaine avant son décès il raconte :</p>
<p>Nous habitons au centre ville du Caire,</p>
<p>Une nuit il faisait très froid et il pleuvait des cordes,</p>
<p>Ma maman est partie faire un tour au balcon,</p>
<p>Et elle a vu un homme dormir en bas de l'immeuble...</p>
<p>Il s'est mis à l'abri de la pluie et du froid,</p>
<p>Et il ne portait que des habits légers...</p>
<p>Ma maman a eu pitié de cet homme,</p>
<p>Il faisait extrêmement froid dehors,</p>
<p>Alors elle a appelé le gardien de l'immeuble,</p>
<p>Elle lui a donné 2 couvertures,</p>
<p>Et elle lui a dit « Prends ces 2 couvertures, et donne-les à l'homme qui dort en bas pour qu'il se réchauffe tout de suite ».</p>
<p>Elle était très soucieuse et très concernée,</p>
<p>Elle a dit à ses enfants « Comment on peut se réchauffer et laisser cet homme dans le froid dehors ?</p>
<p>Dieu ne sera pas satisfait de nous si on ne fait rien ».</p>
<p>Le lendemain matin, le gardien de l'immeuble est monté voir ma maman,</p>
<p>Et il lui a rendu les 2 couvertures...</p>
<p>Elle s'est très fâchée,</p>
<p>Elle lui a dit « Pourquoi tu me les rends ? Je jure que je ne vais pas les reprendre ! »</p>
<p>Le gardien lui a répondu « L'homme d'hier est parti, il a passé la nuit en bas, puis au matin il s'en est allé »</p>
<p>Elle lui a dit « Il va peut-être revenir ce soir »</p>
<p>Mais l'homme n'est pas revenu...</p>
<p>Le lendemain, le gardien de l'immeuble est encore monté rendre les couvertures,</p>
<p>Elle lui a dit « Non, je ne vais pas les reprendre,</p>
<p>Garde-les pour toi et tes enfants,</p>
<p>On a tous froid en ce moment »</p>
<p>Le gardien a pris les 2 couvertures,</p>
<p>Et Ahmed raconte :</p>
<p>1 semaine plus tard, ma maman se sentait de plus en plus fatiguée...</p>
<p>Comme si elle sentait son heure approcher,</p>
<p>Le matin, ma maman m'a déposé moi et ma sœur à l'école,</p>
<p>Puis elle est revenue à la maison,</p>
<p>Mon grand frère lui a ouvert la porte,</p>
<p>Et elle s'est écroulée...</p>
<p>Sa maman est décédée.</p>
<p>Mais à cet instant, le grand frère ignorait qu'elle était décédée,</p>
<p>Il a appelé en urgence le gardien de l'immeuble,</p>
<p>Il lui a dit « Il faut qu'on l'emmène rapidement à l'hôpital »</p>
<p>Et en la transportant vers la voiture,</p>
<p>Le grand frère a voulu la couvrir à cause du froid dehors,</p>
<p>Il a dit au gardien de l'immeuble « Donne-moi n'importe quoi pour la réchauffer »</p>
<p>Le gardien lui a ramené les 2 couvertures...</p>
<p>Gloire à Dieu.</p>
<p>Elle a couvert un être humain dans ce bas monde,</p>
<p>Alors elle a été couverte avec les 2 mêmes couvertures dans son chemin pour retrouver Dieu...</p>
<p>Parfois Dieu nous envoie des messages,</p>
<p>Des messages pour dire que l'humanité et la miséricorde doivent nous être rendues,</p>
<p>Et elles doivent nous êtres rendues rapidement,</p>
<p>Et qu'il faut les voir de nos propres yeux,</p>
<p>Comme dans cette histoire.</p>
<p>Je raconte cette histoire, et je veux aussi dire que j'ai un autre ami,</p>
<p>Et comme il faisait très froid, il est sorti de chez lui avec des couvertures,</p>
<p>Pour les donner à quelqu'un qui dormait dans la rue...</p>
<p>Puis en arrivant, il a vu une voisine à lui,</p>
<p>Les 2 faisaient la course,</p>
<p>Les 2 voulaient se garer rapidement,</p>
<p>Pour descendre en premier et remettre les couvertures...</p>
<p>Chacun d'eux disait « Moi le premier... Moi le premier... »</p>
<p>C'est magnifique mes chers.</p>
<p>Nous concurrencer dans le fait de donner,</p>
<p>C'est mieux que de nous concurrencer dans le fait de prendre,</p>
<p>Tout le monde veut être le premier à prendre...</p>
<p>Mais qui a envie d'être le premier à donner ?</p>
<p>Nous concurrencer dans les devoirs... Avant de nous concurrencer dans les droits...</p>
<p>Un sourire d'espoir pour chaque être humain qui veut être le 1er dans la générosité, dans l'humanité, dans la miséricorde...</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
