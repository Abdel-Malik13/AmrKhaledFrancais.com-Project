                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui est celle d'un jeune qui s'appelle Mahmoud Raafat,</p>
<p>Il est originaire d'Alexandrie,</p>
<p>Le papa de Mahmoud est décédé quand il était petit,</p>
<p>Il n'avait que sa maman et sa sœur...</p>
<p>Ils n'avaient pas d'argent.</p>
<p>Le père de la famille n'était plus là,</p>
<p>Et les ressources financières étaient très faibles...</p>
<p>Mais Mahmoud a fait une très belle chose,</p>
<p>Mahmoud n'a pas baissé les bras face à sa situation,</p>
<p>Mahmoud ne s'est pas dit « Le destin a voulu que mon papa décède... Et le destin a voulu que ma situation financière soit difficile... Alors je ne peux pas faire grand-chose »</p>
<p>Il ne s'est pas mis à pleurer comme certains jeunes le font...</p>
<p>Et il ne s'est pas transformé en quelqu'un qui a la haine envers la société...</p>
<p>Mahmoud est toujours souriant,</p>
<p>Je le connais personnellement et il a toujours le sourire,</p>
<p>Il est très actif,</p>
<p>Il a très envie de réussir,</p>
<p>Qu'a fait Mahmoud ?</p>
<p>Il a décidé de travailler et de gagner de l'argent.</p>
<p>Je dédicace cette histoire à tous les jeunes qui souffrent du chômage,</p>
<p>Et à ceux qui disent « Je n'ai pas de moyens » ou « On ne me donne pas de travail »</p>
<p>Qu'a fait Mahmoud ?</p>
<p>Mahmoud est âgé aujourd'hui de 22 ans,</p>
<p>Et il a commencé à travailler quand il avait 14 ans...</p>
<p>Au début il achetait et revendait des téléphones portables,</p>
<p>Il vendait à ses amis et à ses connaissances...</p>
<p>Il gagnait un peu... Il rachetait des portables... Il les revendait...</p>
<p>Puis à 16 ans, il a pensé à importer des produits qui sont utiles aux gens,</p>
<p>Il s'est mis à réfléchir « De quoi les gens ont besoin ? »</p>
<p>Alors une idée lui est venue,</p>
<p>Vous connaissez le cric pour soulever les voitures ?</p>
<p>C'est difficile à faire quand une fille tombe en panne,</p>
<p>Ou quand ca arrive en pleine nuit...</p>
<p>Alors il a pensé « Il y a un vrai besoin »</p>
<p>Il a importé de l'étranger un nouveau type de cric, pour soulever une voiture en moins de 60 secondes...</p>
<p>Ca s'appelle « Air Jack »</p>
<p>Une sorte de ballon qu'on gonfle avec de l'air,</p>
<p>Tu appuies sur un bouton, et il gonfle tout seul...</p>
<p>Il les a importés,</p>
<p>Et il est parti les vendre aux grossistes,</p>
<p>Il a gagné 25.000 euros...</p>
<p>Puis il a pris les 25.000 euros, il est parti voir sa maman et lui a dit « On les donne à ma sœur pour son mariage »</p>
<p>Ca y est il n'avait plus d'argent...</p>
<p>Il s'est mis à réfléchir « Comment je fais pour me relancer ? »</p>
<p>Il a appris à parler le langage des signes...</p>
<p>Puis il est parti proposer ses services aux agences de tourisme,</p>
<p>Il leur a dit « Si des groupes de touristes viennent, et que parmi eux il y a des sourds ou des muets,</p>
<p>Je peux leur faire la traduction en arabe ou en anglais »</p>
<p>Alors on l'appelait pour accompagner des groupes de touristes,</p>
<p>Il s'est trouvé un nouveau travail...</p>
<p>C'est quoi ca ?</p>
<p>Il a quel âge ce jeune ?</p>
<p>Aujourd'hui il a 22 ans...</p>
<p>Il a commencé en vendant des téléphones portables,</p>
<p>Puis il a importé des produits utiles aux gens,</p>
<p>Ensuite il a appris le langage des signes, il a travaillé avec les agences de tourisme et il a gagné de l'argent...</p>
<p>Il s'est aussi mis à faire de la musculation,</p>
<p>Et il a été sélectionné pour faire partie de l'équipe nationale égyptienne,</p>
<p>Alors il a commencé à gagner de l'expérience dans le domaine,</p>
<p>Et il prenait l'argent qu'il gagnait dans son travail avec les agences de tourisme, et l'investissait en achetant des vêtements de sport,</p>
<p>Il allait voir les grossistes et les revendait...</p>
<p>Et aujourd'hui il fait des études de design graphique, pour devenir un des meilleurs designers en Égypte...</p>
<p>C'est quoi tout ca ?</p>
<p>Je vous raconte son histoire pour vous dire : Mes chers, travaillez !</p>
<p>Si tu es jeune, ne baisse pas les bras devant ta situation,</p>
<p>Mahmoud a gagné beaucoup d'argent,</p>
<p>Et Mahmoud est toujours positif,</p>
<p>Mahmoud est productif...</p>
<p>Mahmoud n'a pas le temps de se disputer avec les autres,</p>
<p>Il n'a pas le temps pour entrer dans des polémiques...</p>
<p>Ce jeune travaille directement,</p>
<p>Il veut réussir dans sa vie,</p>
<p>Dieu dit dans le Coran</p>
<p><strong>{ C'est Lui qui vous a soumis la terre: parcourez donc ses grandes étendues. Mangez de ce qu'Il vous fournit. }</strong></p>
<p>Sourate 67 - Al-Mulk : La Royauté (verset 15)</p>
<p>Comme si c'est une promesse de Dieu,</p>
<p>Si tu parcours la terre, et si tu creuses dans la roche, Il te fera manger de Sa subsistance.</p>
<p>Un sourire d'espoir pour chaque personne comme Mahmoud,</p>
<p>Qui s'active dans la vie,</p>
<p>Qui travaille,</p>
<p>Qui fait des efforts,</p>
<p>Qui creuse dans la roche...</p>
<p>Dieu sera toujours avec ce genre de personnes,</p>
<p>Il récompensera leurs efforts,</p>
<p>Et leur donnera un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
