                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui commence avec un coup de fil,</p>
<p>Un jeune écrivain m’a téléphoné,</p>
<p>Il est originaire d’Assiout (une ville en Égypte, à 320 km de la capitale)</p>
<p>Il m’a dit « Dr. Amr, je suis très triste »</p>
<p>Je lui ai demandé « Qu’est ce qui t’arrive ? »</p>
<p>Il m’a dit « Je suis parti visiter le Caire,</p>
<p>Et j’ai trouvé qu’on vendait de faux livres,</p>
<p>Avec les vrais noms des écrivains marqués dessus,</p>
<p>Mais avec une fausse maison d’édition.</p>
<p>Ils sont vendus sur le trottoir, 4 fois moins chers que les livres originaux.</p>
<p>N’y a-t-il pas Dr. une chose appelée les droits de propriété intellectuelle ?</p>
<p>Chaque être humain qui invente une chose, devrait être celui qui la commercialise à sa manière, n’est ce pas ?</p>
<p>Copier sans autorisation ça stoppe la créativité, et les créateurs arrêtent de travailler, n’est ce pas ? »</p>
<p>En vérité ses paroles m’ont fait mal,</p>
<p>Il a continué en disant « Il existe des jeunes dont c’est devenu le métier…</p>
<p>Ils copient les livres,</p>
<p>Ils volent les droits d’auteurs,</p>
<p>Et ils considèrent ça comme une bonne chose,</p>
<p>Ils pensent qu’ils diffusent la culture,</p>
<p>Alors qu’au contraire, ça stoppe la créativité, plus personne ne voudra écrire de nouveaux livres. »</p>
<p>En réalité ce qu’il m’a dit m’a rappelé une histoire,</p>
<p>Une histoire qui dit : Ne mange jamais les efforts de quelqu’un d’autre, même s’il est décédé depuis 200 ans…</p>
<p>Imaginez,</p>
<p>Pas seulement quand l’auteur est vivant…</p>
<p>L’histoire vient d’Espagne,</p>
<p>Un artiste peintre vivait au 18ème siècle,</p>
<p>Il a dessiné un tableau qui est considéré comme étant le plus célèbre en Espagne.</p>
<p>C’est un tableau historique, qui raconte l’histoire d’une grande famille espagnole,</p>
<p>C’est une œuvre immense, qui va du sol jusqu’au plafond,</p>
<p>Et les espagnols sont très fiers de cette peinture.</p>
<p>Alors un artiste peintre de notre époque, a décidé de reproduire ce tableau à l’identique…</p>
<p>Quand il a fini son tableau, personne ne pouvait faire la différence entre l’original et la copie,</p>
<p>Les mêmes couleurs,</p>
<p>La même précision,</p>
<p>Les mêmes détails,</p>
<p>C’est un génie !</p>
<p>Il a fait une copie exacte.</p>
<p>Les gens venaient lui dire « On voudrait l’acheter,</p>
<p>C’est sûr que son prix sera moins élevé que l’œuvre originale du 18ème siècle »</p>
<p>Mais l’artiste peintre avait un problème de conscience…</p>
<p>« Voler les efforts d’un artiste qui est décédé il y a 200 ans…</p>
<p>Ce serait du vol,</p>
<p>Même si je vends mon tableau à un prix moins élevé que l’original,</p>
<p>Ça reste de la tricherie,</p>
<p>Ça reste le vol du travail de quelqu’un d’autre… »</p>
<p>Cet homme espagnol n’est pas musulman,</p>
<p>Et on ignore quelle est sa religion,</p>
<p>Mais il a senti que c’était du vol,</p>
<p>Il a senti que c’était immoral.</p>
<p>Qu’a-t-il fait ?</p>
<p>Il a fait une chose très étonnante :</p>
<p>Il a ramené un sceau de peinture jaune clair,</p>
<p>À l’opposé des couleurs foncées du tableau…</p>
<p>Puis il a versé la peinture au milieu du tableau,</p>
<p>Il ne l’a pas détruit,</p>
<p>Mais il a rajouté un défaut,</p>
<p>Pour dire « Ce n’est pas l’original ».</p>
<p>Vous imaginez ?</p>
<p>Il a eu le courage de faire ça,</p>
<p>Un immense tableau du sol jusqu’au plafond,</p>
<p>Il a passé plusieurs mois à le dessiner,</p>
<p>Pour faire une copie exacte de l’original,</p>
<p>Mais ça ne se fait pas de voler les efforts de quelqu’un d’autre…</p>
<p>Il a versé le sceau de peinture jaune exactement au milieu du tableau,</p>
<p>Pour que le défaut soit très visible.</p>
<p>Les gens ont apprécié ce qu’il a fait…</p>
<p>Résultat : Il est devenu un des plus célèbres artistes espagnols contemporains,</p>
<p>Grâce à ses principes.</p>
<p>Et son tableau a gagné encore plus en beauté,</p>
<p>J’ai vu son œuvre à Madrid, et son prix a atteint des sommets.</p>
<p>Car ce n’est plus un simple tableau…</p>
<p>C’est devenu un tableau à l’intérieur duquel il y a des principes.</p>
<p>C’est devenu un tableau qui contient une grande valeur morale,</p>
<p>Une valeur appelée : Ne vole pas les efforts de quelqu’un d’autre, pour qu’un sourire d’espoir reste dans nos pays.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
