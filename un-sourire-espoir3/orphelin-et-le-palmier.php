                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui est arrivée à l'époque du Prophète (bénédictions et paix soient sur lui),</p>
<p>Un jeune orphelin est venu se plaindre au Prophète (bpsl)...</p>
<p>Ce jeune lui a dit « Je possède un petit jardin,</p>
<p>C'est ma source de revenus,</p>
<p>Et j'ai voulu construire une muraille autour de mon jardin.</p>
<p>Pendant que je construisais la muraille, un palmier m'a bloqué,</p>
<p>Ce palmier appartient à mon voisin »</p>
<p>Le palmier de son voisin était courbé, donc il l'empêchait de finir la construction du mur,</p>
<p>« Je lui ai alors demandé de me donner son palmier,</p>
<p>Pour pouvoir finir la construction de la muraille,</p>
<p>Le voisin m'a répondu 'Non, bien sûr que je ne vais pas te laisser mon palmier !' »</p>
<p>Le jeune lui a demandé « Est-ce que je peux te l'acheter ? »</p>
<p>L'homme a refusé, il lui a dit « Je ne vais pas te vendre mon palmier, et je ne vais pas te le donner non plus »</p>
<p>Le jeune était bloqué, il n'avait pas de solution...</p>
<p>Le Prophète (bpsl) a demandé au voisin de venir,</p>
<p>Le voisin est arrivé,</p>
<p>Le Prophète (bpsl) lui a dit <strong class="prophete">« Ce jeune dit ça et ça... »</strong></p>
<p>Le voisin lui a répondu « Oui c'est vrai Ô Messager de Dieu »</p>
<p>Le Prophète (bpsl) lui a dit <strong class="prophete">« Soit tu lui vends ce palmier, soit tu le lui donnes »</strong></p>
<p>L'homme lui a répondu « Non Ô Messager de Dieu, je ne veux ni le lui vendre, ni le lui donner »</p>
<p>Le Prophète (bpsl) lui a dit...</p>
<p><strong class="prophete">« Vends-lui ce palmier, et tu auras un palmier au Paradis,</strong></p>
<p><strong class="prophete">Celui qui avance sous ce palmier (à dos de cheval) met 100 ans (tellement il est grand) »</strong></p>
<p>Et c'était la grande surprise...</p>
<p>Cet homme a refusé d'échanger son palmier contre un palmier au Paradis.</p>
<p>Et il est très beau le fait que le Prophète (bpsl) ne l'ait pas obligé,</p>
<p>Et il est très beau le fait que personne le l'ait grondé,</p>
<p>Personne ne lui a fait la morale en disant « Si on te donne une chose au Paradis, tu n'as pas le droit de la refuser ! »</p>
<p>Non... Regardez au contraire la liberté et l'indulgence du Prophète (bpsl) dans ses relations avec les autres.</p>
<p>Quand cet homme a refusé, le Prophète (bpsl) a gardé le silence,</p>
<p>Qu'elle est belle la politesse du Prophète (bpsl)...</p>
<p>Je lui ai proposé le Paradis et il a refusé,</p>
<p>C'est son droit,</p>
<p>Il est libre.</p>
<p>C'est là qu'un compagnon appelé Abou Addah'dah, a rejoint la discussion,</p>
<p>Il a dit « Ô Messager de Dieu,</p>
<p>Si moi j'achète ce palmier, et que je le donne à ce jeune,</p>
<p>Est-ce que j'aurai un palmier au Paradis ?</p>
<p>Sous lequel je peux marcher pendant 100 ans ? »</p>
<p>Le Prophète (bpsl) lui a répondu <strong class="prophete">« Oui Abou Addah'dah »</strong></p>
<p>Abou Addah'dah a regardé le voisin, et lui a dit « Connais-tu mon jardin qui est à cet endroit ? »</p>
<p>Abou Addah'dah en possédait 5 ou 6... Il a choisi un de ses plus beaux jardins,</p>
<p>Il lui a dit « Connais-tu mon jardin qui est à cet endroit à Médine ? »</p>
<p>Le voisin lui a répondu « Et qui ne connait pas ce jardin ?</p>
<p>C'est vrai que tu possèdes plein de jardins,</p>
<p>Mais celui là est un des meilleurs à Médine,</p>
<p>Ses palmiers produisent les meilleures dattes de la ville »</p>
<p>Abou Addah'dah lui a alors dit « Que dis-tu si je te donne mon jardin en échange de ton jardin (qui a beaucoup moins de valeur) ? »</p>
<p>Le voisin lui a demandé « Tu parles sérieusement ? »</p>
<p>Abou Addah'dah lui a répondu « Oui »</p>
<p>Le voisin lui a dit « Bien sûr que j'accepte ! »</p>
<p>Le voisin a pris le jardin d'Abou Addah'dah,</p>
<p>Abou Addah'dah a pris le jardin du voisin, et il a offert le palmier au jeune orphelin.</p>
<p>Puis il a demandé au Prophète (bpsl) « Et maintenant Ô Messager de Dieu, qu'est ce que je vais obtenir ? »</p>
<p>Le Prophète (bpsl) lui a répondu <strong class="prophete">« Maintenant, ce n'est pas un palmier qui t'attend au Paradis,</strong></p>
<p><strong class="prophete">Mais un jardin au Paradis où il y a 600 palmiers »</strong></p>
<p>La beauté de l'histoire réside dans le fait de ne pas avoir forcé le voisin,</p>
<p>On ne lui a pas dit « Si tu veux le Paradis, alors tu es obligé de faire ça ! »</p>
<p>Puis il y a une 2ème beauté dans cette histoire :</p>
<p>La récompense que Dieu donne dans l'au-delà en échange de ce que nous donnons dans ce bas-monde.</p>
<p>Mes chers, si le Prophète (bpsl) a dit précisément à Abou Addah'dah ce qu'il va obtenir au Paradis en échange de son geste,</p>
<p>Dans d'autres paroles, il a promis à chaque personne qui aide un pauvre, qu'elle va beaucoup obtenir comme récompense,</p>
<p>Il a dit qu'une bouchée (de pain) devient dans l'au-delà comme la montagne d'Uhud (en récompense).</p>
<p>Le Prophète (bpsl) a bien dit ça n'est ce pas ?</p>
<p>Aidez les pauvres mes chers, pas avec un palmier,</p>
<p>Mais au moins avec de la nourriture,</p>
<p>Donnez-leur de quoi manger.</p>
<p>Car comme a dit le Prophète (bpsl) <strong class="prophete">« La bouchée devient aussi grande que la montagne d'Uhud »</strong>.</p>
<p>Un sourire d'espoir pour chaque personne qui donne du bonheur aux pauvres.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
