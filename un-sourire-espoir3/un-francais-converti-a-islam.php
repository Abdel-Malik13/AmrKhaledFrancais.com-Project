                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui commence en France et se termine en Afrique...</p>
<p>À Paris il y avait un homme marocain appelé tonton Ibrahim,</p>
<p>Tonton Ibrahim avait un magasin où il vendait des produits alimentaires,</p>
<p>Ce magasin était sous un immeuble,</p>
<p>Et dans un appartement de l'immeuble, vivait une famille française,</p>
<p>Ils n'étaient pas arabes, et ils étaient athées, ils n'avaient pas de religion.</p>
<p>Ils n'avaient aucune croyance religieuse.</p>
<p>Et tonton Ibrahim était bien sûr musulman.</p>
<p>Cette famille avait un enfant de 7 ans...</p>
<p>Ce petit garçon avait l'habitude de venir faire des courses au magasin de tonton Ibrahim,</p>
<p>Et chaque fois qu'il venait, il attendait que tonton Ibrahim regarde ailleurs... Puis il volait un chocolat.</p>
<p>Il faisait ça à chaque fois, il a pris cette mauvaise habitude, il prenait un chocolat en cachette et partait sans le payer.</p>
<p>Un jour le petit garçon est venu faire quelques courses, mais il a oublié de prendre le chocolat,</p>
<p>Puis en partant, tonton Ibrahim l'a appelé...</p>
<p>Il lui a dit « Tu as oublié ton chocolat, tiens prends-le »</p>
<p>L'enfant était choqué...</p>
<p>Quelle catastrophe ! Il savait depuis toujours que je volais le chocolat.</p>
<p>Tonton Ibrahim lui a donné un chocolat et lui a dit « Est-ce que tu me promets de ne plus voler quelque chose dans ta vie ? »</p>
<p>Le petit garçon a pris le chocolat,</p>
<p>Il est parti,</p>
<p>Mais il a été très touché par le comportement de tonton Ibrahim.</p>
<p>Les bons comportements...</p>
<p>Cet homme plein de bonté...</p>
<p>Les jours ont passé,</p>
<p>Et tonton Ibrahim est décédé.</p>
<p>Et quand il est décédé, ses enfants ont trouvé une boîte où il a laissé tous ses papiers et tous les objets qu'il a légués...</p>
<p>Puis dans la boîte il a laissé un petit livre,</p>
<p>Avant de mourir il leur a dit « Ce petit livre, doit être remis au petit garçon qui vient faire les courses au magasin »</p>
<p>Le petit garçon a reçu le livre,</p>
<p>Il l'a ouvert,</p>
<p>Et il a vu que c'était écrit en arabe,</p>
<p>Bien sûr il ne savait pas lire l'arabe, il ne lisait que le français.</p>
<p>Alors il est parti voir un de ses camarades à l'école,</p>
<p>Un garçon tunisien,</p>
<p>Il lui a demandé « Peux-tu me lire ce qu'il y a écrit dans ce livre ? »</p>
<p>Son camarade lui a lu 2 pages,</p>
<p>Il lui a demandé « C'est quoi ce livre ? »</p>
<p>Son camarade lui a répondu « C'est le noble Coran, c'est le livre des musulmans »</p>
<p>Le petit garçon a décidé de devenir musulman,</p>
<p>Et la raison était tonton Ibrahim,</p>
<p>Il s'est converti à l'islam,</p>
<p>Il a appris la langue arabe,</p>
<p>Et le Coran que lui a laissé tonton Ibrahim, ne quittait plus sa poche...</p>
<p>Il a grandi,</p>
<p>Puis un jour en tournant les pages, il a trouvé dans la dernière page une carte géographique dessinée à la main par tonton Ibrahim,</p>
<p>C'était une carte du continent Africain,</p>
<p>Et il avait dessiné une flèche avec marqué « Le Message doit être ici »</p>
<p>L'Afrique a besoin du Message des musulmans...</p>
<p>Gad (son prénom) a décidé de voyager en Afrique,</p>
<p>Et il est effectivement parti au Kenya,</p>
<p>(C'est une histoire vraie)</p>
<p>Il s'est installé au Kenya, en Ouganda et dans cette région...</p>
<p>Et il y est resté pendant 20 ans,</p>
<p>Il parlait aux gens de l'islam,</p>
<p>Il transmettait le Message...</p>
<p>Et des tribus ont commencé à l'écouter,</p>
<p>Puis il est devenu un des plus grands prédicateurs au Kenya et en Ouganda.</p>
<p>Il est décédé à l'âge de 51 ans,</p>
<p>Gad.</p>
<p>Mais pourquoi est-il devenu musulman ?</p>
<p>Jamais tonton Ibrahim ne lui a parlé de l'islam...</p>
<p>Pourquoi est-il devenu musulman ?</p>
<p>Jamais tonton Ibrahim ne lui a dit « Tu n'es pas musulman » ou « Tu vas aller en enfer » ou ce genre de choses...</p>
<p>Jamais il ne lui a parlé de religion.</p>
<p>Tonton Ibrahim n'a fait qu'une seule chose : « Les bons comportements »...</p>
<p>Tonton Ibrahim l'a traité avec indulgence,</p>
<p>Tonton Ibrahim l'a traité avec humanité.</p>
<p>Mes chers, celui qui veut faire découvrir notre religion aux autres,</p>
<p>Doit le faire avec ses comportements... Avant toute autre chose.</p>
<p>1000 conférences religieuses n'égalent pas 1 seul bon comportement.</p>
<p>Un sourire d'espoir pour représenter l'islam avec de bons comportements.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
