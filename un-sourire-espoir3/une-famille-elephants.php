                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle de la famille,</p>
<p>Et comment préserver la famille,</p>
<p>Et comment avoir une famille solide, unie, avec des rapports chaleureux,</p>
<p>Car nos enfants peuvent partir dans de mauvais chemins, et des catastrophes peuvent se produire si la famille n'est pas unie.</p>
<p>L'histoire du jour a été filmée,</p>
<p>C'est un reportage qui est passé sur National Geographic :</p>
<p>C'est arrivé dans un village en Inde,</p>
<p>Les habitants du village vivaient d'agriculture,</p>
<p>Et à côté du village se trouvait une jungle.</p>
<p>Des éléphants y vivaient...</p>
<p>Puis comme vous savez les éléphants vivent en famille,</p>
<p>Il y a les grands parents, puis les parents, puis les petits...</p>
<p>Le problème est que chaque nuit, les éléphants viennent au village pour manger les récoltes des agriculteurs,</p>
<p>Les parents des éléphants guident tout le groupe, et les autres les suivent,</p>
<p>Le lendemain les habitants du village se réveillent et trouvent leurs récoltes détruites,</p>
<p>Ils ont essayé par tous les moyens d'empêcher les éléphants de venir,</p>
<p>Ils ont mis des grillages,</p>
<p>Mais les éléphants les écrasaient avec leurs pates, et venaient manger les récoltes...</p>
<p>Ils ont mis du piment fort pour dissuader les éléphants de manger les récoltes,</p>
<p>Ca n'a servi à rien...</p>
<p>Alors ils ont pris une décision dangereuse,</p>
<p>Ils ont décidé de tuer les grands éléphants,</p>
<p>Car ce sont eux qui guident les autres...</p>
<p>Ils ont appliqué leur idée,</p>
<p>Ils ont tué les grands éléphants du groupe,</p>
<p>Et ils pensaient que le problème était réglé...</p>
<p>Mais les petits éléphants sont devenus violents,</p>
<p>Ils n'attaquaient plus les récoltes,</p>
<p>Mais ils attaquaient le village,</p>
<p>Ils attaquaient les humains,</p>
<p>Ils détruisaient les maisons,</p>
<p>Les habitants du village vivaient dans la terreur,</p>
<p>Ils ont demandé au gouvernement indien de trouver une solution.</p>
<p>Alors un responsable au gouvernement a dit</p>
<p>« La solution est que ces petits éléphants doivent avoir des parents »</p>
<p>Ca veut dire quoi ?</p>
<p>« On va faire venir de grands éléphants d'Afrique,</p>
<p>On les laissera s'habituer les uns aux autres,</p>
<p>Jusqu'à ce qu'ils forment une grande famille,</p>
<p>Les petits éléphants sentiront qu'ils ont des parents qui les cadrent, alors ils vont se calmer. »</p>
<p>Ils lui ont répondu « C'est quoi ces bêtises ? »</p>
<p>Il leur a dit « Je parle sérieusement, j'ai étudié ce sujet »</p>
<p>La suite a été filmée,</p>
<p>De grands éléphants d'Afrique ont été lâchés dans la jungle avec les petits,</p>
<p>Et au bout d'un moment les attaques contre les maisons ont cessé.</p>
<p>Les petits éléphants se sont calmés,</p>
<p>Désormais ils n'attaquaient que les récoltes et les légumes...</p>
<p>Le reportage s'est terminé sur ca,</p>
<p>Mais à la fin ils ont écrit cette phrase très importante :</p>
<p>« Soit tu construis une famille, soit tu construis une prison »</p>
<p>Dans une société, soit tu construis des familles unies,</p>
<p>Soit tu construis des prisons, car les crimes vont augmenter si les parents éduquent mal leurs enfants...</p>
<p>Tu veux des criminels ? Ou bien tu veux des familles solides ?</p>
<p>Faites attention à vos enfants mes chers,</p>
<p>Éduquez-les bien,</p>
<p>Donnez-leur de l'affection, préservez-les,</p>
<p>Donnez une grande importance à votre famille,</p>
<p>Car l'alternative ce sont des prisons.</p>
<p>Nous voulons un sourire d'espoir tant que nos familles sont solides.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
