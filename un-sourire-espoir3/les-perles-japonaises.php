                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle d’une histoire du siècle dernier…</p>
<p>L’idée de l’histoire est : Développe ton hobby, car c’est le secret de ta réussite.</p>
<p>Je le jure mes chers,</p>
<p>Si quelqu’un développe son loisir, il lui ramènera de l’or,</p>
<p>Mais il faut juste qu’il poursuive jusqu’au bout, et qu’il ne se lasse pas au milieu.</p>
<p>À tous les parents : Ne sous-estimez jamais les talents de vos garçons et de vos filles.</p>
<p>On dit tous à nos enfants « Arrête de jouer, et révise »</p>
<p>C’est bien qu’il révise (que personne ne me comprenne mal),</p>
<p>Mais peut-être que son hobby sera la raison de sa réussite dans la vie plus tard…</p>
<p>Peut-être que ce talent c’est un don que Dieu lui a fait,</p>
<p>Un des 99 noms de Dieu est <strong>{Al-Wahhāb}</strong> (Celui qui donne immensément sans rien recevoir en retour)</p>
<p>Si Dieu a donné à ton enfant un talent,</p>
<p>Pourquoi tu voudrais l’étouffer ?</p>
<p>Pourquoi ne pas en prendre soin ?</p>
<p>C’est peut-être ce talent le secret du bien et de la réussite de ton enfant.</p>
<p>L’histoire d’aujourd’hui s’est passée le siècle dernier au Japon,</p>
<p>Il s’appelle : Mikimoto Kōkichi,</p>
<p>C’est un homme japonais,</p>
<p>Il vit à la campagne,</p>
<p>C’est quelqu’un de modeste,</p>
<p>Il n’a pas poursuivi sa scolarité,</p>
<p>Il est né dans un village,</p>
<p>Et son papa était un homme pauvre, qui vendait du riz cuit dans un chariot,</p>
<p>Et parfois il aidait son papa,</p>
<p>Il poussait le chariot et vendait le riz cuit aux gens…</p>
<p>Il a continué à faire ça jusqu’à l’âge de 18 ans.</p>
<p>Puis il avait un hobby,</p>
<p>Lequel ?</p>
<p>La plongée.</p>
<p>Il aimait plonger, et chercher des perles.</p>
<p>Il aimait vendre les perles qu’il pêchait, et sa grande passion c’était de collectionner les plus rares.</p>
<p>Puis il a commencé à se poser une question à l’intérieur de lui-même…</p>
<p>Quelle question ?</p>
<p>Il partait voir ses amis qui avaient fait de grandes études,</p>
<p>Ou qui avaient des informations sur le monde sous-marin,</p>
<p>Et il leur demandait : « Pourquoi les perles se trouvent à l’intérieur de coquilles ?</p>
<p>Et pourquoi dans certaines coquilles il n’y a pas de perles ? »</p>
<p>Ils lui ont appris que quand un objet indésirable passe à l'intérieur de la coquille (un grain de sable, l’écaille d’un poisson, un insecte… ), Pour défendre son intérieur fragile, la coquille réagit en entourant l'objet d'une couche de carbonate de calcium CaCO3… Ce mélange est appelé nacre, après 1, 2 ou 3 ans ça se transforme en perle.</p>
<p>(Gloire à Dieu, c’est comme ça que les perles naissent).</p>
<p>Une idée folle est alors née dans le cerveau de cet homme,</p>
<p>Quelle idée folle ?</p>
<p>Il s’est dit : Si c’est la façon Divine et naturelle d’obtenir des perles, alors on pourrait fabriquer des perles artificielles,</p>
<p>En prenant des coquilles, et en y introduisant un objet indésirable,</p>
<p>On mettra les coquilles dans des cages sous l’eau,</p>
<p>Et on va provoquer leurs réactions pour qu’elles se défendent,</p>
<p>On obtiendra ainsi des perles artificielles.</p>
<p>À cette époque, personne au monde ne savait ce qu’était des perles artificielles,</p>
<p>Cet homme a appliqué son idée,</p>
<p>Il a pris plein de coquilles,</p>
<p>Il a fait entrer des objets indésirables,</p>
<p>Il les a mises dans des cages dans l’océan,</p>
<p>Il a attendu 2 ans…</p>
<p>Il a ressorti les coquilles,</p>
<p>Et en les ouvrants il n’a rien trouvé.</p>
<p>On lui a dit que la température de l’eau change,</p>
<p>Et que les perles devaient être dans une certaine température élevée…</p>
<p>Il a refait son expérience dans des endroits tièdes,</p>
<p>Mais ça n’a rien donné…</p>
<p>Il a pensé que le problème venait du fait qu’il mettait trop de coquilles dans la même cage,</p>
<p>Alors il a réduit le nombre des coquilles, et il a augmenté le nombre des cages,</p>
<p>Pareil sans résultat…</p>
<p>Combien de temps il a continué à tenter ?</p>
<p>Pendant 15 ans.</p>
<p>Sans résultat.</p>
<p>Il a décidé d’abandonner,</p>
<p>Il en a eu marre,</p>
<p>Il a décidé de redevenir vendeur ambulant de riz…</p>
<p>Mais sa femme lui a dit « Non ! Il faut que tu poursuives »</p>
<p>Mais il n’avait plus d’argent pour faire ses expériences…</p>
<p>Sa femme lui a dit « Moi je vais pousser le chariot, et je vais vendre le riz. Et toi poursuis ton projet »</p>
<p>Après 15 ans, il a re-testé sur 5000 coquilles,</p>
<p>Il s’est dit que peut-être le problème venait de l’endroit où il faisait entrer les éléments indésirables,</p>
<p>Il a changé l’endroit…</p>
<p>Il a remis des coquilles dans des cages,</p>
<p>Il les a laissées sous l’eau pendant 2 ans,</p>
<p>Et un jour avec sa femme, ils ont ouvert les coquilles pour vérifier,</p>
<p>Et dans une des coquilles ils ont trouvé une perle…</p>
<p>C’est devenu la 1ère perle artificielle au monde,</p>
<p>C’était un 28 Septembre,</p>
<p>Et dans ses sociétés et ses ateliers (il est devenu un des hommes les plus riches du monde, fabriquant de joyaux et de bijoux)</p>
<p>Mikimoto a rendu chaque 28 Septembre un jour de congé pour ses employés...</p>
