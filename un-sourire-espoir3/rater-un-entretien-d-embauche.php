                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui dit que parfois l’être humain se fâche pour des choses…</p>
<p>Il déteste des choses…</p>
<p>Tu peux pleurer et t’énerver,</p>
<p>Alors que ces choses sont le sommet du bien pour toi.</p>
<p>C’est vrai :</p>
<p><strong>{ Or, il se peut que vous ayez de l’aversion pour une chose alors qu’elle vous est un bien. Et il se peut que vous aimiez une chose alors qu’elle vous est mauvaise. C’est Allah qui sait, alors que vous ne savez pas. }</strong></p>
<p>(Coran - Sourate 02 - Al-Baqara : La Vache verset 216)</p>
<p>Tu ne sais pas,</p>
<p>Alors quand une chose t’arrive dans la vie,</p>
<p>Ne te fâche pas trop,</p>
<p>Et n’exagère pas non plus dans la joie.</p>
<p>L’histoire d’aujourd’hui est un peu ancienne,</p>
<p>Elle date de 1958.</p>
<p>C’est un jeune belge,</p>
<p>Il s’appelle Pierre Culliford,</p>
<p>Il est pauvre financièrement,</p>
<p>Il cherchait du travail,</p>
<p>Et il a enfin eu l’occasion de passer un entretien d’embauche,</p>
<p>Pour travailler dans quoi ?</p>
<p>Comme assistant dans un cabinet dentaire.</p>
<p>Il était très très très content d’avoir décroché cet entretien,</p>
<p>Il n’a pas encore accepté,</p>
<p>Mais rien que le fait de passer un entretien d’embauche…</p>
<p>Il s’est beaucoup beaucoup beaucoup préparé,</p>
<p>Il avait envie de travailler,</p>
<p>Et comme il n’avait pas de grands diplômes,</p>
<p>Travailler comme assistant dans un cabinet dentaire à Bruxelles, c’était une occasion en or.</p>
<p>Il était extrêmement motivé,</p>
<p>Le lendemain matin, il s’est réveillé très tôt,</p>
<p>Il a mis ses habits,</p>
<p>Mais il n’arrivait pas à trouver son portefeuille…</p>
<p>Dedans il y avait sa carte d’identité et son argent,</p>
<p>Il a retourné la chambre de haut en bas,</p>
<p>Sans succès…</p>
<p>Il a cherché partout chez lui,</p>
<p>Il n’a rien trouvé…</p>
<p>Le temps passait très vite,</p>
<p>Il avait envie de casser sa montre pour que le temps s’arrête…</p>
<p>Il ne restait que 40 min avant l’entretien,</p>
<p>Qu’est ce qu’il devrait faire…</p>
<p>Continuer à chercher, ou bien partir sans son portefeuille ?</p>
<p>De chez lui jusqu’au lieu de l’entretien, il faut prendre un Bus, et le trajet dure 30 min…</p>
<p>D’un côté s’il partait sans sa carte d’identité, on la lui demandera à l’accueil,</p>
<p>Et de l’autre côté, s’il continuait à chercher, il arrivera en retard à son premier entretien.</p>
<p>Il a transpiré…</p>
<p>Et dans la panique, il a décidé d’y aller sans son portefeuille,</p>
<p>Il est parti frapper chez son voisin,</p>
<p>Il lui a emprunté de l’argent pour le transport,</p>
<p>Mais il y a eu un nouveau problème…</p>
<p>Le Bus est arrivé en retard,</p>
<p>Il est arrivé en retard à son rendez-vous,</p>
<p>Et en entrant, la personne à l’accueil lui a dit « Tu voudrais qu’on t’embauche alors que tu ne respectes pas les rendez-vous ?</p>
<p>Il n’y a pas de travail pour toi »</p>
<p>Il est rentré chez lui en pleurant…</p>
<p>Il pleurait, et il sentait que le monde s’était refermé.</p>
<p>Il s’est retrouvé dans une situation difficile,</p>
<p>Il devait désormais accepter n’importe quel autre travail,</p>
<p>On lui a proposé un travail modeste dans une société de dessinateurs,</p>
<p>Il devait faire du nettoyage,</p>
<p>Et s’il manquait de la peinture, il devait partir en acheter,</p>
<p>Etc.</p>
<p>Il était obligé d’accepter ce travail,</p>
<p>Et il détestait ce qu’il faisait.</p>
<p>Ils l’ont surnommé Peyo dans le studio,</p>
<p>Il était là tous les jours,</p>
<p>Il regardait les dessinateurs travailler,</p>
<p>Puis à force de les regarder, il s’est dit : Pendant que je suis assis à ne rien faire, pourquoi ne pas dessiner sur un tableau…</p>
<p>Il a commencé à dessiner,</p>
<p>Il a commencé à trouver une idée,</p>
<p>Il a commencé à dessiner de nouveaux personnages…</p>
<p>Savez-vous qui est cet homme ?</p>
<p>C’est lui qui a créé les Schtroumpfs.</p>
<p>Vous connaissez les Schtroumpfs ?</p>
<p>C’est cet homme qui en est l’auteur,</p>
<p>Cet homme qui a raté son entretien d’embauche…</p>
<p>Cet homme qui est entré dans ce domaine par « erreur »…</p>
<p>Cet homme a créé les Schtroumpfs,</p>
<p>C’est devenu un grand succès,</p>
<p>Puis c’est passé d’une bande dessinée sur papier à un dessin animé à la télévision,</p>
<p>Puis c’est passé au cinéma,</p>
<p>Ça a été traduit dans les langues du monde entier,</p>
<p>Et les petits comme les grands connaissent les Schtroumpfs.</p>
<p>Cet homme a gagné beaucoup beaucoup beaucoup d’argent…</p>
<p>Mais imaginez s’il avait trouvé son portefeuille ?</p>
<p>C’est sûr et certain qu’il n’aurait pas connu la même réussite dans sa vie.</p>
<p><strong>{ Or, il se peut que vous ayez de l’aversion pour une chose alors qu’elle vous est un bien. Et il se peut que vous aimiez une chose alors qu’elle vous est mauvaise. C’est Allah qui sait, alors que vous ne savez pas. }</strong></p>
<p>(Coran - Sourate 02 - Al-Baqara : La Vache verset 216)</p>
<p>Fais des efforts dans la vie,</p>
<p>Et laisse Dieu se charger du reste.</p>
<p>À ce moment, tu verras véritablement un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
