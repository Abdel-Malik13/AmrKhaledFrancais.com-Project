                                <article class="texte-francais">
                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui vient de l’Australie,</p>
<p>Et le titre de l’histoire est : « Un voleur, mais un humain ».</p>
<p>C’est le titre qui a été donné à cette histoire dans un article australien.</p>
<p>C’est un voleur…</p>
<p>Un voleur de sac à mains,</p>
<p>Il attend qu’une femme ne fasse pas attention,</p>
<p>Puis il vole son sac à main et part en courant…</p>
<p>Et il vend ce qu’il y a à l’intérieur du sac.</p>
<p>Il vivait comme ça depuis plusieurs années,</p>
<p>Puis un jour, une femme était assise,</p>
<p>Et pendant qu’elle regardait ailleurs,</p>
<p>Il a pris son sac à main, et il est parti en courant…</p>
<p>La femme a crié et pleuré,</p>
<p>Mais il était insensible…</p>
<p>Il est parti sans se retourner,</p>
<p>Et quand il a ouvert le sac à main, il n’a trouvé qu’une caméra,</p>
<p>Mais une très bonne caméra,</p>
<p>C’est-à-dire s’il la vendait, ça lui rapporterait pas mal d’argent…</p>
<p>Donc dans le sac il n’a trouvé qu’une caméra et une lettre,</p>
<p>Il a pris la caméra pour l’examiner,</p>
<p>Et en regardant son contenu,</p>
<p>Il a trouvé plein de photos …</p>
<p>Toutes les photos dans la caméra sont celles de la femme qu’il a volée, avec des enfants.</p>
<p>Il parait que ces enfants sont les siens,</p>
<p>Puis il y avait plein plein de photos,</p>
<p>Et sur chacune des photos il y avait la femme avec les mêmes enfants,</p>
<p>Il a donc compris que ce sont ses enfants.</p>
<p>Puis il a décidé d’ouvrir l’enveloppe,</p>
<p>Et dedans il y avait une lettre touchante qui disait :</p>
<p>« Mes enfants chéris,</p>
<p>Après avoir été atteinte de la maladie du cancer,</p>
<p>Je sens que mes jours dans ce bas-monde sont comptés,</p>
<p>Il ne me reste plus que d’enregistrer mes souvenirs avec vous, à travers les photos dans cette caméra.</p>
<p>Je vous en prie, gardez mes photos avec vous, après ma mort »</p>
<p>Puis en bas de la lettre, elle avait écrit son nom.</p>
<p>L’homme a été ému…</p>
<p>C’est un voleur oui c’est vrai,</p>
<p>Mais il reste un humain au fond…</p>
<p>Il a été touché par l’histoire de cette femme,</p>
<p>Il a réfléchi à ce qu’il devait faire,</p>
<p>Il a décidé qu’il fallait à tout prix lui rendre sa caméra…</p>
<p>Il ne va pas la priver de son dernier rêve,</p>
<p>Il y a beaucoup de photos, ça ne se fait pas…</p>
<p>La femme va décéder, et elle souhaite laisser des souvenirs à ses enfants,</p>
<p>Je ne veux pas voler cette femme,</p>
<p>Je veux lui rendre ses photos,</p>
<p>Mais comment faire ?</p>
<p>Comment la retrouver ?</p>
<p>Il a essayé de la chercher sur internet, il n’a rien trouvé…</p>
<p>Il a alors décidé de se sacrifier,</p>
<p>Imaginez ce qu’il va faire ?</p>
<p>Il est parti se rendre à la police…</p>
<p>Ça reste un humain à la fin,</p>
<p>Il est parti se dénoncer,</p>
<p>Pour que la police retrouve la femme et lui rende sa caméra…</p>
<p>Il a reconnu son crime.</p>
<p>C’est une histoire touchante en réalité,</p>
<p>Imaginez la joie de cette femme quand elle a retrouvé sa caméra…</p>
<p>Elle pouvait mourir de tristesse à cause de ce vol,</p>
<p>Tout son espoir dans la vie c’est de laisser des souvenirs à ses enfants à travers ces photos.</p>
<p>Je raconte cette histoire pour dire un sens très important :</p>
<p>Chaque personne dans la vie, aussi dure soit elle, a à l’intérieur un être humain…</p>
<p>Ne regarde jamais des personnes autour de toi en pensant « Il ont perdu toute leur humanité »,</p>
<p>Chaque être humain possède en lui la miséricorde que Dieu a semée en nous.</p>
<p>Peu importe comment la personne en face te semble mauvaise… Cherche l’être humain en elle,</p>
<p>Cherche bien…</p>
<p>Balaye la poussière,</p>
<p>Nettoie la rouille,</p>
<p>Et tu trouveras un être humain, et un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
