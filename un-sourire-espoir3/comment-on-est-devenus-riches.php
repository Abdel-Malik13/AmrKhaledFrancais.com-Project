                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Mes chers, parfois on ignore d’où nous vient le Bien,</p>
<p>Comment notre subsistance nous arrive,</p>
<p>L’idée de la subsistance est une idée très complexe,</p>
<p>C’est-à-dire qui reçoit de l’argent, et pour le transmettre à qui,</p>
<p>Tu ne peux pas le deviner avec un programme informatique,</p>
<p>Tu ne peux pas en faire des statistiques,</p>
<p>Tu ne peux pas suivre un billet de 10 euros,</p>
<p>Savoir où il ira,</p>
<p>Dans la poche de qui,</p>
<p>Pour finir où…</p>
<p>C’est impossible de le savoir,</p>
<p>C’est très compliqué.</p>
<p>Mais parfois on apprend des idées que des gens ont appliquées,</p>
<p>Et grâce auxquelles Dieu leur a donné de la subsistance,</p>
<p>On les raconte donc,</p>
<p>Et on dit : On devrait apprendre de cette expérience,</p>
<p>Peut-être que toi et moi, on pourrait l’appliquer.</p>
<p>Ou bien ça peut nous inspirer pour chercher de nouvelles idées…</p>
<p>Quelle est l’histoire de cet épisode ?</p>
<p>L’histoire dit que le secret a commencé dans une chambre à coucher…</p>
<p>Quel est le secret ?</p>
<p>Et quel est le rapport avec la chambre à coucher ?</p>
<p>C’est une femme qui a envoyé un message sur l’Email de l’émission,</p>
<p>Elle raconte :</p>
<p>Il y a 3 ans, je suis partie visiter une de mes amies les plus proches,</p>
<p>Elle venait de se marier,</p>
<p>Elle m’a reçue dans un petit logement très modeste,</p>
<p>Un tout petit appartement, proche de la maison de ses beaux-parents,</p>
<p>Et honnêtement j’ai ressenti que sa situation financière était difficile,</p>
<p>On s’est assises,</p>
<p>On a discuté etc.</p>
<p>Puis par la suite, je suis partie vivre avec mon mari à Abou Dhabi (aux Émirats arabes unis),</p>
<p>Je suis restée plusieurs années sans revoir mon amie.</p>
<p>Et 4 ou 5 ans plus tard, je suis revenue,</p>
<p>Je lui ai dit « Je passe te visiter »,</p>
<p>Elle m’a dit « Oui, mais on a changé d’adresse, on a déménagé »,</p>
<p>Je suis donc partie la visiter dans son nouveau logement,</p>
<p>Et dès que je suis entrée, Masha’ Allah j’ai été surprise…</p>
<p>C’est quoi ça ?</p>
<p>C’est quoi cette grande maison ?</p>
<p>C’est quoi ces jolis meubles ?</p>
<p>J’étais curieuse de savoir comment sa vie a changé à ce point…</p>
<p>Je lui ai dit « Je jure que je ne te jalouse pas,</p>
<p>En entrant j’ai dit Masha’ Allah,</p>
<p>Mais je veux juste comprendre comment un changement aussi grand a pu se produire ? »</p>
<p>Mon amie m’a répondu « Je jure que moi non plus je ne comprends pas comment…</p>
<p>Mais je vais te raconter une chose qu’on faisait moi et mon mari,</p>
<p>Dès le début de notre mariage,</p>
<p>C’est-à-dire il y a 5 ans,</p>
<p>À la même période où tu nous avais rendu visite,</p>
<p>On avait mis une Tirelire dans notre chambre à coucher,</p>
<p>Et chaque matin en nous réveillant,</p>
<p>On avait promis de mettre de l’argent dans la Tirelire,</p>
<p>Peu importe la somme,</p>
<p>1 euro, ou 5 ou 10 ou 100…</p>
<p>L’important c’est que chaque matin on devait mettre quelque chose dans la Tirelire,</p>
<p>Et ce qui nous motivait c’était une parole du Prophète Mohamed (bénédictions et paix soient sur lui) dans laquelle il a dit <strong class="prophete">« Chaque jour où le soleil se lève, 2 Anges appellent… L’un d’eux dit : ‘Ô Seigneur donne une compensation à celui qui dépense (dans le Bien)’, et l'autre dit: ‘Ô Seigneur donne une ruine à celui qui refuse de dépenser (pour aider les pauvres)’ »</strong></p>
<p>Avec mon mari, on avait pris cette intention,</p>
<p>Puisque ces 2 Anges disent ça,</p>
<p>Alors on mettra chaque matin de l’argent dans la Tirelire.</p>
<p>Et chaque Vendredi,</p>
<p>On vidait la Tirelire,</p>
<p>Et mon mari prenait ce qu’il y avait dedans sans compter…</p>
<p>Il mettait dans sa poche toute la somme d’argent,</p>
<p>Et après la Prière du Vendredi,</p>
<p>Il allait dans un endroit où des gens sont dans le besoin,</p>
<p>Et il leur donnait l’argent sans le compter.</p>
<p>On a continué à faire ça pendant 4 ans…</p>
<p>Puis soudainement, on a proposé à mon mari un travail dans une grande entreprise, avec un salaire très élevé,</p>
<p>Alors qu’avant il travaillait dans une petite entreprise.</p>
<p>Pendant 3 ans, il a continué à obtenir des promotions dans sa nouvelle entreprise,</p>
<p>Son salaire augmentait… Augmentait… Augmentait…</p>
<p>Et notre situation financière a complètement changé.</p>
<p>Grâce au secret de la Tirelire dans notre chambre à coucher ».</p>
<p>C’est une idée,</p>
<p>Tu peux l’appliquer toi aussi dans ta vie,</p>
<p>Mais tu peux également réfléchir à d’autres idées,</p>
<p>Ce qui est sûr c’est que Dieu récompense pour le Bien qu’on fait,</p>
<p>Et que donner aux pauvres, c’est le secret pour que Dieu nous donne à Son tour.</p>
<p>Personne ne sait qui reçoit de l’argent, pour aider qui,</p>
<p>Personne ne sait où finira l’argent qui passe entre ses mains…</p>
<p>Dépense-le donc en aidant les pauvres,</p>
<p>Peut-être que cet argent ira à la personne qui en avait besoin,</p>
<p>Et Dieu te donnera encore plus comme récompense.</p>
<p>À ce moment on verra un sourire d’espoir dans nos sociétés.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
