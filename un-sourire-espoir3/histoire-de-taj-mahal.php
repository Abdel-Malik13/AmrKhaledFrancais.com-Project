                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Le sourire d'espoir d'aujourd'hui vient de l'Inde,</p>
<p>Car en réalité je reviens d'un voyage en Inde,</p>
<p>Et je suis touché par ma visite.</p>
<p>J'avais fait un épisode sur le Taj Mahal une fois,</p>
<p>C'est un palais magnifique en Inde,</p>
<p>Mais quand j'avais fait l'épisode je ne l'avais jamais vu.</p>
<p>Cette fois, j'y suis allé et j'ai visité le Taj Mahal,</p>
<p>Mes chers, honnêtement c'est une construction architecturale formidable,</p>
<p>Il est construit sur un lac en forme de croissant,</p>
<p>L'eau entoure le palais de chaque côté,</p>
<p>Il fait partie des 7 merveilles du monde...</p>
<p>Chaque année plus de 4 millions de touristes le visitent,</p>
<p>Et en vérité l'histoire de sa construction est ce qu'il y a de plus beau en lui,</p>
<p>C'est cette histoire qui passionne le plus les gens qui le visitent :</p>
<p>Taj Mahal c'est une tombe,</p>
<p>C'est-à-dire ce n'est pas un palais où des gens ont vécu...</p>
<p>Non, non, non... C'est une tombe.</p>
<p>La tombe de qui ?</p>
<p>La tombe d'une femme appelée Mumtaz Mahal (la lumière du palais)</p>
<p>C'était l'épouse de l'empereur,</p>
<p>L'empereur qui gouvernait l'Inde à l'époque,</p>
<p>Et qui était d'ailleurs musulman.</p>
<p>Quand l'épouse de l'empereur allait décéder,</p>
<p>Elle lui a fait 3 demandes :</p>
<p>1-Ne pas se marier après elle... C'est compréhensible, et toutes les femmes demandent ça.</p>
<p>2-Protéger ses enfants et prendre soin d'eux.</p>
<p>3-Lui construire un monument, qui attire les visiteurs,</p>
<p>Pour qu'elle se sente moins seule dans sa tombe...</p>
<p>Alors l'empereur a construit le Taj Mahal par fidélité envers son épouse,</p>
<p>C'est une construction sublime,</p>
<p>Des versets du Coran sont gravés sur les murs,</p>
<p>Et le marbre utilisé est très rare,</p>
<p>Jamais je n'ai vu un marbre de cette qualité,</p>
<p>L'endroit est très beau et très grand.</p>
<p>Mais en réalité ce que je veux dire dans cet épisode c'est une chose plus importante :</p>
<p>Quand l'empereur a terminé la construction du palais,</p>
<p>Et que son épouse y a été enterrée,</p>
<p>Son fils est venu, et lui a retiré le pouvoir...</p>
<p>Il a détrôné son père,</p>
<p>Puis il l'a emprisonné dans une citadelle loin du Taj Mahal.</p>
<p>Le père a dit à son fils « Je ne veux qu'une seule chose :</p>
<p>C'est qu'à l'endroit où je serai emprisonné, il y ait une fenêtre qui donne sur la tombe de ta maman »</p>
<p>Quel est cet amour étonnant ?</p>
<p>Et quelle est cette fidélité ?</p>
<p>Le fils a accepté la demande de son père,</p>
<p>Et cet endroit existe jusqu'à maintenant, je l'ai visité,</p>
<p>C'est une citadelle, dans laquelle il y a une chambre,</p>
<p>Dans la chambre il y a une fenêtre,</p>
<p>Et depuis la fenêtre on peut apercevoir le Taj Mahal en entier.</p>
<p>Le père est resté emprisonné dans cette chambre jusqu'à sa mort...</p>
<p>Et quand il est mort,</p>
<p>Son fils a commencé à ressentir des regrets...</p>
<p>Mais bien sûr c'était trop tard.</p>
<p>Il n'a trouvé qu'une lettre de son père où il a écrit,</p>
<p>« Je ne souhaite qu'une seule chose :</p>
<p>Que je sois enterré à côté de mon épouse chérie »</p>
<p>L'empereur a été enterré au même endroit que son épouse, au Taj Mahal,</p>
<p>Et quand j'ai visité le palais, j'ai vu les deux tombes l'une à côté de l'autre,</p>
<p>L'empereur Shâh Jahân et son épouse Mumtaz.</p>
<p>En vérité, tous les gens qui viennent visiter l'endroit, sont émus par la fidélité et l'amour de l'empereur.</p>
<p>Les gens sont éblouis par le monument, par le génie architectural, par la beauté de la construction, par le fait qu'il fasse partie des 7 merveilles du monde (comme les pyramides)...</p>
<p>Mais ils sont encore plus touchés par l'histoire d'amour et de fidélité de l'empereur.</p>
<p>Un sourire d'espoir pour toutes les histoires d'amour et de fidélité,</p>
<p>Oui,</p>
<p>Mais un sourire d'espoir pour une autre raison...</p>
<p>Les deux palais les plus visités au monde sont le Taj Mahal, et le palais de Grenade « Alhambra » en Espagne,</p>
<p>Et les deux ont été construits par les musulmans, quand leur histoire était glorieuse...</p>
<p>Un sourire d'espoir pour notre passé glorieux,</p>
<p>Et un sourire d'espoir pour l'amour et la fidélité.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
