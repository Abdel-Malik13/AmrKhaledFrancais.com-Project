                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de notre amour pour le Prophète Mohamed (bénédictions et paix soient sur lui)</p>
<p>Tous les gens,</p>
<p>Le monde entier parle en ce moment du Prophète Mohamed (bpsl),</p>
<p>Malheureusement… Il y’en a qui ont touché à des choses sacrées,</p>
<p>Il y en a qui ont dit des mauvaises choses sur le Prophète (bpsl),</p>
<p>Des choses qui ont blessé les sentiments des musulmans,</p>
<p>Et qui ont blessé les sentiments de tous les arabes,</p>
<p>Qui ont blessé même chaque personne qui a de l’humanité en elle.</p>
<p>Pourquoi… Pourquoi faire ça ?</p>
<p>Et pourquoi accuser le Prophète (bpsl) de haine ?</p>
<p>Je vais vous raconter le 1er discours du Prophète (bpsl) en entrant à Médine :</p>
<p>Imaginez, le Prophète (bpsl) est resté 13 ans à la Mecque, où lui et les musulmans subissaient des persécutions etc.</p>
<p>Aujourd’hui il est obligé d’immigrer à Médine…</p>
<p>Imaginez quel est son état d’esprit ?</p>
<p>Écoutez avec moi le 1er discours,</p>
<p>Quel sera le premier message ?</p>
<p>Comme on dit, on connait le contenu d’un livre à partir de son titre…</p>
<p>Quelle sera la 1ère parole que le Prophète (bpsl) dira aux gens ?</p>
<p>Les gens à Médine l’ont accueilli avec le célèbre chant « Et la pleine lune s'est levée sur nous... »</p>
<p>Le Prophète (bpsl) est entré à Médine sur sa chamelle,</p>
<p>Et tous les gens l’attendaient…</p>
<p>Tu vas leur dire quoi Ô Messager de Dieu ?</p>
<p>Quel est le 1er message ?</p>
<p>Si seulement le monde pouvait entendre ça…</p>
<p>Le 1er discours était :</p>
<p><strong class="prophete">« Ô gens, transmettez la paix (salam),</strong></p>
<p><strong class="prophete">Donnez à manger aux autres,</strong></p>
<p><strong class="prophete">Maintenez vos liens,</strong></p>
<p><strong class="prophete">Et vous entrerez au Paradis en paix. »</strong></p>
<p>C’est magnifique…</p>
<p>On s’attache sincèrement au Prophète (bpsl),</p>
<p>Le Prophète de la Miséricorde,</p>
<p>Le Prophète de l’humanité.</p>
<p>Comment peut-il être accusé de haine ?</p>
<p>Alors qu’il est de cette clémence, de cette humanité et de cette paix.</p>
<p>Réécoutez la 1ère parole</p>
<p><strong class="prophete">« Ô gens, transmettez la paix »</strong></p>
<p>Transmettez la paix entre vous,</p>
<p>Transmettez la paix dans le monde,</p>
<p>Transmettez la paix à Médine,</p>
<p>Alors qu’à Médine il y a plusieurs religions,</p>
<p>Il ya des musulmans et des non-musulmans,</p>
<p>Il y a des tribus rivales,</p>
<p>Il y a des juifs,</p>
<p>Il y a des gens qui ne croient pas en Dieu,</p>
<p>Et il y a plein de gens différents.</p>
<p>Alors la 1ère parole c’est :</p>
<p><strong class="prophete">« Ô gens, transmettez la paix »</strong></p>
<p>Installez un esprit de paix,</p>
<p>Vivez en paix.</p>
<p><strong class="prophete">« Ô gens, transmettez la paix,</strong></p>
<p><strong class="prophete">Donnez à manger aux autres »</strong></p>
<p>Il faut que tout le monde puisse manger,</p>
<p>Couvrez les besoins essentiels de l’être humain,</p>
<p>Ne laissez pas de famine.</p>
<p><strong class="prophete">« Donnez à manger aux autres,</strong></p>
<p><strong class="prophete">Maintenez vos liens »</strong></p>
<p>Ça veut dire quoi ?</p>
<p>Ça veut dire ils sont tous différents à Médine,</p>
<p>Mais à la fin, nous sommes tous issus d’Adam et d’Eve.</p>
<p><strong class="prophete">« Maintenez vos liens »</strong></p>
<p><strong>{ C’est Lui (Dieu) qui vous a créés d’un seul être dont il a tiré son épouse, }</strong></p>
<p>Coran : Sourate 7 - Al-A'râf (verset 189)</p>
<p><strong class="prophete">« Ô gens, transmettez la paix (salam),</strong></p>
<p><strong class="prophete">Donnez à manger aux autres,</strong></p>
<p><strong class="prophete">Maintenez vos liens »</strong></p>
<p>Et qu’est ce qui va se produire si on applique ces 3 choses ?</p>
<p><strong class="prophete">« Et vous entrerez au Paradis en paix. »</strong></p>
<p>Que le salut et la paix soient sur toi Ô Messager de Dieu,</p>
<p>Le Prophète de la Miséricorde,</p>
<p>Le Prophète de l’humanité,</p>
<p>Le Prophète de la paix.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
