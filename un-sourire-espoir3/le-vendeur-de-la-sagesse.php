                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>On commencera l’épisode d’aujourd’hui par une question :</p>
<p>Tu médites combien d’heures par semaine ?</p>
<p>C’est-à-dire tu t’assoies tout seul, et tu penses à ton avenir, et à tes prochains pas ?</p>
<p>Ou bien tu médites pendant que tu marches ?</p>
<p>Ou bien on ne marche presque plus de nos jours… Pas grave,</p>
<p>Est-ce que tu t’assois tout seul pour méditer ?</p>
<p>Tu réfléchis aux prochains pas que tu vas faire la semaine prochaine ?</p>
<p>Savez-vous que le cerveau humain est comme les muscles du bras…</p>
<p>Si tu ne portes rien avec tes bras pendant une longue période,</p>
<p>Si ton bras reste immobile,</p>
<p>Alors ses muscles vont diminuer…</p>
<p>La même chose arrive au cerveau, il diminue si tu ne le fais pas travailler.</p>
<p>L’histoire d’aujourd’hui est symbolique :</p>
<p>On raconte qu’un jour, un ancien gouverneur arabe est descendu se balader au Souk (marché),</p>
<p>Mais ce jour-là il n’a pas mis ses habits de gouverneur, ni sa tenue de Calife,</p>
<p>Il s’est déguisé en citoyen normal,</p>
<p>Et il est descendu marcher parmi les gens…</p>
<p>Pendant qu’il se baladait, une vieille boutique a attiré son attention,</p>
<p>Cette boutique était au milieu du Souk,</p>
<p>Mais dedans il n’y avait rien à acheter…</p>
<p>C’est quoi ça ?</p>
<p>C’est une boutique vide… Dedans il n’y a aucune marchandise.</p>
<p>Et dans la boutique il y avait un homme très âgé, assis sur une vielle chaise,</p>
<p>Dans toute la boutique il n’y a rien,</p>
<p>Sauf quelques tableaux remplis de poussière,</p>
<p>Le gouverneur s’est approché du vieil homme,</p>
<p>Il l’a salué,</p>
<p>Puis il lui a dit « Je suis venu au Souk pour faire des achats… Mais qu’as-tu à vendre ? »</p>
<p>Le vieil homme lui a répondu</p>
<p>« J’ai les meilleures marchandises du marché ! »</p>
<p>Le gouverneur a ri,</p>
<p>Et il lui a répondu « Es-tu sûr de ce que tu dis ? »</p>
<p>Le vieil homme lui a dit « Bien sûr, la valeur de mes marchandises est inestimable »</p>
<p>Le gouverneur lui a demandé « Et que vends-tu ? »</p>
<p>Le vieil homme lui a répondu « Je vends la Sagesse »</p>
<p>Le gouverneur lui a dit « Tu vends la sagesse alors que tu ne possèdes que quelques vieux tableaux ? »</p>
<p>Le vieil homme lui a dit « Justement c’est sur ces tableaux que se trouve la Sagesse »</p>
<p>Le gouverneur lui a demandé « Quelle Sagesse ? »</p>
<p>Le vieil homme lui a répondu « Lis ce qu’il y a écrit dessus »</p>
<p>Le gouverneur a regardé le 1er tableau, sur lequel il y avait écrit : « Réfléchis avant d’agir ».</p>
<p>Puis il a regardé le 2ème tableau, sur lequel il y avait marqué : « Réfléchis avant d’agir ».</p>
<p>Et la même phrase était écrite sur le 3ème tableau…</p>
<p>Le gouverneur a ri,</p>
<p>Il lui a dit « C’est ça que tu vends ? »</p>
<p>Le vieil homme lui a répondu « Oui »</p>
<p>Le gouverneur lui a dit « Ah bon ? Et elle est où la grande valeur de ce que tu vends ? »</p>
<p>Le vieil homme lui a dit « C’est la Sagesse.</p>
<p>Et j’ai une condition,</p>
<p>Celui qui veut acheter, doit acheter 5 tableaux.</p>
<p>Un seul tableau ne suffit pas,</p>
<p>Car tu dois accrocher les tableaux partout chez toi,</p>
<p>Pour que la Sagesse puisse fonctionner »</p>
<p>Le gouverneur prenait tout ça sur le ton de l’amusement, il pensait que ce vieil homme était fou,</p>
<p>Il lui a demandé « Et à combien tu vends ? »</p>
<p>Le vieil homme lui a répondu « Je vends chaque tableau à 10.000 dinars »</p>
<p>Le gouverneur a ri,</p>
<p>Il lui a répondu « Je ne vais pas acheter un tableau à ce prix élevé »</p>
<p>Puis il a fait semblant de partir…</p>
<p>Le vieil homme lui a dit « Au revoir ».</p>
<p>Le gouverneur lui a dit « Je t’achète un tableau à 1000 dinars ? »</p>
<p>Puis il lui a fait une offre à 2000 dinars…</p>
<p>Mais le vieil homme refusait de vendre à moins de 10.000 dinars…</p>
<p>Le gouverneur a fini par accepter de payer ce prix,</p>
<p>Il a acheté plusieurs tableaux,</p>
<p>Et le vieil homme lui a dit « Je te les vends à condition que tu les accroches à chaque pièce de ta maison, sinon la Sagesse ne va pas fonctionner »</p>
<p>Le gouverneur est rentré chez lui,</p>
<p>Il a accroché les tableaux par curiosité,</p>
<p>Il se disait qu’il avait aidé financièrement un homme pauvre puis c’est tout.</p>
<p>Les jours vont passer…</p>
<p>Et un coup d’état était en train de se préparer contre le gouverneur,</p>
<p>Un de ses ministres a décidé de le tuer,</p>
<p>Il a chargé le coiffeur personnel du gouverneur de le faire,</p>
<p>« Tu vas entrer au palais, et pendant que tu lui coupes les cheveux, tu le tues par derrière.</p>
<p>Je prendrai la place du gouverneur,</p>
<p>Et je te donnerai telle et telle récompense… »</p>
<p>Le coiffeur est entré au palais,</p>
<p>Et partout où il allait, il lisait « Réfléchis avant d’agir »</p>
<p>Il a commencé à hésiter…</p>
<p>Il est entré dans la pièce où il coiffait le gouverneur,</p>
<p>Et devant lui, il a trouvé un autre tableau « Réfléchis avant d’agir »…</p>
<p>Il tremblait,</p>
<p>Car il réfléchissait aux conséquences,</p>
<p>Le gouverneur s’est assis,</p>
<p>Il a commencé à coiffer le gouverneur,</p>
<p>Il se voyait dans le miroir,</p>
<p>Et au dessus du miroir il y avait un tableau « Réfléchis avant d’agir »…</p>
<p>Les ciseaux sont tombés de ses mains tremblantes,</p>
<p>Le coiffeur a craqué,</p>
<p>Il a tout raconté au gouverneur...</p>
<p>Il lui a pardonné,</p>
<p>Et le ministre a été emprisonné.</p>
<p>Le gouverneur a compris la Sagesse qui nous échappe...</p>
<p>S'il vous plait : Réfléchissez avant d'agir.</p>
<p>Merci, Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
