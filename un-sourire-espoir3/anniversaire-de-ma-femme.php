                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est très sentimental...</p>
<p>Il parle de l'amour,</p>
<p>Il parle du plus beau sentiment qui nait entre un homme et une femme.</p>
<p>En vérité j'enregistre cet épisode sans l'avoir préparé,</p>
<p>C'est-à-dire je suis venu au studio pour enregistrer quelques épisodes,</p>
<p>J'avais préparé des histoires,</p>
<p>Et pendant que j'étais assis,</p>
<p>Un des grands responsables du studio est entré,</p>
<p>Et il a ramené son ordinateur portable avec lui pour me montrer des choses,</p>
<p>Et pendant qu'on regardait l'écran,</p>
<p>J'ai vu qu'il avait posté un message sur Facebook pour une occasion particulière,</p>
<p>Je lui ai demandé « Est-ce que je peux le lire ? »</p>
<p>Il m'a dit « Oui bien sûr, je l'ai publié sur ma page Facebook »</p>
<p>Et quand j'ai lu son message, je me suis dit : Il faut que je le partage avec vous.</p>
<p>Je parle sérieusement,</p>
<p>Ça s'est passé il y a un instant,</p>
<p>Je jure que je n'avais pas préparé cet épisode.</p>
<p>Cet homme a écrit à qui ?</p>
<p>Il a écrit à son épouse chérie,</p>
<p>Ce ne sont que 5 ou 6 lignes,</p>
<p>Mais je tenais à en parler dans cet épisode,</p>
<p>Il dit :</p>
<p>« Le 11 Février est un jour très important dans ma vie,</p>
<p>Car c'est le jour d'anniversaire de la personne la plus importante pour moi,</p>
<p>Mon épouse, ma fille, ma sœur, ma chérie, mon amie, ma maman...</p>
<p>Le 11 Février c'est le jour d'anniversaire de Yatrib (le prénom de sa femme),</p>
<p>Yatrib que Dieu m'a accordé comme bienfait, pour qu'elle soit mon épouse,</p>
<p>Et pour qu'elle m'offre le plus beau cadeau du monde : Nourhane et Maryam (ses 2 filles),</p>
<p>Yatrib qui m'a aidé, et qui m'a soutenu,</p>
<p>Et sans elle je n'aurais rien accompli dans ma vie.</p>
<p>Yatrib à qui je dois tout.</p>
<p>Joyeux anniversaire la plus belle Yatrib au monde.</p>
<p>Je t'aime. »</p>
<p>C'est magnifique !</p>
<p>D'ailleurs mes chers, cet homme est âgé de plus de 60 ans,</p>
<p>Mais il aime beaucoup son épouse,</p>
<p>Et ce sentiment d'amour est ce qui l'a poussé à publier ce message sur sa page Facebook,</p>
<p>Réécoutez ces très belles paroles :</p>
<p>« Mon épouse, ma fille, ma sœur, ma chérie, mon amie, ma maman... »</p>
<p>C'est magnifique !</p>
<p>C'est magnifique quand les sentiments sont abondants et remplis de cet amour,</p>
<p>Pourquoi mes chers, on a perdu l'amour à notre époque ?</p>
<p>Ils ont fait une enquête chez Google,</p>
<p>Pour voir quel est le mot que les gens cherchent le plus sur internet,</p>
<p>Et ils ont été surpris de découvrir que parmi les 10 mots les plus recherchés sur Google il y a le mot « Love » (Amour).</p>
<p>Les gens cherchent l'amour et ne le trouvent pas...</p>
<p>Quelle chance tu as, si tu trouves l'amour dans ta vie, et que tu le préserves.</p>
<p>Un sourire d'espoir pour chaque personne qui sent que son épouse est :</p>
<p>Sa femme, sa fille, sa sœur, sa chérie, son amie, sa maman...</p>
<p>Joyeux anniversaire à Yatrib,</p>
<p>Et un sourire d'espoir pour chaque être humain qui donne du bonheur à son épouse / mari.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
