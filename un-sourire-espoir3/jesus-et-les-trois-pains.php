                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui dit que parfois on se bat les uns contre les autres, pour ce bas-monde…</p>
<p>On s’entretue pour ce bas-monde…</p>
<p>Pourtant cette vie n’a pas beaucoup de valeur,</p>
<p>Ce n’est qu’un passage.</p>
<p>On refuse de vivre ensemble,</p>
<p>On voit cette vie comme très grande,</p>
<p>On s’entretue,</p>
<p>Alors que c’est juste un mirage et une illusion.</p>
<p>L’histoire d’aujourd’hui est avec Jésus (que la paix soit sur lui),</p>
<p>Un jour il marchait,</p>
<p>Et un homme l’accompagnait,</p>
<p>Ce n’était pas quelqu’un de proche, mais juste un de ses compagnons.</p>
<p>Ils avaient 3 pains.</p>
<p>Pendant qu’ils marchaient, Jésus a eu faim,</p>
<p>Il a demandé à son compagnon de préparer à manger.</p>
<p>L’homme a sorti 2 pains…</p>
<p>Jésus lui a demandé « Où est le 3ème pain ? »</p>
<p>L’homme a répondu « Je jure qu’il n’y en avait que 2 »</p>
<p>(Pourtant au départ il y en avait 3… Où est passé le 3ème pain…)</p>
<p>Jésus a gardé le silence,</p>
<p>Ils ont continué à marcher…</p>
<p>Puis ils ont croisé un homme aveugle,</p>
<p>Et comme vous le savez, avec l’autorisation de Dieu, Jésus pouvait passer sa main sur les yeux des aveugles, et ils devenaient voyants.</p>
<p>Jésus a passé sa main sur les yeux de cet homme,</p>
<p>Il a prié Dieu pour qu’Il le guérisse,</p>
<p>Et l’homme a retrouvé sa vue.</p>
<p>Le compagnon de Jésus a dit « Gloire à Dieu ! »</p>
<p>Jésus lui a dit « Par Celui qui a guérit cet aveugle… Où est le 3ème pain ? »</p>
<p>Le compagnon a répondu « Je jure par Dieu, qu’il n’y en avait que 2 »</p>
<p>(Pourtant au départ il y en avait 3…)</p>
<p>Jésus a gardé le silence,</p>
<p>Ils ont poursuivi leur route…</p>
<p>Et ils ont croisé un homme handicapé, qui ne pouvait pas bouger ses jambes,</p>
<p>Jésus a prié Dieu pour qu’Il le guérisse,</p>
<p>Il a passé sa main sur ses jambes,</p>
<p>Et l’homme a commencé à les bouger.</p>
<p>Le compagnon de Jésus a dit « Gloire à Dieu ! »</p>
<p>Jésus lui a redit « Par Celui qui a guéri cet handicapé, où est le 3ème pain ? »</p>
<p>Le compagnon a répondu « Je jure, qu’il n’y en avait que 2 »</p>
<p>Jésus a encore gardé le silence,</p>
<p>Ils ont continué à marcher…</p>
<p>Ils sont passés devant 3 dunes de sable,</p>
<p>Jésus a prié Dieu, et les 3 dunes se sont transformées en or.</p>
<p>Le compagnon a regardé les 3 dunes d’or,</p>
<p>Et il a demandé « Pour qui elles sont ?? »</p>
<p>Jésus lui a dit « La 1ère est pour moi… La 2ème est pour toi… Et la 3ème est pour celui qui a mangé le 3ème pain »</p>
<p>Le compagnon a répondu « C’est moi qui a mangé le 3ème pain ! »</p>
<p>Jésus l’a regardé et lui a dit « Les 3 dunes d’or sont pour toi ».</p>
<p>Jésus l’a laissé, il a poursuivi son chemin.</p>
<p>Et le compagnon était devant ses 3 dunes d’or,</p>
<p>Il allait devenir fou.</p>
<p>Pendant qu’il était là, trois chevaliers sont passés,</p>
<p>Ils ont vu cette grande quantité d’or,</p>
<p>Ils sont descendus de leurs chevaux,</p>
<p>Et ils ont tué le compagnon…</p>
<p>Ils ont pris l’or,</p>
<p>Puis ils ont demandé à l’un d’eux de leur préparer à manger,</p>
<p>Celui des 3 qui est parti préparer à manger s’est dit « Et pourquoi je ne prendrais pas tout l’or à moi seul ? »</p>
<p>Il a mis du poison dans le repas…</p>
<p>Pendant que les 2 autres se sont dit « Au lieu de partager l’or entre nous 3… Tuons-le et partageons l’or entre nous deux »</p>
<p>L’homme est revenu avec le repas,</p>
<p>Les deux autres l’ont tué…</p>
<p>Puis ils ont mangé, et ils sont morts empoisonnés.</p>
<p>Quand Jésus est repassé par-là,</p>
<p>Il a vu cette scène avec 4 hommes morts… Et l’or était toujours à sa place.</p>
<p>Il a regardé les gens qui l’accompagnaient et leur a dit</p>
<p>« C’est ce que fait ce bas-monde à celui qui s’entretue pour lui,</p>
<p>Et qui n’accepte pas de vivre avec les autres »</p>
<p>La coexistence,</p>
<p>Et le refus de s’entretuer pour les biens éphémères.</p>
<p>C’est une histoire très parlante avec Jésus.</p>
<p>Et c’est un sourire d’espoir pour celui qui comprend la réalité de cette vie :</p>
<p>Un mirage, une illusion, un passage pour être testés,</p>
<p>Et la solution c’est le vivre-ensemble.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
