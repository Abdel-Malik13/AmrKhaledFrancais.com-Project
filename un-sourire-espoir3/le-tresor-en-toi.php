                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui, nous l’avons reçue par email,</p>
<p>Celui qui veut nous envoyer une histoire peut le faire sur basmetamal@amrkhaled.net</p>
<p>Donc cette histoire c’est un jeune de 17 ans qui nous l’a envoyée,</p>
<p>Et c’est une histoire très belle.</p>
<p>Il raconte qu’il y avait un homme pauvre, mais talentueux,</p>
<p>Il est pauvre financièrement,</p>
<p>Il traine dans la rue en maudissant sa mauvaise chance,</p>
<p>Et dans quoi il est doué ?</p>
<p>Il a le don de dire des poèmes d’une façon spontanée,</p>
<p>Il est poète,</p>
<p>Mais il est toujours déprimé,</p>
<p>Tout le temps ses sentiments sont négatifs…</p>
<p>D’ailleurs mes chers, la chose qui tue le plus les talents c’est les sentiments négatifs.</p>
<p>Sois positif dans la vie…</p>
<p>Dieu dit dans le Coran :</p>
<p><strong>{ 5. À côté de la difficulté est, certes, une facilité!</strong></p>
<p><strong>6. À côté de la difficulté est, certes, une facilité! }</strong></p>
<p>Sourate 94 - Ach-Charh : L'Ouverture</p>
<p>Renforce cet état d’esprit !</p>
<p>Mais cet homme était pauvre et désespéré,</p>
<p>Il se plaignait sans arrêt de son manque de chance dans la vie…</p>
<p>Pourtant il est talentueux,</p>
<p>Mais il tue son talent…</p>
<p>Une fois un groupe de jeunes jouaient au foot dans la rue,</p>
<p>Et cet homme était assis sur le trottoir,</p>
<p>Puis dans son état dépressif, il s’est mis à dire de la poésie,</p>
<p>Il disait :</p>
<p>« Mes soucis sont incessants, et n’ont pas de barrage…</p>
<p>Le passé m’a vendu, et je n’ai pas de bagages…</p>
<p>Dans une époque, où seuls les charognards sont gagnants…</p>
<p>Et où les pauvres ne trouvent que la perdition… »</p>
<p>C’est-à-dire il faisait des rimes,</p>
<p>Puis il disait ces vers de poésie pendant que les jeunes jouaient au foot,</p>
<p>Ils ont eu pitié pour lui,</p>
<p>Ils sont partis le voir pendant qu’il était assis sur le trottoir,</p>
<p>Ils lui ont demandé « Qu’est ce qui t’arrive ? »</p>
<p>Il leur a répondu avec de la poésie :</p>
<p>« Je suis le pauvre, paumé…</p>
<p>Je suis le triste, affamé…</p>
<p>Je suis celui qui n’a pas de place…</p>
<p>Je suis celui qui a été acheté par les angoisses… »</p>
<p>Ces mots ont touché les jeunes,</p>
<p>Ils ont eu mal pour lui,</p>
<p>Un d’eux lui a donné une livre égyptienne…</p>
<p>Un autre lui a donné 5 livres…</p>
<p>Et un autre 3 livres…</p>
<p>Un des jeunes n’avait pas d’argent, alors il lui a donné un sandwich…</p>
<p>Ils lui ont tous donné quelque chose, sauf 1 seul…</p>
<p>Ce jeune était intelligent,</p>
<p>Il a regardé l’homme et il a vu qu’il portait dans sa main une bague qui avait l’air bien,</p>
<p>Le jeune lui a demandé « Où as-tu trouvé cette bague ? »</p>
<p>L’homme lui a répondu « Je l’ai héritée de ma grand-mère »</p>
<p>Le jeune lui a dit « Et pourquoi tu ne me la vends pas,</p>
<p>Et je te donne de l’argent en échange ? »</p>
<p>L’homme était honnête, il ne mendiait pas exprès, il était vraiment désespéré,</p>
<p>Il a retiré la bague et il lui a dit « Tiens, elle est remplie de rouille blanche »</p>
<p>Le jeune intelligent lui a répondu « Y a-t-il une chose appelée rouille blanche ?</p>
<p>Donne-moi la bague et je te donne de l’argent en échange »</p>
<p>L’homme lui a donné la bague,</p>
<p>Le jeune est rentré chez lui à la maison, il a dit à son papa « J’aimerais vendre cette bague ».</p>
<p>Ils sont partis chez un bijoutier,</p>
<p>Le bijoutier a examiné la bague,</p>
<p>Puis leur a dit « C’est quoi cette bague ?</p>
<p>Dessus il y a de l’or blanc,</p>
<p>Son prix est très élevé, où l’avez-vous trouvée ?</p>
<p>Je veux vous l’acheter »</p>
<p>Le bijoutier leur a acheté la bague pour 5000 livres égyptiennes (plus de 500 euros),</p>
<p>Le jeune a pris l’argent, et il est parti voir le poète,</p>
<p>Il lui a dit « N’est ce pas injuste… De te plaindre tout le temps en disant que tu es le pauvre paumé,</p>
<p>Alors que dans ta main il y a une bague qui vaut 5000 livres,</p>
<p>Et que tu ne connais pas sa valeur ? »</p>
<p>L’homme était très surpris, il lui a dit « Moi je possède ça ?? »</p>
<p>Le jeune intelligent lui a répondu « Tu possèdes ce qui est plus cher que ça…</p>
<p>Tu possèdes un joli talent,</p>
<p>Mais tu le gâches avec le noir dans lequel tu vis »</p>
<p>La morale de cette histoire c’est :</p>
<p>Parfois on a entre nos mains de beaux talents…</p>
<p>Mais les énergies négatives,</p>
<p>Le sentiment de désespoir,</p>
<p>Le sentiment que « Je ne peux pas réussir »,</p>
<p>Le pessimisme,</p>
<p>Et penser négatif tout le temps…
<p>Tout ceci nous fait perdre les plus belles choses qu’on possède.</p>
<p>J’ai la conviction que Dieu est Juste, et qu’Il n’a créé aucun être humain sans lui donner un talent et de grandes capacités,</p>
<p>Je vous jure par Dieu, que chaque personne qui m’écoute en ce moment, possède à l’intérieur d’elle un talent que Dieu lui a donné,</p>
<p>Mais malheureusement, on le tue quand on ne le découvre pas,</p>
<p>Cherche ton talent, et exprime-le,</p>
<p>C’est ton trésor,</p>
<p>C’est ce qui te ramènera de l’or,</p>
<p>C’est ce qui te rendra heureux,</p>
<p>C’est ce qui dessinera sur ton visage un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
