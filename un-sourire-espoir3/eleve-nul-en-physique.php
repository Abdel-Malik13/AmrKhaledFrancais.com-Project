                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui est drôle,</p>
<p>Mais à la fin il y a une surprise.</p>
<p>On peut appeler cette histoire : « L’élève le plus nul en physique ».</p>
<p>L’histoire est arrivée à l’université de Copenhague (au Danemark),</p>
<p>C’est une histoire vraie :</p>
<p>Dans l’examen de physique, il y avait cette question</p>
<p>« Comment calculer la hauteur d’un gratte-ciel, en utilisant un baromètre ? »</p>
<p>(Le baromètre est un instrument de mesure, utilisé en physique et en météorologie, qui sert à mesurer la pression atmosphérique)</p>
<p>Comment calculer la hauteur d’un immeuble à l’aide d’un baromètre ?</p>
<p>Pour les étudiants en physique, la réponse est facile :</p>
<p>Mesurer la pression atmosphérique au niveau du sol,</p>
<p>Ensuite mesurer la pression atmosphérique en haut de l’immeuble,</p>
<p>Et la différence te donnera la hauteur.</p>
<p>Tous les étudiants ont donné cette réponse lors de l’examen,</p>
<p>Sauf un seul…</p>
<p>Il a répondu</p>
<p>« J’attache le baromètre à une longue corde,</p>
<p>Ensuite je le jette du haut de l’immeuble, jusqu’à ce qu’il arrive au sol,</p>
<p>Et je mesure la longueur de la corde »</p>
<p>Bien sûr dès que le professeur a lu cette réponse, il lui a donné 0</p>
<p>Et il n’a même pas lu la suite de sa copie,</p>
<p>Il s’est dit « Cet étudiant n’est pas fait pour cette filière ».</p>
<p>Quand le jeune a vu sa note,</p>
<p>Il s’est vexé…</p>
<p>Il est parti se plaindre à la direction de l’université,</p>
<p>Il était sûr à 100% que sa réponse est correcte !</p>
<p>Et selon le règlement de l’université,</p>
<p>Un expert a été désigné pour vérifier.</p>
<p>Après vérification, il a dit « La réponse est correcte, mais ça montre que cet étudiant n’est pas fait pour la physique »</p>
<p>Mais il a décidé de lui laisser une 2ème chance…</p>
<p>Lui faire repasser l’examen, mais en oral cette fois.</p>
<p>Ils ont fait venir un jury de professeurs,</p>
<p>L’étudiant est arrivé devant eux,</p>
<p>Ils voulaient vite en finir… Alors ils lui ont reposé la même question :</p>
<p>« Comment calculer la hauteur d’un gratte-ciel, en utilisant un baromètre ? »</p>
<p>Le jeune étudiant leur a répondu :</p>
<p>« Il existe plein de méthodes »</p>
<p>Ils lui ont demandé</p>
<p>« Lesquelles ? »</p>
<p>Il leur a dit</p>
<p>« Par exemple : On jette le baromètre du haut de l’immeuble,</p>
<p>Et on calcule le temps qu’il met avant de s’écraser au sol.</p>
<p>Ainsi on peut deviner la hauteur du bâtiment »</p>
<p>Il est fou… Qu’est ce qu’il est en train de raconter ?</p>
<p>Il leur a dit</p>
<p>« Et s’il y a un bon soleil,</p>
<p>On peut mesurer l’ombre du baromètre,</p>
<p>Et mesurer l’ombre de l’immeuble…</p>
<p>On saura ainsi la hauteur du bâtiment, en utilisant la règle de proportionnalité entre les 2 ombres. »</p>
<p>Il disait des choses logiques… Mais qui n’ont pas de rapport avec la fonction du baromètre…</p>
<p>Puis il leur a dit</p>
<p>« Et si vous voulez une solution très simple,</p>
<p>On peut offrir le baromètre comme cadeau, au gardien à l’accueil,</p>
<p>Et il nous dira quelle est la hauteur de l’immeuble »</p>
<p>Ensuite il leur a dit</p>
<p>« Par contre, si vous voulez compliquer les choses…</p>
<p>On peut mesurer la pression atmosphérique au niveau du sol,</p>
<p>Puis mesurer la pression atmosphérique en haut de l’immeuble,</p>
<p>Et la différence te donnera la hauteur. »</p>
<p>C’est quoi ça… Il comprend la physique… Et il se moque de nous ?</p>
<p>Les professeurs du jury n’ont pas apprécié sa méthode,</p>
<p>Ils l’ont fait échouer dans l’examen.</p>
<p>Ce jeune a fini ses études à l’université,</p>
<p>Et pas 1 seule fois, il n’a obtenu une bonne note en physique…</p>
<p>Tout le temps faible… Faible… Faible…</p>
<p>Il a réussi à peine à obtenir son diplôme.</p>
<p>Savez-vous qui est ce jeune ?</p>
<p>Il s’appelle Niels Bohr.</p>
<p>Mais qu’est il devenu… Celui qui n’a jamais réussi en physique ?</p>
<p>C’est le seul danois qui a remporté le prix Nobel de physique.</p>
<p>Imaginez…</p>
<p>Après avoir obtenu son diplôme à l’université, avec des notes très faibles,</p>
<p>Il a continué à travailler dans la physique,</p>
<p>Et il a appris aux gens une chose très belle :</p>
<p>Le génie n’est pas dans la complication,</p>
<p>Le génie est dans la simplicité.</p>
<p>Il a gagné le prix Nobel, car il a simplifié la physique,</p>
<p>La façon d’expliquer la physique,</p>
<p>Et l’a rendue plus accessible aux gens.</p>
<p>Il a transformé ce que les autres pensaient comme faiblesses, en force.</p>
<p>Il leur a dit : Si vous voulez compliquer les choses, alors raisonnez de cette façon.</p>
<p>Mais le vrai génie est dans le fait de simplifier les choses.</p>
<p>Mes chers, soyez simples dans la façon d’éduquer vos enfants,</p>
<p>Bougez dans la vie avec simplicité,</p>
<p>Car la vraie intelligence c’est de Privilégiez la facilité et la simplicité.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
