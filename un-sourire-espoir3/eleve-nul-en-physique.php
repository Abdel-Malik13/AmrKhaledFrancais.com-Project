<p>In the name of Allah the Compassionate, 
The Merciful</p>

<p>Welcome to a new episode of Basmit Amal 
(A Smile of Hope).</p>

<p>Basmit Amal (A Smile of Hope) today 
is a funny story,</p>

<p>but it has a surprise at the end,</p>

<p>we can give it a title of the most
stupid student in physics,</p>

<p>it is a true story that happened in
Copenhagen in Denmark,</p>

<p>in a physics exam, there was a question</p>

<p>"How can you identify the height of
a skyscraper by using a barometer"</p>

<p>- A barometer is a device for measuring
the atmosphere pressure –</p>

<p>so how can you measure the height
of a skyscraper with a barometer?</p>

<p>The right answer is to measure the atmosphere
pressure on the level of earth,</p>

<p>and the atmosphere pressure at the top
of the skyscraper,</p>

<p>and then the difference between them
is the height of the skyscraper.</p>

<p>All of the students give this answer,
except only one student,</p>

<p>who answered: "I would link the barometer
with a long rope and drill down</p>

<p>from the top of the skyscraper,</p>

<p>and then measure the length
of the rope".</p>

<p>His professor gave him zero, and he didn’t  correct 
the rest of the answers,</p>

<p>and said this student is not
a good student.</p>

<p>The student get really upset,</p>

<p>and he filed a complaint to his university,
he was sure of his answer 100%.</p>

<p>The university assigned an expert to investigate the issue.</p>

<p>The student answer was right, but it
shows that he doesn’t understand physics.</p>

<p>The expert recommended giving the student
another chance,</p>

<p>so they made an oral exam for him,
they formulated a committee,</p>

<p>and they asked him the same question</p>

<p>"how can you measure the height
of a skyscraper using a barometer?",</p>

<p>the student answered:</p>

<p>"There are many ways for example;</p>

<p>we can through the barometer from the top
of the skyscraper,</p>

<p>and measure the time it take to reach
the ground,</p>

<p>based on it we measure the height
of the skyscraper</p>

<p>- The committee thought he is craze –</p>

<p>the student continued :</p>

<p>"and if it is really sunny, then we
can measure the length</p>

<p>of the shadow of the barometer, and the length
of the shadow of the skyscraper,</p>

<p>and find the difference between them".</p>

<p>He gives very good answers,
 but not related to physics,</p>

<p>then the students said:</p>

<p>"If you want an easy way, we can give the
barometer to the door man of the skyscraper</p>

<p>and he tells us the height of
the skyscraper very easily,</p>

<p>but if you want a very complicated method,</p>

<p>then we can measure the difference
in atmosphere pressure on the top</p>

<p>and bottom of the skyscraper".</p>

<p>Surprisingly he was giving the right answer,</p>

<p>but the committee was really confused
so he failed again in the physics exam.</p>

<p>This student has finished his studies in the university,</p>

<p>he never gets high scores in his
physics exams,</p>

<p>and he succeeded with poor degrees.</p>

<p>This guy is Niels Bohr, he is the only Danish 
who won the Nobel Prize in physics,</p>

<p>and he never got high scores in
physics exams,</p>

<p>after his graduation he worked
in physics,</p>

<p>and he taught people a very important thing
that genius is not to complicate things,</p>

<p>but genius is in simplicity.</p>

<p>He won Nobel Prize because he simplified
Physics rules, theories,</p>

<p>and equations to normal students, because
he benefited from his experience,</p>

<p>if you want to complicate things do
it this way, but simplicity is genius.</p>

<p>Live in simplicity, deal with children in simplicity, 
genius is in simplicity,</p>

<p>not in complication.</p>

<p>Thank you and May the peace and blessings
 of Allah be upon you.</p>


