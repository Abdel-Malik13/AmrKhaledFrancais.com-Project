                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de la générosité,</p>
<p>Il parle des dons,</p>
<p>Il parle des droits des pauvres.</p>
<p>Il te demande : Combien donnes-tu aux gens autour de toi ?</p>
<p>Vous avez remarqué le nombre de versets du Coran, qui parlent du fait de donner aux autres… Il y en a beaucoup.</p>
<p>Mais il y a des versets qui disent : Chaque argent que tu dépenseras, te sera rendu.</p>
<p><strong>{ Et toute dépense que vous faites [dans le bien], Il la remplace }</strong></p>
<p>Sourate 34 - Saba' (verset 39)</p>
<p>C’est obligé que Dieu te rembourse ce que tu as dépensé !</p>
<p>Mais il y a 2 versets qui ne disent pas seulement que ce que tu dépenses te sera rendu… Mais qu’il te sera rendu multiplié !</p>
<p>Un des versets on le connait tous : <strong>{ Quiconque prête à Allah de bonne grâce, Il le lui rendra multiplié plusieurs fois. }</strong></p>
<p>Sourate 02 - Al-Baqara : La Vache (verset 245)</p>
<p>Et un autre verset qui dit : <strong>{ Ceux-là verront [leurs récompenses] multipliées. }</strong></p>
<p>Sourate 30 - Ar-Rûm : Les Romains (verset 39)</p>
<p>Le principe de « multiplication », c’est-à-dire tu récupéreras plus d’argent que tu n’en as dépensé.</p>
<p>On sait tous que ce qu’on dépense pour Dieu, Il nous le remplace.</p>
<p>Non… Non… Non,</p>
<p>Ces versets te disent que non seulement ce que tu as dépensé sera remplacé, mais que tu obtiendras beaucoup plus !</p>
<p>Aujourd’hui je vais vous raconter un exemple,</p>
<p>Car j’ai cherché un exemple concret, pour illustrer ce principe de « multiplication ».</p>
<p>J’ai cherché parmi les gens qui font beaucoup de dons,</p>
<p>Et j’ai trouvé Bill Gates…</p>
<p>Bill Gates est le patron de Microsoft,</p>
<p>Et cet homme est étonnant.</p>
<p>Sur cet homme s’applique précisément le verset <strong>{ Ceux-là verront [leurs récompenses] multipliées. }</strong></p>
<p>Comment sa fortune va être multipliée plusieurs fois, grâce à ce qu’il a fait…</p>
<p>Mais qu’a-t-il fait ?</p>
<p>Laissez-moi vous raconter comment était Bill Gates, et comment il est devenu.</p>
<p>En chiffres :</p>
<p>En 2007, Bill Gates était l’homme le plus riche sur terre.</p>
<p>Il était classé numéro 1 dans le monde,</p>
<p>Et sa fortune s’élevait à 24 milliards de dollars… C’est inimaginable,</p>
<p>Et c’était en quelle année ? En 2007.</p>
<p>Puis en 2009, qu’a fait Bill Gates ?</p>
<p>Il a fait don de toute sa fortune, sauf 4 milliards de dollars,</p>
<p>C’est-à-dire, il a donné 20 milliards de dollars, aux associations caritatives et humanitaires, pour soigner les maladies en Afrique.</p>
<p>Toutes les maladies qui sont en Afrique, ces 20 milliards vont servir à les soigner.</p>
<p>Donc en 2007, la fortune de Bill Gates était 24 milliards, et en 2009 sa fortune a baissé fortement… De 24 milliards, c’est devenu seulement 4 milliards de dollars.</p>
<p>Bill Gates n’était plus numéro 1, ni l’homme le plus riche au monde.</p>
<p>Et là surprise…</p>
<p><strong>{ Ceux-là verront [leurs récompenses] multipliées. }</strong></p>
<p>Peu importe leur religion…</p>
<p><strong>{ Ceux-là verront [leurs récompenses] multipliées. }</strong></p>
<p>La surprise c’est qu’en 2011, à votre avis combien est devenue la fortune de Bill Gates ?</p>
<p>Elle est remontée à 32 milliards de dollars !</p>
<p>Je vous donne des chiffres concrets…</p>
<p>La fortune de Bill Gates est passée de 4 milliards, à 32 milliards en deux ans.</p>
<p>Je me suis mis à réfléchir…</p>
<p>Comment Bill Gates a eu la capacité de dépenser 20 milliards de dollars ?</p>
<p>On a beaucoup d’hommes riches dans le monde arabe et musulman,</p>
<p>Personne d’entre eux ne dépense une somme aussi élevée…</p>
<p>Ni même le quart de cette somme,</p>
<p>Ni le huitième,</p>
<p>Ni même le cinquième…</p>
<p>Comment Bill Gates a pu faire ça ?</p>
<p>Vous savez pourquoi mes chers…</p>
<p>C’est pour une raison surprenante,</p>
<p>Une raison qui a un rapport avec le travail et la productivité :</p>
<p>Car il sait qu’il pourra les regagner en travaillant.</p>
<p>Et Bill Gates n’est pas le 1er à faire ça dans l’histoire de l’humanité…</p>
<p>Othman Ibn Affane (le compagnon du Prophète (bénédiction et paix soient sur lui)) était un homme riche, et il a fait don de tout son argent, puis il l’a regagné plus tard.</p>
<p>Mes chers, le verset <strong>{ Ceux-là verront [leurs récompenses] multipliées. }</strong> Nécessite 2 choses :</p>
<p>- Une conviction que Dieu te rendra ton argent.</p>
<p>- Que tu sois travailleur et bosseur.</p>
<p>Car pour prendre cette décision, tu as besoin d’être quelqu’un de productif,</p>
<p>Parce que tu es sûr que ce que tu dépenses, tu le récupéreras.</p>
<p>Pourquoi ? « Car je suis productif ».</p>
<p>En 2007, la fortune de Bill Gates était de 24 milliards,</p>
<p>En 2009, il a fait un don de 20 milliards,</p>
<p>En 2011, sa fortune était de 32 milliards,</p>
<p>Et en 2014, sa fortune est estimée à 76 milliards de dollars.</p>
<p>Travaille, sois productif,</p>
<p>Garde une confiance en Dieu,</p>
<p>Dépense pour aider les pauvres,</p>
<p>Et sois heureux du sourire d’espoir que tu obtiendras.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
