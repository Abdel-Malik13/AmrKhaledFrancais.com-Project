                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle du sentiment de responsabilité :</p>
<p>As-tu déjà testé cette sensation ?</p>
<p>C'est-à-dire tu montes dans un bus,</p>
<p>Ou tu conduis ta voiture,</p>
<p>Et tu te dis « Je suis responsable des gens autour de moi »</p>
<p>Bien sûr tu ne pourras pas être responsable de tous les passants dans la rue,</p>
<p>Mais juste te dire « Il faut que je fasse quelque chose d'utile aux gens qui sont là à cet instant »</p>
<p>Même si c'est une chose modeste...</p>
<p>Laissez-moi vous raconter l'histoire :</p>
<p>Elle nous est parvenue par email de la part d'un jeune qui s'appelle Oussama Abdel'Hamid,</p>
<p>Il raconte : J'étais dans le métro sous terre,</p>
<p>C'était l'hiver, il n'y avait pas beaucoup de monde à cette heure,</p>
<p>Et sur le quai il y'avait un homme âgé de plus de 60ans,</p>
<p>Il marchait lentement, et il avait un petit sac en plastique à la main...</p>
<p>Il est venu me voir, et m'a demandé « Mon fils, quel est le nom de cette station ? »</p>
<p>Pourtant c'était écrit en grand sur le panneau...</p>
<p>J'ai compris que cet homme ne savait pas lire,</p>
<p>Il était analphabète.</p>
<p>Alors je lui ai répondu « C'est la station des olives »</p>
<p>L'homme m'a dit « Merci mille fois mon fils »</p>
<p>Le train est arrivé,</p>
<p>Et cet homme est monté dans le même wagon que le jeune Oussama.</p>
<p>Les passagers étaient assis les visages fermés, fatigués... Comme on a l'habitude de voir,</p>
<p>Et cet homme âgé était souriant,</p>
<p>Il a sorti du popcorn,</p>
<p>Il a invité Oussama,</p>
<p>Puis il a aussi invité un autre passager à côté,</p>
<p>C'est un homme gentil, un homme de la belle époque...</p>
<p>Puis arrivés à une station, un garçon de 15 ans est monté,</p>
<p>Ce garçon vendait des journaux,</p>
<p>Il a traversé le wagon de l'avant à l'arrière,</p>
<p>Puis il semble que personne n'a acheté un de ses journaux.</p>
<p>En revenant il est passé devant l'homme âgé,</p>
<p>Alors il lui a dit « Donne-moi ce journal »</p>
<p>Oussama était confus... Il pensait que l'homme âgé ne savait pas lire,</p>
<p>Le garçon lui a donné le journal, l'homme âgé lui a donné l'argent, et il est parti.</p>
<p>Oussama dit « Je ne comprenais pas,</p>
<p>Il sait lire,</p>
<p>Alors pourquoi il n'a pas lu le panneau dans la station ? »</p>
<p>Au bout d'un moment, l'homme âgé a tendu le journal à Oussama et lui a dit</p>
<p>« Qu'est ce qui est marqué sous cette photo ? »</p>
<p>Oussama lui a dit « Monsieur, vous m'avez embrouillé...</p>
<p>Est-ce que vous savez lire ou pas ? »</p>
<p>L'homme âgé lui a dit « Non mon fils, je ne sais ni lire ni écrire. »</p>
<p>Oussama lui a demandé « Alors pourquoi vous avez acheté ce journal ? »</p>
<p>Il lui a répondu « Personne n'a donné 1 centime au petit garçon qui vendait les journaux...</p>
<p>Ce petit se déplace dans le froid,</p>
<p>Je n'ai pas voulu le décourager,</p>
<p>Alors je lui ai acheté un journal »</p>
<p>Oussama raconte</p>
<p>« Je suis descendu à mon arrêt de métro,</p>
<p>Et je me suis dit : C'est magnifique,
<p>Comment ce serait magnifique si les gens se souciaient des autres comme cet homme...</p>
<p>Pour aider un petit garçon et ne pas le décourager, il a acheté un journal alors qu'il ne sait pas lire »</p>
<p>Est-ce que nous faisons la même chose dans nos pays ?</p>
<p>Est-ce que nous sommes prêts à aider les autres de cette façon ?</p>
<p>Est-ce que chez nous il y a ce sentiment de responsabilité ?</p>
<p>Un sentiment de responsabilité à ce point...</p>
<p>Mes chers, je jure que la plus belle sensation au monde, c'est quand tu sens que tu es responsable des gens,</p>
<p>Même si tu ne les aides qu'avec des choses modestes,</p>
<p>C'est un très joli sentiment : « Je suis responsable de vous et j'ai envie de vous aider. »</p>
<p>Un sourire d'espoir pour chaque être humain qui souhaite le bien pour les autres,</p>
<p>Qui aide les autres,</p>
<p>Et qui ressent un devoir vis-à-vis des autres.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
