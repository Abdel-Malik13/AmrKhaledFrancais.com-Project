                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'un ami à moi,</p>
<p>Il s'appelle Mouataz Assayid,</p>
<p>Mouataz est un jeune comme tous les jeunes de nos pays,</p>
<p>Il dit : Quand j'étais au lycée je n'avais aucun objectif,</p>
<p>J'aimais juste jouer au foot avec mes amis,</p>
<p>Mais si tu me demandais « Que veux-tu devenir, comment-vois-tu ton avenir... Etc. »</p>
<p>Je n'en savais rien du tout...</p>
<p>Je joue juste au foot, puis la moyenne que j'obtiendrai au bac me fera entrer dans n'importe quelle université, puis voilà.</p>
<p>Mouataz raconte : J'ai obtenu mon bac, et mes notes m'ont permis d'entrer en faculté de tourisme,</p>
<p>J'y allais juste pour y aller... Dans ma tête il n'y avait pas d'objectif précis, juste obtenir un diplôme de tourisme et trouver un travail dans le domaine...</p>
<p>Jusqu'au jour où j'étais assis avec un ami, il travaillait sur son ordinateur,</p>
<p>Je lui ai demandé « Sur quel logiciel tu es en train de travailler ? »</p>
<p>Il m'a répondu « Je suis designer graphique, et je travaille sur un projet... Etc.»</p>
<p>Je lui ai dit « Fais-moi voir des exemples de tes créations »</p>
<p>Puis je lui demandais de m'apprendre petit à petit...</p>
<p>Mouataz a beaucoup aimé l'idée,</p>
<p>Alors un rêve grandissait chez lui « Je veux devenir designer graphique,</p>
<p>Et je veux devenir un des meilleurs designers au monde ! »</p>
<p>Il passait 5h ou 6h par jour à apprendre, à faire des recherches, à faire des exercices...</p>
<p>Il était étudiant à la faculté de tourisme,</p>
<p>Mais le design graphique est devenu l'histoire de sa vie.</p>
<p>Pourquoi je te raconte son histoire ?</p>
<p>Je te raconte son histoire pour t'apprendre une chose :</p>
<p>Il existe des gens qui se découragent quand ils sont attaqués... Quand ils sont très critiqués, et quand les autres se moquent d'eux... Ils perdent l'espoir.</p>
<p>Pourtant ils avaient un talent et ils pouvaient réussir...</p>
<p>Puis il existe des gens qui, quand tu les presses et quand tu te moques d'eux... Ils transforment cette énergie en désir de réussir !</p>
<p>Et toi, tu es lequel des 2 ?</p>
<p>Regardez Mouataz par exemple :</p>
<p>Il voulait devenir designer graphique, et il aimait ca,</p>
<p>Mais son papa et sa maman ont été les premiers à le casser...</p>
<p>« Pourquoi tu perds ton temps en jouant avec ton logiciel mon fils ?</p>
<p>Tu es en faculté de tourisme, révise tes cours,</p>
<p>Ou sors t'amuser avec tes amis,</p>
<p>C'est quoi ce délire dans lequel tu es ? »</p>
<p>Mouataz entendait ce genre de paroles venant de sa famille...</p>
<p>C'est décourageant n'est ce pas ?</p>
<p>Non il va les transformer en détermination pour réussir !</p>
<p>Malheureusement tous les jeunes ne sont pas comme lui...</p>
<p>Mouataz disait « Si c'est comme ca, je vais leur prouver qu'ils ont tort ! »</p>
<p>Le 1er obstacle c'était son papa et sa maman.</p>
<p>Le 2ème obstacle était encore plus difficile...</p>
<p>Il était assis dans sa classe à la faculté de tourisme,</p>
<p>Celui qui donnait le cours était un grand professeur connu,</p>
<p>Mais malheureusement les professeurs ne font pas attention à la sensibilité des jeunes...</p>
<p>Il a demandé aux étudiants « Qui ici a un projet de carrière ? »</p>
<p>Puis il a pointé du doigt Mouataz,</p>
<p>« Toi que veux-tu devenir ? »</p>
<p>Mouataz a demandé « Moi ? »</p>
<p>Le professeur lui a dit « Oui toi, que veux-tu devenir ? »</p>
<p>Mouataz s'est levé pour répondre « Moi je veux devenir designer graphique »</p>
<p>Le professeur a éclaté de rire en disant « Toi ? Le maximum que tu peux atteindre c'est laver la vaisselle dans un hôtel »</p>
<p>Toute la classe s'est mise à rire...</p>
<p>Mouataz raconte « J'ai transpiré... Je me suis assis, et à l'intérieur de moi j'avais la rage : Je jure par Dieu que je vais réussir ! »</p>
<p>D'ailleurs c'est le caractère de Mouataz qui le fera réussir :</p>
<p>« Je jure que je vais réussir !! Tu as voulu me décourager ? Alors je jure que je réussirai !</p>
<p>Et je me servirai de ce que tu as dit pour me motiver,</p>
<p>Et les moqueries des autres vont se transformer en force ! »</p>
<p>Malheureusement tous les jeunes ne sont pas comme lui...</p>
<p>La 3ème claque c'est une fois pendant qu'il était sur son ordinateur,</p>
<p>Il envoyait des emails à quelqu'un pour poser des questions,</p>
<p>Il voulait des informations sur les meilleurs designers au monde et comment ils travaillent,</p>
<p>Il a envoyé un email à un designer pour lui demander « Comment je fais pour faire ceci »</p>
<p>L'homme lui a répondu « Tu fais comme ca sur le logiciel »</p>
<p>Mouataz lui a dit « Je n'ai pas compris »</p>
<p>Il lui a encore répondu « Tu vas sur le logiciel et tu fais ca »</p>
<p>Mouataz lui a dit « Je n'ai pas compris »</p>
<p>L'homme lui a répondu « Tu es stupide ? »</p>
<p>Mouataz raconte « À ce moment j'ai arrêté la discussion,</p>
<p>Mais j'ai fait une impression d'écran de son message 'tu es stupide ?'</p>
<p>Et je l'ai mis en fond d'écran sur mon ordinateur...</p>
<p>Pour que ce message blessant se transforme en force pour réussir »</p>
<p>Chaque jour je le voyais et je pensais « Non je ne suis pas stupide ! Non je vais réussir ! »</p>
<p>Mouataz a obtenu son diplôme,</p>
<p>Et laissez-moi vous raconter ce qu'il est devenu :</p>
<p>Mouataz est un des meilleurs designers graphiques au monde.</p>
<p>Mouataz a été sélectionné parmi les 500 meilleurs designers dans un rassemblement en Espagne, organisé par le ministère espagnol de tourisme et de culture.</p>
<p>Les créations de Mouataz sont maintenant publiées dans les plus grands magazines du domaine, comme Photoshop Creative, distribué mondialement.</p>
<p>Mouataz a réussi.</p>
<p>Mouataz est un des meilleurs designers au monde.</p>
<p>Mouataz a transformé les paroles écourageantes en énergie pour réussir,</p>
<p>C'est le sourire d'espoir d'aujourd'hui,</p>
<p>Il faut que tu réagisses comme lui pour réussir dans la vie.</p>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
