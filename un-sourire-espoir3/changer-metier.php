                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Je veux commencer l'épisode d'aujourd'hui par un verset du Coran, qui dit</p>
<p><strong>{ Si Allah sait qu'il y a quelque bien dans vos cœurs, Il vous donnera mieux }</strong></p>
<p>Sourate 8 - Al-Anfâl : Le Butin (verset 70)</p>
<br>
<p>Si Dieu voit dans ton cœur une belle intention,</p>
<p>Tu vas voir à quel point il va t'honorer et t'ouvrir les portes.</p>
<p>L'histoire que je vais raconter est celle d'un homme suisse,</p>
<p>C'est un architecte,</p>
<p>Donc il n'a aucun rapport avec la nutrition, ni les produits alimentaires...</p>
<p>Ce n'est pas du tout son domaine.</p>
<p>Mais dans le cœur de cet architecte une grande intention a bougé : Celle de faire le bien,</p>
<p>Il habitait dans un village à côté de Genève,</p>
<p>Une voisine à lui a eu un bébé,</p>
<p>Et quand elle voulait allaiter son bébé, ce dernier refusait...</p>
<p>(Cette histoire date du début des années 1800)</p>
<p>Il n'existait pas à l'époque du lait industriel,</p>
<p>Tous les bébés étaient allaités naturellement,</p>
<p>Donc le monde ne connaissait que le lait de la maman.</p>
<p>Le bébé de sa voisine est né,</p>
<p>La maman voulait l'allaiter, mais le petit refusait...</p>
<p>Elle ne savait pas quoi faire,</p>
<p>Si le bébé ne se nourrit pas, il risque de mourir...</p>
<p>Quand son voisin a entendu ca, quelque chose a bougé dans son cœur,</p>
<p>Il s'est senti mal, il a eu pitié pour cet enfant.</p>
<p>Le bébé est décédé, car personne n'a pu l'allaiter.</p>
<p>Alors cet homme a décidé « Il faut que je fasse quelque chose pour que ca n'arrive à aucun autre enfant »</p>
<p>Mais ton métier c'est l'architecture...</p>
<p>Ce désir de faire le bien a bougé dans son cœur.</p>
<p>Il a improvisé un petit laboratoire dans sa maison,</p>
<p>Il prenait du lait de vache,</p>
<p>Et l'allégeait... L'allégeait...</p>
<p>Il rajoutait un peu de sucre... Un peu de farine...</p>
<p>Pour que les bébés puissent accepter de le boire,</p>
<p>Mais sans résultat.</p>
<p>Il a essayé pendant 6 mois sans y arriver...</p>
<p>Mais son intention était très belle,</p>
<p>Son intention c'est d'améliorer les conditions de vie des enfants.</p>
<p>Au bout de 6 mois, un autre bébé avait le même problème, il refusait de boire le lait de sa maman,</p>
<p>Il lui a donné le lait qu'il avait composé dans son laboratoire,</p>
<p>Et le bébé a accepté de le boire...</p>
<p>Les gens ont commencé à dire « Il y a une nouvelle invention,</p>
<p>Il y a une alternative au lait des mamans »</p>
<p>Les mamans ont commencé à venir commander le nouveau lait,</p>
<p>Il y avait des files d'attentes devant la maison de cet homme,</p>
<p>Le laboratoire a continué de grandir... Grandir...</p>
<br>
<p>Quelqu'un connait le nom de cet homme ?</p>
<p>Henri Nestlé,</p>
<p>Nestlé qui deviendra plus tard une grande entreprise présente dans tous les pays du monde...</p>
<p>Cette entreprise a commencé avec cet homme, alors qu'à la base il était architecte,</p>
<p>Imaginez un architecte devenir une des personnes qui ont amélioré la nutrition du monde entier,</p>
<p>Henri Nestlé.</p>
<p>Mais pourquoi je vous raconte cette histoire ?</p>
<p><strong>{ Si Allah sait qu'il y a quelque bien dans vos cœurs, Il vous donnera mieux }</strong></p>
<p>Sourate 8 - Al-Anfâl : Le Butin (verset 70)</p>
<p>Si tu as une belle intention dans ta vie...</p>
<p>Si dans ta vie tu décides que tu veux faire le bien...</p>
<p>Regarde comment Dieu t'ouvrira les portes.</p>
<p>Cet homme est décédé il y a de longues années,</p>
<p>Mais son entreprise est une des plus grandes au monde.</p>
<p>Et toi alors de quoi as-tu envie ?</p>
<p>Quels sont tes souhaits dans la vie ?</p>
<p>Peux-tu les arroser avec une belle intention,</p>
<p>Une intention de faire le bien,</p>
<p>Et tu verras comment Dieu te fera réussir et t'aidera.</p>
<p>À ce moment tu auras un sourire d'espoir, toi ainsi que le monde entier.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
