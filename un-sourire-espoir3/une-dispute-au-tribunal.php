                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui vient d'Arabie Saoudite,</p>
<p>C'est un fait divers étonnant publié dans les journaux,</p>
<p>C'est un homme appelé Abdelaziz Hizane,</p>
<p>C'est un homme âgé,</p>
<p>Il a perdu une affaire au tribunal,</p>
<p>Alors il s'est mis à pleurer comme un enfant...</p>
<p>Des cris et des larmes...</p>
<p>Quel genre d'affaires pousse un homme à pleurer de cette façon devant tout le monde ?</p>
<p>Est-ce que c'est une propriété qu'il a perdu ? Non pas du tout...</p>
<p>Est-ce que ses enfants lui ont fait du mal ? Non pas du tout...</p>
<p>Est-ce que son épouse a divorcé de lui ? Non plus...</p>
<p>Alors pour quelle raison il a pleuré ?</p>
<p>Abdelaziz a attaqué son frère en justice,</p>
<p>Son propre frère ! Est-ce que quelqu'un de bien peut attaquer son frère en justice ?</p>
<p>Au début tu penseras que c'est quelqu'un de mauvais,</p>
<p>Mais à la fin tu changeras complètement d'avis...</p>
<p>Quelle est l'affaire ?</p>
<p>Lui et son frère sont en dispute : Les deux veulent obtenir la garde de leur maman très âgée, qui est sur une chaise roulante, qui a plus de 95 ans...</p>
<p>Abdelaziz Hizane hébergeait sa maman chez lui,</p>
<p>Mais quand il a pris de l'âge,</p>
<p>Son petit frère lui a dit « Je suis plus jeune et je peux mieux m'occuper de maman, alors il faut qu'elle vienne chez moi »</p>
<p>La dispute c'est pour garder la maman...</p>
<p>La maman est handicapée, elle ne peut pas bouger, il faut la porter etc.</p>
<p>Et les deux se sont disputés « C'est moi qui doit la garder ! »</p>
<p>Est-ce que quelqu'un de nous s'occupe de ses parents à ce point ?</p>
<p>Mes chers, le vrai héroïsme c'est quand ton papa et ta maman deviennent très âgés...</p>
<p>L'héroïsme n'est pas quand tu es jeune et que tu dis « Je m'occupe de mon papa et de ma maman »</p>
<p>L'héroïsme c'est quand ils deviennent très vieux, et que tu insistes pour t'occuper d'eux.</p>
<p>A tous les jeunes : Est-ce que quand ton papa et ta maman vont prendre de l'âge, tu resteras aussi tendre ?</p>
<p>Imaginez... Les deux frères en sont arrivés au tribunal,</p>
<p>Et le juge ne savait pas quoi faire...</p>
<p>Il était obligé de demander l'avis de la maman, il lui a demandé « Madame, vous voulez restez avec qui ? »</p>
<p>La maman très âgée a répondu avec beaucoup de difficulté,</p>
<p>Elle a dit (en parlant d'Abdelaziz) « Lui... C'est mon œil droit »</p>
<p>« Et Lui (son frère)... C'est mon œil gauche... Alors ne me demandez pas de choisir. »</p>
<p>Le juge était obligé de prendre une décision tout seul,</p>
<p>Et il a jugé en faveur du petit frère, car il aurait plus de capacités pour s'occuper de la maman...</p>
<p>La maman devait partir de chez Abdelaziz, et vivre chez son petit frère,</p>
<p>Abdelaziz s'est assis par terre, et il pleurait comme un enfant...</p>
<p>Car sa maman va s'éloigner de lui.</p>
<p>Mes chers la miséricorde envers nos parents,</p>
<p>Un sourire d'espoir pour chaque être humain qui est clément envers ses parents,</p>
<p>Qui prend soin d'eux,</p>
<p>Et surtout quand ils prennent de l'âge.</p>
<p>Dieu (Exalté Soit-Il) dit dans le Coran</p>
<p><strong>{ Et ton Seigneur a décrété: «N'adorez que Lui; et (marquez) de la bonté envers les père et mère: si l'un d'eux ou tous deux doivent atteindre la vieillesse auprès de toi, alors ne leur dis point: «Fi!» et ne les brusque pas, mais adresse-leur des paroles respectueuses. }</strong></p>
<br>
<p>Sourate 17 - Al-Isra' : Le Voyage Nocturne (verset 23)</p>
<p>Pourquoi Dieu a dit <strong>{ Si l'un d'eux ou tous deux doivent atteindre la vieillesse auprès de toi }</strong> ?</p>
<p>Car le vrai héroïsme c'est quand ils atteignent la vieillesse...</p>
<p>Est-ce que tu seras comme Abdelaziz ?</p>
<p>Ou bien tu seras comme un lâche et tu vas les abandonner ?</p>
<p>Un sourire d'espoir pour toi : Car en fonction de ton choix, tes enfants feront la même chose avec toi quand tu vieilliras...</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
