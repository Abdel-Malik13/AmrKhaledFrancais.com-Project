                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui vient de l’Inde,</p>
<p>En ce moment je raconte beaucoup d’histoires indiennes,</p>
<p>Car j’étais en visite en Inde, et que je suis marqué par mon séjour.</p>
<p>L’Inde c’est le pays de la civilisation,</p>
<p>Le pays des sages,</p>
<p>Le pays des philosophes,</p>
<p>Et le pays des religions…</p>
<p>C’est une nation très ancienne,</p>
<p>Du coup ils ont beaucoup de légendes et beaucoup d’histoires qu’ils racontent et se transmettent,</p>
<p>Et ils aiment ça,</p>
<p>Alors je suis revenu avec beaucoup d’histoires indiennes.</p>
<p>Et parmi les histoires qui m’ont plu, et qu’ils apprennent à leurs enfants, il y a celle-ci :</p>
<p>Ça parle de la loyauté, comment être fidèle.</p>
<p>C’est une histoire imaginaire,</p>
<p>On raconte qu’un ancien roi s’est marié avec une femme,</p>
<p>Il l’a beaucoup beaucoup aimé,</p>
<p>Elle est donc devenue la reine.</p>
<p>Le roi aimait lui offrir des cadeaux,</p>
<p>Alors il a demandé à son ministre de ramener les meilleurs vêtements de chaque région en Inde.</p>
<p>Pour qu’on expose ces vêtements devant la reine, et qu’elle choisisse comment elle veut s’habiller pour l’année prochaine.</p>
<p>Le ministre a voyagé, et il a ramené une grande quantité de beaux vêtements,</p>
<p>Avec des couleurs magnifiques,</p>
<p>Puis vous savez à quel point les robes indiennes sont belles et colorées.</p>
<p>Le ministre a exposé les vêtements,</p>
<p>Et la reine ne savait pas quoi choisir…</p>
<p>Tout avait l’air beau,</p>
<p>Elle voulait que quelqu’un l’aide à choisir.</p>
<p>Le roi était là,</p>
<p>La reine ne savait pas quoi choisir,</p>
<p>Elle a regardé le ministre comme pour lui demander son avis : Qu’est ce que je dois choisir ?</p>
<p>Le ministre a fait un clin d’œil en direction d’une robe…</p>
<p>Comme pour dire : C’est la plus belle robe.</p>
<p>Malheureusement à cet instant précis, le roi a aperçu le ministre en train de faire un clin d’œil à son épouse…</p>
<p>Le ministre allait tomber dans une catastrophe,</p>
<p>Pourtant son clin d’œil était innocent,</p>
<p>Le roi a gardé ça au fond de lui, il n’a rien dit ce jour là…</p>
<p>Mais le pauvre ministre a paniqué,</p>
<p>Il a transpiré,</p>
<p>Il ne savait pas comment réagir,</p>
<p>Ça ne se fait pas d’aller parler au roi d’une chose pareille…</p>
<p>S’il lui dit que son intention était innocente, il ne saura pas comment sera la réaction du roi…</p>
<p>Le ministre a réfléchi à une solution,</p>
<p>Et il a décidé de faire désormais semblant que son œil est malade,</p>
<p>Et qu’il cligne tout le temps…</p>
<p>Alors depuis ce jour là, chaque fois qu’il était en présence du roi,</p>
<p>Il clignait de l’œil…</p>
<p>Il a continué à faire ça pendant 1 mois,</p>
<p>Puis 2 mois,</p>
<p>Puis 3 mois…</p>
<p>Pour que le roi ne fasse pas de lien entre le clin d’œil et la reine.</p>
<p>Le ministre a pensé que c’était la seule solution à son problème,</p>
<p>Le pauvre ministre a continué à cligner de l’œil, pas 1 an ou 2,</p>
<p>Mais tout le temps,</p>
<p>C’est devenu une habitude qui ne le quittait plus.</p>
<p>Jusqu’au jour où le roi allait mourir,</p>
<p>Et qu’il a fait venir son fils pour lui succéder,</p>
<p>Et parmi les recommandations qu’il lui a dites :</p>
<p>« Mon fils,</p>
<p>Je te recommande de garder ce ministre,</p>
<p>Car pendant 40 ans, il s’est excusé pour une faute qu’il n’a pas commise »</p>
<p>Le roi savait que son ministre n’a pas eu de mauvaise intention,</p>
<p>Mais il n’a rien dit,</p>
<p>Car c’est une affaire délicate…</p>
<p>Qu’est ce qu’il pouvait lui dire ?</p>
<p>« Je savais que tu n’a pas fait un clin d’œil à mon épouse »</p>
<p>Son statut de roi, l’empêchait de dire ça…</p>
<p>Mais il a apprécié le fait que le ministre se soit excusé pendant 40 ans pour une erreur qu’il n’a pas commise.</p>
<p>Un sourire d’espoir pour la loyauté,</p>
<p>Un sourire d’espoir pour l’intelligence,</p>
<p>Un sourire d’espoir pour les personnes qui savent comment bien réagir,</p>
<p>Et un sourire d’espoir pour la gratitude.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
