                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit : Il y a une différence entre le radin avec sa poche, et le radin avec ses sentiments...</p>
<p>Il y a l'avarice des poches, mais il y a pire : C'est l'avarice des sentiments.</p>
<p>Ne pas dire un mot gentil à ton épouse ou à ton mari,</p>
<p>Ne pas lui faire un câlin,</p>
<p>Ne pas prendre en considération ses sentiments.</p>
<p>À l'intérieur de toi il y a de belles choses, mais tu ne les exprimes pas, tu ne dis rien... C'est de la radinerie.</p>
<p>Vous connaissez le verset du Coran qui dit <strong>{ Puisque les âmes sont portées à la ladrerie }</strong></p>
<p>Sourate 4 - An-Nissa : Les Femmes (verset 128)</p>
<p>Ladrerie = Avarice mesquine.</p>
<p>Il existe des âmes avares que Dieu fera venir le jour du jugement dernier, et à qui Il demandera pourquoi elles l'étaient...</p>
<p>Je jure mes chers que la radinerie des âmes est pire que la radinerie des poches,</p>
<p>Laissez-moi vous raconter l'histoire :</p>
<p>C'est une fille diplômée en beaux arts,</p>
<p>Elle dessine des portraits et des tableaux...</p>
<p>Cette fille aime beaucoup son papa et sa maman,</p>
<p>Elle avait envie de réunir leurs cœurs et de les rapprocher,</p>
<p>Le papa ne parlait pas beaucoup, il n'exprimait pas ses sentiments...</p>
<p>Et la maman avait besoin que son mari lui dise des belles choses.</p>
<p>Alors la fille artiste a voulu rapprocher ses parents,</p>
<p>(Si vous êtes jeunes, faites comme elle, essayez de rapprocher vos parents).</p>
<p>Cette fille a décidé de dessiner un portrait de ses parents réunis, et de le leur offrir pour l'anniversaire de leur mariage.</p>
<p>Elle a pris une vieille photo de ses parents quand ils étaient jeunes,</p>
<p>Puis elle s'est mise à dessiner.</p>
<p>Elle le faisait en cachette dans sa chambre, pour leur faire la surprise le jour d'anniversaire de leur mariage.</p>
<p>Pendant qu'elle finissait son dessin, son papa est entré dans sa chambre...</p>
<p>Elle l'a vite caché,</p>
<p>Son papa lui a demandé « Tu fais quoi ? »</p>
<p>Elle lui a répondu « Rien de spécial, je dessine comme d'habitude »</p>
<p>Il s'est assis...</p>
<p>Elle raconte « J'ai senti une force à l'intérieur de moi qui me poussait à lui montrer le dessin »</p>
<p>Elle lui a dit « Je vais t'avouer un secret, mais tu me promets de ne pas le dire à maman ? »</p>
<p>Il a regardé le portrait et il a dit « C'est magnifique, comme les yeux de ta maman son beaux,</p>
<p>Tu sais que j'aime beaucoup ta maman ?</p>
<p>Tu sais que quand je regarde le visage de ta maman, je me sens rassuré ?</p>
<p>Ce portrait est très beau, et puisque tu me l'as montré,</p>
<p>Je vais acheter un cadre pour le mettre dedans,</p>
<p>Comme ça le jour de l'anniversaire, tu auras offert le portrait et moi j'aurai offert le cadre,</p>
<p>Puis je vais en profiter pour acheter une carte de vœux et y écrire quelques mots à ta maman,</p>
<p>Mais ne la lis pas avant le jour d'anniversaire. »</p>
<p>Le papa a ramené une carte de vœux dans une enveloppe fermée,</p>
<p>Il a ramené le cadre pour mettre le portrait,</p>
<p>Mais avant la date d'anniversaire, le papa est décédé...</p>
<p>C'est le destin de Dieu.</p>
<p>Et la maman était très triste, elle a perdu son mari...</p>
<p>Le jour d'anniversaire est arrivé,</p>
<p>Et la fille raconte « Je suis devenue hésitante,</p>
<p>Mon papa n'est plus là...</p>
<p>Est-ce que je montre le portrait à maman ou bien je ne lui dis rien ? »</p>
<p>Après réflexion elle a décidé de le lui montrer...</p>
<p>Elle a dit à sa maman « Je vous ai dessinés tous les deux »</p>
<p>Puis elle s'est rappelée que son papa avait laissé une enveloppe,</p>
<p>Ils ont ouvert la carte de vœux,</p>
<p>Et le papa y avait écrit : « Ma fille m'a rappelé à quel point je t'aime,</p>
<p>Je continuerai à regarder tes yeux pour me rassurer... Tant que je suis en vie »</p>
<p>La maman a pris la carte, et elle a dit « Jamais il ne m'a dit des paroles comme celles-ci,</p>
<p>Si seulement il m'avait dit ça pendant qu'il était vivant,</p>
<p>J'avais besoin de l'entendre... S'il ressentait ça, pourquoi il ne me le disait pas... »</p>
<p>L'histoire est terminée.</p>
<p>Un sourire d'espoir pour chaque mari qui dit des mots doux à son épouse,</p>
<p>Même si parfois il y a des désaccords...</p>
<p>Juste pour dessiner un sourire sur son visage et sur le visage de ses enfants,</p>
<p>Et pour ne pas être une âme radine, même en étant généreux avec son argent.</p>
<p>Dessine un sourire d'espoir en disant un mot doux à ta femme ou à ton mari.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
