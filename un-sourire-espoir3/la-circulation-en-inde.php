                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Le sourire d'espoir d'aujourd'hui vient de l'Inde,</p>
<p>J'étais en voyage en Inde,</p>
<p>Et bien sûr la population indienne c'est plus d'un milliard de personnes,</p>
<p>J'avais des visites à faire, une conférence et du travail,</p>
<p>Je devais faire beaucoup de déplacements,</p>
<p>Je m'attendais à beaucoup souffrir dans les transports,</p>
<p>Déjà quand on voit la circulation au Caire la capitale d'Égypte (80 millions d'habitants)...</p>
<p>Alors je m'attendais au pire en allant à New Delhi, la capitale d'un pays qui compte plus d'1 milliard d'habitants.</p>
<p>Je me suis préparé psychologiquement avant d'y aller.</p>
<p>Et en arrivant à New Delhi, c'était la grande surprise...</p>
<p>En Inde, et à New Delhi, la circulation est très fluide,</p>
<p>C'est quoi ça ?</p>
<p>Le maximum que j'ai passé devant les feux rouges, c'est 4 ou 5 minutes...</p>
<p>Je me suis dit : C'est le 1er jour... Peut-être que c'est un jour férié.</p>
<p>Mais le 2ème jour... Le 3ème jour...</p>
<p>Je suis resté 5 jours en Inde,</p>
<p>Et je n'ai pas vu de bouchons...</p>
<p>En réalité je ne croyais pas mes yeux,</p>
<p>J'étais obligé de demander,</p>
<p>Car je voulais comprendre comment ces gens ont réglé le problème de la circulation,</p>
<p>Ils m'ont dit : L'Inde a fait 3 grandes choses,</p>
<p>1- Ils ont informatisé tous les services publics,</p>
<p>Tout ce qui est administratif se fait par internet,</p>
<p>Ça signifie il n'existe plus une chose appelée « Je vais chercher une autorisation »</p>
<p>Ou « Je vais déposer les documents pour obtenir mon passeport »</p>
<p>Et il n'y a pas une chose appelée « Je vais payer une facture »</p>
<p>Tout se fait par ordinateur... Vraiment tout.</p>
<p>Aïe... L'Inde nous a beaucoup devancés.</p>
<p>Donc la 1ère chose c'est que tout ce qui est administratif se fait en ligne sur internet.</p>
<p>La 2ème chose qu'ils ont faite pour régler le problème de la circulation c'est :</p>
<p>Il n'y a pas de magasins ou de boutiques dans les rues principales,</p>
<p>Ni même dans les rues secondaires...</p>
<p>Ah bon ? Et comment font les gens pour faire leurs courses ?</p>
<p>Ils ont construit d'énormes centres commerciaux aux coins de la capitale,</p>
<p>Du coup, tout ce que tu veux acheter se trouve au même endroit,</p>
<p>Et tu trouves un immense parking pour te garer.</p>
<p>Mais dans les rues de la ville ? Tu ne trouves aucun commerce...</p>
<p>Ce qui fait que pas 1 seule fois, je n'ai vu à New Delhi une voiture stationnée en double file... Ni même en première file.</p>
<p>La 3ème chose qu'ils ont faite, c'est qu'ils ont mis en place un emploi du temps très précis,</p>
<p>Ils ne plaisantent pas...</p>
<p>Les écoles commencent à telle heure,</p>
<p>Et 30 min après, les universités,</p>
<p>Puis les administrations ouvrent à telle heure...</p>
<p>Ils ont mis en place ces horaires pour entrer et sortir,</p>
<p>Pour que les rues ne soient pas bouchées,</p>
<p>La circulation est fluide, car tout le monde ne sort pas à la même heure...</p>
<p>Je leur ai demandé « Et le reste de la journée ? »</p>
<p>Ils m'ont répondu « Le reste de la journée les gens travaillent »</p>
<p>Il n'existe pas une chose appelée « les rues sont pleines » car chacun est à son travail ou à son école.</p>
<p>En voyant ça, je me suis rappelé de notre état...</p>
<p>Et je me suis rappelé que pendant la journée, personne ne reste à son poste,</p>
<p>Les rues sont tout le temps pleines, car toute la journée les gens n'arrêtent pas de bouger,</p>
<p>Il n'y a pas une chose appelée un emploi du temps,</p>
<p>Et il n'y a pas une chose appelée services administratifs en ligne,</p>
<p>Et des commerces partout dans la ville,</p>
<p>Et des voitures garées en première et en double file...</p>
<p>Peut-on apprendre cette leçon de l'Inde,</p>
<p>Pour dessiner un sourire d'espoir dans les rues de nos pays ?</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
