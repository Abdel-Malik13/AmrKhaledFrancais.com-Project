                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui dit : Le nombre d’occasions dans la vie est égal au nombre d’êtres humains.</p>
<p>Ça veut dire quoi ?</p>
<p>Les gens qui disent : Il n’y a pas d’opportunités …</p>
<p>C’est la crise, il n’y a pas de travail…</p>
<p>Personne ne me donne l’occasion de réussir…</p>
<p>Il y a du chômage… Etc.</p>
<p>Moi je dis le contraire !</p>
<p>Je dis : qu’il y a autant d’occasions de réussir dans la vie, qu’il y a d’êtres humains sur terre.</p>
<p>Tu as juste besoin de chercher,</p>
<p>De faire l’effort,</p>
<p>D’insister,</p>
<p>De creuser dans la roche,</p>
<p>Et de réfléchir en dehors de la boîte (« Think outside the box » en anglais).</p>
<p>Tu dois trouver de nouvelles idées,</p>
<p>Tu verras alors que les occasions vont se présenter !</p>
<p>Et tu te rendras compte que tu te trompais, quand tu voyais la vie étroite et bouchée…</p>
<p>L’histoire que je vais vous raconter est celle d’un homme pakistanais,</p>
<p>Il est musulman,</p>
<p>Et il vit en Autriche,</p>
<p>Cet homme a ouvert un restaurant Halal…</p>
<p>Il servait de la viande, du poulet etc.</p>
<p>Mais il a commis une erreur…</p>
<p>Laquelle ?</p>
<p>Il a ouvert son restaurant dans un quartier chic de Vienne,</p>
<p>Puis ce quartier était plein d’autres restaurants,</p>
<p>Alors la concurrence était très féroce…</p>
<p>Puis il y avait aussi des grandes chaînes internationales de restaurants,</p>
<p>Du coup son projet ne marchait pas du tout…</p>
<p>Il n’avait pas mis un panneau disant que son restaurant était Halal,</p>
<p>Il voulait juste travailler,</p>
<p>Mais son projet n’avançait pas…</p>
<p>Il allait bientôt faire faillite et devoir fermer son restaurant,</p>
<p>C’est là qu’il a eu une idée qui parait folle…</p>
<p>Mais c’est une occasion des occasions de la vie,</p>
<p>Il a réfléchi en dehors de la boîte :</p>
<p>Qu’a-t-il fait ?</p>
<p>Il a décidé de faire une promotion à durée limitée,</p>
<p>Et quelle est cette offre ?</p>
<p>Il a mis un panneau devant son restaurant, avec marqué en anglais « Mange ce que tu veux, et paye ce que tu veux »</p>
<p>(Eat what you want, and pay what you want),</p>
<p>Et pendant cette promotion, son restaurant est devenu un buffet ouvert,</p>
<p>Tu peux manger autant de nourriture que tu veux.</p>
<p>Bien sûr on se dit que son idée ne peut pas fonctionner…</p>
<p>Mais il l’a appliquée en plein centre ville de Vienne,</p>
<p>Les gens entraient manger,</p>
<p>Certains payaient 1 euro…</p>
<p>D’autres payaient 2 euros…</p>
<p>Et d’autres 5 euros…</p>
<p>Puis certains lui disaient juste « Merci » et partaient sans rien payer…</p>
<p>Mais parce que son restaurant est dans un quartier chic,</p>
<p>Certains clients riches venaient, ils étaient satisfaits de la nourriture,</p>
<p>Alors en partant ils lui laissaient 200 euros…</p>
<p>Ces 200 euros, compensaient les clients qui ne payaient presque rien.</p>
<p>Et certains riches appréciaient son idée originale « Mange ce que tu veux, et paye ce que tu veux »</p>
<p>Avant de partir ils lui demandaient « Combien de personnes ne te payent rien ? »</p>
<p>Il leur répondait « Beaucoup »</p>
<p>Ils lui disaient « Tiens voici 500 euros »</p>
<p>C’est beaucoup plus que ce qu’ils avaient mangé…</p>
<p>Au bout d’un moment, l’homme a retiré « Durée limitée »,</p>
<p>C’est devenu le concept du restaurant.</p>
<p>D’ailleurs il existe toujours à Vienne,</p>
<p>Et si vous visitez Vienne, vous verrez son panneau « Mange ce que tu veux, et paye ce que tu veux »</p>
<p>Cet homme s’apprête à ouvrir une 2ème branche de son restaurant,</p>
<p>Et « Mange ce que tu veux, et paye ce que tu veux » est devenu le nom du restaurant.</p>
<p>Je ne te demande pas d’appliquer la même idée que lui, car tu pourrais faire faillite,</p>
<p>Mais je te donne cet exemple,</p>
<p>Pour te dire : Fais preuve d’originalité, et réfléchis en dehors de la boîte.</p>
<p>Le nombre d’occasions dans la vie est égal au nombre d’êtres humains.</p>
<p>À ce moment tu verras un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
