                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'un personnage égyptien, arabe, connu mondialement...</p>
<p>Un célèbre grand personnage,</p>
<p>Mais avant de dire qui c'est, et avant de dire ce qu'il a fait,</p>
<p>J'ai envie de commencer par dire une chose importante :</p>
<p>Mes chers, il y a des gens qui ont un seul rêve toute leur vie...</p>
<p>Et le jour où ils réalisent leur rêve,</p>
<p>Ca y est...</p>
<p>Ils sont finis,</p>
<p>Sa vie est terminée alors qu'il n'a que 40 ans...</p>
<p>Pourquoi ? Car il ne vivait que pour un seul rêve, et il a vécu prisonnier de ce rêve,</p>
<p>Comme s'il avançait sur des rails,</p>
<p>Et que le train est arrivé au terminus...</p>
<p>Après 40 ans, il vit juste pour vivre...</p>
<p>Il vit avec le souvenir d'avoir eu un rêve et de l'avoir réalisé.</p>
<p>Puis il existe des gens qui se renouvellent !</p>
<p>Dès qu'ils réalisent un rêve, ils passent au suivant,</p>
<p>« Non je ne suis pas prisonnier, je ne m'enferme pas dans un seul rêve... »</p>
<p>Ils trouvent un nouveau rêve, et ils recommencent de zéro...</p>
<p>La vie de ce genre de personnes est très belle.</p>
<p>Le personnage dont parle cet épisode, fait partie de ces personnes.</p>
<p>Quand il était enfant, il a rêvé de devenir champion d'Égypte en Ping-pong ...</p>
<p>Il raconte qu'il s'entraînait 7 heures par jour,</p>
<p>Imaginez...</p>
<p>Des entraînements, de la préparation,</p>
<p>Il dormait très tôt,</p>
<p>Il se consacrait entièrement à son rêve...</p>
<p>« Je veux devenir champion d'Égypte en Ping-pong »</p>
<p>Et en effet, il est devenu champion d'Égypte,</p>
<p>Il a rejoint l'équipe nationale,</p>
<p>Et il a voyagé pour participer aux jeux méditerranéens,</p>
<p>Puis il a voyagé pour représenter l'Égypte au championnat d'Afrique,</p>
<p>Il a rencontré beaucoup de succès.</p>
<p>Puis au bout d'un moment il s'est demandé « J'ai réussi dans le Ping-pong... Je fais quoi maintenant, je reste à ne rien faire ?</p>
<p>Non je passe à autre chose !</p>
<p>C'est quoi mon nouveau rêve ?</p>
<p>Qu'est ce que j'aime...</p>
<p>J'aime les hôtels et la vie des hôtels,</p>
<p>Je veux devenir directeur d'un des plus grands hôtels dans le monde,</p>
<p>Que ce soit aux États-Unis, en Europe ou au Canada... »</p>
<p>Mais ca n'a aucun rapport avec le Ping-pong...</p>
<p>Cet homme est comme ca, il ne vit pas en s'enfermant dans une seule case,</p>
<p>Pour continuer à avoir un sourire d'espoir...</p>
<p>Il a commencé à apprendre 3 langues,</p>
<p>Faites attention, quand il rêve, il ne reste pas sans rien faire,</p>
<p>Non... Non... Non, il s'active et s'entraîne,</p>
<p>Albert Einstein dit « La réussite c'est 99% d'efforts et 1% d'intelligence »</p>
<p>Des efforts, de l'entraînement...</p>
<p>Il a appris à parler 3 langues : Anglais, Français et Italien.</p>
<p>Puis il a suivi des formations en gestion hôtelière,</p>
<p>Il apprenait... Il obtenait des diplômes... Il agrandissait ses capacités,</p>
<p>C'est un nouveau rêve, qui n'a rien à voir avec le Ping-pong,</p>
<p>Puis il est parti au Canada,</p>
<p>Mais en déposant ses candidatures, ils lui ont dit « Tu vas travailler comme gardien de nuit »</p>
<p>Il a commencé comme ca,</p>
<p>Puis il a travaillé en cuisine pour laver la vaisselle...</p>
<p>Puis il a travaillé comme adjoint à la personne qui emmène les repas dans les chambres,</p>
<p>Il devait juste la suivre et l'aider...</p>
<p>Puis il a obtenu une promotion,</p>
<p>Puis il a obtenu une autre promotion,</p>
<p>Il a commencé tout en bas de l'échelle,</p>
<p>Mais il a pris de l'expérience,</p>
<p>Et il a obtenu un diplôme là-bas...</p>
<p>Puis il parlait 3 langues,</p>
<p>Et il est devenu vice-directeur de l'hôtel.</p>
<p>Puis en 1999 il est devenu le 1er égyptien à diriger un grand hôtel au Canada...</p>
<p>Et la même année, il a remporté le prix du meilleur gérant d'hôtel en Amérique du Nord...</p>
<p>Ah bon, qui est cet homme ?</p>
<p>Après ca, il s'est dit « Mon rêve s'est réalisé... On arrête ici ?</p>
<p>Non, on passe à autre chose ! »</p>
<p>Mais attends, tu changes tout le temps de rêve ??</p>
<p>Ca y est tu es déjà arrivé au sommet...</p>
<p>Non je ne peux pas, je vais étouffer si je reste enfermé dans le même rêve !</p>
<p>C'est quoi son nouveau rêve?</p>
<p>Il s'est rendu compte qu'il savait parler aux gens... Et qu'il avait ce talent,</p>
<p>Il a suivi des formations, il a obtenu des diplômes et il a suivi un Master en développement personnel,</p>
<p>Puis il a commencé à donner des conférences pour encourager les gens à réussir dans leurs vies...</p>
<p>Puis il a écrit des livres...</p>
<p>Puis il a voyagé dans le monde entier...</p>
<p>Puis il est revenu en Égypte...</p>
<p>Puis il a fait des émissions à la télévision...</p>
<p>Qui est cet homme ?</p>
<p>C'est Dr. Ibrahim El Feki (paix à son âme)</p>
<p>Imaginez il est passé du Ping-pong,</p>
<p>À directeur d'un des plus grands hôtels au monde,</p>
<p>À plus grand coach de développement personnel dans le monde arabe,</p>
<p>Et il a continué à motiver les gens pour qu'ils réussissent dans leurs vies, jusqu'au jour de sa mort...</p>
