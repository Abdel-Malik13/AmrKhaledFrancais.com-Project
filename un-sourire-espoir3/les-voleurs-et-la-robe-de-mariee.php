                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est une histoire que j'ai entendue sur les gens et leur authenticité,</p>
<p>Et malgré tout ce qui est dit, tout ce qu'on voit, et les erreurs commises...</p>
<p>Bien sûr il existe des choses qui nous déplaisent dans nos pays,</p>
<p>Et des mauvais comportements que font certaines personnes,</p>
<p>Mais à l'intérieur les gens restent authentiques,</p>
<p>Même si une personne est mauvaise, à l'intérieur d'elle restera toujours une part de bonté et de bienveillance.</p>
<p>L'histoire c'est qu'il y a 2 semaines, je faisais une Omra (petit pèlerinage) à la Mecque et à Médine,</p>
<p>Quand j'ai croisé un homme saoudien qui m'a raconté une anecdote surprenante.</p>
<p>Il m'a dit :</p>
<p>Mes oncles maternels sont égyptiens,</p>
<p>Moi je suis saoudien bien sûr, et j'ai un passeport saoudien,</p>
<p>Mais une partie de ma famille du côté de ma mère est égyptienne.</p>
<p>Je vis en Arabie Saoudite,</p>
<p>Et il y a un an, je suis parti visiter l'Égypte,</p>
<p>La situation du pays n'était pas stable,</p>
<p>Il y avait des problèmes d'insécurité Etc.</p>
<p>Il dit : Je suis arrivé au Caire,</p>
<p>Puis j'ai décidé de rendre visite au reste de ma famille dans une ville appelée Mansourah (au nord),</p>
<p>Ils m'ont dit « Comment tu vas faire pour y aller ? »</p>
<p>Je leur ai répondu « Quand je suis en Égypte, j'aime conduire »</p>
<p>Ils m'ont dit « Oui, mais en ce moment la situation est dangereuse »</p>
<p>Je leur ai répondu « Non, ne vous inquiétez pas, il n'y aura pas de problèmes »</p>
<p>Ils m'ont dit « Puisque tu y vas, il y a une fille dans un orphelinat à Mansourah,</p>
<p>Elle a grandi, et va bientôt se marier,</p>
<p>Alors on va te donner une valise pleine de vêtements à offrir à cette jeune mariée,</p>
<p>Puis on va te donner une robe de mariée, pour aider au mariage de cette fille orpheline modeste »</p>
<p>Il leur a répondu « Oui bien sûr »</p>
<p>Il a mis la valise pleine de vêtements dans le coffre de sa voiture,</p>
<p>Et il a posé la robe de mariée sur le siège arrière.</p>
<p>Il a pris la route en pleine nuit,</p>
<p>Mais sur le chemin, il a croisé des bandits,</p>
<p>Ils ont fait un barrage sur la route...</p>
<p>Ce n'était pas une plaisanterie,</p>
<p>C'est une bande de criminels qui arrêtaient les voitures,</p>
<p>Ils lui ont demandé de descendre,</p>
<p>« Donne ton portefeuille,</p>
<p>Enlève ta montre »</p>
<p>Ils ont pris les valises dans le coffre,</p>
<p>Puis ils ont vidé ses poches,</p>
<p>Ils ont vu qu'il avait un passeport saoudien...</p>
<p>Alors forcément ils se sont dit qu'il était riche,</p>
<p>Le chef de la bande, prenait le maximum de ce qu'il pouvait prendre,</p>
<p>Et en regardant le siège arrière, il a aperçu la robe de mariée...</p>
<p>Il lui a dit « C'est quoi ça ? »</p>
<p>L'homme lui a répondu « C'est pour une fille orpheline qui va se marier,</p>
<p>Mes oncles m'ont demandé de lui emmener cette robe pour l'aider dans son mariage »</p>
<p>Le chef de la bande lui a dit « Cette robe est pour une fille orpheline ? »</p>
<p>L'homme lui a répondu « Oui »</p>
<p>Il lui a dit « Donc tu viens d'Arabie Saoudite, pour prendre cette robe et l'emmener à Mansourah ? »</p>
<p>L'homme lui a répondu « Oui »</p>
<p>Le chef de la bande lui a dit « Tu viens couvrir nos filles, les aider à se marier...</p>
<p>Et on va prendre ton argent ? »</p>
<p>Il a dit aux hommes de sa bande « Rendez-lui son argent...</p>
<p>Rendez-lui son argent,</p>
<p>Il vient couvrir les filles de notre pays »</p>
<p>L'homme a remis sa montre,</p>
<p>Il a repris ses valises et ses affaires,</p>
<p>Mais à la fin ils restent des bandits.</p>
<p>Il leur a dit « Pourquoi vous ne vous repentez pas ?</p>
<p>Repentez-vous et commencez une nouvelle vie,</p>
<p>Vous pouvez être mieux que ça,</p>
<p>Au lieu de faire ce que vous êtes en train de faire »</p>
<p>Il raconte « J'ai vu dans les yeux de certains d'entre eux, qu'ils avaient envie de se repentir »</p>
<p>L'histoire s'est terminée,</p>
<p>Mais je vous la raconte pour vous dire qu'il existe chez tous les êtres humains des gênes de bonté,</p>
<p>On doit préserver ces gênes,</p>
<p>Imaginez si des bandits ont fait ce geste,</p>
<p>Alors que dire des gens bien,</p>
<p>Préservons ce qu'il y a de plus beau chez nous, pour garder un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
