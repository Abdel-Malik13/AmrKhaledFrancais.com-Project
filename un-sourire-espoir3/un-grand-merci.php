                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de l’intégrité…</p>
<p>Et en réalité cette histoire ne s’est pas passée dans notre monde arabe ni musulman,</p>
<p>Elle est arrivée dans l’occident dans une petite ville.</p>
<p>Mais on a également beaucoup d’histoires d’intégrité dans nos pays,</p>
<p>C’est-à-dire nous ne devons pas croire que les bonnes choses ne se passent qu’à l’étranger…</p>
<p>Non, chez nous aussi il ya des histoires magnifiques d’intégrité,</p>
<p>On les raconte beaucoup.</p>
<p>Mais dans l’histoire d’aujourd’hui il y a une chose en plus…</p>
<p>Ce n’est pas simplement l’intégrité d’un être humain,</p>
<p>Mais comment la société entière encourage l’intégrité.</p>
<p>Dans tous nos pays il existe des gens intègres…</p>
<p>Mais la question c’est : Quand on croise quelqu’un d’intègre, est ce qu’on lui fait des compliments, on l’encourage et on le remercie ?</p>
<p>Pour semer chez nos enfants, et semer dans notre société le sentiment que l’intégrité est une chose très grande ?</p>
<p>Ou bien on pense : « S’il est intègre, c’est une chose qui ne concerne que lui » ?</p>
<p>Je vais vous raconter l’histoire pour que vous compreniez mieux :</p>
<p>C’est arrivé dans une petite ville, dans un pays occidental,</p>
<p>C’est une histoire vraie, elle s’est passée en 2013, donc elle est toute récente.</p>
<p>Un homme était très pauvre,</p>
<p>C’est un SDF,</p>
<p>C’est-à-dire sans domicile fixe,</p>
<p>Il vit dans la rue,</p>
<p>Et fouille dans les poubelles pour trouver à manger…</p>
<p>Malheureusement on voit des choses comme ça.</p>
<p>Cet homme était très très très pauvre.</p>
<p>Et un jour en fouillant dans une poubelle en espérant trouver la moitié d’un sandwich ou quelque chose à manger…</p>
<p>Il a trouvé une valise,</p>
<p>Il s’est demandé ce qu’elle faisait là.</p>
<p>Puis quand il l’a ouverte,</p>
<p>Il a trouvé 40.000 dollars à l’intérieur…</p>
<p>Quel est ton avis ?</p>
<p>40 mille dollars.</p>
<p>Faites bien attention : L’intégrité c’est une vertu,</p>
<p>Elle n’a rien à voir avec la situation des gens.</p>
<p>L’intégrité c’est un principe,</p>
<p>L’intégrité est une chose inconditionnelle !</p>
<p>Tu ne peux pas dire « Mais… Ma situation m’obligeait »</p>
<p>Ou « Mais… Ceci ou cela »</p>
<p>Ça ne marche pas comme ça.</p>
<p>Cet homme a pris les 40.000 dollars,</p>
<p>Et il ne possédait même pas 10 dollars dans sa poche…</p>
<p>Il a pris la valise,</p>
<p>Et il n’a pas touché à 1 seul billet,</p>
<p>Il est parti directement au commissariat,</p>
<p>Et il a remis la valise.</p>
<p>Quel est votre avis ?</p>
<p>C’est beau ?</p>
<p>Oui c’est beau,</p>
<p>C’est une belle histoire d’intégrité.</p>
<p>Mais ce n’est pas pour cette raison que je vous raconte cette histoire.</p>
<p>Je vous la raconte pour ce qui va suivre :</p>
<p>Qu’est ce qui va se passer par la suite ?</p>
<p>Les habitants de la ville… Tous les habitants ont décidé de faire une campagne d’appel aux dons,</p>
<p>Pour récompenser son intégrité.</p>
<p>Et ils ont collé ses photos partout dans la ville, avec marqué en dessous : « Thank you » (Merci).</p>
<p>Remarquez comment la société réagit,</p>
<p>Ce n’est pas seulement lui rassembler de l’argent,</p>
<p>Non… Non… Non,</p>
<p>Nous voulons le remercier,</p>
<p>Il faut que les gens sentent que celui qui fait ça, sa valeur augmente au milieu de la société.</p>
<p>Ce n’est pas seulement « Oui… C’est bien, c’est quelqu’un d’intègre. »</p>
<p>Non… Non… Non,</p>
<p>Ils lui ont donné une grande importance.</p>
<p>La campagne d’appel aux dons a été lancée,</p>
<p>Et savez-vous combien ils ont rassemblé ?</p>
<p>85.000 dollars,</p>
<p>Soit plus du double de l’argent qu’il avait trouvé dans la valise…</p>
<p>Quel est votre avis ?</p>
<p>De cette manière, la société va sentir que l’intégrité c’est une chose très belle,</p>
<p>Car ils ont remboursé cet homme matériellement,</p>
<p>Mais moralement aussi.</p>
<p>Ils l’ont récompensé moralement quand ils ont collé ses photos,</p>
<p>Et ils l’ont récompensé matériellement quand ils lui ont donné beaucoup plus que la somme d’argent dans la valise.</p>
<p>Puis vous imaginez que si vous rassemblez 85.000 dollars dans une petite ville,</p>
<p>C’est que la plupart des habitants ont donné quelque chose…</p>
<p>Même les personnes dans des situations financières difficiles, ont fait un petit don.</p>
<p>Mes chers : Donner de l’estime à l’intégrité, est parfois plus important que l’intégrité elle-même…</p>
<p>Car estimer l’intégrité, fera naître un nouveau bon comportement, puis un 2ème, puis un 3ème…</p>
<p>Et ça fixera cette qualité chez les générations à venir.</p>
<p>Du coup on garantit un sourire d’espoir chez nos enfants, et chez les générations qui suivront.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
