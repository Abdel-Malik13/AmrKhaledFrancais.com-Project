                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit :</p>
<p>Il y a des gens qui transforment leurs souffrances dans la vie et leurs problèmes,</p>
<p>Malheureusement ils les transforment en haine... En aigreur... En dépression... Pour faire souffrir les autres.</p>
<p>Mais il y a d'autres personnes qui transforment leurs souffrances et leurs problèmes, en joie... En espoir... En rêves... Pour rendre heureux les autres.</p>
<p>Et toi de quel type de personnes fais-tu partie ?</p>
<p>Écoutons cette histoire :</p>
<p>Fais-tu partie des personnes, qui lorsque des problèmes ou des catastrophes leurs tombent dessus... Les transforment en haine envers les autres ?</p>
<p>En jalousie des autres ?</p>
<p>En amertume envers les autres ?</p>
<p>Ou bien quand une catastrophe t'arrive dans la vie, ou un mal, ou un problème...</p>
<p>Tu te demandes « Comment transformer mon problème en bonheur pour les autres ? »</p>
<p>Laissez-moi vous raconter l'histoire du jour :</p>
<p>Il est né en 1809, dans un petit village appelé Coupvray, à 40 km de Paris en France,</p>
<p>C'était un enfant avec de très beaux yeux, et très intelligent...</p>
<p>Son papa travaillait comme tailleur,</p>
<p>Mais pas un tailleur normal...</p>
<p>Il utilisait de grandes aiguilles pour fabriquer des objets en cuir, et des selles pour chevaux...</p>
<p>Donc c'était de la couture lourde,</p>
<p>Quand il avait 4 ans, un jour il était en train de jouer dans l'atelier de son papa...</p>
<p>Son papa était occupé,</p>
<p>Il a vu une grosse aiguille,</p>
<p>Il l'a prise pour jouer comme n'importe quel enfant...</p>
<p>Il a pris un marteau et il voulait enfoncer l'aiguille dans un morceau épais de cuir,</p>
<p>L'aiguille a glissé,</p>
<p>Elle l'a blessé dans l'œil...</p>
<p>La blessure était profonde,</p>
<p>Il s'est mis à pleurer,</p>
<p>Malheureusement l'infection était grave, il a perdu son œil gauche...</p>
<p>Puis 1 an plus tard, l'infection a atteint son œil droit,</p>
<p>Il est devenu entièrement aveugle...</p>
<p>Il vivait dans un état de dépression et de tristesse.</p>
<p>Alors est-ce qu'il va transformer son chagrin, sa déprime et sa souffrance en haine envers la société ?</p>
<p>Ou bien il va les transformer en source de bien, pour être utile aux gens ?</p>
<p>Ce jeune est entré à l'Institution Royale des Jeunes Aveugles pour faire ses études à Paris,</p>
<p>Il apprenait à lire dans l'institution,</p>
<p>Mais la méthode utilisée était très difficile,</p>
<p>Il fallait toucher avec les mains de grandes lettres en fer,</p>
<p>On collait les grosses lettres sur des feuilles,</p>
<p>Chaque lettre faisait 7 centimètres,</p>
<p>Donc pour composer une phrase, les enfants mettaient une demi-heure...</p>
<p>C'était très pénible.</p>
<p>Il a senti que ce n'était pas pratique,</p>
<p>Puis quand il a eu 20 ans, il a été nommé professeur dans l'institut.</p>
<p>Et un jour, il s'est mis à réfléchir « Il faut que je trouve une nouvelle méthode »</p>
<p>Une nouvelle méthode pour quoi ?</p>
<p>« C'est injuste que les aveugles dans le monde entier, souffrent comme moi j'ai souffert, et qu'ils apprennent à lire avec autant de difficulté comme j'ai du le faire »</p>
<p>Il a commencé à prendre une aiguille,</p>
<p>Et avec cette aiguille il faisait des trous dans une feuille,</p>
<p>1 trou... Puis 2... Puis 3... C'est devenu 12 trous,</p>
<p>Et il faisait des trous de différentes formes comme des lettres d'alphabet,</p>
<p>Il a inventé une nouvelle technique,</p>
<p>Une nouvelle méthode très différente des grosses lettres en fer qu'on utilisait avant...</p>
<p>Avec 12 trous, il avait composé toutes les lettres de l'alphabet.</p>
<p>Il a testé sa méthode,</p>
<p>Sa méthode fonctionnait,</p>
<p>Il a commencé à l'utiliser dans les cours qu'il donnait à l'institut,</p>
<p>Puis il l'a publiée dans un livre,</p>
<p>Et ensuite il a réussi à traduire un livre en entier avec sa méthode...</p>
<p>Louis Braille.</p>
<p>Qui est Louis Braille ?</p>
<p>C'est celui qui a inventé le « Braille »</p>
<p>Le système d'écriture tactile à points pour les personnes aveugles ou malvoyantes.</p>
<p>Sa méthode s'est généralisée,</p>
<p>Et c'est devenu la façon la plus facile pour apprendre à lire.</p>
<p>Pour l'inventer, il a utilisé une aiguille... Comme l'aiguille qui lui a fait perdre ses yeux,</p>
<p>Il a transformé la source de sa douleur en bonheur pour des millions d'aveugles,</p>
<p>Le secret de sa souffrance est devenu le secret de sa réussite...</p>
<p>Il s'est servi de sa tristesse pour donner du bonheur à tous les autres aveugles...</p>
<p>Le gouvernement français a reconnu sa découverte,</p>
<p>Sa méthode a été généralisée.</p>
<p>Et il a prononcé sa très belle phrase « J'ai pleuré 3 fois dans ma vie...</p>
<p>J'ai pleuré le jour où j'ai perdu la vue,</p>
<p>J'ai pleuré le jour où j'ai découvert cette nouvelle méthode,</p>
<p>Et je pleure aujourd'hui car la méthode sera disponible pour tout le monde »</p>
<p>Cet homme est décédé à l'âge de 43 ans,</p>
<p>Mais des millions d'aveugles dans le monde lui disent merci chaque jour, car il les a aidés à lire et à écrire.</p>
<p>La question est : Il y a des gens qui transforment leurs souffrances en haine envers les autres...</p>
<p>Et il y a des gens qui transforment leurs souffrances en bonheur pour les autres...</p>
<p>Un sourire d'espoir pour chaque être humain qui transforme sa douleur en joie pour les autres.</p>
