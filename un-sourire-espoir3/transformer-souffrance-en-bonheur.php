                                        <br>
                                        <p>In the name of Allah The Compassionate,
 The Merciful</p>


<p>Welcome to a new episode of Basmit Amal
(A Smile of Hope).</p>


<p>Basmit Amal (A Smile of Hope) today says
there are people who transform</p>


<p>their pain and problems in life
to hatred, anger, and depression,</p>


<p>unfortunately, by which they depress
people with it.</p>


<p>However, there are other people who transform
their pain and problems in life</p>


<p>to happiness, hope, and dreams
that make people happy.</p>


<p>I wonder which type are you?
Let's hear the story.</p>


<p>Are you amongst those who turn their
problems into hatred,</p>


<p>and anger towards other?</p>


<p>Or is it that when you go through
tribulations, pain, and hardships</p>


<p>you turn it into good things?</p>


<p>Well how do I turn my problems into
happiness for others?</p>


<p>Let me tell you this story.</p>


<p>He was born 1809, in a small village which
is 400 miles away from Paris, France.</p>


<p>He was a little child, who had beautiful
eyes and was very intelligent.</p>


<p>However, when he was 4 years old his father
worked as a tailor,</p>


<p>because he used big needles for
the horse saddles.</p>


<p>The boy, while he was 4 years old,
sat with his father at his shop.</p>


<p>His father was busy working, and
the boy picked up a big needle</p>


<p>and started to play with it.</p>


<p>He got a hammer and began trying to hammer
the long needle into a thick piece of leather.</p>


<p>However, he wasn’t successful and
the needle flew out of his hands.</p>


<p>The needle injured his eye deeply,
and he kept crying. Unfortunately,</p>


<p>he ended up with an inflammation
in his eye's main nerve</p>


<p>which led to his left eye going blind.</p>


<p>Following this, a year later,</p>


<p>the inflammation came in his other eye,
so he lost both of his eyes.</p>


<p>He became completely blind.</p>


<p>He was in a state of depression
and sadness.</p>


<p>Will he change his depression and sadness
to anger towards society?</p>


<p>Or will he change it to a fountain of goodness
in which humanity benefits out of it?</p>


<p>This young man joined a school</p>


<p>"The National Institute for the Blind
in Paris"</p>


<p>he began learning to read in the institute.</p>


<p>The way was old-fashioned at that time,
where people would touch big letters.</p>


<p>There were huge letters made out of metal
and taped to paper.</p>


<p>The students would spend a long time
to touch every single letter.</p>


<p>It took about 30 minutes to find out
a word using this methodology,</p>


<p>which was difficult.</p>


<p>He felt this way is not a practical
way to read.</p>


<p>When he turned 20 years old, he was
hired as a teacher in the institute.</p>


<p>One day, he began thinking that he must
invent a new way for reading.</p>


<p>He felt that it would be unfair that all
the blind people in the world</p>


<p>would go through so much pain
and struggle the way he did.</p>


<p>He had a new idea of creating
holes in a paper.</p>


<p>He created several holes till
he reached 12 holes.</p>


<p>He begins to put them in different forms
as if they were the letters for reading.</p>


<p>He made an innovation without large letters,</p>


<p>which based on 12 holes put in different forms
to stand for different letters in the alphabet.</p>


<p>He started to test this methodology,
it started to succeed,</p>


<p>and he started using it in the institute.</p>


<p>Then he published it in a book.</p>


<p>Then he translated this book using
this method.</p>


<p>His name was Louis Braille.</p>


<p>Who is Louis Braille?</p>


<p>He is the one who invented Braille,</p>


<p>the way most common for reading among
the blind.</p>


<p>His idea become widespread and it became
the easiest way for reading.</p>


<p>To make this invention he used
the big needle,</p>


<p>which was similar to the one which
caused him to lose his eyesight.</p>


<p>He used the cause of his distress
as a tool for happiness</p>


<p>to millions of blind people
around the world.</p>


<p>He transformed the source of his pain
into the source of his success.</p>


<p>He transformed the secret of joy for
the rest of the blind around the world.</p>


<p>The French government acknowledged
the invention,</p>


<p>and it became the most common method
used in reading for the blind.</p>


<p>Braille said his famous words:</p>


<p>" I cried three times in my life;
when I lost my eyes,</p>


<p>when I invented this new method for
reading, and I am crying today,</p>


<p>when this method became the most common
way for reading"</p>


<p>This man passed away when
he was 43 years old.</p>


<p>However, his invention exists to date.</p>


<p>20 million blind people around the world
have great gratitude towards this man</p>


<p>who helped them in reading and writing.</p>


<p>There are people who turn their pain
into hatred for other people</p>


<p>and there are people who turn their pain
into joy for others.</p>


<p>A smile engraved with hope on the face
of every individual who changes their</p>


<p>struggle to joy for others.</p>


<p>Thank you and May the peace and blessings
of Allah be upon you.</p>
