                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle de l'échec...</p>
<p>Je peux te poser une question ?</p>
<p>Combien de fois as-tu échoué dans ta vie ?</p>
<p>2 fois ?</p>
<p>3 fois ?</p>
<p>100 fois ?</p>
<p>D'accord...</p>
<p>Et combien de fois les gens t'ont dit en face, / ou dans ton dos : « Il n'a pas su réussir dans ça... »</p>
<p>« Il a échoué dans ça... »</p>
<p>Et combien de fois tu as reçu des réponses négatives en cherchant un travail,</p>
<p>Ou combien de fois les gens ont dit en parlant de toi « Il ne mérite pas le poste où il est »</p>
<p>« Il n'est pas adapté... Il n'a pas les compétences nécessaires ... »</p>
<p>Je peux te poser une question encore plus importante ?</p>
<p>Quelle a été ta réaction à chaque fois ?</p>
<p>Tu as gardé le silence ?</p>
<p>Tu as accepté la situation ?</p>
<p>Tu t'es soumis face à la réalité ?</p>
<p>Le sourire d'espoir d'aujourd'hui est avec quelqu'un qui s'appelle Charles,</p>
<p>Charles était l'exemple de l'élève en difficulté...</p>
<p>Quand il était en 2ème année du collège, il a raté toutes les matières,</p>
<p>Vous imaginez ?? Il n'a réussi dans aucune matière.</p>
<p>Puis quand il est arrivé au lycée, il a échoué en physique... en mathématiques... en langue anglaise...</p>
<p>Quelle catastrophe !</p>
<p>À l'école on lui a dit « Tu n'es pas fait pour les études... »</p>
<p>Ils ont voulu le tester dans le sport et les activités physiques,</p>
<p>Rien du tout... Il n'avait aucune compétence... Il était sans espoir.</p>
<p>Il passait d'un échec à un autre...</p>
<p>Et il était tellement en échec, qu'il n'avait pas d'amis...</p>
<p>Car c'était la honte au niveau social.</p>
<p>Il sentait qu'il était un échec sur deux jambes...</p>
<p>Charles s'est mis à dessiner, et c'est devenu tout ce qu'il aime faire.</p>
<p>C'est-à-dire il prenait une feuille, et il dessinait des caricatures et des personnages imaginaires.</p>
<p>Il a décidé que « Ca y est... Je suis en échec dans ma scolarité, je suis en échec dans le sport, je suis en échec dans les relations sociales, je suis en échec dans l'amitié »</p>
<p>Il est en échec dans les 4 choses qui sont la base de tout dans la vie.</p>
<p>Alors il a décidé de dédier tout son temps à ses dessins,</p>
<p>Il a créé quelques personnages,</p>
<p>Puis il les a envoyés à Disney aux États Unis,</p>
<p>Ils lui ont répondu « Ces dessins ne sont pas présentables »</p>
<p>Aïe... Il a échoué même dans la chose qu'il aime.</p>
<p>Mais Charles ne s'est pas découragé,</p>
<p>Il n'a pas désespéré...</p>
<p>Qu'a-t-il fait ?</p>
<p>Il a créé une bande dessinée qui raconte son histoire d'échecs,</p>
<p>C'est-à-dire il s'est dessiné lui-même...
<p>Il a dessiné le personnage d'un garçon qui échoue dans chaque chose qu'il fait.</p>
<p>Et avec ce garçon, il y a un petit chien,</p>
<p>Lui et son petit chien essayent de réussir dans n'importe quelle chose... Jusqu'au jour où ils ont réussi dans 1 seule chose dans la vie.</p>
<p>Il a publié sa bande dessinée,</p>
<p>Sa bande dessinée a connu un succès mondial,</p>
<p>À tel point que partout dans le monde les enfants portent des t-shirts avec le personnage de Charles Brown et son chien Snoopy,</p>
<p>C'est devenu un personnage de bande dessinée très célèbre,</p>
<p>Et il a connu un succès épatant...</p>
<p>Car il voyait l'échec comme une force, et pas comme un découragement.</p>
<p>Du cœur de la souffrance, nait l'espoir...</p>
<p>Du cœur du sentiment d'échec, nait la réussite...</p>
<p>Un sourire d'espoir pour chaque jeune garçon et fille, ne désespérez jamais !</p>
<p>Peu importe ce qu'on vous dit comme paroles décourageantes,</p>
<p>Et un sourire d'espoir pour chaque être humain, qui continue à tenter... Tenter... Tenter, et qui insiste pour réussir dans la vie, et dessiner un sourire d'espoir sur son visage et sur les visages des autres.</p>
<p>Cet homme appelé Charles M. Schulz est devenu un des hommes les plus riches du monde grâce à la popularité de ses personnages, ses licences pour les publicités, et la vente des produits dérivés...</p>
<p>Et ses personnages Charlie Brown et Snoopy sont populaires jusqu'à maintenant...</p>
<p>Malgré tous les échecs qu'il a connu dans sa vie.</p>
<p>Un sourire d'espoir pour chaque être humain qui insiste pour réussir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
