                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'un groupe de jeunes qui ont composé une équipe,</p>
<p>Une équipe pleine de positivité,</p>
<p>Le sourire d'espoir d'aujourd'hui te demande : Es-tu positif ?</p>
<p>Es-tu un être humain positif ?</p>
<p>Est-ce que tu prends l'initiative ?</p>
<p>Es-tu positif en faisant le bien ?</p>
<p>Prends-tu l'initiative pour améliorer ton pays ?</p>
<p>Es-tu positif en aidant les gens qui sont dans le besoin ?</p>
<p>Des étudiants à l'université ont composé une équipe qu'ils ont nommée « Nous bâtissons une vie »</p>
<p>Remarquez le joli slogan qu'ils ont choisi...</p>
<p>Et leur intention est d'améliorer les petits villages dans notre pays,</p>
<p>Ils sont partis voir les villages,</p>
<p>Et ils en ont sélectionnés quelques uns à la hauteur de leurs moyens,</p>
<p>C'est-à-dire des villages avec 200 maisons,</p>
<p>Des villages avec 300 habitants...</p>
<p>Ils ont composé des groupes,</p>
<p>Et ils sont partis voir les maisons de ces villages une par une,</p>
<p>Ils demandaient aux habitants « Quels sont vos problèmes ? »</p>
<p>« Quels sont vos rêves ? »</p>
<p>Puis ils notaient ce que les habitants leurs disaient...</p>
<p>Celui qui veut pouvoir envoyer son enfant à l'école,</p>
<p>Et celui qui a besoin de soins médicaux,</p>
<p>Et celui qui n'a pas de toit au dessus de sa maison,</p>
<p>Et celui qui a besoin d'un travail,</p>
<p>Et celui qui a besoin d'eau courante,</p>
<p>Et celui qui demande à séparer les tuyaux de l'eau douce de ceux de l'eau usagée...</p>
<p>Ces jeunes ont rassemblé toutes les demandes des gens,</p>
<p>Et ils les ont classées par priorité.</p>
<p>Ce sont des jeunes positifs, qui ont envie de faire quelque chose d'utile à leur pays...</p>
<p>Ils veulent faire quelque chose d'utile aux gens...</p>
<p>Ils ont récolté de l'argent chez leurs amis, et chez leurs familles,</p>
<p>Juste un peu d'argent à la hauteur des besoins du village,</p>
<p>Puis pendant 6 mois,</p>
<p>Chaque Vendredi ils partaient dans ce village,</p>
<p>Ils faisaient des travaux,</p>
<p>Ils réparaient,</p>
<p>Ils construisaient des toits aux maisons qui n'en avaient pas,</p>
<p>Puis ils partaient au ministère concerné,</p>
<p>Et ils demandaient au délégué de les aider pour ceci ou cela...</p>
<p>Et les habitants du village les aidaient aussi.</p>
<p>Ils ont fait 5 villages !</p>
<p>Ca veut dire quoi ils ont fait 5 villages ?</p>
<p>Ils les ont changés entièrement,</p>
<p>Des maisons qui manquaient de toits ? Ils ont construit des toits.</p>
<p>De l'eau polluée dans le village ? Ils l'ont traitée.</p>
<p>Des maisons n'avaient pas l'eau courante ? Ils ont installé des tuyaux.</p>
<p>Des enfants non scolarisés ? Ils ont donné des cours d'alphabétisation.</p>
<p>Des gens qui manquent de soins ? Ils ont fait venir des médecins pour les soigner.</p>
<p>Des enfants qui jouent au foot ? Ils ont fait venir des entraineurs pour repérer les enfants les plus talentueux et les inscrire dans un club professionnel.</p>
<p>Des gens qui n'ont pas de travail ? Ils leur ont fait des petits projets autofinancés.</p>
<p>6 mois de travail,</p>
<p>Concentrés sur un seul village : Nous bâtissons une vie.</p>
<p>Ces jeunes ont obtenu un résultat formidable,</p>
<p>Puis ils ont senti qu'ils étaient positifs,</p>
<p>Ils ont senti qu'ils avaient fait le bien,</p>
<p>Et ils ont senti qu'ils ont servi leur pays.</p>
<p>Mais l'un d'entre eux est très beau, il s'appelle Tamer Amr,</p>
<p>Après que les jeunes aient fini leur mission, et qu'ils soient partis,</p>
<p>Il a pensé « Et les autres villages qui sont à côté ? »</p>
<p>Il est revenu tout seul, sans le reste de l'équipe,</p>
<p>Il a dit aux gens du village « On vous a aidés à améliorer votre village,</p>
<p>Et vous, qu'allez-vous faire ? »</p>
<p>Ils lui ont demandé « Ca signifie quoi, qu'allons-nous faire ? »</p>
<p>Il leur a dit « Ca vous dit d'aller aider le village voisin ?</p>
<p>De le transformer comme votre village a été transformé ?</p>
<p>Mais cette fois ce ne sont pas des jeunes bénévoles qui viendront des universités,</p>
<p>Ce sont vous qui allez le faire... »</p>
<p>Et il a convaincu les habitants du village, car ils ont vu de leurs propres yeux le résultat.</p>
<p>Ils ont travaillé avec lui,</p>
<p>Ils sont partis au village d'à côté,</p>
<p>Ils ont discuté avec leurs voisins, car ils se connaissaient déjà...</p>
<p>Ils ont réfléchi « Comment rendre ce village comme le notre »</p>
<p>Ils se sont mis à travailler,</p>
<p>Et l'expérience a réussi,</p>
<p>Tamer a transformé les gens du village aussi, il les a rendus positifs.</p>
<p>Ce serait magnifique si cette expérience se multiplie dans nos pays...</p>
<p>Ce serait magnifique si cette idée se répand,</p>
<p>Si chaque jeune se sent positif et responsable,</p>
<p>Et qu'il se met à construire dans son pays.</p>
<p>Un sourire d'espoir pour chaque être humain qui se soucie des gens pauvres /dans le besoin, et qui répare dans son pays...</p>
<p>À ce moment, un sourire d'espoir sera présent dans tous nos pays, et un bonheur sans semblable.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
