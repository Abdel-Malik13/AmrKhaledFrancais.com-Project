                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui est une histoire avec Jésus (Paix sur lui),</p>
<p>Et la beauté de l’histoire est dans la Miséricorde,</p>
<p>Dans le fait de traiter les gens avec une grande clémence…</p>
<p>L’histoire raconte qu’une femme avait commis un grand péché,</p>
<p>Elle a eu un rapport sexuel hors mariage…</p>
<p>Les gens l’ont attrapée,</p>
<p>Ils l’ont fait venir en la trainant,</p>
<p>Et la femme pleurait,</p>
<p>Jusqu’à ce qu’on la ramène devant Jésus (psl).</p>
<p>Quand Jésus (psl) l’a vue, et qu’il a vu ses larmes,</p>
<p>Il a senti que cette femme s’était repentie,</p>
<p>Et il a senti que maintenant le devoir c’est la Miséricorde, et non pas encore plus d’humiliation.</p>
<p>Alors il a regardé son apprenti,</p>
<p>Les gens étaient là, et la femme était là,</p>
<p>Jésus (psl) lui a dit « Ô Sam’an (le prénom de l’apprenti),</p>
<p>J’ai une question que je voudrais te poser »</p>
<p>Il lui a répondu « Oui dis-moi mon maître »</p>
<p>Jésus (psl) lui a dit « Un homme a prêté de l’argent à 2 personnes,</p>
<p>Quelqu’un lui doit 500 dinars,</p>
<p>Et l’autre lui doit 50 dinars…</p>
<p>Cet homme a décidé de pardonner aux deux, et d’effacer leurs dettes,</p>
<p>Lequel des deux il aime le plus ?</p>
<p>Celui qui lui doit 50 dinars, ou celui qui lui doit 500 dinars ? »</p>
<p>(L’idée vous est arrivée ?)</p>
<p>Sam’an lui a répondu « Je pense que celui qu’il aime le plus, est celui à qui il a pardonné la plus grande somme d’argent »</p>
<p>Jésus (psl) lui a dit « C’est cela la vérité ».</p>
<p>Puis il a regardé la femme et lui a dit « Pars, ta foi te délivre de ton péché ».</p>
<p>C’est magnifique…</p>
<p>La clémence même avec les fautifs,</p>
<p>La Miséricorde mes chers, est ce qui a poussé Jésus (psl) à dire, quand ceux qui lui voulaient du mal l’ont entouré… « Pardonne-leur mon Seigneur, car ils font ce qu’ils ne savent pas ».</p>
<p>Et la Miséricorde est ce qui a poussé le Prophète Mohamed (bénédictions et paix soient sur lui) à dire le jour d’Uhud, quand les mécréants lui ont fait du mal… <strong class="prophete">« Ô Seigneur, pardonne à mon peuple car ils ne savent pas »</strong>.</p>
<p>Et c’est la Miséricorde qui a poussé Jésus (psl) à dire en parlant d’une femme qui avait commis un grand péché,</p>
<p>« Dieu l’a beaucoup aimée, alors Il lui a beaucoup pardonné ».</p>
<p>Et c’est la Miséricorde qui a poussé Mohamed (bpsl) à dire à ses compagnons, en parlant d’un homme qui était ivre après avoir bu de l’alcool,</p>
<p><strong class="prophete">« Ne le maudissez pas, car c’est quelqu’un qui aime Dieu et Son Messager »</strong>.</p>
<p>Jésus (psl) dit « Dieu est amour ».</p>
<p>Et Mohamed (bpsl) dit <strong class="prophete">« L’un de vous n’est croyant que lorsqu’il aime pour son frère, la même chose qu’il aime pour lui-même »</strong>.</p>
<p>Jésus (psl) dit « Bénis soient ceux qui créent la paix ».</p>
<p>Et Mohamed (bpsl) dit <strong class="prophete">« Transmettez la paix (salam) entre vous »</strong>.</p>
<p>Jésus (psl) dit « Je suis le pain de la vie », c’est-à-dire une grâce.</p>
<p>Et Mohamed (bpsl) dit <strong class="prophete">« Je suis une Miséricorde offerte »</strong>.</p>
<p>Effectivement… Les Prophètes sont des frères,</p>
<p>Leurs mamans sont différentes,</p>
<p>Mais leur religion est la même.</p>
<p>Mes chers, nous avons besoin de vivre avec de la Miséricorde,</p>
<p>La Miséricorde est un principe de la religion,</p>
<p>La Miséricorde est un principe pour nos pays,</p>
<p>La Miséricorde est un principe pour la vie…</p>
<p>Garde chez toi de la Miséricorde, pour ton épouse/mari,</p>
<p>Pour tes enfants,</p>
<p>Pour les gens,</p>
<p>Pour les pauvres,</p>
<p>Garde chez toi de la Miséricorde, pour qu’on vive tous avec un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la Miséricorde de Dieu et Sa bénédiction.</p>
