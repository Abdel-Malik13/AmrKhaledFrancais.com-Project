                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'une seule idée :</p>
<p>Chaque bonne chose que tu fais, finit par t'être rendue.</p>
<p>C'est une idée très très très simple...</p>
<p>Mais plus tu en es convaincu, et plus tu vivras heureux dans ce bas-monde,</p>
<p>Et plus un sourire d'espoir se dessinera sur ton visage.</p>
<p>« Chaque bonne chose que tu fais, finit par t'être rendue ».</p>
<p>C'est une affaire réglée !</p>
<p>C'est une des lois de la vie,</p>
<p>Comme le soleil se lève à l'est et se couche à l'ouest.</p>
<p>Le bien est toujours rendu.</p>
<p>L'histoire d'aujourd'hui est celle de Saïd,</p>
<p>C'est un jeune qui venait d'obtenir son diplôme,</p>
<p>Et qui rêvait de travailler dans une grande entreprise,</p>
<p>C'est une des plus grandes sociétés du pays,</p>
<p>Et son directeur est un homme célèbre et respecté,</p>
<p>Saïd est parti déposer son CV,</p>
<p>Le responsable des recrutements lui a demandé de rajouter une photo à son dossier,</p>
<p>Saïd a rajouté sa photo,</p>
<p>Le responsable lui a dit « Nous te rappellerons ».</p>
<p>Saïd est reparti, mais bien sûr au fond de lui il se disait que personne n'allait le rappeler...</p>
<p>Il ne fait que tenter sa chance,</p>
<p>Car c'est sûr que ceux qui sont pistonnés,</p>
<p>Ou ceux qui viennent de grandes familles,</p>
<p>Sont ceux qui auront la priorité.</p>
<p>Et au bout de quelques jours, il a reçu un coup de téléphone,</p>
<p>Ils lui ont dit « On a besoin de quelques informations »,</p>
<p>Saïd s'est demandé « Quelles informations... J'ai déjà tout mis dans mon CV »,</p>
<p>La personne au téléphone lui a demandé « Ton papa travaillait dans quoi ?</p>
<p>Ton papa faisait quoi ? »</p>
<p>Ils lui posaient des questions sur son papa...</p>
<p>Saïd ne comprenait pas,</p>
<p>Mais il a répondu à leurs questions.</p>
<p>Et quelques jours plus tard,</p>
<p>Il a reçu un autre appel,</p>
<p>Quand il a décroché, on lui a dit « Bienvenue Mr. Saïd,</p>
<p>Si vous voulez bien nous honorer,</p>
<p>Le directeur aimerait vous rencontrer »</p>
<p>Saïd a répondu « Non, il s'agit sûrement d'une erreur, je suis Saïd qui cherche un travail ».</p>
<p>La personne au téléphone lui a dit « Oui, oui, on sera honorés de vous accueillir demain ».</p>
<p>Saïd n'y croyait pas...</p>
<p>Il est parti le lendemain,</p>
<p>Et il a trouvé quelqu'un pour l'accueillir devant la porte de l'entreprise,</p>
<p>« Bienvenue Mr. Saïd »</p>
<p>Comme dans un film comique...</p>
<p>« Nous sommes très honorés par votre présence,</p>
<p>Je vais vous accompagner jusqu'au bureau du PDG ».</p>
<p>Saïd lui a répondu « Non, vous me confondez sûrement avec quelqu'un d'autre, je suis juste quelqu'un de modeste qui cherche du travail ».</p>
<p>La personne lui a demandé « vous vous appelez bien Saïd Wahid Farid ? Et votre RDV est à 17h ? »</p>
<p>Il a dit « Oui c'est moi »,</p>
<p>La personne lui a dit « Parfait, venez avec moi ».</p>
<p>Saïd est entré,</p>
<p>Et il a trouvé le PDG en train de l'attendre dans son bureau prestigieux,</p>
<p>Saïd ne comprenait rien du tout...</p>
<p>Il est entré,</p>
<p>Et le directeur s'est levé pour l'accueillir « Bienvenue... Bienvenue mon très cher »</p>
<p>Saïd ne comprenait pas...</p>
<p>Il lui a dit « Je vous remercie beaucoup pour votre accueil,</p>
<p>Mais je pense que vous me confondez avec quelqu'un d'autre »</p>
<p>Le PDG a ouvert le tiroir de son bureau,</p>
<p>Et il a sorti une vieille photo,</p>
<p>Puis lui a demandé « Sais-tu qui est sur cette photo ? »</p>
<p>Saïd a crié « Oui c'est la photo de mon papa (paix à son âme) !</p>
<p>Vous le connaissiez ? »</p>
<p>Le directeur lui a répondu « Est-ce que cet homme rempli de bonté est décédé ?</p>
<p>Paix à son âme... »</p>
<p>Saïd lui a dit « Je ne comprends pas »</p>
<p>Le directeur lui a répondu « Je vais te raconter ce qui s'est passé :</p>
<p>Quand j'avais ton âge Saïd,</p>
<p>Je n'étais pas directeur d'une grande société,</p>
<p>J'étais juste un jeune modeste,</p>
<p>Je suis venu au Caire pour chercher du travail,</p>
<p>Et pendant que j'étais dans l'autocar qui me ramenait de la ville d'Al-Mansoura,</p>
<p>J'ai mis mes mains dans mes poches,</p>
<p>Et je n'ai pas trouvé mon portefeuille...</p>
<p>J'ai paniqué,</p>
<p>J'ai transpiré,</p>
<p>Je ne savais pas quoi faire...</p>
<p>Et à côté de moi, un homme était assis,</p>
<p>Cet homme était ton papa,</p>
<p>Cet homme a sorti de l'argent avec plein d'altruisme,</p>
<p>Et il m'a payé le prix du billet,</p>
<p>Puis il m'a demandé « Combien de temps tu vas rester au Caire ? »</p>
<p>Je lui ai répondu « Jusqu'à ce que je trouve une solution »</p>
<p>Il m'a donné de l'argent,</p>
<p>Je lui ai dit « Non, merci je ne veux pas le prendre »</p>
<p>Mais il a insisté pour que je le prenne,</p>
<p>Il m'a payé tous les frais de mon séjour,</p>
<p>Et je lui ai posé la question « Quel est ton nom ? »</p>
<p>Il m'a répondu « Wahid Farid, et on me surnomme : Le papa de Saïd »</p>
<p>Et quand j'ai regardé les billets d'argent qu'il m'a donnés,</p>
<p>J'ai vu une photo qui s'est glissée par erreur dedans...</p>
<p>Je lui ai dit « Je ne vais pas te rendre cette photo,</p>
<p>Je vais la garder car tu m'as rendu un grand service.</p>
<p>Que Dieu m'aide un jour, et me permette de te rendre ce service »</p>
<p>Je ne l'ai plus revu depuis ce jour,</p>
<p>Mais il est resté dans ma mémoire,</p>
<p>J'ai grandi et j'ai réussi,</p>
