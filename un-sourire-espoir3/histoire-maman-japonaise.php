                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui vient du Japon,</p>
<p>Elle parle de la maman,</p>
<p>Et à quel point une maman peut sacrifier sa vie pour ses enfants.</p>
<p>Cette histoire s'est produite lors du dernier tremblement de terre au Japon, et elle a été filmée,</p>
<p>Après le tremblement de terre, les équipes de secours sont parties voir les endroits touchés,</p>
<p>Et dans une maison qui s'est écroulée, ils ont trouvé une femme,</p>
<p>Cette femme est morte,</p>
<p>Mais ils l'ont trouvée dans une position inhabituelle,</p>
<p>Ils l'ont trouvée dans la position de la prosternation,</p>
<p>Bien sûr cette femme japonaise n'est pas musulmane,</p>
<p>Mais elle était en position de prosternation,</p>
<p>C'est-à-dire elle était sur ses genoux,</p>
<p>Elle avait la tête et les mains par terre,</p>
<p>Le plafond s'est écroulé sur son dos, et elle n'a pas survécu.</p>
<p>L'équipe de secours a constaté que cette femme est décédée,</p>
<p>Alors le chef leur a dit de passer à la maison suivante, peut-être qu'il y aura des survivants...</p>
<p>Et pendant qu'ils partaient, ils ont entendu un léger mouvement,</p>
<p>Ils n'y ont pas fait attention,</p>
<p>Mais le chef d'équipe leur a dit « Attendez un peu... Ne faites aucun bruit »</p>
<p>Il y avait bien un léger mouvement,</p>
<p>Il y avait des petits gémissements...
<p>Il leur a dit « Refaisons des recherches »</p>
<p>Ils ont regardé partout dans la maison,</p>
<p>Et ils se sont rendus compte que le bruit venait d'en dessous de la femme...</p>
<p>Ils ont déplacé son corps qui était en position de prosternation,</p>
<p>Et ils ont trouvé un bébé de 3 mois enroulé dans une couverture...</p>
<p>Quand cette maman a senti que la maison s'écroulait,</p>
<p>Elle a eu peur pour son enfant...</p>
<p>Qu'elle est grande la tendresse des mamans,</p>
<p>À quel point votre maman vous aime les jeunes, que vous soyez garçon ou fille...</p>
<p>Et à quel point elle est prête à se sacrifier pour vous,</p>
<p>Cette maman japonaise a enroulé son bébé dans une couverture,</p>
<p>Elle l'a caché en dessous d'elle,</p>
<p>Puis elle s'est mise dans une position de prosternation pour que le plafond s'écroule sur son dos sans atteindre son bébé...</p>
<p>Les secouristes ont sorti le bébé,</p>
<p>Il était vivant,</p>
<p>Sa maman a sauvé sa vie.</p>
<p>En prenant le bébé, les secouristes ont trouvé un téléphone dans la couverture,</p>
<p>La maman a laissé son téléphone portable dedans,</p>
<p>Et dans le téléphone, elle avait laissé un message pour son bébé,</p>
<p>Elle disait « Si tu restes en vie,</p>
<p>Souviens-toi que ta maman t'aime. »</p>
<p>Cette maman japonaise a laissé ce message,</p>
<p>Mais ce message chaque maman au monde est prête à le laisser,</p>
<p>N'importe quelle maman à sa place aurait fait la même chose...</p>
<p>Elle veut qu'après son décès, son enfant se rappelle qu'elle l'aime.</p>
<p>« Si tu restes en vie,</p>
<p>Souviens-toi que ta maman t'aime »</p>
<p>Chaque maman est prête à se sacrifier pour que son enfant vive...</p>
<p>Mais toi, qu'es-tu prêt à faire pour satisfaire ta maman ?</p>
<p>Ta maman qui a sacrifié ses rêves pour toi,</p>
<p>Ta maman avait plein de projets dans la vie, mais elle les a tous abandonnés pour un seul projet : Toi.</p>
<p>Soyez miséricordieux envers vos mamans,</p>
<p>Et soyez miséricordieux envers vos papas,</p>
<p>Car ils sont prêts à sacrifier leurs vies pour vous.</p>
<p>Dessinez un sourire d'espoir sur leurs visages.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
