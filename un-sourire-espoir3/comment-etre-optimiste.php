                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui te demande : Es-tu de nature optimiste ou bien es-tu de nature pessimiste ?</p>
<p>Es-tu quelqu'un de positif ?</p>
<p>Il existe des gens qui peu importe ce qu'ils ont,</p>
<p>Peu importe ce que Dieu leur donne comme bienfaits,</p>
<p>Restent négatifs...</p>
<p>Ils voient toujours le verre à moitié vide.</p>
<p>Et il existe des gens qui malgré les catastrophes qui leurs tombent dessus, parlent toujours d'espoir et restent toujours souriants...</p>
<p>À votre avis lequel des 2 est le plus heureux ?</p>
<p>Je jure par Dieu qu'un seul optimiste (peu importe ses problèmes) est plus heureux que 100 pessimistes (peu importe leurs bienfaits).</p>
<p>Le bonheur et le malheur commencent à l'intérieur de l'être humain,</p>
<p>Ils ne commencent pas à l'extérieur.</p>
<p>Es-tu optimiste ou pessimiste ?</p>
<p>Laisse-moi te raconter l'histoire d'un professeur à l'université de Cambridge :</p>
<p>Il enseigne les Mathématiques,</p>
<p>Et il s'assoit au même endroit que Newton.</p>
<p>(ça vous donne une idée sur son importance)</p>
<p>Cet homme s'appelle Stephen Hawking,</p>
<p>Quand Stephen Hawking avait 20 ans,</p>
<p>Il a été atteint d'une maladie grave qui l'a handicapé,</p>
<p>Il ne pouvait plus marcher,</p>
<p>Il s'est retrouvé dans un fauteuil roulant.</p>
<p>Imaginez un jeune de 20 ans à qui il arrive ça...</p>
<p>Mais il a insisté pour suivre ses études, réussir Etc.</p>
<p>Puis il est devenu professeur de Mathématiques,</p>
<p>Et quand il a eu 30 ans,</p>
<p>Il a été atteint d'une autre maladie...</p>
<p>Une infection grave des poumons,</p>
<p>Qui lui a fait perdre la capacité de parler.</p>
<p>Donc il ne pouvait ni marcher ni parler...</p>
<p>Tu imagines quelqu'un qui se retrouve dans cette situation ?</p>
<p>Mais le plus étonnant,</p>
<p>C'est qu'il a insisté pour inventer un système sur son ordinateur,</p>
<p>Qui lui permet de communiquer avec ses étudiants,</p>
<p>Communiquer par écrit, sans avoir besoin de parler.</p>
<p>Et c'est devenu un des meilleurs professeurs à l'université de Cambridge en Angleterre,</p>
<p>Puis il a été nommé pour le prix Nobel,</p>
<p>Il a remporté des récompenses dans le monde entier,</p>
<p>Il voyage partout pour donner des conférences,</p>
<p>Sans parler et sans marcher...</p>
<p>Il a un succès impressionnant,</p>
<p>Dans une matière qui n'est pas facile en plus : les Mathématiques.</p>
<p>Il a rédigé des dizaines de livres publiés partout,</p>
<p>Et pendant qu'il était candidat au prix Nobel,</p>
<p>On lui a demandé dans une interview,</p>
<p>« Quel est ton secret ? »</p>
<p>Il leur a répondu « Mon secret est un seul mot : Je suis toujours optimiste »</p>
<p>Ils lui ont demandé « Et comment tu fais pour être toujours optimiste ? »</p>
<p>Il leur a donné une méthode magnifique...</p>
<p>(Bien sûr il leur a dit par écrit)</p>
<p>« Je ne parle pas, et je ne marche pas,</p>
<p>Mais chaque soir avant de dormir, j'écris sur mon ordinateur 3 choses dans lesquelles je suis chanceux,</p>
<p>Ou dans lesquelles je suis plus doué que les autres personnes que je connais »</p>
<p>« Chaque soir, j'écris 3 choses que j'ai obtenu,</p>
<p>Et que les autres ne possèdent pas »</p>
<p>« Chaque soir j'écris 3 choses que je possède,</p>
<p>Et dont les autres gens sont privés,</p>
<p>Puis je médite dessus pendant 30 minutes,</p>
<p>Pour pouvoir dormir.</p>
<p>Au bout d'un moment je me suis rendu compte que je suis très chanceux,</p>
<p>Et que sans les maladies que j'ai eu, je serais la personne la plus chanceuse sur terre »</p>
<p>Stephen Hawking a rédigé un livre appelé « Le chemin vers l'optimisme » (l'histoire de ma réussite)</p>
<p>Dedans il a écrit l'histoire de sa vie, et raconté comment il a fait pour réussir.</p>
<p>Et toi, es-tu optimiste ou pessimiste ?</p>
<p>Malgré toutes les catastrophes qu'a subies Stephen Hawking, on dit que c'est très rare de le voir triste ou déprimé,</p>
<p>Il est tout le temps souriant.</p>
<p>Es-tu quelqu'un de positif ?</p>
<p>Crois-moi ça changera tout chez toi,</p>
<p>Si tu décides de devenir optimiste, un sourire d'espoir débutera dans ta vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
