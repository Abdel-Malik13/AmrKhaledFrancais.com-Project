                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de l’exemple à suivre…</p>
<p>Es-tu un exemple à suivre pour tes enfants ?</p>
<p>Si tu es un papa ou une maman, est-ce que je peux te faire une demande très très facile ?</p>
<p>Peux-tu ne pas donner beaucoup de leçons…</p>
<p>Garde entièrement le silence,</p>
<p>Mais fais une seule chose : Sois un modèle.</p>
<p>Donne l’exemple.</p>
<p>1000 leçons que tu vas donner à tes enfants, sur les bons comportements,</p>
<p>Seront réduites en poussière, le jour où le téléphone va sonner,</p>
<p>Et que tu diras « Répondez et dites-lui que je ne suis pas là ».</p>
<p>Sois un modèle,</p>
<p>Sois un exemple à suivre,</p>
<p>Car il existe un très beau proverbe syrien qui dit :</p>
<p>Réfléchis 100 fois, avant de mentir aux gens…</p>
<p>Mais réfléchis 1 million de fois, avant de mentir à tes enfants…</p>
<p>Et réfléchis 1 milliard de fois, avant de mentir à Dieu.</p>
<p>Cette idée, et ce principe d’être un modèle, j’ai envie de l’illustrer avec une histoire de Gandhi :</p>
<p>Un jour, une femme pauvre est venue voir Gandhi,</p>
<p>Et elle a ramené son fils avec elle.</p>
<p>Elle s’est déplacée d’une région éloignée en Inde,</p>
<p>Car les gens voyaient Gandhi comme le grand sage.</p>
<p>Elle est donc partie dans la maison de Gandhi,</p>
<p>Et elle lui a dit « Mon fils t’aime beaucoup,</p>
<p>Et mon fils est un grand admirateur de toi,</p>
<p>Tout ce que tu lui diras, il va l’appliquer tout de suite,</p>
<p>Et en vérité j’ai besoin que tu lui donnes un conseil…</p>
<p>Mon fils risque de mourir,</p>
<p>Il est diabétique,</p>
<p>Et il n’arrête pas de manger des morceaux de sucre…</p>
<p>Il en mange beaucoup, et s’il continue il va mourir,</p>
<p>S’il te plait, conseille-le »</p>
<p>Gandhi a regardé la femme avec le sourire,</p>
<p>Puis il lui a dit « S’il te plait, pars avec ton fils, et reviens me voir dans 4 semaines »</p>
<p>Il fallait qu’elle revienne 1 mois plus tard…</p>
<p>La femme est pauvre,</p>
<p>Elle est déjà sur place,</p>
<p>La distance est longue,</p>
<p>Et les transports sont chers en Inde…</p>
<p>C’est vrai que Gandhi a l’air occupé,</p>
<p>Il y a beaucoup de gens qui l’attendent,</p>
<p>Mais il pourrait prendre 2 minutes pour parler au fils…</p>
<p>La femme s’est beaucoup fâchée,</p>
<p>Elle lui a dit « Je viens de très loin,</p>
<p>Je sais que beaucoup de gens t’attendent,</p>
<p>Mais tout ce que je veux c’est que tu dises 2 mots à mon fils »</p>
<p>Gandhi lui a dit « Pars avec ton fils, et revenez me voir dans un mois ».</p>
<p>La femme s’est vexée,</p>
<p>Elle est partie,</p>
<p>Et elle a décidé de ne jamais revenir…</p>
<p>Mais après quelques semaines, elle s’est calmée,</p>
<p>Et elle a décidé de revenir voir Gandhi,</p>
<p>Car son fils n’arrête pas de manger dangereusement du sucre.</p>
<p>Elle est arrivée,</p>
<p>Et dès que Gandhi l’a vue,</p>
<p>Il a laissé tous les gens avec qui il était,</p>
<p>Et il lui a dit « Viens »</p>
<p>Il s’est assis,</p>
<p>Et lui a dit « C’est bien toi qui était venue me voir il y a 4 semaines, à propos de ton fils et le sucre ? »</p>
<p>Elle lui a répondu « Oui, tu te souviens encore de moi ? »</p>
<p>Il lui a dit « Oui bien sûr ».</p>
<p>Gandhi a fait venir le garçon, et lui a dit « Je te conseille fortement, de ne jamais manger les morceaux de sucre, pour préserver ta santé.</p>
<p>Est-ce que tu me le promets ? »</p>
<p>Le garçon lui a dit « Oui je te le promets ! »</p>
<p>Il aimait beaucoup Gandhi.</p>
<p>Gandhi a souri,</p>
<p>Mais la femme lui a dit « Je ne comprends pas,</p>
<p>Tu aurais pu lui dire la même chose il y a 4 semaines… »</p>
<p>Gandhi lui a répondu « Il y a 4 semaines, je mangeais aussi des morceaux de sucre,</p>
<p>J’avais le même problème…</p>
<p>Comment je pouvais conseiller ton fils de ne pas faire une chose, alors que moi-même je la fais ?</p>
<p>Mais j’ai décidé que 4 semaines c’était une durée suffisante pour que j’arrête de manger les morceaux de sucre,</p>
<p>Pour avoir la force de conseiller ton fils.</p>
<p>Aujourd’hui je sentais que j’avais une grande force pour le convaincre.</p>
<p>Car je suis un exemple, car j’applique la même chose ».</p>
<p>Mon cher,</p>
<p>Avant de conseiller les gens autour de toi…</p>
<p>Vérifie d’abord que tu es sincère dans ce que tu leur dis.</p>
<p>Il faut que tu donnes l’exemple,</p>
<p>Si tu es papa ou maman,</p>
<p>Il faut que tu sois un modèle à suivre.</p>
<p>Comment veux-tu que ton fils ne fume pas de cigarettes, alors que toi-même tu es un grand fumeur ?</p>
<p>Il faut que tu donnes l’exemple,</p>
<p>Pour que les valeurs que tu souhaites voir chez tes enfants, se concrétisent.</p>
<p>C’est un sourire d’espoir avec la façon de raisonner de Gandhi,</p>
<p>Et c’est un sourire d’espoir pour chaque être humain qui décide de devenir un modèle à suivre.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
