                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle du défi de réaliser les rêves.</p>
<p>Tu as un rêve ?</p>
<p>Tu y crois ?</p>
<p>Es-tu prêt à faire des efforts pour y arriver ?</p>
<p>Ou bien tu penses que c'est impossible à réaliser ?</p>
<p>Tu as honte d'en parler ?</p>
<p>Ou bien tu l'as écrit ?</p>
<p>Et tu insistes pour y arriver ?</p>
<p>Les rêves deviennent réalité quand on y croit.</p>
<p>C'était un petit garçon,</p>
<p>Issu d'une famille très pauvre,</p>
<p>Il travaillait dans une écurie de chevaux.</p>
<p>C'est l'histoire d'un petit argentin qui s'appelle Monte.</p>
<p>Il travaillait donc dans une étable,</p>
<p>Et son travail n'était même pas d'entrainer les chevaux...</p>
<p>Il faisait juste du nettoyage et ce genre de choses,</p>
<p>C'est-à-dire il faisait un travail très très très modeste...</p>
<p>Mais il avait un grand rêve.</p>
<p>Quel est son rêve ?</p>
<p>Il voulait devenir propriétaire des plus grandes écuries en Argentine.</p>
<p>C'est son rêve...</p>
<p>Tu me diras, « C'est ça... Comme si ça pouvait se réaliser ».</p>
<p>Bref, un jour il était à l'école,</p>
<p>Et le professeur a demandé à chaque élève de faire un exercice où il explique son but dans la vie,</p>
<p>Ou le rêve qu'il souhaite réaliser un jour dans sa vie.</p>
<p>Le professeur voulait des réponses réalistes...</p>
<p>Monte a écrit que son but dans la vie c'est de posséder une écurie de chevaux,</p>
<p>Et il a décrit son projet dans tous ses détails...</p>
<p>À tel point qu'il a fait un dessin qui montre la superficie totale,</p>
<p>400 m²</p>
<p>Et ici il y aura ceci... Et à cet endroit je mettrai cela...</p>
<p>Tout ce qu'il a vu dans le monde des chevaux, et en travaillant dans l'écurie,</p>
<p>Il l'a transformé en un grand projet détaillé.</p>
<p>Quand il a fini, il était content de remettre sa copie au professeur.</p>
<p>Mais quand les notes sont tombées,</p>
<p>Il a échoué...</p>
<p>Il a obtenu une note très faible.</p>
<p>Il s'est mis en colère,</p>
<p>Il a pris sa copie,</p>
<p>Il est parti voir le professeur,</p>
<p>Et lui a demandé « Pourquoi vous m'avez donné cette note ?? »</p>
<p>Le professeur lui a répondu « Mon fils, je t'ai demandé un rêve réalisable,</p>
<p>Et je l'ai bien précisé au début de l'examen.</p>
<p>Ta copie est bien écrite,</p>
<p>Mais ton projet n'est pas réalisable,</p>
<p>Tu as besoin de beaucoup d'argent,</p>
<p>Et tu as besoin d'une terre avec une grande superficie,</p>
<p>Et tu as besoin d'un grand nombre de chevaux...</p>
<p>Du coup, tu n'as pas respecté les conditions de l'exercice,</p>
<p>Et je t'ai donné une note qui correspond à ce que tu as fait.</p>
<p>Mais puisque tu es venu me voir, je vais t'accorder une 2ème chance »</p>
<p>Monte lui a demandé « Quelle chance ? »</p>
<p>Le professeur lui a dit « Je te donne l'occasion de refaire l'exercice en parlant d'un projet plus réaliste.</p>
<p>Reviens me voir une fois que tu as terminé,</p>
<p>Et je te donnerai une nouvelle note qui remplacera l'ancienne. »</p>
<p>Le petit Monte est rentré chez lui,</p>
<p>Et il a demandé à son papa « Qu'est ce que je fais ?</p>
<p>Est-ce que j'écris un nouveau sujet ? »</p>
<p>Son papa lui a répondu « Mon fils, c'est une décision que tu dois prendre tout seul.</p>
<p>Si tu crois que ton projet est réalisable, alors n'écris pas un nouveau sujet.</p>
<p>Et si au contraire tu penses que ton projet est impossible à réaliser, alors écris un nouveau sujet, et prends une meilleure note »</p>
<p>Le petit Monte n'a pas dormi toute la nuit...</p>
<p>Est-ce que quelqu'un fonctionne comme lui ?</p>
<p>Je te propose d'utiliser cette méthode,</p>
<p>Si tu as un rêve,</p>
<p>Imagine que tu es à la place de Monte,</p>
<p>Est-ce que tu écris un nouveau sujet pour obtenir une meilleure note ?</p>
<p>Ou bien tu insistes sur ton grand rêve ?</p>
<p>Monte est revenu voir son professeur le lendemain,</p>
<p>Et il lui a dit une phrase très forte...</p>
<p>Il lui a dit quoi ?</p>
<p>Il lui a dit « Professeur, gardez votre note et je garderai mon rêve ».</p>
<p>Gardez votre note « Très faible ».</p>
<p>Et moi je vais garder mon rêve.</p>
<p>Monte a obtenu une mauvaise note...</p>
<p>Les jours on passé...</p>
<p>Les années ont passé...</p>
<p>Monte a fini ses études,</p>
<p>La vie a suivi son cours...</p>
<p>Et il a réussi,</p>
<p>Il a réalisé son rêve,</p>
<p>Il a construit son écurie géante de chevaux.</p>
<p>Et un jour son ancienne école a organisé une sortie pour les élèves,</p>
<p>Ils sont partis visiter l'écurie de Monte,</p>
<p>Et dans le groupe il y avait son ancien professeur qui lui avait donné une mauvaise note...</p>
<p>Et dès qu'il l'a vu,</p>
<p>Il lui a dit « Combien de rêves d'enfants on a volés...</p>
<p>Et de combien de grandes occasions nous les avons privés...</p>
<p>Je te présente mes excuses ».</p>
<p>Un sourire d'espoir pour chaque personne qui insiste sur son rêve, jusqu'à ce qu'il devienne réalité.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
