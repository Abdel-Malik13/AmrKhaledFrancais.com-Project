                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit que ta subsistance et ta durée de vie sont entre les mains de Dieu,</p>
<p>N'ait pas peur pour tes sources de revenus et n'ait pas peur de mourir,</p>
<p>Ne vis pas dans un état de stress.</p>
<p>Je vous dis ça car j'ai un ami qui a été atteint par la maladie de l'époque : Le stress.</p>
<p>Au milieu des crises financières, des problèmes d'insécurité etc.</p>
<p>Il est devenu excessivement peureux pour ses enfants,</p>
<p>Il leur disait « Ne marchez jamais près des murs, si jamais un immeuble s'écroule »</p>
<p>« Aujourd'hui le ciel n'est pas clair, j'ai peur qu'il pleuve, qu'une voiture glisse et que vous ayez un accident en allant à l'école»</p>
<p>« Ne partez pas au club de foot pour ne pas avoir une blessure »</p>
<p>« Ne jouez pas dehors avec vos amis »</p>
<p>« Ne sortez pas la nuit »...</p>
<p>Ses enfants n'en pouvaient plus,</p>
<p>C'est leur papa, il les aime,</p>
<p>Mais il est devenu inquiet à un niveau paranoïaque...</p>
<p>Les enfants étouffaient, ils n'arrivaient pas à vivre normalement,</p>
<p>Ce n'est pas une façon de vivre...</p>
<p>Je me suis mis à réfléchir « Comment je fais pour lui expliquer »,</p>
<p>Alors j'ai trouvé une histoire que je lui dédicace et que je dédicace à tous les papas et à toutes les mamans,</p>
<p>Ceux qui sont devenus angoissés à un point anormal,</p>
<p>L'histoire dit qu'il y avait un jeune marin,</p>
<p>Qui travaillait en pleine mer.</p>
<p>Il était ami avec un autre jeune dont le métier est architecte.</p>
<p>Le papa du premier est décédé, et le papa du deuxième est aussi décédé...</p>
<p>Ils étaient tous les deux orphelins.</p>
<p>Un jour ils ont eu cette discussion émouvante,</p>
<p>Le jeune architecte a demandé au jeune marin</p>
<p>« Comment ton papa est décédé ? »</p>
<p>Il lui a dit « Mon papa est mort en pleine mer...</p>
<p>Il y a eu une tempête violente,</p>
<p>Le bateau s'est renversé,</p>
<p>Et mon papa est mort noyé... »</p>
<p>Le jeune architecte lui a répondu</p>
<p>« Je suis vraiment navré,</p>
<p>Ca doit être très difficile à vivre...</p>
<p>Et comment est décédé ton grand-père ? »</p>
<p>Il lui a répondu « Mon grand-père est aussi mort en pleine mer »</p>
<p>Le jeune architecte lui a dit « Ton papa est mort noyé,</p>
<p>Et ton grand-père est aussi mort noyé ? »</p>
<p>Il lui a répondu « Ils étaient tous les deux marins, et ils travaillaient dans la pêche »</p>
<p>Le jeune architecte lui a dit « Et toi aussi tu travailles dans la pêche ? »</p>
<p>Il lui a répondu « Oui »</p>
<p>Le jeune architecte lui a dit « Est-ce que tu es fou ?</p>
<p>Ton papa est mort en pleine mer,</p>
<p>Ton grand-père est mort en pleine mer,</p>
<p>Et tu continues à travailler en mer ? »</p>
<p>Le jeune pêcheur l'a regardé avec le sourire, et lui a demandé</p>
<p>« Et toi comment est décédé ton papa ? »</p>
<p>Il lui a dit « Il est mort dans son lit »</p>
<p>Puis il lui a demandé « Et comment est décédé ton grand-père ? »</p>
<p>Il lui a répondu « Il est aussi mort dans son lit »</p>
<p>Le jeune pêcheur lui a dit « Est-ce que tu es fou ?</p>
<p>Tu continues à dormir dans un lit chaque soir ? »</p>
<p>Quelqu'un a compris l'idée qu'il voulait lui transmettre ?</p>
<p>Il voulait lui dire : Cette vie est passagère, on partira tous de différentes façons, ca ne sert à rien de vivre en ayant peur.</p>
<p>On peut résumer cette histoire en un seul mot,</p>
<p>Un mot qu'a dit Jacob (le père du Prophète Joseph)</p>
<p>(Par le passé, il avait confié Joseph à ses frères qui l'ont jeté dans un puits et sont revenus en disant qu'un loup l'avait mangé,</p>
<p>Puis des années plus tard, il s'est trouvé obligé de confier le plus petit de ses fils à ses frères)</p>
<p><strong>{ Il dit: « Vais-je vous le confier comme, auparavant, je vous ai confié son frère? Mais Allah est le meilleur gardien, et Il est Le plus Miséricordieux des miséricordieux! » }</strong></p>
<p>Sourate 12 - Yûsuf : Joseph (verset 64)</p>
<p>Je le dédicace aux parents qui ont peur pour leurs enfants à un niveau excessif,</p>
<p>Et qui vivent constamment dans le stress,</p>
<p>Je leur dédicace ce verset du Coran <strong>{ Mais Allah est le meilleur gardien, et Il est Le plus Miséricordieux des miséricordieux! » }</strong></p>
<p>Le jeune architecte lui a dit « Ton papa est noyé en mer, et ton grand-père est aussi mort noyé... Tu es sûrement fou »</p>
<p>Le jeune pêcheur lui a répondu « Ton papa et ton grand-père sont morts dans leur lits... Comment peux-tu encore dormir sur un lit ? »</p>
<p>Mes chers : <strong>{ Mais Allah est le meilleur gardien }</strong></p>
<p>L'essentiel c'est qu'on ne fasse pas des choses dangereuses,</p>
<p>Mais en même temps on ne doit pas être paranoïaques,</p>
<p>On doit faire confiance à Dieu,</p>
<p>Pour qu'on vive nous et nos enfants avec un sourire d'espoir.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
