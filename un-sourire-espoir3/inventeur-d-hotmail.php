                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de la fidélité envers son pays,</p>
<p>C’est-à-dire la gratitude envers ton pays,</p>
<p>Es-tu reconnaissant ?</p>
<p>Mais je vais commencer l’idée de très très très loin…</p>
<p>C’est un homme indien qui s’appelle Sabeer Bhatia,</p>
<p>Sabeer est un indien musulman,</p>
<p>Et il a inventé quelque chose qui n’existait pas avant lui,</p>
<p>Et qu’on utilise tous aujourd’hui…</p>
<p>C’est lui qui a inventé Hotmail, et l’E-mail.</p>
<p>Avant Sabeer Bhatia, les gens ne communiquaient pas par E-mail,</p>
<p>Ça s’est démocratisé avec cet homme indien.</p>
<p>En 1988, Sabeer est parti faire ses études aux États-Unis,</p>
<p>À l’université de Stanford,</p>
<p>Et il a obtenu son diplôme avec mention.</p>
<p>Puis il a travaillé comme informaticien programmeur dans une société,</p>
<p>Et dans cette société il a fait connaissance avec un collègue qui s’appelle Jack Smith.</p>
<p>Tous les 2, ont beaucoup réfléchi à une nouvelle idée, qui pourrait faciliter la communication entre les gens…</p>
<p>Ils voulaient une méthode gratuite,</p>
<p>Une méthode personnelle,</p>
<p>Une méthode qui respecte l’intimité de chacun,</p>
<p>Pouvoir écrire et lire tes messages, sans que personne d’autre n’y accède,</p>
<p>Et pouvoir recevoir et envoyer des messages peu importe où tu te trouves dans le monde…</p>
<p>Ils se réunissaient et discutaient pour trouver une solution,</p>
<p>Alors le patron de la société se disputait avec eux…</p>
<p>Il leur disait « Arrêtez de discuter et concentrez-vous sur votre travail »</p>
<p>À tel point que tous les 2 ont fini par être renvoyés de la société…</p>
<p>Mais leur rêve était grand.</p>
<p>Mes chers, c’est de cette façon que les rêves se réalisent,</p>
<p>Des gens qui meurent d’envie de réaliser leur projet, qui s’y voient déjà…</p>
<p>Albert Einstein a dit « Le rêve est plus important que l’invention.</p>
<p>Car à l’origine de chaque invention il y a un rêve. »</p>
<p>C’est magnifique !</p>
<p>Les deux amis ont continué à développer leur idée,</p>
<p>Ils ont créé Hotmail,</p>
<p>Ça a commencé à se faire connaitre,</p>
<p>Et au bout d’un an, 10 millions de personnes utilisaient Hotmail…</p>
<p>Alors Bill Gates, le patron de Microsoft était très intéressé, il voulait acheter leur projet.</p>
<p>Il est parti voir Sabeer en 1997, et il lui a proposé 50 millions de dollars.</p>
<p>Mais Sabeer était intelligent, il comprenait l’importance de son invention,</p>
<p>Il lui a dit « 50 ? C’est non bien sûr,</p>
<p>Je demande 500 millions de dollars »</p>
<p>Vous imaginez bien que Sabeer maintenant est très riche,</p>
<p>Mais c’est son droit.</p>
<p>(Et vous allez voir comment il raisonne)</p>
<p>Il a demandé 500 millions de dollars à Microsoft,</p>
<p>Bill Gates a dit « C’est beaucoup trop »</p>
<p>Ils ont négocié pendant 1 an…</p>
<p>Et en 1998, Sabeer a accepté de vendre Hotmail pour 400 millions de dollars,</p>
<p>Mais à une condition : Travailler comme expert chez Microsoft.</p>
<p>Hotmail a atteint 90 millions d’utilisateurs,</p>
<p>Et l’utilisation de l’E-mail fait désormais partie de la vie de chacun de nous.</p>
<p>Sabeer a été honoré, il a rencontré les chefs d’états du monde entier,</p>
<p>Il a été reçu par le président indien,</p>
<p>Il a été invité par le président américain, et le président français…</p>
<p>Il est devenu quelqu’un d’important.</p>
<p>Et sa fortune s’élève à combien ?</p>
<p>Je vous ai dit qu’il a vendu Hotmail pour 400 millions de dollars,</p>
<p>Mais sa fortune est de 100 millions de dollars.</p>
<p>Ah bon ?</p>
<p>Où sont les 300 millions qui manquent ?</p>
<p>Il les a donnés à son pays l’Inde…</p>
<p>Il a dit « J’ai fait exprès d’élever le prix de vente,</p>
<p>Pour obtenir plus, et investir dans l’éducation des enfants indiens »</p>
<p>…</p>
<p>Sabeer a fondé un organisme pour scolariser les enfants en Inde,</p>
<p>Et les étudiants les plus brillants,</p>
<p>Voyagent à ses frais, et vont finir leurs études à l’étranger.</p>
<p>Puis il a créé un centre pour découvrir les jeunes talents,</p>
<p>Pour leur donner une chance de réussir, et financer leurs projets.</p>
<p>Sabeer travaille toujours dans l’informatique,</p>
<p>Mais ce qui le passionne le plus, sont les 300 millions qu’il a investit dans son pays.</p>
<p>Puis il s’est demandé « Je vais faire quoi des 100 millions qui restent ? C’est beaucoup trop… »</p>
<p>Alors il a commencé à investir même les 100 millions dans les écoles indiennes.</p>
<p>L’histoire de Sabeer est une histoire magnifique,</p>
<p>D’un musulman, inventeur,</p>
<p>Qui a fait une chose qui a fait progresser le monde.</p>
<p>Enfin nous avons trouvé un musulman qui a réalisé une grande chose dans la programmation informatique,</p>
<p>Et qui a facilité la vie du monde entier…</p>
<p>Mais en même temps Sabeer aime son pays,</p>
<p>Il aime son pays, et il est fidèle,</p>
<p>Il est prêt à donner pour le développer.</p>
<p>Un sourire d’espoir pour chaque personne qui aime son pays, et qui ne l’oublie pas une fois qu’elle a réussi dans sa vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
