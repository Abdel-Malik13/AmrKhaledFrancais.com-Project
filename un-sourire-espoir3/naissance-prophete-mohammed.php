                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle du souvenir de la naissance du Prophète Mohamed (bénédictions et paix soient sur lui),</p>
<p>Aujourd'hui c'est la date de naissance du Prophète (bpsl),</p>
<p>Et en réalité la chose que j'ai envie de transmettre dans cet épisode c'est l'amour de Dieu pour le Prophète (bpsl),</p>
<p>À quel point Dieu aime le Prophète (bpsl)...</p>
<p>Car j'ai trouvé dans le Coran, une Sourate qui parle de cet amour de Dieu pour le Prophète (bpsl),
<p>Sourate 93 - Ad-Duhâ : Le Jour Montant</p>
<p><strong>{ 1. Par le Jour Montant!</strong></p>
<p><strong>2. Et par la nuit quand elle couvre tout!</strong></p>
<p><strong>3. Ton Seigneur ne t'a ni abandonné, ni détesté. }</strong></p>
<p>Comment peux-tu imaginer Mohamed que Dieu va t'abandonner ?</p>
<p>Notre Seigneur ne t'abandonnera pas Ô Messager de Dieu.</p>
<p>Imaginez la tendresse...</p>
<p><strong>{ 3. Ton Seigneur ne t'a ni abandonné, ni détesté.</strong></p>
<p><strong>4. La vie dernière t'est, certes, meilleure que la vie présente.</strong></p>
<p><strong>5. Ton Seigneur t'accordera certes [Ses faveurs], et alors tu seras satisfait. }</strong></p>
<p>Tu vas obtenir beaucoup de choses... On te donnera satisfaction,</p>
<p>Imagine quand Dieu dit au Prophète (bpsl) qu'Il va le satisfaire,</p>
<p><strong>{ Ton Seigneur t'accordera certes [Ses faveurs] }</strong> jusqu'à ce que tu sois satisfait.</p>
<p><strong>{ 6. Ne t'a-t-Il pas trouvé orphelin? Alors Il t'a accueilli! }</strong></p>
<p>Comment peux-tu penser qu'On va t'abandonner ?</p>
<p>N'étais-tu par orphelin alors On t'a accueilli...</p>
<p><strong>{ 7. Ne t'a-t-Il pas trouvé égaré? Alors Il t'a guidé.</strong></p>
<p><strong>8. Ne t'a-t-Il pas trouvé pauvre? Alors Il t'a enrichi. }</strong></p>
<p>Tu étais pauvre, tu étais orphelin...</p>
<p>N'est ce pas Dieu qui t'a soutenu et aidé ?</p>
<p>Puis le plus étonnant mes chers, c'est que la sourate suivante du Coran, parle de la même façon...</p>
<p>Sourate 94 - Ach-Charh : L'Ouverture</p>
<p><strong>{ N'avons-Nous pas ouvert(*) pour toi ta poitrine? }</strong></p>
<p>(*) Ouvert: pour recevoir la foi, les directives et les préceptes énoncés dans le Coran.</p>
<p>Comme si c'était relié à la sourate d'avant...</p>
<p>Deux sourates qui se suivent,</p>
<p>Comment Ô Messager de Dieu, tu peux imaginer que Dieu va t'abandonner ?</p>
<p>Car il y avait une rumeur dans la tribu de Qoraich (à la Mecque) que le Dieu de Mohamed l'a abandonné, car les révélations (de l'ange Gabriel) s'étaient retardées...</p>
<p>Ca faisait quelques semaines ou quelques mois que le Prophète (bpsl) n'avait pas reçu des révélations,</p>
<p>Donc certains disaient c'est sûr que son Dieu l'a abandonné... C'est fini...</p>
<p>Alors la sourate 93 est descendue,</p>
<p>Surtout pas !</p>
<p><strong>{ 3. Ton Seigneur ne t'a ni abandonné, ni détesté.</strong></p>
<p><strong>4. La vie dernière t'est, certes, meilleure que la vie présente.</strong></p>
<p><strong>5. Ton Seigneur t'accordera certes [Ses faveurs], et alors tu seras satisfait. }</strong></p>
<p>Ce sont des paroles extrêmement tendres... Entièrement composées de miséricorde, et d'amour de Dieu (Exalté Soit-Il) pour le Prophète (bpsl),</p>
<p>Puis la sourate suivante continue</p>
<p><strong>{ 1. N'avons-Nous pas ouvert pour toi ta poitrine?</strong></p>
<p><strong>2. Et ne t'avons-Nous pas déchargé du fardeau</strong></p>
<p><strong>3. qui accablait ton dos?</strong></p>
<p><strong>4. Et exalté pour toi ta renommée? }</strong></p>
<br>
<p>Puis tu es triste car tu as quelques difficultés...</p>
<p><strong>{ 5. À côté de la difficulté est, certes, une facilité!</strong></p>
<p><strong>6. À côté de la difficulté est, certes, une facilité! }</strong></p>
<p>C'est quoi cette tendresse ?</p>
<p>Quel est cet amour immense pour le Prophète (bpsl) ?</p>
<p>C'est-à-dire on ressent ceci :</p>
<p>Si Dieu Lui-même dit ca du Prophète (bpsl),</p>
<p>J'ai envie de le rencontrer le jour de la résurrection,</p>
<p>De profiter de sa présence,</p>
<p>Et de voir celui pour qui Dieu a dit ces très belles paroles,</p>
<p>À quoi ressemblaient ses comportements...</p>
<p>Et à quel point les relations étaient belles avec lui...</p>
<p>Et comment était sa clémence envers les gens...</p>
<p>Et à quel point son humanité était grande...</p>
<p>Le Prophète de la miséricorde,</p>
<p>Le Prophète de l'humanité,</p>
<p><strong>{ Et tu es certes, d'une moralité éminente. }</strong></p>
<p>Sourate 68 - Al-Qalam : La Plume (verset 4)</p>
<p>C'est lui notre Prophète (bpsl),</p>
<p>Et c'est ca l'amour de Dieu pour lui...</p>
<p>Vous pouvez me dire « Tous les gens disent ce que tu viens de dire le jour de la naissance du Prophète (bpsl)... Ils parlent tous de l'amour de Dieu pour le Prophète (bpsl),</p>
<p>Et tous les gens parlent de notre amour pour le Prophète (bpsl),</p>
<p>Qu'y a-t-il de nouveau ? »</p>
<p>Le nouveau c'est le mot que je vais dire à la fin...</p>
<p>Les derniers versets de la Sourate 94 - Ach-Charh : L'Ouverture</p>
<p><strong>{ 1. N'avons-Nous pas ouvert pour toi ta poitrine? }</strong></p>
<br>
<p>Après que Dieu lui a dit qu'Il l'aimait tout cet amour,</p>
<p>Et qu'Il lui a accordé toutes ces choses,</p>
<p>Dieu lui a fait une demande...</p>
<p>C'est le nouveau que j'ai envie de vous dire le jour de la naissance du Prophète (bpsl),</p>
<p>Si vous aimez le Prophète (bpsl)...</p>
<p>Quelle demande ?</p>
<p><strong>{ 7. Quand tu te libères, donc, lève-toi, }</strong></p>
<p>Que signifie <strong>{ Lève-toi, }</strong>?</p>
<p>Ca signifie lève-toi pour prier.</p>
<p>Mais <strong>{ Quand tu te libères }</strong> de quoi ?</p>
<p>Du travail,</p>
<p>Ah bon...</p>
<p>Comme si Dieu lui disait :</p>
<p>Je t'ai donné toutes ces choses,</p>
<p>Et je ne veux de toi qu'une seule chose,</p>
<p>« Le travail ».</p>
<p>Je jure que c'est un message fort pour nous tous...</p>
<p>La communauté de Mohamed ne travaille pas (sauf très peu),</p>
