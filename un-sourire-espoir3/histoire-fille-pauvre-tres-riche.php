                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit : Quand on vit dans un état de pauvreté,</p>
<p>Quand notre famille, ou notre région, ou notre quartier est pauvre...</p>
<p>Quand on vit avec très peu d'argent,</p>
<p>Nous devons éduquer nos enfants d'une façon différente.</p>
<p>Une éducation qui corrige le problème négatif que peut être la pauvreté,</p>
<p>Une éducation riche en valeurs, pour combler le manque d'argent chez nos enfants.</p>
<p>Vous comprenez l'idée ?</p>
<p>C'est-à-dire la maman qui n'a pas d'argent,</p>
<p>Et dont les enfants se sentent pauvres,</p>
<p>« Qu'est ce qu'on peut y faire ?</p>
<p>C'est notre réalité qui est comme ça... »</p>
<p>Et bien il faut réfléchir à comment combler ce côté,</p>
<p>Et comment faire comprendre à tes enfants « Non mon chéri, tu es quelqu'un de riche ».</p>
<p>L'histoire d'aujourd'hui c'est une jeune femme qui la raconte,</p>
<p>Elle dit :</p>
<p>Quand j'étais petite, ma maman m'a élevée sans que je remarque qu'on était pauvres,</p>
<p>J'ignorais qu'on était une famille pauvre.</p>
<p>Je ne l'ai su que quand je suis arrivée à la 3ème année de l'école primaire.</p>
<p>J'avais 5 frères et sœurs qui jouaient avec moi,</p>
<p>Je trouvais tout le temps mes vêtements propres,</p>
<p>Ma maman faisait très attention à ce que mes vêtements soient lavés,</p>
<p>Puis chaque fois qu'il y avait une déchirure, elle prenait une aiguille pour la coudre sans que ça se voie.</p>
<p>Et maman lavait chaque matin mes cheveux,</p>
<p>Et chaque matin je trouvais mes chaussures cirées,</p>
<p>Je n'avais jamais remarqué qu'on était pauvres, grâce à tout ce que maman faisait.</p>
<p>Jusqu'au jour où je me suis disputée avec 2 filles de ma classe,</p>
<p>J'étais en 3ème année du primaire,</p>
<p>Elles m'ont regardé de haut en disant « Tu n'es qu'une pauvre ! »</p>
<p>(C'est une petite fille... Cette phrase lui a fait très mal)</p>
<p>Elle raconte :</p>
<p>Je suis rentrée de l'école en pleurant,</p>
<p>Et en arrivant à la maison, je regardais autour de moi,</p>
<p>Et je me suis rendue compte qu'effectivement on était pauvres...</p>
<p>Le drap sur le lit est déchiré,</p>
<p>La peinture des murs est en mauvais état,</p>
<p>Puis je suis partie ouvrir le frigo</p>
<p>(Ma maman me regardait pendant que je faisais tout ça)</p>
<p>Je n'ai trouvé à l'intérieur du frigo qu'un plat avec un peu d'haricots, et un peu de lentilles...</p>
<p>Et dans la cuisine je n'ai trouvé que du pain vieux de 3 jours,</p>
<p>C'est quoi ça ?</p>
<p>Je ne savais pas que j'étais pauvre...</p>
<p>Les 2 filles à l'école avaient raison,</p>
<p>J'ai continué à pleurer,</p>
<p>Puis je suis partie voir maman,</p>
<p>Et avec toutes mes forces je lui ai dit « Est-ce qu'on est pauvres ?? »</p>
<p>J'imaginais que maman allait se défendre,</p>
<p>J'imaginais qu'elle allait me donner un câlin,</p>
<p>Et me donner des explications...</p>
<p>Mais maman m'a regardé en étant étonnée,</p>
<p>Elle m'a dit « Pauvres ?</p>
<p>Tu dis que nous sommes pauvres ?</p>
<p>Non ma chérie, nous ne sommes pas pauvres »</p>
<p>Puis elle a ouvert la porte de la cuisine en me disant « Regarde tes frères qui jouent.</p>
<p>Nous possédons des choses que d'autres gens n'arrivent pas à obtenir.</p>
<p>Tu vois comment tes frères ont le sourire ?</p>
<p>Tu vois comment ils sautent partout ?</p>
<p>Tu vois comment ils sont en bonne santé ? »</p>
<p>Puis elle est partie chercher une photo,</p>
<p>Elle me l'a montrée en disant « Tu te souviens de cette photo ?</p>
<p>Pendant qu'on mangeait tous ensemble?</p>
<p>Il y a des haricots et des lentilles sur la table,</p>
<p>Mais tu vois comment on a le sourire ? »</p>
<p>Puis elle a ouvert le frigo en disant</p>
<p>« Tu vois les haricots qu'on a ?</p>
<p>Il y a des gens qui n'ont pas ça à manger »</p>
<p>Puis elle m'a regardée,</p>
<p>Et elle m'a dit « Ah oui ma chérie... Tu parlais d'argent, tu voulais dire qu'on était pauvres financièrement ?</p>
<p>Peut-être qu'on est pauvres en argent,</p>
<p>Mais nous possédons d'autres choses dans lesquelles nous sommes très riches,</p>
<p>Peut-être que même ceux qui ont beaucoup d'argent n'arrivent pas les à avoir.</p>
<p>Ma fille, peut-être que ceux qui sont riches en argent, ne savent pas sourire comme nous.</p>
<p>Peut-être que même ceux qui ont de l'argent ne sont pas en bonne santé comme nous.</p>
<p>Peut-être que même ceux qui ont de l'argent ne sont pas unis et ne s'aiment pas comme dans notre famille.</p>
<p>Non ma chérie,</p>
<p>Puis nous sommes très intègres,</p>
<p>Nous sommes très riches, car on possède beaucoup de choses. »</p>
<p>Ma maman m'a laissée,</p>
<p>Puis elle a continué à cuisiner comme si rien ne s'était passé.</p>
<p>Ce qu'elle ne savait pas, c'est que ce jour là, elle m'a donné plus important que la nourriture du ventre.</p>
<p>Elle a nourrit mon cœur et mon âme,</p>
<p>Elle a dessiné sur mon visage un sourire d'espoir : Je suis riche.</p>
<p>Nous avons besoin de donner des sourires d'espoir à nos enfants,</p>
<p>Pour les rassurer au milieu de la pauvreté financière,</p>
<p>Et pour leur faire sentir qu'ils sont riches avec plein d'autres choses.</p>
<p>Dieu est Généreux, et Il rend riches les cœurs de ceux qui cherchent la vraie richesse.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
