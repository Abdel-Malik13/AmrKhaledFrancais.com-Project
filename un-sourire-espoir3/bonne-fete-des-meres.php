                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est à l'occasion de la fête des mères...</p>
<p>Pendant cette fête, je n'avais pas envie de raconter une histoire mais plutôt dire un sens, qui attendrisse nos cœurs et qui nous fasse aimer nos mamans encore et encore.</p>
<p>Ce sens est : La maman est la personnification de la Miséricorde de Dieu.</p>
<p>Ah bon ?</p>
<p>La Miséricorde de Dieu est représentée par les mamans ?</p>
<p>Oui.</p>
<p>Vous connaissez la parole du Prophète (bénédiction et paix soient sur lui) dans laquelle il dit</p>
<p><strong class="prophete">« Dieu a créé la Miséricorde en 100 parties.</strong></p>
<p><strong class="prophete">Il a fait descendre 1 partie sur terre,</strong></p>
<p><strong class="prophete">Et Il a gardé chez Lui 99 parties ».</strong></p>
<p>Et si tu me dis « Je veux un exemple de la Miséricorde de Dieu »</p>
<p>Tu en as beaucoup...</p>
<p>Le soleil fait partie de la Miséricorde de Dieu,</p>
<p>L'eau est une Miséricorde de Dieu,</p>
<p>Et pareil pour l'air,</p>
<p>Toutes celles-ci font partie de la Miséricorde de Dieu.</p>
<p>Mais toutes celles-ci, sont des Miséricordes silencieuses...</p>
<p>Ça veut dire quoi ?</p>
<p>Le soleil est silencieux, il ne parle pas,</p>
<p>Ce sont des Miséricordes qui ne parlent pas...</p>
<p>Alors Dieu (Exalté Soit-Il) a voulu nous donner une Miséricorde vivante, qui bouge devant nous et qui parle devant nous,</p>
<p>Quand tu la vois en train de parler, tu dis « Oui ! C'est ça la Miséricorde de Dieu ».</p>
<p>La maman...</p>
<p>Ta maman est la représentation de la Miséricorde de Dieu.</p>
<p>C'est magnifique,</p>
<p>Dieu a créé beaucoup de Miséricordes,</p>
<p>Mais Il a voulu qu'une d'entre elles soit proche de chacun de nous,</p>
<p>Qu'on la touche,</p>
<p>Qu'on la ressente beaucoup,</p>
<p>Qu'on vive sous son ombre,</p>
<p>Pour pouvoir toujours ressentir la Miséricorde de Dieu...</p>
<p>C'est ta maman.</p>
<p>Ta maman c'est la Miséricorde de Dieu.</p>
<p>Tu veux ressentir la Miséricorde de Dieu ?</p>
<p>Regarde ta maman pendant qu'elle te fait des Doua'a (Prières),</p>
<p>Ou pendant qu'elle fait des sacrifices pour toi,</p>
<p>Ou pendant qu'elle est heureuse de te voir,</p>
<p>Ou pendant qu'elle te souhaite tout le bien du monde, même à ses dépens.</p>
<p>Et le plus surprenant mes chers, c'est que le Prophète (bpsl) nous a dit ce sens,</p>
<p>Un jour il a vu une maman porter son bébé,</p>
<p>Elle l'a enroulé dans un tissu pour le protéger du soleil en plein désert,</p>
<p>Cette maman avait peur que le soleil atteigne son bébé,</p>
<p>Il n'y avait aucune ombre pour s'abriter,</p>
<p>Alors elle le couvrait, et s'exposait elle même au soleil.</p>
<p>Le Prophète (bpsl) a regardé les compagnons et leur a dit <strong class="prophete">« Est-ce que vous imaginez cette maman jeter son enfant dans le feu ? »</strong></p>
<p>Ils lui ont répondu « Non Ô Messager de Dieu, c'est impossible »</p>
<p>Le Prophète (bpsl) a dit <strong class="prophete">« Dieu est plus Miséricordieux envers vous, que cette maman envers son enfant »</strong></p>
<p>Il voulait nous dire quoi ?</p>
<p>Vous voyez la maman ?</p>
<p>C'est la représentation de la Miséricorde de Dieu.</p>
<p>Dieu est encore plus Miséricordieux.</p>
<p>Est-ce que tu aimes ta maman ?</p>
<p>Ou bien tu vas lui acheter un cadeau juste parce que c'est la tradition pendant la fête des mères ?</p>
<p>Est-ce que tu ressens la valeur de ta maman ?</p>
<p>Est-ce que tu sens que ta maman symbolise la Miséricorde de Dieu ?</p>
<p>Va embrasser sa main aujourd'hui,</p>
<p>Et embrasse sa tête,</p>
<p>Donne-lui un câlin,</p>
<p>Cherche à la satisfaire,</p>
<p>Demande-lui de te faire des Doua'a,</p>
<p>C'est magnifique si elle te fait des Prières aujourd'hui...</p>
<p>Ta journée,</p>
<p>Non ta vie entière sera remplie de bien.</p>
<p>Et ne provoque surtout pas la colère de ta maman,</p>
<p>Car si la maman représente la Miséricorde de Dieu,</p>
<p>Alors c'est aussi le cas pour sa colère,</p>
<p>Si ta maman est fâchée contre toi, ta vie sera triste, malheureuse et douloureuse...</p>
<p>Satisfais ta maman et tu vivras heureux,</p>
<p>Puis tu auras la garantie d'un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
