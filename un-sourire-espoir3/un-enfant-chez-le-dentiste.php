                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Le sourire d'espoir d'aujourd'hui vient d'un cabinet de dentiste,</p>
<p>Je soigne mes dents chez le même dentiste depuis longtemps,</p>
<p>Et à l'accueil du cabinet travaille un jeune homme,</p>
<p>Il m'a raconté une histoire qui m'a beaucoup touchée,</p>
<p>Et je me suis dit : Il faut que je vous la raconte dans cet épisode.</p>
<p>Il m'a dit :</p>
<p>Un jour pendant que j'étais au travail,</p>
<p>Un petit garçon est venu s'asseoir à côté de moi,</p>
<p>Je jouais avec mon téléphone portable,</p>
<p>Le petit garçon a laissé sa maman, et il est venu s'asseoir à côté de moi,</p>
<p>Il regardait mon téléphone portable,</p>
<p>Alors je le lui ai donné,</p>
<p>Il a joué un peu avec...</p>
<p>Sa maman avait l'air d'une femme très modeste,</p>
<p>Pauvre financièrement,</p>
<p>Elle l'a grondé, elle lui a dit « Laisse le téléphone de tonton, et viens t'asseoir ici à côté de moi »</p>
<p>Je lui ai dit « Non il n'y a pas de problème, tu peux le laisser jouer »</p>
<p>Le petit doit être âgé de 6 ans,</p>
<p>Quand sa maman l'a grondé,</p>
<p>Il m'a dit « Tonton, quelle chance tu as !</p>
<p>Tu es un adulte, tu n'as pas de problèmes.</p>
<p>Mais moi je suis petit, j'ai beaucoup de problèmes avec maman. »</p>
<p>En entendant ça, j'ai rit...</p>
<p>Le petit ne comprenait pas ce qui me faisait rire,</p>
<p>Je lui ai alors dit « Raconte-moi quels sont tes plus grands problèmes dans la vie »</p>
<p>Il m'a répondu « Mes plus grands problèmes, c'est quand on va faire des courses,</p>
<p>J'ai envie d'acheter des biscuits, des chips, du chocolat...</p>
<p>Mais maman me dit toujours de n'acheter qu'une seule chose à la fois.</p>
<p>C'est le plus gros problème que j'ai. »</p>
<p>Comme il n'y avait pas beaucoup de monde au cabinet, j'ai fait une pause,</p>
<p>J'ai demandé à mon collègue de me remplacer à l'accueil,</p>
<p>Je suis parti voir la maman, et je lui ai dit « Je veux descendre avec ton fils au supermarché d'en bas, pour acheter des sucreries »</p>
<p>La maman était très contente, et très touchée par mon geste.</p>
<p>J'ai décidé de résoudre les problèmes de cet enfant ne serait-ce que pour un seul jour...</p>
<p>C'est-à-dire au moins pour une seule fois, son problème sera résolu,</p>
<p>Je suis descendu avec lui au supermarché,</p>
<p>Et je lui ai dit « Achète tout ce que tu veux »</p>
<p>Il a pris des chocolats, des chips etc.</p>
<p>Et il était très très très content...</p>
<p>On est repartis,</p>
<p>Et moi aussi j'étais très content : Je lui ai résolu son problème.</p>
<p>On est remontés au cabinet,</p>
<p>Sa maman m'a beaucoup remercié,</p>
<p>Il s'est assis à côté de moi,</p>
<p>Et pendant qu'il était assis, il m'a regardé, et m'a dit « Et toi tonton, quel est le plus gros problème dans ta vie ? »</p>
<p>Je me suis senti secoué...</p>
<p>Je lui ai dit « Non mon chéri, mon problème est beaucoup plus grand que ton problème...</p>
<p>Je n'arrive pas à me marier,</p>
<p>Car je n'ai pas d'appartement »</p>
<p>Le petit m'a répondu avec une grande innocence « Pas grave tonton,</p>
<p>Comme Dieu t'a envoyé à moi, pour résoudre mon problème,</p>
<p>Il t'enverra quelqu'un pour résoudre ton problème,</p>
<p>L'essentiel c'est de faire ce que tu as à faire tonton. »</p>
<p>J'ai senti que c'était une leçon formidable venant d'un enfant...</p>
<p>« Comme Dieu t'a envoyé à moi, pour résoudre mon problème,</p>
<p>Il t'enverra quelqu'un pour résoudre ton problème,</p>
<p>L'essentiel c'est de faire ce que tu as à faire »</p>
<p>Une leçon formidable d'un enfant,</p>
<p>Qui dessine un sourire d'espoir sur le visage de chaque personne qui a baissé les bras devant son problème,</p>
<p>Qui a pensé qu'il n'y avait pas de solution,</p>
<p>Tu as un Dieu Généreux, qui t'enverra celui qui te résoudra ton problème,</p>
<p>L'essentiel c'est de t'activer et de faire ta part d'effort.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
