                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui parle de la satisfaction du destin de Dieu.</p>
<p>Mes chers : Réagir face au bien et face au mal,</p>
<p>Réagir face aux joies et face aux catastrophes,</p>
<p>Ça nécessite un esprit calme…</p>
<p>Ça veut dire quoi ?</p>
<p>Ça veut dire : Quand tu entends une bonne nouvelle, n’exulte pas au point de faire une crise cardiaque.</p>
<p>Et quand tu entends une mauvaise nouvelle, ne déprime pas au point de mourir de tristesse.</p>
<p>Réagir face aux joies et face aux catastrophes,</p>
<p>Ça a besoin d’un genre de satisfaction.</p>
<p>Moi, je fais face au destin avec un esprit calme,</p>
<p>Un état d’esprit serein et équilibré.</p>
<p>C’est une histoire symbolique :</p>
<p>On raconte qu’un bateau naviguait en pleine mer,</p>
<p>Puis une tempête est arrivée…</p>
<p>Le bateau a heurté des rochers volcaniques,</p>
<p>Du coup il a coulé…</p>
<p>Beaucoup de gens sont morts,</p>
<p>Il n’y avait qu’un petit nombre de rescapés.</p>
<p>Parmi ces survivants, il y avait un homme qui a été emmené par les vagues,</p>
<p>Il a essayé de nager pour rester en vie,</p>
<p>Et à la fin il a échoué sur une île déserte en zone des Caraïbes,</p>
<p>Quelque part entre l’Amérique du nord et l’Amérique du sud.</p>
<p>Une île sauvage, au milieu d’un océan sans fin…</p>
<p>Dès que cet homme est arrivé sur l’île,</p>
<p>Il s’est évanoui tellement il était fatigué,</p>
<p>Puis quand il s’est réveillé il ne savait pas où il était…</p>
<p>Il a commencé à regarder autour de lui,</p>
<p>Il avait peur,</p>
<p>Puis il a aperçu quelques fruits dans les arbres,</p>
<p>Il a commencé à en manger…</p>
<p>Ensuite il a trouvé une source d’eau,</p>
<p>Il a commencé à en boire…</p>
<p>Puis il a pris des branches d’arbres,</p>
<p>Et il a construit une cabane de fortune…</p>
<p>Le jour, il s’abritait du soleil dans sa cabane,</p>
<p>Et il ne savait pas comment il allait s’en sortir…</p>
<p>Il désespérait.</p>
<p>Puis un jour il est sorti de sa cabane pour chercher quelque chose à manger,</p>
<p>Il est resté dans la forêt pendant 3 jours,</p>
<p>Et en revenant il a trouvé que sa cabane était en feu…</p>
<p>Ça y est, il était à bout…</p>
<p>Il a crié « Mon Dieu même ma cabane ??!</p>
<p>Mon Dieu je n’ai rien du tout !</p>
<p>Toutes ces catastrophes en 3 jours ??! »</p>
<p>Cet homme a craqué,</p>
<p>C’est-à-dire il a dit des choses qui ne se disent pas…</p>
<p>« Pourquoi Tu me fais ça mon Dieu ?? »</p>
<p>Et pendant qu’il faisait cette crise nerveuse,</p>
<p>Un navire s’approchait de l’île…</p>
<p>Puis le navire a envoyé un petit bateau de sauvetage,</p>
<p>Le bateau de sauvetage s’approchait, comme s’il venait spécialement pour cet homme.</p>
<p>Il ne comprenait pas « Ils viennent pour moi ? Comment ils ont su que j’étais là ? »</p>
<p>Le capitaine du bateau lui a dit « Nous avons vu une fumée »</p>
<p>C’était la fumée de la cabane qui a brûlé…</p>
<p>« Et en voyant la fumée, on a pensé que c’était peut-être un appel au secours,</p>
<p>Alors on a envoyé quelqu’un pour vérifier »</p>
<p>L’homme a été sauvé, et il a retrouvé sa vie normale.</p>
<p>Et toi… Combien de fois t’es-tu opposé au destin de Dieu quand une catastrophe t’est arrivée ?</p>
<p>Combien de fois tu as dit « Pourquoi ça tombe sur moi » ?</p>
<p>Alors que cette catastrophe a été la raison de ton sauvetage…</p>
<p>Pouvez-vous croire que ¾ des « mauvais » événements dans nos vies, ramènent derrière eux un grand Bien,</p>
<p>Les jours passent, et tu te rends compte que grâce à ce problème que tu as vécu, Dieu t’a facilité une autre partie de ta vie.</p>
<p>Je connais personnellement beaucoup de gens qui appliquent cette idée,</p>
<p>Ils font quoi ?</p>
<p>Prends une feuille et un stylo,</p>
<p>Puis note toutes les catastrophes qui te sont arrivées dans ta vie,</p>
<p>Et regarde quel est le résultat final de chaque catastrophe…</p>
<p>As-tu déjà testé une idée pareille ?</p>
<p>Comme ça avant de dire « Pourquoi ça tombe sur moi ?? »</p>
<p>Avant de t’énerver,</p>
<p>Et avant de faire une dépression…</p>
<p>Souviens-toi de cette parole de Dieu :</p>
<p><strong>{Or, il se peut que vous ayez de l’aversion pour une chose alors qu’elle vous est un bien.}</strong></p>
<p>Coran : Sourate 02 - Al-Baqara : La Vache (verset 216)</p>
<p>(Aversion = haine, hostilité…)</p>
<p>Donc je vous propose une idée qui vous ramènera un sourire d’espoir,</p>
<p>Prends une feuille et un stylo,</p>
<p>Tente l’expérience avec moi…</p>
<p>Puis note toutes les catastrophes que tu as vécues dans ta vie,</p>
<p>Et regarde où t’a mené chaque catastrophe.</p>
<p>Je suis certain que la plupart d’elles se terminent en Bien,</p>
<p>Et que la plupart d’elles se terminent en bonheur, que toi-même tu as vécu.</p>
<p>Et les autres où tu ne vois pas une bonne fin,</p>
<p>C’est juste que tu ne le sais pas encore,</p>
<p>Peut-être que ça t’apparaitra dans 20 ans,</p>
<p>Et peut-être que ça apparaitra pour tes enfants ou tes petits enfants…</p>
<p>J’ignore quand,</p>
<p>Mais ce dont je suis sûr c’est que réagir face aux joies et face aux catastrophes,</p>
<p>Ça nécessite une satisfaction du destin de Dieu,</p>
<p>Et à ce moment tu verras un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
