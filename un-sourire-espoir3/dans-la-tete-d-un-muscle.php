                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit que les pensées à l'intérieur d'une personne, ont une grande influence sur son avenir,</p>
<p>Parfois à l'intérieur de toi tu as de belles pensées, et parfois de mauvaises pensées...</p>
<p>L'histoire d'aujourd'hui tourne autour de ce sujet :</p>
<p>Est-ce que tu te vois beau, ou bien tu es modeste ?</p>
<p>Est-ce que tu te vois comme quelqu'un de grand et fort, ou bien tu es humble et simple?</p>
<p>C'est l'histoire d'un ami à moi qui s'appelle Amr Hassib,</p>
<p>Aujourd'hui il travaille dans une grande entreprise,</p>
<p>Et il m'a raconté ceci :</p>
<p>Quand j'étais jeune et que j'avais entre 17 et 20 ans,</p>
<p>Ma force physique était exceptionnelle,</p>
<p>J'étais en salle de gym toute la journée,</p>
<p>J'avais des muscles partout,</p>
<p>Et je me trouvais beau...</p>
<p>Je courrais 10 km,</p>
<p>Je faisais de la natation,</p>
<p>J'étais ceinture noire en kung-fu,</p>
<p>Je sentais que personne ne pouvait me battre,</p>
<p>Je sentais que j'étais super fort physiquement,</p>
<p>Et je le montrais aux gens.</p>
<p>Je me sentais supérieur aux autres, et c'est devenu une partie de mon caractère...</p>
<p>Pourtant à la base je suis quelqu'un de gentil,</p>
<p>Mais à cette époque de ma vie, je me voyais meilleur que les autres.</p>
<p>À l'intérieur de moi il y avait ce sentiment de supériorité, et ca se voyait dans mes relations.</p>
<p>Puis je ne buvais pas d'alcool et je ne fumais pas,</p>
<p>Alors j'avais une grande endurance, je ne me fatiguais jamais.</p>
<p>« Je suis très fort »</p>
<p>Il y avait cette pensée à l'intérieur de moi, ce sentiment de supériorité et d'arrogance,</p>
<p>J'étais comme ca pendant plusieurs années,</p>
<p>Jusqu'au jour où mon papa m'a dit « C'est quoi cette chose que tu as sur ton cou ? »</p>
<p>Je lui ai répondu en plaisantant « C'est sûrement un nouveau muscle »</p>
<p>Il m'a dit « Non sérieusement, tu dois aller voir le médecin pour l'examiner »</p>
<p>En réalité c'était un cancer grave,</p>
<p>Quelle catastrophe !</p>
<p>Un cancer des glandes qui nécessite un traitement immédiat,</p>
<p>Et un traitement chimique qui peut provoquer des effets secondaires...</p>
<p>Il me raconte :</p>
<p>Je me suis transformé en une feuille,</p>
<p>Si tu me soufflais dessus je tombais...</p>
<p>J'ai perdu toute ma force physique,</p>
<p>Je sentais que c'était à cause de mon manque de modestie,</p>
<p>Et que c'était un message de Dieu.</p>
<p>Ca ne se fait pas de se sentir supérieur aux autres...</p>
<p>Pendant 6 mois j'ai suivi un traitement chimique,</p>
<p>J'ai perdu mes cheveux,</p>
<p>Et je ne pouvais pas me déplacer tout seul, on devait me porter,</p>
<p>Mais Dieu (Exalté Soit-Il) a voulu que je guérisse entièrement,</p>
<p>À ce moment j'ai eu une nouvelle intention...</p>
<p>J'ai décidé « Ô Seigneur, je vais m'abaisser... Je vais devenir modeste,</p>
<p>Ca y est j'ai retenu la leçon »</p>
<p>Et dès que je me suis senti bien, je suis parti faire un petit pèlerinage (Omra),</p>
<p>À cette époque il y avait des travaux de rénovation de la ka'aba et de la mosquée sacrée à la Mecque...</p>
<p>J'ai dit « Ô Seigneur, avant il y avait à l'intérieur de moi une mauvaise pensée, un sentiment de supériorité...</p>
<p>Aujourd'hui j'ai une belle pensée, je veux m'abaisser,</p>
<p>Ô Seigneur, j'ai envie de travailler dans la rénovation de Ta maison,</p>
<p>(Son travail est architecte),</p>
<p>J'ai envie d'être un ouvrier modeste entre Tes mains »</p>
<p>Amr me jure : Pendant que je faisais le pèlerinage,</p>
<p>Mon papa m'a téléphoné en disant</p>
<p>« J'ai un ami qui travaille dans l'entreprise responsable de la rénovation de la mosquée sacrée,</p>
<p>Je lui ai dit que mon fils voulait y participer,</p>
<p>Il m'a demandé où est ton fils ?</p>
<p>Je lui ai dit il est sur place en train de faire un petit pèlerinage,</p>
<p>Il m'a demandé que tu ailles le voir demain »</p>
<p>Amr me jure que 10 jours après son arrivée, il a commencé à travailler dans la rénovation de la mosquée sacrée,</p>
<p>Il me dit « J'ai compris que Dieu soutient les belles pensées... Et que les mauvaises pensées entrainent de mauvaises choses,</p>
<p>Depuis j'ai appris la modestie et la simplicité »</p>
<p>À tous les jeunes garçons ou filles : Si Dieu t'a donné de la force ou de la beauté, ou de l'argent, ou une bonne situation,</p>
<p>Sois modeste... Sois modeste car si tu te vois supérieur(e) aux autres, tu ne sais pas ce qui peut t'arriver.</p>
<p>Mais si tu es modeste, Dieu réalisera tes souhaits comme Amr qui au bout de 10 jours s'est trouvé en train de rénover la Ka'aba.</p>
<p>La modestie est belle, et les belles pensées sont récompensées.</p>
<p>C'est le sourire d'espoir d'aujourd'hui.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
