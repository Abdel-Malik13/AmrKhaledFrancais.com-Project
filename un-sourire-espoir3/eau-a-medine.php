                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'un des compagnons du Prophète (bénédictions et paix soient sur lui),</p>
<p>C'est Othmane Ibn Affane.</p>
<p>Quand la mosquée du Prophète (bpsl) a été construite,</p>
<p>Et que les musulmans ont émigré de la Mecque vers Médine,</p>
<p>Le nombre d'habitants augmentait dans la ville...</p>
<p>Il y avait un besoin de ressources naturelles,</p>
<p>On a besoin de manger, boire etc.</p>
<p>Les musulmans avaient donc besoin d'eau.</p>
<p>Et il existait à Médine un puits appelé « Rouma ».</p>
<p>C'était le plus grands puits de la ville,</p>
<p>Et du coup la ressource principale d'eau.</p>
<p>Mais ce puits appartenait à quelqu'un,</p>
<p>Il n'appartenait pas aux musulmans...</p>
<p>Le propriétaire de ce puits vendait l'eau,</p>
<p>Il vendait même une seule goutte...</p>
<p>C'est-à-dire tout avait un prix.</p>
<p>Le nombre de musulmans a augmenté,</p>
<p>Et beaucoup ont émigré de la Mecque vers Médine,</p>
<p>Ils ne savaient pas quoi faire,</p>
<p>Ils avaient besoin d'eau.</p>
<p>Alors Othmane Ibn Affane est parti voir le propriétaire du puits,</p>
<p>Il lui a dit « Je veux t'acheter le puits ».</p>
<p>L'homme a refusé de vendre.</p>
<p>Othmane a augmenté le prix,</p>
<p>L'homme a refusé.</p>
<p>Othmane lui a alors proposé « Je t'achète la moitié du puits.</p>
<p>C'est-à-dire on se partage le puits 1 jour sur 2 »</p>
<p>Le propriétaire a accepté cette proposition.</p>
<p>Bien sûr Othmane était un commerçant très expérimenté.</p>
<p>Qu'est ce qu'il a fait ?</p>
<p>Il a dit aux musulmans de venir prendre l'eau comme ils le souhaitent, les jours où c'est son tour.</p>
<p>Puis il a retiré le prix.</p>
<p>Pendant ses jours, l'eau du puits était gratuite.</p>
<p>L'ancien propriétaire vendait l'eau à un prix très élevé,</p>
<p>Et Othmane donnait l'eau gratuitement...</p>
<p>Les gens venaient donc se servir pendant les jours d'Othmane,</p>
<p>Et même les non-musulmans venaient,</p>
<p>Car Othmane a ouvert son puits à tout le monde,</p>
<p>Sans racisme ni discrimination.</p>
<p>« J'offre ma moitié gratuitement ».</p>
<p>L'ancien propriétaire ne vendait plus rien...</p>
<p>Alors il a proposé à Othmane d'acheter tout le puits.</p>
<p>Ohtmane a accepté comme n'importe quel arabe authentique.</p>
<p>Il ne lui a pas dit « Non, c'est comme ça et pas autrement ».</p>
<p>Non... Non... Non...</p>
<p>Il y a une indulgence.</p>
<p>Othmane a acheté la 2ème moitié du puits,</p>
<p>Et il l'a offert aux musulmans.</p>
<p>C'est magnifique.</p>
<p>Et il l'a payé combien ?</p>
<p>Il l'a payé 20.000 dirhams</p>
<p>C'est vrai que c'est un homme riche, mais je jure que personne ne fait ça sauf s'il a une grandeur d'âme.</p>
<p>Puis il a arrêté le puits pour Dieu (Exalté Soit-Il).</p>
<p>Ça veut dire quoi « Arrêté » ?</p>
<p>Ça veut dire « Ce puits est pour les musulmans,</p>
<p>Il ne m'appartient pas,</p>
<p>Et ni mes enfants, ni mes petits enfants ne vont l'hériter »</p>
<p>Puis au bout d'un moment, quelqu'un est venu voir Othmane,</p>
<p>Il lui a dit « J'aimerais t'acheter le puits au double de son prix »</p>
<p>Othmane lui a répondu « Non, propose-moi plus »</p>
<p>(C'est son côté commerçant)</p>
<p>L'homme lui a proposé le triple du prix...</p>
<p>Othmane lui a répondu « Non, bien sûr que ce n'est pas suffisant. Propose-moi plus »</p>
<p>Le compagnon augmentait sa proposition, jusqu'à ce qu'il arrive à 9 fois le prix initial.</p>
<p>Othmane a refusé à la fin de vendre le puits.</p>
<p>Le compagnon lui a dit « Je t'ai proposé 9 fois le prix... As-tu un meilleur acheteur que moi ? Qui te proposera plus que moi ? »</p>
<p>Othmane lui a répondu « Oui, Il m'a multiplié la bonne action par 10 (ou plus) »</p>
<p>Dieu m'a proposé plus...</p>
<p>Tu m'as proposé de multiplier le prix par 9</p>
<p>Dieu me multiplie chaque bonne action par 10 (ou plus).</p>
<p>Othmane n'a pas vendu le puits,</p>
<p>Othmane est décédé,</p>
<p>Et son puits est resté comme « Arrêt »,</p>
<p>Et autour du puits, beaucoup de palmiers ont poussé,</p>
<p>Et tout ceci dans la surface qu'il a arrêtée pour Dieu.</p>
<p>Avec le temps le nombre de palmiers est arrivé à 1500,</p>
<p>Et le gouvernement saoudien est arrivé,</p>
<p>Il a pris soin de cet endroit,</p>
<p>Et c'est toujours un « Arrêt ».</p>
<p>Imaginez mes chers... Depuis plus de 1400 ans, ce puits rapporte à Othmane des bonnes actions.</p>
<p>À quel point le commerce est beau avec Dieu ?</p>
<p>C'est pour ça que le Prophète (bpsl) a dit <strong class="prophete">« Chaque Prophète a un compagnon au Paradis, et mon compagnon au Paradis sera Othmane Ibn Affane »</strong>.</p>
<p>Imaginez à quel point Othmane sera récompensé.</p>
<p><strong>{ Quiconque prête à Allah de bonne grâce, Il le lui rendra multiplié plusieurs fois. }</strong></p>
<p>Sourate 02 - Al-Baqara : La Vache (verset 245)</p>
<p>Dépensez mes chers,</p>
<p>Dépensez et Dieu bénira vos actions,</p>
<p>Il fera perdurer le bien même après votre mort,</p>
<p>Est-ce qu'Othmane pouvait imaginer qu'on parlerait de son puits, de sa terre, et de ses palmiers jusqu'au jour d'aujourd'hui ?</p>
<p>Un sourire d'espoir pour chaque être humain qui fait le bien dans ce bas-monde.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
