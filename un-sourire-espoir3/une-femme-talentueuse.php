                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit : le talent compense la pauvreté.</p>
<p>Et le talent ramène une grande réussite dans la vie.</p>
<p>Si tu as un talent :</p>
<p>Si tu aimes la photographie,</p>
<p>Si tu aimes dessiner,</p>
<p>Si tu aimes écrire,</p>
<p>Si tu as un don pour parler aux gens,</p>
<p>Et même si tu sais sourire... C'est un moyen de gagner les cœurs.</p>
<p>Si tu possèdes un talent, cherche à le développer,</p>
<p>Cherche comment en tirer profit,</p>
<p>Cherche comment le faire grandir,</p>
<p>Et ton talent deviendra la source de ta subsistance.</p>
<p>Et ton talent deviendra le secret de ton bonheur.</p>
<p>L'histoire que je vais raconter aujourd'hui vient d'Angleterre :</p>
<p>C'était une femme très très très pauvre...</p>
<p>Quand elle était petite, elle avait un talent,</p>
<p>Elle aimait écrire,</p>
<p>Mais en grandissant elle est entrée dans le tourbillon de la vie,</p>
<p>Puis elle a délaissé son talent...</p>
<p>Elle s'est mariée,</p>
<p>Elle est devenue maman,</p>
<p>Et dès qu'elle a eu son bébé,</p>
<p>Son mari l'a laissée puis il est parti...</p>
<p>Alors une nouvelle pauvreté s'est ajoutée à sa pauvreté.</p>
<p>(Mais madame, tu avais un talent, tu aurais du t'y accrocher dès le départ)</p>
<p>À tous les gens qui ont un talent, retenez la leçon de cette histoire...</p>
<p>Cette femme s'est retrouvée sans travail,</p>
<p>Avec un bébé,</p>
<p>Et sans argent...</p>
<p>Elle était extrêmement pauvre,</p>
<p>Elle vivait très difficilement,</p>
<p>Et son mari l'a abandonnée, et il est parti.</p>
<p>Cette femme a décidé « Je n'ai pas le choix, je dois trouver un travail »</p>
<p>(Mais madame, tu as un talent...)</p>
<p>Elle a passé beaucoup d'entretiens d'embauche,</p>
<p>Elle n'a reçu que des réponses négatives...</p>
<p>Elle a fini par trouver un travail,</p>
<p>Mais elle n'était pas bonne dans ce qu'elle faisait.</p>
<p>Car Dieu lui a donné un talent dans un autre domaine...</p>
<p>Elle a continué à vivre de cette façon pendant 3 ans,</p>
<p>Elle passait d'un échec à un autre,</p>
<p>Elle a fini par dépenser tout l'argent qu'elle avait.</p>
<p>Puis soudainement, elle a décidé de faire revivre son talent...</p>
<p>Ça veut dire quoi ?</p>
<p>« Je vais arrêter de faire des choses que je n'aime pas,</p>
<p>Je vais faire la chose que j'aime ».</p>
<p>Bien sûr elle n'avait pas de bureau,</p>
<p>Elle partait s'asseoir dans un café toute la journée,</p>
<p>Elle posait son bébé à côté d'elle,</p>
<p>Puis elle ramenait beaucoup de livres en rapport avec le sujet qu'elle voulait écrire,</p>
<p>Et elle prenait un stylo et se mettait à écrire...</p>
<p>Elle va écrire quoi ?</p>
<p>Elle va écrire une histoire.</p>
<p>Et pendant qu'elle était assise au café, elle commandait quoi ?</p>
<p>Elle commandait 1 seule tasse de café.</p>
<p>Juste une... Et elle buvait sa tasse de café toute la journée,</p>
<p>Elle n'avait pas assez d'argent pour commander une 2ème tasse de café... Vous imaginez ?</p>
<p>D'ailleurs c'est elle qui raconte tout ça,</p>
<p>Elle écrivait... Écrivait... Écrivait...</p>
<p>1 mois est passé... Puis 2... Puis 3...</p>
<p>Les gens du café lui offraient un petit gâteau,</p>
<p>Car elle venait chaque jour,</p>
<p>Et elle s'asseyait sur la même table,</p>
<p>Presque gratuitement,</p>
<p>En payant le prix d'un seul café.</p>
<p>Elle posait ses livres sur la table... Elle lisait... Lisait... lisait...</p>
<p>Et écrivait... Écrivait... Écrivait son histoire.</p>
<p>Au bout d'un moment elle a terminé l'écriture de son histoire,</p>
<p>Alors elle est partie la proposer à une 1ère maison d'édition,</p>
<p>Ils l'ont refusée.</p>
<p>Elle est partie la proposer à une 2ème maison d'édition,</p>
<p>Ils l'ont refusée.</p>
<p>Elle est partie la proposer à une 3ème maison d'édition,</p>
<p>Ils l'ont acceptée.</p>
<p>Son histoire deviendra un best Seller,</p>
<p>C'est l'histoire d'Harry Potter.</p>
<p>Les aventures d'Harry Potter sont très populaires chez les jeunes de 15 ans en Europe,</p>
<p>Puis elles ont été traduites en toutes les langues,</p>
<p>Elles sont même étudiées dans des écoles,</p>
<p>Ensuite elles ont été adaptées au cinéma et en dessin animé...</p>
<p>Cette femme est classée parmi les 10 plus grandes fortunes féminines au monde,</p>
<p>Car les producteurs d'Hollywood payent très cher les droits d'adaptation de ses livres au cinéma.</p>
<p>Cette femme qui s'appelle Joanne Rowling, est devenue très riche et très célèbre.</p>
<p>Un sourire d'espoir pour chaque personne qui cherche où se trouve son talent,</p>
<p>Qui le développe,</p>
<p>Qui travaille dessus,</p>
<p>Qui le fait grandir,</p>
<p>Et qui sait que c'est la source de sa subsistance, la source de sa richesse et la source de son bonheur.</p>
<p>Un sourire d'espoir pour les gens qui possèdent des talents.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
