                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est footballistique, égyptien et mondial.</p>
<p>Mohamed Salah,</p>
<p>Le joueur du club de Chelsea,</p>
<p>Et qui a été transféré du FC Bâle pour un montant de 16 millions d'euros,</p>
<p>C'est un des transferts les plus chers du monde arabe.</p>
<p>Mais je ne raconte pas l'histoire de Mohamed Salah...</p>
<p>Je raconte l'histoire de l'entraîneur de Chelsea : José Mourinho,</p>
<p>Et surtout pour quelle raison il a choisi Mohamed Salah.</p>
<p>Après la signature du contrat,</p>
<p>Une conférence a été organisée,</p>
<p>Et la presse a demandé à Mourinho « Sur quels critères techniques tu as choisi Mohamed Salah, et qu'est ce qui justifie le prix de 16 millions d'euros ? »</p>
<p>Mourinho leur a donné une réponse qui nous concerne...</p>
<p>Remarquez Mourinho est un professionnel,</p>
<p>Et il n'est pas musulman,</p>
<p>Il ne parle pas d'un point de vue religieux, il parle d'un point de vue professionnel,</p>
<p>Il a répondu « En réalité la raison principale pour laquelle nous avons choisi Mohamed Salah, n'est pas ses qualités footballistiques,</p>
<p>Car on savait qu'il est très doué techniquement,</p>
<p>N'importe quelle personne qui le voit jouer à la télévision s'en rendra compte.</p>
<p>Mais nous l'avons choisi, et nous avons accepté de payer une telle somme pour un autre critère »</p>
<p>Les journalistes lui ont demandé « Quel critère ? »</p>
<p>Mourinho leur a répondu « Nous l'avons suivi en dehors du terrain,</p>
<p>Et on a vu qu'il a un très bon comportement, et que c'est quelqu'un de droit.</p>
<p>On l'a choisi pour cette raison »</p>
<p>...</p>
<p>Mes chers, quand est ce que nous allons comprendre la vérité ?</p>
<p>Quelle vérité ?</p>
<p>La vérité de : Qu'est ce qui rapporte le plus ?</p>
<p>Le talent, ou bien le bon comportement ?</p>
<p>On pense tous que pour réussir, il faut faire preuve de malice, et contourner le système...</p>
<p>Mourinho a dit « Nous l'avons observé en dehors du terrain »</p>
<p>Pas à l'intérieur...</p>
<p>Mais en dehors, pour voir si ça vaut le coup de le recruter, et de payer des millions d'euros, ou pas.</p>
<p>Qu'est ce que ça signifie ?</p>
<p>Ça signifie que la droiture et les bons comportements, c'est ce qui fait grandir les gens dans ce bas-monde.</p>
<p>Malheureusement on ne comprend pas ça...</p>
<p>Je jure qu'on a un problème avec ce point.</p>
<p>On pense tous que les bons comportements et la droiture, nous ramènent des bonnes actions, et ne concernent que l'au-delà, pour qu'on puisse entrer au Paradis.</p>
<p>Mais que pour réussir dans ce bas-monde, il faut être malin et rusé.</p>
<p>Non mes chers,</p>
<p>Croyez-moi, les bons comportements et la droiture c'est ce qui fait le plus réussir les gens dans le bas-monde.</p>
<p>C'est un des principes de la vie.</p>
<p>Mohamed Salah a été choisi car son comportement est exemplaire en dehors du terrain.</p>
<p>Mourinho a pensé « Cet homme est droit = Cet homme jouera pour nous »</p>
<p>Qui raisonne de cette façon ?</p>
<p>Vous voyez comment les gens réfléchissent ?</p>
<p>Est-ce que les sportifs et les personnes talentueuses se voient de cette manière : La droiture est plus importante que le talent ?</p>
<p>Après avoir signé son contrat avec Chelsea,</p>
<p>Mohamed Salah est rentré en Égypte,</p>
<p>Il s'est marié,</p>
<p>Puis il est parti avec son épouse faire une Omra (petit pèlerinage),</p>
<p>Et il lui a dit « Nous allons commencer une nouvelle vie,</p>
<p>Une vie remplie de droiture.</p>
<p>Je vais jouer dans ce club, et le titre principal c'est : La droiture et l'honnêteté »</p>
<p>Je dédicace cette histoire,</p>
<p>Et je dédicace la façon de penser de Mourihno,</p>
<p>À chaque personne qui pense que pour réussir dans la vie, il faut être plus malin que le système...</p>
<p>Et à chaque personne qui pense que seul son talent la fera réussir.</p>
<p>Un sourire d'espoir pour chaque être humain droit dans la vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
