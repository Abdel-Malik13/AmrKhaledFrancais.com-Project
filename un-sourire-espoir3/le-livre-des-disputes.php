                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui est une anecdote que j’ai vécue personnellement,</p>
<p>Mais avant de vous la raconter,</p>
<p>Il y a plusieurs siècles un écrivain est venu voir l’imam Ahmad Ibn Hanbal,</p>
<p>Cet écrivain a écrit un livre où il a mis toutes les oppositions entre les savants musulmans…</p>
<p>Cet homme a donc rassemblé dans ce livre tous les désaccords des savants,</p>
<p>Et il lui a donné comme titre : « Le livre des désaccords ».</p>
<p>Cet écrivain est parti voir l’imam Ahmad Ibn Hanbal pour qu’il lui donne son avis etc.</p>
<p>L’imam Ahmad lui a dit</p>
<p>« Que dis-tu de changer le titre du livre ? »</p>
<p>L’homme lui a répondu « Comment ça ? »</p>
<p>L’imam Ahmad lui a dit « Au lieu de l’appeler «Le livre des désaccords », on pourrait l’appeler « Le livre de l’ouverture d’esprit » »</p>
<p>Car à la fin les différents avis des savants, permettent aux gens d’avoir une plus grande liberté.</p>
<p>La divergence est une miséricorde,</p>
<p>Les différences sont utiles.</p>
<p>Par exemple, en cas de voyage, est ce qu’on raccourcit la durée de la Prière pendant 3 jours, 20 jours, ou pendant toute la durée de ton voyage ?</p>
<p>La divergence des savants sur cette question, est utile aux gens qui voyagent,</p>
<p>Du coup tu peux choisir la durée qui te facilite le plus ta vie.</p>
<p>L’imam Ahmad avait cette belle mentalité,</p>
<p>Il lui a dit au lieu d’appeler ton livre « Le livre des oppositions et des disputes… », On devrait l’appeler « Le livre de l’ouverture d’esprit et de la liberté… »</p>
<p>L’écrivain a accepté, et en effet il l’a appelé « Le livre de l’ouverture d’esprit ».</p>
<p>En réalité, j’ai toujours été très touché par cette histoire,</p>
<p>L’intelligence de l’imam Ahmad me plait beaucoup,</p>
<p>Comment il a gardé le livre tel qu’il est, mais dorénavant celui qui le lit le voit d’une façon entièrement différente.</p>
<p>Et il n’y a pas longtemps, un jeune écrivain plein d’énergie et de motivation est venu me voir,</p>
<p>Il m’a donné un livre, et m’a dit « Peux-tu m’écrire une préface ? »</p>
<p>Le livre s’appelle « Ceux qui font du mal au Prophète (bénédictions et paix soient sur lui) »</p>
<p>Et quand j’ai lu le livre,</p>
<p>Dedans il a rassemblé toutes les situations où quelqu’un a fait du mal au Prophète (bpsl)…</p>
<p>Je devais écrire une introduction,</p>
<p>Alors l’idée de l’imam Ahmad Ibn Hanbal m’est revenue à l’esprit…</p>
<p>Je lui ai dit « Je vais t’écrire une préface, mais je n’ai pas beaucoup de temps comme je dois bientôt prendre l’avion, je vais te la dire avec la voix, et tu devras la noter »</p>
<p>Il m’a dit « Oui je suis d’accord »,</p>
<p>Il a commencé à noter :</p>
<p>« Ce livre rassemble toutes les situations où quelqu’un a fait du mal au Prophète (bpsl),</p>
<p>Et c’est un effort formidable de l’auteur, »</p>
<p>Puis je lui ai dit :</p>
<p>« Reviens à la ligne :</p>
<p>Et ce livre peut être vu de 2 angles…</p>
<p>Soit comme un livre qui pousse à avoir la haine, et nous met en colère contre ceux qui ont fait du mal à notre Prophète (bpsl).</p>
<p>Mais on peut voir ce livre d’un autre angle…</p>
<p>Apprendre comment le Prophète (bpsl) a réagi dans ces situations avec pardon et miséricorde.</p>
<p>Et comment il a fini par gagner les cœurs de ses ennemis, malgré tout le mal qu’ils lui ont fait. »</p>
<p>Puis je lui ai dit :</p>
<p>« Note à la fin :</p>
<p>Et après chaque situation où quelqu’un a fait du mal au Prophète (bpsl),</p>
<p>Je souhaite que l’auteur écrive une situation où le Prophète (bpsl) a pardonné.</p>
<p>Pour que les lecteurs voient comment le Prophète (bpsl) a réagi dans ces situations ».</p>
<p>Ce jeune écrivain a gardé le silence… Puis il a dit</p>
<p>« C’est vrai que c’est une très belle idée…</p>
<p>Et si je changeais le titre du livre,</p>
<p>Au lieu de « Des exemples de ceux qui ont fait du mal au Prophète (bpsl),</p>
<p>Le nommer « Des exemples du pardon du Prophète (bpsl) avec ceux qui lui ont fait mal »</p>
<p>Je lui ai répondu</p>
<p>« C’est exactement ce que je voulais te dire dès le départ…</p>
<p>Nous ne voulons pas faire grandir chez les gens, le sentiment de haine, et le désir de vengeance…</p>
<p>Nous voulons faire grandir chez les gens, la miséricorde et le pardon.</p>
<p>Puis pour quelle raison tout ceci est arrivé au Prophète (bpsl) ?</p>
<p>C’est pour qu’on apprenne à bien réagir dans ces situations.</p>
<p>Nous aimons imiter le Prophète (bpsl) et sa Sunna…</p>
<p>Alors imitons-le dans son indulgence. »</p>
<p>C’est une façon de voir l’islam,</p>
<p>C’est une manière de voir la religion,</p>
<p>Une manière basée sur la tolérance, au lieu de chercher la haine…</p>
<p>Une manière basée sur le pardon, au lieu de vouloir la vengeance…</p>
<p>Je remercie cet écrivain formidable, qui a été réactif,</p>
<p>Et qui a décidé d’écrire après chaque histoire où quelqu’un a fait du mal au Prophète (bpsl), une autre histoire où il a pardonné à quelqu’un.</p>
<p>Imitons le Prophète (bpsl) et pardonnons aux autres comme il l’a fait.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
