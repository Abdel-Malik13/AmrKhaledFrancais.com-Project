                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit : L'école de la vie est plus forte que 1000 écoles académiques...</p>
<p>C'est l'école de la vie.</p>
<p>Mes chers, inscrivez vos enfants à l'école de la vie.</p>
<p>Ça veut dire quoi ?</p>
<p>« Mon enfant doit aller à l'école puis c'est tout »</p>
<p>Aujourd'hui c'est une méthode dépassée...</p>
<p>Ton enfant doit descendre travailler pendant les vacances d'été,</p>
<p>Il doit côtoyer les gens, et découvrir comment les autres vivent,</p>
<p>Il doit faire des petits boulots, transpirer et apprendre.</p>
<p>Inscris tes enfants à l'école de la vie.</p>
<p>C'est l'histoire d'un film indien,</p>
<p>Mais ce n'est pas un film indien comme les autres, qui raconte des légendes etc.</p>
<p>Ce film a remporté 8 Oscars,</p>
<p>Et il a fait d'énormes bénéfices.</p>
<p>C'est-à-dire il a coûté 15 millions,</p>
<p>Et il a rapporté 377 millions.</p>
<p>Vous imaginez à quel point c'est un grand succès ?</p>
<p>C'est quoi le nom de ce film ?</p>
<p>C'est « Slumdog Millionaire ».</p>
<p>Il raconte l'histoire d'un jeune des bidonvilles, qui est devenu millionnaire,</p>
<p>C'est un jeune très pauvre,</p>
<p>Il a passé toute sa vie dans des bidonvilles,</p>
<p>Il a travaillé dans les rues,</p>
<p>Il a beaucoup souffert.</p>
<p>Et bien sûr il n'a pas fait d'études,</p>
<p>Il sait à peine lire et écrire.</p>
<p>Puis un jour, dans l'émission « Qui veut gagner des millions » en Inde,</p>
<p>Ils ont voulu faire venir des personnes ignorantes pour se moquer d'elles.</p>
<p>Un participant à qui on poserait des questions très faciles,</p>
<p>Mais qui ne saura pas y répondre,</p>
<p>Et ça fera rire les téléspectateurs...</p>
<p>Et parmi les gens qu'ils ont sélectionnés, il y avait ce jeune.</p>
<p>Donc ce jeune a participé à l'émission,</p>
<p>Et la grande surprise c'est qu'il a répondu à toutes les questions, et qu'il a remporté le million.</p>
<p>La police indienne l'a soupçonné d'avoir triché...</p>
<p>Comment un jeune des bidonvilles, qui est presque analphabète, peut répondre à toutes ces questions ?</p>
<p>Ce jeune a été emmené au poste de police,</p>
<p>Et l'inspecteur a commencé à enquêter.</p>
<p>Tout le film tourne autour de ça,</p>
<p>L'inspecteur lui repose chacune des questions auxquelles il a répondu dans l'émission « Qui veut gagner des millions »,</p>
<p>Et ce jeune doit justifier ses réponses...</p>
<p>Alors on revoit les souvenirs du passé,</p>
<p>Comment chaque question de l'émission correspond à une situation difficile qu'il a vécue dans le passé.</p>
<p>Ce ne sont que des coïncidences.</p>
<p>Par exemple une des questions qu'on lui a posé pendant l'émission c'est :</p>
<p>Sur le Livre Sterling, il y a la photo de qui ?</p>
<p>Il devait choisir entre 4 réponses,</p>
<p>Et sans réfléchir, il a répondu « C'est la photo de la reine Elisabeth ».</p>
<p>L'inspecteur lui demande « Comment tu as su la réponse ? »</p>
<p>Le jeune lui répond « Quand je cirais les chaussures des touristes anglais,</p>
<p>Ils me donnaient des billets,</p>
<p>Je leur demandais : C'est qui sur la photo ?</p>
<p>Ils me répondaient : C'est la photo de la reine Elisabeth. »</p>
<p>L'inspecteur lui a sorti une monnaie indienne (Avec le visage de Gandhi),</p>
<p>Et lui a demandé « C'est la photo de qui dessus ? »</p>
<p>Le jeune lui a répondu « Je ne sais pas »</p>
<p>L'inspecteur lui a dit « Donc tu sais qui est sur la monnaie britannique, et tu ne sais pas qui est sur la monnaie indienne ? Tu te moques de moi ?? »</p>
<p>Le jeune lui a répondu « Je n'ai pas vécu ça, mais j'ai vécu ça »</p>
<p>L'inspecteur continuait à lui poser les questions,</p>
<p>Et le jeune justifiait chacune de ses réponses.</p>
<p>Le film s'est terminé avec le jeune qui a obtenu son million,</p>
<p>Il a su répondre à chacune des questions de l'émission,</p>
<p>Car chacune était liée avec une situation difficile qu'il a vécue.</p>
<p>Je vous invite à regarder ce film,</p>
<p>Pour comprendre que l'école de la vie est plus forte que des paroles académiques, si à la fin tu te retrouves sans expérience.</p>
<p>Et nous, combien avons-nous de génies dans les bidonvilles ?</p>
<p>Combien d'enfants pauvres possèdent des talents, mais n'ont pas l'occasion de les exprimer...</p>
<p>Et combien de personnes qui n'ont pas fait d'études, possèdent des compétences que même ceux qui ont fait de longues études ne possèdent pas...</p>
<p>Est-ce qu'on peut respecter les gens qui n'ont pas eu la chance de faire des études ?</p>
<p>Et est-ce qu'on peut faire descendre nos enfants pour qu'ils apprennent dans l'école de la vie ?</p>
<p>Un sourire d'espoir pour chaque être humain qui passe par l'école de la vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
