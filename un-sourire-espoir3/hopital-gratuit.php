                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui vient de l’Ouganda en Afrique,</p>
<p>Je suis parti visiter l’Ouganda, lors d’une tournée en Afrique,</p>
<p>Et c’est là-bas que j’ai connu cette histoire…</p>
<p>L’histoire en toute simplicité, est celle d’un jeune du moyen orient,</p>
<p>Son papa est décédé,</p>
<p>Son papa était un homme très très très riche,</p>
<p>Mais il était très charitable et généreux,</p>
<p>Il donnait de l’argent pour aider les associations caritatives,</p>
<p>Pour construire des hôpitaux, des centres d’éducation… Etc.</p>
<p>C’était un homme qui dépensait généreusement.</p>
<p>Et quand le papa est décédé,</p>
<p>Son fils a hérité de sa fortune,</p>
<p>C’était le seul fils, avec quelques sœurs.</p>
<p>Et quand le fils a touché la fortune, et qu’il est devenu riche,</p>
<p>La 1ère chose qu’il a faite,</p>
<p>Quand les associations caritatives sont venues le voir…</p>
<p>Il leur a dit « Vous vous croyez chez vous ??</p>
<p>Vous pensez que vous allez m’arnaquer comme vous arnaquiez mon père ?</p>
<p>Que vous allez prendre l’argent et le dépenser à gauche et à droite ?</p>
<p>Cette époque est révolue !</p>
<p>Ne revenez plus ici,</p>
<p>Je ne veux plus vous revoir ! »</p>
<p>Les gens des associations sont partis…</p>
<p>Et ça y est, il a arrêté tout ce que son papa faisait,</p>
<p>Il a continué à vivre normalement sa vie,</p>
<p>C’est un homme d’affaires, qui veut développer sa fortune,</p>
<p>Puis c’est tout.</p>
<p>Tout son objectif dans la vie c’est d’agrandir son chiffre d’affaires…</p>
<p>Les jours vont passer,</p>
<p>Puis un jour, une affaire s’est présentée en Ouganda,</p>
<p>Il est parti sur place,</p>
<p>Pour rencontrer les responsables, signer de gros contrats…</p>
<p>Et vendre ses produits là-bas.</p>
<p>Pendant que ce jeune du moyen orient était en Ouganda,</p>
<p>Il se baladait pour visiter le pays,</p>
<p>Et pendant qu’il marchait il s’est arrêté choqué…</p>
<p>Il a trouvé un hôpital qui soigne gratuitement les pauvres,</p>
<p>Chaque personne pauvre peut venir se soigner sans payer d’argent.</p>
<p>Jusqu’ici ça peut sembler normal,</p>
<p>Mais sur l’hôpital il y a marqué le nom de son papa décédé…</p>
<p>Imaginez quelqu’un à qui il arrive ça,</p>
<p>Après qu’il ait arrêté de faire le Bien…</p>
<p>Ce jeune a vu que l’hôpital portait le nom de son père.</p>
<p>Et sans dire qui il était,</p>
<p>Il est entré dans l’hôpital,</p>
<p>Il a commencé à regarder…</p>
<p>Puis il a demandé à rencontrer le directeur,</p>
<p>Il est entré voir le directeur en tant qu’un arabe du moyen orient qui pourrait les aider,</p>
<p>Puis le directeur a commencé à lui raconter…</p>
<p>Mais le jeune voulait poser une question précise : « Qui a construit cet hôpital ? »</p>
<p>Le directeur lui a dit le nom de son papa…</p>
<p>Il lui a dit « Cet homme a sauvé la vie de milliers d’Ougandais…</p>
<p>Cet homme a soigné beaucoup de gens,</p>
<p>Mais malheureusement il est décédé il y a 3 mois,</p>
<p>Et depuis, les conditions de l’hôpital n’arrêtent pas de se détériorer…</p>
<p>Avant on Priait pour que Dieu récompense cet homme,</p>
<p>Aujourd’hui on Prie pour que Dieu se venge de son fils »</p>
<p>Le jeune a pleuré…</p>
<p>Il lui a dit « Je suis le fils de cet homme,</p>
<p>Et dès aujourd’hui l’argent va vous revenir »</p>
<p>Puis il a dit aux gens qui sont dans l’hôpital</p>
<p>« J’ai appris une grande leçon,</p>
<p>Je remercie Dieu de m’avoir fait venir ici…</p>
<p>Comme si mon papa m’envoyait un signe : Ne détruis pas ce que j’ai construit.</p>
<p>L’argent va revenir comme avant. »</p>
<p>S’il te plait : Si ton papa fait le Bien,</p>
<p>Ne change surtout pas le chemin qu’il a commencé…</p>
<p>Si ton papa était bon avec les gens,</p>
<p>Sois comme lui…</p>
<p>Plus on fait le Bien, plus on aide les pauvres, et plus Dieu nous rend heureux, et plus Dieu protège nos enfants quand on n’est plus là,</p>
<p>La lignée de la famille continue avec un sourire d’espoir,</p>
<p>Génération après génération…</p>
<p>Car on continue sur le chemin du Bien.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
