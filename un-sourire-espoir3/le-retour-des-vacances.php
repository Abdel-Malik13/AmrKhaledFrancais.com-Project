                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui commence par une question :</p>
<p>As-tu tiré profit des vacances d’été de cette année ?</p>
<p>Si tu es un jeune à l’école ou à l’université,</p>
<p>Tu as eu 3 mois de vacances,</p>
<p>Sens-tu que tu en as tiré un bénéfice ?</p>
<p>Est-ce que tu sens que les vacances ont une valeur ?</p>
<p>Est-ce que tu sens que tu en sors en ayant quelque chose d’utile cette année ?</p>
<p>Ou bien les vacances se sont écoulées en long et en large, et à la fin il ne reste aucun acquis…</p>
<p>Bien sûr tu dois profiter de tes vacances, et tu dois passer du bon temps,</p>
<p>Mais il faut réunir 2 choses :</p>
<p>Un plaisir et une utilité.</p>
<p>L’histoire que je vais raconter aujourd’hui, est celle de deux jeunes anglais,</p>
<p>Ils s’appellent Paul Watson et Matthew Conrad.</p>
<p>Ils ont 20 ans, et il leur reste 1 année d’études à l’université.</p>
<p>Ces deux copains se sont assis après la fin de l’année scolaire,</p>
<p>Et ils ont commencé à planifier…</p>
<p>Ils veulent profiter de leurs vacances,</p>
<p>Mais en même temps ils veulent en tirer un bénéfice.</p>
<p>Ils se sont mis à réfléchir : Comment tirer un bénéfice des vacances de cette année ?</p>
<p>Ils ont commencé en se posant la question « Qu’est ce qu’on aime ? »</p>
<p>Ils aiment le football,</p>
<p>Et quand ils étaient petits, ils s’entrainaient dans le club d’Arsenal, puis en grandissant ils ont arrêté.</p>
<p>« C’est notre dernière année d’études, avant de commencer à travailler…</p>
<p>Comment obtenir le plus grand bénéfice ?</p>
<p>Et en même temps prendre du plaisir. »</p>
<p>Leur plaisir c’est de jouer au football,</p>
<p>Alors ils ont décidé de jouer dans un tournoi étranger, ou dans une équipe nationale pendant les 4 mois des vacances,</p>
<p>Bien sûr ils ne pouvaient pas jouer pour l’équipe nationale d’Angleterre,</p>
<p>Car ils n’avaient pas le niveau…</p>
<p>Alors ils ont cherché une équipe nationale qui joue des matchs en été,</p>
<p>Et qui participe à des matchs amicaux,</p>
<p>Mais il fallait une équipe faible, pour accepter de les sélectionner…</p>
<p>Ils ont pris le classement de la FIFA,</p>
<p>Et ils ont cherché les équipes les moins bien classées,</p>
<p>Et au rang 150, ils ont trouvé une petite île qui s’appelle Pohnpei,</p>
<p>Le nombre d’habitants de cette île est très petit,</p>
<p>Et leur équipe nationale n’a jamais gagné un seul match international…</p>
<p>Pas 1 seule fois ils n’ont gagné contre un autre pays.</p>
<p>Ces deux jeunes se sont dits, c’est comme ça qu’on va tirer un bénéfice de nos vacances,</p>
<p>Une nouvelle expérience,</p>
<p>On va visiter un nouveau pays,</p>
<p>On va rencontrer les responsables,</p>
<p>On va jouer au foot,</p>
<p>Ainsi on va à la fois prendre du plaisir, et acquérir une nouvelle expérience.</p>
<p>Remarquez l’esprit d’aventure à la fois, et le désir de tirer un bénéfice et de prendre du plaisir,</p>
<p>Quand tout ceci se rassemble dans un seul projet…</p>
<p>Ils ont contacté les responsables de l’île,</p>
<p>« Nous voulons jouer dans votre équipe nationale de football »</p>
<p>Les responsables leur ont répondu « Pour être sélectionnés, il faut que vous ayez la nationalité de l’île de Pohnpei »</p>
<p>Bien sûr, ils sont anglais, ils n’avaient pas la nationalité…</p>
<p>Ça ne peut pas marcher.</p>
<p>Mais les responsables leur ont dits « Par contre, nous jouons un tournoi amical avec les autres îles du Pacifique, il dure 4 mois »</p>
<p>Ca correspond à la durée de leurs vacances scolaires…</p>
<p>« Et nous ne trouvons personne pour entraîner notre équipe »</p>
<p>Car son niveau est très faible,</p>
<p>Et car aucun entraîneur ne veut partir dans une île lointaine…</p>
<p>Remarquez l’aventure, avec le bénéfice et le plaisir :</p>
<p>Les deux jeunes leur ont répondu tout de suite « Nous avons une petite expérience dans les entraînements, car quand on était petits, on jouait au club d’Arsenal.</p>
<p>Nous pouvons venir entraîner votre équipe nationale pendant 4 mois ».</p>
<p>Les responsables leur ont dits « Venez sans attendre ».</p>
<p>Ils ont pris l’avion,</p>
<p>Ils sont arrivés à l’île de Pohnpei en plein océan Pacifique,</p>
<p>Ils ont trouvé une équipe en perdition :)</p>
<p>Tous les joueurs étaient en surpoids,</p>
<p>Et l’état du terrain était très mauvais…</p>
<p>Ce sont les deux jeunes qui arrosaient le gazon,</p>
<p>Et ce sont eux qui faisaient en sorte que les joueurs soient en bonne forme physique,</p>
<p>Ils ont commencé à entrainer l’équipe,</p>
<p>Ils ont participé au tournoi amical,</p>
<p>Et ils ont gagné le 1er match de l’histoire de Pohnpei.</p>
<p>Jamais l’île n’avait remporté une victoire depuis son existence,</p>
<p>Ils ont gagné 1 match,</p>
<p>Et ils en ont perdu 2.</p>
<p>Ils sont sortis du tournoi sans le gagner,</p>
<p>Mais ils ont réalisé un exploit en gagnant le 1er match international de l’histoire de Pohnpei.</p>
<p>Ils sont revenus en Angleterre avec un certificat de reconnaissance de la part de l’île de Pohnpei, pour avoir entrainé leur équipe nationale.</p>
<p>Ils sont revenus avec quelque chose d’utile,</p>
<p>Ils sont revenus heureux,</p>
<p>Ils ont transformé leurs vacances en plaisir et en bénéfice.</p>
<p>C’est un sourire d’espoir pour chaque jeune qui fait la même chose.</p>
