                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui vient de la Mecque et de (la ville de) Djeddah en Arabie Saoudite,</p>
<p>Mais avant de la raconter,</p>
<p>J'ai envie de dire :</p>
<p>Dieu exauce les souhaits de Ses créatures,</p>
<p>C'est comme ça que je connais Dieu...</p>
<p>Dieu réalise les souhaits des gens, surtout si dedans se trouvent de belles intentions,</p>
<p>Si tu as une belle intention, Dieu te donnera ce que tu souhaites.</p>
<p>Combien de souhaits on a demandé à Dieu et Il nous les a réalisés ?</p>
<p>C'est beau de t'en rendre compte et de ne pas l'oublier,</p>
<p>Même si tu souhaites une chose pour la vie de ce bas monde,</p>
<p>Dieu te l'accorde.</p>
<p>Laissez-moi vous raconter l'histoire :</p>
<p>J'ai un ami que j'ai rencontré à la Mecque pendant que je faisais une Omra (petit pèlerinage)</p>
<p>C'est un homme saoudien,</p>
<p>On était assis après la fin du pèlerinage, et il m'a dit</p>
<p>« Je vais te raconter une chose incroyable »</p>
<p>Je lui ai dit « Oui dis-moi »</p>
<p>Il m'a dit « Il y a longtemps, j'ai souhaité avoir une maison à Djeddah en face de la mer,</p>
<p>C'était le rêve de ma vie,</p>
<p>Mais je n'avais pas l'argent pour acheter une maison en face de la mer.</p>
<p>Je rêvais d'être assis dans ma maison et de regarder l'océan...</p>
<p>Puis j'avais un 2ème souhait,</p>
<p>C'est d'habiter à côté d'une mosquée,</p>
<p>C'est-à-dire que la mosquée soit très proche...</p>
<p>Juste en bas de ma maison,</p>
<p>Pour pouvoir descendre chaque matin avec un ascenseur,</p>
<p>Faire la prière du Fajr (matin) en groupe,</p>
<p>Et remonter chez moi dormir un peu, avant de partir au travail... »</p>
<p>Il m'a dit « C'est le rêve de ma vie,</p>
<p>Je travaille,</p>
<p>Je cherche à satisfaire Dieu,</p>
<p>Je prends soin de ma femme et de mes enfants,</p>
<p>J'aime faire le bien.</p>
<p>Et cette maison c'est un souhait que j'ai »</p>
<p>Je lui ai demandé « Et la suite ? »</p>
<p>Il m'a répondu « Il n'y a pas de suite.</p>
<p>Tu fais quoi demain ? »</p>
<p>Je lui ai dit « Demain je pars à Djeddah pour prendre mon avion »</p>
<p>Il m'a dit « Je jure que tu vas venir déjeuner chez moi »</p>
<p>Je lui ai répondu « Je dois partir en voyage, et je n'ai pas beaucoup de temps »</p>
<p>Il m'a dit « Non c'est obligé que tu viennes déjeuner chez moi demain »</p>
<p>Le lendemain je suis parti chez lui,</p>
<p>Et c'était la grande surprise...</p>
<p>C'est exactement comme dans son rêve,</p>
<p>Je vous jure par Dieu, que j'ai vu la maison de mes propres yeux,</p>
<p>Je suis entré dans sa maison à Djeddah,</p>
<p>En face de la mer,</p>
<p>Et en bas il y a une mosquée.</p>
<p>Je vous jure que c'est exactement comme je le décris,</p>
<p>J'ai vu ça de mes propres yeux.</p>
<p>Je lui ai dit « C'est quoi ça ?</p>
<p>C'est le souhait dont tu m'as parlé hier »</p>
<p>Il m'a répondu « Maintenant tu as compris pourquoi j'ai insisté pour que tu viennes déjeuner chez moi ?</p>
<p>Je t'ai raconté ce que j'ai souhaité,</p>
<p>Et je voulais que tu voies que Dieu me l'a réalisé exactement comme je l'ai voulu...</p>
<p>Et je jure que chaque matin je descends avec l'ascenseur pour faire la prière à la mosquée,</p>
<p>Comme Dieu m'a réalisé mon souhait, il faut que je tienne ma parole.»</p>
<p>Je lui ai dit « Mais tu n'avais pas d'argent, comment tu as fait pour l'acheter ? »</p>
<p>Il m'a répondu « Demande-moi comment l'ancien propriétaire était pressé, et voulait se débarrasser de la maison au plus vite... Car il devait partir en voyage, il a vendu au premier venu.</p>
<p>Et demande-moi comment j'ai reçu l'héritage d'un oncle qui est décédé, et j'ai utilisé cet argent pour financer une partie de l'achat.</p>
<p>Et demande-moi comment j'ai réussi à prendre une avance sur mon salaire au travail.</p>
<p>Mais mon rêve s'est réalisé,</p>
<p>Et en achetant la maison, j'ai trouvé la mosquée en bas et l'océan en face »</p>
<p>C'est l'histoire de cet homme,</p>
<p>Mais dans la vie de chacun de nous, il y a des souhaits...</p>
<p>Dieu (Exalté Soit-Il) ne prive pas Ses créatures, surtout quand elles ont de bonnes intentions.</p>
<p>Fais donc des prières (Doua'a),</p>
<p>Demande à Dieu ce que tu souhaites,</p>
<p>Teste et tu verras comment Il te le réalisera.</p>
<p>Dieu exauce les prières, car Il aime Ses serviteurs,</p>
<p>Dieu dessine un sourire d'espoir sur leurs visages quand ils lui font des demandes.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
