                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est l'histoire d'une maman.</p>
<p>Nos chères mamans,</p>
<p>Prendre soin de nos mamans,</p>
<p>Aimer nos mamans,</p>
<p>Prendre de leurs nouvelles,</p>
<p>Les rendre heureuses...</p>
<p>Je jure que rien ne nous fera entrer au Paradis autant que notre maman.</p>
<p>Et notre vie sur terre ne sera facilitée que si notre maman est satisfaite de nous.</p>
<p>Je jure que notre vie ne devient facile que si notre maman est satisfaite...</p>
<p>Malheur... Malheur à celui contre qui sa maman est fâchée,</p>
<p>Ou celui qui brise le cœur de sa maman,</p>
<p>Il mènera une vie triste, une vie malheureuse, il verra des catastrophes arriver dans sa vie...</p>
<p>Mon cher, cours satisfaire ta maman.</p>
<p>Laissez-moi vous raconter l'histoire :</p>
<p>C'est une femme veuve,</p>
<p>Elle a un seul fils,</p>
<p>Après l'avoir fait grandir, éduqué, marié...</p>
<p>Il a décidé de la laisser et de partir vivre loin,</p>
<p>C'est son seul enfant, et son mari est décédé...</p>
<p>Elle lui a dit « Tu vas me laisser toute seule ? »</p>
<p>Il lui a répondu « Pardonne-moi maman,</p>
<p>Ne sois pas inquiète,</p>
<p>Ne te mets pas au milieu du chemin de mon bonheur et de mon avenir »</p>
<p>Bien sûr la phrase « Ne te mets pas au milieu du chemin de mon bonheur et de mon avenir »</p>
<p>A entièrement calmé la maman...</p>
<p>Son fils a voyagé,</p>
<p>Elle lui envoyait des lettres,</p>
<p>Au début il lui écrivait des réponses...</p>
<p>Et au début il lui répondait rapidement,</p>
<p>Puis il s'est occupé avec son épouse et ses enfants,</p>
<p>Il a arrêté de répondre à ses lettres...</p>
<p>Aïe... C'est quoi cette dureté ?</p>
<p>Déjà tu l'as laissée toute seule,</p>
<p>En plus tu n'as pas voulu l'emmener avec toi,</p>
<p>Puis tu n'as pas voulu rester dans la même ville qu'elle,</p>
<p>C'est peut-être les aléas de la vie, ça peut arriver...</p>
<p>Mais ne pas prendre de ses nouvelles ?</p>
<p>Ne jamais téléphoner ?</p>
<p>Je parle à tous ceux qui partent vivre à l'étranger et qui oublient leurs mamans,</p>
<p>Et à ceux qui deviennent trop occupés, une fois qu'ils sont mariés et qu'ils ont des enfants...</p>
<p>Il y en a qui même s'ils sont dans la même ville, ne vont plus voir leurs parents,</p>
<p>Je jure que c'est injuste de faire ça.</p>
<p>Cette maman a réfléchi à une façon astucieuse qui pousse son fils à lui parler...</p>
<p>Imagine ce qu'elle a fait ?</p>
<p>Elle lui a envoyé une lettre,</p>
<p>Elle savait que son fils aimait l'argent malheureusement :)</p>
<p>Elle lui a écrit :</p>
<p>« Mon fils chéri,</p>
<p>J'ai hérité de mon oncle d'une grande terre,</p>
<p>J'ai maintenant beaucoup d'argent,</p>
<p>Donc si tu as besoin de quoique ce soit, envoie-moi une lettre et je t'enverrai tout ce dont tu as besoin »</p>
<p>Le plan a fonctionné...</p>
<p>Son fils s'est remis à lui écrire,</p>
<p>Aïe... C'est très dur...</p>
<p>Il lui envoie une lettre, il lui dit des paroles tendres, et à la fin de sa lettre il demande de l'argent...</p>
<p>La maman n'était pas fâchée,</p>
<p>Car c'est le seul espoir qu'elle avait pour parler à son fils.</p>
<p>Mes chers, nos mamans sacrifient leurs vies pour nous,</p>
<p>Et à la fin, il ne leur reste aucun rêve dans leurs vies à part nous,</p>
<p>Cette maman n'était pas rancunière, elle continuait à lui envoyer de l'argent.</p>
<p>Son fils lui donnait quelques paroles d'amour, et à la fin il demandait telle ou telle somme d'argent...</p>
<p>L'échange de courrier et l'envoi d'argent a duré 6 mois,</p>
<p>Puis soudainement, les lettres de la maman se sont arrêtées,</p>
<p>Il lui a envoyé une lettre pour demander de l'argent... Elle n'a pas répondu,</p>
<p>Il a envoyé une 2ème lettre... Elle n'a pas répondu,</p>
<p>Il a téléphoné à la maison... Personne n'a décroché,</p>
<p>Alors il a téléphoné aux voisins,</p>
<p>Les voisins lui ont dit « Ta maman est décédée »</p>
<p>Il a décidé de venir très vite... Pour prendre ce qui reste de l'héritage,</p>
<p>Et en profiter pour présenter ses condoléances au reste de la famille.</p>
<p>Il est arrivé,</p>
<p>Et il a été surpris de voir quelqu'un dans la maison de sa maman,</p>
<p>La maison où il a grandi... Et dont ils étaient propriétaires.</p>
<p>Un homme qu'il ne connaissait pas y vivait,</p>
<p>Il lui a dit « Qui es-tu ? »</p>
<p>L'homme lui a répondu « Je suis le propriétaire,</p>
<p>Ta maman m'a vendu la maison il y a 6 mois »</p>
<p>Le fils est parti demander aux voisins « Mais où vivait ma maman ? »</p>
<p>Ils lui ont dit « Elle a loué un tout petit appartement, où elle a passé les derniers jours de sa vie.</p>
<p>Elle vendait parfois ses bijoux,</p>
<p>Et parfois elle vendait les meubles de la maison,</p>
<p>Mais on n'a jamais su ce qu'elle faisait de cet argent »</p>
<p>Le fils a pleuré...</p>
<p>Et il leur a dit « Mais moi je sais ce qu'elle en faisait...</p>
<p>Je le savais et je n'ai rien fait »</p>
<p>Il a pleuré... Et je pense qu'il va beaucoup pleurer.</p>
<p>Je pense qu'il va pleurer tout le reste de sa vie,</p>
<p>Sa maman est décédée alors qu'il était loin et qu'il ne prenait pas de ses nouvelles.</p>
<p>Comment un sourire d'espoir peut venir à quelqu'un qui a fait ça ?</p>
<p>S'il te plait prends soin de ton papa et de ta maman.</p>
