                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui vient d’Arabie Saoudite,</p>
<p>C’est un médecin saoudien,</p>
<p>Il s’appelle Saïd Mohamed Al-Qahtani.</p>
<p>C’est une histoire vraie,</p>
<p>Et si vous entrez sur ma page Facebook, vous trouverez la photo du Dr. Saïd Mohamed Al-Qahtani.</p>
<p>Dr. Saïd a reçu un coup de téléphone,</p>
<p>« S’il vous plait docteur, venez de toute urgence,</p>
<p>Un enfant vient d’arriver à l’hôpital dans un état critique,</p>
<p>Et il a besoin que vous lui fassiez une opération chirurgicale. »</p>
<p>Personne d’autre ne pouvait l’opérer,</p>
<p>Car Dr. Saïd était spécialisé dans un certain type d’opérations.</p>
<p>« S’il vous plait docteur, venez de toute urgence »</p>
<p>Dr. Saïd leur a répondu « Je suis désolé, je ne peux pas venir »</p>
<p>Ils lui ont dit « Docteur, l’enfant est dans un état critique,</p>
<p>S’il n’est pas opéré, il peut lui arriver quelque chose… »</p>
<p>Dr. Saïd a gardé un peu le silence, puis il leur a dit « D’accord, j’arrive ».</p>
<p>Mais malheureusement Dr. Saïd est arrivé en retard…</p>
<p>L’enfant n’est pas décédé,</p>
<p>Il va être opéré etc.</p>
<p>Mais il est juste arrivé avec du retard,</p>
<p>Alors le papa de l’enfant sentait « C’est quoi ce manque de responsabilité ?!</p>
<p>Est-ce possible ?</p>
<p>Est-ce possible d’être aussi irresponsable que ça ?? »</p>
<p>Le père de l’enfant était très en colère,</p>
<p>Il a reçu Dr. Saïd avec des paroles sèches…</p>
<p>Il a pu même lui dire des paroles blessantes.</p>
<p>Et à la fin il lui a dit « Si cet enfant était le votre,</p>
<p>Seriez-vous arrivé en retard comme ça ?? »</p>
<p>Cette phrase a fait très mal au Dr. Saïd</p>
<p>Il s’est excusé au père,</p>
<p>Il lui a dit « Je suis désolé »,</p>
<p>Sans donner d’explications.</p>
<p>Dr. Saïd lui a juste dit « Désolé », il est entré dans la salle d’opérations,</p>
<p>Et dans son oreille résonnait la phrase « Si c’était votre fils, seriez-vous arrivé en retard… »</p>
<p>Dr. Saïd a opéré l’enfant,</p>
<p>Tout s’est bien passé,</p>
<p>L’opération était réussie.</p>
<p>Dr. Saïd est sorti de la salle des opérations,</p>
<p>Et le père de l’enfant était soulagé,</p>
<p>Mais il ne savait pas comment réagir avec le docteur.</p>
<p>D’un côté il voulait le remercier, mais de l’autre côté il restait fautif d’être arrivé en retard…</p>
<p>Quand le père est parti voir le docteur,</p>
<p>Il a vu que les infirmières, les infirmiers et la direction de l’hôpital… Venaient tous lui présenter leurs condoléances,</p>
<p>« Nos plus sincères condoléances Dr. Saïd… »</p>
<p>« Toutes nos condoléances Dr. Saïd… »</p>
<p>Le père de l’enfant a demandé « Qu’est ce qui se passe ? »</p>
<p>On lui a répondu « Le fils du Dr. Saïd est décédé aujourd’hui,</p>
<p>Il était aux obsèques,</p>
<p>Il est arrivé en retard, car il venait à peine d’enterrer son fils,</p>
<p>Il n’est pas rentré chez lui,</p>
<p>Il est venu directement à l’hôpital pour opérer votre fils,</p>
<p>Car il considère votre fils comme le sien »</p>
<p>Aïe…</p>
<p>Parfois on juge trop vite,</p>
<p>Et on commet des injustices.</p>
<p>Et parfois on refuse de croire qu’il existe de belles choses dans le monde…</p>
<p>Est-ce que quelqu’un peut imaginer l’état d’un papa qui a perdu son enfant ?</p>
<p>Pourtant il est venu faire une opération à l’hôpital pour sauver un autre enfant…</p>
<p>Ce sont de très beaux principes.</p>
<p>On remercie Dr. Saïd Mohamed Al-Qahtani,</p>
<p>Et nous sommes très heureux quand on voit dans notre monde arabe et musulman, des gens de cette grandeur et de cette beauté.</p>
<p>Je jure que cette histoire dessine vraiment sur nos visages un sourire d’espoir.</p>
<p>J’ai posté la photo de Dr. Saïd sur ma page Facebook,</p>
<p>Pour que vous puissiez voir à quoi ressemble cet être humain, qui a fait ce très bel acte.</p>
<br>
<p>Je vous remercie,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
