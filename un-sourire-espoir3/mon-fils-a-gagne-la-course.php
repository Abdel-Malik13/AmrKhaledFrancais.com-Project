                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui est l’histoire d’un jeune âgé de 16 ans,</p>
<p>Il s’appelle Moustapha Khaled,</p>
<p>Il est né un 26 du Ramadan,</p>
<p>Et c’était un petit garçon très beau,</p>
<p>Il avait de l’humour,</p>
<p>Il était aimé par tout le monde…</p>
<p>Il faisait bien ses Prières (Salate).</p>
<p>Puis il aimait faire le Bien en cachette,</p>
<p>Il aimait aider les gens,</p>
<p>Il avait toujours le sourire.</p>
<p>La maman de Moustapha a commencé à faire le même rêve plusieurs fois…</p>
<p>Elle ne comprenait pas son sens,</p>
<p>Le rêve c’est que Moustapha participait à une course,</p>
<p>Et que les gens venaient la féliciter,</p>
<p>« Félicitations, Moustapha est arrivé premier ! Moustapha a gagné ! »</p>
<p>Elle se réveillait de son sommeil, en se demandant ce qu’avait gagné son fils, et quelle était cette course ?</p>
<p>Elle n’arrivait pas à comprendre…</p>
<p>Par la suite, Moustapha est tombé malade pendant 5 jours,</p>
<p>Puis il est décédé.</p>
<p>Il est décédé il y a 2 mois,</p>
<p>Que Dieu lui accorde Sa miséricorde.</p>
<p>Et en réalité, la maman de Moustapha a commencé à découvrir la beauté de son fils après son décès…</p>
<p>Elle n’a compris la signification de « Moustapha a gagné »,</p>
<p>Que quand les gens venaient lui présenter leurs condoléances,</p>
<p>Et qu’ils lui parlaient de ce jeune qui avait 16 ans…</p>
<p>La femme qui vendait des légumes dans le quartier, est venue en pleurant,</p>
<p>Elle lui a dit « Il venait toujours me voir en fin de journée,</p>
<p>Et il me demandait : ‘As-tu bien vendu aujourd’hui ?’</p>
<p>Je lui disais : ‘Aujourd’hui j’ai bien vendu Dieu merci’</p>
<p>Il me souriait et me disait : ‘Félicitations’ »</p>
<p>Puis des jours il venait me demander si j’avais bien vendu,</p>
<p>Je lui disais : ‘Non pas beaucoup mon fils’</p>
<p>Il me disait : ‘Nous avons besoin d’acheter beaucoup de légumes pour la maison’</p>
<p>Puis il m’achetait… »</p>
<p>La maman lui a dit « Oui c’est vrai, il y a des jours où il venait à la maison avec beaucoup de légumes, et je lui demandais pourquoi il en avait ramené autant »</p>
<p>La vendeuse lui a dit « Justement il faisait ça pour m’aider… »</p>
<p>Puis la grand-mère de Moustapha a raconté à sa maman :</p>
<p>« Il m’emmenait jusqu’au lit,</p>
<p>Il attendait jusqu’à ce que je m’endorme,</p>
<p>Et le matin en me réveillant, je trouvais l’eau et mes médicaments juste à côté,</p>
<p>Il restait pour s’assurer que je n’avais besoin de rien,</p>
<p>Et il me disait : ‘Ne le dis pas à maman, c’est un amour entre toi et moi grand-mère’ »</p>
<p>La maman lui a dit « J’ignorais qu’il faisait ça… »</p>
<p>Puis la professeur de Moustapha à l’école est aussi venue,</p>
<p>Elle a dit à sa maman « Parfois il venait me demander : ‘ Y a-t-il quelque chose que je peux faire pour t’aider ?’</p>
<p>Je lui disais : ‘J’ai besoin de telle chose’</p>
<p>Il allait l’acheter et il me la ramenait… »</p>
<p>Il avait 16 ans,</p>
<p>Que Dieu lui accorde Sa miséricorde,</p>
<p>C’est après son décès que sa maman a compris le sens « Il a gagné »</p>
<p>Le dernier post de Moustapha sur sa page Facebook, c’était cette phrase « Ô Seigneur, emporte-moi en étant satisfait de moi »</p>
<p>Est-ce qu’il le sentait ?</p>
<p>Est-ce qu’au fond de lui il avait le sentiment qu’il allait bientôt décéder ?</p>
<p>Je suis en contact avec la maman de Moustapha,</p>
<p>Et elle sait que je vais enregistrer cet épisode,</p>
<p>Elle est contente, et tout ce qu’elle me demande c’est une seule chose :</p>
<p>Que les gens qui écoutent cet épisode prient pour Moustapha,</p>
<p>Qu’ils demandent à Dieu de lui pardonner et de lui accorder Sa miséricorde.</p>
<p>Quelle chance il a Moustapha,</p>
<p>Peut-être qu’il a réellement gagné.</p>
<p>Je jure que c’est un sourire d’espoir pour chaque jeune qui fait le Bien autour de lui,</p>
<p>C’est un sourire d’espoir pour chaque maman qui voit des gens complimenter son fils et prier pour lui.</p>
<p>Et c’est un sourire d’espoir pour le Bien.</p>
<p>Que Dieu accorde à Moustapha Son pardon et Sa miséricorde, et qu’Il le réunisse au Paradis avec sa maman.</p>
<p>Que Dieu fasse de sa tombe un jardin des jardins du Paradis.</p>
<p>Paix à ton âme Moustapha.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
