                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est un message... Ce n'est pas une histoire,</p>
<p>C'est un message qu'on a reçu sur la boite email de l'émission,</p>
<p>On invite les gens qui ont de belles histoires à nous les envoyer sur basmetamal[at]amrkhaled[point]net</p>
<p>Et en réalité on a reçu un message d'une fille âgée de 15 ans,</p>
<p>Son papa est décédé...</p>
<p>Elle dit « J'aimerais envoyer un sourire d'espoir au milieu de ma tristesse, à tous les jeunes garçons et filles dont les parents sont encore en vie, prenez soin de vos parents. »</p>
<p>Le message qu'elle a envoyé est très beau,</p>
<p>Permettez-moi de le lire dans l'épisode d'aujourd'hui.</p>
<p>Elle dit :</p>
<p>Aujourd'hui un Grand homme a quitté notre monde...</p>
<p>Il n'était pas un grand patron,</p>
<p>Ni un médecin célèbre,</p>
<p>Ni un héros de guerre,</p>
<p>Ni un personnage sportif,</p>
<p>Ni un roi,</p>
<p>Jamais sa photo n'est apparue dans les journaux économiques ou dans les magazines...</p>
<p>Et pourtant à mes yeux, il était un des Grands de ce monde,</p>
<p>Aujourd'hui mon papa est décédé.</p>
<p>Il ne faisait rien d'extraordinaire...</p>
<p>Papa ne faisait que des choses ordinaires,</p>
<p>Papa payait les factures téléphoniques,</p>
<p>Papa m'aidait à faire mes devoirs pour l'école,</p>
<p>Papa allait faire les courses avec maman...</p>
<p>Mais je le vois comme quelqu'un de Grand.</p>
<p>Je le vois Grand car c'est mon Papa.</p>
<p>Aujourd'hui c'est la 1ère nuit que je passerai sans Papa,</p>
<p>Je ressens de la douleur, car certains jours il me demandait des choses, et je ne les faisais pas,</p>
<p>Aujourd'hui j'ai envie de pleurer du sang à la place des larmes, pour chaque fois qu'il m'a fait une demande et que je ne l'ai pas faite ou que je l'ai oubliée...</p>
<p>Mais en même temps je remercie Dieu,</p>
<p>Car Il a permis à Papa de vivre à mes côtés pendant 15 ans,</p>
<p>Dieu m'a laissé profiter de mon Papa pendant 15 ans.</p>
<p>(Et à la fin du message elle dit)</p>
<p>Je sais que j'envoie un message à l'émission « Un sourire d'espoir »</p>
<p>Donc il faut qu'il y ait un « sourire » dedans...</p>
<p>Et le « sourire » c'est qu'avant que Papa décède,</p>
<p>Il m'a regardé avec satisfaction,</p>
<p>Et je me souviendrai de ce regard pour toujours,
<p>Car avant qu'il décède, il m'a dit « Je suis satisfait de toi ma fille ».</p>
<p>Sa lettre est terminée,</p>
<p>Mais ce que je veux dire :</p>
<p>Mes chers, profitez de vos parents avant de les perdre,</p>
<p>Que Dieu préserve nos papas et nos mamans,</p>
<p>Prends soin de tes parents,</p>
<p>Cherche à les satisfaire,</p>
<p>Rends les heureux,</p>
<p>Avant de dire un jour « Papa n'est plus là »...</p>
<p>Que Dieu préserve nos papas et nos mamans.</p>
<p>Et celui qui a perdu ses parents : Cherche à les satisfaire dans leurs tombes,</p>
<p>Envoie-leur des bonnes actions (hassanates),</p>
<p>Prends des nouvelles de leurs amis,</p>
<p>Fais des aumônes,</p>
<p>Et fais de bonnes choses pour eux.</p>
<p>Profite de ton papa et de ta maman avant de les perdre,</p>
<p>Dessine un sourire d'espoir sur leurs visages,</p>
<p>Pour qu'au moment de leur décès, ils dessinent un sourire d'espoir sur ton visage pour le reste de ta vie,</p>
<p>Quand ils diront « Je suis satisfait de toi mon enfant »</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
