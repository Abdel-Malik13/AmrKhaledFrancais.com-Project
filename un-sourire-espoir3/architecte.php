                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle du respect de l'argent publique,</p>
<p>C'est l'histoire d'un grand architecte,</p>
<p>Un des symboles d'Égypte,</p>
<p>Il s'appelle Osman Ahmed Osman,</p>
<p>Il a fondé une des plus grandes entreprises de construction au moyen orient et en Afrique,</p>
<p>Mais je ne vais pas parler d'architecture ou de construction...</p>
<p>Non... Non... Non,</p>
<p>Je vais parler d'intégrité et de conservation de l'argent publique,</p>
<p>L'argent de nos pays.</p>
<p>C'est lui qui raconte son histoire,</p>
<p>Il dit dans ses mémoires :</p>
<p>À la fin des années 1940 et au début des années 1950,</p>
<p>Le gouvernement égyptien lui a confié la construction d'une école dans la ville d'Ismaïlia,</p>
<p>C'est la ville où il a grandi.</p>
<p>C'était l'époque de la colonisation anglaise,</p>
<p>Il a donc construit cette école comme il se doit,</p>
<p>Et il s'apprêtait à remettre les clés au gouvernement égyptien,</p>
<p>Puis un problème est apparu entre le gouvernement égyptien et les anglais,</p>
<p>La dispute a duré...</p>
<p>Et les anglais ont décidé de prendre cette école et d'autres bâtiments publics, et les transformer en logements pour leurs soldats etc.</p>
<p>L'architecte Osman Ahmed Osman s'est mis à réfléchir,</p>
<p>« Je vais leur donner l'école aussi facilement ?</p>
<p>Est-ce qu'on va abandonner l'école qui appartient à nos enfants ?</p>
<p>Non je dois les empêcher de prendre l'école, je dois trouver une solution »</p>
<p>Et pendant qu'il réfléchissait, un officier de l'armée anglaise est arrivé,</p>
<p>Il lui a dit « Je viens prendre les clés de l'école »</p>
<p>L'architecte Osman voulait gagner du temps... Il lui a dit</p>
<p>« Je dois prévenir les ouvriers qui sont encore sur place, on doit ranger nos outils etc. Alors s'il vous plait, laissez-moi 2h puis revenez pour que je vous donne les clés »</p>
<p>L'officier anglais est reparti et lui a dit qu'il allait revenir dans 2 heures.</p>
<p>Osman a rassemblé ses ouvriers,</p>
<p>Il a fait venir des camions,</p>
<p>Et leur a dit « Retirez toutes les portes et toutes les fenêtres de l'école »</p>
<p>Il voulait que le bâtiment ne soit pas prêt à être utilisé,</p>
<p>Il a retiré en vitesse toutes les portes et toutes les fenêtres...</p>
<p>Il a chargé ses camions...</p>
<p>Puis il a tout emmené à l'entrepôt de son entreprise,</p>
<p>L'officier anglais est venu prendre les clés,</p>
<p>Et il a été surpris de voir que le bâtiment n'était pas utilisable tout de suite, car il n'y a ni portes ni fenêtres...</p>
<p>L'école a été laissée à l'abandon pendant 1 an, le bâtiment n'a pas été utilisé.</p>
<p>Puis au bout d'un an, le gouvernement égyptien a repris l'école,</p>
<p>On a appelé l'architecte Osman,</p>
<p>Le délégué du ministère de l'éducation lui a dit</p>
<p>« Merci de finir la construction de l'école, nous allons scolariser les enfants à partir de l'année prochaine,</p>
<p>Combien d'argent ca va coûter ? »</p>
<p>Car ils ignoraient que c'était lui qui avait retiré les portes et les fenêtres...</p>
<p>« De combien tu as besoin ? Nous allons te donner un budget pour ajouter les portes et les fenêtres »</p>
<p>Osman Ahmed Osman a répondu « Ca va coûter Zéro »</p>
<p>Ils lui ont dit « Comment ca ? »</p>
<p>Il leur a répondu « 0 centimes,</p>
<p>Les portes et les fenêtres sont dans l'entrepôt de l'entreprise,</p>
<p>Je vais aller les chercher et les remettre en place »</p>
<p>Le délégué du ministère de l'éducation l'a regardé et lui a dit</p>
<p>« Mais tu pouvais le cacher, et gagner beaucoup d'argent »</p>
<p>L'architecte Osman lui a donné cette très belle réponse</p>
<p>« Et comment je pouvais le cacher, alors que Dieu qui voit tout,</p>
<p>M'a vu pendant que je les emmenais...</p>
<p>Et comment je pouvais le cacher, et marcher parmi les citoyens de mon pays,</p>
<p>Qu'est ce qu'ils auraient pensé de moi...</p>
<p>Et qu'est ce que j'aurais pensé de moi »</p>
<p>Si seulement on apprenait de lui l'intégrité, et l'amour de notre pays,</p>
<p>À ce moment nous verrons tous un sourire d'espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
