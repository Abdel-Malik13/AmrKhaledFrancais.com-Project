                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit : L'altruisme est un des secrets de la miséricorde que Dieu te donne.</p>
<p>Je le répète : L'altruisme est un des secrets pour obtenir la miséricorde de Dieu.</p>
<p>Sois altruiste et Dieu te donnera Sa miséricorde,</p>
<p>L'histoire d'aujourd'hui date d'il y a très longtemps...</p>
<p>Elle est arrivée en 1949,</p>
<p>C'est une histoire très ancienne, mais elle est très belle,</p>
<p>En réalité je ne la connaissais pas,</p>
<p>Celui qui me l'a racontée, c'est le fils de l'homme à qui cette histoire est arrivée.</p>
<p>Il m'a raconté :</p>
<p>Ma grand-mère était gravement malade...</p>
<p>Elle a été emmenée à l'hôpital dans un état critique,</p>
<p>Elle souffrait des reins,</p>
<p>Les médecins ont essayé de la sauver,</p>
<p>Mais elle avait besoin d'une transfusion sanguine,</p>
<p>Et à l'époque il n'existait pas encore des banques du sang...</p>
<p>Puis on a été surpris d'apprendre que le groupe sanguin de ma grand-mère est très rare,</p>
<p>Ab-</p>
<p>Ab négatif...</p>
<p>On n'avait pas de solution,</p>
<p>Et le temps passait,</p>
<p>On a tous fait des prélèvements de sang,</p>
<p>Mon papa, mon grand-père... Etc.</p>
<p>Mais aucun sang ne correspondait à celui de ma grand-mère,</p>
<p>On est partis demander dans les chambres de l'hôpital,</p>
<p>Sans résultat...</p>
<p>Les médecins ont commencé à dire à mon père « Si on ne lui fait pas une transfusion sanguine avant ce soir... Elle risque de mourir.</p>
<p>Il faut trouver d'urgence du sang AB- »</p>
<p>Mon père courrait dans l'hôpital comme un fou...</p>
<p>Il cherchait n'importe qui,</p>
<p>Il demandait à tous les gens qu'il croisait,</p>
<p>Sans succès...</p>
<p>La nuit commençait à tomber,</p>
<p>Mon père s'est soumis à la situation,</p>
<p>Il a pensé qu'il n'y avait plus d'espoir...</p>
<p>Il est sorti de l'hôpital,</p>
<p>Il est monté dans sa voiture,</p>
<p>Et il a décidé de partir chercher le reste des membres de la famille,</p>
<p>Leur dire « Venons nous rassembler à l'hôpital, peut-être que l'un de nous possède du sang AB-</p>
<p>Et au cas où elle décède... Toute la famille sera autour d'elle »</p>
<p>Et pendant que mon père quittait l'hôpital,</p>
<p>Il a croisé un autre homme qui marchait,</p>
<p>Il faisait très froid,</p>
<p>Cet homme tremblait tellement il avait froid...</p>
<p>Malgré tous les problèmes qu'avait mon père, il a décidé d'être altruiste,</p>
<p>Il s'est arrêté, et lui a demandé « Vers quelle direction tu vas ? »</p>
<p>L'homme lui a répondu « Je vais à tel endroit »</p>
<p>C'était sur le même chemin que mon père,</p>
<p>Mon père m'a dit « Je me suis senti très content, qu'au milieu de la catastrophe dans laquelle je suis, je puisse être altruiste, et que la tragédie ne m'ait pas empêché d'aider cet être humain sur ma route »</p>
<p>Mon père l'a fait monter dans sa voiture,</p>
<p>Et pendant qu'ils discutaient, il lui a demandé « D'où viens-tu ? »</p>
<p>L'homme lui a répondu « Je reviens de l'hôpital, je faisais des analyses de sang »</p>
<p>Ça a fait sourire mon père,</p>
<p>Il lui a dit « Et quel est ton groupe sanguin ? »</p>
<p>Et c'était la grande surprise... C'était AB-</p>
<p>Mon père avait du mal à y croire...</p>
<p>Il lui a dit « Est-ce que je peux te demander un service ?</p>
<p>Ma maman est entre la vie et la mort,</p>
<p>Car son groupe sanguin est AB- et on ne trouve personne pour lui en donner »</p>
<p>L'homme lui a répondu « Tu as été altruiste avec moi,</p>
<p>Tu as décidé de me déposer,</p>
<p>Fais demi-tour avec la voiture,</p>
<p>Je vais donner à ta maman de mon sang, pour la sauver »</p>
<p>Ils sont revenus à l'hôpital,</p>
<p>Ma grand-mère a été sauvée,</p>
<p>Et elle a vécu jusqu'en 1996,</p>
<p>Soit 47 ans après cette anecdote...</p>
<p>L'histoire te dit à la fin : Sois altruiste, et Dieu te donnera Sa miséricorde,</p>
<p>Mes chers, l'altruisme est un des secrets de la miséricorde que Dieu nous donne.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
