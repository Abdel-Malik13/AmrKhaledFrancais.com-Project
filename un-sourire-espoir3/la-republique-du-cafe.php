                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Je veux commencer cet épisode avec un sens que je répète beaucoup…</p>
<p>Que je ressens,</p>
<p>Et sur lequel j’insiste…</p>
<p>Un sens qui affronte le chômage,</p>
<p>Qui aide les gens qui ne trouvent pas de travail,</p>
<p>Et qui contredit les gens qui disent que les sources de revenus sont faibles…</p>
<p>Moi je dis que « Il y a autant d’occasions dans la vie, qu’il y a d’êtres humains sur terre »</p>
<p>Les occasions dans la vie pour réussir… Pour gagner de l’argent… Pour travailler… Pour sortir du chômage…</p>
<p>Je le redis : « Le nombre d’occasions dans la vie est égal au nombre d’êtres humains ».</p>
<p>Que personne ne dise qu’elles sont rares…</p>
<p>L’histoire d’aujourd’hui a un rapport avec ce sens :</p>
<p>C’est l’histoire d’une femme qui s’appelle Sahar Al-Hachmi,</p>
<p>C’est elle qui a fondé la république du café…</p>
<p>C’est quoi la république du café ?</p>
<p>Et c’est quoi l’histoire de Sahar Al-Hachmi ?</p>
<p>Sahar est née en 1968,</p>
<p>C’est-à-dire aujourd’hui elle doit avoir environ 45 ans.</p>
<p>Elle est née en Iraq,</p>
<p>Et pendant la guerre qui a éclatée entre l’Iraq et l’Iran,</p>
<p>Elle s’est réfugiée en Angleterre,</p>
<p>Elle a appris la langue anglaise,</p>
<p>Elle a fait des études de droit…</p>
<p>Puis elle a essayé de travailler comme avocate,</p>
<p>Mais elle n’y arrivait pas…</p>
<p>Elle a essayé par tous les moyens de trouver un travail dans le domaine du droit,</p>
<p>Mais sans succès,</p>
<p>Elle n’est pas douée en tant qu’avocate…</p>
<p>Du coup elle était au chômage,</p>
<p>Et la vie devenait de plus en plus difficile pour elle…</p>
<p>Elle a commencé à essayer de travailler dans un autre domaine,</p>
<p>Mais au bout de 5 mois ils l’ont licenciée,</p>
<p>Car elle n’était pas douée…</p>
<p>Rien ne marchait pour elle,</p>
<p>Les occasions de la vie se sont beaucoup réduites…</p>
<p>Qu’est ce qu’elle va faire ?</p>
<p>Son frère a fait des études aux États-Unis,</p>
<p>Ensuite il a travaillé dans une banque là-bas,</p>
<p>Alors elle est partie chez son frère,</p>
<p>Elle a cherché du travail aux États-Unis…</p>
<p>Sans succès.</p>
<p>Puis la vie est chère en Amérique,</p>
<p>Alors elle a décidé de revenir en Angleterre…</p>
<p>Un jour, elle s’est assise dans un café pour réfléchir,</p>
<p>Elle a pris une feuille et un stylo,</p>
<p>Et elle se demandait dans quoi elle pourrait travailler ?</p>
<p>Sa situation était difficile…</p>
<p>Elle a commandé un café,</p>
<p>Et quand elle a commencé à le boire,</p>
<p>Elle s’est dite « C’est quoi ce mauvais café ?</p>
<p>Ça n’a aucun goût. »</p>
<p>Elle est partie s’asseoir dans un autre café,</p>
<p>Pareil il était mauvais…</p>
<p>Elle s’est demandée pourquoi le café en Angleterre a un mauvais goût ?</p>
<p>Pourquoi ce n’est pas aussi développé qu’aux États-Unis ?</p>
<p>Elle a senti une grande différence par rapport aux cafés qu’elle buvait en Amérique lorsqu’elle était partie visiter son frère…</p>
<p>C’est là qu’elle a eu une idée : Pourquoi je n’ouvre pas un café spécialisé où on sert des produits au bon goût ?</p>
<p>Elle a présenté son projet à 19 instituts de financement…</p>
<p>Imaginez,</p>
<p>Ils ont tous refusé de la financer.</p>
<p>Ils lui ont dit « C’est une idée insensée,</p>
<p>Comment veux-tu changer le café ?</p>
<p>C’est un projet qui manque de sérieux »</p>
<p>Mais elle ne s’est pas découragée…</p>
<p>Elle a continué à chercher des moyens de financer son projet,</p>
<p>Et elle a enfin pu obtenir une réponse favorable de la part du ministère du commerce et de l’industrie anglais.</p>
<p>Ils lui ont donné un prêt de 75.000 livres sterling,</p>
<p>Elle a pu ouvrir son premier café en 1995,</p>
<p>Elle y servait 10 types de cafés,</p>
<p>Un café au miel…</p>
<p>Un café aux noisettes…</p>
<p>Un café moka…</p>
<p>Un café à la cannelle…</p>
<p>Son projet grandissait,</p>
<p>Et qu’est ce qui s’est passé par la suite ?</p>
<p>Quelques années plus tard,</p>
<p>Sahar possède 42 branches de son café,</p>
<p>42 endroits où travaillent 800 employés…</p>
<p>Son entreprise est entrée en bourse,</p>
<p>Puis elle l’a vendue pour 8,5 millions de livres sterling.</p>
<p>Et depuis, Sahar dit « J’ai appris que la vie est pleine d’occasions ».</p>
<p>Après avoir vendu son entreprise, elle s’est mise à chercher de nouveaux projets...</p>
<p>Et qu'à-t-elle nommé son entreprise ?</p>
<p>Coffee Republic (La république du café)</p>
<p>« Il y a autant d'occasions dans la vie, qu'il y a d'êtres humains sur terre »</p>
<p>Et où est-ce que Sahar a trouvé l'idée de son projet ?</p>
<p>En buvant un café, et en trouvant que son goût était maivais...</p>
<p>Elle a décidé de sortir un nouveau produit sur le marché.</p>
<p>Rêve,</p>
<p>Cours derrière ton rêve,</p>
<p>Et sois certain que Dieu est Généreux et qu'Il ouvre les portes à Ses créatures,</p>
<p>Il faut juste qu'ils réfléchissent,</p>
<p>Qu'il fassent des efforts, qu'ils persévèrent,</p>
<p>Et à ce moment, c'est obligé que Dieu les fasses réussir et qu'Il leur ouvre les portes de la subsistance,</p>
<p>Je jure que la vie est ainsi.</p>
<p>Croyez-moi, tous les gens découragés,</p>
<p>Tous ceux qui disent : « Il n'y a plus d'occasions de réussir... »</p>
<p>L'histoire que je vous ai racontée aujourd'hui vous prouve le contraire :</p>
<p>« Le nombre d'occasions dans la vie est égal au nombre d'êtres humains ».</p>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses Bénédictions.</p>
