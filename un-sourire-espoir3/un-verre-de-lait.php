                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>Mes chers, il y a une phrase négative très répandue chez nous...</p>
<p>Une phrase qui dit « Comme si ca ne dépendait que de moi... Vous ne voyez que moi ?</p>
<p>Comme si ce que j'ai fait (de mal) va ruiner l'univers ? »</p>
<p>Ce sont des paroles négatives et pas positives.</p>
<p>L'être humain positif ne sait pas raisonner de cette façon,</p>
<p>Je suis désolé de vous dire que c'est de l'égoïsme,</p>
<p>C'est l'être humain égoïste qui raisonne de cette façon...</p>
<p>Je vais vous raconter une histoire symbolique :</p>
<p>On dit qu'un village a été frappé par la famine...</p>
<p>Une famine très dure,</p>
<p>Il n'y avait plus rien à manger,</p>
<p>Les habitants souffraient de la faim et de la soif...</p>
<p>Et c'étaient les enfants qui souffraient le plus.</p>
<p>Alors le chef du village a fait une demande à tous les habitants :</p>
<p>Vu la crise que nous vivons,</p>
<p>Je vais mettre un énorme récipient au centre du village,</p>
<p>Et chaque villageois, grand ou petit... Homme ou femme... Devra ramener un verre de lait de chez lui, et le verser dans le récipient.</p>
<p>C'est-à-dire un verre de lait, n'affectera pas une maison entière,</p>
<p>Mais si on rassemblait beaucoup de lait dans le récipient, ca fera une grande différence,</p>
<p>On pourra tous boire,</p>
<p>L'essentiel c'est qu'aujourd'hui chacun de nous ramène un verre de lait,</p>
<p>Et demain matin, on se rassemblera tous,</p>
<p>Puis tout le monde pourra boire du lait, ceux qui en ont comme ceux qui n'en ont pas...</p>
<p>Les habitants du village ont aimé l'idée, et se sont activés pour aller chercher les verres de lait chez eux,</p>
<p>Puis le chef du village n'a pas eu besoin de mettre un gardien pour surveiller le récipient,</p>
<p>Tous les gens avaient faim, donc personne ne viendra boire en cachette avant le lendemain matin.</p>
<p>Et en effet, personne n'est venu voler ni quoi que ce soit...</p>
<p>La nuit s'est bien passée,</p>
<p>Et au petit matin, tous les habitants du village se sont rassemblés autour du récipient,</p>
<p>C'est magnifique,</p>
<p>Le récipient est rempli jusqu'au bout !</p>
<p>Mais il n'est pas rempli de lait...</p>
<p>Il est rempli d'eau.</p>
<p>Mais il est où le lait ?</p>
<p>Chaque habitant s'est dit « Ce n'est pas grave, je laisserai le verre de lait chez moi, et j'irai verser un verre d'eau dans le récipient...</p>
<p>Ca ne fera pas de différence,</p>
<p>Un verre d'eau ne se verra pas dans un immense récipient rempli de lait »</p>
<p>Donc chaque habitant du village a compté sur les autres...</p>
<p>Ils ont tous raisonné de la même façon,</p>
<p>Et chacun d'eux pensait qu'il était le seul à avoir versé de l'eau à la place du lait...</p>
<p>Le résultat est que tout le village a eu encore plus faim,</p>
<p>Et que ce jour là, 3 enfants sont décédés à cause de la famine...</p>
<p>Et toi mon cher ?</p>
<p>Est-ce que tu es de ceux qui remplissent le récipient de la vie par de l'eau ?</p>
<p>Ou bien tu le rempli de lait ?</p>
<p>« Comme si ca ne dépendait que de moi... »</p>
<p>En voiture, on brûle le feu rouge et on dit : « Tout le monde le fait, alors que je le fasse ou pas ca ne changera rien »</p>
<p>On jette des ordures dans la rue et on dit : « La rue est déjà sale, alors ca ne fera pas de différence »</p>
<p>On sème le désordre sur terre et on dit : « Et alors ? C'est déjà le désordre de toute façon »</p>
<p>On bâcle notre travail et on dit : « De toute manière personne dans l'entreprise ne travaille sérieusement »</p>
<p>Un sourire d'espoir pour chaque être humain positif,</p>
<p>Qui dit « Je suis responsable... Je suis responsable devant Dieu,</p>
<p>Et je suis responsable de mon pays,</p>
<p>Je vais faire ce qui est juste, même si je suis le seul à le faire »</p>
<p>Un sourire d'espoir pour chaque personne qui fait le bien, et qui n'attend rien des autres,</p>
<p>Sauf qu'ils la prennent comme exemple,</p>
<p>Mais elle / lui ne se laisse pas influencer par les mauvais comportements des autres.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
