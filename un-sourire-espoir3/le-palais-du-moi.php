                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui dit :</p>
<p>Il existe une maladie grave qui arrive aux gens,</p>
<p>Mais ce n’est pas une maladie dans le corps…</p>
<p>C’est une maladie dans le cœur,</p>
<p>Une maladie appelée « Le gonflement du Moi ».</p>
<p>Que le « Moi » devient très grand…</p>
<p>Te voir au dessus de tout le monde,</p>
<p>Traiter les gens avec arrogance,</p>
<p>Par exemple quand tu conduis ta voiture, et que tu t’arrêtes devant un feu rouge…</p>
<p>Quand le feu passe au vert,</p>
<p>Tu avances tout doucement…</p>
<p>Mais si quelqu’un est devant toi, et qu’il avance doucement avec sa voiture,</p>
<p>Tu lui klaxonnes 100 fois,</p>
<p>Tu te dis « Comment ose-t-il retarder les gens ! »</p>
<p>Et c’est pareil dans d’autres situations,</p>
<p>Quand tu attends ton tour chez le médecin,</p>
<p>Dans ta façon de marcher,</p>
<p>Dans ta façon de saluer les gens…</p>
<p>L’histoire d’aujourd’hui s’est passée à l’époque de l’empire Ottoman,</p>
<p>Un homme riche aimait qu’on dise avant son nom, 4 ou 5 titres…</p>
<p>Son excellence… Son ceci… Son cela…</p>
<p>On lui demandait « Pourquoi c’est important à ce point pour toi ? »</p>
<p>Il répondait « Les titres, sont des pilules qu’on prend pour grossir »</p>
<p>Pour faire grossir le « Moi »…</p>
<p>L’histoire d’aujourd’hui est une histoire arabe très ancienne :</p>
<p>Un grand architecte appelé Sinmar,</p>
<p>Était spécialisé dans la construction de palais et de châteaux…</p>
<p>Un jour, le roi d’Al-Hira (une ville d’Irak), lui a commandé un palais,</p>
<p>Ce roi s’appelait Anou’man.</p>
<p>Sinmar, l’architecte avait un « Moi » très gonflé…</p>
<p>Et Anou’man, le roi avait aussi un « Moi » très gonflé…</p>
<p>Alors les 2 « Moi » sont entrés en rivalité.</p>
<p>En construisant le palais, Sinmar voyait son « Moi »… C’est mon palais !</p>
<p>Et Anou’man, voyait son « Moi »… C’est moi le propriétaire du palais !</p>
<p>La construction s’est terminée,</p>
<p>Et Sinmar s’attendait à ce que son nom soit gravé dans le palais…</p>
<p>Mais Anou’man n’a pas cité le nom de l’architecte,</p>
<p>Il a gravé juste son nom à lui, « Le palais d’Anou’man ».</p>
<p>Les gens venaient visiter le palais, et Anou’man leur disait « C’est mon palais ».</p>
<p>Sinmar bouillait de l’intérieur…</p>
<p>Il y avait un clash,</p>
<p>Entre le « Moi » de Sinmar, et le « Moi » d’Anou’man.</p>
<p>Sinmar descendait dans la rue, et disait aux gens « Vous voyez ce palais ? C’est moi qui l’ai construit ! »</p>
<p>Les gens lui répondaient « Non, c’est le palais d’Anou’man, on ignore qui l’a construit »</p>
<p>Sinmar leur disait « Je peux construire un palais encore plus beau ! »</p>
<p>Quand le roi Anou’man a appris que Sinmar voulait construire un palais plus beau que le sien,</p>
<p>Il a dit « Je vais l’en empêcher ! Je vais l’emprisonner s’il le faut ! »</p>
<p>Quand Sinmar a appris ça, il a surenchérit, en disant « J’ai placé une brique dans le palais d’Anou’man, si on la retire, le palais s’écroulera ! Et j’ai fait exprès de la placer dans l’architecture »</p>
<p>Du coup, il y avait la brique d’un côté, et la tête de Sinmar de l’autre…</p>
<p>Qui est le plus important chez Anou’man ?</p>
<p>Son palais, ou bien la vie de Sinmar ?</p>
<p>Anou’man a emmené Sinmar au dessus du palais, et il l’a jeté…</p>
<p>Sinmar est mort,</p>
<p>Anou’man a gagné…</p>
<p>Mais qu’a-t-il gagné ?</p>
<p>Son « Moi » a gonflé au point de devenir plus important que la vie d’un être humain,</p>
<p>Aïe quand notre « Moi » gonfle…</p>
<p>Aïe quand on se voit très grands…</p>
<p>Même au détriment des autres.</p>
<p>La rivalité était féroce entre le « Moi » de Sinmar, et le « Moi » d’Anou’man.</p>
<p>C’est pour cela qu’elle est belle la parole du Prophète (bénédiction et paix soient sur lui),</p>
<p>Dans laquelle il a dit :</p>
<p><strong class="prophete">« Celui qui se baisse par modestie comme ceci »</strong> (Et il a baissé sa main vers le bas)</p>
<p><strong class="prophete">« Dieu l’élève comme cela »</strong> (et il a levé sa main vers le haut)</p>
<p>Sois modeste pour Dieu, et Il t’élèvera.</p>
<p>Celui qui fait preuve de modestie, Dieu l’élève.</p>
<p>Un sourire d’espoir pour l’humilité, et pour la vie simple et belle.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
