                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui commence par une question :</p>
<p>Quel est votre avis, quand l’ignorance et la croyance au surnaturel deviennent plus fortes que la médecine ?</p>
<p>Quel est votre avis, quand les légendes, la sorcellerie et les superstitions deviennent plus fortes que le cerveau et la logique ?</p>
<p>Qu’est ce qui se produit dans ce cas ?</p>
<p>Laissez-moi vous raconter l’histoire d’aujourd’hui :</p>
<p>C’est un grand écrivain qui s’appelle Yahya Haqqi,</p>
<p>Yahya Haqqi a écrit une célèbre histoire appelée : « La lampe d’Oum Hachim ».</p>
<p>D’ailleurs son histoire a été adaptée au cinéma,</p>
<p>Mais beaucoup de gens n’ont pas vu le film, car il date de l’époque du noir et blanc.</p>
<p>C’est l’histoire d’un médecin appelé Dr. Ismaïl,</p>
<p>Dr. Ismaïl est parti en Europe,</p>
<p>Et il a étudié la médecine des yeux,</p>
<p>C’est devenu un grand médecin spécialiste,</p>
<p>Mais car il est patriotique, il a décidé de revenir vivre en Égypte, pour soigner les gens atteints de maladies des yeux.</p>
<p>Et quand il est rentré dans son village,</p>
<p>Il a été surpris de voir que beaucoup de gens ont des problèmes avec leurs yeux,</p>
<p>Et que beaucoup de personnes perdent la vue…</p>
<p>Car à cause de l’ignorance et de la croyance aux légendes,</p>
<p>Ils soignent leurs yeux avec de l’huile de lampes…</p>
<p>Vous voyez les lampes dans les rues ?</p>
<p>On y mettait de l’huile pour qu’elles fonctionnent.</p>
<p>Et un quartier s’appelait le quartier d’Oum Hachim,</p>
<p>Les gens pensent que cette femme est une sainte… Et ils cherchent sa baraka (bénédiction),</p>
<p>Alors pour soigner leurs yeux, ils utilisent l’huile des lampes de ce quartier,</p>
<p>Et ils pensent que dans cette huile il y a une baraka…</p>
<p>Alors qu’en réalité ils perdent la vue à cause de cette huile.</p>
<p>Un affrontement va alors se produire entre la médecine et la légende…</p>
<p>Dr. Ismaïl dans l’histoire, recevait même des menaces de mort,</p>
<p>Car les gens pensaient qu’il refusait la bénédiction d’Oum Hachim.</p>
<p>Alors pour régler ce problème, et pour sauver sa vie,</p>
<p>Dr. Ismaïl a servi la médecine dans un emballage de légende…</p>
<p>Qu’a-t-il fait ?</p>
<p>Il a convaincu les gens que les gouttes qu’il leur donnait, n’étaient pas des médicaments fabriqués dans un laboratoire,</p>
<p>Mais que c’était de l’huile d’Oum Hachim.</p>
<p>Les gens y ont cru, et ils ont commencé à se soigner, leur état s’améliorait…</p>
<p>Et du coup leur croyance dans les capacités de l’huile d’Oum Hachim augmentait de plus en plus.</p>
<p>Qu’est ce qui se passe mes chers, quand la médecine devient plus faible que la sorcellerie et les superstitions ?</p>
<p>Qu’est ce qui se produit quand beaucoup de filles pensent qu’elles ne trouvent pas de mari… Ou quand des mamans pensent que leurs filles ne se marient pas car quelqu’un leur a jeté un sort… à cause du mauvais œil… Ou à cause d’autres superstitions ?</p>
<p>Qu’est ce qui arrive quand les jeunes pensent qu’ils ont échoué à l’école ou raté leur année scolaire, car quelqu’un / quelque chose leur a porté malheur… à cause de la sorcellerie etc. ?</p>
<p>Il est magnifique le Prophète Mohamed (bénédictions et paix soient sur lui), le professeur de l’humanité,</p>
<p>Quand il a combattu les superstitions dans un des moments les plus difficiles de sa vie…</p>
<p>Vous savez quand ?</p>
<p>Quand son fils Ibrahim est décédé… C’était encore un enfant.</p>
<p>Et le même jour où Ibrahim le fils du Prophète (bpsl) est décédé,</p>
<p>Une éclipse solaire s’est produite…</p>
<p>Le soleil s’était éclipsé,</p>
<p>Alors les gens ont dit « Le soleil s’est éclipsé pour la mort d’Ibrahim ».</p>
<p>Le Prophète (bpsl), dans un moment de grande douleur, après avoir perdu son fils… N’a pas laissé les gens se soumettre aux superstitions et à l’ignorance,</p>
<p>Il s’est levé, et il a dit</p>
<p><strong class="prophete">« Ô gens ! Le soleil et la lune sont des preuves parmi les preuves de Dieu,</strong></p>
<p><strong class="prophete">Ils ne s’éclipsent pour la mort de personne. »</strong></p>
<p>Comme c’est magnifique de la part du Prophète (bpsl) !</p>
<p>Quelqu’un à sa place pouvait dire « Regardez ce que le soleil a fait pour moi »</p>
<p>Ou bien il pouvait se dire « Je suis en plein deuil, je n’ai pas le temps de m’occuper de ça »</p>
<p>Que le salut et la paix soient sur Mohamed,</p>
<p>L’islam est une grande religion, qui a combattu l’ignorance,</p>
<p>Et qui a combattu les superstitions,</p>
<p>C’est le Prophète (bpsl) lui-même qui leur a dit « C’est faux ce que vous dites ».</p>
<p>Mes chers : Qu’est ce qui nous est demandé de faire ?</p>
<p>Il t’est demandé de t’activer dans la vie, de faire des efforts,</p>
<p>Et d’avancer avec la logique, pas avec les superstitions,</p>
<p>Et à ce moment tu verras un sourire d’espoir dans ta vie.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
