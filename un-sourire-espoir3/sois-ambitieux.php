                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui vient d’Égypte,</p>
<p>C’était un agent de nettoyage dans une grande entreprise.</p>
<p>Son travail était donc de nettoyer les toilettes,</p>
<p>Balayer les couloirs etc.</p>
<p>C’est le travail qu’il fait, mais c’est un jeune très ambitieux…</p>
<p>D’ailleurs c’est une histoire vraie,</p>
<p>Et elle s’est passée récemment,</p>
<p>On parle des 2 ou 3 années dernières.</p>
<p>Donc ce jeune travaillait comme agent de nettoyage,</p>
<p>Mais un jour, le serveur de la cafétéria n’est pas venu…</p>
<p>Celui qui servait le thé et le café aux employés, était absent,</p>
<p>Alors ils ont appelé ce jeune,</p>
<p>Ils lui ont demandé de se laver les mains,</p>
<p>De mettre une tenue propre,</p>
<p>Puis de servir le thé et le café aux employés ce jour là…</p>
<p>Du coup il est passé d’agent de nettoyage à serveur de la cafétéria.</p>
<p>Il a servi le thé et le café ce jour là,</p>
<p>Ils étaient très contents de la qualité de son travail…</p>
<p>Le lendemain, le responsable de la cafétéria n’est pas venu,</p>
<p>Puis le 3ème jour pareil…</p>
<p>Ce jeune a été nommé officiellement comme responsable de la cafétéria.</p>
<p>Il est passé d’agent de nettoyage à serveur de thé et de café.</p>
<p>Puis en servant le thé et le café aux employés,</p>
<p>Ce jeune avait l’air très intelligent et très ambitieux,</p>
<p>Bien sûr il n’avait pas poursuivi ses études,</p>
<p>Il a quitté la scolarité avant le Bac.</p>
<p>Les employés ont commencé à lui dire « Nous voulons que tu nous fasses des photocopies »</p>
<p>Il prenait les documents et il allait les photocopier,</p>
<p>Puis il était tellement sérieux,</p>
<p>Qu’il a été promu…</p>
<p>Il est passé de serveur de la cafétéria, à responsable des photocopies de l’entreprise.</p>
<p>Puis un jour un employé lui a demandé s’il s’y connaissait en informatique,</p>
<p>Ce jeune lui a répondu « Je sais bien taper »</p>
<p>L’employé lui a dit « J’ai écrit une feuille à la main, j’ai besoin que tu me la tapes sur l’ordinateur »</p>
<p>Le jeune a tapé le document sur l’ordinateur,</p>
<p>L’employé était très content du résultat…</p>
<p>Depuis ce jour, chaque fois qu’un employé n’avait pas le temps de taper un document,</p>
<p>Il venait demander à ce jeune de le faire…</p>
<p>Du coup il est passé d’agent de nettoyage à Serveur à la cafétéria, Responsable des photocopies, Taper des documents sur ordinateur</p>
<p>Puis ensuite ils ont vu qu’il s’y connaissait bien en informatique,</p>
<p>Alors ils lui ont donné un poste plus important.</p>
<p>Puis il est entré en université libre,</p>
<p>Il étudiait le commerce,</p>
<p>Il réussissait année après année…</p>
<p>Et aujourd’hui savez-vous dans quoi il travail ?</p>
<p>Il est fonctionnaire dans l’entreprise,</p>
<p>Mais pas seulement…</p>
<p>Il fait partie des personnes qualifiées pour le poste de directeur.</p>
<p>La vie est une question d’efforts mes chers,</p>
<p>Celui qui persiste et qui a une ambition, finira par réussir !</p>
<p>Nous n’avons pas la culture de l’effort…</p>
<p>Mais ce jeune qui s’appelle Ayman, il l’a.</p>
<p>C’est quelqu’un dont on est fiers.</p>
<p>Imaginez son désir de réussir,</p>
<p>Il a commencé à travailler comme agent de nettoyage, et il n’a pas eu honte…</p>
<p>Il a travaillé, il a fait des efforts, il a cherché à se développer,</p>
<p>Puis il est entré en université libre,</p>
<p>Jusqu’à ce qu’il monte en haut.</p>
<p>Imaginez, aujourd’hui il est bien placé pour un poste de directeur…</p>
<p>Les jeunes : Faites des efforts,</p>
<p>Soyez ambitieux,</p>
<p>N’arrêtez pas de rêver,</p>
<p>Ne cessez pas de vous accrocher, de vous activer.</p>
<p>Et soyez sûrs que Dieu vous ouvrira les portes, et qu’Il vous fera grandir.</p>
<p>C’est beau, c’est magnifique que parmi nous il y ait quelqu’un comme Ayman,</p>
<p>Que Dieu le bénisse,</p>
<p>Et on espère voir des gens comme lui dans tous les domaines,</p>
<p>Des gens prêts à faire des efforts comme il l’a fait.</p>
<p>À ce moment, un sourire d’espoir se dessinera.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
