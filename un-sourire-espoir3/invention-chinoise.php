                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui vient de la Chine,</p>
<p>Mais avant de vous raconter ce que la Chine a fait,</p>
<p>Chez nous en Égypte, le nombre de personnes handicapées atteint des millions.</p>
<p>On dit qu’ils sont 8 ou 9 millions, mais seul Dieu connait le nombre exact.</p>
<p>Mais ce que je veux vous raconter aujourd’hui c’est une chose étonnante que la Chine a commencé à faire récemment,</p>
<p>Un ami qui revient de là-bas me raconte :</p>
<p>Ils ont commencé à réaménager les rues de Pékin,</p>
<p>Et de chaque côté du trottoir,</p>
<p>Ils ont commencé à mettre une nouvelle peinture blanche,</p>
<p>Mais dans cette peinture blanche, il y a une nouvelle matière.</p>
<p>Les gens ne comprenaient pas ce que c’était…</p>
<p>Puis au bout d’un moment, les gens ont découvert la raison de cette nouvelle peinture :</p>
<p>C’est au service des personnes handicapées,</p>
<p>Et surtout les personnes malvoyantes.</p>
<p>C’est une nouvelle technologie,</p>
<p>Une nouvelle invention,</p>
<p>C’est quoi au juste ?</p>
<p>La partie peinte en blanc,</p>
<p>Est magnétisée d’une certaine façon.</p>
<p>Du coup, quand une personne malvoyante marche dans la rue, dès que sa canne touche cette zone magnétisée,</p>
<p>Ça transmet un signal à la canne,</p>
<p>La canne est reliée à des écouteurs,</p>
<p>Et une voix automatique lui dit dans son oreille le nom de la rue dans laquelle elle se trouve…</p>
<p>Et combien de pas il reste avant le feu rouge,</p>
<p>Et le nom de la rue d’en face,</p>
<p>Et si le feu est rouge, orange ou vert pour pouvoir traverser…</p>
<p>C’est-à-dire les rues communiquent désormais avec les personnes malvoyantes,</p>
<p>Qui n’ont plus besoin de demander l’aide des autres.</p>
<p>C’est une nouvelle technologie,</p>
<p>Et la canne utilisée est aussi nouvelle,</p>
<p>Ils l’ont inventée spécialement pour qu’elle soit reliée aux rues magnétisées, et pour qu’elle donne les informations aux personnes qui en ont besoin.</p>
<p>Bien sûr la Chine a les moyens financiers pour réaliser un projet pareil,</p>
<p>Nos pays peuvent ne pas avoir les mêmes moyens,</p>
<p>Mais nous avons autre chose…</p>
<p>Pas une chose qui pourra remplacer la nouvelle technologie,</p>
<p>Mais une chose très importante et essentielle :</p>
<p>La sympathie et l’entraide.</p>
<p>Voir les personnes handicapées avec amour, avec compréhension, avec réactivité, vouloir leur rendre service.</p>
<p>Car si la technologie n’est pas disponible pour les aider,</p>
<p>Que chacun de nous devienne une technologie vivante,</p>
<p>Pour les aider à avoir une meilleure vie.</p>
<p>S’il vous plait : Tant qu’on bouge, on peut croiser une personne handicapée.</p>
<p>Si tu peux lui apprendre,</p>
<p>Si tu peux l’aider,</p>
<p>Si tu peux le soutenir dans un de ses projets…</p>
<p>Parfois l’entraide est plus forte que la technologie,</p>
<p>Car avec trop de technologie, il finira seul, et personne ne lui parlera.</p>
<p>Nous pouvons trouver une solution qui ne remplace pas la technologie,</p>
<p>Mais qui aide à amortir le manque de moyens technologiques.</p>
<p>S’il vous plait : Voyez les personnes handicapées d’un regard sympathique.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
