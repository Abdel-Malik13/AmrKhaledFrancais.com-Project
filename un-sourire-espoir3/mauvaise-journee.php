                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui, c'est un médecin algérien qui me l'a racontée,</p>
<p>Il s'appelle Abderrahmane Draga,</p>
<p>Mais l'histoire est arrivée à un collègue à lui qui s'appelle Dr. Eshan,</p>
<p>(C'est une histoire vraie)</p>
<p>Dr. Eshan est un des plus grands chirurgiens en Inde,</p>
<p>Dr. Eshan est très demandé, il donne des conférences partout dans le monde...</p>
<p>Un jour il est parti à l'aéroport pour prendre l'avion,</p>
<p>Il devait assister à une conférence scientifique très importante,</p>
<p>Il est monté dans l'avion avec les autres passagers,</p>
<p>L'avion allait décoller,</p>
<p>Puis le staff a annoncé qu'il y avait un problème technique,</p>
<p>Les passagers devaient descendre de l'avion, et attendre à l'aéroport entre 6 à 8 heures...</p>
<p>Dr. Eshan était très mécontent « 6 à 8h d'attente ? La conférence sera déjà terminée ! »</p>
<p>Il s'est plaint auprès des responsables de la compagnie aérienne,</p>
<p>Alors ils lui ont dit « Il y a un autre aéroport à 3h de trajet d'ici,</p>
<p>Donc au lieu d'attendre 8h,</p>
<p>Tu peux prendre une voiture,</p>
<p>Et le temps d'arriver là-bas on t'aura déjà réservé ton billet, l'avion décollera d'ici 3h »</p>
<p>Il leur a dit « C'est mieux que rien... »</p>
<p>Ils lui ont dit « Par contre il y a un seul problème... La route d'ici jusqu'à l'autre aéroport est difficile »</p>
<p>En plus il faisait froid et il pleuvait...</p>
<p>Dr. Eshan s'est énervé,</p>
<br>
<p>Mais il n'avait pas le choix... Il a décidé d'y aller en voiture,</p>
<p>Il a pris un taxi,</p>
<p>Il roulait sous la pluie,</p>
<p>Puis Dr. Eshan n'avait pas de chance ce jour là...</p>
<p>Le pneu du taxi a crevé,</p>
<p>Cette fois il n'en pouvait plus... Il était énervé au maximum,</p>
<p>Il est descendu de la voiture,</p>
<p>Dans le froid, et sous la pluie...</p>
<p>Il a vu une petite maison,</p>
<p>Il est parti frapper à la porte,</p>
<p>Une vieille femme indienne lui a ouvert...</p>
<p>Il lui a dit « Je suis tombé en panne dans votre village,</p>
<p>Est-ce que je peux m'abriter de la pluie, en attendant que la voiture soit réparée ? »</p>
<p>Elle lui a dit « Entre mon fils »</p>
<p>Et elle lui a servi du thé,</p>
<p>C'était une femme très âgée, et la maison était vide...</p>
<p>Dr. Eshan était assis en train de boire son thé,</p>
<p>Et la dame est partie prier...</p>
<p>Elle était musulmane,</p>
<p>Elle faisait la prière, et à côté d'elle se trouvait un petit enfant de 3 ans,</p>
<p>Elle s'assurait chaque fois qu'il était bien couvert,</p>
<p>Puis elle revenait prier,</p>
<p>Puis elle venait voir Dr. Eshan pour lui servir de l'eau...</p>
<p>Dr. Eshan sortait, et voyait que la voiture n'était toujours pas réparée,</p>
<p>Il revenait...</p>
<p>Et cette scène se répétait :</p>
<p>La dame faisait 2 unités de prière,</p>
<p>Elle allait voir l'enfant qui dormait,</p>
<p>Puis elle venait demander à Dr. Eshan s'il n'avait besoin de rien...</p>
<p>Le pneu de la voiture a enfin été réparé,</p>
<p>Mais Dr. Eshan était très touché par la gentillesse et le calme de cette femme,</p>
<p>Cette scène était très émouvante pour lui...</p>
<p>En partant il lui a dit</p>
<p>« Je t'ai vu en train de prier... »</p>
<p>Elle lui a dit « Oui je fais beaucoup de doua'a...</p>
<p>Pendant toute ma vie, chaque fois que j'ai demandé quelque chose à Dieu, Il me l'a donnée.</p>
<p>Sauf une seule demande... »</p>
<p>Dr. Eshan lui a demandé « Laquelle ? »</p>
<p>La femme lui a répondu « Ce garçon, est mon petit fils,</p>
<p>Son papa et sa maman sont décédés...</p>
<p><p>Il est orphelin et c'est moi qui l'élève,</p>
<p>Mais le pauvre, il est malade...</p>
<p>Il a un problème de développement musculaire...</p>
<p>Il est affaibli physiquement,</p>
<p>Et je n'ai pas les moyens de l'emmener chez un chirurgien pour le soigner »</p>
<p>La femme ne savait pas qu'il était chirurgien...</p>
<p>Il lui a dit « Est-ce que je peux l'examiner ? »</p>
<p>Il l'a examiné...</p>
<p>Puis lui a dit « Ton petit fils subira une opération, et il deviendra en bonne santé.</p>
<p>Mais madame, tu as dit que Dieu avait exaucé toutes tes prières sauf une seule...</p>
<p>Sache que ta prière a été exaucée,</p>
<p>Ta prière a retardé des avions,</p>
<p>Ta prière a crevé des pneus de voitures,</p>
<p>Et ta prière m'a fait venir jusqu'à ta maison,</p>
<p>Pour réaliser ce que tu souhaites. »</p>
<p>Mes chers, Dieu exauce les prières,</p>
<p>Et ce qui nous arrive est entièrement bien,</p>
<p>Parfois tu te demandes pourquoi telle « mauvaise » chose t'est arrivée...</p>
<p>Ce que tu ignores c'est que grâce à cette « mauvaise » chose, Dieu aide quelqu'un d'autre...</p>
<p>Dieu déplace les gens, et fait bouger l'univers entier pour exaucer la prière d'une femme modeste, qui lui a demandé de l'aide.</p>
<p>Un sourire d'espoir avec le Doua'a.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
