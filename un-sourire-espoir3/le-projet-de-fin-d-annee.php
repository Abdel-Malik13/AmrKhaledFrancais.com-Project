                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui vient de la Malaisie,</p>
<p>Et le sourire d’espoir d’aujourd’hui est une idée qui dit :</p>
<p>« Si tu ne peux pas faire de grandes choses… Alors fais une petite chose, mais d’une grande façon ».</p>
<p>Quelqu’un a compris le principe ?</p>
<p>Si tu n’as pas les moyens de faire des choses immenses,</p>
<p>Alors fais une petite chose, mais d’une grande manière.</p>
<p>Par exemple : Dans la rue, parfois une personne pauvre vient demander ton aide,</p>
<p>Et tu te dis « Même si je l’aidais, qu’est ce que ça changerait ? Il restera des millions de pauvres dans le monde »</p>
<p>Non mon cher…</p>
<p>Ce pauvre, c’est Dieu qui te l’envoie précisément,</p>
<p>Alors aide-le.</p>
<p>Et pourquoi tu devrais l’aider ?</p>
<p>Car si tu n’as pas les moyens d’aider les 90 millions de pauvres dans le monde,</p>
<p>Aide celui-là, mais d’une grande façon.</p>
<p>« Si tu ne peux pas faire de grandes choses… Alors fais une petite chose, mais d’une grande façon ».</p>
<p>Venons appliquer cette idée sur une histoire qui vient de la Malaisie :</p>
<p>C’était un professeur dans une université en Malaisie,</p>
<p>Il enseignait la sociologie,</p>
<p>Ce professeur n’enseignait pas seulement la théorie, mais il était surtout intéressé par la pratique.</p>
<p>Alors il a dit à ses 30 étudiants :</p>
<p>« Votre projet de fin d’année, c’est de rendre les gens heureux.</p>
<p>Chacun de vous devra présenter un projet dans lequel il y a le fait de « rendre les autres heureux ».</p>
<p>Et il faudra filmer le projet en vidéo. »</p>
<p>C’est-à-dire chacun de ses 30 étudiants devra présenter une vidéo dans son projet de fin d’année.</p>
<p>Puis le professeur ne s’est pas contenté de ça,</p>
<p>Il s’est mis d’accord avec une grande entreprise Malaisienne,</p>
<p>Pour que les 5 meilleurs projets reçoivent 5000 dollars en récompense.</p>
<p>Le professeur leur a laissé 3 mois,</p>
<p>Et chacun de ses élèves était motivé,</p>
<p>Celui qui cherchait un orphelinat…</p>
<p>Et celui qui prenait sa caméra et partait dans un hôpital pour enfants…</p>
<p>Et celui qui partait dans une maison de retraite…</p>
<p>Tous les étudiants ont choisi cette voie,</p>
<p>Sauf 1 seul…</p>
<p>Il s’appelle Mohamed Al-Sharif,</p>
<p>Qu’a fait cet étudiant Malaisien ?</p>
<p>Il s’est dit :</p>
<p>« Un orphelinat c’est beaucoup trop grand pour moi,</p>
<p>Je ne saurais pas changer l’état d’un orphelinat, je n’ai pas l’argent pour le faire.</p>
<p>Les autres étudiants iront filmer quelques scènes, mais moi je ne saurais pas faire une grande différence,</p>
<p>Le but du projet c’est de « rendre heureux ».</p>
<p>Avec quoi je pourrais les rendre heureux ?</p>
<p>Je ne pourrais pas rendre toute une maison de retraite heureuse… »</p>
<p>Il a donc décidé de faire une chose très simple,</p>
<p>Il s’est dit « Je vais rendre 1 seule personne heureuse… Juste une. »</p>
<p>Et il a choisi un étudiant Indien,</p>
<p>Qui réside dans la cité universitaire,</p>
<p>Il est célibataire et seul…</p>
<p>Cet étudiant Indien est renfermé sur lui-même, et son visage est toujours triste.</p>
<p>Pourquoi ?</p>
<p>Car il souffre de l’éloignement de son pays + l’éloignement de sa famille + la pauvreté + la difficulté des études (il étudiait la Médecine).</p>
<p>L’étudiant Malaisien à décidé de le rendre heureux…</p>
<p>Juste 1 seule personne.</p>
<p>Les gens lui ont dit « Tu vas rater ton projet…</p>
<p>Tous les autres font leurs projets dans des orphelinats et des hôpitaux,</p>
<p>Et toi tu parles d’une seule personne ?</p>
<p>Ce ne sera pas suffisant ».</p>
<p>Il leur a dit « Aider une seule personne d’une grande façon,</p>
<p>C’est mieux que de me lancer dans une grande chose sans pouvoir la changer ».</p>
<p>Il a essayé de rentrer en contact avec l’étudiant Indien…</p>
<p>Ce dernier était renfermé sur lui-même, il ne voulait parler à personne.</p>
<p>Alors il lui a envoyé un message sur Facebook dans lequel il a écrit :</p>
<p>« Je suis un camarade de l’université,</p>
<p>Je souhaitais devenir un médecin comme toi,</p>
<p>Malheureusement ma faiblesse dans les matières scientifiques ne m’a pas permis d’entrer dans cette filière,</p>
<p>Mais je suis content qu’il y ait avec moi, quelqu’un qui réussit comme toi. »</p>
<p>Quand l’étudiant Indien a lu ce message,</p>
<p>Il l’a fièrement envoyé à son papa en Inde,</p>
<p>Son papa lui a répondu :</p>
<p>« C’est formidable, je souhaite que tu t’accroches à cette réussite. »</p>
<p>L’étudiant Malaisien a senti qu’il commençait à toucher son camarade Indien,</p>
<p>Il a fait de plus en plus sa connaissance,</p>
<p>Puis ils ont commencé à faire des activités ensemble,</p>
<p>L’étudiant Indien avait enfin retrouvé le sourire,</p>
<p>Il a commencé à sortir de sa solitude,</p>
<p>Il n’était plus renfermé sur lui-même…</p>
<p>Ses notes augmentaient,</p>
<p>Et il a réussi sa 1ère année en Médecine,</p>
<p>Alors qu’avant il voulait tout abandonner et rentrer chez lui en Inde…</p>
<p>Mais quand son papa lui avait dit ces paroles, il avait retrouvé le courage de continuer.</p>
<p>Les 2 camarades sont restés en contact pendant 3 mois...</p>
