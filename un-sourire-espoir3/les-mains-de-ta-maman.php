                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui date des années 1970,</p>
<p>Elle s’est passée en Inde :</p>
<p>Il y avait un institut qui s’occupait des personnes âgées,</p>
<p>Les papas et les mamans qui n’avaient personne pour prendre soin d’eux…</p>
<p>Cet institut de charité avait 10 branches en Inde,</p>
<p>Le propriétaire de cet institut était un homme indien très riche,</p>
<p>Mais il avait toujours un problème :</p>
<p>Il cherchait un directeur qui puisse gérer les nombreuses branches,</p>
<p>Car cet institut de charité grandissait chaque année,</p>
<p>Il rencontrait du succès,</p>
<p>Le propriétaire donnait beaucoup d’argent pour le faire marcher,</p>
<p>Mais le souci c’est qu’il ne trouvait pas un directeur qui possède 2 qualités :</p>
<p>Un directeur qui sache gérer l’institut, et qui soit miséricordieux envers les personnes âgées.</p>
<p>Il trouvait tout le temps des directeurs avec beaucoup de compétences, mais qu’ils soient doux avec les hommes et les femmes âgées… Il leur manquait ça.</p>
<p>Ou bien des personnes très douces avec les gens âgés, mais qui n’avaient pas les compétences pour gérer l’institut.</p>
<p>Alors cet homme riche indien a décidé de faire passer des entretiens lui-même, pour trouver le directeur qu’il cherchait.</p>
<p>À l’intérieur de ces entretiens, il allait tester les candidats, pour trouver ses 2 critères.</p>
<p>Il a publié une annonce,</p>
<p>Et parmi les gens qui se sont présentés, il y avait un jeune homme,</p>
<p>Il venait juste d’obtenir son diplôme,</p>
<p>Il s’est assis devant le patron, qui lui a demandé</p>
<p>« Tu viens juste de finir tes études ? »</p>
<p>Le jeune lui a répondu « Oui »</p>
<p>Le patron lui a demandé « Touchais-tu des bourses d’études, ou l’état t’a aidé pour payer ton école ? »</p>
<p>Le jeune lui a répondu « Non pas du tout »</p>
<p>Le patron lui a demandé « C’est ton papa qui finançait tes études ? »</p>
<p>Le jeune lui a répondu « Non pas du tout, ni mon papa, ni le gouvernement n’a financé mes études »</p>
<p>Le patron lui a demandé « Et pourquoi ton papa n’a pas financé tes études ? »</p>
<p>Le jeune lui a répondu « Mon papa est décédé quand j’étais un enfant, j’avais 2 ans »</p>
<p>Le patron lui a demandé « Alors qui payait les frais de tes études ? »</p>
<p>Le jeune lui a répondu « Ma maman, c’est elle qui s’occupait de tous mes frais »</p>
<p>Le patron lui a demandé « Et ta maman travaillait dans quoi ? »</p>
<p>Le jeune a gardé le silence…</p>
<p>Puis il a essayé de changer de sujet.</p>
<p>Mais le patron lui a reposé la même question</p>
<p>« Dans quoi travaillait ta maman ? »</p>
<p>Le jeune lui a répondu « Ma maman lavait les vêtements des gens »</p>
<p>(À Delhi, c’est une chose connue,</p>
<p>Les gens rassemblent leur linge, et des femmes le font à leur place)</p>
<p>Le patron lui a demandé « Et elle lave les vêtements des gens jusqu’à maintenant ? »</p>
<p>Le jeune lui a répondu « Oui, c’est pour ça que je cherche un travail,</p>
<p>Car jusqu’à maintenant c’est ma maman qui paye mes frais, et qui fait le linge des gens »</p>
<p>Le patron était un homme intelligent,</p>
<p>Il lui a dit « Peux-tu me montrer tes mains ? »</p>
<p>Le jeune lui a demandé « Pourquoi ? »</p>
<p>Le patron lui a dit « Je veux juste voir quelque chose »</p>
<p>Le jeune lui a montré ses mains,</p>
<p>Qui étaient douces et bien entretenues.</p>
<p>Le patron lui a demandé « As-tu déjà aidé ta maman en lavant des vêtements ? »</p>
<p>Le jeune lui a répondu « Non jamais, ma maman me demandait toujours de réviser et de me concentrer sur mes études,</p>
<p>Puis ma maman lave beaucoup plus rapidement que moi »</p>
<p>Le patron lui a dit « J’ai une petite demande à te faire : Tu vas rentrer chez toi, et tu vas laver les mains de ta maman.</p>
<p>Puis demain reviens me voir »</p>
<p>Le jeune n’a pas compris pourquoi cette demande…</p>
<p>Mais il n’avait pas le choix,</p>
<p>Il est rentré chez lui,</p>
<p>Et il a raconté ça à sa maman,</p>
<p>« On m’a demandé de faire ceci et cela »,</p>
<p>Sa maman était surprise,</p>
<p>Mais elle lui a dit « D’accord fais ce que le patron t’a demandé de faire »</p>
<p>Il a commencé à laver ses mains,</p>
<p>Et il était très concentré…</p>
<p>Car il sentait que dans les mains de sa maman, il y avait quelque chose sur laquelle le patron lui posera une question le lendemain,</p>
<p>Mais quand il a commencé à laver les mains de sa maman,</p>
<p>Il s’est mis à pleurer…</p>
<p>Les mains de sa maman étaient en mauvais état,</p>
<p>À cause du linge,</p>
<p>Elles avaient des blessures qui ne pouvaient pas être soulagées par des crèmes…</p>
<p>Elles avaient des rides très profondes…</p>
<p>Le jeune regardait les mains de sa maman, et il regardait ses propres mains,</p>
<p>Il pleurait sans arrêt,</p>
<p>Il a fini de laver les mains de sa maman,</p>
<p>Et il a discuté avec elle toute la nuit,</p>
<p>« Raconte-moi ce que tu faisais en lavant les vêtements des gens »</p>
<p>Sa maman lui racontait, et il pleurait encore plus...</p>
<p>Et il a insisté pour laver lui-même ses vêtements ce jour là.</p>
<p>Le lendemain il est revenu voir e patron qui lui a demandé :</p>
<p>« As-tu lavé les mains de ta maman hier ? »</p>
<p>Le jeune lui a répondu « Oui je les ai lavées »</p>
<p>Le directeur lui a demandé</p>
<p>« Et que ressens-tu ? »</p>
<p>Le jeune lui a répondu</p>
<p>« Je suis fier de ma maman, Je n'aurai plus honte de dire le travail de ma maman si on me le demande »</p>
<p>Le patron lui a dit</p>
<p>« Félicitations, tu viens d'être accepté pour le poste de directeur »</p>
<p>Et toi ?</p>
<p>Est-ce qu'on peut te dire :</p>
<p>Félicitations, tu es accepté chez Dieu ?</p>
<p>Est-ce que ta maman est satisfaite de toi ?</p>
<p>Si c'est le cas, tu verras un sourire d'espoir dans ce bas-monde, et un sourire d'espoir dans l'au-delà.</p>
<p>Merci, Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
