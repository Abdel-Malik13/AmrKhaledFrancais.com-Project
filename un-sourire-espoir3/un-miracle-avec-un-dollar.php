<p>Welcome to a new episode of Basmit Amal
(A Smile of Hope).</p>

<p>A Smile of Hope today is about a little girl,
who is 6 years old.</p>

<p>She lives with her family in America,
and she comes from a family who is poor.</p>

<p>Beneath the house she lives in
was a pharmacy.</p>


<p>In the pharmacy, there is a pharmacist
who was talking to his brother</p>


<p>when the little girl left her house
without her parents knowing.</p>


<p>She knocked on the table while
they were talking,</p>


<p>and pulled out a dollar,
put it on the table,</p>


<p>-“May I buy a miracle, please?”</p>


<p>She’s a little girl,
so he told her,</p>


<p>-“Little one, what would you like?”</p>


<p>-“Here’s 1 dollar, I would like a miracle.”</p>


<p>He responded back saying,</p>


<p>-“Please go on, we don’t sell miracles.”</p>


<p>The girl began crying.</p>


<p>-“I can collect money from my friends,
and get another dollar</p>


<p>in addition to the dollar I have,
and I can give you $2,</p>


<p>but please sell the miracle to me.”</p>


<p>-“Little one, I don’t get what you want.”</p>


<p>His brother was standing next to him,
and was a little calmer than he was.</p>


<p>-“Dear, what is the miracle that you’re
looking to purchase?”</p>


<p>-“Right now my brother is upstairs,
and he just got back from the hospital.</p>


<p>He has a tumor in the brain, he will die,
and he needs a miracle to live.”</p>


<p>The little girl thought that a miracle
is something she can buy.</p>


<p>The doctor in front of their whole family
said that he needs a miracle to live,</p>


<p>and the girl with her innocence pulled out
the dollar she had thinking she could buy it.</p>


<p>The brother of the doctor, standing next to him,
asked her,</p>


<p>-“How much do you want the miracle for?”</p>


<p>-“I have 1 dollar and 11 cents.”</p>


<p>-“That’s exactly how much I need
for the miracle.”</p>


<p>He took the money, and told her,
come show me your house.</p>


<p>What was he doing?</p>


<p>It turns out that the pharmacist’s brother
was Dr. Carlton Armstrong.</p>


<p>Who is Dr. Carlton Armstrong?</p>


<p>He is one of the best neurosurgeons in America.</p>


<p>His brother is a pharmacist, which he was
visiting by pure coincidence</p>


<p>and talking to him when the girl went and
asked for a miracle while she had a dollar.</p>


<p>The doctor went with her, and went
to their home, and saw the little boy.</p>


<p>He told their family,</p>


<p>-“I will treat your son, and I won’t take
any money from you.”</p>


<p>The family couldn’t believe the miracle
that came to their door to treat their son.</p>


<p>It became one of the surgeries that were
taught in medical school in America</p>


<p>and around the world as one of the most
successful surgeries for Dr. Carlton Armstrong.</p>


<p>The boy recovered due to the treatment.</p>


<p>The family was sitting together,
and they said</p>


<p>-“It’s wonderful that the miracle came to us
without paying a penny.”</p>


<p>The little girl told them, because
they didn’t know what she did;</p>


<p>-“No! That’s not true. I paid $1.11
for this miracle.”</p>


<p>They told her,
-“What, you’re the one who did this?”</p>


<p>Yes, she’s the one who did that.</p>


<p>What should we gather from this story?</p>


<p>We can gather many different things.</p>


<p>First, the innocence of children brings
all the best.</p>


<p>Secondly, Allah is merciful.</p>


<p>Thirdly, never think that the miracle
is far from us,</p>


<p>if each one of us moved
and worked hard towards it.</p>


<p>Allah sent this doctor, at the time
the girl went to ask for a miracle,</p>


<p>so the boy is cured, and the miracle happened.</p>


<p>A miracle that a child was saved
with for $1.11!</p>


