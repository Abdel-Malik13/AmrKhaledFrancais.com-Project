                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle d’une petite fille âgée de 6 ans,</p>
<p>Sa famille vit aux États-Unis,</p>
<p>Ils sont modestes et n’ont pas beaucoup d’argent,</p>
<p>Et en dessous de leur immeuble se trouve une pharmacie…</p>
<p>Ce jour-là le frère du pharmacien est venu le visiter,</p>
<p>Ils discutaient ensemble,</p>
<p>Quand la petite fille de 6 ans est entrée dans la pharmacie.</p>
<p>Elle est descendue de chez elle, sans le dire à sa famille,</p>
<p>Elle est entrée dans la pharmacie,</p>
<p>Elle a tapé sur le comptoir,</p>
<p>(Le pharmacien discutait avec son frère),</p>
<p>Elle a sorti 1 dollar,</p>
<p>Elle a posé le billet sur la table,</p>
<p>Et elle a dit</p>
<p>« S’il vous plait ! Je veux acheter un miracle »</p>
<p>C’est une petite fille de 6 ans…</p>
<p>Le pharmacien ne l’a bien sûr pas prise au sérieux,</p>
<p>Il lui a dit</p>
<p>« Tu veux quoi ma fille ? »</p>
<p>Elle lui a répondu</p>
<p>« Voici 1 dollar, je veux acheter un miracle »</p>
<p>Le pharmacien lui a dit</p>
<p>« Vas t’en ma chérie, on ne vend pas de miracles »</p>
<p>La fille s’est mise à pleurer…</p>
<p>Elle lui a dit « Je peux emprunter de l’argent chez mes copines,</p>
<p>Je peux rassembler un 2ème dollar,</p>
<p>Mais vendez-moi le miracle,</p>
<p>Je veux acheter un miracle »</p>
<p>Le pharmacien s’est agacé, il lui a répondu</p>
<p>« De quoi tu parles ma fille ? »</p>
<p>Mais le frère du pharmacien était plus calme,</p>
<p>Il lui a dit</p>
<p>« Explique-moi ma chérie,</p>
<p>C’est quoi le miracle que tu veux acheter ? »</p>
<p>Elle lui a répondu</p>
<p>« Mon petit frère est en haut,</p>
<p>Il est revenu de l’hôpital tout à l’heure,</p>
<p>Et le médecin a dit qu’il a une tumeur au cerveau,</p>
<p>Qu’il va mourir,</p>
<p>Et que pour vivre il a besoin d’un miracle »</p>
<p>La petite fille pensait que le miracle était une chose qui se vend…</p>
<p>Chez elle, tout le monde pleurait,</p>
<p>Elle a entendu le médecin dire « Votre fils est atteint d’une tumeur au cerveau,</p>
<p>Et qu’il faudrait un miracle pour qu’il continue de vivre »</p>
<p>Avec l’innocence des enfants, la petite a pris 1 dollar,</p>
<p>Et elle est descendue voir le pharmacien,</p>
<p>Car c’est celui qui vend les médicaments…</p>
<p>Et elle lui a dit « S’il vous plait, je veux acheter un miracle »</p>
<p>Le frère du pharmacien lui a dit</p>
<p>« Ma chérie, combien tu peux payer pour 1 miracle ? »</p>
<p>Elle lui a répondu « J’ai 1 dollar, et 11 centimes »</p>
<p>Il lui a dit « C’est exactement le prix du miracle !</p>
<p>Donne-moi l’argent ma chérie,</p>
<p>Et montre-moi où est ton appartement »</p>
<p>C’est quoi ça ?</p>
<p>Il fait quoi au juste ?</p>
<p>Il se trouve que le frère du pharmacien s’appelle Carlton Armstrong,</p>
<p>Et qui est Carlton Armstrong ?</p>
<p>C’est un des plus grands chirurgiens du cerveau et des nerfs aux États-Unis…</p>
<p>Son frère est le propriétaire de la pharmacie,</p>
<p>Et ce jour là il est venu le visiter par hasard…</p>
<p>Et pendant qu’il était là,</p>
<p>La petite fille qui voulait acheter un miracle est entrée.</p>
<p>Il a pris le dollar,</p>
<p>Il est parti avec elle,</p>
<p>Il a examiné son petit frère,</p>
<p>Et il leur a dit « Je vais le soigner, et vous n’aurez rien à payer »</p>
<p>La famille n’arrivait pas à y croire…</p>
<p>Un miracle est venu jusqu’à chez eux,</p>
<p>Le petit frère a été guéri,</p>
<p>Et son cas est étudié dans les facultés de médecine aux États-Unis et dans le monde,</p>
<p>Parce que c’est une des meilleures opérations du Dr. Carlton Armstrong</p>
<p>Le fils a guéri,</p>
<p>Et un jour pendant que toute la famille était rassemblée,</p>
<p>Ils discutaient en disant</p>
<p>« C’est une chose formidable,</p>
<p>Le Docteur est venu nous voir,</p>
<p>Et le miracle est venu jusqu’à chez nous,</p>
<p>Sans qu’on paye un centime… »</p>
<p>Car ils ignoraient le reste de l’histoire,</p>
<p>La petite fille leur a dit</p>
<p>« Non ! J’ai payé 1 dollar et 11 centimes »</p>
<p>C’est elle qui a payé…</p>
<p>Qu’est ce qu’on pourrait apprendre de cette histoire ?</p>
<p>On pourrait apprendre beaucoup de choses :</p>
<p>Premièrement, l’innocence des enfants ramène tout le bien.</p>
<p>Deuxièmement, Dieu est Miséricordieux.</p>
<p>Troisièmement, ne pense pas que le miracle est loin de toi, il suffit que tu bouges et que tu fasses l’effort.</p>
<p>Dieu a envoyé ce médecin, chez son frère le pharmacien,</p>
<p>Au moment où cette fille a décidé d’aider son frère,</p>
<p>Pour que ce dernier guérisse.</p>
<p>C’est le miracle de la guérison d’un enfant, avec 1 dollar et 11 centimes.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
