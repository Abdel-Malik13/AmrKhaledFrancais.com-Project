                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui est celle d’un sportif,</p>
<p>Un coureur…</p>
<p>Un athlète britannique célèbre,</p>
<p>Il s’appelle Roger Bannister.</p>
<p>Cet homme a changé les lois de la physique…</p>
<p>Quel est votre avis ?</p>
<p>C’est un athlète, qui court,</p>
<p>Et il a changé les lois de la physique…</p>
<p>Comment ça ?</p>
<p>Avant 1954, les lois de la physique disaient que la capacité de course de l’être humain, ne pouvait pas atteindre 1 Mile (1609,34 mètres) en moins de 4 minutes.</p>
<p>C’est-à-dire courir le Mile en moins de 4 minutes, c’est impossible selon les lois de la physique qui existaient avant 1954…</p>
<p>Les physiciens ont fixé la capacité maximale de la vitesse de course de l’être humain.</p>
<p>Mais en 1954, Roger Bannister a battu ce record…</p>
<p>Et il a détruit cette loi des physiciens.</p>
<p>Il a couru le Mile en 3 minutes et demie…</p>
<p>Du coup il a prouvé que ce que disaient les physiciens était faux.</p>
<p>Cet homme a cassé la règle qui disait que les capacités de l’être humain ne lui permettaient pas de courir le Mile en moins de 4 minutes.</p>
<p>Alors les physiciens ont réagi en disant « Cet homme est une exception,</p>
<p>La règle tient toujours. »</p>
<p>Le souci c’est que juste après : 20.000 autres jeunes athlètes ont aussi couru le Mile en moins de 4 minutes…</p>
<p>Et même des gens normaux ont pu faire la même chose.</p>
<p>Roger Bannister n’était plus le seul,</p>
<p>Et tout le monde a reconnu que cette loi de la physique était fausse,</p>
<p>Car 20 mille personnes ont pu courir le Mile en moins de 4 minutes.</p>
<p>C’est devenu la nouvelle norme,</p>
<p>Et en effet les lois de la physique ont été changées.</p>
<p>Mais pourquoi je vous raconte cette histoire ?</p>
<p>Ce que je veux vous dire c’est :</p>
<p>Ces 20.000 personnes,</p>
<p>Et d’autres… Et d’autres… Et d’autres…</p>
<p>Pensaient que c’était impossible de courir en moins de 4 minutes,</p>
<p>Car les lois de la physique disent ça…</p>
<p>Jusqu’à ce qu’un seul arrive,</p>
<p>1 qui croyait en lui-même,</p>
<p>Et qui ne croyait pas en ce qui est écrit dans les livres…</p>
<p>Quelqu’un qui a essayé, au lieu de dire que c’est impossible,</p>
<p>Il s’est entraîné,</p>
<p>Il a fait des efforts.</p>
<p>Puis après lui, 20.000 autres personnes ont pu faire la même chose que lui…</p>
<p>Mais pourquoi ces 20.000 personnes ne sont pas apparues dès le départ ?</p>
<p>Car les gens n’y croient pas…</p>
<p>Les gens ne pensent pas qu’ils peuvent,</p>
<p>Jusqu’à ce qu’ils voient quelqu’un qui réussit.</p>
<p>Et toi, peux-tu croire que tu es capable de réussir ?</p>
<p>Peux-tu croire que tu peux inspirer les gens ?</p>
<p>Pourquoi on ne fait tous « qu’imiter »…</p>
<p>Pourquoi personne d’entre nous, ne se met à faire une nouvelle chose tout seul ?</p>
<p>Pourquoi on reste les bras croisés ?</p>
<p>Pourquoi nos cerveaux sont devenus programmés avec l’idée « d’imiter » ?</p>
<p>Et pas avec la créativité, et le désir d’inventer des nouvelles choses…</p>
<p>Roger Bannister était comme ça,</p>
<p>Il a sorti une nouvelle chose,</p>
<p>Il ne s’est pas soumis à ce qui existait déjà…</p>
<p>Imaginez si les jeunes de nos pays se mettent à raisonner comme lui ?</p>
<p>Que ça devienne leur façon de vivre et de réfléchir :</p>
<p>J’invente de nouvelles choses, et je ne dis pas « ça ne sert à rien… ».</p>
<p>Qu’est ce qui va se produire ?</p>
<p>D’abord : Nos pays vont beaucoup se développer,</p>
<p>Ensuite : Chaque jeune deviendra entrepreneur, et sortira de nouvelles idées,</p>
<p>Au lieu de dire « Si ça pouvait marcher, quelqu’un l’aurait fait avant… ».</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
