                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui dit : Le bien dure pour toujours, et le mal dure pour toujours.</p>
<p>Ça veut dire quoi ?</p>
<p>Ça veut dire que le bien est un cercle,</p>
<p>Il commence dans un point,</p>
<p>Il fait le tour,</p>
<p>Et il finit par revenir à son point de départ.</p>
<p>Autrement dit : Le bien que tu fais, finit toujours par t’être rendu.</p>
<p>Le bien est un cercle,</p>
<p>Je crois que Dieu a créé l’univers de cette façon.</p>
<p>Es-tu convaincu par ce que je suis en train de dire ?</p>
<p>Que le bien est un cercle,</p>
<p>Que le bien que tu fais, finit par t’être rendu.</p>
<p>Et c’est pareil avec le mal…</p>
<p>Le bien est un cercle,</p>
<p>Et le mal est un cercle.</p>
<p>L’histoire d’aujourd’hui date d’il y a 1 an,</p>
<p>Elle est arrivée à un ami à moi,</p>
<p>Toute l’histoire se passe entre lui et entre une de ses professeurs à l’université.</p>
<p>Il lui a demandé de l’aide pour un de ses projets,</p>
<p>Alors elle l’a beaucoup aidé,</p>
<p>Elle lui a ouvert son bureau,</p>
<p>Et ses collègues professeurs l’aidaient aussi.</p>
<p>Donc cette femme l’a beaucoup aidé… C’est une partie du cercle.</p>
<p>Cette femme rêvait de partir faire une Omra (petit pèlerinage), mais elle n’arrivait pas à faire les démarches etc.</p>
<p>Alors il lui a fait ses démarches, et il l’a aidée à partir en Omra.</p>
<p>Puis quand elle est revenue,</p>
<p>Elle a voulu le remercier,</p>
<p>Elle l’a invité à manger, lui, son épouse, son papa et sa maman,</p>
<p>Elle tenait à le remercier pour l’avoir aidé.</p>
<p>Et pendant qu’ils mangeaient et discutaient,</p>
<p>Il lui a demandé « Quels sont tes projets ».</p>
<p>Elle lui a dit « Je suis en train de construire une mosquée, mais financièrement ça bloque. Et si ça n’avance pas je risque de perdre l’autorisation ».</p>
<p>Il lui a demandé « Combien il te faut ? »</p>
<p>(Il n’avait pas d’argent)</p>
<p>Elle lui a répondu « Pour faire avancer les travaux, il faut pour le moment 20 ou 30 mille euros.</p>
<p>En réalité il faudrait 100 mille euros pour finir, mais on doit déjà commencer »</p>
<p>Il lui a dit « Si Dieu le veut, je te trouverai l’argent »</p>
<p>Il n’avait aucune idée sur comment il allait faire…</p>
<p>Mais le jour même, dans la famille d’un de ses amis lointains, il y avait des obsèques,</p>
<p>« J’y vais… Ou je n’y vais pas… Ça ne changerait rien de toute façon… »</p>
<p>Mais le bien a bougé dans son cœur,</p>
<p>« Non, je vais aller leur présenter mes condoléances ! »</p>
<p>Il était connu pour être quelqu’un qui aimait faire le bien,</p>
<p>Alors le mari de la femme décédée lui a dit</p>
<p>« Je veux faire quelque chose de bien pour mon épouse décédée,</p>
<p>J’ai envie de participer à la construction d’une mosquée »</p>
<p>Il l’a alors mis en relation le même jour avec la professeur de l’université,</p>
<p>L’homme lui a donné un chèque de 30.000 euros,</p>
<p>Elle allait enfin pouvoir lancer la construction.</p>
<p>La professeur a pleuré tellement elle était émue,</p>
<p>Et lui aussi, il a pleuré…</p>
<p>Mes chers, le bien est un cercle,</p>
<p>Dans l’histoire que j’ai racontée, le cercle s’est fermé en 1 mois…</p>
<p>Mais le cercle peut se fermer avec tes enfants,</p>
<p>Tu peux faire le bien, et il sera rendu à tes enfants.</p>
<p>Et c’est pareil avec le mal, c’est aussi un cercle,</p>
<p>C’est Dieu qui le dit dans le Coran :</p>
<p><strong>{ La manœuvre perfide n'enveloppe que ses propres auteurs. }</strong></p>
<p>Sourate 35 - Fâtir : Le Créateur (verset 43)</p>
<p>Perfide = Trompeur et mauvais</p>
<p>Quand tu fais du mal, c’est obligé qu’il fasse le tour et qu’il te revienne…</p>
<p>Es-tu convaincu que le bien est un cercle ?</p>
<p>Fais le bien en étant rassuré.</p>
<p>Et es-tu convaincu que le mal est un cercle ?</p>
<p>Ne fais surtout pas de mal, car il te sera rendu.</p>
<p>Vis en faisant le bien, et tu verras un sourire d’espoir dans ta vie et dans la vie de tes enfants.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
