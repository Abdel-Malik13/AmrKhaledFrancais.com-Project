                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui est une histoire qui date de 1890,</p>
<p>C’est une histoire ancienne,</p>
<p>Et c’est une histoire qui a un rapport avec l’Art.</p>
<p>Quand tu étais artiste-peintre avant 1890,</p>
<p>Il fallait que tu fasses des dessins réalistes,</p>
<p>Tout l’Art était de cette façon,</p>
<p>C’est-à-dire si tu voulais dessiner un arbre, il fallait que ton dessin soit réaliste,</p>
<p>Si tu dessinais une fleur, il fallait qu’elle soit réaliste…</p>
<p>Si tu dessinais le visage de quelqu’un, il fallait qu’il soit réaliste…</p>
<p>Tout l’art était basé sur le mot « Réalisme ».</p>
<p>Puis une grande exposition a été organisée en France,</p>
<p>Dans cette exposition, les tableaux de beaucoup d’artistes allaient être exposés,</p>
<p>Et bien sûr tous ces tableaux étaient dessinés d’une façon réaliste.</p>
<p>Puis un groupe d’artistes sont venus proposer leurs tableaux pour l’exposition,</p>
<p>Mais ils ont tous été refusés…</p>
<p>Et quelle est la raison de ce refus ?</p>
<p>Leurs tableaux ne correspondaient pas aux critères artistiques…</p>
<p>On leur a dit que ce n’était pas de l’Art tout court.</p>
<p>Qui étaient les gens de ce groupe ?</p>
<p>C’étaient les 1ers à faire de l’Art abstrait,</p>
<p>C’est-à-dire leurs dessins n’étaient pas réalistes,</p>
<p>Et tous les dessins non-réalistes qu’on voit aujourd’hui, leur point de départ était cette anecdote en 1890.</p>
<p>Les responsables de l’exposition leur ont dit « Vos tableaux sont refusés, et vous êtes interdits de les exposer »</p>
<p>Et qu’ont-ils-fait ?</p>
<p>Ils ont mis en place une tente, juste en face de l’exposition,</p>
<p>Et ils ont mis un panneau « L’exposition des tableaux refusés ».</p>
<p>À votre avis, laquelle des 2 expositions a été le plus visitée ?</p>
<p>Les gens sont curieux,</p>
<p>Ils voulaient voir à quoi ressemblaient les tableaux refusés…</p>
<p>Résultat : L’exposition des tableaux refusés, a été beaucoup plus visitée que l’exposition des tableaux classiques,</p>
<p>Car c’était une chose nouvelle, une chose différente…</p>
<p>Et l’Art abstrait s’est répandu dans le monde.</p>
<p>Puis dans l’exposition de l’année suivante, le nombre de tableaux abstraits était plus élevé que le nombre de tableaux classiques...</p>
<p>Le visage de l’Art a changé dans le monde.</p>
<p>Et qui était parmi ce groupe d’artistes dont les tableaux ont été refusés ?</p>
<p>Parmi eux il y avait le célèbre Pablo Picasso…</p>
<p>Pourquoi je raconte cette histoire ?</p>
<p>Je raconte cette anecdote pour te dire :</p>
<p>N’aie surtout pas peur, si tu as une nouvelle idée,</p>
<p>Ne laisse pas les autres te dire que ton idée n’entre pas dans les critères, alors tu l’enterres, tu dis que ça ne marchera pas, et tu te tais…</p>
<p>Non !</p>
<p>Mes chers, comment le monde change, si ce n’est avec des idées nouvelles ?</p>
<p>C’est comme si je dis aux papas et aux mamans : Si vos enfants ont une nouvelle idée, ne riez surtout pas !</p>
<p>Ne vous moquez pas d’eux.</p>
<p>Et je dis aux jeunes : Si une nouvelle idée nait dans ta tête,</p>
<p>Souviens-toi que Pablo Picasso, un des plus grands artistes au monde, était parmi le groupe qui s’est fait rejeter…</p>
<p>Imagine s’il s’était dit « Ce que je fais est différent, ça ne peut pas réussir »</p>
<p>Mes chers, ceux qui font des choses différentes sont ceux qui réussissent à l’avenir,</p>
<p>Car ils n’attendent pas dans une file d’attente…</p>
<p>Ils créent un chemin rien que pour eux.</p>
<p>Donc si tu as une nouvelle idée, que tu y crois,</p>
<p>(Ça ne veut pas dire être différent juste pour être différent… Il faut être sérieux)</p>
<p>Mais si tu es convaincu que ta nouvelle idée est utile,</p>
<p>Alors n’aie pas honte des moqueries des autres.</p>
<p>Pousse-la vers l’avant,</p>
<p>Et fais des efforts pour la développer,</p>
<p>Propose-la une fois, puis 2, puis 3…</p>
<p>Et crois-moi : Les belles choses, et les idées utiles… Aucun obstacle ne peut les bloquer.</p>
<p>Tu réussiras, tu réussiras !</p>
<p>Et à ce moment, un sourire d’espoir se dessinera, pas seulement pour toi, mais pour le monde entier qui profitera de ta nouvelle idée.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
