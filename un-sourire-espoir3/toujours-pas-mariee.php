                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est un message qu'on a reçu sur l'email de l'émission,</p>
<p>Parfois on reçoit des histoires magnifiques et touchantes,</p>
<p>Donc j'invite tous ceux qui ont des histoires intéressantes, à nous les envoyer sur</p>
<p>basmetamal[at]amrkhaled[point]net</p>
<p>L'histoire d'aujourd'hui c'est une fille appelée Loubna Abdel'Aalim qui nous l'a envoyée,</p>
<p>Elle raconte qu'elle connait une amie âgée de 34 ans..</p>.
<p>Son mariage a tardé,</p>
<p>À 34 ans elle n'était toujours pas mariée.</p>
<p>Et puis finalement quelqu'un s'est présenté,</p>
<p>Elle était bien sûr très très très contente,</p>
<p>Pour elle c'était une occasion en or (pour ne pas dire la dernière occasion).</p>
<p>Elle s'est donc beaucoup beaucoup beaucoup interessée à cet homme,</p>
<p>Mais elle a remarqué que la maman du fiancé n'était pas très contente...</p>
<p>Le fiancé avait à peu près le même âge qu'elle,</p>
<p>Du coup ils étaient tous les deux pressés de se marier,</p>
<p>Ils ont fixé une date pour le mariage,</p>
<p>Mais plus le jour du mariage approchait, plus la maman du fiancé avait l'air mécontente et frustrée...</p>
<p>Puis juste avant la date du mariage, la maman du fiancé à demandé à organiser une réunion,</p>
<p>Elle voulait que tout le monde soit présent :</p>
<p>Elle, son fils, la future mariée et sa maman,</p>
<p>Elle a rassemblé tout le monde dans une même pièce,</p>
<p>Puis elle a fait une chose qui a brisé les cœurs...</p>
<p>Mes chers, faites très attention,</p>
<p>L'épisode d'aujourd'hui dit : Briser les cœurs est une chose très grave pour Dieu !</p>
<p>Briser le cœur d'une personne ? C'est une catastrophe.</p>
<p>Si tu veux annuler un mariage, c'est ton droit,</p>
<p>Mais casser les cœurs ??</p>
<p>Cette femme a fait ça...</p>
<p>Elle a fait quoi ?</p>
<p>Pendant qu'ils étaient tous assis dans le salon,</p>
<p>Elle a dit à la future mariée « Je n'ai pas encore vu ta pièce d'identité... Peux-tu me la montrer ? »</p>
<p>La fille n'a pas compris...</p>
<p>Elle lui a demandé « Y a-t-il un problème ? »</p>
<p>La mère du fiancé lui a dit « Je veux juste la voir »</p>
<p>La fille lui a donné sa carte d'identité,</p>
<p>La mère du fiancé a lu la date de naissance marquée dessus, puis elle a dit</p>
<p>« Oulala tu as 34 ans ?</p>
<p>Non... Non... Non... Je ne savais pas que tu étais âgée à ce point.</p>
<p>J'aimerais que mon fils épouse une fille plus jeune que lui de 5 ans,</p>
<p>Et pas une femme de 34 ans, car j'ai envie d'avoir des petits enfants...</p>
<p>Je ne suis pas sûre que tu pourras tomber enceinte à ton âge »</p>
<p>Puis elle a pris son fils, et ils sont partis...</p>
<p>Son fils l'a suivi sans dire un mot.</p>
<p>À propos mes chers, obéir à nos parents ne signifie pas qu'on doit accepter les injustices ou accepter de briser les cœurs,</p>
<p>Le fils avait besoin de réagir mieux que ça dans cette situation.</p>
<p>Mais il a suivi sa mère, et ils sont partis.</p>
<p>La pauvre fille est entrée dans une dépression... Une dépression grave.</p>
<p>Elle a déprimé pendant très longtemps,</p>
<p>Puis elle a décidé de partir faire une Omra (petit pèlerinage),</p>
<p>Ses parents l'ont poussé à y aller, histoire de soulager sa douleur.</p>
<p>Elle est donc partie faire une Omra,</p>
<p>Elle a beaucoup pleuré, elle a fait beaucoup de Doua'a (prières),</p>
<p>Elle a beaucoup répété le verset du Coran qui dit <strong>{ Ne désespérez pas de la miséricorde d'Allah }</strong></p>
<p>Sourate 39 - Az-Zumar : Les Groupes</p>
<p>Puis elle a fini son petit pèlerinage, et elle est rentrée chez elle.</p>
<p>Pendant qu'elle descendait de l'avion, en voulant prendre ses valises,</p>
<p>Un homme est venu l'aider,</p>
<p>Elle a senti qu'il était très poli,</p>
<p>Elle a récupéré ses valises, puis elle est partie.</p>
<p>En sortant de l'aéroport, elle a trouvé sa sœur et le mari de sa sœur qui l'attendaient,</p>
<p>Elle est montée dans la voiture,</p>
<p>Puis le mari de sa sœur a dit « Attendez juste un peu, car un de mes amis était dans le même vol, on va le déposer »</p>
<p>Et qui était cet ami ?</p>
<p>C'était l'homme qui l'avait aidé avec ses valises,</p>
<p>Il est monté dans la voiture,</p>
<p>Ils ont fait connaissance,</p>
<p>Ils se sont attachés l'un à l'autre...</p>
<p>Ils se sont mariés,</p>
<p>Et la grande surprise c'est qu'elle est tombée enceinte d'un triplé...</p>
<p>Pas 1 enfant... Pas des jumeaux... Mais 3 d'un seul coup !</p>
<p>Elle les a appelés Al Hassan, Al Houssein et Fatima (Comme les prénoms des enfants du Prophète (bénédictions et paix soient sur lui)).</p>
<p>Cette fille est heureuse dans sa vie.</p>
<p>Mais il y a une autre surprise que je n'ai pas racontée...</p>
<p>L'homme qui l'avait abandonnée, s'est aussi marié,</p>
<p>Mais il n'a pas eu d'enfants jusqu'à maintenant...</p>
<p>Il a découvert qu'il était stérile.</p>
<p>Ne cassez surtout pas les cœurs des gens... Car Dieu (Exalté Soit-Il) se venge pour eux, et casse vos propres cœurs.</p>
<p>Un sourire d'espoir pour chaque être humain qui respecte les sentiments des autres,</p>
<p>Qui ne brise jamais leurs cœurs,</p>
<p>Et qui préserve leurs sentiments,</p>
<p>Pour que Dieu ne brise pas son propre cœur.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
