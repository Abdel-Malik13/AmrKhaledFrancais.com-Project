                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’histoire d’aujourd’hui vient du Yémen,</p>
<p>Cette histoire est arrivée à une fille au Yémen,</p>
<p>Et celui qui me l’a racontée est un professeur à l’école où cette fille étudiait.</p>
<p>Quelle est l’histoire ?</p>
<p>Il y a eu une inspection de la part du ministère de l’éducation,</p>
<p>Certaines écoles pour filles ont été choisies pour être inspectées,</p>
<p>Et plus précisément dans la ville de Sanaá (la capitale).</p>
<p>Le groupe d’inspecteurs visitaient les écoles une par une,</p>
<p>Puis ils sont arrivés dans l’école en question.</p>
<p>Le travail des inspecteurs c’est d’entrer dans les classes,</p>
<p>De vérifier que la tenue vestimentaire des filles est conforme,</p>
<p>Puis de vérifier le contenu de leurs sacs,</p>
<p>S’assurer qu’il n’y a pas de produits interdits dans les sacs ou dans les casiers.</p>
<p>C’est une opération administrative d’inspection,</p>
<p>Une chose habituelle.</p>
<p>Les inspecteurs sont entrés dans la classe où se trouvait cette fille,</p>
<p>Ils sont passés voir les filles une par une,</p>
<p>« Ouvre ton sac »,</p>
<p>Ils vérifiaient qu’il n’y avait rien d’interdit à l’intérieur,</p>
<p>Puis comme dans la classe il n’y avait que des filles,</p>
<p>Il n’y avait rien de gênant à ouvrir le sac devant les autres.</p>
<p>Puis les inspecteurs sont arrivés à cette fille,</p>
<p>« Ouvre ton sac »,</p>
<p>Impossible…</p>
<p>« Ouvre ton sac »,</p>
<p>Impossible, je ne vais pas l’ouvrir !</p>
<p>« Tu vas ouvrir ton sac, ça veut dire que tu vas l’ouvrir ! »</p>
<p>La fille s’accrochait à son sac,</p>
<p>« Lâche ton sac »</p>
<p>La fille ne voulait pas le lâcher,</p>
<p>Elle a commencé à crier,</p>
<p>Elle a pleuré,</p>
<p>C’est impossible que j’ouvre le sac ici !</p>
<p>Ils l’ont alors emmenée dans le bureau de la directrice,</p>
<p>Puis la fille s’accrochait à son sac d’une extrême violence…</p>
<p>Bien sûr les inspecteurs sont devenus encore plus insistants,</p>
<p>« Nous allons ouvrir ce sac ! »</p>
<p>La directrice à essayé de raisonner la fille,</p>
<p>« S’il te plait, fais ce qu’ils te demandent… »</p>
<p>À la fin, la fille a craqué en pleurant…</p>
<p>Les inspecteurs ont enfin pu ouvrir le sac,</p>
<p>Et qu’ont-ils trouvé à l’intérieur ?</p>
<p>Ils ont trouvé des bouts de pain,</p>
<p>Et des restes de nourriture à l’intérieur de son sac…</p>
<p>« Mais pourquoi tu refusais d’ouvrir ton sac ? »</p>
<p>Cette fille est issue d’une famille très pauvre,</p>
<p>Quand ses camarades finissaient de manger, elle récupérait les restes de leurs repas,</p>
<p>Elle les mettait en cachette dans son sac.</p>
<p>Car le soir elle ramène ça à ses frères et sœurs,</p>
<p>Sa famille est tellement pauvre qu’ils n’ont rien à manger à la maison…</p>
<p>Chaque fois qu’une de ses camarades laissait un bout de pain,</p>
<p>Elle attendait qu’elle parte,</p>
<p>Et elle le récupérait dans la poubelle,</p>
<p>Puis elle le mettait dans son sac…</p>
<p>Cette fille refusait d’ouvrir son sac, car elle avait honte que ses camarades de classe découvrent son secret.</p>
<p>Car elle donnait à manger à ses frères et sœurs en rentrant à la maison.</p>
<p>Cette petite fille qui a fait ça… Est une honte pour nous tous,</p>
<p>Nous n’aidons pas sérieusement les pauvres…</p>
<p>Nous ne dépensons pas sérieusement…</p>
<p>Est-ce possible que dans le monde, il y ait des gens qui souffrent à ce point ?</p>
<p>Est-ce possible qu’une enfant, dans une école, puisse être de cette noblesse et de cette pudeur,</p>
<p>Qu’elle ait pu ressentir cette blessure à son âge ?</p>
<p>Elle avait peur de se faire exposer devant ses camarades de classe.</p>
<p>Est-ce que chacun de nous veille sur l’état des gens autour de lui ?</p>
<p>Je raconte cette histoire pour te dire :</p>
<p>Peux-tu veiller sur la partie pauvre de ta famille,</p>
<p>Car parmi eux il se peut qu’une fille fasse la même chose…</p>
<p>Peux-tu veiller sur tes voisins… Car parmi eux, il se peut qu’il y ait des personnes dans le besoin.</p>
<p>Peux-tu veiller sur les gens autour de toi ?</p>
<p>S’il te plait commence par le petit cercle,</p>
<p>Je ne vais pas te demander de t’occuper des pauvres dans le monde…</p>
<p>Non, commence par ton entourage, commence par ton cercle,</p>
<p>Sinon pourquoi Dieu nous aurait recommandé en priorité les proches ?</p>
<p><strong>{ Ils t’interrogent: «Qu’est-ce qu’on doit dépenser?» Dis: «Ce que vous dépensez de bien devrait être pour les père et mère, les proches, les orphelins, les pauvres et les voyageurs indigents. }</strong></p>
<p>Coran : Sourate 02 - Al-Baqara : La Vache (verset 215)</p>
<br>
<p>Commence par tes proches, comme ça au moins tu auras fait quelque chose de positif.</p>
<p>En écoutant l’histoire de cette fille,</p>
<p>Je me demande si elle aurait pu être la mienne…</p>
<p>Et si c’était ma fille, et qu’elle se trouvait dans cette situation ?</p>
<p>Et si mes enfants ne sont pas dans cette situation,</p>
<p>Qu’est ce que je pourrais faire pour remercier Dieu ?</p>
<p>Regardez les personnes autour de vous, et aidez-les.</p>
<p>Pour qu’il reste un sourire d’espoir pour nos enfants, et pour nos petits enfants.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
