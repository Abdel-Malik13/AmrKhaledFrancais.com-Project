                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui vient du Ghana en Afrique,</p>
<p>D'une tribu appelée les Ashantis,</p>
<p>Cette tribu a une particularité étonnante,</p>
<p>Quelle particularité ?</p>
<p>Ils donnent à leurs enfants des prénoms en fonction du jour de la semaine où ils sont nés...</p>
<p>Ça veut dire quoi ?</p>
<p>Celui qui nait un Samedi, est appelé « Samedi »,</p>
<p>Mais pour eux le Samedi a un sens,</p>
<p>Et le Dimanche a un sens,</p>
<p>Et le Lundi a un sens...</p>
<p>Par exemple il y a un enfant appelé « Akwasi »</p>
<p>Akwasi signifie : Le Dimanche.</p>
<p>Et le Dimanche pour eux signifie : Le gentil, le tendre, le sensible.</p>
<p>Alors quand l'enfant grandit, tu es surpris de voir qu'il est vraiment tendre, sensible et gentil...</p>
<p>Cette tribu fonctionne de cette façon,</p>
<p>C'est une méthode unique au monde.</p>
<p>Et parmi les prénoms qu'ils ont, il y a « Kwako ».</p>
<p>Kwako signifie : Le Mercredi.</p>
<p>Et le Mercredi pour eux signifie : Le méchant, l'agressif.</p>
<p>Et tu es surpris d'apprendre que 60% de ceux qui commettent des crimes au Ghana, s'appellent « Kwako »...</p>
<p>Comment est-ce possible ?</p>
<p>Il y a quelque chose d'incompréhensible... N'est ce pas ?</p>
<p>Est-ce que c'est logique, parce que quelqu'un est né un Dimanche, et parce qu'il s'appelle « Akwasi », il devient quelqu'un de gentil et de sensible ?</p>
<p>Et d'un autre côté, parce que quelqu'un est né un Mercredi, et parce qu'il s'appelle « Kwako », il devient méchant et agressif ?</p>
<p>Imaginez une maman enceinte... Et qui prie pour que son enfant ne naisse pas un Mercredi.</p>
<p>Elle souhaite que son enfant naisse un Dimanche ou un Lundi...</p>
<p>Puis imaginez avec moi, si un enfant est né un Mercredi, et qu'il a commis une faute ?</p>
<p>Tout de suite ses parents vont lui dire « Évidemment, tu es né un Mercredi ! »</p>
<p>Quelle est cette histoire ?</p>
<p>Les sociologues en Afrique ont étudié le sujet,</p>
<p>Et ils ont donné une explication rationnelle.</p>
<p>Quelle est l'explication ?</p>
<p>Ils ont dit que les pensées négatives qui naissent dans les cerveaux des parents...</p>
<p>Ces derniers les transmettent à leurs enfants.</p>
<p>Alors l'enfant grandit en ayant certains défauts.</p>
<p>Non pas car le Mercredi est un mauvais jour,</p>
<p>Dieu a créé tous les jours de la semaine à égalité.</p>
<p>Et pareil pour le Dimanche, ce n'est pas un jour plus gentil ou plus tendre qu'un autre...</p>
<p>C'est uniquement à cause de leur méthode,</p>
<p>Chaque fois qu'un enfant commet une erreur, ils lui disent « C'est parce que tu es né un Mercredi ».</p>
<p>À l'intérieur de l'enfant, cette idée se fixe : « Même mon prénom est un prénom de méchant »</p>
<p>Alors il devient méchant effectivement.</p>
<p>Et au contraire, pour l'enfant né un Dimanche, les pensées positives se fixent dans sa tête...</p>
<p>« Regardez comment notre enfant est gentil, c'est parce qu'il est né un Dimanche »</p>
<p>Tu sèmes chez lui l'idée que c'est quelqu'un de gentil, que même son prénom est gentil.</p>
<p>Mais que pouvons-nous apprendre de cet épisode ?</p>
<p>Mes chers, l'être humain récolte ce qu'il sème comme pensées positives ou négatives dans son cerveau...</p>
<p>Pour faire court : Ton cerveau est comme un jardin,</p>
<p>Soit tu le remplis de fleurs, de roses et de belles odeurs...</p>
<p>Soit tu le remplis de pesticides, d'épines et de pierres...</p>
<p>Mes chers, en fonction de votre façon de voir la vie, cette dernière le deviendra pour vous.</p>
<p>Et en fonction de votre façon de voir vos enfants, ils le deviennent.</p>
<p>S'il vous plait mes chers,</p>
<p>Élevez vos enfants en leur transmettant des sens positifs,</p>
<p>N'élevez pas vos enfants avec des pensées négatives,</p>
<p>Ne dis pas à ton enfant « Tu es mauvais... Tu es fautif... Tu es médiocre à l'école... Tu es désobéissant... Tu es insupportable... Tu es un monstre... Tu es ceci... Tu es cela... »</p>
<p>Non...</p>
<p>Élevez vos enfants en leur disant « Je pense que tu vas devenir quelqu'un de très important »</p>
<p>Il deviendra effectivement quelqu'un d'important.</p>
<p>« La plus belle chose chez toi mon enfant, c'est ton sourire »</p>
<p>Il sera souriant toute sa vie.</p>
<p>Sème les pensées positives dans la tête de ton enfant, et il grandira en étant quelqu'un positif.</p>
<p>N'imite surtout pas la tribu des Ashantis !</p>
<p>Un sourire d'espoir pour chaque personne qui sème des idées positives dans sa tête, et dans la tête de ses enfants.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
