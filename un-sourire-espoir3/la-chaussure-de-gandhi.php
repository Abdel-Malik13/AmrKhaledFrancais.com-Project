                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui commence par une question :</p>
<p>Que ferais-tu, si tu courais pour monter dans un train,</p>
<p>Quand soudainement tu perds une de tes chaussures ?</p>
<p>Tu es monté dans le train avec une seule chaussure...</p>
<p>Quelle est la bonne réaction à avoir dans cette situation ?</p>
<p>Réfléchis un peu...</p>
<p>Tu cours pour rattraper ton train,</p>
<p>Et pendant que tu montes dans le train, tu perds une de tes chaussures,</p>
<p>Et les portes du train se ferment.</p>
<p>Laissez-moi vous raconter ceci :</p>
<p>Gandhi est un personnage très célèbre en Inde et dans le monde,</p>
<p>C'est un de ceux qui ont aidé l'Inde à prendre son indépendance,</p>
<p>Et quand j'étais en Inde, sur toutes les monnaies et les billets d'argent, il y avait la photo de Gandhi...</p>
<p>C'est-à-dire on ne voit la photo de personne d'autre,</p>
<p>Ni le président actuel,</p>
<p>Ni un ancien président,</p>
<p>Ni un ancien roi ou empereur...</p>
<p>Mes chers, sur les pièces et les billets d'argent en Inde, il y a la photo d'une seule personne : Gandhi.</p>
<p>Et pendant la fête d'indépendance, les indiens vont toujours visiter le mémorial de Gandhi.</p>
<p>Donc pour les indiens, Gandhi c'est quelqu'un de très très très important.</p>
<p>Je leur ai posé la question,</p>
<p>Je leur ai demandé « Pourquoi Gandhi est important chez vous à ce point ? C'est grâce aux choses qu'il a faites ? »</p>
<p>Ils m'ont répondu « Non, c'est parce qu'on l'a cru »</p>
<p>« On a cru à sa sincérité, alors on a écouté ses paroles »</p>
<p>Puis ils m'ont raconté l'histoire que je vais vous raconter tout de suite.</p>
<p>Pourquoi les indiens l'ont cru ?</p>
<p>C'est une des anecdotes qui les ont convaincus de sa sincérité.</p>
<p>Gandhi courait pour rattraper son train,</p>
<p>Et les gares en Inde sont difficiles,</p>
<p>Il y a beaucoup de monde etc.</p>
<p>Et pendant qu'il courait, une de ses chaussures est tombée,</p>
<p>Il n'avait plus qu'une seule chaussure,</p>
<p>Et le train avançait...</p>
<p>Tous les gens le regardaient,</p>
<p>Alors il a enlevé la 2ème paire,</p>
<p>Il a regardé par la fenêtre,</p>
<p>Il l'a jetée sur le quai,</p>
<p>Et il a demandé à un passant « S'il te plait, prends cette chaussure, et pose-la à côté de l'autre »</p>
<p>« Pose-la à côté de l'autre chaussure »</p>
<p>Les autres passagers du train lui ont posé la question</p>
<p>« Pourquoi tu as fait ça ? »</p>
<p>Il leur a répondu « Puisque j'ai déjà perdu une chaussure,</p>
<p>Alors je ne pourrais pas utiliser la 2ème.</p>
<p>Je me suis dit autant jeter la 2ème chaussure, pour que quelqu'un puisse porter les chaussures en entier.</p>
<p>Comme ça j'aurais aidé quelqu'un à avoir des chaussures. »</p>
<p>Ils lui ont dit « Mais tu pouvais demander au train de s'arrêter,</p>
<p>Ou rien qu'en utilisant ton grand nom, tu pouvais demander au responsable de la gare de récupérer la chaussure, et te l'envoyer avec le train suivant »</p>
<p>Gandhi leur a répondu « Tant qu'elle s'est retirée de mon pied, c'est comme un signe : Aide un autre être humain »</p>
<p>En vérité, malgré la simplicité de cette histoire, elle nous apprend une chose très importante :</p>
<p>Quand les gens te croient, à ce moment ils te soutiennent,</p>
<p>À ce moment ils te suivent,</p>
<p>Et c'est de cette façon que certaines personnes restent gravées dans les mémoires.</p>
<p>Tu veux que les employés écoutent ce que tu leur dis, si tu es le patron d'une entreprise ?</p>
<p>Il faut d'abord qu'ils croient en ta sincérité.</p>
<p>Tu veux que tes enfants écoutent ce que tu leur dis, et qu'ils t'obéissent ?</p>
<p>Il faut d'abord qu'ils te croient.</p>
<p>Tu veux que les gens t'aiment, et fassent ce que tu leur demande ?</p>
<p>Il faut d'abord qu'ils croient en ta sincérité.</p>
<p>Et ils ne vont croire en ta sincérité, que s'ils voient des actes concrets de ta part,</p>
<p>Qui leur montrent que tu es prêt à les aider,</p>
<p>Que tu penses à eux,</p>
<p>Et que tu penses à leurs intérêts.</p>
<p>À ce moment tu vivras avec un sourire d'espoir,</p>
<p>Et ton souvenir restera gravé chez les gens.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
