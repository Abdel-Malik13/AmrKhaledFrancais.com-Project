                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui vient de la Corée du sud,</p>
<p>C'est une histoire qui parle de la tricherie chez les coréens,</p>
<p>Tricher dans les écoles et dans les examens...</p>
<p>Celui qui m'a envoyé cette histoire est un jeune appelé Hosam,</p>
<p>Il est parti faire son doctorat en Corée du sud,</p>
<p>Et il m'a envoyé son histoire par email.</p>
<p>Il m'a dit : C'est ma 1ère année ici,</p>
<p>Et c'est la période des examens...</p>
<p>Puis là où je passe mon stage, j'ai un camarade coréen qui étudie aussi à l'université,</p>
<p>Je l'ai vu aujourd'hui, il était mal à l'aise...</p>
<p>Il était stressé et préoccupé,</p>
<p>Je lui ai demandé « Ça va ? »</p>
<p>Il m'a répondu « Oui ça va, mais je suis en train de passer un examen, et je dois rendre ma copie à 18h »</p>
<p>Il était assis à côté de moi...</p>
<p>Je lui ai répondu « Tu veux dire que tu es en train de faire un rapport de ton stage »</p>
<p>Il m'a dit « Non, c'est l'examen final, la copie qui sera notée »</p>
<p>Je ne comprenais pas...</p>
<p>Je me suis demandé : Est ce qu'il y a une fuite dans les questions des examens en Corée ?</p>
<p>Mais quand il m'a expliqué plus en détail,</p>
<p>J'ai compris que son professeur à l'université, fait passer à ses étudiants des examens du genre « Go home exams »</p>
<p>Ça signifie quoi « Go home exam » ?</p>
<p>Ça signifie que tu prends la feuille des questions à 10h du matin,</p>
<p>Puis tu repars dans ta chambre, ou dans la bibliothèque,</p>
<p>Et tu réponds aux questions.</p>
<p>Mais bien sûr il est interdit de demander de l'aide à quelqu'un, ou de chercher les réponses dans un livre.</p>
<p>Et quand tu finis de répondre aux questions, tu viens remettre ta copie au professeur à 18h.</p>
<p>Hosam m'a dit : Je n'arrivais pas à comprendre...</p>
<p>Mon camarade coréen me racontait quelque chose que je ne saisissais pas,</p>
<p>Il me disait que l'examen était composé de (seulement) 4 questions,</p>
<p>Il a réussi à répondre à 2 questions et demie,</p>
<p>Mais il est bloqué pour le reste...</p>
<p>Hosam m'a dit : J'étais très très très confus,</p>
<p>Je voulais comprendre à tout prix.</p>
<p>Alors Hosam est parti voir le professeur de son camarade,</p>
<p>Et il lui a dit : « Je ne comprends pas ce que vous faites,</p>
<p>Partout en Corée du sud, les examens sont passés de la même manière,</p>
<p>Pourquoi ? »</p>
<p>Le professeur lui a donné une réponse très simple,</p>
<p>Mais une réponse qui fait mal...</p>
<p>Qui fait mal à qui ? Qui fait mal à nous.</p>
<p>Il lui a répondu « On est en train de construire la Corée.</p>
<p>Le savoir pour nous c'est un plaisir,</p>
<p>Ce n'est pas un lourd fardeau pour obtenir un diplôme.</p>
<p>Et celui qui obtient son diplôme sans avoir assimilé des connaissances, ne trouvera pas de travail,</p>
<p>Aucune entreprise ne l'embauchera...</p>
<p>Il aura perdu des années de sa vie pour rien.</p>
<p>Car la Corée est en train d'être construite avec des gens qui ont des connaissances,</p>
<p>Pas avec des gens qui ont de grandes notes. »</p>
<p>Hosam m'a dit :</p>
<p>J'ai senti que j'avais reçu une claque...</p>
<p>J'ai eu très mal,</p>
<p>Et j'ai compris que dans nos pays on est dans le faux...</p>
<p>« Pour construire la Corée,</p>
<p>Le savoir c'est un plaisir, et non pas un lourd fardeau pour obtenir un diplôme »</p>
<p>Mes chers,</p>
<p>Trichez ou ne trichez pas...</p>
<p>Votre pays ne sera construit qu'avec des gens qui possèdent des compétences.</p>
<p>Un sourire d'espoir pour un pays qui aime le savoir, pour pouvoir se reconstruire de nouveau.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
