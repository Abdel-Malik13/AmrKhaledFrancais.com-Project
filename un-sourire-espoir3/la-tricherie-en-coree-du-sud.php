<p>A smile of hope today is about cheating
in the schools and universities in Korea,</p>

<p>the story was sent to me by an assistant
professor in the faculty of Engineering</p>

<p>and he is doing his PHD in Korea
his name is Hossam Rakayb,</p>

<p>and he sent this story on the email
of Basmit Amal (A Smile of Hope).</p>

<p>He says that it is his first semester
and he is having exams now,</p>

<p>he says I have a Korean colleague
in the same lab and office</p>

<p>but he is not taking the same subject
like me,</p>

<p>I have found him today very tensioned,</p>

<p>I asked him if he is alright,
and he said I am fine,</p>

<p>I am just having an exam right now
and I have to submit it at 6 PM.</p>

<p>He was sitting with me in a coffee shop,</p>

<p>and I asked him are you solving a quiz
or an exercise or something,</p>

<p>but he said no it is my final exam.</p>

<p>I laughed secretly and I said
to myself:</p>

<p>"Oh my God they have leakage for
exams papers also like us".</p>

<p>But he explained to me that their professor
make a "Go home exam".</p>

<p>What is "Go Home Exam"?,</p>

<p>The professor give them the exam at 10 AM,</p>

<p>then you go to your office, or the library
and start working on it,</p>

<p>but of course it is not allowed to ask your
colleagues, or look at any reference or book.</p>

<p>When you finish, you submit the exam
answers to the professor at 6 PM.</p>

<p>" I was really surprised and I didn’t
understand the concept,</p>

<p>my friend had solved 2 and half questions
out of four</p>

<p>and he is having troubles in solving
the rest of them.</p>

<p>I really wanted to understand,
so I went to the professor,</p>

<p>and I asked him:
-" How you are doing this?</p>

<p>Why all the exams in Korea in the same way?"</p>

<p>The professor answered simply
– But painfully for me –</p>

<p>-"We are building Korea, science for us
is enjoyment,</p>

<p>and not a heavy burden to get a certificate,</p>

<p>and if people succeed without understanding
they will not find a job anyway,</p>

<p>so they have just wasted their time.</p>

<p>To build Korea we have to have people
who understand not people with high grades."</p>

<p>His words hurt me deeply;
I felt that we don’t understand</p>

<p>"Science is for enjoyment, to build Korea,
and not a heavy burden to get a certificate".</p>

<p>People, cheat in exams or don’t cheat;</p>

