                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit que la vie est courte,</p>
<p>Et nous apprend comment réagir dans cette vie courte, quand on perd des proches...</p>
<p>La solution c'est : La satisfaction et la patience.</p>
<p>Cette vie courte a besoin que tu sois patient,</p>
<p>Et elle a besoin que tu sois satisfait de ce que Dieu te donne,</p>
<p>Pour que tu saches vivre,</p>
<p>Et car la vie peut être dure et difficile,</p>
<p>Dedans il y a beaucoup de séparations,</p>
<p>Et beaucoup de décès de nos proches...</p>
<p>Mais à la fin tu n'es que de passage sur terre, alors patiente et aie une satisfaction.</p>
<p>L'histoire d'aujourd'hui est avec Jésus (paix sur lui),</p>
<p>Une femme est venue le voir car son fils est décédé...</p>
<p>Et y a-t-il une catastrophe qui fait plus mal que de perdre un enfant ?</p>
<p>Cette femme avait très mal,</p>
<p>Et Dieu a donné à Jésus des capacités miraculeuses.</p>
<p>Cette femme est donc partie voir Jésus en pleurant,</p>
<p>Elle le suppliait de redonner la vie à son fils,</p>
<p>« Fais revenir mon fils... Fais revenir mon fils par n'importe quel moyen »,</p>
<p>Jésus lui a dit des paroles très belles, et remplies de sagesse...</p>
<p>Il lui a dit</p>
<p>« Va dans chaque maison du village,</p>
<p>Et prends dans chacune d'entre elles un grain de riz,</p>
<p>Et je ferai revenir ton fils »</p>
<p>Juste ça ?</p>
<p>C'est très facile !</p>
<p>Mais il lui a donné une condition,</p>
<p>Laquelle ?</p>
<p>« Ne prends pas un grain de riz chez une famille qui a perdu un membre.</p>
<p>Mais chez les familles où personne n'est décédé, prends autant de riz que tu veux »</p>
<p>La femme était pleine d'espoir, il y avait une chance pour qu'elle retrouve son fils,</p>
<p>Elle est partie frapper aux portes de chaque maison du village,</p>
<p>« C'est possible de me donner un grain de riz pour que je retrouve mon fils ? »</p>
<p>Les habitants lui répondaient</p>
<p>« Oui bien sûr ! Aucun problème »</p>
<p>Puis la femme leur disait « Par contre la seule condition c'est que vous n'ayez perdu aucun de vos proches »</p>
<p>« Non, désolés l'année dernière on a perdu un cousin... »</p>
<p>« Non, désolé mon papa est décédé il y a 6 mois... »</p>
<p>« Non, désolé ma maman est décédée il y a 2 ans... »</p>
<p>« Non, désolé mon fils est décédé... »</p>
<p>« Non, désolé j'ai perdu quelqu'un... »</p>
<p>La femme a fait le tour du village, et elle n'a pas obtenu 1 seul grain de riz...</p>
<p>Elle n'a pas trouvé une seule famille qui n'a pas perdu quelqu'un,</p>
<p>Dans chaque maison quelqu'un est décédé...</p>
<p>Elle a compris ce que Jésus voulait lui dire.</p>
<p>Elle est revenue le voir,</p>
<p>Et lui a dit</p>
<p>« Ne fais pas revenir mon fils, mais apprends-moi la patience et la satisfaction ».</p>
<p>Jésus lui a répondu</p>
<p>« Toi, apprends à toi-même la patience en patientant »</p>
<p>C'est presque la même parole du Prophète Mohamed (bénédictions et paix soient sur lui) quand il a dit <strong class="prophete">« La patience s'apprend en patientant »</strong>.</p>
<p>Jésus a dit à cette femme</p>
<p>« Toi, apprends à toi-même la patience en patientant,</p>
<p>Et toi, apprends à toi-même la satisfaction jusqu'à ce que tu deviennes satisfaite »</p>
<p>Les gens,</p>
<p>Mes chers,</p>
<p>La vie est très courte,</p>
<p>Elle passe très vite...</p>
<p>Sa valeur n'est pas grande,</p>
<p><strong>{ Alors que l'au-delà est meilleur et plus durable }</strong></p>
<p>Sourate 87 - Al-A'lâ : Le Très-Haut (verset 17)</p>
<p>Si tu souffres dans cette vie,</p>
<p>Pour garder un sourire d'espoir,</p>
<p>Je te recommande : La patience et la satisfaction.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
