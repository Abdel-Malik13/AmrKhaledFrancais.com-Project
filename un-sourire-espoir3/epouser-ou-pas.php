                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de la sagesse du destin,</p>
<p>Mes chers, le destin de Dieu est entièrement miséricorde.</p>
<p>Le destin de Dieu est entièrement bien.</p>
<p>Mais parfois on se fâche… Car on ne voit que la scène dans laquelle nous sommes en ce moment,</p>
<p>Nous ne voyons pas le film jusqu’à sa fin…</p>
<p>Nous ne voyons pas l’histoire jusqu’au bout.</p>
<p>Tu prends une scène et tu te fâches.</p>
<p>Tu dis « Pourquoi il m’est arrivé ceci »…</p>
<p>« Pourquoi j’ai perdu ce bonheur »…</p>
<p>Mais ça te dit de voir la fin ?</p>
<p>Au bout tu verras que ce qui t’est arrivé c’est entièrement miséricorde.</p>
<p>Puis quand les années passent, et que tu repenses au moment où tu as pleuré,</p>
<p>Tu dis « C’est magnifique… C’est la meilleure chose qui me soit arrivée ».</p>
<p>Le destin de Dieu est entièrement miséricorde,</p>
<p>Mais ce que tu ne vois pas, et ce qui est caché… Parfois Dieu le fait savoir à quelqu’un.</p>
<p>Comment ça ?</p>
<p>Je vous raconte l’histoire :</p>
<p>C’est un jeune qui a un ami médecin,</p>
<p>Les deux sont très amis,</p>
<p>Ils vivent dans le même quartier,</p>
<p>Et peut-être dans la même rue.</p>
<p>Ce jeune a beaucoup beaucoup beaucoup aimé une fille qui vit dans le même quartier.</p>
<p>C’est une jolie fille,</p>
<p>Et ils sont voisins,</p>
<p>Les immeubles sont les uns à côté des autres…</p>
<p>Cette fille est très belle,</p>
<p>Il s’est attaché à elle,</p>
<p>Et dès qu’il a fini ses études, il a décidé de l’épouser.</p>
<p>Mais son ami lui a dit « Ne l’épouse surtout pas. »</p>
<p>Le jeune lui a demandé « Et pourquoi ? »</p>
<p>Son ami lui a dit « On est voisins… Et je ne te dirai pas plus que ça.</p>
<p>Juste ne l’épouse pas »</p>
<p>Mais je l’aime beaucoup… J’avais envie de l’épouser… Et dès que je la vois marcher dans la rue… Etc.</p>
<p>Vous connaissez l’amour et les sentiments chez les jeunes.</p>
<p>« Je veux l’épouser »</p>
<p>Mais la réponse de son ami était pleine de fermeté,</p>
<p>« Je te dis ne l’épouse surtout pas ».</p>
<p>Et son ami était très réservé.</p>
<p>Ce jeune s’est trouvé face à un dilemme,</p>
<p>Entre quelque chose dont il avait très envie…</p>
<p>Et entre la parole de son ami « Ne l’épouse surtout pas », qui lui a mis un doute…</p>
<p>C’est sûr qu’il a vu quelque chose de mauvais dans le comportement de cette fille, mais il ne veut pas l’exposer car nous sommes voisins.</p>
<p>L’essentiel c’est qu’à la fin, il a décidé de ne pas l’épouser.</p>
<p>Les jours ont passé,</p>
<p>Il a épousé une autre fille.</p>
<p>Il a eu 3 enfants,</p>
<p>Il a formé sa famille,</p>
<p>Et avec son épouse il a mené une vie heureuse.</p>
<p>Et il est resté en contact avec son ami.</p>
<p>Puis la fille qu’il voulait épouser, a fini par se marier,</p>
<p>Ça y est, chacun a déménagé, et a fait sa vie de son côté.</p>
<p>Puis après plusieurs années,</p>
<p>Il s’est assis avec son ami,</p>
<p>Et dans un moment de pureté.</p>
<p>Il lui a dit « Tu te souviens quand tu m’as demandé de ne pas épouser cette fille ? »</p>
<p>Son ami lui a répondu « Oui bien sûr ».</p>
<p>Il lui a dit « Je jure par Dieu,</p>
<p>Que j’ai respecté ton choix de ne pas vouloir m’expliquer,</p>
<p>Car on était voisins.</p>
<p>Mais maintenant je veux que tu me dises honnêtement pourquoi tu ne voulais pas que je l’épouse avec autant de fermeté.</p>
<p>Que voyais-tu de mauvais dans son comportement ?</p>
<p>Elle faisait quoi de mal ?</p>
<p>Juste pour que je comprenne »</p>
<p>Son ami lui a répondu « Mais je n’ai jamais dit qu’elle faisait quelque chose de mal.</p>
<p>Cette fille est formidable »</p>
<p>Il lui a dit « Pardon ???</p>
<p>Tu viens me dire maintenant que cette fille est formidable ? »</p>
<p>Son ami lui a répondu « Oui je le jure par Dieu,</p>
<p>Cette fille est magnifique,</p>
<p>Son comportement est exemplaire »</p>
<p>Il lui a dit « Et pourquoi tu m’as empêché de l’épouser ? »</p>
<p>Son ami lui a répondu « As-tu oublié que j’étais médecin ?</p>
<p>Cette fille faisait des analyses,</p>
<p>Et on connaissait sa situation,</p>
<p>Elle est stérile…</p>
<p>Et je sais que toute ta vie tu meurs d’envie de devenir papa,</p>
<p>Même quand on était des jeunes à l’université, personne à part toi ne répétait ‘J’ai envie d’avoir des enfants’ comme toi tu le faisais.</p>
<p>J’ai fait ça par miséricorde envers toi, et envers elle…</p>
<p>Car tu allais lui compliquer la vie,</p>
<p>Vous alliez vous détester,</p>
<p>Et vous auriez fini par divorcer.</p>
<p>Voilà pourquoi je t’ai demandé de ne pas l’épouser.</p>
<p>Puis regarde, tu t’es marié, et tu as eu 3 enfants.</p>
<p>Et de son côté elle s’est aussi mariée »</p>
<p>Il lui a dit « Oui c’est vrai ! Et elle n’a toujours pas d’enfants »</p>
<p>Son ami lui a répondu « Oui, elle n’aura sûrement pas d’enfants,</p>
<p>Mais elle est heureuse avec son mari,</p>
<p>Car il l’aime comme elle est.</p>
<p>Et toi, tu aimes ta nouvelle épouse comme elle est. »</p>
<p>Il lui a dit « C’est magnifique… »</p>
