                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle d’un des plus grands jours de l’année…</p>
<p>Non c’est le plus grand jour de l’année !</p>
<p>Le sourire d’espoir d’aujourd’hui est sur le jour d’Arafat.</p>
<p>C’est le jour où Dieu (Exalté Soit-Il) pardonne le plus à Ses créatures…</p>
<p>Que Dieu nous comble de Sa miséricorde, et qu’Il nous pardonne.</p>
<p>Ce jour là, mes chers n’est pas seulement pour les pèlerins,</p>
<p>Ce jour là, n’est pas seulement pour les gens qui sont sur le mont Arafat (à la Mecque),</p>
<p>Ce jour là, est pour chaque être humain, musulman, croyant,</p>
<p>Qui se dirige vers Dieu avec son cœur, avec sa foi, avec ses Doua’a (Prières), avec ses souhaits,</p>
<p>Avec sa loyauté, avec son jeûne…</p>
<p>Le Prophète (bénédictions et paix soient sur lui) nous a dit <strong class="prophete">« Il n’existe pas un jour où Dieu pardonne autant à Ses créatures, comme pendant le jour d’Arafat »</strong>.</p>
<p>C’est le jour de l’année, où les péchés sont le plus pardonnés,</p>
<p>Imaginez…</p>
<p>Des centaines de péchés… Des milliers de péchés… Dieu les pardonne en ce jour.</p>
<p>Le Prophète (bpsl) nous a aussi dit <strong class="prophete">« Le jeûne du jour d’Arafat, efface les péchés de l’année précédente, et de l’année prochaine »</strong>.</p>
<p>Imagine… Tout ce que tu as fait de mal est effacé si tu jeûnes ce jour, mais aussi tout le mal que tu pourrais faire l’année prochaine.</p>
<p>Et le Prophète (bpsl) nous a également dit <strong class="prophete">« Il n’y a pas un jour où le diable est vu humilié, et dans un aussi mauvais état… Comme pendant le jour d’Arafat.</strong></p>
<p><strong class="prophete">il (le diable) met la poussière sur sa tête et dit : ‘je les ai égarés pendant une année entière, et Dieu leur pardonne en 1 seul jour’ ».</strong></p>
<p>Tout le travail que le diable a fait pendant l’année est détruit…</p>
<p>C’est un jour où Dieu répand Sa miséricorde,</p>
<p>C’est un jour où Dieu répand Son pardon.</p>
<p>Mes chers, le jour d’Arafat pourrait être plus fort que la nuit du Destin,</p>
<p>Car nous ne connaissons pas avec certitude laquelle des 10 dernières nuits du Ramadan est celle du Destin…</p>
<p>Mais on connait précisément quel jour Arafat tombe dans l’année.</p>
<p>C’est le jour du pardon, et le jour de la grâce.</p>
<p>Mes chers, que celui qui a un péché, demande pardon à Dieu pendant ce jour.</p>
<p>Mes chers, pendant ce jour, les Prières (Doua’a) sont exaucées.</p>
<p>Le Prophète (bpsl) a dit <strong class="prophete">« Le meilleur Doua’a est celui du jour d’Arafat »</strong>.</p>
<p>Le Doua’a le plus exaucé,</p>
<p>Les demandes les plus proches d’être réalisées, sont les demandes que tu fais à Dieu pendant le jour d’Arafat.</p>
<p>Quels sont tes souhaits ? De quoi as-tu envie ?</p>
<p>Je te propose de les demander à Dieu dans des Doua’a après la Prière du Dohr (début d’après-midi),</p>
<p>Puis ré-insister après la Prière de l’Asr (milieu d’après-midi),</p>
<p>Et te concentrer entre L’Asr et Al’Maghrib (coucher du soleil),</p>
<p>Et dans les derniers instants avant le coucher du soleil,</p>
<p>Active-toi à fond dans les Doua’a.</p>
<p>Puis qu’est ce qui nous empêche de nous rassembler en famille,</p>
<p>De rassembler nos enfants,</p>
<p>Que la maman, le papa, et les enfants fassent tous des Doua’a ensemble ?</p>
<p>Mes chers, les demandes de ce jour sont exaucées,</p>
<p><strong class="prophete">« Le meilleur Doua’a est celui du jour d’Arafat »</strong>.</p>
<p>C’est un jour dans lequel on a besoin de se concentrer,</p>
<p>Tu dois te mettre dans un état de rapprochement de Dieu,</p>
<p>Et dans un état de recueillement…</p>
<p>Tu te souviens comment sont les gens pendant les 10 dernières nuits du Ramadan ?</p>
<p>Quand ils cherchent la nuit du Destin,</p>
<p>Quand ils se demandent : Est-ce que c’est la nuit du 27 ou la nuit du 25, ou celle du 29…</p>
<p>Puis la nuit du 27, les mosquées sont remplies et tout le monde se dirige vers Dieu…</p>
<p>Tout ceci alors qu’ils ne connaissent pas la nuit exacte.</p>
<p>Alors que dire du jour d’Arafat, dont on connait précisément la date ?</p>
<p>C’est un jour où Dieu réalise les demandes,</p>
<p>C’est un jour de pardon,</p>
<p>C’est un jour de miséricorde.</p>
<p>Dirige-toi vers Dieu avec toute ta foi,</p>
<p>Et Il sera fier de toi, Il dira aux Anges : <strong>{ Regardez mes serviteurs, (Regardez) comment ils sont venus à Moi }</strong>.</p>
<p>Et que dire des pèlerins qui se trouvent ce jour sur le mont Arafat, et qui s’adressent à Dieu…</p>
<p>Chacun de nous doit mettre au courant ses proches,</p>
<p>Et leur dire « Faites des Doua’a, c’est un jour spécial »</p>
<p>Et chacun de nous doit se préparer,</p>
<p>Et se diriger vers Dieu avec son cœur,</p>
<p>Demander pardon,</p>
<p>Se repentir,</p>
<p>Et commencer une nouvelle page.</p>
<p>C’est comme si ce jour, est celui d’un nouveau départ avec Dieu,</p>
<p>Un nouveau rapprochement,</p>
<p>Des Prières exaucées…</p>
<p>Que Dieu nous accepte et vous accepte,</p>
<p>Qu’Il nous pardonne,</p>
<p>Qu’Il soit satisfait de nous,</p>
<p>Et qu’Il fasse de l’année prochaine, une année de miséricorde, de bienfaits, de pardon, de générosité, d’élargissement de nos subsistances…</p>
<p>Je vous remercie,</p>
<p>Que Dieu accorde un sourire d’espoir à nous tous.</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
