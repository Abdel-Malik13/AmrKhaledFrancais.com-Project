                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui est une valeur dont on a beaucoup besoin…</p>
<p>C’est une valeur noble,</p>
<p>Elle s’appelle : La douceur.</p>
<p>Es-tu doux avec les gens ?</p>
<p>Cet épisode a aussi un rapport avec la miséricorde,</p>
<p>Mais son idée essentielle c’est de ne pas être nerveux,</p>
<p>Ne pas être violent dans tes relations avec les autres.</p>
<p>Être doux.</p>
<p>Le Prophète (bénédiction et paix soient sur lui) a dit</p>
<p><strong class="prophete">« Chaque fois que la douceur se trouve dans une chose, elle l’embellit »</strong></p>
<p>Chaque chose à laquelle tu rajoutes de la douceur, devient plus belle.</p>
<p><strong class="prophete">« Et chaque fois que la douceur est retirée d’une chose, elle l’amoche »</strong></p>
<p>Chaque chose de laquelle tu retires la douceur, devient moche.</p>
<p>Chaque situation,</p>
<p>Chaque endroit,</p>
<p>Chaque maison,</p>
<p>Chaque entreprise ou société,</p>
<p>Chaque club,</p>
<p>N’importe quel lieu…</p>
<p>C’est comme si c’était un principe pour les entreprises,</p>
<p>Et un principe pour les familles,</p>
<p>Quand la douceur est présente, l’endroit devient plus beau et le sourire d’espoir y grandit.</p>
<p>Et quand tu retires la douceur, l’endroit devient détestable et le sourire d’espoir le quitte.</p>
<p>L’épisode d’aujourd’hui est une histoire du Prophète (bpsl),</p>
<p>Les juifs vivaient à Médine,</p>
<p>Et ils ont fait 3 tentatives pour l’assassiner…</p>
<p>Ils ont essayé de le tuer 3 fois à Médine.</p>
<p>Les juifs de la tribu de Banou Nadir ont fait une tentative,</p>
<p>Les juifs de la tribu de Banou Qoraida en ont fait une autre,</p>
<p>Et les juifs de Banou Qainouqa’a ont fait la troisième.</p>
<p>Les juifs de ces 3 tribus vivaient à Médine,</p>
<p>Mais il y avait un autre groupe de juifs à Médine, qui ne faisaient pas partie de ces tribus,</p>
<p>Alors le Prophète (bpsl) les a invités dans sa maison.</p>
<p>À sa place certains auraient dit « Pourquoi faire ? Ils sont tous mauvais… Etc. »</p>
<p>Non, le Prophète (bpsl) les a invités chez lui,</p>
<p>C’est-à-dire ils sont partis dans la maison du Prophète (bpsl),</p>
<p>Et qui était présent dans la maison ?</p>
<p>Il y avait Aïcha (l’épouse du Prophète (bpsl)),</p>
<p>Pendant qu’ils entraient à la maison,</p>
<p>Ils ont dit « Assamou aalayka Ô Abou Al Qassem »</p>
<p>Abou Al Qassem était le surnom du Prophète (bpsl),</p>
<p>Par contre ils ont dit « Assamou Aalayka » au lieu de dire « Assalamou Aalayka »…</p>
<p>Ils ont retiré la lettre « L »,</p>
<p>Dans leur langue, « Assam » signifie la mort,</p>
<p>C’est-à-dire ils ont dit « Que la mort soit sur toi » au lieu de dire « Que la paix soit sur toi »,</p>
<p>Ils ont prononcé ça rapidement,</p>
<p>Pour que personne ne s’en rende compte…</p>
<p>« Assamou aalayka Ô Abou Al Qassem »</p>
<p>Le Prophète (bpsl) a sourit, et il leur a répondu</p>
<p><strong class="prophete">« Et sur vous »</strong>.</p>
<p>En toute simplicité, et en toute spontanéité.</p>
<p>Mais qui a entendu la discussion ?</p>
<p>Aïcha l'épouse du Prophète (bpsl)...</p>
<p>Elle n'a pas supporté,</p>
<p>Elle est venue leur dire,</p>
<p>« Non, que la mort soit sur vous !</p>
<p>Ainsi que la malédiction ! »</p>
<p>Le Prophète (bpsl) l'a emmenée en privé, et il lui a dit <strong class="prophete">« Ô Aïcha, je te vois fâchée »</strong></p>
<p>Elle lui a dit « Oui Ô Messager de Dieu, n’as-tu pas entendu ce qu’ils ont dit ? »</p>
<p>Il lui a répondu <strong class="prophete">« Oui j’ai entendu, et je leur ai répondu ‘Et sur vous’ »</strong></p>
<p>:)</p>
<p><strong class="prophete">« Doucement Aïcha, car Dieu n'aime pas la grossièreté dans les paroles.</strong></p>
<p><strong class="prophete">Sois douce Aïcha, car je jure que quand la douceur se trouve dans une chose elle l'embellit,</strong></p>
<p><strong class="prophete">Et quand la douceur est retirée d’une chose, elle l'amoche.</strong></p>
<p><strong class="prophete">Ô Aïcha, Dieu est Doux, et Il aime la douceur dans toute chose. »</strong></p>
<p>C’est une très belle parole du Prophète (bpsl)…</p>
<p>L’histoire est très simple,</p>
<p>Et le sens à retenir c’est :</p>
<p>Es-tu doux avec ta femme, ou ton mari ?</p>
<p>Es-tu doux avec tes enfants ?</p>
<p>Es-tu doux avec les gens qui travaillent pour toi ?</p>
<p>Es-tu doux avec les gens différents de toi ?</p>
<p>Est-ce que la douceur est une de tes qualités ?</p>
<p>Crois-moi, si tu es quelqu’un de doux, alors tu es aimé par Dieu.</p>
<p><strong class="prophete">« Dieu est Doux, et Il aime la douceur dans toute chose. »</strong></p>
<p>Si tu appliques ça, je jure que tu vivras avec un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
