                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de la gratitude,</p>
<p>Du mari fidèle à sa femme…</p>
<p>De l’homme loyal envers sa femme.</p>
<p>La gratitude est une qualité très belle,</p>
<p>On l’aime tous.</p>
<p>Mais la gratitude avec la maman, avec l’épouse, a un goût différent…</p>
<p>Elle a une valeur plus élevée,</p>
<p>Et une beauté plus grande.</p>
<p>Surtout si l’épouse tombe malade,</p>
<p>Surtout si l’épouse prend de l’âge… Que tu restes fidèle à elle.</p>
<p>C’est une chose très grande, très belle, et qui nécessite une âme forte et patiente…</p>
<p>Et une âme qui se rend compte de la beauté et de la valeur de la gratitude.</p>
<p>L’histoire d’aujourd’hui est celle d’un homme qui arrive à la fin de ses 70 ans,</p>
<p>Sa santé est fragile…</p>
<p>Et un jour il s’est blessé à la main,</p>
<p>Il lui fallait des points de suture,</p>
<p>Alors il est parti vite à l’hôpital…</p>
<p>On lui a fait les points de suture pour fermer la blessure,</p>
<p>Mais il fallait qu’il revienne quelques jours plus tard pour les enlever,</p>
<p>Et pour s’assurer que la blessure est guérie.</p>
<p>Donc quelques jours plus tard, cet homme âgé de plus de 70 ans, est arrivé à l’hôpital à 8h00 précise du matin pour son rendez-vous…</p>
<p>Le médecin est arrivé avec un peu de retard,</p>
<p>L’homme a commencé à stresser,</p>
<p>Il demandait à l’accueil « Où est le docteur ? »</p>
<p>Jusqu’à ce que le médecin arrive,</p>
<p>Il est entré le voir,</p>
<p>Et le docteur lui a dit « Les infirmières m’ont dit que vous étiez pressé… »</p>
<p>Comme pour lui dire « Tu es un homme retraité, pourquoi être pressé ? »</p>
<p>L’homme lui a dit « S’il vous plait docteur, faites vite car j’ai un rendez-vous important auquel je dois me rendre »</p>
<p>Le docteur lui a demandé « Vous êtes pressé car vous avez un rendez-vous dans un ministère ou dans une administration? »</p>
<p>L’homme lui a répondu « Non, je dois partir à la maison de retraite pour prendre le petit déjeuner avec ma femme »</p>
<p>Le docteur lui a dit « Prendre le petit déjeuner avec votre épouse dans une maison de retraite ? Mais que fait votre femme là-bas ? »</p>
<p>L’homme lui a répondu « Ma femme est atteinte de la maladie d’Alzheimer,</p>
<p>C’est pour ça qu’elle ne pouvait plus vivre avec moi à la maison,</p>
<p>Elle risquait de sortir n’importe où et se perdre…</p>
<p>Elle est dans la maison de retraite car il faut qu’elle soit sous surveillance »</p>
<p>Le docteur était curieux, il lui a demandé « Et votre femme va beaucoup s’inquiéter si vous arrivez en retard pour le petit déjeuner ? »</p>
<p>L’homme lui a répondu « Non, mon épouse ne sait même plus qui je suis.</p>
<p>Ça fait 5 ans que ma femme ne reconnait plus personne »</p>
<p>Le docteur lui a dit « Et chaque jour vous allez prendre le petit déjeuner avec elle, alors qu’elle ne sait même pas qui vous êtes ?»</p>
<p>L’homme lui a répondu avec le sourire « Elle ne sait pas qui je suis, mais moi si.</p>
<p>Si elle ne se souvient plus de moi, alors moi je me souviens d’elle. »</p>
<p>Le docteur dit qu’il a pleuré,</p>
<p>C’est lui qui raconte cette histoire,</p>
<p>Il dit « J’ai été très heureux de savoir qu’il existe encore une fidélité pareille »</p>
<p>La phrase de cet homme est très belle :</p>
<p>« Si elle ne se souvient plus de moi, alors moi je me souviens d’elle.</p>
<p>Si elle ne me connait pas, alors moi je la connais. »</p>
<p>Est-ce que quelqu’un parmi nous possède une loyauté pareille ?</p>
<p>Quand une femme trouve de la loyauté chez un homme, elle se sent en sécurité, dans un monde où il y a beaucoup d’insécurité…</p>
<p>Gardez chez vous de la gratitude pour vos épouses, et pour vos mamans,</p>
<p>Pour qu’il reste… Qu’il reste un sourire d’espoir.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
