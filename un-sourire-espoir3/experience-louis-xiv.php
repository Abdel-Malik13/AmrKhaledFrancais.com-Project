                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui dit : Il existe des gens qui aiment compliquer la vie...</p>
<p>La vie est devenue difficile en réalité,</p>
<p>La vie est déjà compliquée et on la complique encore plus de notre côté...</p>
<p>Alors que souvent il existe des solutions très simples et très faciles.</p>
<p>Parfois les décisions sont très faciles à prendre,</p>
<p>Mais c'est nous qui compliquons les choses...</p>
<p>On complique pour que 2 personnes puissent se marier,</p>
<p>Les deux familles compliquent... Compliquent... Jusqu'à ce que le mariage soit gâché.</p>
<p>Alors qu'ils pouvaient se marier avec peu de moyens,</p>
<p>Compter sur Dieu,</p>
<p>Et grandir petit à petit.</p>
<p>Pourquoi mes chers, on cherche toujours les solutions compliquées et on ignore les solutions simples ?</p>
<p>Dans notre mariage,</p>
<p>Dans notre travail,</p>
<p>Dans notre façon de vivre...</p>
<p>Je vais vous raconter une histoire :</p>
<p>C'est à l'époque de Louis XIV (Le roi soleil),</p>
<p>Un grand roi de France.</p>
<p>Il y avait en prison un homme condamné à mort,</p>
<p>Et l'exécution allait avoir lieu le lendemain...</p>
<p>Ce prisonnier était au courant de la décision, et il attendait l'heure de son exécution.</p>
<p>Puis soudainement il a été surpris de voir le roi Louis XIV arriver...</p>
<p>Il est venu avec ses gardes pour voir le prisonnier dans sa cellule,</p>
<p>Louis XIV prenait parfois des décisions surprenantes,</p>
<p>Et faisait des propositions qui étonnaient les gens...</p>
<p>Il est venu voir le prisonnier, et lui a dit</p>
<p>« Je vais t'accorder une dernière chance,</p>
<p>Une chance pour sauver ta vie et reprendre ta liberté,</p>
<p>L'exécution aura lieu demain.</p>
<p>Et dans cette prison il y a plein de pièces, plein de passages secrets, plein de portes,</p>
<p>Mais il y a un seul passage, si tu le traverses aucun gardien ne t'arrêtera,</p>
<p>Tu te retrouveras à l'extérieur, et tu pourras t'en aller,</p>
<p>Tu as jusqu'au lever du soleil pour le trouver »</p>
<p>Puis Louis XIV a demandé aux gardes de détacher les chaînes du prisonnier.</p>
<p>Louis XIV est parti,</p>
<p>Il était sérieux,</p>
<p>Il n'allait pas faire le déplacement et venir spécialement le voir juste pour se moquer de lui...</p>
<p>Le prisonnier s'est mis à chercher,</p>
<p>Et effectivement il a trouvé une petite porte bien cachée,</p>
<p>Il a tiré la porte,</p>
<p>Elle s'est ouverte...</p>
<p>Il a trouvé un couloir,</p>
<p>Il a continué à avancer,</p>
<p>Il a trouvé des escaliers,</p>
<p>Bien sûr son cœur battait de joie,</p>
<p>Il est monté... Monté... Monté...</p>
<p>Il commençait à sentir l'air frais,</p>
<p>La sortie semblait pas loin...</p>
<p>Mais quand il est arrivé au bout,</p>
<p>Il s'est retrouvé devant une fenêtre qui donne sur le vide,</p>
<p>Il n'allait pas sauter et mourir...</p>
<p>Le temps passait, il est redescendu très vite,</p>
<p>Il était fatigué, il transpirait,</p>
<p>Et en cherchant en bas, il a marché sur une pierre qui s'est décollée,</p>
<p>Il a soulevé les autres pierres à côté,</p>
<p>Il y avait un passage secret...</p>
<p>Il est descendu,</p>
<p>Il rampait... Rampait... Rampait...</p>
<p>Puis il a commencé à entendre un bruit d'eau,</p>
<p>« Ca y est je vais être sauvé ! »</p>
<p>Il avançait... Avançait... Avançait...</p>
<p>Et il a trouvé une rivière qui coulait,</p>
<p>Mais au bout du passage il y avait une grille en fer solide,</p>
<p>Il ne pouvait pas sortir...</p>
<p>Il est revenu au point de départ,</p>
<p>Il a continué à chercher,</p>
<p>Mais tous les passages étaient sans issue,</p>
<p>« Il s'est moqué de moi ?</p>
<p>Ou il veut juste me faire souffrir ? »</p>
<p>Le soleil s'est levé,</p>
<p>Et Louis XIV est arrivé,</p>
<p>Il a demandé au prisonnier « Alors as-tu trouvé ? »</p>
<p>Il lui a répondu « Non je n'ai rien trouvé »</p>
<p>Louis XIV lui a demandé « As-tu tout essayé ? »</p>
<p>Il lui a répondu « Oui j'ai essayé tous les passages, tu t'es moqué de moi ? »</p>
<p>Louis XIV lui a dit « Non je ne me suis pas moqué de toi,</p>
<p>La grande porte de la prison était ouverte,</p>
<p>La porte est le moyen le plus simple pour sortir,</p>
<p>Et en partant hier moi et mes gardes, on t'a laissé la porte ouverte,</p>
<p>Si tu étais sorti par la porte, personne ne t'aurait arrêté, et tu serais en liberté»</p>
<p>Quelqu'un a compris ce que je veux vous dire ?</p>
<p>Pourquoi on complique toujours les choses ?</p>
<p>Dieu dit dans le Coran</p>
<p><strong>{ Nous te mettrons sur la voie la plus facile. }</strong></p>
<p>Sourate 87 - Al-A'lâ : Le Très-Haut (verset 8)</p>
<p>La facilité est très belle :</p>
<p>Chercher les solutions faciles avant de compliquer pour les gens...</p>
<p>Combien de parents compliquent la vie de leurs enfants...</p>
<p>Mes chers, voyez la vie avec simplicité,</p>
<p>Facilitez la vie aux gens, pour voir un sourire d'espoir dans vos vies.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
