                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle de la tendresse avec les enfants,</p>
<p>Du besoin qu'a un enfant d'affection, de tendresse, de sourires, de miséricorde...</p>
<p>L'histoire c'est une femme qui la raconte :</p>
<p>Elle dit qu'elle et ses copines ont décidé de rendre visite à un orphelinat,</p>
<p>Avec ses copines elle avait l'habitude de passer du temps avec les orphelins,</p>
<p>Mais cette fois elles ont décidé de faire quelque chose de différent :</p>
<p>On ne va pas seulement aller les visiter, leur donner du chocolat, des bonbons... Et repartir...</p>
<p>Non, nous allons faire des activités amusantes avec les enfants de l'orphelinat,</p>
<p>Pour les détendre et les mettre de bonne humeur.</p>
<p>On va ramener des feuilles,</p>
<p>Et beaucoup de crayons en couleur,</p>
<p>Puis on va s'asseoir pour dessiner tous ensemble...</p>
<p>Les copines ont aimé l'idée,</p>
<p>Elles ont acheté les crayons et les accessoires pour dessiner,</p>
<p>Puis elles sont parties à l'orphelinat.</p>
<p>Elles ont commencé à jouer avec les enfants,</p>
<p>Mais cette femme a remarqué un petit garçon à l'écart...</p>
<p>Ca se voyait qu'il était triste,</p>
<p>Puis il était renfermé sur lui-même.</p>
<p>Elle est partie lui donner un câlin,</p>
<p>Elle ne le connaissait pas vraiment,</p>
<p>Donc elle a essayé de faire connaissance,</p>
<p>Puis elle lui a donné une feuille et lui a dit « Mon chéri, ca te dit de dessiner un cœur ? Tu as plein de crayons de couleur, dessine comme tu veux »</p>
<p>Le petit garçon s'est mis à dessiner,</p>
<p>Puis la dame est partie voir les autres enfants...</p>
<p>Et en revenant elle a vu que le garçon avait dessiné un cœur de couleur noir foncé...</p>
<p>Elle lui a dit « Mon chéri, pourquoi tu as choisi cette couleur ? »</p>
<p>Il lui a répondu avec l'innocence des enfants « C'est la couleur de mon cœur »</p>
<p>À son âge il ne comprend pas la philosophie de la vie, mais il a juste senti ca par instinct...</p>
<p>« C'est la couleur de mon cœur »</p>
<p>La dame lui a donné un gros câlin cette fois...</p>
<p>Elle s'est beaucoup intéressée à lui,</p>
<p>Elle a fait connaissance,</p>
<p>Elle l'a embrassé,</p>
<p>Elle lui a dit des paroles gentilles...</p>
<p>Le petit garçon commençait à s'ouvrir,</p>
<p>Il a commencé à sourire,</p>
<p>Il a commencé à s'attacher à elle,</p>
<p>Il la tenait par la main, la suivait partout, et ne la quittait plus...</p>
<p>Puis la journée s'est terminée,</p>
<p>La directrice de l'orphelinat leur a dit « Venez je vous fais visiter le lieu »</p>
<p>La femme a laissé le petit garçon, et elle est partie visiter l'orphelinat avec ses copines,</p>
<p>Puis elles allaient rentrer chez elles...</p>
<p>Elle avait oublié le petit garçon,</p>
<p>Mais il est venu la chercher,</p>
<p>Il lui a dit « Regarde... Regarde... J'ai changé la couleur de mon cœur,</p>
<p>Je l'ai colorié avec un rouge vif »</p>
<p>Elle l'a regardé, elle lui a fait un câlin...</p>
<p>Il lui a dit « La couleur de mon cœur a changé quand tu es restée avec moi,</p>
<p>Peux-tu venir souvent ? »</p>
<p>Combien y'a t'il d'orphelins dans nos pays qui ont besoin de tendresse et d'affection...</p>
<p>Et même nos propres enfants deviennent comme des orphelins,</p>
<p>Quand leurs parents leurs donnent de l'argent, les envoient à l'école... Mais ne leur donnent pas de la tendresse et des sentiments.</p>
<p>Ton fils et ta fille, ont besoin de ton affection pour que leurs cœurs soient de couleur rouge vif.</p>
<p>Un mot gentil,</p>
<p>Un câlin,</p>
<p>Ne privez pas vos enfants, ni les orphelins, ni n'importe quel enfant... D'amour et de douceur,</p>
<p>Pour dessiner des sourires d'espoir sur leurs visages.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
