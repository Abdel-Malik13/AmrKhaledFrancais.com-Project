                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui a été écrite dans un livre en 1904,</p>
<p>Elle raconte qu'un groupe de personnes avait immigré du Pérou en Amérique Latine...</p>
<p>Ils ont fuit pour échapper aux injustices que leur faisaient subir les colons espagnols qui occupaient l'Amérique du sud à cette époque,</p>
<p>Et ils ont immigré vers où ?</p>
<p>Ils se sont dirigés vers une zone inhabitée en dehors du Pérou,</p>
<p>Cette région était remplie de montagnes et de rochers...</p>
<p>Mais pendant qu'ils marchaient, un tremblement de terre s'est produit,</p>
<p>Des rochers ont chuté,</p>
<p>Une fissure s'est formée entre les montagnes,</p>
<p>Et ils ont glissé vers le bas à cause du sable qui s'écroulait...</p>
<p>Ils se sont retrouvés dans une vallée tout en bas.</p>
<p>Et à cause de la quantité de sable et de la poussière qui est tombée sur eux,</p>
<p>Leurs yeux ont attrapé une infection,</p>
<p>Ils ont perdu la vue.</p>
<p>(C'est une histoire imaginaire)</p>
<p>Tout le groupe était devenu aveugle.</p>
<p>Ils ont construit un petit village,</p>
<p>Et ce village ou cette vallée était devenue « La vallée des aveugles ».</p>
<p>Génération après génération... Tous les enfants qui naissaient étaient également aveugles,</p>
<p>C'est devenu un héritage...</p>
<p>Au bout d'un moment ils ont oublié la signification du mot « Vue ».</p>
<p>C'était « La vallée des aveugles », ils ne connaissaient plus le mot « yeux ».</p>
<p>Pareil pour le verbe « Voir », il a disparu de la langue,</p>
<p>« Je vais voir quelque chose » Une phrase pareille n'existe pas dans leur vocabulaire...</p>
<p>Jusqu'au jour où un homme escaladait une montagne aux environs,</p>
<p>C'était l'hiver,</p>
<p>Il pleuvait...</p>
<p>Son pied a glissé,</p>
<p>Il a fait une grande chute,</p>
<p>Et il a fini dans la vallée des aveugles...</p>
<p>Cet homme voyait,</p>
<p>Et il n'est pas mort car il est tombé sur une couverture épaisse.</p>
<p>Il s'est retrouvé dans la vallée des aveugles,</p>
<p>Mais il ne le savait pas encore...</p>
<p>Il marchait dans le village, et il remarquait que les maisons étaient construites d'une façon bizarre,</p>
<p>A tel point qu'il s'est dit « Celui qui a construit ces maisons, c'est sûr qu'il ne voyait pas ce qu'il faisait »</p>
<p>Il a commencé à parler aux gens, mais ils ne le regardaient pas...</p>
<p>Il a fini par comprendre qu'ils ne voyaient pas,</p>
<p>Il leur a expliqué d'où il venait,</p>
<p>Il leur a dit « Je viens de l'endroit où les gens voient »</p>
<p>Et là un problème est apparu...</p>
<p>Ils lui ont dit « Ca veut dire quoi 'voient' ? »</p>
<p>« Ca veut dire quoi 'l'endroit où les gens voient' ? »</p>
<p>Il a essayé de leur expliquer...</p>
<p>Mais ils l'ont accusé de folie,</p>
<p>Ils lui ont dit « Tu es un homme fou, ca veut dire quoi 'voir' et les choses insensées que tu nous raconte ? »</p>
<p>Ils lui ont dit « On entend tout le temps des anges »</p>
<p>Il leur a répondu « Ce ne sont pas des anges... Ce sont des oiseaux qui volent autour de vous »</p>
<p>Ils lui ont dit « Le monde est divisé en 2 parties, une partie froide et une partie chaude »</p>
<p>Il leur a répondu « Non ce ne sont pas 2 parties... C'est la nuit et le jour, et c'est le soleil qui vous réchauffe »</p>
<p>Ils ont continué à l'accuser de folie,</p>
<p>Et il a essayé... Essayé... Essayé de les raisonner,</p>
<p>C'était sans espoir.</p>
<p>Il a essayé de s'enfuir,</p>
<p>Mais leurs 4 autres sens étaient plus forts que lui,</p>
<p>Ils l'ont attrapé...</p>
<p>Avec le temps il avait perdu sa capacité à résister,</p>
<p>Et pour qu'il puisse vivre avec eux, les habitants du village l'ont obligé à reconnaitre qu'une chose appelée « La vue » n'existe pas,</p>
<p>Il devait reconnaitre qu'il était atteint de folie.</p>
<p>Il a commencé à s'excuser...</p>
<p>Et il a commencé à reconnaitre « Oui il n'existe pas une chose appelée 'La vue' »</p>
<p>Il a fait ca, pour pouvoir vivre dans le pays des aveugles.</p>
<p>Et toi mon cher, si tu étais à sa place, qu'aurais-tu fait ?</p>
<p>Non mes chers... Non...</p>
<p>Si tous les gens trichent, alors toi ne triche pas.</p>
<p>Si tous les étudiants trichent dans les examens, toi ne triche pas...</p>
<p>Il faut que tu restes « Voyant ».</p>
<p>Et si tous les gens deviennent négatifs... Ca ne te concerne pas, reste toi positif,</p>
<p>N'accepte pas de vivre en étant « aveugle » alors que tu as des yeux pour voir...</p>
<p>Si tous les gens oublient l'amour de la patrie, alors toi reste patriotique et cherche à développer ton pays,</p>
<p>Si tous les gens deviennent aveugles, toi reste voyant...</p>
<p>Le Prophète (bénédictions et paix soient sur lui) a dit cette très belle parole :</p>
<p><strong class="prophete">« Que l'un de vous ne soit pas une girouette, qui dit 'Si les gens font le bien, je ferai le bien, et si les gens font le mal alors je le ferai aussi... (Je vais suivre la majorité) ' Mais enracinez-vous : Si les gens font le bien, faites-le également, et si les gens font le mal, alors ne le faites pas. »</strong></p>
<p>C'est le vrai sourire d'espoir : Il faut qu'il reste parmi nous de vrais gens qui « voient ».</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
