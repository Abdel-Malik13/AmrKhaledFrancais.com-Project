                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui parle d'un enfant âgé de 7 ans,</p>
<p>Son papa est décédé suite à une maladie,</p>
<p>Mais sa maman lui a caché la vérité...</p>
<p>Il savait que son papa était malade, mais il ignorait qu'il était décédé.</p>
<p>Mais alors que lui a raconté sa maman ?</p>
<p>Elle lui disait : Ton papa est parti en voyage... Ton papa est parti en voyage... Ton papa est parti en voyage...</p>
<p>Et elle l'a élevé toute seule.</p>
<p>2 ans plus tard, la maman fumait beaucoup,</p>
<p>Parfois quand une femme élève seule ses enfants, elle devient stressée, nerveuse...</p>
<p>Elle fumait des cigarettes d'une façon excessive,</p>
<p>Et un soir en rentrant à la maison, elle a remarqué que son fils n'était pas dans son état normal...</p>
<p>Elle est entrée dans sa chambre pendant qu'il n'y était pas,</p>
<p>Et sous l'oreiller elle a trouvé un paquet de cigarettes...</p>
<p>Elle était fumeuse,</p>
<p>Mais qu'un enfant de 7 ans fume des cigarettes ??</p>
<p>Elle s'est mise en colère,</p>
<p>Elle a pris son fils et l'a tapé d'une façon sévère...</p>
<p>L'enfant a pleuré... Pleuré...</p>
<p>Puis il a avoué la vérité,</p>
<p>Quelle vérité ?</p>
<p>La vérité qu'il cachait à sa maman,</p>
<p>(Parfois on pense que nos enfants sont trop petits pour comprendre, pourtant ils voient et comprennent tout)</p>
<p>Qu'est ce qu'il a dit ?</p>
<p>Quand sa maman l'a tapé et qu'il a pleuré,</p>
<p>Il lui a dit</p>
<p>« Papa n'est pas en voyage,</p>
<p>Papa est décédé,</p>
<p>Le fils de ta copine m'a dit que papa est mort,</p>
<p>Et je te le cachais »</p>
<p>Il pensait que sa maman ne le savait pas, et qu'elle croyait sérieusement que son papa était en voyage...</p>
<p>C'est un petit enfant innocent,</p>
<p>Il lui a dit « Je te le cachais car tu croyais qu'il était en voyage,</p>
<p>Il est mort car il était malade,</p>
<p>Alors j'ai eu peur que tu tombes malade à cause des cigarettes comme papa,</p>
<p>Chaque fois je prenais des paquets de cigarettes et je les cachais,</p>
<p>Pour que tu ne fumes pas,</p>
<p>Pour que tu ne meurs pas comme papa »</p>
<p>Qu'est ce que je veux vous transmettre à travers cette histoire ?</p>
<p>Je veux vous dire que parfois nos enfants sont plus miséricordieux envers nous, que nous envers nous-mêmes...</p>
<p>Parfois nos enfants s'inquiètent pour nous, et font très attention à nous,</p>
<p>Alors qu'on a la tête ailleurs...</p>
<p>Certains parents sont occupés nuit et jour,</p>
<p>Leurs enfants ne sont pas orphelins,</p>
<p>Mais ils vivent comme des orphelins...</p>
<p>Car leur papa ou leur maman ne leur donne pas de temps,</p>
<p>Et ils souffrent en silence sans que leurs parents ne s'en rendent compte.</p>
<p>Mes chers, prenez soin de vos enfants,</p>
<p>Ne pensez jamais qu'ils ne comprennent pas les choses,</p>
<p>Faites attention à eux, et soyez tendres,</p>
<p>Un sourire d'espoir pour chaque papa/maman qui donne de la tendresse à ses enfants,</p>
<p>Qui leur donne de l'affection,</p>
<p>Qui se rapproche d'eux,</p>
<p>Qui discute avec eux...</p>
<p>La maman de l'histoire élevait son fils comme dans une administration :</p>
<p>Mange,</p>
<p>Dors,</p>
<p>Fais tes devoirs...</p>
<p>En 2 ans elle n'a jamais pensé à s'asseoir et discuter avec lui,</p>
<p>Pourtant si elle lui avait parlé, elle aurait découvert qu'il savait déjà que son papa était décédé...</p>
<p>Et que c'est lui qui le lui cache,</p>
<p>Car il a peur que sa maman souffre en apprenant la nouvelle...</p>
<p>Puis il cachait aussi les cigarettes car il avait peur pour sa santé.</p>
<p>Un sourire d'espoir pour chaque papa et chaque maman,</p>
<p>Qui accorde du temps à ses enfants,</p>
<p>Qui leur donne des câlins,</p>
<p>Et qui discute avec eux...</p>
<p>Qui leur donne de la tendresse, de l'affection et de l'amour.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
