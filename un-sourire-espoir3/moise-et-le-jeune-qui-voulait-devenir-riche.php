                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est une histoire ancienne,</p>
<p>Une histoire qui parle des enfants d'Israël et de Moïse,</p>
<p>L'histoire dit :</p>
<p>Les enfants d'Israël faisaient des demandes à Moïse,</p>
<p>Ils lui disaient de demander des choses à Dieu.</p>
<p>Moïse s'adressait à Dieu pour lui demander des choses, et Dieu les lui donnait.</p>
<p>C'était une chose très répandue à l'époque de Moïse.</p>
<p>Et un jour un jeune de 20 ans est venu voir Moïse,</p>
<p>Ce jeune voulait que Moïse demande à Dieu de le rendre riche.</p>
<p>Alors Moïse a fait la demande à Dieu.</p>
<p>Dieu lui a répondu <strong>{ Pose-lui cette question Moïse : Veux-tu que Dieu te rende riche pendant les 20 premières années de ta vie, ou bien qu'Il te rende riche pendant les 20 dernières années de ta vie ? }</strong></p>
<p>Moïse a compris que la richesse de ce jeune n'allait pas durer...</p>
<p>Il devait choisir soit les 20 premières années, soit les 20 dernières années de sa vie.</p>
<p>Moïse lui a posé la question,</p>
<p>Et le jeune s'est mis à réfléchir...</p>
<p>Puis après réflexion il a décidé « Non, je veux devenir riche les 20 premières années ».</p>
<p>Je veux profiter pendant que je suis jeune,</p>
<p>Puis rien ne me garantit que je vais vivre jusqu'à l'âge de 60 ans...</p>
<p>Laisse-moi profiter de ma jeunesse.</p>
<p>Moïse a fait la demande à Dieu,</p>
<p>Et Dieu l'a réalisée.</p>
<p>Le jeune est devenu très riche...</p>
<p>Dieu lui a ouvert les portes de la subsistance.</p>
<p>Et ce jeune avait une belle mentalité,</p>
<p>Il a commencé à ouvrir les portes de la subsistance à d'autres gens.</p>
<p>Il aidait les gens...</p>
<p>Il n'achetait pas toutes les marchandises du marché, pour ne pas avoir le monopole...</p>
<p>Il finançait des petits projets pour les pauvres, il leur ouvrait un petit commerce, ou leur achetait une petite ferme, afin qu'ils puissent avoir une source de revenus...</p>
<p>Il mariait celui qui n'avait pas les moyens de se marier...</p>
<p>Il aidait les orphelins et les femmes veuves...</p>
<p>Ce jeune faisait cette prière en s'adressant à Dieu « Ô Seigneur, puisque Tu m'as rendu riche,</p>
<p>Je Te promets qu'aucun jour ne passera sans que j'ouvre à Tes serviteurs une porte de la subsistance.</p>
<p>Comme Toi Tu m'as ouvert cette porte. »</p>
<p>C'est-à-dire c'était une promesse,</p>
<p>Comme Dieu lui a ouvert la porte de la subsistance, il ouvrait à son tour les portes de la subsistance aux gens.</p>
<p>Les 20 années s'écoulaient...</p>
<p>Et Moïse (qui a vécu longtemps) attendait de voir ce qui va se passer,</p>
<p>Mais rien ne changeait...</p>
<p>Moïse a alors demandé à Dieu « Ô Seigneur, les 20 années sont passées, et ce jeune est toujours riche »</p>
<p>Dieu lui a répondu <strong>{ J'ai trouvé que Mon serviteur ouvrait les portes de Ma subsistance à Mes serviteurs. Alors Je Me suis senti gêné de fermer la porte de Ma subsistance à Mon serviteur. }</strong></p>
<p>C'est magnifique...</p>
<p>Dieu a vu que ce jeune ouvrait les portes de la subsistance aux autres... Alors Dieu s'est senti gêné de lui fermer Sa porte.</p>
<p>Mes chers : L'équation de la subsistance n'est connue que par Dieu.</p>
<p>Qui reçoit de l'argent, et pour quelle raison...</p>
<p>Qui prend de l'argent, pour le transmettre à qui...</p>
<p>C'est une équation compliquée, que seul Dieu connait.</p>
<p>Personne ne peut savoir s'il a reçu une somme d'argent, pour venir en aide à qui...</p>
<p>Alors aide les gens... Pour que Dieu t'aide, et t'ouvre Ses portes.</p>
<p>Ouvre ta porte aux gens, en faisant des dons et en cherchant à les aider,</p>
<p>Et Dieu t'ouvrira Sa porte et t'honorera.</p>
<p>Je redis la très belle parole Divine :</p>
<p><strong>{ J'ai trouvé que Mon serviteur ouvrait les portes de Ma subsistance à Mes serviteurs. Alors Je Me suis senti gêné de fermer la porte de Ma subsistance à Mon serviteur. }</strong></p>
<p>Un sourire d'espoir pour chaque personne qui cherche à aider les autres.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
