                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui est celle d'un professeur à l'université,</p>
<p>Il donnait des cours de gestion d'affaires,</p>
<p>Un jour les étudiants étaient surpris de le voir arriver avec un grand vase et des sacs...</p>
<p>Il leur a dit « Le cours d'aujourd'hui est très pratique »</p>
<p>Il a posé le vase sur la table,</p>
<p>Les étudiants étaient bien sûr très curieux,</p>
<p>Puis il a sorti des roches,</p>
<p>Et il mettait les roches dans le vase... Jusqu'à ce qu'il soit rempli.</p>
<p>Il leur a demandé « À votre avis, Le vase est rempli au maximum ? »</p>
<p>Les étudiants lui ont répondu « Oui bien sûr il est rempli »</p>
<p>Il leur a dit « Vous êtes sûrs ? »</p>
<p>Ils lui ont répondu « Oui nous sommes sûrs »</p>
<p>Il leur a dit « Est-ce qu'on peut mettre autre chose dans le vase ? »</p>
<p>Ils lui ont répondu « Non, c'est impossible, le vase est plein jusqu'au bout »</p>
<p>Le professeur leur a dit « Vous vous trompez »</p>
<p>Les étudiants lui ont demandé « Comment ? »</p>
<p>Il a sorti un sac plein de petits cailloux...</p>
<p>Il l'a versé sur le vase,</p>
<p>Alors les petits cailloux passaient entre les roches...</p>
<p>Quand il a fini, le professeur leur a demandé « Et maintenant, est ce que le vase est rempli ? »</p>
<p>Les étudiants lui ont répondu « Oui, cette fois il est rempli au maximum »</p>
<p>Il leur a dit « Vous êtes sûrs ? »</p>
<p>Ils lui ont dit « Oui nous sommes sûrs »</p>
<p>Il leur a demandé « Est-ce qu'on peut rajouter autre chose dans le vase ? »</p>
<p>Ils lui ont répondu « Non c'est impossible, il est rempli jusqu'au bout »</p>
<p>Il leur a dit « Non, le vase peut encore être rempli... »</p>
<p>Ils lui ont demandé « Et comment ? »</p>
<p>Il a sorti du sable, et il l'a versé dans le vase...</p>
<p>Alors le sable passait dans le vide qu'il y avait entre les roches et les petits cailloux,</p>
<p>Le vase se remplissait... se remplissait... Se remplissait.</p>
<p>Puis le professeur leur a demandé « Et maintenant, est ce que le vase est plein ? »</p>
<p>Bien sûr cette fois, les étudiants étaient confus...</p>
<p>Ils lui ont dit « Oui on pense qu'il est plein »</p>
<p>Il leur a demandé « Est-ce qu'on peut rajouter autre chose dans le vase ? »</p>
<p>Ils lui ont répondu « Non, nous ne pensons pas »</p>
<p>Le professeur leur a dit « Non, le vase peut se remplir encore... »</p>
<p>Il a sorti de l'eau, et il l'a versée sur les roches, les cailloux et le sable...</p>
<p>L'eau a été absorbée par le sable,</p>
<p>Et le vase s'est rempli jusqu'au bout.</p>
<p>Le professeur a gardé un peu le silence,</p>
<p>Puis leur a demandé</p>
<p>« À votre avis, quelle est la leçon à retenir de cette expérience ? »</p>
<p>Qu'est ce que cette histoire nous apprend ?</p>
<p>Ils lui ont répondu « On ne sait pas... »</p>
<p>Il leur a dit « Supposons que j'avais commencé par le contraire,</p>
<p>Si j'avais versé l'eau en premier, puis le sable, puis les cailloux...</p>
<p>Est-ce qu'il y aurait eu de la place pour les grandes roches ? »</p>
<p>Les étudiants lui ont répondu « Non il n'y aurait pas eu de place pour les roches »</p>
<p>Il leur a dit « C'est pareil dans nos vies...</p>
<p>Pour réussir dans la vie, il faut commencer par les grandes choses,</p>
<p>Il faut dessiner les grandes lignes »</p>
<p>Ton but dans la vie, il faut le mettre en 1er ! Et travailler pour l'accomplir.</p>
<p>Ta famille doit être la priorité,</p>
<p>Ton pays doit être la priorité,</p>
<p>Définis d'abord les grandes priorités,</p>
<p>Pose d'abord les grands rochers,</p>
<p>Ensuite rajoute ce que tu désires d'autre...</p>
<p>Chaque chose que tu rajouteras, passera sans problème,</p>
<p>Pourquoi ?</p>
<p>Car tu as déjà placé les choses essentielles.</p>
<p>Et dans le vide tu peux rajouter tes autres centres d'intérêts :</p>
<p>Je veux jouer aux jeux vidéo,</p>
<p>Et je veux m'amuser avec mes amis,</p>
<p>Et je veux discuter au téléphone...</p>
<p>Commence d'abord par les grandes choses, et tu obtiendras un sourire d'espoir dans ta vie.</p>
<p>Ton but,</p>
<p>Ta famille,</p>
<p>Ton pays,</p>
<p>Ce sont tes priorités.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
