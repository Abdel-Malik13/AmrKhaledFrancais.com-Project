                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'histoire d'aujourd'hui vient d'Afrique du sud,</p>
<p>Il y a de longues années en Afrique du sud, une politique d'Apartheid était menée,</p>
<p>Une séparation entre les blancs et les noirs...</p>
<p>L'histoire d'aujourd'hui est celle d'un homme noir âgé,</p>
<p>Il avait fait une chose qui l'a mené au tribunal,</p>
<p>Il a volé...</p>
<p>Mais qu'a-t-il volé ?</p>
<p>Il a volé 5 pains,</p>
<p>Il avait faim, et sa famille n'avait rien à manger...</p>
<p>Les noirs étaient discriminés à cette époque en Afrique du sud,</p>
<p>Cet homme avait très faim...</p>
<p>Il est parti dans une boulangerie,</p>
<p>Et pendant que le boulanger était occupé, il a pris 5 pains, puis il est parti sans payer...</p>
<p>(Le boulanger était un homme blanc)</p>
<p>On l'a rattrapé,</p>
<p>Puis on l'a emmené au poste de police,</p>
<p>Et comme il était noir, il a été transféré directement au tribunal...</p>
<p>Le juge était un homme blanc, mais il était juste.</p>
<p>Il s'est assis,</p>
<p>Le boulanger s'est assis,</p>
<p>On a fait venir l'accusé... Etc.</p>
<p>Le juge lui a demandé « As-tu volé 5 pains ? »</p>
<p>L'homme lui a répondu en toute spontanéité « Oui j'ai volé les 5 pains,</p>
<p>J'avais faim,</p>
<p>Et mes enfants ont faim.</p>
<p>Puis si je sors de cette affaire, et que j'ai encore faim... Je vais refaire la même chose,</p>
<p>Car mes enfants n'ont rien à manger »</p>
<p>Le juge a gardé le silence un peu...</p>
<p>Puis il a demandé au boulanger « Combien valent les 5 pains ? »</p>
<p>Il lui a répondu « 10 Rands »</p>
<p>(La monnaie sud africaine)</p>
<p>Le juge a regardé l'homme noir âgé qui a volé,</p>
<p>Et lui a dit « Tu es condamné à verser 10 Rands,</p>
<p>Mais tu avais faim,</p>
<p>Alors je vais payer les 10 Rands à ta place »</p>
<p>Le juge a sorti 10 Rands de sa poche, et il a dit « Ces 10 Rands doivent être mis dans les caisses du tribunal, pour payer le patron de la boulangerie »</p>
<p>Puis il a gardé le silence un peu...</p>
<p>Et il a regardé les gens assis au tribunal en disant</p>
<p>« Et vous aussi, vous êtes condamnés à verser 10 Rands,</p>
<p>Chacun de vous doit verser 10 Rands,</p>
<p>Car vous avez laissé dans votre pays quelqu'un obligé de voler pour pouvoir manger »</p>
<p>Il y avait 48 personnes présentes,</p>
<p>Le juge a rassemblé 480 Rands,</p>
<p>Puis il les a donnés à l'homme en disant « Prends-les pour ne pas voler une 2ème fois.</p>
<p>Car si tu voles encore, tu seras puni sévèrement »</p>
<p>Puis le juge a dit aux autres gens</p>
<p>« Et vous, si vous ne faites rien pour aider ceux qui sont dans le besoin,</p>
<p>Vous serez encore condamnés à payer »</p>
<p>Je veux terminer l'épisode en disant comme ce juge :</p>
<p>Nous sommes tous condamnés...
<p>Chacun de nous doit sortir aujourd'hui 5 ou 10 euros de sa poche,</p>
<p>Pour que dans notre pays, il n'y ait pas quelqu'un obligé de voler pour manger,</p>
<p>Si chacun de nous fait un effort,</p>
<p>Et se sent responsable des pauvres,</p>
<p>Pour qu'ils aient au moins de quoi manger...</p>
<p>C'est tout.</p>
<p>Je parle juste de la chose essentielle.</p>
<p>Et en réalité en voyant la réaction de ce juge,</p>
<p>Je me suis rappelé qu'une fois on a demandé au Prophète (bénédictions et paix soient sur lui)</p>
<p>« Quel est le meilleur islam Ô Messager de Dieu ? »</p>
<p>Quelle est la meilleure chose dans l'islam ?</p>
<p>Il a répondu <strong class="prophete">« Donner à manger »</strong></p>
<p>C'est le besoin essentiel des êtres humains,</p>
<p>Donnez à manger aux gens,</p>
<p>C'est le plus haut niveau et la meilleure chose dans l'islam...</p>
<p><strong class="prophete">« Donner à manger »</strong></p>
<p>Une histoire pour dessiner un sourire d'espoir dans nos pays,</p>
<p>Et dessiner un sourire d'espoir chez chaque personne qui n'a pas de quoi manger.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
