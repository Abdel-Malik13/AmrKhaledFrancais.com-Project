                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L'épisode d'aujourd'hui est un conseil aux parents :</p>
<p>Les paroles encourageantes qu'on dit à nos enfants, vivent avec eux, elles ne s'oublient jamais.</p>
<p>Et non seulement ça,</p>
<p>Nos enfants y croient,</p>
<p>Ils y sont convaincus,</p>
<p>Et parfois ça les sauve quand ils affrontent des grands problèmes.</p>
<p>Ne sois surtout par radin avec tes enfants, en paroles d'encouragements,</p>
<p>En paroles qui renforcent leur confiance en eux même,</p>
<p>Surtout quand ils sont en bas âge.</p>
<p>C'est une histoire vraie qui s'est produite en 1988 dans un pays appelé l'Arménie,</p>
<p>Un tremblement de terre puissant a frappé l'Arménie en 1988,</p>
<p>C'est le tremblement de terre le plus grave du 20ème siècle,</p>
<p>Entre 25.000 et 30.000 personnes y sont mortes.</p>
<p>Toute la région touchée par le tremblement de terre a été anéantie,</p>
<p>Mais un modeste agriculteur vivait avec sa femme, juste à la frontière de cette région,</p>
<p>Leur maison a été secouée, mais elle ne s'est pas effondrée,</p>
<p>Quand cet agriculteur s'est rassuré que sa femme allait bien,</p>
<p>Il est parti en courant vers l'école primaire,</p>
<p>Dans laquelle son fils est scolarisé...</p>
<p>Et qui se trouve dans la région frappée par le tremblement de terre.</p>
<p>Quand il est arrivé sur place,</p>
<p>Il a trouvé l'école entièrement détruite,</p>
<p>Il était sous le choc.</p>
<p>Il voyait autour de lui les autres parents qui pleuraient, les policiers, les pompiers... Ils étaient tous arrêtés, ils pensaient qu'il n'y avait pas de survivants.</p>
<p>Ça faisait plusieurs heures qu'ils cherchaient, et ils n'ont trouvé aucun survivant.</p>
<p>Soudainement ce père s'est senti boosté,</p>
<p>Il s'est rappelé d'une phrase qu'il disait à son fils...</p>
<p>Il lui disait toujours « Je serai toujours à tes côtés »</p>
<p>Chaque soir avant de dormir, il lui disait « N'aie pas peur dans ta vie, je serai toujours là pour toi »</p>
<p>Les larmes de ce père coulaient...</p>
<p>Puis il a rassemblé ses forces,</p>
<p>Il a essuyé ses larmes,</p>
<p>Et il a commencé à réfléchir,</p>
<p>« À quel endroit de l'école se trouvait mon fils ?</p>
<p>Il était à droite, dans cette partie...»</p>
<p>Il a pris une pioche,</p>
<p>Et il est parti creuser à cet endroit,</p>
<p>Un des autres parents est venu lui dire « C'est trop tard... »</p>
<p>Il lui a répondu « Peux-tu juste m'aider à creuser ? »</p>
<p>Un pompier est venu lui dire la même chose,</p>
<p>Il lui a encore répondu « Peux-tu juste m'aider à creuser ? »</p>
<p>Tous les gens pensaient qu'il avait perdu la tête...</p>
<p>Il les a regardés en disant « Soit vous m'aidez, soit vous me laissez tranquille ! »</p>
<p>Il a continué à creuser... Creuser... Creuser...</p>
<p>Il a creusé pendant 12 heures.</p>
<p>C'est une histoire vraie,</p>
<p>Au bout de 12 heures, il est tombé sur un grand rocher,</p>
<p>Il l'a poussé,</p>
<p>Il a vu un vide,</p>
<p>Il a mis sa tête dedans, et il a appelé son fils,</p>
<p>Une voix lui a répondu « Je suis ici Papa !</p>
<p>J'ai dit à mes camarades : 'N'ayez pas peur,</p>
<p>Papa m'a promis qu'il serait toujours là pour moi' »</p>
<p>Le fils a été sauvé,</p>
<p>Et avec lui 33 autres enfants.</p>
<p>Mais 14 enfants sont morts, et si les secours n'étaient pas arrivés à temps, peut-être que les 33 autres seraient aussi morts...</p>
<p>Le fils a été emmené à l'hôpital,</p>
<p>Il a été soigné de ses blessures,</p>
<p>Il a grandi,</p>
<p>Et il est devenu ingénieur.</p>
<p>Et maintenant qu'il est papa à son tour, il répète toujours à chacun de ses enfants « Je serai toujours là pour toi ».</p>
<p>Ne soyez pas radins avec vos enfants, en phrases encourageantes,</p>
<p>Rassurez-les, et donnez-leur de l'espoir,</p>
<p>Dites-leur que vous serez toujours là pour eux,</p>
<p>Pour dessiner sur leurs visages un sourire d'espoir pendant toutes leurs vies.</p>
<br>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
