                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle des 10 premiers jours du mois de Dhûl-Hijja (calendrier musulman),</p>
<p>Ce sont des jours d’une grande valeur et d’une grande importance,</p>
<p>Mais les gens n’y font pas attention…</p>
<p>Ces 10 jours se terminent par le passage au mont Arafat (des pèlerins à la Mecque),</p>
<p>Et par le jour de l’Aïd (fête du sacrifice).</p>
<p>Il existe une grande parole du Prophète (bénédictions et paix soient sur lui) qui parle de ces 10 jours,</p>
<p>Et ils sont également cités dans un verset très fort du Coran,</p>
<p>Dieu a juré par ces 10 jours…</p>
<p><strong>{1. Par l’Aube!</strong></p>
<p><strong>2. Et par les dix nuits !</strong></p>
<p><strong>3. Par le pair et l’impair! }</strong></p>
<p>Sourate 89 - Al-Fajr : L'Aube</p>
<p><strong>{ Les dix nuits }</strong> sont justement les 10 premiers jours de Dhûl-Hijja,</p>
<p>Quand Dieu jure par ces 10 jours, imaginez à quel point ils doivent être précieux…</p>
<p>Puis il existe une autre parole très très très belle du Prophète (bpsl), qui parle du mérite des 10 premiers jours de Dhûl-Hijja,</p>
<p>Nous sommes en plein dedans en ce moment,</p>
<p>Le Prophète (bpsl) a dit <strong class="prophete">« Il n’existe pas d’autres jours (dans l’année),</strong></p>
<p><strong class="prophete">Où les bonnes œuvres sont aimées par Dieu (Exalté Soit-Il),</strong></p>
<p><strong class="prophete">Comme elles le sont pendant les 10 premiers jours de Dhûl-Hijja. »</strong></p>
<p>Les jours où Dieu aime le plus te voir faire des bonnes œuvres, ce sont les 10 premiers jours de Dhûl-Hijja.</p>
<p>Pendant ces jours, fais la Prière,</p>
<p>Aide les pauvres,</p>
<p>Satisfais ton papa et ta maman,</p>
<p>Visite ta famille…</p>
<p>Toutes ces bonnes œuvres sont encore plus aimées par Dieu pendant ces 10 jours de Dhûl-Hijja.</p>
<p>C’est comme si le Prophète (bpsl) t’incitait à multiplier les bonnes œuvres pendant cette période,</p>
<p>Si tu le fais, Dieu t’aimera encore plus.</p>
<p>Mes chers : Celui qui veut obtenir l’amour de Dieu,</p>
<p>Doit s’activer pendant ces 10 jours.</p>
<p>Et le Prophète (bpsl) faisait quoi pendant cette période de l’année ?</p>
<p>Pour qu’on l’imite,</p>
<p>Il jeûnait les 9 premiers jours,</p>
<p>Et il faisait des Prières (Salate) la nuit,</p>
<p>Et il multipliait les aumônes,</p>
<p>Et s’il avait un souhait, il insistait dessus dans sa Prière (Doua’a),</p>
<p>Et il visitait sa famille et ses proches.</p>
<p>Ce sont 5 choses à faire :</p>
<p>Prendre soin de tes parents, contacter tes proches et renforcer tes liens familiaux.</p>
<p>Lire du Coran et prier la nuit.</p>
<p>Jeûner le jour.</p>
<p>Faire des aumônes et aider les pauvres.</p>
<p>Et demander à Dieu ce que tu souhaites (Doua’a).</p>
<p>Ces 10 jours, sont les plus chers pour Dieu dans toute l’année,</p>
<p>Et si tu sais en profiter,</p>
<p>En étant éveillé, et en voulant satisfaire Dieu,</p>
<p>Tu obtiendras l’amour de Dieu.</p>
<p>Peut-être que toute l’année tu de demandes si Dieu t’aime ou pas…</p>
<p>Mais si tu t’actives pendant ces 10 jours, c’est sûr et certain que tu seras aimé par Dieu.</p>
<p>Car s’il ne t’aimait pas, Il ne t’aurait pas permis de L’adorer et de faire le bien pendant les jours qu’Il aime le plus.</p>
<p>Ce sont des jours d’une grande valeur,</p>
<p>Et ils se terminent par le passage des pèlerins au mont Arafat,</p>
<p>Le jour d’Arafat est un grand jour,</p>
<p>C’est le jour où Dieu pardonne le plus à ses créatures.</p>
<p>Mais si tu n’as pas pu aller à la Mecque pour effectuer un pèlerinage…</p>
<p>Tu as l’occasion d’adorer Dieu chez toi,</p>
<p>Et être aimé par Dieu.</p>
<p>Car ces 10 jours de Dhûl-Hijja ne sont pas seulement pour les pèlerins…</p>
<p>Non, non, non,</p>
<p>Ils sont aussi pour les gens qui restent dans leurs maisons,</p>
<p>Pour leur permettre de beaucoup se rapprocher de Dieu,</p>
<p>Car Dieu a juré par ces jours dans le Coran.</p>
<p>La dernière chose que je souhaite dire,</p>
<p>C’est qu’un long débat a eu lieu entre les savants musulmans,</p>
<p>Autour de la question : Qu’est ce qui est le plus aimé par Dieu ? Est-ce que ce sont les 10 dernières nuits du Ramadan ou bien les 10 premiers jours de Dhûl-Hijja ?</p>
<p>Imaginez, ces 10 premiers jours ont une grande valeur à ce point…</p>
<p>Au point d’être comparés avec les 10 dernières nuits du mois de Ramadan (parmi lesquelles se trouve la nuit du Destin),</p>
<p>Et les savants ont déduit que ces 2 périodes de l’année sont au même niveau…</p>
<p>Qu’elles ont la même valeur…</p>
<p>Mais malheureusement les musulmans négligent les 10 premiers jours de Dhûl-Hijja.</p>
<p>La dernière chose que je vous recommande :</p>
<p>Mes chers, que celui qui a un souhait, ou qui a besoin d’une chose,</p>
<p>Ces 10 jours sont le meilleur moment pour faire des demandes à Dieu,</p>
<p>Tout comme les 10 dernières nuits du Ramadan.</p>
<p>Les 10 premiers jours de Dhûl-Hijja, sont des jours où les Prières (Doua’a) sont exaucées,</p>
<p>Donc multiplie tes demandes,</p>
<p>Et Dieu te les réalisera.</p>
<p>Tu verras alors un sourire d’espoir dans toute ta vie, grâce à la bénédiction de ces jours précieux.</p>

<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Sa bénédiction.</p>
