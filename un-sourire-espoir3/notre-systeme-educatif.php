                                    <div class="drapeau"><span><img src="../francais.png" alt="Drapeau Français" title="Français" /></span></div>
                                        <br>
                                        <p>Quand la vie semble difficile, Parfois nous avons besoin d'entendre une nouvelle idée qui nous aide, entendre un bon comportement qui nous protège, entendre une histoire ou une anecdote d'une autre personne qui a réussi... Quand on entend son histoire, un sourire d'espoir se dessine sur notre visage.</p>

<p>L'émission "Un sourire d'espoir" tous les jours sur la radio "nougoum fm" dessine sur nos visages des idées, des bonnes manières, de la joie nous aidant à avoir plus d'espoir dans nos vies.</p>
<br>
<p>Au nom de Dieu, le tout Miséricordieux, le très Miséricordieux :</p>
<br>
<p>Bienvenue avec un nouveau sourire d'espoir.</p>
<br>
                                        <p>L’épisode d’aujourd’hui parle de l’enseignement,</p>
<p>On nous disait toujours que l’enfant égyptien est l’enfant le plus intelligent au monde,</p>
<p>Selon les critères que les psychologues ont mis en place.</p>
<p>L’enfant égyptien est l’enfant le plus intelligent au monde… Jusqu’à ce qu’il entre à l’école.</p>
<p>:)</p>
<p>C’est-à-dire jusqu’à l’âge de 6 ans seulement,</p>
<p>Puis une fois qu’il entre à l’école,</p>
<p>Il devient un enfant très banal…</p>
<p>Le souci et le problème, c’est que l’enseignement chez nous c’est de l’apprentissage…</p>
<p>Ça veut dire quoi de l’apprentissage ?</p>
<p>Ça veut dire : Apprends par cœur… Apprends… Enregistre les informations… Enregistre… Puis c’est tout.</p>
<p>Du coup l’enfant perd sa créativité,</p>
<p>Il perd la capacité de sortir une nouvelle idée,</p>
<p>Car ça y est, son cerveau a gelé…</p>
<p>Il sait juste écouter une information, la retenir… Puis c’est tout.</p>
<p>Résultat il perd les capacités intellectuelles que Dieu lui a données.</p>
<p>En vérité cette situation m’a toujours fait mal,</p>
<p>Mais je me consolais en disant que ça devait être pareil dans tous les pays du monde,</p>
<p>Par exemple : Une matière comme la géographie ou l’histoire,</p>
<p>Ce sont des matières que les enfants sont obligés d’apprendre par cœur…</p>
<p>Jusqu’à ce que je croise un ami, qui travaille dans la mise en place des programmes scolaires en Angleterre,</p>
<p>Il est d’origine syrienne,</p>
<p>Il vit depuis longtemps en Angleterre, et il supervise les programmes scolaires,</p>
<p>Alors je lui ai posé la question,</p>
<p>Je lui ai dit : Dans les écoles de nos pays, on dit à nos enfants d’apprendre par cœur… Mémoriser…</p>
<p>Et chez vous ?</p>
<p>Il existe des matières qu’on est obligés d’apprendre par cœur,</p>
<p>Comment vous faites ?</p>
<p>Il m’a demandé « Des matières comme quoi ? »</p>
<p>Je lui ai répondu : La géographie par exemple,</p>
<p>Mes 2 fils, Ali et Omar s’assoient pour réviser… Réviser…</p>
<p>Mais ils n’ont pas le choix, car la géographie c’est une question de mémorisation.</p>
<p>Il m’a dit « Non, chez nous on a une autre méthode ».</p>
<p>Il m’a ensuite expliqué une méthode qui m’a beaucoup plu,</p>
<p>Il m’a dit « Sais-tu à quoi ressemble l’examen de géographie que nos élèves passent ? »</p>
<p>Je lui ai demandé « Comment ? »</p>
<p>Il m’a répondu « Il faut qu’ils mémorisent,</p>
<p>Mais il faut qu’on fasse travailler le reste de leurs cerveaux aussi…</p>
<p>Pour que ça ne soit pas de l’apprentissage par cœur, et c’est tout. »</p>
<p>Je lui ai demandé « Et comment vous faites ? »</p>
<p>Il m’a répondu « Dans l’examen de géographie,</p>
<p>On donne à l’élève une carte colorée,</p>
<p>Sur la carte il y a plusieurs couleurs,</p>
<p>Puis on donne aussi l’image d’un train,</p>
<p>La question dit que le train va s’arrêter à plusieurs stations,</p>
<p>Chaque station est dans un pays différent de l’Europe…</p>
<p>Et on demande à l’élève d’écrire le nom de chaque pays où le train va faire un arrêt.</p>
<p>Alors les enfants prennent du plaisir, en regardant la carte géographique devant eux,</p>
<p>En couleurs, ici c’est l’Allemagne,</p>
<p>Ici c’est la France,</p>
<p>Ici c’est la Suisse…</p>
<p>Et l’enfant suit l’avancement du train,</p>
<p>Il part d’Angleterre,</p>
<p>Il traverse la photo du tunnel,</p>
<p>Et où est ce qu’il sort de l’autre côté ?</p>
<p>Il sort en France…</p>
<p>L’enfant écrit « La France »,</p>
<p>Puis le train avance,</p>
<p>Il arrive à une nouvelle station,</p>
<p>L’enfant écrit « La Belgique »,</p>
<p>Et après la Belgique, c’est la Hollande…</p>
<p>L’enfant vit avec les photos, il vit avec les couleurs, et il réfléchit en même temps. »</p>
<p>C’est vrai qu’il faut qu’il mémorise la carte d’Europe,</p>
<p>Mais même dans la matière basée sur la mémorisation,</p>
<p>L’enfant prend du plaisir,</p>
<p>Et il fait travailler toutes les autres parties de son cerveau,</p>
<p>Les couleurs font travailler une partie, les photos sur la carte en font travailler une autre…</p>
<p>Mon ami m’a dit que toutes les matières étaient de cette façon.</p>
<p>Je vous dis la vérité ?</p>
<p>Ce système éducatif n’est pas une question d’argent ou de moyens financiers…</p>
<p>C’est surtout lié à la capacité des professeurs d’être créatifs,</p>
<p>D’inventer des façons de donner du plaisir à leurs élèves pendant qu’ils révisent,</p>
<p>Ou même leur donner du plaisir pendant qu’ils passent leurs examens…</p>
<p>Je ne sais pas comment résoudre un problème pareil dans nos pays,</p>
<p>Mais ce dont je suis sûr c’est que si toi en tant que parent,</p>
<p>Ou toi en tant que professeur,</p>
<p>Ou même toi en tant qu’étudiant…</p>
<p>Même s’il t’est demandé de réviser, peux-tu faire preuve de créativité,</p>
<p>Trouver une façon de transformer cette matière en images,</p>
<p>Et ne pas apprendre par cœur juste pour réciter…</p>
<p>Si on applique ce concept, les cerveaux de nos enfants vont se développer,</p>
<p>Ils deviendront très créatifs,</p>
<p>Ils raisonneront d’une nouvelle façon différente,</p>
<p>Même si les livres scolaires ne les aident pas, c’est notre rôle en tant que parents de les aider...</p>
<p>Et vous les jeunes :</p>
<p>Réfléchissez et vous trouverez des solutions originales qui vous aideront à réviser.</p>
<p>Merci,</p>
<p>Et que la paix soit sur vous ainsi que la miséricorde de Dieu et Ses bénédictions.</p>
