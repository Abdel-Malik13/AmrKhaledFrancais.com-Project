                                        <br>
                                        
                                            <p>En vérité, j'aimerais dire quelques sens...</p>
<p>Des sens qui sont utiles à nous tous,</p>
<p>Et j'espère qu'ils seront bénéfiques, et que vous repartirez avec quelque chose de bien.</p>
<p>Allah (Exalté Soit-Il) a créé la vie, et Il a rendu sa durée courte,</p>
<p>Courte... Et la vie est juste une question de tests,</p>
<p>Pour parvenir au Paradis de Dieu (Exalté Soit-Il).</p>
<p>C'est une question de tests... Une suite d'épreuves qui te font retourner de tous les côtés,</p>
<p>Un peu comme ci... Un peu comme ca... Un peu de ce côté... Un peu de l'autre côté...</p>
<p>Pour que personne n'ait d'excuse le jour du jugement dernier.</p>
<p>Des tests en toutes formes : Réussite et échec, facilité et difficulté, bonheur et tristesse, soucis et inquiétudes, joie et jouissance,</p>
<p>Argent, et manque d'argent, mariage et sans mariage, enfants et pas enfants...</p>
<p>Chagrin et bien-être, en haut et en bas... C'est la vie.</p>
<p>Ce sont des expériences indispensables à la vie,</p>
<p>Sinon pourquoi te trouves-tu dans ce bas-monde ?</p>
<p>La vie ne peut pas fonctionner sans ces épreuves.</p>
<p>Et il parait que cet enchainement d'épreuves te fait arriver à la fin à un mot,</p>
<p>Soit tu es soumis entièrement à ce mot, soit tu en doutes, soit tu le refuses...</p>
<p>« La croyance au destin de Dieu dans son univers ».</p>
<p>Être satisfait du destin de Dieu,</p>
<p>Tu dois arriver à un seul mot, « Es-tu satisfait du destin de Dieu, en toi, en ton pays, en ta communauté, en ton projet... »</p>
<p>Es-tu satisfait du destin de Dieu ?</p>
<p>Ou bien tu es mécontent ?</p>
<p>Quel est le niveau de ta croyance au destin de Dieu ?</p>
<p>Est-ce que tu es fragile ?</p>
<p>Es-tu le genre de personnes de qui Dieu dit</p>
<p><strong>{ Il en est parmi les gens qui adorent Allah marginalement. S'il leur arrive un bien, ils s'en tranquillisent, et s'il leur arrive une épreuve, ils détournent leur visage, perdant ainsi (le bien) de l'ici-bas et de l'au-delà. Telle est la perte évidente! }</strong></p>
<p>Sourate 22 - Al-Hajj : Le Pèlerinage (verset 11)</p>
<p>Et toi quel est ton degré de croyance au destin ?</p>
<p>Quelle est ta relation avec le destin ?</p>
<p>Est-ce que tu crois que toutes les sentences de Dieu qui t'arrivent dans la vie sont un bien,</p>
<p>Que tout est bien...</p>
<p>Et que tout ce qui t'arrive toi, ta famille, tes amis, ton pays et ta communauté... C'est un bien.</p>
<p>As-tu cette conviction chez toi ?</p>
<p>Ou bien au milieu d'événements difficiles etc. tu te mets à douter ?</p>
<p>Quel est ton niveau ?</p>
<p>Où en es-tu de la parole du Prophète (bénédictions et paix soient sur lui) dans laquelle il dit <strong class="prophete">« Ô Seigneur, je réponds à Ton appel avec joie, tout le bien est entre Tes mains, et le mal n'est pas de Toi. »</strong></p>
<p>Quelles sont tes nouvelles dans les dernières périodes ?</p>
<p>Et je parle de moi-même... Je parle de toi, et de tout le monde.</p>
<p>Quelles sont les nouvelles de ce mot ?</p>
<p>Peut-être que l'examen de toute la vie est pour qu'à la fin tu rencontres Dieu avec ce mot.</p>
<p>Cet être humain est soumis au destin de Dieu, il accepte entièrement le destin de Dieu.</p>
<p>Quand un jour l'ange Gabriel est venu voir le Prophète (bpsl) et lui a demandé « Ô Mohamed parle-moi de la foi »</p>
<p>Il lui a répondu <strong class="prophete">« Que tu croies en Dieu, en Ses anges, en Ses Livres sacrés, en Ses messagers... »</strong></p>
<p>Puis croire en quoi ? « Au destin »</p>
<p>Comment ? « Au destin bon ou mauvais »</p>
<p>Quel est son mauvais ? On a dit tout à l'heure que tout était un bien...</p>
<p>On a dit que le destin de Dieu est entièrement bien,</p>
<p>C'est ce que toi tu vois comme « mauvais »,</p>
<p>Il faut que tu croies qu'il est bien.</p>
<p>Et que signifie croire au destin ? Ca signifie te soumettre, le mot « croyance » signifie ici la soumission,</p>
<p>Que tu sois soumis au destin bon ou mauvais (selon ton point de vue),</p>
<p>Car en réalité Dieu ne veut pour ses créatures que le bien.</p>
<p>La vie en entier c'est : Le bien que Dieu veut pour ses créatures, vivantes ou décédées, ceux qui réussissent et ceux qui ne réussissent pas... La volonté de Dieu c'est le bien,</p>
<p>Quelles sont tes nouvelles avec cette parole au milieu de ce qu'on vit actuellement ?</p>
<p>D'ailleurs tu peux repartir d'ici en étant une nouvelle personne,</p>
<p>A propos, la croyance au destin n'est pas une énergie négative...</p>
<p>La croyance au destin c'est une énergie positive :</p>
<p>Les pensées négatives ont été nettoyées de mon cerveau, alors dirigeons-nous vers la construction,</p>
<p>Je crois au destin, et tout le destin de Dieu est un bien,</p>
<p>Du coup, tu ne vas pas te décourager, car tu crois au destin,</p>
<p>Et car la croyance au destin aura des signes chez toi au bout d'un moment,</p>
<p>La croyance au destin c'est : Rester positif, continuer à construire quoi qu'il arrive...</p>
